SUBROUTINE GPTF11C(YDGEOMETRY,YDMODEL,KSTART,KPROF,LDLSTEP,&
 &PUT0,PVT0,PTT0,PQT0,PDIVT0,PTT0L,PTT0M,PSVT0,PSPT0,PSPT0L,PSPT0M,&
 &PUT9,PVT9,PTT9,PQT9,PDIVT9,PTT9L,PTT9M,PSVT9,PSPT9,PSPT9L,PSPT9M,&
 &PLT0,PIT0,PAT0,PRT0,PST0, PSPD0, PSVD0, PSPD0L, PSVD0L, PSPD0M, PSVD0M,&
 &PLT9,PIT9,PAT9,PRT9,PST9, PSPD9, PSVD9, PSPD9L, PSVD9L, PSPD9M, PSVD9M)

!**   *GPTF11C* - Timefilter part 1

!     Purpose.
!     --------
!           Performs part 1 of the time-filtering for the t-dt array.

!**   Interface.
!     ----------
!        *CALL* *GPTF11C(...)

!        Explicit arguments :
!        --------------------
!                              KSTART -  start of work
!                              KPROF  -  depth of work
!                              P.T0 - timestep t
!                              P.T9 - timestep t-dt
!                              LDLSTEP - check on the last time step.

!        Implicit arguments :  None
!        --------------------

!     Method.
!     -------
!        See documentation

!     Externals.  None.
!     ----------

!     Reference.
!     ----------
!        ECMWF Research Department documentation of the single column model.

!     Author.
!     -------
!        Joao Teixeira  *ECMWF*

!     Modifications.
!     --------------
!        Original (based on gptf1.F): Nov.96
!        M. Ko"hler  6-6-2006  Single Column Model integration within IFS 
!     ------------------------------------------------------------------

USE GEOMETRY_MOD , ONLY : GEOMETRY
USE TYPE_MODEL   , ONLY : MODEL
USE PARKIND1 , ONLY : JPIM, JPRB
USE YOMHOOK  , ONLY : LHOOK, DR_HOOK, JPHOOK
IMPLICIT NONE

TYPE(GEOMETRY), INTENT(INOUT) :: YDGEOMETRY
TYPE(MODEL),    INTENT(INOUT) :: YDMODEL
INTEGER(KIND=JPIM),PARAMETER :: NFPASS=1

!     DUMMY INTEGER SCALARS
INTEGER(KIND=JPIM) :: KPROF
INTEGER(KIND=JPIM) :: KSTART

!     DUMMY LOGICAL SCALARS
LOGICAL LDLSTEP

REAL(KIND=JPRB) :: PUT0(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG),PUT9(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG)
REAL(KIND=JPRB) :: PVT0(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG),PVT9(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG)
REAL(KIND=JPRB) :: PTT0(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG),PTT9(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG)
REAL(KIND=JPRB) :: PQT0(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG),PQT9(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG)
REAL(KIND=JPRB) :: PDIVT0(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG), &
 & PDIVT9(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG)
REAL(KIND=JPRB) :: PTT0L(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG), &
 & PTT9L(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG)
REAL(KIND=JPRB) :: PTT0M(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG), &
 & PTT9M(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG)
REAL(KIND=JPRB) :: PSVT0(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG,NFPASS), &
 & PSVT9(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG,NFPASS)
REAL(KIND=JPRB) :: PSPT0 (YDGEOMETRY%YRDIM%NPROMA),PSPT9 (YDGEOMETRY%YRDIM%NPROMA)
REAL(KIND=JPRB) :: PSPT0L(YDGEOMETRY%YRDIM%NPROMA),PSPT9L(YDGEOMETRY%YRDIM%NPROMA)
REAL(KIND=JPRB) :: PSPT0M(YDGEOMETRY%YRDIM%NPROMA),PSPT9M(YDGEOMETRY%YRDIM%NPROMA)
REAL(KIND=JPRB) :: PLT0(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG),PLT9(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG)
REAL(KIND=JPRB) :: PIT0(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG),PIT9(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG)
REAL(KIND=JPRB) :: PAT0(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG),PAT9(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG)
REAL(KIND=JPRB) :: PRT0(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG),PRT9(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG)
REAL(KIND=JPRB) :: PST0(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG),PST9(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG)
REAL(KIND=JPRB) :: PSPD0(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG), &
 & PSPD9(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG)
REAL(KIND=JPRB) :: PSVD0(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG), &
 & PSVD9(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG)
REAL(KIND=JPRB) :: PSPD0L(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG), &
 & PSPD9L(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG)
REAL(KIND=JPRB) :: PSVD0L(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG), &
 & PSVD9L(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG)
REAL(KIND=JPRB) :: PSPD0M(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG), &
 & PSPD9M(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG)
REAL(KIND=JPRB) :: PSVD0M(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG), &
 & PSVD9M(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG)

!     LOCAL INTEGER SCALARS
INTEGER(KIND=JPIM) :: JK, JROF, JVAR

!     LOCAL REAL SCALARS
REAL(KIND=JPRB) :: ZREST, ZRESTM, RKROMA
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!     ------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('GPTF11C',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM=>YDGEOMETRY%YRDIM,YDDIMV=>YDGEOMETRY%YRDIMV,YDGEM=>YDGEOMETRY%YRGEM,YDMP=>YDGEOMETRY%YRMP, &
 & YDVAB=>YDGEOMETRY%YRVAB, YGFL=>YDMODEL%YRML_GCONF%YGFL, &
 & YDVETA=>YDGEOMETRY%YRVETA,YDVFE=>YDGEOMETRY%YRVFE,YDSTA=>YDGEOMETRY%YRSTA, &
 & YDDYN=>YDMODEL%YRML_DYN%YRDYN,YDDIMF=>YDMODEL%YRML_GCONF%YRDIMF)
ASSOCIATE(NFTHER=>YDDIMF%NFTHER, &
 & NFLEVG=>YDDIMV%NFLEVG, &
 & LNHDYN=>YDMODEL%YRML_DYN%YRDYNA%LNHDYN, &
 & REPS1=>YDDYN%REPS1, REPS2=>YDDYN%REPS2, REPSM1=>YDDYN%REPSM1, &
 & REPSM2=>YDDYN%REPSM2, &
 & YA=>YGFL%YA, YI=>YGFL%YI, YL=>YGFL%YL, YQ=>YGFL%YQ, YR=>YGFL%YR, YS=>YGFL%YS)
!*       1. PERFORM TIME FILTER (PART 1)
!           ----------------------------

IF ( YDMODEL%YRML_DYN%YRDYNA%LTWOTL ) THEN 
   RKROMA = 0.      ! 0. if Matsuno
ELSE
   RKROMA = 1.      ! 1. if leap-frog timescheme
ENDIF

ZREST  = (1.0_JPRB-(REPS1+REPS2))*RKROMA
ZRESTM = (1.0_JPRB-(REPSM1+REPSM2))*RKROMA
IF(.NOT.LDLSTEP) THEN
  DO JK=1,NFLEVG
    DO JROF=KSTART,KPROF
      PUT9(JROF,JK)  = REPS1*PUT9(JROF,JK)  +ZREST*PUT0(JROF,JK)
      PVT9(JROF,JK)  = REPS1*PVT9(JROF,JK)  +ZREST*PVT0(JROF,JK)
      PDIVT9(JROF,JK)= REPS1*PDIVT9(JROF,JK)+ZREST*PDIVT0(JROF,JK)
      IF(NFTHER >= 1) THEN
        PTT9(JROF,JK)  = REPS1*PTT9(JROF,JK) +ZREST*PTT0(JROF,JK)
        PTT9L(JROF,JK) = REPS1*PTT9L(JROF,JK)+ZREST*PTT0L(JROF,JK)
        PTT9M(JROF,JK) = REPS1*PTT9M(JROF,JK)+ZREST*PTT0M(JROF,JK)
      ENDIF
      IF(YQ%LACTIVE) THEN
        PQT9(JROF,JK) = REPSM1*PQT9(JROF,JK)  +ZRESTM*PQT0(JROF,JK)
      ENDIF
      IF(YL%LACTIVE) THEN
        PLT9(JROF,JK) = REPSM1*PLT9(JROF,JK)  +ZRESTM*PLT0(JROF,JK)
      ENDIF
      IF(YI%LACTIVE) THEN
        PIT9(JROF,JK) = REPSM1*PIT9(JROF,JK)  +ZRESTM*PIT0(JROF,JK)
      ENDIF
      IF(YA%LACTIVE) THEN
        PAT9(JROF,JK) = REPSM1*PAT9(JROF,JK)+ZRESTM*PAT0(JROF,JK)
      ENDIF
      IF(YR%LACTIVE) THEN
        PRT9(JROF,JK) = REPSM1*PRT9(JROF,JK)  +ZRESTM*PRT0(JROF,JK)
      ENDIF
      IF(YS%LACTIVE) THEN
        PST9(JROF,JK) = REPSM1*PST9(JROF,JK)+ZRESTM*PST0(JROF,JK)
      ENDIF
      IF(LNHDYN) THEN
        PSPD9(JROF,JK) = REPSM1*PSPD9(JROF,JK)+ZRESTM*PSPD0(JROF,JK)
        PSVD9(JROF,JK) = REPSM1*PSVD9(JROF,JK)+ZRESTM*PSVD0(JROF,JK)
        PSPD9L(JROF,JK) = REPSM1*PSPD9L(JROF,JK)+ZRESTM*PSPD0L(JROF,JK)
        PSVD9L(JROF,JK) = REPSM1*PSVD9L(JROF,JK)+ZRESTM*PSVD0L(JROF,JK)
        PSPD9M(JROF,JK) = REPSM1*PSPD9M(JROF,JK)+ZRESTM*PSPD0M(JROF,JK)
        PSVD9M(JROF,JK) = REPSM1*PSVD9M(JROF,JK)+ZRESTM*PSVD0M(JROF,JK)
      ENDIF
    ENDDO
  ENDDO
  DO JVAR=1,NFPASS
    DO JK=1,NFLEVG
      DO JROF=KSTART,KPROF
        PSVT9(JROF,JK,JVAR) = REPS1*PSVT9(JROF,JK,JVAR)&
         &+ZREST*PSVT0(JROF,JK,JVAR)
      ENDDO
    ENDDO
  ENDDO
  DO JROF=KSTART,KPROF
    PSPT9(JROF)  = REPS1*PSPT9(JROF) +ZREST*PSPT0(JROF)
    PSPT9L(JROF) = REPS1*PSPT9L(JROF)+ZREST*PSPT0L(JROF)
    PSPT9M(JROF) = REPS1*PSPT9M(JROF)+ZREST*PSPT0M(JROF)
  ENDDO
ELSE
  DO JK=1,NFLEVG
    DO JROF=KSTART,KPROF
      PUT9(JROF,JK)   = 0.0_JPRB
      PVT9(JROF,JK)   = 0.0_JPRB
      PDIVT9(JROF,JK) = 0.0_JPRB
      IF(NFTHER >= 1) THEN
        PTT9(JROF,JK)  = 0.0_JPRB
        PTT9L(JROF,JK) = 0.0_JPRB
        PTT9M(JROF,JK) = 0.0_JPRB
      ENDIF
      IF(YQ%LACTIVE) THEN
        PQT9(JROF,JK) = 0.0_JPRB
      ENDIF
      IF(YL%LACTIVE) THEN
        PLT9(JROF,JK) = 0.0_JPRB
      ENDIF
      IF(YI%LACTIVE) THEN
        PIT9(JROF,JK) = 0.0_JPRB
      ENDIF
      IF(YR%LACTIVE) THEN
        PRT9(JROF,JK) = 0.0_JPRB
      ENDIF
      IF(YS%LACTIVE) THEN
        PST9(JROF,JK) = 0.0_JPRB
      ENDIF
      IF(YA%LACTIVE) THEN
        PAT9(JROF,JK) = 0.0_JPRB
      ENDIF
      IF(LNHDYN) THEN
        PSPD9(JROF,JK)  = 0.0_JPRB
        PSVD9(JROF,JK)  = 0.0_JPRB
        PSPD9L(JROF,JK) = 0.0_JPRB
        PSVD9L(JROF,JK) = 0.0_JPRB
        PSPD9M(JROF,JK) = 0.0_JPRB
        PSVD9M(JROF,JK) = 0.0_JPRB
      ENDIF
    ENDDO
  ENDDO
  DO JVAR=1,NFPASS
    DO JK=1,NFLEVG
      DO JROF=KSTART,KPROF
        PSVT9(JROF,JK,JVAR) = 0.0_JPRB
      ENDDO
    ENDDO
  ENDDO
  DO JROF=KSTART,KPROF
    PSPT9(JROF)  = 0.0_JPRB
    PSPT9L(JROF) = 0.0_JPRB
    PSPT9M(JROF) = 0.0_JPRB
  ENDDO
ENDIF

!     ------------------------------------------------------------------

END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('GPTF11C',1,ZHOOK_HANDLE)

END SUBROUTINE GPTF11C
