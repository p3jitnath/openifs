ALLOCATE( ZSMOS_FARAD(NGPTOT,0:NPOL_MAX-1,NANG_MAX) )
ALLOCATE( ZSMOS_GEOMET(NGPTOT,0:NPOL_MAX-1,NANG_MAX) )
ALLOCATE( ZSMAP_OBS_BUF(NGPTOT,0:NPOL_MAX-1) )
ALLOCATE( ISMAP_BUF(NGPTOT,0:NPOL_MAX-1) )
ALLOCATE( ZSMAP_TB_BUF(NGPTOT,0:NPOL_MAX-1) )
ALLOCATE( ZSMAP_ANGLE(NGPTOT,0:NPOL_MAX-1) )

IF( (LIFSTRAJ .OR. LIFSMIN) .AND. LEGBRAD .AND.&
