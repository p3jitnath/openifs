    YDFIELDS%YRGMV%GMVT1(:,:,:,J)=0.0_JPRB
  ENDDO
!$OMP END PARALLEL DO
ENDIF
IF(.NOT.ALLOCATED(YDGMV%GMVT1S)) THEN
  ALLOCATE(YDGMV%GMVT1S(NPROMA,YT1%NDIMS,NGPBLKS))
!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(J)
  DO J=1,NGPBLKS
    YDFIELDS%YRGMV%GMVT1S(:,:,J)=0.0_JPRB
  ENDDO
!$OMP END PARALLEL DO
ENDIF
IF(.NOT.ALLOCATED(YDGFL%GFLT1))  THEN
  ALLOCATE(YDGFL%GFLT1(NPROMA,NFLEVG,NDIM1,NGPBLKS))
!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(J)
  DO J=1,NGPBLKS
    YDFIELDS%YRGFL%GFLT1(:,:,:,J)=0.0_JPRB
  ENDDO
!$OMP END PARALLEL DO
ENDIF
CALL GSTATS(1228,1)

IF (LCANARI .AND. LHOOK) CALL DR_HOOK('GP_MODEL',1,ZHOOK_HANDLE)
IF (LCANARI) RETURN

CALL GSTATS(1228,0)
IF (YDSL%NSLPAD > 0)THEN
!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(J)
  DO J=1,NFLDSLB1
    PSLBUF1(:,J)=HUGE(ZZZ)
  ENDDO
!$OMP END PARALLEL DO
ENDIF

!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(J)
DO J=1,NGPBLKS
  PSLBUF2(:,:,J)=0.0_JPRB
ENDDO
!$OMP END PARALLEL DO
CALL GSTATS(1228,1)

! Create field registry, if it hasn't been set up yet
IF (.NOT. ASSOCIATED(YDFIELDS%VARIABLES)) THEN
  CALL YDFIELDS%REGISTRY%INIT(YDGEOMETRY)
  CALL YDFIELDS%REGISTRY%REGISTER_GEOMETRY(YDFIELDS%VARIABLES, YDGEOMETRY)
  CALL YDFIELDS%REGISTRY%REGISTER_GMV_FIELDS(YDFIELDS%VARIABLES, YDFIELDS%YRGMV)
  CALL YDFIELDS%REGISTRY%REGISTER_GFL_FIELDS(YDFIELDS%VARIABLES, YDFIELDS%YRGFL, YDFIELDS%YRGFL%YGFL)
  CALL YDFIELDS%REGISTRY%REGISTER_SURFACE_FIELDS(YDFIELDS%SURFVARS, YDSURF)
  CALL YDFIELDS%REGISTRY%REGISTER_EC_PHYS_FIELDS(YDFIELDS%VARIABLES, YDFIELDS%YEC_PHYS_FIELDS)
  CALL YDFIELDS%REGISTRY%REGISTER_PHYSICS_RADIATION_FIELDS(YDFIELDS%VARIABLES, YDFIELDS%STATE_MODEL%YRML_PHY_RAD)
ENDIF

IF(CDCONF(6:6) == 'V' .AND. LESMOS) THEN
  LESMOS_ACTIVE=.TRUE.
ELSE
  LESMOS_ACTIVE=.FALSE.
ENDIF

IF(CDCONF(6:6) == 'V' .AND. LESMAP) THEN
  LESMAP_ACTIVE=.TRUE.
ELSE
  LESMAP_ACTIVE=.FALSE.
ENDIF

IF(NCONF==302 .AND. LUSE_SMOS_TB) THEN
  LESMOS_SEKF=.TRUE.
ELSE
  LESMOS_SEKF=.FALSE.
ENDIF

ALLOCATE( ZSMOS_OBS_BUF(NGPTOT,0:NPOL_MAX-1,NANG_MAX) )
ALLOCATE( ISMOS_BUF(NGPTOT,0:NPOL_MAX-1,NANG_MAX) )
