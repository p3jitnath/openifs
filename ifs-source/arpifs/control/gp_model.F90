IF (KCHEM_NOXADV > 0) THEN
  ! fill NOXA/CLXA array with NOx / NOy
  !  - has to be done before CPG because SL Buffers are set in there
  CALL CHEM_NOYADV(YDGEOMETRY,YGFL,YDCHEM,1,KCHEM_NOXADV,GFL,YDGFL%GFLT1)
ENDIF

!     ------------------------------------------------------------------
!*        CELLULAR AUTOMATON SCHEME (FOR ALARO CONVECTION)
!     ------------------------------------------------------------------

!  (Must be called outside the SM-parallel region as it issues MPI comms.)
IF(.NOT.LECMWF .AND. LCUCONV_CA) CALL CUCONVCA(YDGEOMETRY,YDGMV,YDMODEL,NSTEP)

!     ------------------------------------------------------------------
!*       3.    DYNAMICS
!     ------------------------------------------------------------------

IF (.NOT.LR2D) THEN

  ALLOCATE(ZGEO0(NPROMA,NFLEVG,NGPBLKS))
  ALLOCATE(ZRCP0(NPROMA,NFLEVG,YYTRCP0%NDIM,NGPBLKS))
  ALLOCATE(ZPRE0F(NPROMA,NFLEVG,NGPBLKS))
  ALLOCATE(ZCTY0(NPROMA,0:NFLEVG,YYTCTY0%NDIM,NGPBLKS))
  ALLOCATE(ZWRL9(NPROMA,NFLEVG,NGPBLKS))

!     ------------------------------------------------------------------
!*      3.0 Compute pressure gradient term in GP space, then
!           transfer to SP and back to GP to eliminate aliasing in GP space
!   ----------------------------------------------------

  IF( LPGFSF ) THEN
    ! Constructor of PG terms (to be used under CPG_DRV)

!$OMP PARALLEL PRIVATE(J1)
!$OMP DO SCHEDULE(STATIC,1)
    DO J1=1,NGPBLKS
      ! Already allocated above (to be used for multigrid)
      ZPGTERM(J1)%PHI0F=>ZGEO0(:,:,J1)
      ! Output, must be allocated in any case
      ALLOCATE(ZPGTERM(J1)%PSGRTL(NPROMA,NFLEVG))
      ALLOCATE(ZPGTERM(J1)%PSGRTM(NPROMA,NFLEVG))
      ! optional allocation, saving duplicated computation for a price
      ! of extra storage
      IF (LPGREUSE) THEN
        ALLOCATE(ZPGTERM(J1)%PHI0FL(NPROMA,NFLEVG))
        ALLOCATE(ZPGTERM(J1)%PHI0FM(NPROMA,NFLEVG))
        ALLOCATE(ZPGTERM(J1)%RT0(NPROMA,NFLEVG))
        ALLOCATE(ZPGTERM(J1)%RT0L(NPROMA,NFLEVG))
        ALLOCATE(ZPGTERM(J1)%RT0M(NPROMA,NFLEVG))
        ALLOCATE(ZPGTERM(J1)%PHI0H(NPROMA,0:NFLEVG))
        ALLOCATE(ZPGTERM(J1)%PHI0HL(NPROMA,0:NFLEVG))
        ALLOCATE(ZPGTERM(J1)%PHI0HM(NPROMA,0:NFLEVG))
      ENDIF
    ENDDO
!$OMP END DO
!$OMP END PARALLEL

    CALL GPTOSPTOGP(YDGEOMETRY,YDGMV,YDMODEL%YRML_GCONF,YDMODEL%YRML_DYN,GMV,GMVS,GFL,ZPGTERM)
  ENDIF

  IF (LNHQE.AND.(.NOT.LNHQE_SIHYD)) THEN
    ! ky: GMVS(1:NPROMA,YT9%MPREHYDS,1:NGPBLKS)=EXP(GMVS(1:NPROMA,YT0%MSP,1:NGPBLKS))
    !     And the saved version of "prehyds(t)" is assumed to be untouched by some Asselin filtering.
    !     For LAM models saving must be done for C+I+E (all the NGPTOT points):
    !     this is why this memory transfer is not under CPG_DRV nor in CPG_GP.
    !     The content of GMVS(:,YT9%MPREHYDS,:) is used in (E)SPNHQESI_PRODSKAP.
    DO JBL=1,NGPBLKS
      DO JROF=1,NPROMA
        GMVS(JROF,YT9%MPREHYDS,JBL)=EXP(GMVS(JROF,YT0%MSP,JBL))
      ENDDO
    ENDDO
  ENDIF

  CALL CPG_DRV(YDGEOMETRY,YDMODEL,YDFIELDS,CDCONF(4:4),LD_DFISTEP,YDSL,&
   & IGPCOMP,GFLSLP,GMVTNDHD_DDH,GFLTNDHD_DDH,YDSPPT%YGPSDT(1)%GP2D,SD_PF,&
   & ZPGTERM,&
   & GMV,GMVS,GFL,GFLPC,GFLPT,&
   & SP_SB,SP_SG,SP_RR,SP_CL,SD_VF,SD_VP,SD_VV,SD_VH,SD_VK,SD_VA,SD_VC,&
   & SD_VD,SD_SFL,SD_SFO,SD_XA,SD_DI,SD_XP,SD_XP2,&
   & EMTD,EMTU,TRSW,RMOON,GMVTNDSI_DDH,&
   & YDTRC%GDEOSI,YDTRC%GUEOSI,YDTRC%GMU0,YDTRC%GMU0_MIN,YDTRC%GMU0_MAX,&
   & YDTRC%GDEOTI,YDTRC%GDEOTI2,YDTRC%GUEOTI,YDTRC%GUEOTI2,YDTRC%GEOLT,YDTRC%GEOXT,&
   & YDTRC%GRPROX,YDTRC%GMIXP,YDTRC%GFLUXC,YDTRC%GRSURF,&
   & PSLBUF1,PSLBUF2,YDGMV%GMVT1,YDGMV%GMVT1S,YDGFL%GFLT1,ZEXTRADYN,&
   & GMVTNDSL_DDH,GFLTNDSL_DDH,YLDDH,ZGEO0,ZRCP0,ZPRE0F,ZCTY0,ZWRL9,&
   & PTRAJEC=PTRAJEC,PTRAJEC_OOPS=PTRAJEC_OOPS)

  IF( LPGFSF ) THEN
    !Cleaning
!$OMP PARALLEL PRIVATE(J1)
!$OMP DO SCHEDULE(STATIC,1)
    DO J1=1,NGPBLKS
      IF (LPGREUSE) THEN
        DEALLOCATE(ZPGTERM(J1)%PHI0FL,ZPGTERM(J1)%PHI0FM)
        DEALLOCATE(ZPGTERM(J1)%RT0,ZPGTERM(J1)%RT0L,ZPGTERM(J1)%RT0M)
        DEALLOCATE(ZPGTERM(J1)%PHI0H,ZPGTERM(J1)%PHI0HL,ZPGTERM(J1)%PHI0HM)
      ENDIF
      DEALLOCATE(ZPGTERM(J1)%PSGRTL,ZPGTERM(J1)%PSGRTM)
      NULLIFY(ZPGTERM(J1)%PHI0F)
    ENDDO
!$OMP END DO
!$OMP END PARALLEL
  ENDIF

  IF (LFLEXDIA.AND.LDDH_OMP) THEN
    CALL STOREDDH(YDMDDH,YDMODEL%YRML_DIAG%YRTDDH,YLDDH)
    IF (CDCONF(4:4)=='X') THEN
      ! equivalent of reset_ddhflex for lddhomp
      NTOTFIELD=0
      NTOTVAR=0
      NTOTSURF=0
      NTOTSVAR=0
      NTOTSVFS=0
    ENDIF
  ENDIF

ELSE
IF(PRESENT(PTRAJEC_OOPS)) CALL ABOR1('GP_MODEL: CPG2 not done yet for OOPS')
!$OMP PARALLEL PRIVATE(ICEND,IBL,ZSLBUF1AUX)
!$OMP DO SCHEDULE(STATIC,1)
  DO JKGLO=1,IGPCOMP,NPROMA
    ICEND=MIN(NPROMA,IGPCOMP-JKGLO+1)
    IBL=(JKGLO-1)/NPROMA+1

    CALL CPG2(YDGEOMETRY,YDGMV,YDMODEL%YRML_GCONF,YDMODEL%YRML_DYN,CDCONF(4:4),ICEND,JKGLO,&
     & ZSLBUF1AUX,PSLBUF2(1,1,IBL),GMV(:,:,:,IBL),GMVS(:,:,IBL),&
     & IBL,&
     & GMV(:,:,YT0%MU,IBL)  ,GMV(:,:,YT0%MV,IBL),&
     & GMV(:,:,YT0%MUL,IBL) ,GMV(:,:,YT0%MVL,IBL),&
     & GMV(:,:,YT0%MDIV,IBL),GMV(:,:,YT0%MVOR,IBL),&
     & GMVS(:,YT0%MSP,IBL)  ,GMVS(:,YT0%MSPL,IBL),&
     & GMVS(:,YT0%MSPM,IBL),&
     & GMV(:,:,YT9%MU,IBL)  ,GMV(:,:,YT9%MV,IBL),&
     & GMV(:,:,YT9%MDIV,IBL),GMV(:,:,YT9%MSPNL,IBL),&
     & GMVS(:,YT9%MSP,IBL)  ,GMVS(:,YT9%MSPL,IBL),&
     & GMVS(:,YT9%MSPM,IBL),&
     & YDGMV%GMVT1(:,:,YT1%MU,IBL),YDGMV%GMVT1(:,:,YT1%MV,IBL),&
     & YDGMV%GMVT1S(:,YT1%MSP,IBL),PTRAJEC%SLAG(IBL))

    ! move data from blocked form to latitude (NASLB) form
    IF (LSLAG) THEN
      DO JROF=JKGLO,MIN(JKGLO-1+NPROMA,IGPCOMP)
        PSLBUF1(YDSL%NSLCORE(JROF),:)=ZSLBUF1AUX(JROF-JKGLO+1,:)
      ENDDO
    ENDIF
  ENDDO
!$OMP END DO
!$OMP END PARALLEL
ENDIF ! LR2D


!IF (LCOUPLO4_ENV) THEN
!  ! call of oasis4 exchange - get chemistry
!  CALL COUPLO4_EXCHANGE(1)
!ENDIF


!     ------------------------------------------------------------------
!*       4.     SEMI-LAGRANGIAN INTERPOLATION
!     ------------------------------------------------------------------

IF (LSLAG) THEN
  IF (LSLPHY) THEN
    ALLOCATE (ZPBUP9(NPROMA,NFLEVG,NGPBLKS))
    ALLOCATE (ZPBVP9(NPROMA,NFLEVG,NGPBLKS))
    ALLOCATE (ZPBTP9(NPROMA,NFLEVG,NGPBLKS))
    ALLOCATE (ZPBGFLP9(NPROMA,NFLEVG,NDIMSLP,NGPBLKS))
  ELSE
    ! minimal alloc to avoid false alarms with bound checking option
    IF (.NOT.LR2D) THEN
      ALLOCATE (ZPBUP9(1,NFLEVG,NGPBLKS))
      ALLOCATE (ZPBVP9(1,NFLEVG,NGPBLKS))
      ALLOCATE (ZPBTP9(1,NFLEVG,NGPBLKS))
      ALLOCATE (ZPBGFLP9(1,NFLEVG,NDIMSLP,NGPBLKS))
    ENDIF
  ENDIF
ENDIF


IF (.NOT.LR2D) THEN

  IVSEPC(:) = 0
  IVSEPL(:) = 0

  LL_CALL_SL=LSLAG
  IF(LL_CALL_SL) THEN

    !--------------------------------------------------------------------
    ! Advection on multiple grids
    !--------------------------------------------------------------------
#ifdef WITH_MGRIDS
    CALL YDMODEL%YRML_DYN%YR_MGRIDS_ADVECTION%EXECUTE( YDGMV, GMV, GFL, ZWRL9, ZGEO0, ZRCP0, ZPRE0F, ZCTY0, YDGFL%GFLT1 )
#endif

    !FIELDS: PSLBUF1 contains PB1 fields to be interpolated (input)
    !FIELDS: PSLBUF2 contains PB2 fields (input and output)

    IF (NOPT_MEMORY == 0) THEN
      CALL CALL_SL_HEAP(YDGEOMETRY,YDGMV,YDMODEL,YDGFL%GFL,YDSL,&
       & IVSEPC,IVSEPL,PSLBUF1,PSLBUF2,ZWRL9,YDGMV%GMVT1,YDGMV%GMVT1S,YDGFL%GFLT1,&
       & GFLPC,KL0,PLSCAW,ZPBUP9,ZPBVP9,ZPBTP9,ZPBGFLP9)
    ELSEIF (NOPT_MEMORY == 1) THEN
      CALL CALL_SL_STACK(YDGEOMETRY,YDGMV,YDMODEL,YDGFL%GFL,YDSL,&
       & IVSEPC,IVSEPL,PSLBUF1,PSLBUF2,ZWRL9,YDGMV%GMVT1,YDGMV%GMVT1S,YDGFL%GFLT1,&
       & GFLPC,KL0,PLSCAW,ZPBUP9,ZPBVP9,ZPBTP9,ZPBGFLP9)
    ELSE
      CALL CALL_SL_HEAP2(YDGEOMETRY,YDGMV,YDMODEL,YDGFL%GFL,YDSL,&
       & IVSEPC,IVSEPL,PSLBUF1,PSLBUF2,ZWRL9,YDGMV%GMVT1,YDGMV%GMVT1S,YDGFL%GFLT1,&
       & GFLPC,KL0,PLSCAW,ZPBUP9,ZPBVP9,ZPBTP9,ZPBGFLP9)
    ENDIF

!------------------------------------------------------------------
!*  TRACER MASS FIXER - MADE OPTIONAL THROUGH LTRCMFIX
!   A FIX FOR A LOSS OR GAIN OF MASS DUE TO THE ADVECTION SCHEME ONLY
!------------------------------------------------------------------
    IF ((LTRCMFIX.OR.LADVNEGFIX).AND.((LPC_FULL.AND.NCURRENT_ITER==NSITER).OR.(.NOT.LPC_FULL))) THEN
      IF (NMFDIAGLEV>2) THEN ! Full range of mass diags including troposhere before fixers applied
        CALL GMASSDIAG(YDGEOMETRY,YDGMV,YDMODEL%YRML_CHEM,YDMODEL%YRML_GCONF%YGFL,YDDYN,GMV,GMVS,YDGMV%GMVT1S, &
 &                     GFL,YDGFL%GFLT1,KOPT=1)
        CALL GMASSDIAG(YDGEOMETRY,YDGMV,YDMODEL%YRML_CHEM,YDMODEL%YRML_GCONF%YGFL,YDDYN,GMV,GMVS,YDGMV%GMVT1S, &
 &                     GFL,YDGFL%GFLT1,KOPT=1, LDTROP=.TRUE.)
      ENDIF
      CALL GSTATS(7,0)
      CALL TRMFIXERS(YDGEOMETRY,YDGMV,YDMODEL%YRML_CHEM%YRCOMPO,YDDPHY,YDMODEL%YRML_GCONF,YDMODEL%YRML_DYN, &
        & LL_CALL_SL,KL0,PLSCAW,PSLBUF1,YDSL,GMVS,YDGMV%GMVT1S,&
        & GFL,YDGFL%GFLT1,ZEXTRADYN)
      CALL GSTATS(7,1)
      IF (NMFDIAGLEV>2) THEN ! Full range of mass diags including troposhere after fixers applied
        CALL GMASSDIAG(YDGEOMETRY,YDGMV,YDMODEL%YRML_CHEM,YDMODEL%YRML_GCONF%YGFL,YDDYN,GMV,GMVS,YDGMV%GMVT1S, &
 &                     GFL,YDGFL%GFLT1)
        CALL GMASSDIAG(YDGEOMETRY,YDGMV,YDMODEL%YRML_CHEM,YDMODEL%YRML_GCONF%YGFL,YDDYN,GMV,GMVS,YDGMV%GMVT1S, &
 &                     GFL,YDGFL%GFLT1, LDTROP=.TRUE.)
      ENDIF
    ENDIF
! The following call allow basic diags to be printed regardless if mass fixer is active or not.
    IF (NMFDIAGLEV==1.OR.NMFDIAGLEV==2) THEN
      CALL GMASSDIAG(YDGEOMETRY,YDGMV,YDMODEL%YRML_CHEM,YDMODEL%YRML_GCONF%YGFL, &
 &                   YDDYN,GMV,GMVS,YDGMV%GMVT1S,GFL,YDGFL%GFLT1)
    ENDIF

  ENDIF

  CALL GSTATS(8,2)
  CALL GSTATS(10,0)
! cop
  IF (LCHEM_ANAO3 ) CALL CPICGFL(YDGEOMETRY,YDGMV,YDMODEL%YRML_CHEM,YGFL,GMV,GMVS,YDGFL%GFLT1)

  IF (KCHEM_NOXADV>0) THEN
    ! reset arrays with individual NOx/NOy/CLx components
    CALL CHEM_NOYADV(YDGEOMETRY,YGFL,YDCHEM,2,KCHEM_NOXADV,GFL,YDGFL%GFLT1)
  ENDIF

  ! COLLECT SMOS DATA IN GRID POINT
  IF(LESMOS_ACTIVE.OR.LESMOS_SEKF) THEN
    IF(LSCREEN.OR.LESMOS_SEKF)THEN
      CALL SMOS_PROCESS(YDGEM,ZSMOS_OBS_BUF,ISMOS_BUF,ZSMOS_ANGLE,ZSMOS_FARAD,ZSMOS_GEOMET)
    ENDIF
  ENDIF

  ! COLLECT SMAP DATA IN GRID POINT
  IF(LESMAP_ACTIVE) THEN
    IF(LSCREEN)THEN
      CALL SMAP_PROCESS(YDGEM,ZSMAP_OBS_BUF,ISMAP_BUF,ZSMAP_ANGLE)
    ENDIF
  ENDIF

!     ------------------------------------------------------------------
!*       5.     ECMWF RADIATION
!     ------------------------------------------------------------------

  IF (LEPHYS.AND.LAGPHY.AND.LERADI.AND.(.NOT.LPHYLIN)) THEN
    IF (NRADFR == 0 ) THEN
      CALL ABOR1('GP_MODEL: NRADFR=0 !')
    ELSEIF (MOD(NSTEP,NRADFR) == 0) THEN
      IF (LPC_FULL) THEN
        LL_CALLRADDRV=(NCURRENT_ITER == NSITER)
      ELSE
        LL_CALLRADDRV=.TRUE.
      ENDIF
      IF (LL_CALLRADDRV) THEN
        CALL GSTATS(8,2)
        CALL GSTATS(13,0)

        ! Modify input state of GFL
        IF(LERADIMPL)THEN
!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(JKGLO,ICEND,IBL,JGFL)
          DO JKGLO=1,NGPTOT,NPROMA
            IBL=(JKGLO-1)/NPROMA+1
            ICEND=MIN(NPROMA,NGPTOT-JKGLO+1)
            DO JGFL=1,NUMFLDS
              IF(YCOMP(JGFL)%LT1) THEN
                ! Store
                ZGFLT9(JKGLO:JKGLO+ICEND-1,1:NFLEVG,JGFL)= &
                 &  GFL(1:ICEND,1:NFLEVG,YCOMP(JGFL)%MP9_PH,IBL)
                ! Overwrite
                GFL(1:ICEND,1:NFLEVG,YCOMP(JGFL)%MP9_PH,IBL)= &
                 &  YDGFL%GFLT1(1:ICEND,1:NFLEVG,YCOMP(JGFL)%MP1,IBL)
              ENDIF
            ENDDO
          ENDDO
!$OMP END PARALLEL DO
        ENDIF

        CALL RADDRV(YDGEOMETRY,YDFIELDS,YDMODEL,NGPTOT,NFLDSLB2,NGPBLKS,PSLBUF2)

        ! Restore back the original state
        IF(LERADIMPL)THEN
!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(JKGLO,ICEND,IBL,JGFL)
          DO JKGLO=1,NGPTOT,NPROMA
            IBL=(JKGLO-1)/NPROMA+1
            ICEND=MIN(NPROMA,NGPTOT-JKGLO+1)
            DO JGFL=1,NUMFLDS
              IF(YCOMP(JGFL)%LT1) THEN
                GFL(1:ICEND,1:NFLEVG,YCOMP(JGFL)%MP9_PH,IBL)= &
                 &  ZGFLT9(JKGLO:JKGLO+ICEND-1,1:NFLEVG,JGFL)
              ENDIF
            ENDDO
          ENDDO
!$OMP END PARALLEL DO
        ENDIF

        CALL GSTATS(13,1)
        CALL GSTATS(8,3)
        ! UNCOMMENT THE FOLLOWING LINE TO GET FORECAST DAYS PER DAY STATS
        ! SINCE THE PREVIOUS RADIATION STEP (USEFUL WHEN BENCHMARKING)
        ! IF( NCONF==1 ) CALL FORECAST_DAYS_CALC
      ENDIF
    ENDIF
  ENDIF

  !   ------------------------------------------------------------------
  !*     6.    ECMWF Physics
  !   ------------------------------------------------------------------

  IF( LGPSTRESS ) THEN
    CALL GPSTRESS(YDGEOMETRY,YDGMV,YDDPHY,YDRIP,YDDYN,LGRADSP,GMV,GMVS,POUT=ZOUT)
  ENDIF

  IF((LEPHYS.OR.LHELDSUAREZ) .AND. LAGPHY) THEN
    IF ((LPC_FULL.AND.(NCURRENT_ITER == NSITER)).OR.(.NOT.LPC_FULL)) THEN
      IF (LCUCONV_CA) CALL CUCONVCA(YDGEOMETRY,YDGMV, YDMODEL,NSTEP) !update CA
      CLCONF=CDCONF(4:4)
      ! For the predictor-corrector scheme, the PH9 field pointers are
      ! not in sync with the underlying GMV base arrays, so we force
      ! a re-sync of GMV fields here. A similar re-aliasing is done
      ! for GFL fields in `DEFINE_POINTERS_MP9` above
      IF (LPC_FULL.AND.LTWOTL.AND.LEPHYS)  CALL YDFIELDS%VARIABLES%GMV_RESET_PH9(YDGMV,YDGFL)

      CALL EC_PHYS_DRV(YDGEOMETRY,YDFIELDS,YDFIELDS%VARIABLES,YDGMV,YDSURF,YDMODEL,CLCONF,&
          & PSLBUF2,ZPBUP9,ZPBVP9,ZPBTP9,ZPBGFLP9,ZEXTRADYN,SD_PF,&
          & GMV,GMVS,YDGMV%GMVT1,YDGMV%GMVT1S,&
          & YDGFL%GFLT1,GFLPT,GFL,GFLSLP,&
          & SP_SB,SP_SG,SP_SL,SP_RR,&
          & SP_EP,SP_X2,&
          & SD_VF,SD_VN,SD_VD,&
          & SD_WS,SD_WW,&
          & SD_XA,SD_X2,&
          & SD_SM,&
          & SP_OM,SD_V2,SD_V3,&!KPP
          & YDFIELDS%YEC_PHYS_FIELDS%YRTILEPROP,&
          & YDFIELDS%YEC_PHYS_FIELDS%YRPHYSMWAVE,&
          & ZSMOS_OBS_BUF,ZSMOS_TB_BUF,ZSMOS_ANGLE,ZSMOS_FARAD,ZSMOS_GEOMET,&
          & ISMOS_BUF,ZSMAP_OBS_BUF,ZSMAP_TB_BUF,ZSMAP_ANGLE,ISMAP_BUF,&
          & PTRAJEC=PTRAJEC,PTRAJEC_OOPS=PTRAJEC_OOPS)
    ENDIF
  ENDIF

  IF (ALLOCATED(ZPBUP9)) DEALLOCATE(ZPBUP9)
  IF (ALLOCATED(ZPBVP9)) DEALLOCATE(ZPBVP9)
  IF (ALLOCATED(ZPBTP9)) DEALLOCATE(ZPBTP9)
  IF (ALLOCATED(ZPBGFLP9)) DEALLOCATE(ZPBGFLP9)
  IF (ALLOCATED(ZWRL9)) DEALLOCATE(ZWRL9)
  IF (ALLOCATED(ZRCP0)) DEALLOCATE(ZRCP0)
  IF (ALLOCATED(ZPRE0F)) DEALLOCATE(ZPRE0F)
  IF (ALLOCATED(ZCTY0)) DEALLOCATE(ZCTY0)
  IF (ALLOCATED(ZGEO0)) DEALLOCATE(ZGEO0)

  IF (LESMOS_ACTIVE)THEN
    IF (LSCREEN) THEN
       CALL SMOS_UPDATE(YDGEM,ZSMOS_TB_BUF,ISMOS_BUF)
    ENDIF
  ENDIF

  IF (LESMAP_ACTIVE)THEN
    IF (LSCREEN) THEN
       CALL SMAP_UPDATE(YDGEM,ZSMAP_TB_BUF,ISMAP_BUF)
    ENDIF
  ENDIF

! IF (LCOUPLO4_ENV) THEN
!   ! call of oasis4 exchange - put meteo
!   CALL COUPLO4_EXCHANGE(2)
! ENDIF

  !   ------------------------------------------------------------------
  !*     7.    TRANSFER TO PUT1 ARRAYS
  !   ------------------------------------------------------------------


  LLCONFX=(CDCONF(4:4)=='X')

  CALL GSTATS(1040,0)

!$OMP PARALLEL PRIVATE(JKGLO,IBL)
!$OMP DO SCHEDULE(DYNAMIC,1) FIRSTPRIVATE(YLDDH) LASTPRIVATE(YLDDH)
  DO JKGLO=1,IGPCOMP,NPROMA
    IBL=(JKGLO-1)/NPROMA+1
    CALL CPGLAG(YDGEOMETRY,YDGMV,YDMODEL%YRML_DIAG,YDMODEL%YRML_GCONF,YDMODEL%YRML_DYN,&
     & IGPCOMP,JKGLO,LLCONFX,&
     & GMV(:,:,:,IBL),GMVS(:,:,IBL),PSLBUF2(:,:,IBL),YDGFL%GFLT1(:,:,:,IBL),YDGMV%GMVT1(:,:,:,IBL), &
     & YDGMV%GMVT1S(:,:,IBL),GFL(:,:,:,IBL),&
     & GMVTNDSL_DDH,GFLTNDSL_DDH,YLDDH,PGPSTRESS=ZOUT(:,:,:,IBL),PTRAJEC=PTRAJEC,PTRAJEC_OOPS=PTRAJEC_OOPS)
  ENDDO
!$OMP END DO
!$OMP END PARALLEL

  CALL GSTATS(1040,1)

  IF (LFLEXDIA.AND.LDDH_OMP) THEN
    CALL STOREDDH(YDMDDH,YDMODEL%YRML_DIAG%YRTDDH,YLDDH)
    IF (CDCONF(4:4)=='X') THEN
      ! equivalent of reset_ddhflex for lddhomp
      NTOTFIELD=0
      NTOTVAR=0
      NTOTSURF=0
      NTOTSVAR=0
      NTOTSVFS=0
    ENDIF
  ENDIF

  CALL GSTATS(10,1)
  CALL GSTATS(8,3)

  IF (LFINDVSEP) THEN
    DO IBL=1,NGPBLKS
      NVSEPC=MAX(NVSEPC,IVSEPC(IBL))
      NVSEPL=MAX(NVSEPL,IVSEPL(IBL))
    ENDDO
  ENDIF

ELSE

  IF (LSLAG) THEN
    IF (NPROC > 1) THEN
      LLINC = .FALSE.
      CALL SLCOMM(YDSL,IDUMARR,NFLDSLB1,LLINC,0,PSLBUF1)
    ENDIF
    CALL SLEXTPOL(YDDIM,YDSL,NFLDSLB1,IDUMARR,1,RPARSL1,PSLBUF1)
    IF( LSLDEBUG) CALL CHECK_SL_STRUCT(YDSL,'SLEXTPOL')
  ENDIF
  DO JKGLO=1,NGPTOT,NPROMA
    ICEND=MIN(NPROMA,NGPTOT-JKGLO+1)
    IBL=(JKGLO-1)/NPROMA+1
    CALL CPG2LAG(YDGEOMETRY,YDGMV,YDMODEL%YRML_GCONF,YDMODEL%YRML_DYN,ICEND,YDSL,IBL,&
     & PSLBUF1,PSLBUF2(1,1,IBL),GMV(:,:,:,IBL),GMVS(:,:,IBL),&
     & YDGMV%GMVT1(:,:,YT1%MU,IBL),YDGMV%GMVT1(:,:,YT1%MV,IBL),YDGMV%GMVT1S(:,YT1%MSP,IBL))
  ENDDO

ENDIF ! LR2D

IF (ALLOCATED(ZSMOS_OBS_BUF)) DEALLOCATE(ZSMOS_OBS_BUF)
IF (ALLOCATED(ISMOS_BUF)) DEALLOCATE(ISMOS_BUF)
IF (ALLOCATED(ZSMOS_TB_BUF)) DEALLOCATE(ZSMOS_TB_BUF)
IF (ALLOCATED(ZSMOS_ANGLE)) DEALLOCATE(ZSMOS_ANGLE)
IF (ALLOCATED(ZSMOS_FARAD)) DEALLOCATE(ZSMOS_FARAD)
IF (ALLOCATED(ZSMOS_GEOMET)) DEALLOCATE(ZSMOS_GEOMET)
IF (ALLOCATED(ZSMAP_OBS_BUF)) DEALLOCATE(ZSMAP_OBS_BUF)
IF (ALLOCATED(ISMAP_BUF)) DEALLOCATE(ISMAP_BUF)
IF (ALLOCATED(ZSMAP_TB_BUF)) DEALLOCATE(ZSMAP_TB_BUF)
IF (ALLOCATED(ZSMAP_ANGLE)) DEALLOCATE(ZSMAP_ANGLE)

!     ------------------------------------------------------------------
END ASSOCIATE
END ASSOCIATE
#ifdef WITH_ATLAS
CALL TRACE%FINAL()
#endif
IF (LHOOK) CALL DR_HOOK('GP_MODEL',1,ZHOOK_HANDLE)
END SUBROUTINE GP_MODEL
