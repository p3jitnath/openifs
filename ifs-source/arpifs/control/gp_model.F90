 & GMVTNDSL_DDH=>YDGPDDH%GMVTNDSL_DDH, &
 & NFLDSLB1=>YDPTRSLB1%NFLDSLB1, RPARSL1=>YDPTRSLB1%RPARSL1, &
 & NFLDSLB2=>YDPTRSLB2%NFLDSLB2, &
 & EMTD=>YDRADF%EMTD, EMTU=>YDRADF%EMTU, RMOON=>YDRADF%RMOON, TRSW=>YDRADF%TRSW, &
