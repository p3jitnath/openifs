IF( (LIFSTRAJ .OR. LIFSMIN) .AND. LERAINGG .AND.&
 & MOD((NSTEP+1)*NINT(TSPHY),3600) == 0 .AND. NSTEP < NSTOP)THEN
  LERAINGG_ACTIVE=.TRUE.
