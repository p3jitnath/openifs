SUBROUTINE WRITE_GRID_GRIB(YDGEOMETRY,YDRIP,CDFILE,YDGRID,KGRIB_HANDLE,KTYPE_INTERP,CDMODE,PGPSP)

!     Purpose.
!     --------
!     Gather grid point fields on one processor
!     and write it out in GRIB file.

!     Author.
!     -------
!       Y. Tremolet

!     Modifications.
!     --------------
!       Original : 31-01-01
!        M.Hamrud      01-Oct-2003 CY28 Cleaning
!        M.Hamrud      10-Jan-2004 CY28R1 Cleaning
!        Y.Tremolet    07-Jun-2004 Use type gridpoint_field
!        Y.Tremolet    04-Nov-2004 Added optional mode for appending to file
!        Y.Tremolet    10-Nov-2004 Option to force ECMWF local GRIB definition
!        G. Radnoti    25-Jan-2005 dimensioning from grib header info  
!        Y.Tremolet    17-Feb-2005 Conserving interpolation (copied from WRITE_GRID_GRIB routine)
!        Y.Tremolet    30-Mar-2005 PREGRBENC -> PRESET_GRIB_HEAD
!        E.Holm        04-Jan-2008 Conserving interpolation interface
!                                  changed to use surface pressure
!        G.Mozdzynski (Feb 2011): OOPS cleaning, use of derived type TCSGLEG
!        T.Wilhelmsson 31-Aug-2012 Use IOSTREAM
! ----------------------------------------------------------------------

USE YOMRIP              , ONLY : TRIP
USE GEOMETRY_MOD        , ONLY : GEOMETRY
USE PARKIND1            , ONLY : JPIM, JPRB
USE YOMHOOK             , ONLY : LHOOK, DR_HOOK, JPHOOK
USE YOMLUN              , ONLY : NULERR, NULOUT
USE YOMMP0              , ONLY : MYPROC
USE YOM_GRIB_CODES      , ONLY : NGRBV
USE GRIDPOINT_FIELDS_MIX, ONLY : ASSIGNMENT(=), GRIDPOINT_FIELD
USE IOSTREAM_MIX        , ONLY : SETUP_IOSTREAM, SETUP_IOREQUEST, INQUIRE_GRIB,&
 &                               IO_PUT, CLOSE_IOSTREAM, TYPE_IOSTREAM, TYPE_IOREQUEST, CLOSE_IOREQUEST

IMPLICIT NONE

TYPE(GEOMETRY)       , INTENT(IN) :: YDGEOMETRY
TYPE(TRIP)           , INTENT(IN) :: YDRIP
CHARACTER(LEN=*)     , INTENT(IN) :: CDFILE 
TYPE(GRIDPOINT_FIELD), INTENT(IN) :: YDGRID
INTEGER(KIND=JPIM), OPTIONAL, INTENT(IN) :: KGRIB_HANDLE
INTEGER(KIND=JPIM), OPTIONAL, INTENT(IN) :: KTYPE_INTERP 
CHARACTER(LEN=1)  , OPTIONAL, INTENT(IN) :: CDMODE
REAL(KIND=JPRB)   , OPTIONAL, INTENT(IN) :: PGPSP(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIM%NGPBLKS)

INTEGER(KIND=JPIM) :: II, JB, JJ, JF, JL, IOPROC
INTEGER(KIND=JPIM) :: ILATS, IGPOUT, ILEN, IDGLG, ITYPE_INTERP
LOGICAL :: LLINTERP, LLPARITY
REAL(KIND=JPRB), ALLOCATABLE :: ZLOCAL(:), ZLOWRES(:,:,:), ZGRID(:,:,:), ZLATS(:)
REAL(KIND=JPRB), ALLOCATABLE :: ZPSPIN(:), ZPSPOUT(:)
INTEGER(KIND=JPIM), ALLOCATABLE :: ILONS(:), ILEVS(:),ICODES(:)
TYPE(TYPE_IOSTREAM) :: YL_IOSTREAM
TYPE(TYPE_IOREQUEST) :: YL_IOREQUEST
CHARACTER(LEN=1) :: CLMODE
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

#include "abor1.intfb.h"
#include "gathergpf.intfb.h"
#include "grid_bicubic.intfb.h"
#include "grid_bilinear.intfb.h"
#include "grid_biconserv.intfb.h"
#include "pre_grid_biconserv.intfb.h"

! ----------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('WRITE_GRID_GRIB',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM=>YDGEOMETRY%YRDIM, &
  & YDDIMV=>YDGEOMETRY%YRDIMV,YDGEM=>YDGEOMETRY%YRGEM, YDMP=>YDGEOMETRY%YRMP, YDCSGLEG=>YDGEOMETRY%YRCSGLEG)
ASSOCIATE(NDGLG=>YDDIM%NDGLG, NGPBLKS=>YDDIM%NGPBLKS, NPROMA=>YDDIM%NPROMA, &
 & NGPTOTG=>YDGEM%NGPTOTG, NLOENG=>YDGEM%NLOENG)
ITYPE_INTERP=2
IF (PRESENT(KTYPE_INTERP)) ITYPE_INTERP=KTYPE_INTERP

IOPROC=1
CLMODE="w"
IF (PRESENT(CDMODE)) CLMODE=CDMODE
CALL SETUP_IOSTREAM(YL_IOSTREAM,'CIO',CDFILE,CLMODE,KIOMASTER=IOPROC,LDNOHELP=.TRUE.)

IF (PRESENT(KGRIB_HANDLE)) THEN
  CALL INQUIRE_GRIB(KGRIB_HANDLE,KLATS=IDGLG)
  ALLOCATE(ILONS(IDGLG))
  ALLOCATE(ZLATS(IDGLG))
  CALL INQUIRE_GRIB(KGRIB_HANDLE,KLATS=ILATS,KLONS=ILONS,PLATS=ZLATS,KNVALS=IGPOUT)
  
  LLINTERP=(YDGRID%NGPTOTG/=IGPOUT)
  IF (LLINTERP) THEN
    SELECT CASE (ITYPE_INTERP)
    CASE(1)
      WRITE(NULOUT,*)'WRITE_GRID_GRIB: doing linear interpolation'
    CASE(2)
      WRITE(NULOUT,*)'WRITE_GRID_GRIB: doing cubic interpolation'
    CASE(3)
      WRITE(NULOUT,*)'WRITE_GRID_GRIB: doing conserving interpolation'
    CASE DEFAULT
      CALL ABOR1('WRITE_GRID_GRIB: unkown interpolation type')
    END SELECT
  ENDIF
ELSE
  LLINTERP=.FALSE.
  IGPOUT=YDGRID%NGPTOTG
ENDIF

IF (YDGRID%NG3D>0) THEN
  ALLOCATE(ZLOCAL(YDGRID%NGPTOT*YDGRID%NFLEVG))
  ALLOCATE(ZGRID(YDGRID%NGPTOTG,YDGRID%NFLEVG,1))
  IF (LLINTERP.AND.ITYPE_INTERP==3) THEN
    ALLOCATE(ZPSPIN(YDGRID%NGPTOTG))
    ALLOCATE(ZPSPOUT(IGPOUT))
    IF (PRESENT(PGPSP)) THEN
      CALL PRE_GRID_BICONSERV(YDGEOMETRY,IGPOUT,ILATS,ILONS,ZLATS,&
                            & ZPSPIN,ZPSPOUT,PGPSP=PGPSP)
    ELSE
      IF (ASSOCIATED(YDGRID%SPR)) THEN
        CALL PRE_GRID_BICONSERV(YDGEOMETRY,IGPOUT,ILATS,ILONS,ZLATS,&
                              & ZPSPIN,ZPSPOUT,PGPSP=YDGRID%SPR)
      ELSE
        CALL ABOR1('WRITE_GRID_GRIB: LNSP required for interpolation')
      ENDIF
    ENDIF
  ENDIF
ENDIF

IF (MYPROC==IOPROC) THEN
  IF (LLINTERP) ALLOCATE(ZLOWRES(IGPOUT,YDGRID%NFLEVG,1))
  ALLOCATE(ILEVS(YDGRID%NFLEVG))
  ALLOCATE(ICODES(YDGRID%NFLEVG))
ENDIF

DO JF=1,YDGRID%NG3D
  CALL GSTATS(1946,0)
  II=0
  DO JL=1,YDGRID%NFLEVG
    DO JB=1,YDGRID%NGPBLKS
      ILEN=MIN(YDGRID%NPROMA, YDGRID%NGPTOT-(JB-1)*YDGRID%NPROMA)
      DO JJ=1,ILEN
        II=II+1
        ZLOCAL(II)=YDGRID%GP3D(JJ,JL,JF,JB)
      ENDDO
    ENDDO
  ENDDO
  CALL GSTATS(1946,1)

  CALL GATHERGPF(YDGEOMETRY,ZLOCAL,ZGRID(:,:,1),YDGRID%NFLEVG,IOPROC)

  IF (MYPROC==IOPROC) THEN
    DO JL=1,YDGRID%NFLEVG
      ILEVS(JL)=JL
    ENDDO
    ICODES(:)=YDGRID%NGRIB3(JF)

    IF (LLINTERP) THEN
      IF (NGPTOTG /= YDGRID%NGPTOTG) THEN
        WRITE(NULERR,*)'WRITE_GRID_GRIB: NGPTOT /= YDGRID%NGPTOT'
        CALL ABOR1('WRITE_GRID_GRIB: Un supported option used')
      ENDIF

      DO JL=1,YDGRID%NFLEVG
        SELECT CASE (ITYPE_INTERP)
        CASE(1)
          CALL GRID_BILINEAR(NDGLG,NLOENG(1:NDGLG),YDCSGLEG%RLATIG(1:NDGLG),&
           & NGPTOTG,ILATS,ILONS,ZLATS,IGPOUT,ZGRID(:,JL,1),ZLOWRES(:,JL,1))
        CASE(2)
          CALL GRID_BICUBIC (NDGLG,NLOENG(1:NDGLG),YDCSGLEG%RLATIG(1:NDGLG),&
           & NGPTOTG,ILATS,ILONS,ZLATS,IGPOUT,ZGRID(:,JL,1),ZLOWRES(:,JL,1))
        CASE(3)
          LLPARITY=.NOT.(YDGRID%NGRIB3(JF)==NGRBV)
          CALL GRID_BICONSERV(YDGEOMETRY%YRVAB%VAH,YDGEOMETRY%YRVAB%VBH, NDGLG,NLOENG(1:NDGLG),YDCSGLEG%RLATIG(1:NDGLG),&
           & NGPTOTG,ILATS,ILONS,ZLATS,IGPOUT,ZGRID(:,JL,1),ZLOWRES(:,JL,1),&
           & LLPARITY,JL,ZPSPIN,ZPSPOUT)
        END SELECT
      ENDDO

      CALL SETUP_IOREQUEST(YL_IOREQUEST,'GRIDPOINT_FIELDS',LDGRIB=.TRUE.,CDLEVTYPE='ML',&
        & LDISTRIBUTE=.FALSE.,KPROMA=IGPOUT,KGPTOTG=IGPOUT,KGRIB_HANDLE=KGRIB_HANDLE,&
        & KGRIB2D=ICODES,KLEVS2D=ILEVS,PTSTEP=YDRIP%TSTEP)
      CALL IO_PUT(YL_IOSTREAM,YL_IOREQUEST,PR3=ZLOWRES)
      CALL CLOSE_IOREQUEST(YL_IOREQUEST)
    ELSE
      CALL SETUP_IOREQUEST(YL_IOREQUEST,'GRIDPOINT_FIELDS',LDGRIB=.TRUE.,CDLEVTYPE='ML',&
        & LDISTRIBUTE=.FALSE.,KPROMA=IGPOUT,KGRIB2D=ICODES,KLEVS2D=ILEVS,PTSTEP=YDRIP%TSTEP)
      CALL IO_PUT(YL_IOSTREAM,YL_IOREQUEST,PR3=ZGRID)
      CALL CLOSE_IOREQUEST(YL_IOREQUEST)
    ENDIF
  ENDIF
ENDDO

IF (ALLOCATED(ZLOCAL  )) DEALLOCATE(ZLOCAL)
IF (ALLOCATED(ZGRID   )) DEALLOCATE(ZGRID)

IF (YDGRID%NG2D>0) THEN
  ALLOCATE(ZLOCAL(YDGRID%NGPTOT*YDGRID%NG2D))
  ALLOCATE(ZGRID(YDGRID%NGPTOTG,YDGRID%NG2D,1))

  CALL GSTATS(1946,0)
  II=0
  DO JF=1,YDGRID%NG2D
    DO JB=1,YDGRID%NGPBLKS
      ILEN=MIN(YDGRID%NPROMA, YDGRID%NGPTOT-(JB-1)*YDGRID%NPROMA)
      DO JJ=1,ILEN
        II=II+1
        ZLOCAL(II)=YDGRID%GP2D(JJ,JF,JB)
      ENDDO
    ENDDO
  ENDDO
  CALL GSTATS(1946,1)

  CALL GATHERGPF(YDGEOMETRY,ZLOCAL,ZGRID(:,:,1),YDGRID%NG2D,IOPROC)

  IF (MYPROC==IOPROC) THEN
    IF (ALLOCATED(ZLOWRES)) DEALLOCATE(ZLOWRES)
    IF (LLINTERP) ALLOCATE(ZLOWRES(IGPOUT,YDGRID%NG2D,1))

    IF (ALLOCATED(ICODES)) DEALLOCATE(ICODES)
    ALLOCATE(ICODES(YDGRID%NG2D))
    DO JF=1,YDGRID%NG2D
      ICODES(JF) = YDGRID%NGRIB2(JF)
    ENDDO

    IF (LLINTERP) THEN
      DO JF=1,YDGRID%NG2D
        SELECT CASE (ITYPE_INTERP)
        CASE(1:2)
          CALL GRID_BILINEAR (NDGLG,NLOENG(1:NDGLG),YDCSGLEG%RLATIG(1:NDGLG),&
           & NGPTOTG,ILATS,ILONS,ZLATS,IGPOUT,ZGRID(:,JF,1),ZLOWRES(:,JF,1))
        CASE(3)
          CALL GRID_BICONSERV(YDGEOMETRY%YRVAB%VAH,YDGEOMETRY%YRVAB%VBH, NDGLG,NLOENG(1:NDGLG),YDCSGLEG%RLATIG(1:NDGLG),&
           & NGPTOTG,ILATS,ILONS,ZLATS,IGPOUT,ZGRID(:,JF,1),ZLOWRES(:,JF,1))
        END SELECT
      ENDDO

      CALL SETUP_IOREQUEST(YL_IOREQUEST,'GRIDPOINT_FIELDS',LDGRIB=.TRUE.,CDLEVTYPE='ML',&
        & LDISTRIBUTE=.FALSE.,KPROMA=IGPOUT,KGPTOTG=IGPOUT,KGRIB_HANDLE=KGRIB_HANDLE,&
        & KGRIB2D=ICODES,PTSTEP=YDRIP%TSTEP)    
      CALL IO_PUT(YL_IOSTREAM,YL_IOREQUEST,PR3=ZLOWRES)
      CALL CLOSE_IOREQUEST(YL_IOREQUEST)
    ELSE
      CALL SETUP_IOREQUEST(YL_IOREQUEST,'GRIDPOINT_FIELDS',LDGRIB=.TRUE.,CDLEVTYPE='ML',&
        & LDISTRIBUTE=.FALSE.,KPROMA=IGPOUT,KGRIB2D=ICODES,PTSTEP=YDRIP%TSTEP)    
      CALL IO_PUT(YL_IOSTREAM,YL_IOREQUEST,PR3=ZGRID)
      CALL CLOSE_IOREQUEST(YL_IOREQUEST)
    ENDIF
  ENDIF

  DEALLOCATE(ZLOCAL)
  DEALLOCATE(ZGRID)
ENDIF

IF (MYPROC==IOPROC) THEN
  CALL CLOSE_IOSTREAM(YL_IOSTREAM)
  IF (ALLOCATED(ZLOWRES)) DEALLOCATE(ZLOWRES)
  IF (ALLOCATED(ILEVS  )) DEALLOCATE(ILEVS)
  IF (ALLOCATED(ICODES )) DEALLOCATE(ICODES)
ENDIF
IF (ALLOCATED(ILONS  )) DEALLOCATE(ILONS)
IF (ALLOCATED(ZLATS  )) DEALLOCATE(ZLATS)
IF (ALLOCATED(ZPSPIN )) DEALLOCATE(ZPSPIN)
IF (ALLOCATED(ZPSPOUT)) DEALLOCATE(ZPSPOUT)

END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('WRITE_GRID_GRIB',1,ZHOOK_HANDLE)
END SUBROUTINE WRITE_GRID_GRIB
