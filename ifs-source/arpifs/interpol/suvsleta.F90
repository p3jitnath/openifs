!   NOTE: is done for the full level interpolation only as there is no half level trajectory
!          research available
ALLOCATE(YDVSLETA%GAMMA_WENO(NFLEVG,3))
  
IF (LLWENO.AND.(.NOT. YDCVER%LREGETA)) THEN
  DO JLEV=2,NFLEVG-4
    ZD1=YDVETA%VETAF(JLEV-1)-YDVETA%VETAF(JLEV+3)
    ZD2=YDVETA%VETAF(JLEV-1)-YDVETA%VETAF(JLEV+4)
    ZD3=YDVETA%VETAF(JLEV  )-YDVETA%VETAF(JLEV+3)
    ZD4=YDVETA%VETAF(JLEV  )-YDVETA%VETAF(JLEV+4)
    ZD5=YDVETA%VETAF(JLEV  )-YDVETA%VETAF(JLEV-1)

    ! eliminated from i-1
    YDVSLETA%GAMMA_WENO(JLEV,3)=1._JPRD/(ZD1*ZD2)
    ! eliminated from i+4
    YDVSLETA%GAMMA_WENO(JLEV,2)=1._JPRD/(ZD2*ZD4)
    ! eliminated from i
    ! (probably just a bit more sophisticated way to define gamma1 = -(gamma2+gamma3)
    YDVSLETA%GAMMA_WENO(JLEV,1)=(1._JPRD-(ZD3*ZD4)/(ZD1*ZD2))/(ZD5*ZD4)
  ENDDO
ENDIF

