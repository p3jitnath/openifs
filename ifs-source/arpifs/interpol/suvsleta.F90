#endif
SUBROUTINE SUVSLETA(YDGEOMETRY,YDDYNA,YDVSLETA,LDVERBOSE)

!**** *SUVSLETA*  - Routine to initialize structure VSLETA
!                   (externalisable part of interpolator)

!     Purpose.
!     --------
!           Routine to initialize structure VSLETA.
!           Intermediate quantities used for vertical interpolations.

!**   Interface.
!     ----------

!     *CALL* SUVSLETA

!        Explicit arguments :
!        --------------------
!        LDVERBOSE : more printings.

!        Implicit arguments :
!        --------------------
!        see the modules used above.

!     Method.
!     -------

!     Externals.
!     ----------

!     Reference.
!     ----------
!        ECMWF Research Department documentation of the IFS

!     Author.
!     -------
!      Mats Hamrud and Philippe Courtier  *ECMWF* (87-10-15) in routine SUVERT.
!      K. Yessad (2012) after SUVERT.

!     Modifications.
!     --------------
!      F. Vana 13-Feb-2014 option LSLHDVER
!      T. Wilhelmsson (Sept 2013) Geometry and setup refactoring.
!      K. Yessad (March 2017): simplify level numbering in interpolator.
!      F. Vana    21-Nov-2017: Option LHOISLT
!      F. Vana    20-Feb-2019: Fix for WENO weights
!      F. Vana  18-Jul-2019: SLVF + cleaning
!      F. Vana    22-Oct-2019: Fixes for single precision
! End Modifications
!-------------------------------------------------------------------------------

USE GEOMETRY_MOD , ONLY : GEOMETRY
USE YOMVSLETA,     ONLY : TVSLETA
USE PARKIND1 , ONLY : JPIM, JPRB, JPRD
USE YOMHOOK  , ONLY : LHOOK, DR_HOOK, JPHOOK

USE YOMLUN_IFSAUX, ONLY : NULOUT

! arp/ifs dependencies to be solved later.
USE YOMDYNA  , ONLY : TDYNA

!-------------------------------------------------------------------------------

IMPLICIT NONE 
TYPE(GEOMETRY),INTENT(IN   ) :: YDGEOMETRY
TYPE(TDYNA),   INTENT(IN)    :: YDDYNA
TYPE(TVSLETA), INTENT(INOUT) :: YDVSLETA
LOGICAL       ,INTENT(IN)    :: LDVERBOSE

!-------------------------------------------------------------------------------

INTEGER(KIND=JPIM) :: IREF, IINCR, JL, JLEV, I_SLVF_BOT
LOGICAL :: LLWENO

REAL(KIND=JPRB) :: ZP_LEV_H(0:YDGEOMETRY%YRDIMV%NFLEVG)
REAL(KIND=JPRB) :: ZSLVFEPS_MASK(YDGEOMETRY%YRDIMV%NFLEVG)
REAL(KIND=JPRB) :: ZFAC, ZDVETAH
REAL(KIND=JPRD) :: ZD1, ZD2, ZD3, ZD4, ZD5, ZD6

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!     ------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('SUVSLETA',0,ZHOOK_HANDLE)
ASSOCIATE(YDVAB=>YDGEOMETRY%YRVAB,YDVETA=>YDGEOMETRY%YRVETA,YDVFE=>YDGEOMETRY%YRVFE,YDSTA=>YDGEOMETRY%YRSTA, &
 & YDLAP=>YDGEOMETRY%YRLAP,YDCSGLEG=>YDGEOMETRY%YRCSGLEG,YDCVER=>YDGEOMETRY%YRCVER, &
 & YDCSGEOM=>YDGEOMETRY%YRCSGEOM,YDCSGEOM_NB=>YDGEOMETRY%YRCSGEOM_NB,YDGSGEOM=>YDGEOMETRY%YRGSGEOM, &
 & YDGSGEOM_NB=>YDGEOMETRY%YRGSGEOM_NB, YDSPGEOM=>YDGEOMETRY%YSPGEOM)
ASSOCIATE(NFLEVG=>YDGEOMETRY%YRDIMV%NFLEVG, &
 & LSLHD=>YDDYNA%LSLHD, LSLHDQUAD=>YDDYNA%LSLHDQUAD, SLHDEPSV=>YDDYNA%SLHDEPSV, &
 & LSLTVWENO=>YDDYNA%LSLTVWENO, LHOISLT=>YDDYNA%LHOISLT, HOISLTH=>YDDYNA%HOISLTH, HOISLTV=>YDDYNA%HOISLTV, &
 & LRHSVWENO=>YDDYNA%LRHSVWENO, LSLVF=>YDDYNA%LSLVF, P_SLVF_BOT=>YDDYNA%P_SLVF_BOT, &
 & SLVFEPS=>YDDYNA%SLVFEPS, SLVFMIN=>YDDYNA%SLVFMIN, SLVFTR=>YDDYNA%SLVFTR, P_SLVF_TR=>YDDYNA%P_SLVF_TR, &
 & NRLEVX=>YDVSLETA%NRLEVX,VBH=>YDVAB%VBH, STPREH=>YDSTA%STPREH, STPRE=>YDSTA%STPRE,  &
 & VRLEVX=>YDVSLETA%VRLEVX,YDDIMV=>YDGEOMETRY%YRDIMV)
!     ------------------------------------------------------------------

!*            1. SET UP VERTICAL SYSTEM: YRVSLETA.
!             ------------------------------------

LLWENO = (LSLTVWENO.OR.LRHSVWENO)

! Setup of vertical profiles for SLVF activity
IF (LSLVF) THEN
  ZP_LEV_H(0:NFLEVG)=0._JPRB
  IF (P_SLVF_BOT == 0._JPRB) THEN
    ! Apply SLVF to pure p-levels, hybrid levels will be masked out.
    DO JLEV=0,NFLEVG
      IF (VBH(JLEV) /= 0._JPRB) EXIT
      ZP_LEV_H(JLEV)=1.0_JPRB
    ENDDO
  ELSE
    ! Alternatively use the SLVF for specified levels
    I_SLVF_BOT=0
    DO JLEV=1,NFLEVG
      IF (STPREH(JLEV)<= P_SLVF_BOT) I_SLVF_BOT=I_SLVF_BOT+1
    ENDDO
    I_SLVF_BOT=MIN(NFLEVG,I_SLVF_BOT)
    ZP_LEV_H(0:I_SLVF_BOT)=1.0_JPRB
  ENDIF
  ! Setup of a vertical masking function that smoothly transitions from strong value of SLVF in the
  ! lower stratosphere to small value in the upper stratosphere 
  DO JLEV=1,NFLEVG
    ZSLVFEPS_MASK(JLEV)=SLVFEPS*(SLVFMIN+(1.0_JPRB-SLVFMIN)*1.0_JPRB/(1.0_JPRB+EXP((1.0_JPRB-STPRE(JLEV))/P_SLVF_TR)/SLVFTR))
  ENDDO
ENDIF
 
! * attributes VCUICO, VSLD, VSLDW, VSLVF:
ALLOCATE(YDVSLETA%VCUICO(4,0:NFLEVG-1))
ALLOCATE(YDVSLETA%VSLD(3,0:NFLEVG-1))
ALLOCATE(YDVSLETA%VSLDW(3,3,0:NFLEVG-1))
ALLOCATE(YDVSLETA%VSLVF(3,3,0:NFLEVG-1))

IINCR=0
DO JLEV=1,NFLEVG-3
  ZD1=YDVETA%VETAF(JLEV  +IINCR)-YDVETA%VETAF(JLEV+1+IINCR)
  ZD2=YDVETA%VETAF(JLEV  +IINCR)-YDVETA%VETAF(JLEV+2+IINCR)
  ZD3=YDVETA%VETAF(JLEV  +IINCR)-YDVETA%VETAF(JLEV+3+IINCR)
  ZD4=YDVETA%VETAF(JLEV+1+IINCR)-YDVETA%VETAF(JLEV+2+IINCR)
  ZD5=YDVETA%VETAF(JLEV+1+IINCR)-YDVETA%VETAF(JLEV+3+IINCR)
  ZD6=YDVETA%VETAF(JLEV+2+IINCR)-YDVETA%VETAF(JLEV+3+IINCR)

  YDVSLETA%VCUICO(1,JLEV)= 1.0_JPRD/(ZD1*ZD2*ZD3)
  YDVSLETA%VCUICO(2,JLEV)=-1.0_JPRD/(ZD1*ZD4*ZD5)
  YDVSLETA%VCUICO(3,JLEV)= 1.0_JPRD/(ZD2*ZD4*ZD6)
  YDVSLETA%VCUICO(4,JLEV)=-1.0_JPRD/(ZD3*ZD5*ZD6)

  IF (LSLHDQUAD.OR.(LHOISLT.AND.((HOISLTH /= 0._JPRB).OR.(HOISLTV /= 0._JPRB)))) THEN
    YDVSLETA%VSLD(1,JLEV)=(ZD2-ZD6)*ZD4/(ZD1*ZD2+ZD5*ZD6)
    YDVSLETA%VSLD(2,JLEV)=(ZD5-ZD1)*ZD4/(ZD1*ZD2+ZD5*ZD6)
    YDVSLETA%VSLD(3,JLEV)=    -ZD4 *ZD4/(ZD1*ZD2+ZD5*ZD6)
  ELSE
    YDVSLETA%VSLD(1,JLEV)=0._JPRB
    YDVSLETA%VSLD(2,JLEV)=0._JPRB
    YDVSLETA%VSLD(3,JLEV)=0._JPRB
  ENDIF

  IF (LSLHD) THEN
    YDVSLETA%VSLDW(1,1,JLEV)=1.0_JPRD-2.0_JPRD*SLHDEPSV*ZD4/ZD1
    YDVSLETA%VSLDW(1,2,JLEV)=2.0_JPRD*SLHDEPSV*ZD4/ZD5
    YDVSLETA%VSLDW(1,3,JLEV)=0.0_JPRB
    YDVSLETA%VSLDW(2,1,JLEV)=2.0_JPRD*SLHDEPSV*ZD4/ZD2
    YDVSLETA%VSLDW(2,2,JLEV)=1.0_JPRD-2.0_JPRD*SLHDEPSV*ZD4/ZD6
    YDVSLETA%VSLDW(2,3,JLEV)=0.0_JPRB
    YDVSLETA%VSLDW(3,1,JLEV)=0.0_JPRB
    YDVSLETA%VSLDW(3,2,JLEV)=2.0_JPRD*SLHDEPSV*(ZD4*ZD4)/(ZD5*ZD6)
    YDVSLETA%VSLDW(3,3,JLEV)=1.0_JPRB
  ELSE
    ! Should not be used (i.e. NaN would be more appropriate)
    ! If used neutrality is guaranted by setting [1,1]=[2,2]=[3,3]=1.
    YDVSLETA%VSLDW(1,1,JLEV)=1.0_JPRB
    YDVSLETA%VSLDW(1,2,JLEV)=0.0_JPRB
    YDVSLETA%VSLDW(1,3,JLEV)=0.0_JPRB
    YDVSLETA%VSLDW(2,1,JLEV)=0.0_JPRB
    YDVSLETA%VSLDW(2,2,JLEV)=1.0_JPRB
    YDVSLETA%VSLDW(2,3,JLEV)=0.0_JPRB
    YDVSLETA%VSLDW(3,1,JLEV)=0.0_JPRB
    YDVSLETA%VSLDW(3,2,JLEV)=0.0_JPRB
    YDVSLETA%VSLDW(3,3,JLEV)=1.0_JPRB
  ENDIF

  IF (LSLVF) THEN
    YDVSLETA%VSLVF(1,1,JLEV)=1.0_JPRD-ZSLVFEPS_MASK(JLEV)*(ZP_LEV_H(JLEV-1)+ZP_LEV_H(JLEV))*ZD4/ZD1
    YDVSLETA%VSLVF(1,2,JLEV)=ZSLVFEPS_MASK(JLEV)*(ZP_LEV_H(JLEV-1)+ZP_LEV_H(JLEV))*ZD4/ZD5
    YDVSLETA%VSLVF(1,3,JLEV)=0.0_JPRB
    YDVSLETA%VSLVF(2,1,JLEV)=ZSLVFEPS_MASK(JLEV)*(ZP_LEV_H(JLEV-1)+ZP_LEV_H(JLEV))*ZD4/ZD2
    YDVSLETA%VSLVF(2,2,JLEV)=1.0_JPRD-ZSLVFEPS_MASK(JLEV)*(ZP_LEV_H(JLEV-1)+ZP_LEV_H(JLEV))*ZD4/ZD6
    YDVSLETA%VSLVF(2,3,JLEV)=0.0_JPRB
    YDVSLETA%VSLVF(3,1,JLEV)=0.0_JPRB
    YDVSLETA%VSLVF(3,2,JLEV)=ZSLVFEPS_MASK(JLEV)*(ZP_LEV_H(JLEV-1)+ZP_LEV_H(JLEV))*(ZD4*ZD4)/(ZD5*ZD6)
    YDVSLETA%VSLVF(3,3,JLEV)=1.0_JPRB
  ELSE
    ! Should not be used
    YDVSLETA%VSLVF(1,1,JLEV)=1.0_JPRB
    YDVSLETA%VSLVF(1,2,JLEV)=0.0_JPRB
    YDVSLETA%VSLVF(1,3,JLEV)=0.0_JPRB
    YDVSLETA%VSLVF(2,1,JLEV)=0.0_JPRB
    YDVSLETA%VSLVF(2,2,JLEV)=1.0_JPRB
    YDVSLETA%VSLVF(2,3,JLEV)=0.0_JPRB
    YDVSLETA%VSLVF(3,1,JLEV)=0.0_JPRB
    YDVSLETA%VSLVF(3,2,JLEV)=0.0_JPRB
    YDVSLETA%VSLVF(3,3,JLEV)=1.0_JPRB
  ENDIF

ENDDO

IF (LDVERBOSE) THEN
  WRITE(NULOUT,*) ''
  WRITE(NULOUT,*) ' * VCUICO at full levels:'
  WRITE(NULOUT,*) ''
  WRITE(UNIT=NULOUT,FMT='(1X,''JLEV'',1X,5X,''     VCUICO(1,JLEV) '')')
  DO JLEV=1,NFLEVG-3
    WRITE(UNIT=NULOUT,FMT='(1X,I3,2X,(5X,F20.10))')JLEV,YDVSLETA%VCUICO(1,JLEV)
  ENDDO
  WRITE(NULOUT,*) ''
  WRITE(UNIT=NULOUT,FMT='(1X,''JLEV'',1X,5X,''     VCUICO(2,JLEV) '')')
  DO JLEV=1,NFLEVG-3
    WRITE(UNIT=NULOUT,FMT='(1X,I3,2X,(5X,F20.10))')JLEV,YDVSLETA%VCUICO(2,JLEV)
  ENDDO
  WRITE(NULOUT,*) ''
  WRITE(UNIT=NULOUT,FMT='(1X,''JLEV'',1X,5X,''     VCUICO(3,JLEV) '')')
  DO JLEV=1,NFLEVG-3
    WRITE(UNIT=NULOUT,FMT='(1X,I3,2X,(5X,F20.10))')JLEV,YDVSLETA%VCUICO(3,JLEV)
  ENDDO
  WRITE(NULOUT,*) ''
  WRITE(UNIT=NULOUT,FMT='(1X,''JLEV'',1X,5X,''     VCUICO(4,JLEV) '')')
  DO JLEV=1,NFLEVG-3
    WRITE(UNIT=NULOUT,FMT='(1X,I3,2X,(5X,F20.10))')JLEV,YDVSLETA%VCUICO(4,JLEV)
  ENDDO
  WRITE(NULOUT,*) ''
  WRITE(NULOUT,*) ' * VSLD at full levels:'
  WRITE(NULOUT,*) ''
  WRITE(UNIT=NULOUT,FMT='(1X,''JLEV'',1X,5X,''     VSLD(1,JLEV) '')')
  DO JLEV=1,NFLEVG-3
    WRITE(UNIT=NULOUT,FMT='(1X,I3,2X,(5X,F20.10))')JLEV,YDVSLETA%VSLD(1,JLEV)
  ENDDO
  WRITE(NULOUT,*) ''
  WRITE(UNIT=NULOUT,FMT='(1X,''JLEV'',1X,5X,''     VSLD(2,JLEV) '')')
  DO JLEV=1,NFLEVG-3
    WRITE(UNIT=NULOUT,FMT='(1X,I3,2X,(5X,F20.10))')JLEV,YDVSLETA%VSLD(2,JLEV)
  ENDDO
  WRITE(NULOUT,*) ''
  WRITE(UNIT=NULOUT,FMT='(1X,''JLEV'',1X,5X,''     VSLD(3,JLEV) '')')
  DO JLEV=1,NFLEVG-3
    WRITE(UNIT=NULOUT,FMT='(1X,I3,2X,(5X,F20.10))')JLEV,YDVSLETA%VSLD(3,JLEV)
  ENDDO
  WRITE(NULOUT,*) ''
  WRITE(NULOUT,*) ' * VSLDW at full levels:'
  WRITE(NULOUT,*) ''
  WRITE(UNIT=NULOUT,FMT='(1X,''JLEV'',1X,5X,''    VSLDW(1,:,JLEV) '')')
  DO JLEV=1,NFLEVG-3
    WRITE(UNIT=NULOUT,FMT='(1X,I3,2X,3(5X,F20.10))')JLEV,YDVSLETA%VSLDW(1,:,JLEV)
  ENDDO
  WRITE(NULOUT,*) ''
  WRITE(UNIT=NULOUT,FMT='(1X,''JLEV'',1X,5X,''    VSLDW(2,:,JLEV) '')')
  DO JLEV=1,NFLEVG-3
    WRITE(UNIT=NULOUT,FMT='(1X,I3,2X,3(5X,F20.10))')JLEV,YDVSLETA%VSLDW(2,:,JLEV)
  ENDDO
  WRITE(NULOUT,*) ''
  WRITE(UNIT=NULOUT,FMT='(1X,''JLEV'',1X,5X,''    VSLDW(3,:,JLEV) '')')
  DO JLEV=1,NFLEVG-3
    WRITE(UNIT=NULOUT,FMT='(1X,I3,2X,3(5X,F20.10))')JLEV,YDVSLETA%VSLDW(3,:,JLEV)
  ENDDO
  WRITE(NULOUT,*) ''
  WRITE(NULOUT,*) ' * VSLVF at full levels:'
  WRITE(NULOUT,*) ''
  WRITE(UNIT=NULOUT,FMT='(1X,''JLEV'',1X,5X,''    VSLVF(1,:,JLEV) '')')
  DO JLEV=1,NFLEVG-3
    WRITE(UNIT=NULOUT,FMT='(1X,I3,2X,3(5X,F20.10))')JLEV,YDVSLETA%VSLVF(1,:,JLEV)
  ENDDO
  WRITE(NULOUT,*) ''
  WRITE(UNIT=NULOUT,FMT='(1X,''JLEV'',1X,5X,''    VSLVF(2,:,JLEV) '')')
  DO JLEV=1,NFLEVG-3
    WRITE(UNIT=NULOUT,FMT='(1X,I3,2X,3(5X,F20.10))')JLEV,YDVSLETA%VSLVF(2,:,JLEV)
  ENDDO
  WRITE(NULOUT,*) ''
  WRITE(UNIT=NULOUT,FMT='(1X,''JLEV'',1X,5X,''    VSLVF(3,:,JLEV) '')')
  DO JLEV=1,NFLEVG-3
    WRITE(UNIT=NULOUT,FMT='(1X,I3,2X,3(5X,F20.10))')JLEV,YDVSLETA%VSLVF(3,:,JLEV)
  ENDDO
ENDIF

! * attributes VCUICOH, VSLDH, VSLDWH, VSLVFH:
ALLOCATE(YDVSLETA%VCUICOH(4,0:NFLEVG-1))
ALLOCATE(YDVSLETA%VSLDH(3,0:NFLEVG-1))
ALLOCATE(YDVSLETA%VSLDWH(3,3,0:NFLEVG-1))
ALLOCATE(YDVSLETA%VSLVFH(3,3,0:NFLEVG-1))

IINCR=-1
DO JLEV=1,NFLEVG-3
  ZD1=YDVETA%VETAH(JLEV  +IINCR)-YDVETA%VETAH(JLEV+1+IINCR)
  ZD2=YDVETA%VETAH(JLEV  +IINCR)-YDVETA%VETAH(JLEV+2+IINCR)
  ZD3=YDVETA%VETAH(JLEV  +IINCR)-YDVETA%VETAH(JLEV+3+IINCR)
  ZD4=YDVETA%VETAH(JLEV+1+IINCR)-YDVETA%VETAH(JLEV+2+IINCR)
  ZD5=YDVETA%VETAH(JLEV+1+IINCR)-YDVETA%VETAH(JLEV+3+IINCR)
  ZD6=YDVETA%VETAH(JLEV+2+IINCR)-YDVETA%VETAH(JLEV+3+IINCR)

  YDVSLETA%VCUICOH(1,JLEV)= 1.0_JPRD/(ZD1*ZD2*ZD3)
  YDVSLETA%VCUICOH(2,JLEV)=-1.0_JPRD/(ZD1*ZD4*ZD5)
  YDVSLETA%VCUICOH(3,JLEV)= 1.0_JPRD/(ZD2*ZD4*ZD6)
  YDVSLETA%VCUICOH(4,JLEV)=-1.0_JPRD/(ZD3*ZD5*ZD6)

  IF (LSLHDQUAD) THEN
    YDVSLETA%VSLDH(1,JLEV)=(ZD2-ZD6)*ZD4/(ZD1*ZD2+ZD5*ZD6)
    YDVSLETA%VSLDH(2,JLEV)=(ZD5-ZD1)*ZD4/(ZD1*ZD2+ZD5*ZD6)
    YDVSLETA%VSLDH(3,JLEV)=    -ZD4 *ZD4/(ZD1*ZD2+ZD5*ZD6)
  ELSE
    YDVSLETA%VSLDH(1,JLEV)=0._JPRB
    YDVSLETA%VSLDH(2,JLEV)=0._JPRB
    YDVSLETA%VSLDH(3,JLEV)=0._JPRB
  ENDIF

  IF (LSLHD) THEN
    YDVSLETA%VSLDWH(1,1,JLEV)=1.0_JPRD-2.0_JPRD*SLHDEPSV*ZD4/ZD1
    YDVSLETA%VSLDWH(1,2,JLEV)=2.0_JPRD*SLHDEPSV*ZD4/ZD5
    YDVSLETA%VSLDWH(1,3,JLEV)=0.0_JPRB
    YDVSLETA%VSLDWH(2,1,JLEV)=2.0_JPRD*SLHDEPSV*ZD4/ZD2
    YDVSLETA%VSLDWH(2,2,JLEV)=1.0_JPRD-2.0_JPRD*SLHDEPSV*ZD4/ZD6
    YDVSLETA%VSLDWH(2,3,JLEV)=0.0_JPRB
    YDVSLETA%VSLDWH(3,1,JLEV)=0.0_JPRB
    YDVSLETA%VSLDWH(3,2,JLEV)=2.0_JPRD*SLHDEPSV*(ZD4*ZD4)/(ZD5*ZD6)
    YDVSLETA%VSLDWH(3,3,JLEV)=1.0_JPRB
  ELSE
    YDVSLETA%VSLDWH(1,1,JLEV)=1._JPRB
    YDVSLETA%VSLDWH(1,2,JLEV)=0._JPRB
    YDVSLETA%VSLDWH(1,3,JLEV)=0._JPRB
    YDVSLETA%VSLDWH(2,1,JLEV)=0._JPRB
    YDVSLETA%VSLDWH(2,2,JLEV)=1._JPRB
    YDVSLETA%VSLDWH(2,3,JLEV)=0._JPRB
    YDVSLETA%VSLDWH(3,1,JLEV)=0._JPRB
    YDVSLETA%VSLDWH(3,2,JLEV)=0._JPRB
    YDVSLETA%VSLDWH(3,3,JLEV)=1._JPRB
  ENDIF

  IF (LSLVF) THEN
    YDVSLETA%VSLVFH(1,1,JLEV)=1.0_JPRD-2.0_JPRD*ZSLVFEPS_MASK(JLEV)*ZP_LEV_H(JLEV)*ZD4/ZD1
    YDVSLETA%VSLVFH(1,2,JLEV)=2.0_JPRD*ZSLVFEPS_MASK(JLEV)*ZP_LEV_H(JLEV)*ZD4/ZD5
    YDVSLETA%VSLVFH(1,3,JLEV)=0.0_JPRB
    YDVSLETA%VSLVFH(2,1,JLEV)=2.0_JPRD*ZSLVFEPS_MASK(JLEV)*ZP_LEV_H(JLEV)*ZD4/ZD2
    YDVSLETA%VSLVFH(2,2,JLEV)=1.0_JPRD-2.0_JPRD*ZSLVFEPS_MASK(JLEV)*ZP_LEV_H(JLEV)*ZD4/ZD6
    YDVSLETA%VSLVFH(2,3,JLEV)=0.0_JPRB
    YDVSLETA%VSLVFH(3,1,JLEV)=0.0_JPRB
    YDVSLETA%VSLVFH(3,2,JLEV)=2.0_JPRD*ZSLVFEPS_MASK(JLEV)*ZP_LEV_H(JLEV)*(ZD4*ZD4)/(ZD5*ZD6)
    YDVSLETA%VSLVFH(3,3,JLEV)=1.0_JPRB
  ELSE
    YDVSLETA%VSLVFH(1,1,JLEV)=1._JPRB
    YDVSLETA%VSLVFH(1,2,JLEV)=0._JPRB
    YDVSLETA%VSLVFH(1,3,JLEV)=0._JPRB
    YDVSLETA%VSLVFH(2,1,JLEV)=0._JPRB
    YDVSLETA%VSLVFH(2,2,JLEV)=1._JPRB
    YDVSLETA%VSLVFH(2,3,JLEV)=0._JPRB
    YDVSLETA%VSLVFH(3,1,JLEV)=0._JPRB
    YDVSLETA%VSLVFH(3,2,JLEV)=0._JPRB
    YDVSLETA%VSLVFH(3,3,JLEV)=1._JPRB
  ENDIF
ENDDO

IF (LDVERBOSE) THEN
  WRITE(NULOUT,*) ''
  WRITE(NULOUT,*) ' * VCUICOH at half levels:'
  WRITE(NULOUT,*) ''
  WRITE(UNIT=NULOUT,FMT='(1X,''JLEV'',1X,5X,''     VCUICOH(1,JLEV)'')')
  DO JLEV=1,NFLEVG-3
