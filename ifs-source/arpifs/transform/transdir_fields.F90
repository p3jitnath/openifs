SUBROUTINE TRANSDIR_FIELDS(YDGEOMETRY,YDSPEC,YDGRID,KFIDS)
USE GEOMETRY_MOD , ONLY : GEOMETRY
USE PARKIND1 , ONLY : JPRB,JPIM
USE YOMHOOK  , ONLY : LHOOK, DR_HOOK, JPHOOK
USE FIELD_DEFINITIONS
USE FIELD_CONTAINER_GP_MOD, ONLY : FIELD_CONTAINER_GP
USE FIELD_CONTAINER_SP_MOD, ONLY : FIELD_CONTAINER_SP,TYPE_ITERATOR
IMPLICIT NONE
TYPE(GEOMETRY)    ,INTENT(IN)    :: YDGEOMETRY
TYPE(FIELD_CONTAINER_SP),INTENT(INOUT) :: YDSPEC
TYPE(FIELD_CONTAINER_GP),INTENT(INOUT) :: YDGRID
INTEGER(KIND=JPIM),INTENT(IN) :: KFIDS(:)

REAL(KIND=JPRB),POINTER :: ZREEL(:,:,:)
INTEGER(KIND=JPIM) :: INFIDS,ID,JBLK,IVSETSC(1)
TYPE(FIELD_ACCESS) :: YLSPUV,YLGRID
TYPE(TYPE_ITERATOR) :: YLIT
REAL(KIND=JPRB), POINTER :: ZSPEC_2D(:,:)
REAL(KIND=JPRB), POINTER :: ZSPEC_1D(:)
REAL(KIND=JPRB), POINTER :: ZGRID_2D(:,:)
REAL(KIND=JPRB), POINTER :: ZGRID_1D(:)
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

LOGICAL :: LLWINDS
#include "dir_trans.h"
!-------------------------------------------------------------------------------
IF (LHOOK) CALL DR_HOOK('TRANSDIR_FIELDS',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM=>YDGEOMETRY%YRDIM,YDDIMV=>YDGEOMETRY%YRDIMV,YDGEM=>YDGEOMETRY%YRGEM, YDMP=>YDGEOMETRY%YRMP)
ASSOCIATE(NPROMA=>YDDIM%NPROMA, NRESOL=>YDDIM%NRESOL, NSPEC2=>YDDIM%NSPEC2, &
 & NFLSUR=>YDDIMV%NFLSUR,NGPBLKS=>YDDIM%NGPBLKS,NFLEVG=>YDDIMV%NFLEVG, &
 & NBSETLEV=>YDMP%NBSETLEV,NBSETSP=>YDMP%NBSETSP)

IVSETSC(1) = NBSETSP
INFIDS = SIZE(KFIDS)
LLWINDS = ANY(FID%VOR == KFIDS) .AND. ANY(FID%DIV == KFIDS)
IF (LLWINDS) THEN
  ALLOCATE(ZREEL(NPROMA,2*NFLEVG,NGPBLKS))
  DO JBLK=1,NGPBLKS
    CALL YDGRID%FIELD_OPEN_FAC(KBLOCK=JBLK,KFIDS=(/FID%U,FID%V/),FAC=YLGRID)
    ZREEL(:,1:NFLEVG,JBLK) = YLGRID%U(:,:)
    ZREEL(:,NFLEVG+1:2*NFLEVG,JBLK) = YLGRID%V(:,:)
    CALL YDGRID%FIELD_CLOSE_FAC(YLGRID)
  ENDDO
  CALL YDSPEC%FIELD_OPEN_FAC(KFIDS=(/FID%VOR,FID%DIV/),FAC=YLSPUV)
  CALL DIR_TRANS(PSPVOR=YLSPUV%VOR,PSPDIV=YLSPUV%DIV,PGP=ZREEL, &
  & KVSETUV=NBSETLEV, KRESOL=NRESOL, KPROMA=NPROMA)
  CALL YDSPEC%FIELD_CLOSE_FAC(FAC=YLSPUV)
  DEALLOCATE(ZREEL)
ENDIF

DO WHILE(YDSPEC%FIELD_ITERATED(ITERATOR=YLIT,FIELD_1D=ZSPEC_1D,FIELD_2D=ZSPEC_2D,KOTID=ID) == 1)
  IF (ID == FID%VOR .OR. ID == FID%DIV) CYCLE
  IF(ASSOCIATED(ZSPEC_2D)) THEN
    ALLOCATE(ZREEL(NPROMA,NFLEVG,NGPBLKS))
    DO JBLK=1,NGPBLKS
      CALL YDGRID%FIELD_OPEN_ONE(KFID=ID,KBLOCK=JBLK,PFIELD=ZGRID_2D)
      ZREEL(:,:,JBLK) = ZGRID_2D(:,:)
      CALL YDGRID%FIELD_CLOSE_ONE(KFID=ID,KBLOCK=JBLK,PFIELD=ZGRID_2D)
    ENDDO
    CALL DIR_TRANS(PSPSCALAR=ZSPEC_2D,PGP=ZREEL,&
  & KVSETSC=NBSETLEV, KRESOL=NRESOL, KPROMA=NPROMA)
    DEALLOCATE(ZREEL)
  ELSEIF(ASSOCIATED(ZSPEC_1D)) THEN
    ALLOCATE(ZSPEC_2D(1,SIZE(ZSPEC_1D)))
    ALLOCATE(ZREEL(NPROMA,1,NGPBLKS))
    DO JBLK=1,NGPBLKS
      CALL YDGRID%FIELD_OPEN_ONE(KFID=ID,KBLOCK=JBLK,PFIELD=ZGRID_1D)
      ZREEL(:,1,JBLK) = ZGRID_1D(:)
      CALL YDGRID%FIELD_CLOSE_ONE(KFID=ID,KBLOCK=JBLK,PFIELD=ZGRID_1D)
    ENDDO
    CALL DIR_TRANS(PSPSCALAR=ZSPEC_2D,PGP=ZREEL,&
  & KVSETSC=IVSETSC, KRESOL=NRESOL, KPROMA=NPROMA)
    ZSPEC_1D(:) = ZSPEC_2D(1,:)
    DEALLOCATE(ZSPEC_2D)
    DEALLOCATE(ZREEL)
  ENDIF
ENDDO
END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('TRANSDIR_FIELDS',1,ZHOOK_HANDLE)

END SUBROUTINE TRANSDIR_FIELDS
