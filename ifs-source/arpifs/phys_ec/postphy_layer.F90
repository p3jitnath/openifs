    PSURF%PSD_VD(JROF,YSD_VD%YTCCHEM(JCHEM)%MP)=ZCHEM(JROF,JCHEM)
  ENDDO
ENDDO

! The rest of PAERODDF will already have been filled in by AER_PHY3, but we
! haven't calculated the total column mass until now.
DO JAERO=1,NACTAERO
  DO JROF=DIMS%KIDIA,DIMS%KFDIA
    FLUX%PAERODDF(JROF,JAERO,JPAERODIAG_MSS)=ZAEROTC(JROF,JAERO)
  ENDDO
ENDDO

DO JDIAG=1,NPAERODIAG
  DO JAERO=1,NACTAERO
    DO JROF=DIMS%KIDIA,DIMS%KFDIA
      PSURF%PSD_VD(JROF,YSD_VD%YAERODIAG(JAERO,JDIAG)%MP)=FLUX%PAERODDF(JROF,JAERO,JDIAG)
    ENDDO
  ENDDO
ENDDO



