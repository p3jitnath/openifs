SUBROUTINE STATE_INCREMENT(KDIM,POSTATE, PA_U,PA_V,PA_T,PA_O3,PA_Q,PA_A,PA_QL,PA_QI,PA_QR,PA_QS,&
                          &PA_TKE)
! Increment state by tendencies of a single process
  !     POSTATE  - state variable to be incremented
  !     PA_xx    - field to add 
USE PARKIND1 , ONLY : JPIM, JPRB
USE YOMHOOK  , ONLY : LHOOK, DR_HOOK, JPHOOK
USE YOMPHYDER  , ONLY :  STATE_TYPE, MASK_GFL_TYPE, DIMENSION_TYPE
USE YOECLDP  , ONLY : NCLDQL, NCLDQR, NCLDQI, NCLDQS

IMPLICIT NONE

TYPE (DIMENSION_TYPE) ,INTENT(IN)  :: KDIM 
TYPE (STATE_TYPE),INTENT(INOUT) :: POSTATE
REAL(KIND=JPRB),OPTIONAL,TARGET,INTENT(IN) :: PA_U(KDIM%KLON,KDIM%KLEV)
REAL(KIND=JPRB),OPTIONAL,TARGET,INTENT(IN) :: PA_V(KDIM%KLON,KDIM%KLEV)
REAL(KIND=JPRB),OPTIONAL,TARGET,INTENT(IN) :: PA_T(KDIM%KLON,KDIM%KLEV)
REAL(KIND=JPRB),OPTIONAL,TARGET,INTENT(IN) :: PA_O3(KDIM%KLON,KDIM%KLEV)
REAL(KIND=JPRB),OPTIONAL,TARGET,INTENT(IN) :: PA_Q(KDIM%KLON,KDIM%KLEV)
REAL(KIND=JPRB),OPTIONAL,TARGET,INTENT(IN) :: PA_A(KDIM%KLON,KDIM%KLEV)
REAL(KIND=JPRB),OPTIONAL,TARGET,INTENT(IN) :: PA_QL(KDIM%KLON,KDIM%KLEV)
REAL(KIND=JPRB),OPTIONAL,TARGET,INTENT(IN) :: PA_QI(KDIM%KLON,KDIM%KLEV)
REAL(KIND=JPRB),OPTIONAL,TARGET,INTENT(IN) :: PA_QR(KDIM%KLON,KDIM%KLEV)
REAL(KIND=JPRB),OPTIONAL,TARGET,INTENT(IN) :: PA_QS(KDIM%KLON,KDIM%KLEV)
REAL(KIND=JPRB),OPTIONAL,TARGET,INTENT(IN) :: PA_TKE(KDIM%KLON,KDIM%KLEV)

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('STATE_INCREMENT',0,ZHOOK_HANDLE)
!DIR$ IVDEP
!DIR$ SAFE_ADDRESS
!DIR$ PREFERVECTOR
IF (PRESENT(PA_U)) POSTATE%U(KDIM%KIDIA:KDIM%KFDIA,:)= &
  & POSTATE%U(KDIM%KIDIA:KDIM%KFDIA,:) + PA_U(KDIM%KIDIA:KDIM%KFDIA,:)
IF (PRESENT(PA_V)) POSTATE%V(KDIM%KIDIA:KDIM%KFDIA,:)= &
  & POSTATE%V(KDIM%KIDIA:KDIM%KFDIA,:) + PA_V(KDIM%KIDIA:KDIM%KFDIA,:)
!DIR$ IVDEP
!DIR$ SAFE_ADDRESS
!DIR$ PREFERVECTOR
IF (PRESENT(PA_T)) POSTATE%T(KDIM%KIDIA:KDIM%KFDIA,:)= &
  & POSTATE%T(KDIM%KIDIA:KDIM%KFDIA,:) + PA_T(KDIM%KIDIA:KDIM%KFDIA,:)
!DIR$ IVDEP
!DIR$ SAFE_ADDRESS
!DIR$ PREFERVECTOR
IF (PRESENT(PA_Q).AND.POSTATE%KEYMASK%Q) POSTATE%Q(KDIM%KIDIA:KDIM%KFDIA,:)= &
  & POSTATE%Q(KDIM%KIDIA:KDIM%KFDIA,:) + PA_Q(KDIM%KIDIA:KDIM%KFDIA,:)
!DIR$ IVDEP
!DIR$ SAFE_ADDRESS
!DIR$ PREFERVECTOR
IF (PRESENT(PA_O3).AND.POSTATE%KEYMASK%O3) POSTATE%O3(KDIM%KIDIA:KDIM%KFDIA,:)= &
  & POSTATE%O3(KDIM%KIDIA:KDIM%KFDIA,:) + PA_O3(KDIM%KIDIA:KDIM%KFDIA,:)
!DIR$ IVDEP
!DIR$ SAFE_ADDRESS
!DIR$ PREFERVECTOR
IF (PRESENT(PA_A).AND.POSTATE%KEYMASK%A) POSTATE%A(KDIM%KIDIA:KDIM%KFDIA,:)= &
  & POSTATE%A(KDIM%KIDIA:KDIM%KFDIA,:) + PA_A(KDIM%KIDIA:KDIM%KFDIA,:)
!DIR$ IVDEP
!DIR$ SAFE_ADDRESS
!DIR$ PREFERVECTOR
IF (PRESENT(PA_TKE).AND.POSTATE%KEYMASK%TKE) POSTATE%TKE(KDIM%KIDIA:KDIM%KFDIA,:)= &
  & POSTATE%TKE(KDIM%KIDIA:KDIM%KFDIA,:) + PA_TKE(KDIM%KIDIA:KDIM%KFDIA,:)
!DIR$ IVDEP
!DIR$ SAFE_ADDRESS
!DIR$ PREFERVECTOR
IF (PRESENT(PA_QL).AND.POSTATE%KEYMASK%QL) POSTATE%CLD(KDIM%KIDIA:KDIM%KFDIA,:,NCLDQL)= &
  & POSTATE%CLD(KDIM%KIDIA:KDIM%KFDIA,:,NCLDQL) + PA_QL(KDIM%KIDIA:KDIM%KFDIA,:)
!DIR$ IVDEP
!DIR$ SAFE_ADDRESS
!DIR$ PREFERVECTOR
IF (PRESENT(PA_QI).AND.POSTATE%KEYMASK%QI) POSTATE%CLD(KDIM%KIDIA:KDIM%KFDIA,:,NCLDQI)= &
  & POSTATE%CLD(KDIM%KIDIA:KDIM%KFDIA,:,NCLDQI) + PA_QI(KDIM%KIDIA:KDIM%KFDIA,:)
!DIR$ IVDEP
!DIR$ SAFE_ADDRESS
!DIR$ PREFERVECTOR
IF (PRESENT(PA_QR).AND.POSTATE%KEYMASK%QR) POSTATE%CLD(KDIM%KIDIA:KDIM%KFDIA,:,NCLDQR)= &
  & POSTATE%CLD(KDIM%KIDIA:KDIM%KFDIA,:,NCLDQR) + PA_QR(KDIM%KIDIA:KDIM%KFDIA,:)
IF (PRESENT(PA_QS).AND.POSTATE%KEYMASK%QS) POSTATE%CLD(KDIM%KIDIA:KDIM%KFDIA,:,NCLDQS)= &
  & POSTATE%CLD(KDIM%KIDIA:KDIM%KFDIA,:,NCLDQS) + PA_QS(KDIM%KIDIA:KDIM%KFDIA,:)

IF (LHOOK) CALL DR_HOOK('STATE_INCREMENT',1,ZHOOK_HANDLE)
END SUBROUTINE STATE_INCREMENT

