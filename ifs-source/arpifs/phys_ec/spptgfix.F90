SUBROUTINE SPPTGFIX(YDMODEL,YDGEOMETRY,YDGMV,YGFL,PDT,PGMVS,PDPTEN,PGFL,PGFLT1,PGMVT1,PRLXFACT)
!
!     Purpose.   Correct SPPT tendencies to guarantee that global average of tendencies does not change
!     --------   
!
!*   Interface.
!     ----------
!
!        *CALL* *SPPTGFIX (PGMVS, PDPTEN, PGFLT1)
!
!     Explicit arguments :
!        --------------------
!
!     INPUT:
!     -------------
!        PGMVS      : surface GMV variables 
!        PDPTEN     : tendency change due to SPPT (before and after),
!                     computed in SPPTEN
!        PGFLT1     : GFL tendencies
!        PGMVT1     : GMV tendencies
!
!     OUTPUT:
!     -------
!        PGFLT1      : GFL tendencies
!        PGMVT1      : GMV tendencies
!
!     Implicit arguments :  None.
!     --------------------
!
!     Author:
!     -------
!     June 2015 - Jost von Hardenberg
!     Dec  2015 - S. Lang - clean up and openmp
!     Jun  2020 - S. Lang - mass weighting
!     May  2022 - S. Lang - prevent neg. humidity
  
USE TYPE_MODEL  , ONLY : MODEL
USE GEOMETRY_MOD, ONLY : GEOMETRY
USE YOMGMV      , ONLY : TGMV
USE PARKIND1    , ONLY : JPIM, JPRB
USE YOMHOOK     , ONLY : LHOOK, DR_HOOK, JPHOOK
USE YOM_YGFL    , ONLY : TYPE_GFLD

IMPLICIT NONE

! arguments
TYPE(GEOMETRY)    , INTENT(IN)    :: YDGEOMETRY
TYPE(MODEL)       , INTENT(IN)    :: YDMODEL
TYPE(TGMV)        , INTENT(INOUT) :: YDGMV
TYPE(TYPE_GFLD)   , INTENT(INOUT) :: YGFL
REAL(KIND=JPRB)   , INTENT(IN)    :: PDT
REAL(KIND=JPRB)   , INTENT(IN)    :: PGMVS(YDGEOMETRY%YRDIM%NPROMA,YDGMV%NDIMGMVS,YDGEOMETRY%YRDIM%NGPBLKS) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PDPTEN(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG,4,2,YDGEOMETRY%YRDIM%NGPBLKS)
REAL(KIND=JPRB)   , INTENT(IN)    :: PGFL(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG,YGFL%NDIM,YDGEOMETRY%YRDIM%NGPBLKS)
REAL(KIND=JPRB)   , INTENT(INOUT) :: PGFLT1(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG,YGFL%NDIM1,YDGEOMETRY%YRDIM%NGPBLKS)
REAL(KIND=JPRB)   , INTENT(INOUT) :: PGMVT1(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG,YDGMV%YT1%NDIM,&
                                   & YDGEOMETRY%YRDIM%NGPBLKS)
REAL(KIND=JPRB)   , INTENT(IN)    :: PRLXFACT

! local variables
INTEGER(KIND=JPIM) :: IBL,ICEND,JKGLO,JROF
INTEGER(KIND=JPIM) :: IST,JLEV,JFLD

REAL(KIND=JPRB) :: ZFIELD3DT0(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG,YDGEOMETRY%YRDIM%NGPBLKS)
REAL(KIND=JPRB) :: ZFIELD3DT1(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG,YDGEOMETRY%YRDIM%NGPBLKS)
REAL(KIND=JPRB) :: ZFIELD3DT2(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG,YDGEOMETRY%YRDIM%NGPBLKS)
REAL(KIND=JPRB) :: ZFIELD2DT0(YDGEOMETRY%YRDIM%NPROMA,0:YDGEOMETRY%YRDIMV%NFLEVG,YDGEOMETRY%YRDIM%NGPBLKS)
REAL(KIND=JPRB) :: ZFIELD2DT1(YDGEOMETRY%YRDIM%NPROMA,0:YDGEOMETRY%YRDIMV%NFLEVG,YDGEOMETRY%YRDIM%NGPBLKS)
REAL(KIND=JPRB) :: ZFIELD2DT2(YDGEOMETRY%YRDIM%NPROMA,0:YDGEOMETRY%YRDIMV%NFLEVG,YDGEOMETRY%YRDIM%NGPBLKS)
REAL(KIND=JPRB) :: ZFIELD1D(YDGEOMETRY%YRDIM%NPROMA,12,YDGEOMETRY%YRDIM%NGPBLKS)
REAL(KIND=JPRB) :: ZPRES9(YDGEOMETRY%YRDIM%NPROMA,0:YDGEOMETRY%YRDIMV%NFLEVG,YDGEOMETRY%YRDIM%NGPBLKS)
REAL(KIND=JPRB) :: ZWEIGHT, ZWEIVOL, ZNORMS(3,12), ZSMALL, ZQ1, ZQ2

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

#include "gpnorm1.intfb.h"
#include "gppwc.intfb.h"
#include "gphpre.intfb.h"

IF (LHOOK) CALL DR_HOOK('SPPTGFIX',0,ZHOOK_HANDLE)

ASSOCIATE(YDDIM=>YDGEOMETRY%YRDIM, YDDIMV=>YDGEOMETRY%YRDIMV, YDGEM=>YDGEOMETRY%YRGEM, &
 & YDVAB=>YDGEOMETRY%YRVAB,YDCVER=>YDGEOMETRY%YRCVER)
ASSOCIATE( YQ=>YGFL%YQ, YPH9=>YDGMV%YPH9, NDIMGMVS=>YDGMV%NDIMGMVS, YT1=>YDGMV%YT1,  &
   & NPROMA=>YDDIM%NPROMA, NGPBLKS=>YDDIM%NGPBLKS, NFLEVG=>YDDIMV%NFLEVG, &
   & NGPTOT=>YDGEM%NGPTOT )

ZSMALL= 0.00001_JPRB*EPSILON(1._JPRB)

! calculate pressure 
IST=1
!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(JKGLO,ICEND,IBL,JROF)
DO JKGLO=1,NGPTOT,NPROMA
  ICEND=MIN(NPROMA,NGPTOT-JKGLO+1)
  IBL=(JKGLO-1)/NPROMA+1
  DO JROF=IST,ICEND
    ZPRES9(JROF,NFLEVG,IBL) = EXP(PGMVS(JROF,YPH9%MSP,IBL))
  ENDDO
  CALL GPHPRE(NPROMA,NFLEVG,IST,ICEND,YDVAB,YDCVER,ZPRES9(:,:,IBL))
ENDDO
!$OMP END PARALLEL DO


! Calculate total average mass of Q, energy and momentum tendencies before and after advection
! NB: we use GPPWC also to compute the mass-weighted vertical integrals of T,U and V tendencies 
! e.g. int(C_p T(p) dp)/g. We can neglect the constants C_p and g.

!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(JKGLO,ICEND,IBL,JFLD,JLEV,JROF)
DO JKGLO=1,NGPTOT,NPROMA
  ICEND=MIN(NPROMA,NGPTOT-JKGLO+1)
  IBL=(JKGLO-1)/NPROMA+1
  DO JFLD=1,4
    DO JLEV=1,NFLEVG
      DO JROF=IST,ICEND
        ZFIELD3DT0(JROF,JLEV,IBL)=PDPTEN(JROF,JLEV,JFLD,1,IBL)
        ZFIELD3DT1(JROF,JLEV,IBL)=PDPTEN(JROF,JLEV,JFLD,2,IBL)
        ZFIELD3DT2(JROF,JLEV,IBL)=ABS(PDPTEN(JROF,JLEV,JFLD,2,IBL)-PDPTEN(JROF,JLEV,JFLD,1,IBL)) * &
             & (YDGEOMETRY%YRGSGEOM(IBL)%GAW(JROF) * &
             & (ZPRES9(JROF,JLEV,IBL)-ZPRES9(JROF,JLEV-1,IBL)))
      ENDDO
    ENDDO
    CALL GPPWC(NPROMA,IST,ICEND,NFLEVG,ZFIELD2DT0(:,:,IBL),ZFIELD3DT0(:,:,IBL),ZPRES9(:,:,IBL))
    CALL GPPWC(NPROMA,IST,ICEND,NFLEVG,ZFIELD2DT1(:,:,IBL),ZFIELD3DT1(:,:,IBL),ZPRES9(:,:,IBL))
    CALL GPPWC(NPROMA,IST,ICEND,NFLEVG,ZFIELD2DT2(:,:,IBL),ZFIELD3DT2(:,:,IBL),ZPRES9(:,:,IBL))
    DO JROF=IST,ICEND
      ZFIELD1D(JROF,1+(JFLD-1)*3,IBL)=ZFIELD2DT0(JROF,NFLEVG,IBL)
      ZFIELD1D(JROF,2+(JFLD-1)*3,IBL)=ZFIELD2DT1(JROF,NFLEVG,IBL)
      ZFIELD1D(JROF,3+(JFLD-1)*3,IBL)=ZFIELD2DT2(JROF,NFLEVG,IBL)
    ENDDO
  ENDDO
ENDDO
!$OMP END PARALLEL DO

! norms are broadcast if PNORMS=ZNORMS specified
CALL GPNORM1(YDGEOMETRY,ZFIELD1D,12,.FALSE.,PNORMS=ZNORMS)
    
! Q,T,U,V tendency after SPPT minus tendency before SPPT 
! p=>p +<p0>*w -<p1>*w 
! w=abs(p0-p1)/<abs(p0-p1)> 
! This way we distribute the excess/defect based on the amplitude of the local change in p due to SPPT
    
! Calculate corrected tendencies PGFLT1 and PGMVT1

!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(JKGLO,ICEND,IBL,JLEV,JROF,ZWEIGHT,ZWEIVOL,ZQ1,ZQ2)
DO JKGLO=1,NGPTOT,NPROMA
  ICEND=MIN(NPROMA,NGPTOT-JKGLO+1)
  IBL=(JKGLO-1)/NPROMA+1
  DO JLEV=1,NFLEVG
    DO JROF=IST,ICEND

      ZWEIVOL = PRLXFACT * (YDGEOMETRY%YRGSGEOM(IBL)%GAW(JROF) * (ZPRES9(JROF,JLEV,IBL)-ZPRES9(JROF,JLEV-1,IBL)))

      IF (ABS(ZNORMS(1,3)) > ZSMALL) THEN
        ZWEIGHT=ABS(PDPTEN(JROF,JLEV,1,2,IBL)-PDPTEN(JROF,JLEV,1,1,IBL))*ZWEIVOL/ZNORMS(1,3)
        ZQ1 = PGFLT1(JROF,JLEV,YQ%MP1,IBL) + ZWEIGHT*ZNORMS(1,1) - ZWEIGHT*ZNORMS(1,2) 
        ZQ2 = PGFL(JROF,JLEV,YQ%MP9_PH,IBL) + PDT * ZQ1
        IF (ZQ2>YDMODEL%YRML_PHY_EC%YRECND%REPQMI) THEN
          PGFLT1(JROF,JLEV,YQ%MP1,IBL)=ZQ1
        ELSE
          PGFLT1(JROF,JLEV,YQ%MP1,IBL)=MIN((YDMODEL%YRML_PHY_EC%YRECND%REPQMI - PGFL(JROF,JLEV,YQ%MP9_PH,IBL))/PDT, &
               & PGFLT1(JROF,JLEV,YQ%MP1,IBL))
        ENDIF
      ENDIF
      !
      IF (ABS(ZNORMS(1,6)) > ZSMALL) THEN
        ZWEIGHT=ABS(PDPTEN(JROF,JLEV,2,2,IBL)-PDPTEN(JROF,JLEV,2,1,IBL))*ZWEIVOL/ZNORMS(1,6)
        PGMVT1(JROF,JLEV,YT1%MT,IBL)=PGMVT1(JROF,JLEV,YT1%MT,IBL) + ZWEIGHT*ZNORMS(1,4) - ZWEIGHT*ZNORMS(1,5) 
      ENDIF
      !
      IF (ABS(ZNORMS(1,9)) > ZSMALL) THEN
        ZWEIGHT=ABS(PDPTEN(JROF,JLEV,3,2,IBL)-PDPTEN(JROF,JLEV,3,1,IBL))*ZWEIVOL/ZNORMS(1,9)
        PGMVT1(JROF,JLEV,YT1%MU,IBL)=PGMVT1(JROF,JLEV,YT1%MU,IBL) + ZWEIGHT*ZNORMS(1,7) - ZWEIGHT*ZNORMS(1,8) 
      ENDIF
      !
      IF (ABS(ZNORMS(1,12)) > ZSMALL) THEN
        ZWEIGHT=ABS(PDPTEN(JROF,JLEV,4,2,IBL)-PDPTEN(JROF,JLEV,4,1,IBL))*ZWEIVOL/ZNORMS(1,12)
        PGMVT1(JROF,JLEV,YT1%MV,IBL)=PGMVT1(JROF,JLEV,YT1%MV,IBL) + ZWEIGHT*ZNORMS(1,10) - ZWEIGHT*ZNORMS(1,11) 
      ENDIF
    ENDDO
  ENDDO   
ENDDO
!$OMP END PARALLEL DO

END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('SPPTGFIX',1,ZHOOK_HANDLE)
END SUBROUTINE SPPTGFIX
