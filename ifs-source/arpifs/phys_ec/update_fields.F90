SUBROUTINE UPDATE_FIELDS (YDPHY2,KONFIG, KIDIA, KFDIA, KLON, KLEV, &
! optional input
                      & PI1, PI2, PI3, PI4, PI5, PI6, PI7, PI8, PI9, PI10, &
                      & PI11,PI12,PI13, &
                      & PS1, PS2, PS3, PS4, PS5, PS6, PS7, PS8, PS9, PS10, &
                      & PS11,PS12,PS13, &
                      & PA1, PA2, PA3, PA4, PA5, PA6, PA7, PA8, PA9, PA10, &
                      & PA11,PA12,PA13, &
                      & LDV1, LDV2, LDV3, LDV4, LDV5, LDV6, LDV7, LDV8, LDV9, LDV10, &
                      & LDV11,LDV12,LDV13, &
                      & PTA1, PTA2, PTA3, PTA4, PTA5, PTA6, PTA7, PTA8, PTA9, PTA10, &
                      & PTA11,PTA12,PTA13, &
                      & PTS1, PTS2, PTS3, PTS4, PTS5, PTS6, PTS7, PTS8, PTS9, PTS10, &
                      & PTS11,PTS12,PTS13, &
! output
                      & PO1, PO2, PO3, PO4, PO5, PO6, PO7, PO8, PO9, PO10, &
                      & PO11,PO12,PO13)

!===============================================================================
!**** *UPDATE_FIELDS* -  Routine for transfer or update of fields used in physics
! 
!     PIxx  - input field
!     PSxx  - field to subtract from input field
!     PAxx  - field to add to input field
!     PTAxx - positive tendency to update input field (if KONFIG=1)
!     PTSxx - negative tendency to update input field (if KONFIG=1)
!     LDVxx - key activating the computation for variable xx
!     POxx  - output field
!
!
!     F. Vana (ECMWF)
!
!     MODIFICATIONS.
!     -------------
!     Original 18-May-2012
!     F. Vana  25-Jul-2012  optimized and secured for NaN initialization arrays
!     F. Vana  14-Sep-2012  better handling of incrementing arrays
!
! ---------------------------------------------------------------------------------

USE PARKIND1 , ONLY : JPIM     ,JPRB
USE YOMHOOK  , ONLY : LHOOK,   DR_HOOK, JPHOOK

USE YOMPHY2  , ONLY : TPHY2

! ---------------------------------------------------------------------------------

IMPLICIT NONE

TYPE(TPHY2)       ,INTENT(INOUT) :: YDPHY2
INTEGER(KIND=JPIM),INTENT(IN)    :: KONFIG
INTEGER(KIND=JPIM),INTENT(IN)    :: KIDIA
