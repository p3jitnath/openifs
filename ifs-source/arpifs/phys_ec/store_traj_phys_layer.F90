SUBROUTINE STORE_TRAJ_PHYS_LAYER(&
  & YDGMV,YDSURF, YDEPHLI,YDDPHY,YDML_PHY_SLIN,YGFL,YDRIP,KDIM, &
  & U, V, T, SP, Q, A, L, I, PGFL, PAUX, PGFLT1, PRAD, PSURF, PTRAJ_PHYS)

!**** *STORE_TRAJ_PHYS_LAYER* - Layer routine calling phys trajectory storage

!     PURPOSE.
!     --------

!**   INTERFACE.
!     ----------

!        EXPLICIT ARGUMENTS :
!        --------------------
!     ==== INPUTS ===
! KDIM     : Derived variable for dimensions
! U,V,T,SP,Q,A,L,I  : Prognostic variables derived type
! PAUX     : D.V. for additional variables like vertical velocity
! PGFL     : Prognostic GFL variables for current timestep
! PGFLT1   : Tendecy for GFL variables
! PRAD     : D. V. for radiative quantities
! PSURF    : D.V. for surface quantities

!     ==== OUTPUT ===
! PTRAJ_PHYS : Trajectory from NL model

!        --------------------

!     METHOD.
!     -------
!        SEE DOCUMENTATION

!     EXTERNALS.
!     ----------

!     REFERENCE.
!     ----------
!        ECMWF RESEARCH DEPARTMENT DOCUMENTATION OF THE IFS

!     AUTHOR.
!     -------
!      Original : 2012-02-07  F. VANA (c) ECMWF

!     MODIFICATIONS.
!     --------------
!     F. Vana  28-Nov-2013 : Redesigned trajectory handling
!     E. Dutra/G.Arduini Jan 2018: PSP_SG has 4 Dimensions, only 1st level is used, snow multi-layer 
!     F. Vana  14-Sep-2020 : Prognostic cloud

!-----------------------------------------------------------------------

USE SURFACE_FIELDS_MIX , ONLY : TSURF
USE VARIABLE_MODULE, ONLY : VARIABLE_2D, VARIABLE_3D
USE YOMGMV   , ONLY : TGMV
USE PARKIND1 , ONLY : JPIM, JPRB
USE YOMHOOK  , ONLY : LHOOK, DR_HOOK, JPHOOK
USE YOMPHYDER, ONLY : DIMENSION_TYPE, AUX_TYPE, AUX_RAD_TYPE, SURF_AND_MORE_TYPE
USE YOMTRAJ  , ONLY : TRAJ_PHYS_TYPE
USE YOM_YGFL , ONLY : TYPE_GFLD
USE YOEPHLI  , ONLY : TEPHLI
USE YOMDPHY  , ONLY : TDPHY
USE MODEL_PHYSICS_SIMPLINEAR_MOD , ONLY : MODEL_PHYSICS_SIMPLINEAR_TYPE
USE YOMRIP   , ONLY : TRIP
USE YOMLUN   , ONLY : NULOUT

!-----------------------------------------------------------------------

IMPLICIT NONE

TYPE(TGMV)     ,INTENT(INOUT) :: YDGMV
TYPE(TSURF)    ,INTENT(INOUT) :: YDSURF
TYPE(TDPHY)    ,INTENT(INOUT) :: YDDPHY
TYPE(TEPHLI)   ,INTENT(INOUT) :: YDEPHLI
TYPE(TYPE_GFLD),INTENT(INOUT) :: YGFL
TYPE(TRIP)     ,INTENT(INOUT) :: YDRIP
TYPE(MODEL_PHYSICS_SIMPLINEAR_TYPE),INTENT(IN):: YDML_PHY_SLIN
TYPE(DIMENSION_TYPE), INTENT(IN) :: KDIM
TYPE(VARIABLE_3D) ,INTENT(INOUT) :: U
TYPE(VARIABLE_3D) ,INTENT(INOUT) :: V
TYPE(VARIABLE_3D) ,INTENT(INOUT) :: T
TYPE(VARIABLE_2D) ,INTENT(INOUT) :: SP
TYPE(VARIABLE_3D) ,INTENT(INOUT) :: Q
TYPE(VARIABLE_3D) ,INTENT(INOUT) :: A  ! not used so far
TYPE(VARIABLE_3D) ,INTENT(INOUT) :: L
TYPE(VARIABLE_3D) ,INTENT(INOUT) :: I
REAL(KIND=JPRB)   ,INTENT(IN)    :: PGFL(KDIM%KLON,KDIM%KLEV,YGFL%NDIM)
TYPE (AUX_TYPE)   ,INTENT(IN)    :: PAUX
REAL(KIND=JPRB)   ,INTENT(IN)    :: PGFLT1(KDIM%KLON,KDIM%KLEV,YGFL%NDIM1) 
TYPE (AUX_RAD_TYPE)       , INTENT (IN)   :: PRAD
TYPE (SURF_AND_MORE_TYPE) , INTENT (IN)   :: PSURF
TYPE (TRAJ_PHYS_TYPE)     , INTENT (INOUT):: PTRAJ_PHYS
!-----------------------------------------------------------------------
INTEGER(KIND=JPIM) :: JGFL
REAL(KIND=JPRB)    :: ZSSN(KDIM%KLON)

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!     ------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('STORE_TRAJ_PHYS_LAYER',0,ZHOOK_HANDLE)
ASSOCIATE(NAERO=>YGFL%NAERO, NCHEM=>YGFL%NCHEM, NDIM=>YGFL%NDIM, &
 & NDIM1=>YGFL%NDIM1, NGHG=>YGFL%NGHG, YAERO=>YGFL%YAERO, YCHEM=>YGFL%YCHEM, &
 & YGHG=>YGFL%YGHG, YLRCH4=>YGFL%YLRCH4, YQ=>YGFL%YQ, &
 & NTILES=>YDDPHY%NTILES, TSTEP=>YDRIP%TSTEP, &
 & LPHYLIN=>YDEPHLI%LPHYLIN,  &
 & YT1=>YDGMV%YT1, LEPCLD2=>YDML_PHY_SLIN%YRPHNC%LEPCLD2, &
 & YSD_VD=>YDSURF%YSD_VD, YSD_VN=>YDSURF%YSD_VN, YSP_RR=>YDSURF%YSP_RR, &
 & YSP_SB=>YDSURF%YSP_SB, YSP_SBD=>YDSURF%YSP_SBD, YSP_SGD=>YDSURF%YSP_SGD,&
 & YSP_SG=>YDSURF%YSP_SG)
 

!     ------------------------------------------------------------------

! Note that this routine is no longer a layer routine. 


!       1.      UPPER AIR 3D FIELDS
PTRAJ_PHYS%PTT95(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)= T%PH9(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)
PTRAJ_PHYS%PQT95(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)= Q%PH9(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)
PTRAJ_PHYS%PUT95(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)= U%PH9(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)
PTRAJ_PHYS%PVT95(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)= V%PH9(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)
PTRAJ_PHYS%PTET5(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)= T%T1(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)
PTRAJ_PHYS%PTEQ5(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)= Q%T1(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)
PTRAJ_PHYS%PTEU5(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)= U%T1(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)
PTRAJ_PHYS%PTEV5(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)= V%T1(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)
PTRAJ_PHYS%PVVEL5(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)= PAUX%PVERVEL(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)
IF(LEPCLD2) THEN
  PTRAJ_PHYS%PLT95(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)= L%PH9(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV) 
  PTRAJ_PHYS%PIT95(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)= I%PH9(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)
  PTRAJ_PHYS%PTEL5(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)= L%T1(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)
  PTRAJ_PHYS%PTEI5(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)= I%T1(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)
ENDIF
IF (.NOT.LPHYLIN) THEN ! sort of temporal fix
  PTRAJ_PHYS%PEMTED5(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV+1)=PRAD%PEMTD(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV+1)
  PTRAJ_PHYS%PTRSOL5(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV+1)=PRAD%PTRSW(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV+1)
ENDIF
DO JGFL=1,NGHG
  PTRAJ_PHYS%PGFLT9_GHG5(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV,JGFL)= &
   &   PGFL(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV,YGHG(JGFL)%MP9_PH)
  PTRAJ_PHYS%PTEGFL_GHG5(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV,JGFL)= &
   &   PGFLT1(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV,YGHG(JGFL)%MP1)
ENDDO
IF(NGHG > 0 .AND. YLRCH4%LGP) THEN
  PTRAJ_PHYS%PGFLT9_LRCH45(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV)= &
   &   PGFL(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV,YLRCH4%MP9_PH)
ENDIF
DO JGFL=1,NAERO
  PTRAJ_PHYS%PGFLT9_AERO5(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV,JGFL)= &
   &   PGFL(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV,YAERO(JGFL)%MP9_PH)
  PTRAJ_PHYS%PTEGFL_AERO5(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV,JGFL)= &
   &   PGFLT1(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV,YAERO(JGFL)%MP1)
ENDDO
DO JGFL=1,NCHEM
  PTRAJ_PHYS%PGFLT9_CHEM5(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV,JGFL)= &
   &   PGFL(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV,YCHEM(JGFL)%MP9_PH)
  PTRAJ_PHYS%PTEGFL_CHEM5(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV,JGFL)= &
   &   PGFLT1(KDIM%KIDIA:KDIM%KFDIA,1:KDIM%KLEV,YCHEM(JGFL)%MP1)
ENDDO

!       2.      SOIL 3D FIELDS
PTRAJ_PHYS%PTSA95(KDIM%KIDIA:KDIM%KFDIA,1:YSP_SBD%NLEVS)= &
  & PSURF%PSP_SB(KDIM%KIDIA:KDIM%KFDIA,1:YSP_SBD%NLEVS,YSP_SB%YT%MP9)
PTRAJ_PHYS%PWSA95(KDIM%KIDIA:KDIM%KFDIA,1:YSP_SBD%NLEVS)= &
  & PSURF%PSP_SB(KDIM%KIDIA:KDIM%KFDIA,1:YSP_SBD%NLEVS,YSP_SB%YQ%MP9)
PTRAJ_PHYS%PTIA95(KDIM%KIDIA:KDIM%KFDIA,1:YSP_SBD%NLEVS)= &
  & PSURF%PSP_SB(KDIM%KIDIA:KDIM%KFDIA,1:YSP_SBD%NLEVS,YSP_SB%YTL%MP9)
! Snow 
PTRAJ_PHYS%PSNS95(KDIM%KIDIA:KDIM%KFDIA,1:YSP_SGD%NLEVS)= MAX(0._JPRB, &
  & PSURF%PSP_SG(KDIM%KIDIA:KDIM%KFDIA,1:YSP_SGD%NLEVS,YSP_SG%YF%MP9))
PTRAJ_PHYS%PRSN95(KDIM%KIDIA:KDIM%KFDIA,1:YSP_SGD%NLEVS)= & 
  & PSURF%PSP_SG(KDIM%KIDIA:KDIM%KFDIA,1:YSP_SGD%NLEVS,YSP_SG%YR%MP9) 

!       3.     SURFACE 2D FIELDS
PTRAJ_PHYS%PSPT95(KDIM%KIDIA:KDIM%KFDIA)=SP%PH9(KDIM%KIDIA:KDIM%KFDIA)
PTRAJ_PHYS%PSPT15(KDIM%KIDIA:KDIM%KFDIA)=SP%T1(KDIM%KIDIA:KDIM%KFDIA)

!PTRAJ_PHYS%PSNS95(KDIM%KIDIA:KDIM%KFDIA)=ZSSN(KDIM%KIDIA:KDIM%KFDIA)
PTRAJ_PHYS%PWL95 (KDIM%KIDIA:KDIM%KFDIA)=PSURF%PSP_RR(KDIM%KIDIA:KDIM%KFDIA,YSP_RR%YW%MP9)
PTRAJ_PHYS%PTL95 (KDIM%KIDIA:KDIM%KFDIA)=PSURF%PSP_RR(KDIM%KIDIA:KDIM%KFDIA,YSP_RR%YT%MP9)
PTRAJ_PHYS%PNTOP5(KDIM%KIDIA:KDIM%KFDIA)=PSURF%PSD_VN(KDIM%KIDIA:KDIM%KFDIA,YSD_VN%YTOP%MP)
PTRAJ_PHYS%PNBAS5(KDIM%KIDIA:KDIM%KFDIA)=PSURF%PSD_VN(KDIM%KIDIA:KDIM%KFDIA,YSD_VN%YBAS%MP)
PTRAJ_PHYS%PACPR5(KDIM%KIDIA:KDIM%KFDIA)=PSURF%PSD_VN(KDIM%KIDIA:KDIM%KFDIA,YSD_VN%YACPR%MP)
PTRAJ_PHYS%PACCPR5(KDIM%KIDIA:KDIM%KFDIA)=PSURF%PSD_VN(KDIM%KIDIA:KDIM%KFDIA,YSD_VN%YACCPR%MP)
PTRAJ_PHYS%PGZ0M5(KDIM%KIDIA:KDIM%KFDIA)=PSURF%PSD_VD(KDIM%KIDIA:KDIM%KFDIA,YSD_VD%YZ0F%MP)
PTRAJ_PHYS%PLZ0H5(KDIM%KIDIA:KDIM%KFDIA)=PSURF%PSD_VD(KDIM%KIDIA:KDIM%KFDIA,YSD_VD%YLZ0H%MP)

!       4.      SOIL 2D FIELDS (TILES)
PTRAJ_PHYS%PUSTRTI5(KDIM%KIDIA:KDIM%KFDIA,1:NTILES)= PSURF%PUSTRTI(KDIM%KIDIA:KDIM%KFDIA,1:NTILES)
PTRAJ_PHYS%PVSTRTI5(KDIM%KIDIA:KDIM%KFDIA,1:NTILES)= PSURF%PVSTRTI(KDIM%KIDIA:KDIM%KFDIA,1:NTILES)
PTRAJ_PHYS%PAHFSTI5(KDIM%KIDIA:KDIM%KFDIA,1:NTILES)= PSURF%PAHFSTI(KDIM%KIDIA:KDIM%KFDIA,1:NTILES)
PTRAJ_PHYS%PEVAPTI5(KDIM%KIDIA:KDIM%KFDIA,1:NTILES)= PSURF%PEVAPTI(KDIM%KIDIA:KDIM%KFDIA,1:NTILES)
PTRAJ_PHYS%PTSKTI5 (KDIM%KIDIA:KDIM%KFDIA,1:NTILES)= PSURF%PTSKTI (KDIM%KIDIA:KDIM%KFDIA,1:NTILES)

!     ------------------------------------------------------------------
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('STORE_TRAJ_PHYS_LAYER',1,ZHOOK_HANDLE)
END SUBROUTINE STORE_TRAJ_PHYS_LAYER
