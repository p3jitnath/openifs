  SUBROUTINE UPDATE_ARR_2D(KDIM1,KDIM2,ARR_IN, ARR_OUT, KM1, KM2,PTIMI)
  IMPLICIT NONE
  REAL(KIND=JPRB), DIMENSION(KDIM1,KDIM2,12), INTENT(IN)  :: ARR_IN
  REAL(KIND=JPRB), DIMENSION(KDIM1,KDIM2),    INTENT(OUT) :: ARR_OUT
  INTEGER(KIND=JPIM), INTENT(IN) :: KDIM1, KDIM2, KM1, KM2
  REAL(KIND=JPRB),    INTENT(IN) :: PTIMI

  INTEGER :: JI, JL

  !$OMP DO
  DO JI=1,KDIM2
    DO JL=1,KDIM1
      ARR_OUT(JL,JI)=ARR_IN(JL,JI,KM2)&
       & +PTIMI*(ARR_IN(JL,JI,KM1)-ARR_IN(JL,JI,KM2))       
    END DO
  END DO
  !$OMP ENDDO

  END SUBROUTINE UPDATE_ARR_2D 

  SUBROUTINE UPDATE_ARR_3D(KDIM1,KDIM2,KDIM3,ARR_IN, ARR_OUT, KM1, KM2,PTIMI)
  IMPLICIT NONE
  REAL(KIND=JPRB), DIMENSION(KDIM1,KDIM2,KDIM3, 12), INTENT(IN)  :: ARR_IN
  REAL(KIND=JPRB), DIMENSION(KDIM1,KDIM2,KDIM3),    INTENT(OUT) :: ARR_OUT
  INTEGER(KIND=JPIM), INTENT(IN) :: KDIM1,KDIM2,KDIM3,KM1, KM2
  REAL(KIND=JPRB),    INTENT(IN) :: PTIMI

  INTEGER :: JI, JL, JK

!DIR$ ASSUME_ALIGNED ARR_IN: 64
!DIR$ ASSUME_ALIGNED ARR_OUT: 64

   !$OMP DO COLLAPSE(2) SCHEDULE(STATIC)
   DO JK=1,KDIM3
      DO JI=1,KDIM2
        !$omp simd 
        DO JL=1,KDIM1
          ARR_OUT(JL,JI,JK)=ARR_IN(JL,JI,JK,KM2)&
           & +PTIMI*(ARR_IN(JL,JI,JK,KM1)-ARR_IN(JL,JI,JK,KM2))
        END DO
      END DO
   END DO
   !$OMP ENDDO

   END SUBROUTINE UPDATE_ARR_3D 

END SUBROUTINE UPDECAEC
