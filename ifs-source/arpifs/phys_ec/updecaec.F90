  DO JL=1,23
    IF (ZLAT == YDEAERC_TEGEN%RLATV(JL)) THEN
      ZRES=ZW1*YDEAERC_TEGEN%RAERV(JL,IYR1,IMV1)+ZW2*YDEAERC_TEGEN%RAERV(JL,IYR2,IMV2)
    ELSEIF (ZLAT < YDEAERC_TEGEN%RLATV(JL).AND.ZLAT >= YDEAERC_TEGEN%RLATV(JL+1))THEN
      ZINT=(ZLAT-YDEAERC_TEGEN%RLATV(JL+1))/(YDEAERC_TEGEN%RLATV(JL)-YDEAERC_TEGEN%RLATV(JL+1))
      ZRESM=ZW1*YDEAERC_TEGEN%RAERV(JL  ,IYR1,IMV1)+ZW2*YDEAERC_TEGEN%RAERV(JL  ,IYR2,IMV2)
      ZRESP=ZW1*YDEAERC_TEGEN%RAERV(JL+1,IYR1,IMV1)+ZW2*YDEAERC_TEGEN%RAERV(JL+1,IYR2,IMV2)
      ZRES=ZRESP-ZINT*(ZRESP-ZRESM)
    ENDIF
  ENDDO
  YDEAERC_TEGEN%RTAEVO(JI)=ZRES
ENDDO

