SUBROUTINE FPCHRESOL(YDFPDATA,YDFIELDS_IN,YDFIELDS_OUT)

!**** *FPCHRESOL*  - Fullpos Change Resolution

!     Purpose.
!     --------
!           To change the resolution of a FIELDS object containing plain fields

!**   Interface.
!     ----------
!        *CALL* *FPCHRESOL

!        Explicit arguments :
!        --------------------
!          YDFPDATA     : post-processor
!          YDFIELDS_IN  : input FIELDS object
!          YDFIELDS_OUT : ouput FIELDS object

!        Implicit arguments :
!        --------------------
!        None

!     Method.
!     -------
!        Each elementary array of contiguous data in a FIELDS object object is transformed.
!        Then a direct spectral transform is performed to complete the FIELDS with the spectral fields
!        Then an inverse spectral transform is performed to complete the FIELDS with the gridpoint derivatives.

!     Externals.
!     ----------

!     Reference.
!     ----------
!        ECMWF Research Department documentation of the IFS

!     Author.
!     -------
!      R. El Khatib *METEO-FRANCE* 15-Jun-2018

!     Modifications.
!     ------------------------------------------------------------------

USE GEOMETRY_MOD  , ONLY : GEOMETRY
USE TYPE_MODEL    , ONLY : MODEL
USE PARKIND1      , ONLY : JPIM,    JPRB, JPRD
USE YOMHOOK       , ONLY : LHOOK,   DR_HOOK, JPHOOK
USE FIELDS_MOD    , ONLY : FIELDS
USE FULLPOS       , ONLY : TFPDATA
USE YOMFP4L       , ONLY : TRQFP
USE YOM4FPOS      , ONLY : TRQFPDYN, ALLOCATE_TRQ3FP, ALLOCATE_TRQ2FP
USE YOMLUN        , ONLY : NULOUT
USE YOMFPC        , ONLY : LALLOFP
USE PARFPOS       , ONLY : JPOSDYN
USE TYPE_FPRQPHYS , ONLY : ALLOCATE_FPRQPHY, DEALLOCATE_FPRQPHY
USE YOMCT0        , ONLY : LECMWF
USE YOMMP0        , ONLY : NPROC, MYSETV
USE MPL_MODULE    , ONLY : MPL_BARRIER, MPL_BROADCAST

IMPLICIT NONE

TYPE(TFPDATA)     ,INTENT(IN)    :: YDFPDATA
TYPE(FIELDS)      ,INTENT(IN)    :: YDFIELDS_IN
TYPE(FIELDS)      ,INTENT(INOUT) :: YDFIELDS_OUT

CHARACTER(LEN=9) :: CLCONF
LOGICAL :: LLTIMER, LLDATER, LLRQCFU, LLRQXFU, LLFPOS

INTEGER(KIND=JPIM) :: ILOGSTEP, IFPDOM, JF, JLEV, IFIELDS, J, IERR, IOFF, IST, IBL, ICEND, JKGLO
INTEGER(KIND=JPIM) :: IVSETSC(1)
INTEGER(KIND=JPIM),ALLOCATABLE:: IFPCOD(:) ! Fullpos internal fields codes

TYPE(TRQFP) :: YLRQPHY
TYPE(TRQFPDYN) :: YLRQDYN, YLRQGMV, YLRQGMVS, YLRQDIAGSURF

REAL(KIND=JPRB), ALLOCATABLE :: Z3DFIELDS(:,:,:,:), Z2DFIELDS(:,:,:)
REAL(KIND=JPRB), ALLOCATABLE :: ZSP(:,:)
REAL(KIND=JPRB) :: ZWSXI, ZWDXI
REAL(KIND=JPRB) :: ZAVEG(1)
REAL(KIND=JPRB) :: ZMCUFGP(YDFIELDS_IN%GEOM%YRDIM%NPROMA,YDFIELDS_IN%YMCUF%NCUFNR,YDFIELDS_IN%GEOM%YRDIM%NGPBLKS)
REAL(KIND=JPRD) :: ZT1, ZT2 ! jprd because of user_clock
REAL(KIND=JPRB) :: ZLOGTIME
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

#include "user_clock.intfb.h"

#include "dir_trans.h"
#include "specnorm.h"

#include "maxgpfv.intfb.h"
#include "varfpos.intfb.h"
#include "logdis.intfb.h"
#include "gpnorm3.intfb.h"
#include "gpnorm2.intfb.h"
#include "spnorm.intfb.h"
#include "suorog.intfb.h"
#include "transinvh.intfb.h"
#include "specrt.intfb.h"
#include "ctvtot.intfb.h"

#include "abor1.intfb.h"

!      -----------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('FPCHRESOL',0,ZHOOK_HANDLE)
ASSOCIATE(YDML_GCONF=>YDFIELDS_OUT%STATE_MODEL%YRML_GCONF,YDGEOM=>YDFIELDS_OUT%GEOM,&
 & YDDYNA=>YDFIELDS_OUT%STATE_MODEL%YRML_DYN%YRDYNA)
ASSOCIATE(YDAFN=>YDFPDATA%YFPOS%YAFN, YDFPGEOMETRY=>YDFPDATA%YFPOS%YFPGEOMETRY, YDSURF=>YDFIELDS_OUT%YRSURF, &
 & YDMP=>YDGEOM%YRMP, YDDIM=>YDGEOM%YRDIM, YDDIMV=>YDGEOM%YRDIMV, YDSPEC=>YDFIELDS_OUT%YRSPEC,&
 & YDGFL=>YDML_GCONF%YGFL, YT1=>YDFIELDS_OUT%YRGMV%YT1)
ASSOCIATE(YDFPUSERGEO=>YDFPGEOMETRY%YFPUSERGEO, TFP_DYNDS=>YDAFN%TFP_DYNDS, GFP_PHYDS=>YDAFN%GFP_PHYDS, &
 & NUMFLDS1=>YDGFL%NUMFLDS1, NUMSPFLDS1=>YDGFL%NUMSPFLDS1, YDCOMP=>YDGFL%YCOMP,&
 & NS3D=>YDML_GCONF%YRDIMF%NS3D, NS2D=>YDML_GCONF%YRDIMF%NS2D)


!*       0.    MATCH THE FIELDS OBJECTS WITH THE POST-PROCESSOR
!              ------------------------------------------------

IF (YDFPGEOMETRY%NMDLRESOL /= YDFIELDS_IN%GEOM%YRDIM%NRESOL) THEN
  WRITE(NULOUT,'(''INPUT RESOLUTIONS MISMATCH : FIELDS_IN = '',I2,'' POST-PROCESSOR = '',I2)') &
   & YDFIELDS_IN%GEOM%YRDIM%NRESOL,YDFPGEOMETRY%NMDLRESOL 
  CALL ABOR1('FPCHRESOL : INPUT RESOLUTIONS MISMATCH')
ENDIF
IF (YDFPUSERGEO(1)%NFPRESOL /= YDFIELDS_OUT%GEOM%YRDIM%NRESOL) THEN
  WRITE(NULOUT,'(''OUTPUT RESOLUTIONS MISMATCH : FIELDS_OUT = '',I2,'' POST-PROCESSOR = '',I2)') &
   & YDFIELDS_OUT%GEOM%YRDIM%NRESOL,YDFPUSERGEO(1)%NFPRESOL
  CALL ABOR1('FPCHRESOL : OUTPUT RESOLUTIONS MISMATCH')
ENDIF

!*       1.    MISCELLANEOUS SETUP
!              -------------------

! Global Statistics (start)
CALL GSTATS(1,0)

! Timing (start)
CALL USER_CLOCK(PTOTAL_CP=ZT1)


IFPDOM=SIZE(YDFPUSERGEO)


!*       2.    SURFACE OR PHYSICAL FIELDS
!              --------------------------

LLRQCFU=YDFIELDS_OUT%YRCFU%LCUMFU
LLRQXFU=YDFIELDS_OUT%YRXFU%LXFU

! PROVISIONAL COMPUTATION OF INPUT MAX. VALUES RELATED TO SOIL MOISTURE
IF (YDFIELDS_IN%STATE_MODEL%YRML_PHY_MF%YRPHY%LMPHYS) THEN
  CALL MAXGPFV(YDFIELDS_IN%GEOM,YDFIELDS_IN%YRSURF,ZWSXI,ZWDXI)
ENDIF

! pronostic groups:
CALL PPSURF3D(' SOILB  ',YDSURF%YSP_SBD%NLEVS,YDSURF%YSP_SBD%NUMFLDS,YDSURF%YSP_SB%YSB,YDSURF%SP_SB(:,:,1:YDSURF%YSP_SBD%NUMFLDS,:))
CALL PPSURF3D(' OML    ',YDSURF%YSP_OMD%NLEVS,YDSURF%YSP_OMD%NUMFLDS,YDSURF%YSP_OM%YOM,YDSURF%SP_OM(:,:,1:YDSURF%YSP_OMD%NUMFLDS,:))
CALL PPSURF3D(' EXTRP  ',YDSURF%YSP_EPD%NLEVS,YDSURF%YSP_EPD%NUMFLDS,YDSURF%YSP_EP%YEP,YDSURF%SP_EP(:,:,1:YDSURF%YSP_EPD%NUMFLDS,:))
CALL PPSURF3D(' SNOWG  ',YDSURF%YSP_SGD%NLEVS,YDSURF%YSP_SGD%NUMFLDS,YDSURF%YSP_SG%YSG,YDSURF%SP_SG(:,:,1:YDSURF%YSP_SGD%NUMFLDS,:))
CALL PPSURF2D(' LAKEG  ',YDSURF%YSP_SLD%NUMFLDS,YDSURF%YSP_SL%YSL,YDSURF%SP_SL(:,1:YDSURF%YSP_SLD%NUMFLDS,:))
CALL PPSURF2D(' RESVR  ',YDSURF%YSP_RRD%NUMFLDS,YDSURF%YSP_RR%YRR,YDSURF%SP_RR(:,1:YDSURF%YSP_RRD%NUMFLDS,:))
CALL PPSURF2D(' CLS    ',YDSURF%YSP_CLD%NUMFLDS,YDSURF%YSP_CL%YCL,YDSURF%SP_CL(:,1:YDSURF%YSP_CLD%NUMFLDS,:))
CALL PPSURF2D(' XTRP2  ',YDSURF%YSP_X2D%NUMFLDS,YDSURF%YSP_X2%YX2,YDSURF%SP_X2(:,1:YDSURF%YSP_X2D%NUMFLDS,:))
CALL PPSURF2D(' CANRI  ',YDSURF%YSP_CID%NUMFLDS,YDSURF%YSP_CI%YCI,YDSURF%SP_CI(:,1:YDSURF%YSP_CID%NUMFLDS,:))
! diagnostic groups 3D :
CALL PPDIAGSURF3D(' VDIAGO3',YDSURF%YSD_V3D%NLEVS,YDSURF%YSD_V3D%NUMFLDS,YDSURF%YSD_V3%YV3,YDSURF%SD_V3(:,:,1:YDSURF%YSD_V3D%NUMFLDS,:))
CALL PPDIAGSURF3D(' STASIM ',YDSURF%YSD_SMD%NLEVS,YDSURF%YSD_SMD%NUMFLDS,YDSURF%YSD_SM%YSM,YDSURF%SD_SM(:,:,1:YDSURF%YSD_SMD%NUMFLDS,:))
CALL PPDIAGSURF3D(' XA     ',YDSURF%YSD_XAD%NLEVS,YDSURF%YSD_XAD%NUMFLDS,YDSURF%YSD_XA%YXA,YDSURF%SD_XA(:,:,1:YDSURF%YSD_XAD%NUMFLDS,:))
CALL PPDIAGSURF3D(' XR     ',YDSURF%YSD_XRD%NLEVS,YDSURF%YSD_XRD%NUMFLDS,YDSURF%YSD_XR%YXA,YDSURF%SD_XR(:,:,1:YDSURF%YSD_XRD%NUMFLDS,:))
CALL PPDIAGSURF3D(' PF     ',YDSURF%YSD_PFD%NLEVS,YDSURF%YSD_PFD%NUMFLDS,YDSURF%YSD_PF%YXA,YDSURF%SD_PF(:,:,1:YDSURF%YSD_PFD%NUMFLDS,:))
CALL PPDIAGSURF3D(' DI     ',YDSURF%YSD_DID%NLEVS,YDSURF%YSD_DID%NUMFLDS,YDSURF%YSD_DI%YXA,YDSURF%SD_DI(:,:,1:YDSURF%YSD_DID%NUMFLDS,:))
! diagnostic groups 2D :
CALL PPSURF2D(' VARSF  ',YDSURF%YSD_VFD%NUMFLDS,YDSURF%YSD_VF%YVF,YDSURF%SD_VF(:,1:YDSURF%YSD_VFD%NUMFLDS,:))
CALL PPSURF2D(' VCLIP  ',YDSURF%YSD_VPD%NUMFLDS,YDSURF%YSD_VP%YVP,YDSURF%SD_VP(:,1:YDSURF%YSD_VPD%NUMFLDS,:))
CALL PPSURF2D(' VCLIV  ',YDSURF%YSD_VVD%NUMFLDS,YDSURF%YSD_VV%YVV,YDSURF%SD_VV(:,1:YDSURF%YSD_VVD%NUMFLDS,:))
CALL PPSURF2D(' VCLIN  ',YDSURF%YSD_VND%NUMFLDS,YDSURF%YSD_VN%YVN,YDSURF%SD_VN(:,1:YDSURF%YSD_VND%NUMFLDS,:))
CALL PPSURF2D(' VCLIH  ',YDSURF%YSD_VHD%NUMFLDS,YDSURF%YSD_VH%YVH,YDSURF%SD_VH(:,1:YDSURF%YSD_VHD%NUMFLDS,:))
CALL PPSURF2D(' VCLIK  ',YDSURF%YSD_VKD%NUMFLDS,YDSURF%YSD_VK%YVK,YDSURF%SD_VK(:,1:YDSURF%YSD_VKD%NUMFLDS,:))
CALL PPSURF2D(' VCLIA  ',YDSURF%YSD_VAD%NUMFLDS,YDSURF%YSD_VA%YVA,YDSURF%SD_VA(:,1:YDSURF%YSD_VAD%NUMFLDS,:))
CALL PPSURF2D(' VO3ABC ',YDSURF%YSD_VCD%NUMFLDS,YDSURF%YSD_VC%YVC,YDSURF%SD_VC(:,1:YDSURF%YSD_VCD%NUMFLDS,:))
CALL PPSURF2D(' VDIAGO2',YDSURF%YSD_V2D%NUMFLDS,YDSURF%YSD_V2%YV2,YDSURF%SD_V2(:,1:YDSURF%YSD_V2D%NUMFLDS,:))
CALL PPSURF2D(' VDIAG  ',YDSURF%YSD_VDD%NUMFLDS,YDSURF%YSD_VD%YVD,YDSURF%SD_VD(:,1:YDSURF%YSD_VDD%NUMFLDS,:))
CALL PPSURF2D(' WS     ',YDSURF%YSD_WSD%NUMFLDS,YDSURF%YSD_WS%YWS,YDSURF%SD_WS(:,1:YDSURF%YSD_WSD%NUMFLDS,:))
CALL PPSURF2D(' WW     ',YDSURF%YSD_WWD%NUMFLDS,YDSURF%YSD_WW%YWW,YDSURF%SD_WW(:,1:YDSURF%YSD_WWD%NUMFLDS,:))
CALL PPSURF2D(' VCLIX  ',YDSURF%YSD_VXD%NUMFLDS,YDSURF%YSD_VX%YVX,YDSURF%SD_VX(:,1:YDSURF%YSD_VXD%NUMFLDS,:))
CALL PPSURF2D(' VEXTR2 ',YDSURF%YSD_X2D%NUMFLDS,YDSURF%YSD_X2%YX2,YDSURF%SD_X2(:,1:YDSURF%YSD_X2D%NUMFLDS,:))
CALL PPSURF2D(' SFLUX  ',YDSURF%YSD_SFLD%NUMFLDS,YDSURF%YSD_SFL%YSFL,YDSURF%SD_SFL(:,1:YDSURF%YSD_SFLD%NUMFLDS,:))
CALL PPSURF2D(' SFORC  ',YDSURF%YSD_SFOD%NUMFLDS,YDSURF%YSD_SFO%YSFO,YDSURF%SD_SFO(:,1:YDSURF%YSD_SFOD%NUMFLDS,:))
CALL PPSURF2D(' OCEAN  ',YDSURF%YSD_OCD%NUMFLDS,YDSURF%YSD_OC%YOC,YDSURF%SD_OC(:,1:YDSURF%YSD_OCD%NUMFLDS,:))

!*       3.    DYNAMICAL FIELDS
!              ----------------

LLRQCFU=.FALSE.
LLRQXFU=.FALSE.

!*       3.3 GMVT1 AND GFL
!            -------------

IFIELDS=YT1%NDIM+NUMFLDS1
CALL ALLOCATE_TRQ3FP(IFIELDS,YDDIMV%NFLEVG,IFPDOM,LALLOFP,NULOUT,YLRQGMV%YES)
DO JLEV=1,YDDIMV%NFLEVG
  YLRQGMV%YES%ZPXLEV(JLEV)=REAL(JLEV,JPRB)
ENDDO
IERR=0
YLRQGMV%YES%ICOD(:)=0
DO JF=1,YT1%NDIM
  IF (JF==YT1%MU) THEN
    YLRQGMV%YES%ICOD(JF)=YDAFN%TFP%U%ICOD
  ELSEIF (JF==YT1%MV) THEN
    YLRQGMV%YES%ICOD(JF)=YDAFN%TFP%V%ICOD
  ELSEIF (JF==YT1%MT) THEN
    YLRQGMV%YES%ICOD(JF)=YDAFN%TFP%T%ICOD
  ELSEIF (JF==YT1%MSPD) THEN
    YLRQGMV%YES%ICOD(JF)=YDAFN%TFP%PD%ICOD
  ELSEIF (JF==YT1%MSVD) THEN
    YLRQGMV%YES%ICOD(JF)=YDAFN%TFP%VD%ICOD
  ENDIF
ENDDO
DO JF=1,YT1%NDIM
  IF (YLRQGMV%YES%ICOD(JF)==0) THEN
    WRITE(NULOUT,'('' UNIDENTIFIED REQUESTED GMV FIELD NR '',I8)') JF
    IERR=IERR+1
  ENDIF
ENDDO
! sugfl* and suafn use LECMWF, so use it here.
IF (LECMWF) THEN
  DO JF=1,NUMFLDS1
    DO J=1,JPOSDYN
      IF (YDCOMP(JF)%IGRBCODE == TFP_DYNDS(J)%IGRIB) THEN
        YLRQGMV%YES%ICOD(YT1%NDIM+JF)=J
        EXIT
      ENDIF
    ENDDO
    IF (J > JPOSDYN) THEN
      WRITE(NULOUT,'('' UNIDENTIFIED REQUESTED GFL FIELD '',I8)') YDCOMP(JF)%IGRBCODE
      IERR=IERR+1
    ENDIF
  ENDDO
ELSE
  DO JF=1,NUMFLDS1
    DO J=1,JPOSDYN
      IF (YDCOMP(JF)%CNAME == TFP_DYNDS(J)%CLNAME(1:12)) THEN
        YLRQGMV%YES%ICOD(YT1%NDIM+JF)=J
        EXIT
      ENDIF
    ENDDO
    IF (J > JPOSDYN) THEN
      WRITE(NULOUT,'('' UNIDENTIFIED REQUESTED GFL FIELD '',A16)') YDCOMP(JF)%CNAME 
      IERR=IERR+1
    ENDIF
  ENDDO
ENDIF
IF (IERR > 0) THEN
  CALL ABOR1('FPCHRESOL : UNIDENTIFIED REQUESTED GMV OR GFL FIELD(S)')
ENDIF
! Post-processing itself
CALL VARFPOS(YLRQGMV,YDFPDATA,YLRQPHY,LLRQCFU,LLRQXFU,YDFIELDS_IN%GEOM, &
 & YDFIELDS_IN%YRGMV,YDFIELDS_IN%YRGFL,YDFIELDS_IN%YRSURF,YDFIELDS_IN%YRCFU,YDFIELDS_IN%YRXFU, &
 & YDFIELDS_IN%STATE_MODEL,ZWSXI,ZWDXI,YDFIELDS_IN%YRCFU%GFUBUF,YDFIELDS_IN%YRXFU%XFUBUF,YDFIELDS_IN%YMCUF%NCUFNR,ZMCUFGP, &
 & KP3LEVS=YDDIMV%NFLEVG,KP3FIELDS=IFIELDS,P3DGFL=Z3DFIELDS)


!*       3.2 GMVT1S + Orography
!            ------------------

! needed !! ... at least for now ! but why ?
IFIELDS=0
CALL ALLOCATE_TRQ3FP(IFIELDS,YDDIMV%NFLEVG,IFPDOM,LALLOFP,NULOUT,YLRQGMVS%YES)
DO JLEV=1,YDDIMV%NFLEVG
  YLRQGMVS%YES%ZPXLEV(JLEV)=REAL(JLEV,JPRB)
ENDDO

IFIELDS=YT1%NDIMS+1
CALL ALLOCATE_TRQ2FP(IFIELDS,IFPDOM,LALLOFP,NULOUT,YLRQGMVS%Y2D)
IERR=0
YLRQGMVS%Y2D%ICOD(:)=0
DO JF=1,YT1%NDIMS
  IF (JF==YT1%MSP) THEN
    YLRQGMVS%Y2D%ICOD(JF)=YDAFN%TFP%LNSP%ICOD
  ENDIF
ENDDO
DO JF=1,YT1%NDIMS
  IF (YLRQGMVS%Y2D%ICOD(JF)==0) THEN
    WRITE(NULOUT,'('' UNIDENTIFIED REQUESTED GMVS FIELD NR '',I8)') JF
    IERR=IERR+1
  ENDIF
ENDDO
IF (IERR > 0) THEN
  CALL ABOR1('FPCHRESOL : UNIDENTIFIED REQUESTED FIELD(S)')
ENDIF
YLRQGMVS%Y2D%ICOD(YT1%NDIMS+1)=YDAFN%TFP%FIS%ICOD
! Post-processing itself
CALL VARFPOS(YLRQGMVS,YDFPDATA,YLRQPHY,LLRQCFU,LLRQXFU,YDFIELDS_IN%GEOM, &
 & YDFIELDS_IN%YRGMV,YDFIELDS_IN%YRGFL,YDFIELDS_IN%YRSURF,YDFIELDS_IN%YRCFU,YDFIELDS_IN%YRXFU, &
 & YDFIELDS_IN%STATE_MODEL,ZWSXI,ZWDXI,YDFIELDS_IN%YRCFU%GFUBUF,YDFIELDS_IN%YRXFU%XFUBUF,YDFIELDS_IN%YMCUF%NCUFNR,ZMCUFGP, &
 & KP3LEVS=0,KP3FIELDS=0,PGMVS=Z2DFIELDS)


!*       4.    DIRECT SPECTRAL TRANSFORMS
!              --------------------------

IVSETSC(1) = YDMP%NBSETSP
IF(YDMP%NBSETSP == MYSETV) THEN
  IST = 1
ELSE
  IST = 0
ENDIF
ALLOCATE(ZSP(1,YDDIM%NSPEC2))

! Normal configuration (u,v grid-point to vor,div spectral)
IF(NUMSPFLDS1>0) THEN
  IOFF=YT1%NDIM+NUMFLDS1-NUMSPFLDS1
  CALL DIR_TRANS(PSPVOR=YDSPEC%VOR,PSPDIV=YDSPEC%DIV,&
   & PSPSC2=ZSP(1:IST,:),PSPSC3A=YDSPEC%HV,&
   & PSPSC3B=YDSPEC%GFL,&
   & KRESOL=YDDIM%NRESOL,KPROMA=YDDIM%NPROMA,KVSETUV=YDMP%NBSETLEV,KVSETSC2=IVSETSC(1:1),&
   & KVSETSC3A=YDMP%NBSETLEV,KVSETSC3B=YDMP%NBSETLEV,&
   & PGPUV=Z3DFIELDS(:,:,1:2,:),PGP2=Z2DFIELDS(:,YT1%MSP:YT1%MSP,:),&
   & PGP3A=Z3DFIELDS(:,:,3:YT1%NDIM,:),PGP3B=Z3DFIELDS(:,:,IOFF+1:IOFF+NUMSPFLDS1,:))  
ELSE
  CALL DIR_TRANS(PSPVOR=YDSPEC%VOR,PSPDIV=YDSPEC%DIV,&
   & PSPSC2=ZSP(1:IST,:),PSPSC3A=YDSPEC%HV,&
   & KRESOL=YDDIM%NRESOL,KPROMA=YDDIM%NPROMA,KVSETUV=YDMP%NBSETLEV,KVSETSC2=IVSETSC(1:1),&
   & KVSETSC3A=YDMP%NBSETLEV,&
   & PGPUV=Z3DFIELDS(:,:,1:2,:),PGP2=Z2DFIELDS(:,YT1%MSP:YT1%MSP,:),&
   & PGP3A=Z3DFIELDS(:,:,3:YT1%NDIM,:))  
ENDIF

IF(YDMP%NBSETSP == MYSETV) THEN
  IF (SIZE(YDSPEC%SP2D,2) > 0) THEN
    YDSPEC%SP(:) = ZSP(1,:)
  ENDIF
ENDIF

CALL DIR_TRANS(PSPSC2=ZSP(1:IST,:),KRESOL=YDDIM%NRESOL,KPROMA=YDDIM%NPROMA,&
 & KVSETSC2=IVSETSC(1:1),PGP2=Z2DFIELDS(:,YT1%MSP+1:YT1%MSP+1,:))
IF(YDMP%NBSETSP == MYSETV) THEN
  IF (SIZE(YDSPEC%SP2D,2) > 1) THEN
    YDSPEC%OROG(:) = ZSP(1,:)
  ENDIF
ENDIF
CALL SUOROG(YDGEOM,YDSPEC%OROG(:))

! GFL : probaly faster to do so, than calling fullpos just for them
IOFF=YT1%NDIM
YDFIELDS_OUT%YRGFL%GFLT1(:,:,1:NUMFLDS1-NUMSPFLDS1,:)=Z3DFIELDS(:,:,IOFF+1:IOFF+NUMFLDS1-NUMSPFLDS1,:)

IF (YDDYNA%LSPRT) THEN
  CALL SPECRT(YDFIELDS_OUT%GEOM,YDFIELDS_OUT%YRGFL,YDGFL,0,YDSPEC)
ENDIF

WRITE(NULOUT,'('' '')')
CALL SPECNORM(KRESOL=YDDIM%NRESOL,PSPEC=ZSP(1:IST,:),KVSET=IVSETSC,KMASTER=1,PNORM=ZAVEG)
! Broadcast norm on each task
DO J=1,NPROC
  CALL MPL_BROADCAST(ZAVEG(1),KTAG=J,KROOT=J,CDSTRING='FPCHRESOL:')
ENDDO
WRITE(NULOUT,'('' SPECTRAL NORMS -      OROGRAPHY      '',E21.15)') ZAVEG(1)
CALL SPNORM(YDGEOM,YDML_GCONF,YDDYNA,YDSPEC)
DO JF=1,NUMFLDS1-NUMSPFLDS1
  CALL GPNORM3(YDGEOM,YDFIELDS_OUT%YRGFL%GFLT1(:,:,1:NUMFLDS1-NUMSPFLDS1,:),YDGFL%NDIM,JF,YDCOMP(JF)%CNAME)
ENDDO
WRITE(NULOUT,'('' '')')

! Now, prepare to start a new step by filling t0 and derivatives, etc :
LLFPOS=.TRUE. ! one never knows !
CLCONF='0AA000000'
WRITE(NULOUT,'('' INVERSE TRANSFORMS IN FPCHRESOL '')')
