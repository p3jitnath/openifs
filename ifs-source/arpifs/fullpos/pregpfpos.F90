SUBROUTINE PREGPFPOS(YDGEOMETRY,YDSURF,YDCFU,YDXFU,PXFUBUF,PCFUBUF,LDXFU,LDCFU)

!**** *PREGPFPOS*  - Prepare Full Post-Processing on grid point fields

!     PURPOSE.
!     --------
!        To save model gp data and pack current data before go post-processing

!**   INTERFACE.
!     ----------
!       *CALL* *PREGPFPOS*

!        EXPLICIT ARGUMENTS
!        --------------------
!           LDXFU   : Post-processing on instantaneous fluxes activated
!           LDCFU   : Post-processing on cumulated fluxes activated

!        IMPLICIT ARGUMENTS
!        --------------------
!        NONE.

!     METHOD.
!     -------
!        SEE DOCUMENTATION

!     EXTERNALS.
!     ----------

!     REFERENCE.
!     ----------
!        ECMWF Research Department documentation of the IFS

!     AUTHOR.
!     -------
!      RYAD EL KHATIB *METEO-FRANCE*
!      ORIGINAL : 98-01-28 by R. El Khatib (from gridfpos)

!     MODIFICATIONS.
!     --------------
!      R. El Khatib : 03-05-16 (Fix) parity array
!      M.Hamrud      01-Oct-2003 CY28 Cleaning
!      M.Hamrud      10-Jan-2004 CY28R1 Cleaning
!      M.Hamrud      01-Jul-2006  Revised surface fields
!      R. El Khatib : 30-Jul-2009 Support of GRIB 1 encoding needs names
!      G.Mozdzynski (Feb 2011): OOPS cleaning, use of derived type TGSGEOM
!      T. Wilhelmsson (Sept 2013) Geometry and setup refactoring.
!      R. El khatib 16-May-2014 Optimization of in-line/off-line post-processing reproducibility
!      E.Dutra/G.Arduini Jan 2018: PSP_SG 4 dimensions, snow multi-layer 
!     ------------------------------------------------------------------

USE GEOMETRY_MOD       , ONLY : GEOMETRY
USE SURFACE_FIELDS_MIX , ONLY : TSURF
USE PARKIND1           , ONLY : JPIM     ,JPRB
USE YOMHOOK            , ONLY : LHOOK,   DR_HOOK, JPHOOK
USE YOMCT0             , ONLY : LRPLANE
USE YOMCFU             , ONLY : TCFU
USE YOMXFU             , ONLY : TXFU

IMPLICIT NONE
TYPE(GEOMETRY)    ,INTENT(IN)    :: YDGEOMETRY
TYPE(TSURF)       ,INTENT(INOUT) :: YDSURF
TYPE(TCFU)        ,INTENT(INOUT) :: YDCFU
TYPE(TXFU)        ,INTENT(INOUT) :: YDXFU
REAL(KIND=JPRB)   ,INTENT(INOUT) :: PCFUBUF(YDGEOMETRY%YRDIM%NPROMA,YDCFU%NFDCFU,YDGEOMETRY%YRDIM%NGPBLKS)
REAL(KIND=JPRB)   ,INTENT(INOUT) :: PXFUBUF(YDGEOMETRY%YRDIM%NPROMA,YDXFU%NFDXFU,YDGEOMETRY%YRDIM%NGPBLKS)
LOGICAL           ,INTENT(IN)    :: LDXFU 
LOGICAL           ,INTENT(IN)    :: LDCFU
INTEGER(KIND=JPIM), ALLOCATABLE :: IPARC(:), IPARX(:)

INTEGER(KIND=JPIM) :: JF, JL, IOFF, IST, IEND, JROF, JV, JC, ISURF, IFLDS, IFF
INTEGER(KIND=JPIM) :: IFIELDSCFU, IFIELDSXFU, IFDFU , IFDSURF, IFLDTOT, IOFF2
INTEGER(KIND=JPIM) :: IKGLO, JBLK

REAL(KIND=JPRB) :: Z1SGM(YDGEOMETRY%YRDIM%NPROMA)
REAL(KIND=JPRB), ALLOCATABLE :: ZSURF(:,:,:)

CHARACTER(LEN=16), ALLOCATABLE :: CLNAME2(:)
LOGICAL, ALLOCATABLE :: LL2(:)
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE


#include "pksurfa.intfb.h"

!     ------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('PREGPFPOS',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM=>YDGEOMETRY%YRDIM,YDDIMV=>YDGEOMETRY%YRDIMV, &
  & YDGEM=>YDGEOMETRY%YRGEM, YDMP=>YDGEOMETRY%YRMP, YDGSGEOM_NB=>YDGEOMETRY%YRGSGEOM_NB)
ASSOCIATE(NFDCFU=>YDCFU%NFDCFU, NTYPCFU=>YDCFU%NTYPCFU, &
 & TYPE_CFU=>YDCFU%TYPE_CFU, &
 & NPROMA=>YDDIM%NPROMA, &
 & NGPTOT=>YDGEM%NGPTOT, &
 & SD_VA=>YDSURF%SD_VA, SD_VC=>YDSURF%SD_VC, SD_VD=>YDSURF%SD_VD, &
 & SD_VF=>YDSURF%SD_VF, SD_VH=>YDSURF%SD_VH, SD_VN=>YDSURF%SD_VN, &
 & SD_VP=>YDSURF%SD_VP, SD_VV=>YDSURF%SD_VV, SD_WS=>YDSURF%SD_WS, &
 & SP_CI=>YDSURF%SP_CI, SP_EP=>YDSURF%SP_EP, SP_RR=>YDSURF%SP_RR, &
 & SP_SB=>YDSURF%SP_SB, SP_SG=>YDSURF%SP_SG, SP_X2=>YDSURF%SP_X2, &
 & YSD_VA=>YDSURF%YSD_VA, YSD_VAD=>YDSURF%YSD_VAD, YSD_VC=>YDSURF%YSD_VC, &
 & YSD_VCD=>YDSURF%YSD_VCD, YSD_VD=>YDSURF%YSD_VD, YSD_VDD=>YDSURF%YSD_VDD, &
 & YSD_VF=>YDSURF%YSD_VF, YSD_VFD=>YDSURF%YSD_VFD, YSD_VH=>YDSURF%YSD_VH, &
 & YSD_VHD=>YDSURF%YSD_VHD, YSD_VN=>YDSURF%YSD_VN, YSD_VND=>YDSURF%YSD_VND, &
 & YSD_VP=>YDSURF%YSD_VP, YSD_VPD=>YDSURF%YSD_VPD, YSD_VV=>YDSURF%YSD_VV, &
 & YSD_VVD=>YDSURF%YSD_VVD, YSD_WS=>YDSURF%YSD_WS, YSD_WSD=>YDSURF%YSD_WSD, &
 & YSP_CI=>YDSURF%YSP_CI, YSP_CID=>YDSURF%YSP_CID, YSP_EP=>YDSURF%YSP_EP, &
 & YSP_EPD=>YDSURF%YSP_EPD, YSP_RR=>YDSURF%YSP_RR, YSP_RRD=>YDSURF%YSP_RRD, &
 & YSP_SB=>YDSURF%YSP_SB, YSP_SBD=>YDSURF%YSP_SBD, YSP_SG=>YDSURF%YSP_SG, &
 & YSP_SGD=>YDSURF%YSP_SGD, YSP_X2=>YDSURF%YSP_X2, YSP_X2D=>YDSURF%YSP_X2D, &
 & NFDXFU=>YDXFU%NFDXFU, NTYPXFU=>YDXFU%NTYPXFU, TYPE_XFU=>YDXFU%TYPE_XFU, &
 & NGPBLKS=>YDDIM%NGPBLKS, SD_DI=>YDSURF%SD_DI, YSD_DI=>YDSURF%YSD_DI, &
 & YSD_DID=>YDSURF%YSD_DID)
IF (LDCFU) THEN
  IFIELDSCFU=SIZE (PCFUBUF, 2)
ELSE
  IFIELDSCFU=0
ENDIF
IF (LDXFU) THEN
  IFIELDSXFU=SIZE (PXFUBUF, 2)
ELSE
  IFIELDSXFU=0
ENDIF
IFDFU=IFIELDSCFU+IFIELDSXFU
IF (LRPLANE) THEN
  ALLOCATE(IPARC(IFIELDSCFU))
  ALLOCATE(IPARX(IFIELDSXFU))
ENDIF
IFDSURF=YDSURF%NSURFL

IFLDTOT=IFIELDSCFU+IFIELDSXFU+IFDSURF

IST=1

!*       1. BEFORE POST-PROCESSING
!           ----------------------


!        1.1 Save & pack physical fields


  ALLOCATE(CLNAME2(IFLDTOT))
  CLNAME2(:)=' '
  ALLOCATE(LL2(IFLDTOT))
  LL2(:)=.TRUE.
  ALLOCATE(ZSURF(NPROMA,IFLDTOT,NGPBLKS))

  IOFF=0
  IF (IFDSURF > 0) THEN
    ISURF=1
    IFLDS=YSP_SBD%NUMFLDS*YSP_SBD%NLEVS
    IF (IFLDS > 0) THEN
      LL2(ISURF:ISURF+IFLDS-1)=.FALSE.
      DO JV=1,YSP_SBD%NUMFLDS
        DO JC=1,YSP_SBD%NLEVS
          ZSURF(:,ISURF+(JC+(JV-1)*YSP_SBD%NLEVS)-1,:)=SP_SB(:,JC,JV,:)
          CLNAME2(ISURF+(JC+(JV-1)*YSP_SBD%NLEVS)-1)=YSP_SB%YSB(JV)%CNAME(JC)
        ENDDO
      ENDDO
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSP_EPD%NUMFLDS*YSP_EPD%NLEVS
    IF (IFLDS > 0) THEN
      LL2(ISURF:ISURF+IFLDS-1)=.FALSE.
      DO JV=1,YSP_EPD%NUMFLDS
        DO JC=1,YSP_EPD%NLEVS
          ZSURF(:,ISURF+(JC+(JV-1)*YSP_EPD%NLEVS)-1,:)=SP_EP(:,JC,JV,:)
          CLNAME2(ISURF+(JC+(JV-1)*YSP_EPD%NLEVS)-1)=YSP_EP%YEP(JV)%CNAME(JC)
        ENDDO
      ENDDO
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSP_SGD%NUMFLDS*YSP_SGD%NLEVS
    IF (IFLDS > 0) THEN
      LL2(ISURF:ISURF+IFLDS-1)=.FALSE.
      DO JV=1,YSP_SGD%NUMFLDS
        DO JC=1,YSP_SGD%NLEVS
          ZSURF(:,ISURF+(JC+(JV-1)*YSP_SGD%NLEVS)-1,:)=SP_SG(:,JC,JV,:)
          CLNAME2(ISURF+(JC+(JV-1)*YSP_SGD%NLEVS)-1)=YSP_SG%YSG(JV)%CNAME(JC)
        ENDDO
      ENDDO
      ISURF=ISURF+IFLDS
    ENDIF   
    IFLDS=YSP_RRD%NUMFLDS
    IF (IFLDS > 0) THEN
      ZSURF(:,ISURF:ISURF+IFLDS-1,:)=SP_RR(:,1:IFLDS,:)
      CLNAME2(ISURF:ISURF+IFLDS-1)=YSP_RR%YRR(1:IFLDS)%CNAME
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSP_X2D%NUMFLDS
    IF (IFLDS > 0) THEN
      ZSURF(:,ISURF:ISURF+IFLDS-1,:)=SP_X2(:,1:IFLDS,:)
      CLNAME2(ISURF:ISURF+IFLDS-1)=YSP_X2%YX2(1:IFLDS)%CNAME
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSP_CID%NUMFLDS
    IF (IFLDS > 0) THEN
      ZSURF(:,ISURF:ISURF+IFLDS-1,:)=SP_CI(:,1:IFLDS,:)
      CLNAME2(ISURF:ISURF+IFLDS-1)=YSP_CI%YCI(1:IFLDS)%CNAME
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_VFD%NUMFLDS
    IF (IFLDS > 0) THEN
      ZSURF(:,ISURF:ISURF+IFLDS-1,:)=SD_VF(:,1:IFLDS,:)
      CLNAME2(ISURF:ISURF+IFLDS-1)=YSD_VF%YVF(1:IFLDS)%CNAME
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_VPD%NUMFLDS
    IF (IFLDS > 0) THEN
      ZSURF(:,ISURF:ISURF+IFLDS-1,:)=SD_VP(:,1:IFLDS,:)
      CLNAME2(ISURF:ISURF+IFLDS-1)=YSD_VP%YVP(1:IFLDS)%CNAME
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_VVD%NUMFLDS
    IF (IFLDS > 0) THEN
      ZSURF(:,ISURF:ISURF+IFLDS-1,:)=SD_VV(:,1:IFLDS,:)
      CLNAME2(ISURF:ISURF+IFLDS-1)=YSD_VV%YVV(1:IFLDS)%CNAME
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_VND%NUMFLDS
    IF (IFLDS > 0) THEN
      ZSURF(:,ISURF:ISURF+IFLDS-1,:)=SD_VN(:,1:IFLDS,:)
      CLNAME2(ISURF:ISURF+IFLDS-1)=YSD_VN%YVN(1:IFLDS)%CNAME
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_VHD%NUMFLDS
    IF (IFLDS > 0) THEN
      ZSURF(:,ISURF:ISURF+IFLDS-1,:)=SD_VH(:,1:IFLDS,:)
      CLNAME2(ISURF:ISURF+IFLDS-1)=YSD_VH%YVH(1:IFLDS)%CNAME
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_VAD%NUMFLDS
    IF (IFLDS > 0) THEN
      ZSURF(:,ISURF:ISURF+IFLDS-1,:)=SD_VA(:,1:IFLDS,:)
      CLNAME2(ISURF:ISURF+IFLDS-1)=YSD_VA%YVA(1:IFLDS)%CNAME
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_VCD%NUMFLDS
    IF (IFLDS > 0) THEN
      ZSURF(:,ISURF:ISURF+IFLDS-1,:)=SD_VC(:,1:IFLDS,:)
      CLNAME2(ISURF:ISURF+IFLDS-1)=YSD_VC%YVC(1:IFLDS)%CNAME
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_VDD%NUMFLDS
    IF (IFLDS > 0) THEN
      ZSURF(:,ISURF:ISURF+IFLDS-1,:)=SD_VD(:,1:IFLDS,:)
      CLNAME2(ISURF:ISURF+IFLDS-1)=YSD_VD%YVD(1:IFLDS)%CNAME
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_WSD%NUMFLDS
    IF (IFLDS > 0) THEN
      ZSURF(:,ISURF:ISURF+IFLDS-1,:)=SD_WS(:,1:IFLDS,:)
      CLNAME2(ISURF:ISURF+IFLDS-1)=YSD_WS%YWS(1:IFLDS)%CNAME
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_DID%NUMFLDS*YSD_DID%NLEVS
    IF (IFLDS > 0) THEN
      DO JV=1,YSD_DID%NUMFLDS
        DO JC=1,YSD_DID%NLEVS
          ZSURF(:,ISURF+(JC+(JV-1)*YSD_DID%NLEVS)-1,:)=SD_DI(:,JC,JV,:)
          CLNAME2(ISURF+(JC+(JV-1)*YSD_DID%NLEVS)-1)=YSD_DI%YXA(JV)%CNAME(JC)
        ENDDO
      ENDDO
      ISURF=ISURF+IFLDS
    ENDIF

  ENDIF

  IOFF=IOFF+IFDSURF
  IF (IFIELDSCFU > 0) THEN
    IF (LRPLANE) THEN
      IOFF2=0
      DO JF=1,NTYPCFU
        DO JL=1,TYPE_CFU(JF)%ILEV
          IPARC(IOFF2+JL)=TYPE_CFU(JF)%IPARITY
        ENDDO
        IOFF2=IOFF2+TYPE_CFU(JF)%ILEV
      ENDDO
      IF (ANY(IPARC==1)) THEN
        DO JBLK=1,NGPBLKS
          IKGLO=(JBLK-1)*NPROMA+1
          IEND=MIN(NPROMA,NGPTOT-IKGLO+1)
          DO JF=1, SIZE (PCFUBUF, 2)
            IF (IPARC(JF) == 1) THEN
              DO JROF=IST,IEND
                PCFUBUF(JROF,JF,JBLK)=PCFUBUF(JROF,JF,JBLK)*YDGSGEOM_NB%GM(JROF+IKGLO-1)
              ENDDO
            ENDIF
          ENDDO
        ENDDO
      ENDIF
    ENDIF  
    DO JBLK=1,NGPBLKS
      IKGLO=(JBLK-1)*NPROMA+1
      IEND=MIN(NPROMA,NGPTOT-IKGLO+1)
      DO JF=1, SIZE (PCFUBUF, 2)
        IFF=IOFF+JF
        DO JROF=IST,IEND
          ZSURF(JROF,IFF,JBLK)=PCFUBUF(JROF,JF,JBLK)
        ENDDO
      ENDDO
    ENDDO
    CLNAME2(IOFF+1:IOFF+IFIELDSCFU)=' '
    LL2(IOFF+1:IOFF+IFIELDSCFU)=.FALSE.
  ENDIF  

  IOFF=IOFF+IFIELDSCFU
  IF (IFIELDSXFU > 0) THEN
    IF (LRPLANE) THEN
      IOFF2=0
      DO JF=1,NTYPXFU
        DO JL=1,TYPE_XFU(JF)%ILEV
          IPARX(IOFF2+JL)=TYPE_XFU(JF)%IPARITY
        ENDDO
        IOFF2=IOFF2+TYPE_XFU(JF)%ILEV
      ENDDO
      IF (ANY(IPARX==1)) THEN
        DO JBLK=1,NGPBLKS
          IKGLO=(JBLK-1)*NPROMA+1
          IEND=MIN(NPROMA,NGPTOT-IKGLO+1)
          DO JF=1, SIZE (PXFUBUF, 2)
            IF (IPARX(JF) == 1) THEN
              DO JROF=IST,IEND
                PXFUBUF(JROF,JF,JBLK)=PXFUBUF(JROF,JF,JBLK)*YDGSGEOM_NB%GM(JROF+IKGLO-1)
              ENDDO
            ENDIF
          ENDDO
        ENDDO
      ENDIF
    ENDIF
    DO JBLK=1,NGPBLKS
      IKGLO=(JBLK-1)*NPROMA+1
      IEND=MIN(NPROMA,NGPTOT-IKGLO+1)
      DO JF=1, SIZE (PXFUBUF, 2)
        IFF=IOFF+JF
        DO JROF=IST,IEND
          ZSURF(JROF,IFF,JBLK)=PXFUBUF(JROF,JF,JBLK)
        ENDDO
      ENDDO
    ENDDO
    CLNAME2(IOFF+1:IOFF+IFIELDSXFU)=' '
    LL2(IOFF+1:IOFF+IFIELDSXFU)=.FALSE.
  ENDIF

  IF (IFLDTOT > IOFF+IFIELDSXFU) THEN
    ZSURF(:,IOFF+IFIELDSXFU+1:IFLDTOT,:)=0._JPRB
  ENDIF

  CALL PKSURFA(YDGEOMETRY,YDSURF,CLNAME2,ZSURF,LL2)

  IOFF=0
  IF (IFDSURF > 0) THEN
    ISURF=1
    IFLDS=YSP_SBD%NUMFLDS*YSP_SBD%NLEVS
    IF (IFLDS > 0) THEN
      DO JV=1,YSP_SBD%NUMFLDS
        DO JC=1,YSP_SBD%NLEVS
          SP_SB(:,JC,JV,:)=ZSURF(:,ISURF+(JC+(JV-1)*YSP_SBD%NLEVS)-1,:)
        ENDDO
      ENDDO
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSP_EPD%NUMFLDS*YSP_EPD%NLEVS
    IF (IFLDS > 0) THEN
      DO JV=1,YSP_EPD%NUMFLDS
        DO JC=1,YSP_EPD%NLEVS
          SP_EP(:,JC,JV,:)=ZSURF(:,ISURF+(JC+(JV-1)*YSP_EPD%NLEVS)-1,:)
        ENDDO
      ENDDO
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSP_SGD%NUMFLDS*YSP_SGD%NLEVS
    IF (IFLDS > 0) THEN
      DO JV=1,YSP_SGD%NUMFLDS
        DO JC=1,YSP_SGD%NLEVS
          SP_SG(:,JC,JV,:)=ZSURF(:,ISURF+(JC+(JV-1)*YSP_SGD%NLEVS)-1,:)
        ENDDO
      ENDDO
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSP_RRD%NUMFLDS
    IF (IFLDS > 0) THEN
      SP_RR(:,1:IFLDS,:)=ZSURF(:,ISURF:ISURF+IFLDS-1,:)
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSP_X2D%NUMFLDS
    IF (IFLDS > 0) THEN
      SP_X2(:,1:IFLDS,:)=ZSURF(:,ISURF:ISURF+IFLDS-1,:)
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSP_CID%NUMFLDS
    IF (IFLDS > 0) THEN
      SP_CI(:,1:IFLDS,:)=ZSURF(:,ISURF:ISURF+IFLDS-1,:)
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_VFD%NUMFLDS
    IF (IFLDS > 0) THEN
      SD_VF(:,1:IFLDS,:)=ZSURF(:,ISURF:ISURF+IFLDS-1,:)
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_VPD%NUMFLDS
    IF (IFLDS > 0) THEN
      SD_VP(:,1:IFLDS,:)=ZSURF(:,ISURF:ISURF+IFLDS-1,:)
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_VVD%NUMFLDS
    IF (IFLDS > 0) THEN
      SD_VV(:,1:IFLDS,:)=ZSURF(:,ISURF:ISURF+IFLDS-1,:)
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_VND%NUMFLDS
    IF (IFLDS > 0) THEN
      SD_VN(:,1:IFLDS,:)=ZSURF(:,ISURF:ISURF+IFLDS-1,:)
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_VHD%NUMFLDS
    IF (IFLDS > 0) THEN
      SD_VH(:,1:IFLDS,:)=ZSURF(:,ISURF:ISURF+IFLDS-1,:)
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_VAD%NUMFLDS
    IF (IFLDS > 0) THEN
      SD_VA(:,1:IFLDS,:)=ZSURF(:,ISURF:ISURF+IFLDS-1,:)
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_VCD%NUMFLDS
    IF (IFLDS > 0) THEN
      SD_VC(:,1:IFLDS,:)=ZSURF(:,ISURF:ISURF+IFLDS-1,:)
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_VDD%NUMFLDS
    IF (IFLDS > 0) THEN
      SD_VD(:,1:IFLDS,:)=ZSURF(:,ISURF:ISURF+IFLDS-1,:)
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_WSD%NUMFLDS
    IF (IFLDS > 0) THEN
      SD_WS(:,1:IFLDS,:)=ZSURF(:,ISURF:ISURF+IFLDS-1,:)
      ISURF=ISURF+IFLDS
    ENDIF
    IFLDS=YSD_DID%NUMFLDS*YSD_DID%NLEVS
    IF (IFLDS > 0) THEN
      DO JV=1,YSD_DID%NUMFLDS
        DO JC=1,YSD_DID%NLEVS
          SD_DI(:,JC,JV,:)=ZSURF(:,ISURF+(JC+(JV-1)*YSD_DID%NLEVS)-1,:)
        ENDDO
      ENDDO
      ISURF=ISURF+IFLDS
    ENDIF
  ENDIF

  IOFF=IOFF+IFDSURF
  IF (IFIELDSCFU > 0) THEN
    DO JBLK=1,NGPBLKS
      IKGLO=(JBLK-1)*NPROMA+1
      IEND=MIN(NPROMA,NGPTOT-IKGLO+1)
      DO JF=1, SIZE (PCFUBUF, 2)
        IFF=IOFF+JF
        DO JROF=IST,IEND
          PCFUBUF(JROF,JF,JBLK)=ZSURF(JROF,IFF,JBLK)
        ENDDO
      ENDDO
    ENDDO
  ENDIF

  IOFF=IOFF+IFIELDSCFU
  IF (IFIELDSXFU > 0) THEN
    DO JBLK=1,NGPBLKS
      IKGLO=(JBLK-1)*NPROMA+1
      IEND=MIN(NPROMA,NGPTOT-IKGLO+1)
      DO JF=1, SIZE (PXFUBUF, 2)
        IFF=IOFF+JF
        DO JROF=IST,IEND
          PXFUBUF(JROF,JF,JBLK)=ZSURF(JROF,IFF,JBLK)
        ENDDO
      ENDDO
    ENDDO
  ENDIF

  DEALLOCATE(CLNAME2)
  DEALLOCATE(LL2)
  DEALLOCATE(ZSURF)

  IF (LRPLANE) THEN
    IF (IFIELDSCFU > 0) THEN
      IF (ANY(IPARC==1)) THEN
        DO JBLK=1,NGPBLKS
          IKGLO=(JBLK-1)*NPROMA+1
          IEND=MIN(NPROMA,NGPTOT-IKGLO+1)
          DO JROF=IST,IEND
