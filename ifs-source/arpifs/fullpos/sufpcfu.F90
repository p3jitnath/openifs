SUBROUTINE SUFPCFU(YDCFU,YDRIP,YDPHY)

!**** *SUFPCFU*  - INITIALIZE CUMULATED FLUXES SWITCHES FOR POST-PROCESSING

!     PURPOSE.
!     --------
!        Modify default values to allow post-processing

!**   INTERFACE.
!     ----------
!       *CALL* *SUFPCFU*

!        EXPLICIT ARGUMENTS
!        --------------------
!        NONE

!        IMPLICIT ARGUMENTS
!        --------------------
!        YOMFPC
!        YOMCFU

!     METHOD.
!     -------
!        SEE DOCUMENTATION

!     EXTERNALS.
!     ----------

!     REFERENCE.
!     ----------
!        ECMWF Research Department documentation of the IFS

!     AUTHOR.
!     -------
!      RYAD EL KHATIB *METEO-FRANCE*
!      ORIGINAL : 94-04-08

!     MODIFICATIONS.
!     --------------
!      R. El Khatib : 01-05-04 Duration of total precipitations
!      R. El Khatib : 01-08-07 pruning options
!      R. El Khatib : 03-04-17 Fullpos improvemnts
!      M.Hamrud      01-Oct-2003 CY28 Cleaning
!      K. Yessad (July 2014): Move some variables.
!      R. Brozkova (Sep 2018): Added global normal irradiance.
!     ------------------------------------------------------------------

USE PARKIND1  ,ONLY : JPIM     ,JPRB
USE YOMHOOK   ,ONLY : LHOOK,   DR_HOOK, JPHOOK

USE YOMLUN   , ONLY : NULERR
USE YOMRIP   , ONLY : TRIP
USE YOMFA    , ONLY : YFA_CLSP  ,YFA_CCP  ,YFA_CLSG , YFA_CCSG  ,&
 & YFA_CLSH ,YFA_CCSH ,YFA_CLSS ,YFA_CCSF ,YFA_CUSS , &
 & YFA_CVSS ,YFA_CSSH ,YFA_CSLH ,YFA_CTSP ,YFA_CSMR ,YFA_CTP  , &
 & YFA_CTCC ,YFA_CBLD ,YFA_CSSR ,YFA_CSTR ,YFA_CTSR ,YFA_CETP , &
 & YFA_CTTR ,YFA_CCCC ,YFA_CHCC ,YFA_CMCC ,YFA_CLCC ,YFA_CIRU , &
 & YFA_CUGW ,YFA_CVGW ,YFA_CE   ,YFA_CS   ,YFA_CLHE ,YFA_CDRU , &
 & YFA_CLHS ,YFA_CWS  ,YFA_CSNS ,YFA_CQTO ,YFA_CDUTP,YFA_CTTHC, &
 & YFA_CTO3 ,YFA_CTME ,YFA_CICE ,YFA_CLI  ,YFA_CCVU ,YFA_CTSOC, &
 & YFA_CCVV ,YFA_CCVQ ,YFA_CCVS ,YFA_CTUQ ,YFA_CTUS ,YFA_CSRU , &
 & YFA_CSOC ,YFA_CTHC ,YFA_CSOP ,YFA_CTOP ,YFA_CSOD ,YFA_CEAS , &
 & YFA_CTHD ,YFA_CFON ,YFA_CCHS ,YFA_CLHT ,YFA_CT   ,YFA_CUTO , &
 & YFA_CVTO ,YFA_CDNI ,YFA_CGNI ,YFA_CFLASH
USE YOMCFU   , ONLY : TCFU
USE YOMPHY   , ONLY : TPHY
USE YOMFPC   , ONLY : TNAMFPL

!     ------------------------------------------------------------------

IMPLICIT NONE

TYPE(TCFU),INTENT(INOUT):: YDCFU
TYPE(TPHY),INTENT(INOUT):: YDPHY
TYPE(TRIP),INTENT(INOUT):: YDRIP

CHARACTER :: CLMARK*39
INTEGER(KIND=JPIM) :: IUCV, IUGW, IUSS, IVCV, IVGW, IVSS, JFLD
TYPE(TNAMFPL) :: YLNAMFPC

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!     ------------------------------------------------------------------

#include "sufpc.intfb.h"
#include "abor1.intfb.h"

!     ------------------------------------------------------------------
IF (LHOOK) CALL DR_HOOK('SUFPCFU',0,ZHOOK_HANDLE)
ASSOCIATE(LAMIP=>YDCFU%LAMIP, LCUMFU=>YDCFU%LCUMFU, LFDUTP=>YDCFU%LFDUTP, &
 & LFLASH=>YDCFU%LFLASH,&
 & LFPLC=>YDCFU%LFPLC, LFPLCG=>YDCFU%LFPLCG, LFPLCH=>YDCFU%LFPLCH, &
 & LFPLS=>YDCFU%LFPLS, LFPLSG=>YDCFU%LFPLSG, LFPLSH=>YDCFU%LFPLSH, &
 & LFR=>YDCFU%LFR, LFRRC=>YDCFU%LFRRC, LFSF=>YDCFU%LFSF, LFSOIL=>YDCFU%LFSOIL, &
 & LMOON=>YDCFU%LMOON, LNEBPAR=>YDCFU%LNEBPAR, LNEBTT=>YDCFU%LNEBTT, &
 & LRAYD=>YDCFU%LRAYD, LRAYS=>YDCFU%LRAYS, LREACFU=>YDCFU%LREACFU, &
 & LSTRC=>YDCFU%LSTRC, LSTRD=>YDCFU%LSTRD, LSTRT=>YDCFU%LSTRT, &
 & LOZONE=>YDPHY%LOZONE, LSOLV=>YDPHY%LSOLV, LNEIGE=>YDPHY%LNEIGE, &
 & NSTOP=>YDRIP%NSTOP,CFPCFU=>YLNAMFPC%CFPCFU)
!     ------------------------------------------------------------------

!*       1. MODIFY DEFAULT VALUES
!           ---------------------

CALL SUFPC(YDNAMFPL=YLNAMFPC,LDPRINT=.FALSE.)

IUSS=0
IVSS=0
IUGW=0
IVGW=0
IUCV=0
IVCV=0

DO JFLD=1,SIZE(CFPCFU)
  IF(CFPCFU(JFLD) == ' ') THEN
    IF (JFLD > 1) THEN
      LCUMFU=.TRUE.
      LREACFU=(LREACFU .OR. NSTOP == 0)
    ENDIF
    EXIT
  ELSEIF (CFPCFU(JFLD) == YFA_CLSP%CLNAME) THEN
    LFPLS=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CCP%CLNAME ) THEN
    LFPLC=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CLSS%CLNAME) THEN
    LNEIGE=.TRUE.
    LFPLS=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CCSF%CLNAME) THEN
    LNEIGE=.TRUE.
    LFPLC=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CLSG%CLNAME ) THEN
    LFPLSG=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CCSG%CLNAME ) THEN
    LFPLCG=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CLSH%CLNAME ) THEN
    LFPLSH=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CCSH%CLNAME ) THEN
    LFPLCH=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CUSS%CLNAME) THEN
    LFSF=.TRUE.
    IUSS=1
  ELSEIF (CFPCFU(JFLD) == YFA_CVSS%CLNAME) THEN
    LFSF=.TRUE.
    IVSS=1
  ELSEIF (CFPCFU(JFLD) == YFA_CSSH%CLNAME) THEN
    LFSF=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CSLH%CLNAME) THEN
    LAMIP=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CTSP%CLNAME) THEN
    LAMIP=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CTCC%CLNAME) THEN
    LNEBTT=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CBLD%CLNAME) THEN
    LAMIP=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CSSR%CLNAME) THEN
    LFR=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CSTR%CLNAME) THEN
    LFR=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CTSR%CLNAME) THEN
    LFR=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CTTR%CLNAME) THEN
    LFR=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CCCC%CLNAME) THEN
    LNEBPAR=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CHCC%CLNAME) THEN
    LNEBPAR=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CMCC%CLNAME) THEN
    LNEBPAR=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CLCC%CLNAME) THEN
    LNEBPAR=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CUGW%CLNAME) THEN
    LSTRD=.TRUE.
    IUGW=1
  ELSEIF (CFPCFU(JFLD) == YFA_CVGW%CLNAME) THEN
    LSTRD=.TRUE.
    IVGW=1
  ELSEIF (CFPCFU(JFLD) == YFA_CUTO%CLNAME) THEN
    LFSF=.TRUE.
    IUSS=1
    LSTRD=.TRUE.
    IUGW=1
  ELSEIF (CFPCFU(JFLD) == YFA_CVTO%CLNAME) THEN
    LFSF=.TRUE.
    IVSS=1
    LSTRD=.TRUE.
    IVGW=1
  ELSEIF (CFPCFU(JFLD) == YFA_CE%CLNAME) THEN
    LFSF=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CS%CLNAME  ) THEN
    LFSF=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CT%CLNAME  ) THEN
    LFSF=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CLHE%CLNAME) THEN
    LFSF=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CLHS%CLNAME) THEN
    LFSF=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CLHT%CLNAME) THEN
    LFSF=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CWS%CLNAME ) THEN
    LAMIP=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CSNS%CLNAME) THEN
    LAMIP=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CQTO%CLNAME) THEN
    LAMIP=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CTO3%CLNAME) THEN
    LAMIP=.TRUE.
    LOZONE=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CTME%CLNAME) THEN
    LAMIP=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CICE%CLNAME) THEN
    LAMIP=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CLI%CLNAME ) THEN
    LAMIP=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CCVU%CLNAME) THEN
    LSTRC=.TRUE.
    IUCV=1
  ELSEIF (CFPCFU(JFLD) == YFA_CCVV%CLNAME) THEN
    LSTRC=.TRUE.
    IVCV=1
  ELSEIF (CFPCFU(JFLD) == YFA_CCVQ%CLNAME) THEN
    LSTRC=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CCVS%CLNAME) THEN
    LSTRC=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CTUQ%CLNAME) THEN
    LSTRT=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CTUS%CLNAME) THEN
    LSTRT=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CSOC%CLNAME) THEN
    LFRRC=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CTHC%CLNAME) THEN
    LFRRC=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CTSOC%CLNAME) THEN
    LFRRC=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CTTHC%CLNAME) THEN
    LFRRC=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CDNI%CLNAME) THEN
    LRAYS=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CGNI%CLNAME) THEN
    LRAYS=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CSOP%CLNAME) THEN
    LRAYS=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CTOP%CLNAME) THEN
    LRAYS=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CSOD%CLNAME) THEN
    LRAYD=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CTHD%CLNAME) THEN
    LRAYD=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CSMR%CLNAME) THEN
    LMOON=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CFON%CLNAME) THEN
    LFSOIL=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CCHS%CLNAME) THEN
    LFSOIL=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CEAS%CLNAME) THEN
    LFSOIL=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CSRU%CLNAME) THEN
    LFSOIL=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CDRU%CLNAME) THEN
    LFSOIL=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CIRU%CLNAME) THEN
    LFSOIL=.TRUE.
    LSOLV=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CETP%CLNAME) THEN
    LFSOIL=.TRUE.
    LSOLV=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CTP%CLNAME ) THEN
    LFSOIL=.TRUE.
    LSOLV=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CDUTP%CLNAME) THEN
    LFDUTP=.TRUE.
  ELSEIF (CFPCFU(JFLD) == YFA_CFLASH%CLNAME) THEN
    LFLASH=.TRUE.
  ELSE
    CLMARK=CFPCFU(JFLD)//' NOT FOUND '
    WRITE(NULERR,*) CLMARK
  ENDIF
ENDDO
IF ((IUSS /= IVSS).OR.(IUGW /= IVGW).OR.(IUCV /= IVCV)) THEN
  CALL ABOR1('U AND V COMPONENTS ARE BOTH NECESSARY !')
ENDIF

!     ------------------------------------------------------------------
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('SUFPCFU',1,ZHOOK_HANDLE)
END SUBROUTINE SUFPCFU
