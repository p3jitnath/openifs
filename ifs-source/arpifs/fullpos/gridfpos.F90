SUBROUTINE GRIDFPOS(YDRQPHY,YDNAMFPINT,YDNAMFPSCI,YDAFN,KFPXFLD,YDFPGEOMETRY,YDFPDATA,PWSXI,PWDXI,YDFPWSTD,YDFPSTRUCT,CDCONF, &
 & LDEZO,KFPDOM,YDGEOMETRY,YDSURF,YDCFU,YDXFU,YDMODEL,PCFUBUF,PXFUBUF,PBUF,YDAUX,P2DFIELDS,P3DFIELDS,KP3LEVS,KP3FIELDS)

!**** *GRIDFPOS*  - Full Post-Processing on grid point fields

!     PURPOSE.
!     --------
!        Performs horizontal post-processing on physical fields and fluxes

!**   INTERFACE.
!     ----------
!       *CALL* *GRIDFPOS*

!        EXPLICIT ARGUMENTS
!        --------------------
!           KFPXFLD : maximum number of fields to extract at a time
!           LDEZO : .TRUE. if biperiodicization active
!           KFPDOM : number of subdomains
!           YDGEOMETRY : input model geometry
!           YDSURF : input surface fields
!           PCFUBUF : input cumulated fluxes
!           PXFUBUF : input instantaneous fluxes
!           P2DFIELDS : alternative 2D output array to PBUF
!           P3DFIELDS : alternative 3D output array to PBUF
!           KP3LEVS : number of levels for each field in P3DFIELDS
!           KP3FIELDS : number of fields in P3DFIELDS

!        IMPLICIT ARGUMENTS
!        --------------------
!        NONE.

!     METHOD.
!     -------
!        SEE DOCUMENTATION

!     EXTERNALS.
!     ----------

!     REFERENCE.
!     ----------
!        ECMWF Research Department documentation of the IFS

!     AUTHOR.
!     -------
!      RYAD EL KHATIB *METEO-FRANCE*
!      ORIGINAL : 94-04-08

!     MODIFICATIONS.
!     --------------
!      R. El Khatib : 02-21-20 Fullpos B-level distribution + remove IO scheme
!      R. El Khatib : 03-04-17 Fullpos improvemnts
!      M.Hamrud      01-Oct-2003 CY28 Cleaning
!      R. El Khatib : 05-03-23 Option to subcontract surface field transformation to SURFEX
!      K. Yessad : 28-02-2007 DM-features optimisations in FULL-POS
!      K. Yessad (oct 2010): multi-linear interpolations.
!      K. Yessad (dec 2011): call directly SCAN2M.
!      R. El Khatib : 18-Jul-2012 Move away from SCAN2M.
!      R. El Khatib 31-Aug-2012 new E-zone management
!      P.Marguinaud (sep 2012): disable Further post-processing after FP2SX1
!      R. El Khatib 13-Dec-2012 Fullpos buffers reshaping
!      R. El Khatib 27-Sep-2013 Boyd periodization in Fullpos-2
!      K. Yessad (July 2014): Move some variables.
!      R. El Khatib 04-Aug-2014 Pruning of the conf. 927/928
!      R. El khatib 16-May-2014 Optimization of in-line/off-line post-processing reproducibility
!      P.Marguinaud 01-Oct-2014 More parameters for PREP fields interpolations
!      O. Marsden: June 2015 CY42 YRGMV, YRGFL, YRSURF, YRGMV5, and YRGFL5 are now passed by argument
!      R. El Khatib 02-Jul-2015 Cleanings
!      R. El Khatib 20-Nov-2015 computation of norms moved out of IOs
!      R. El Khatib 09-Dec-2015 NFPWRITE
!      R. El Khatib 10-Dec-2015 KSTEP
!      R. El Khatib 08-Mar-2016 call to ini3wrfp, sufpofname
!      R. El Khatib 22-mar-2016 Climatology filenames with sufpclifname
!      R. El Khatib 09-Aug-2016 Cleaning of biperiodicization management
!      R. El Khatib : 23-Aug-2016 KDATEF in argument
!      R. El Khatib 09-Sep-2016 Norms of auxilary fields ; cleanings
!     ------------------------------------------------------------------

USE PARKIND1           , ONLY : JPIM     ,JPRB
USE YOMHOOK            , ONLY : LHOOK,   DR_HOOK, JPHOOK

USE GEOMETRY_MOD       , ONLY : GEOMETRY
USE SURFACE_FIELDS_MIX , ONLY : TSURF
USE YOMCFU             , ONLY : TCFU
USE YOMXFU             , ONLY : TXFU
USE TYPE_MODEL         , ONLY : MODEL
USE YOMLUN             , ONLY : NULOUT
USE YOMFPGEOMETRY      , ONLY : TFPGEOMETRY, LFPOSBUF, LFPDISTRIB
USE YOMCT0             , ONLY : LELAM
USE YOMMP0             , ONLY : NPROC
USE YOMFPC             , ONLY : TNAMFPINT, TNAMFPSCI, LALLOFP
USE EINT_MOD           , ONLY : SL_STRUCT
USE YOMAFN             , ONLY : TAFN
USE YOMFP4L            , ONLY : TRQFP
USE YOMWFPB            , ONLY : TFPWSTD
USE FULLPOS            , ONLY : TFPDATA
USE TYPE_FPOSBUF       , ONLY : FPOSBUF
USE TYPE_FPRQDYNS, ONLY : TYPE_FPRQDYN

!-----------------------------------------------------------------------------

IMPLICIT NONE

TYPE (TRQFP),  INTENT(IN) :: YDRQPHY
TYPE(TNAMFPINT)   ,INTENT(IN) :: YDNAMFPINT
TYPE(TNAMFPSCI)   ,INTENT(IN) :: YDNAMFPSCI
TYPE(TAFN)        ,INTENT(IN) :: YDAFN
INTEGER(KIND=JPIM),INTENT(IN) :: KFPXFLD
TYPE(TFPGEOMETRY) ,INTENT(IN) :: YDFPGEOMETRY
TYPE(TFPDATA)     ,INTENT(IN) :: YDFPDATA
REAL(KIND=JPRB)   ,INTENT(IN) :: PWSXI
REAL(KIND=JPRB)   ,INTENT(IN) :: PWDXI
TYPE(TFPWSTD)     ,INTENT(IN) :: YDFPWSTD
TYPE(SL_STRUCT)   ,INTENT(IN) :: YDFPSTRUCT
CHARACTER(LEN = 1),INTENT(IN) :: CDCONF
LOGICAL           ,INTENT(IN) :: LDEZO
INTEGER(KIND=JPIM),INTENT(IN) :: KFPDOM
TYPE(GEOMETRY)    ,INTENT(IN) :: YDGEOMETRY
TYPE(TSURF)       ,INTENT(IN) :: YDSURF
TYPE(TCFU)        ,INTENT(IN) :: YDCFU
TYPE(TXFU)        ,INTENT(IN) :: YDXFU
TYPE(MODEL)       ,INTENT(IN) :: YDMODEL
REAL(KIND=JPRB)   ,INTENT(IN) :: PCFUBUF(YDGEOMETRY%YRDIM%NPROMA,YDCFU%NFDCFU,YDGEOMETRY%YRDIM%NGPBLKS)
REAL(KIND=JPRB)   ,INTENT(IN) :: PXFUBUF(YDGEOMETRY%YRDIM%NPROMA,YDXFU%NFDXFU,YDGEOMETRY%YRDIM%NGPBLKS)
INTEGER(KIND=JPIM),INTENT(IN) :: KP3LEVS
INTEGER(KIND=JPIM),INTENT(IN) :: KP3FIELDS
REAL(KIND=JPRB)   ,POINTER, INTENT(OUT) :: PBUF(:,:,:)
TYPE (FPOSBUF)    ,INTENT(IN), OPTIONAL :: YDAUX
REAL(KIND=JPRB)   ,TARGET, INTENT(INOUT), OPTIONAL :: P2DFIELDS(YDFPGEOMETRY%YFPGEO%NFPROMA,YDRQPHY%NFIELDG,YDFPGEOMETRY%YFPGEO%NFPBLOCS)
REAL(KIND=JPRB)   ,TARGET, INTENT(INOUT), OPTIONAL :: P3DFIELDS(YDFPGEOMETRY%YFPGEO%NFPROMA,KP3LEVS,KP3FIELDS,YDFPGEOMETRY%YFPGEO%NFPBLOCS)


REAL(KIND=JPRB), ALLOCATABLE :: ZHALO(:,:), ZBUF_DEP(:,:,:)
REAL(KIND=JPRB) :: ZCORE(YDGEOMETRY%YRDIM%NPROMA,YDRQPHY%NFIELDG), ZPARITY(YDRQPHY%NFIELDG)
INTEGER(KIND=JPIM) :: IORDER(YDRQPHY%NFIELDG), IDUMARR(2)
INTEGER(KIND=JPIM) :: IBL, IEND, JKGLO, JROF, JFLD, IOFF
LOGICAL :: LLINC, LLCTLCLIM, LLINTERPOL, LLBIP(YDRQPHY%NFIELDG)
CHARACTER(LEN = 1) :: CLCONF
TYPE(TYPE_FPRQDYN) :: YLQTYPE

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
#ifdef __INTEL_COMPILER
INTRINSIC :: SHAPE ! Fails with Intel compiler as it thinks we use ATLAS shape function
#endif

#include "scan2m_hpos.intfb.h"
#include "fptensor.intfb.h"
#include "fphalo.intfb.h"
#include "slcomm.intfb.h"
#include "slextpol.intfb.h"
#include "eslextpol.intfb.h"
#include "fposhorphy.intfb.h"
#include "fptrdtoa.intfb.h"
#include "fposhorlagphy.intfb.h"
#include "fpezo2h.intfb.h"
#include "fpboyd.intfb.h"
#include "suefpbip.intfb.h"
#include "abor1.intfb.h"

#include "hpossfx.h"
!-----------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('GRIDFPOS',0,ZHOOK_HANDLE)
ASSOCIATE(GFP_PHYDS=>YDAFN%GFP_PHYDS, YDRQAUX=>YDAUX%YRQPHY, &
 & YDFPGEO_DEP=>YDFPGEOMETRY%YFPGEO_DEP, YDFPGEO=>YDFPGEOMETRY%YFPGEO, YDFPGIND=>YDFPGEOMETRY%YFPGIND, &
 & YDFPUSERGEO=>YDFPGEOMETRY%YFPUSERGEO, LFPOSHOR=>YDFPGEOMETRY%LFPOSHOR, &
 & YDCLIMO=>YDFPDATA%YFPCLIMO,YDFPSUW=>YDFPDATA%YFPSUW, &
 & YDDIM=>YDGEOMETRY%YRDIM, YDGEM=>YDGEOMETRY%YRGEM)
ASSOCIATE(YDRQCLI=>YDCLIMO%YRQPHY, NFIELDG=>YDRQPHY%NFIELDG, NFPINPHY=>YDNAMFPINT%NFPINPHY, &
 & NFPBOYD=>YDNAMFPSCI%NFPBOYD, RFPBSCAL=>YDNAMFPSCI%RFPBSCAL, NFPLAKE=>YDNAMFPSCI%NFPLAKE, NFPCLI=>YDNAMFPSCI%NFPCLI, &
 & YDPHY=>YDMODEL%YRML_PHY_MF%YRPHY, YDPHY1=>YDMODEL%YRML_PHY_MF%YRPHY1, YDEPHY=>YDMODEL%YRML_PHY_EC%YREPHY, &
 & NPROMA=>YDDIM%NPROMA, NGPTOT=>YDGEM%NGPTOT, NGPBLKS=>YDDIM%NGPBLKS, &
 & NFPBLOCS_DEP=>YDFPGEO_DEP%NFPBLOCS, NFPROMA_DEP=>YDFPGEO_DEP%NFPROMA, &
 & NFPBLOCS=>YDFPGEO%NFPBLOCS, NFPROMA=>YDFPGEO%NFPROMA, &
 & MWIC=>YDFPSUW%MWIC)


IF (CDCONF == 'S') THEN
  CLCONF='P'
ELSE
  CLCONF=CDCONF
ENDIF
LLCTLCLIM=(CDCONF=='P')

WRITE(NULOUT,'('' GRIDFPOS : CONFIGURATION '',A1)') CDCONF

!*      5.2 Interpolations



IF (LFPOSBUF(YDFPGEOMETRY)) THEN

!*          NON-LAGGED PART OF COMPUTAIONS

  ZPARITY(:)=1.0_JPRB

  IF (LFPOSHOR) THEN
    ! Tensors aspects :
    DO JFLD=1,NFIELDG
      IORDER(JFLD)=ABS(GFP_PHYDS(YDRQPHY%ICOD(JFLD))%IORDR)
    ENDDO
  ENDIF

  ALLOCATE(ZHALO(YDFPSTRUCT%NASLB1,NFIELDG))

  CALL GSTATS(1434,0)
!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1) PRIVATE(JKGLO,IEND,IBL,JROF,JFLD,IOFF,ZCORE)
  DO JKGLO=1,NGPTOT,NPROMA

    IBL=(JKGLO-1)/NPROMA+1
    IEND=MIN(NPROMA,NGPTOT-JKGLO+1)

    IF (CDCONF == 'S') THEN
      DO JFLD=1,NFIELDG
        CALL HPOSSFX(NPROMA,IEND,JKGLO, GFP_PHYDS(YDRQPHY%ICOD(JFLD))%IPREP, ZCORE(:,JFLD))
      ENDDO
    ELSE
      CALL SCAN2M_HPOS(YDRQPHY,YDRQCLI,YDNAMFPSCI,YDAFN,LLCTLCLIM,JKGLO,IBL,IEND,LFPOSHOR, &
       & YDGEOMETRY,YDSURF,YDCFU,YDXFU,YDMODEL,PCFUBUF,PXFUBUF,PWSXI,PWDXI,ZCORE)
    ENDIF

    IF (LFPOSHOR) THEN
      CALL FPTENSOR(1,IEND,NPROMA,NFIELDG,IORDER,YDGEOMETRY%YRGSGEOM(IBL)%GM,ZCORE,ZPARITY)
    ENDIF
    CALL FPHALO(YDFPSTRUCT,NPROMA,NFIELDG,JKGLO,IEND,NFIELDG,ZCORE,ZHALO)
  ENDDO
!$OMP END PARALLEL DO
  CALL GSTATS(1434,1)

  IF (YDFPSTRUCT%NSLWIDE > 0) THEN

!*          COMMUNICATION BETWEEN PROCESSORS TO MAKE HALOS

    IF (NPROC > 1) THEN
      LLINC=.FALSE.
      CALL SLCOMM(YDFPSTRUCT,IDUMARR,NFIELDG,LLINC,0,ZHALO)
    ENDIF

!*          POLAR OR E-ZONE EXTENSIONS

    IF (LELAM) THEN
      CALL ESLEXTPOL(YDGEOMETRY,YDFPSTRUCT,NFIELDG,IDUMARR,1,ZHALO)
    ELSE
      CALL SLEXTPOL(YDGEOMETRY%YRDIM,YDFPSTRUCT,NFIELDG,IDUMARR,1,ZPARITY,ZHALO)
    ENDIF        
  ENDIF        

!*          HORIZONTAL INTERPOLATIONS

  IF (LFPDISTRIB(YDFPGEOMETRY)) THEN
    ALLOCATE(ZBUF_DEP(NFPROMA_DEP,NFIELDG,NFPBLOCS_DEP))
    CALL FPOSHORPHY(YDRQPHY,YDCLIMO,YDNAMFPINT,YDNAMFPSCI,YDAFN,YDFPGEOMETRY,YDFPSUW,YDFPWSTD,YDFPSTRUCT,YDFPGEO_DEP, &
     & YDRQPHY%NFIELDG,ZHALO,YDRQPHY%ICOD,ZBUF_DEP,YDRQAUX)
    DEALLOCATE(ZHALO)
    IF (PRESENT(P2DFIELDS)) THEN
      PBUF => P2DFIELDS
    ELSEIF (PRESENT(P3DFIELDS)) THEN
      IF (NFIELDG /= KP3LEVS*KP3FIELDS) THEN
        CALL ABOR1('GRIDFPOS : NFIELDG /= KP3LEVS*KP3FIELDS')
      ELSE
        PBUF(1:NFPROMA,1:NFIELDG,1:NFPBLOCS) => P3DFIELDS
      ENDIF
    ELSE
      ALLOCATE(PBUF(NFPROMA,NFIELDG,NFPBLOCS))
    ENDIF
    IF (LDEZO .AND. NFPBOYD /= 0 ) THEN
      CALL FPBOYD(RFPBSCAL,KFPXFLD,YDFPGEO_DEP,YDFPGIND,YDFPGEO,YDFPUSERGEO,ZBUF_DEP,PBUF)
    ELSE
      CALL FPTRDTOA(KFPXFLD,YDFPGEO_DEP,YDFPGIND,YDFPGEO,NFIELDG,ZBUF_DEP,PBUF)
    ENDIF
    DEALLOCATE(ZBUF_DEP)
  ELSE
    IF (PRESENT(P2DFIELDS)) THEN
      PBUF => P2DFIELDS
    ELSEIF (PRESENT(P3DFIELDS)) THEN
      IF (NFIELDG /= KP3LEVS*KP3FIELDS) THEN
        CALL ABOR1('GRIDFPOS : NFIELDG /= KP3LEVS*KP3FIELDS')
      ELSE
        PBUF(1:NFPROMA,1:NFIELDG,1:NFPBLOCS) => P3DFIELDS
      ENDIF
    ELSE
      ALLOCATE(PBUF(NFPROMA,NFIELDG,NFPBLOCS))
    ENDIF
    IF (LALLOFP) THEN
      WRITE(NULOUT,'(1X,''ARRAY '',A10,'' ALLOCATED '',8I8)') 'PBUF',SIZE(PBUF),SHAPE(PBUF)
    ENDIF
    CALL FPOSHORPHY(YDRQPHY,YDCLIMO,YDNAMFPINT,YDNAMFPSCI,YDAFN,YDFPGEOMETRY,YDFPSUW,YDFPWSTD,YDFPSTRUCT,YDFPGEO_DEP, &
     & YDRQPHY%NFIELDG,ZHALO,YDRQPHY%ICOD,PBUF,YDRQAUX)
    DEALLOCATE(ZHALO)
  ENDIF

ELSE

  IF (PRESENT(P2DFIELDS)) THEN
    PBUF => P2DFIELDS
  ELSEIF (PRESENT(P3DFIELDS)) THEN
    IF (NFIELDG /= KP3LEVS*KP3FIELDS) THEN
      CALL ABOR1('GRIDFPOS : NFIELDG /= KP3LEVS*KP3FIELDS')
    ELSE
      PBUF(1:NFPROMA,1:NFIELDG,1:NFPBLOCS) => P3DFIELDS
    ENDIF
  ELSE
    ALLOCATE(PBUF(NPROMA,NFIELDG,NGPBLKS))
  ENDIF
  IF (LALLOFP) THEN
    WRITE(NULOUT,'(1X,''ARRAY '',A10,'' ALLOCATED '',8I8)') 'PBUF',SIZE(PBUF),SHAPE(PBUF)
  ENDIF

  CALL GSTATS(1434,0)
!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1) PRIVATE(JKGLO,IEND,IBL,JFLD)
  DO JKGLO=1,NGPTOT,NPROMA

    IEND=MIN(NPROMA,NGPTOT-JKGLO+1)
    IBL=(JKGLO-1)/NPROMA+1

    IF (CDCONF == 'S') THEN
      DO JFLD=1,NFIELDG
        CALL HPOSSFX(NPROMA,IEND,JKGLO, GFP_PHYDS(YDRQPHY%ICOD(JFLD))%IPREP, PBUF(:,JFLD,IBL))
      ENDDO
    ELSE
      CALL SCAN2M_HPOS(YDRQPHY,YDRQCLI,YDNAMFPSCI,YDAFN,LLCTLCLIM,JKGLO,IBL,IEND,LFPOSHOR,YDGEOMETRY, &
       & YDSURF,YDCFU,YDXFU,YDMODEL,PCFUBUF,PXFUBUF,PWSXI,PWDXI,PBUF(:,:,IBL))
    ENDIF

  ENDDO
!$OMP END PARALLEL DO
  CALL GSTATS(1434,1)

ENDIF

LLINTERPOL=NFPINPHY > 0
IF (NFPLAKE /= 0) THEN
  CALL FPOSHORLAGPHY(YDRQPHY,YDCLIMO,PWSXI,PWDXI,YDNAMFPSCI,YDAFN,YDFPGEO,YDEPHY,YDPHY,YDMODEL%YRML_PHY_MF%YRPHY1,LLINTERPOL,PBUF,LFPOSHOR, &
   & KWIC=MWIC,YDAUX=YDAUX)
ELSE
  CALL FPOSHORLAGPHY(YDRQPHY,YDCLIMO,PWSXI,PWDXI,YDNAMFPSCI,YDAFN,YDFPGEO,YDEPHY,YDPHY,YDMODEL%YRML_PHY_MF%YRPHY1,LLINTERPOL,PBUF,LFPOSHOR, &
   & YDAUX=YDAUX)
ENDIF

IF (LFPOSBUF(YDFPGEOMETRY)) THEN
  IF (LDEZO .AND. NFPBOYD==0) THEN
    CALL SUEFPBIP(YDCLIMO,NFPCLI,YDAFN,NFIELDG,YDRQPHY%ICOD,CLCONF,LLBIP,YDRQAUX)
    IF (ANY(LLBIP(:))) THEN
      CALL FPEZO2H(YLQTYPE,NFIELDG,YDRQPHY%ICOD,LLBIP,YDCLIMO,PWSXI,PWDXI,YDNAMFPSCI,YDAFN,KFPXFLD,YDFPGEO,YDEPHY,YDMODEL%YRML_PHY_MF,YDFPUSERGEO,CLCONF, & 
       & LFPOSHOR,PBUF,YDAUX)
    ENDIF
  ENDIF
ENDIF

! ---------------------------------------------------------------------
END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('GRIDFPOS',1,ZHOOK_HANDLE)
END SUBROUTINE GRIDFPOS
