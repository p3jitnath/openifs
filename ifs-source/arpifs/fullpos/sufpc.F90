  IF (PRESENT(LDNAMELIST)) THEN
    LLPPNAMELIST=LDNAMELIST
  ELSE
    LLPPNAMELIST=.FALSE.
  ENDIF
  IF (LLPPNAMELIST) THEN
    REWIND(NPPPSH)
    DO WHILE(LLPPNAMELIST)
      READ(NPPPSH,'(A)',END=900) CLFILE
      IF(CLFILE == CDPATH) THEN
        READ(NPPPSH,NAMFPC)
        GOTO 910
      ENDIF
    ENDDO
    900 CONTINUE
    WRITE(CLFILE,FMT='('' NO NAMELIST ENTRY FOUND IN ppnamelist '')')
    WRITE(NULOUT,'(A)') CLFILE
    CALL ABOR1(CLFILE)
  ELSE
    ! CDPATH might be already opened by nulnam :
    INQUIRE(UNIT=NULNAM,OPENED=LLOPENED,NAME=CLFILE)
    IF (LEN(CLFILE) < LEN(CDPATH)) THEN
      CALL ABOR1('SUFPC INTERNAL ERROR : LEN(CLFILE) TOO SMALL !')
    ENDIF
    IF (LLOPENED.AND.TRIM(CLFILE)==TRIM(CDPATH)) THEN
      CALL POSNAM(NULNAM,'NAMFPC')
      READ(NULNAM,NAMFPC)
    ELSE
      OPEN(NPPPSH,FILE=CDPATH,ACTION='READ')
      CALL POSNAM(NPPPSH,'NAMFPC')
      READ(NPPPSH,NAMFPC)
      CLOSE(NPPPSH)
    ENDIF
  ENDIF
  910 CONTINUE

ENDIF


