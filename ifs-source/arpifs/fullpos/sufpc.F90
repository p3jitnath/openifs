      WRITE(UNIT=NULOUT,FMT='('' RWIDOM = '',E15.7)') RWIDOM
    ENDIF
  ENDIF

  IF (NFPOS /= 0) THEN
    IF (LFPMOIS .AND. NSUPERSEDE /= 1) THEN
     ! A helpful restriction :
      CALL ABOR1('SUFPC :LFPMOIS==T NEEDS NSUPERSEDE==1')
    ENDIF
  ENDIF

ENDIF

!       4.1 NAMFPOBJ (Fields object dependent parameters)

IF (PRESENT(YDNAMFPOBJ)) THEN

  IF (LLPRINT) THEN
    WRITE(UNIT=NULOUT,FMT='('' NFPGRIB = '',I3)') NFPGRIB
  ENDIF
  IF (NFPGRIB == 3) THEN
    WRITE(NULOUT,*) 'WARNING : GRIBEX TABLES ARE STILL HARD-CODED FOR FULL-POS FIELDS'
  ELSEIF ((NFPGRIB < -1 .OR.NFPGRIB > 3) .AND. (NFPGRIB < 100 .OR. NFPGRIB > 200)) THEN
    CALL ABOR1('SUFPC : ILLEGAL VALUE NFPGRIB')
  ENDIF

  IF (PRESENT(YDNAMFPSCI)) THEN
    IF (CFPFMT == 'MODEL' .AND. ICONF /= 2) THEN
      IF (PRESENT(KFPCONF)) THEN
        CALL ABOR1('SUFPC : CFPFMT=MODEL REQUIRES NFPCONF=2')
      ELSE
        CALL ABOR1('SUFPC : CFPFMT=MODEL REQUIRES NFPOS=2')
      ENDIF
    ENDIF
    IF (CFPFMT == 'MODEL'.AND. NFPCLI /= 0) THEN
      CALL ABOR1('SUFPC : CFPFMT=MODEL NOT COMPATIBLE WITH NFPCLI /=0')
    ENDIF
  ENDIF

  ! When everything is OK, save the structure
  YDNAMFPOBJ=YLNAMFPOBJ

ENDIF


!       4.2 NAMFPSCI (scientific parameters)

IF (PRESENT(YDNAMFPSCI)) THEN

  NFPSURFEX=MAX(0,MIN(NFPSURFEX,2))
  NFPSWI=MIN(MAX(0,NFPSWI),2)
  IF (NFPSWI==2) THEN
    NFPCLI=3
  ELSE
    NFPCLI=MAX(0,MIN(NFPCLI,3))
  ENDIF

  ! NFIT* validity
  IF (ICONF==1) THEN
  ! Spectral fit for terrain-following levels on model geometry is excluded ; => reset to 0 if not 2 :
    IF (NFITS==1) NFITS=0
    IF (NFITH==1) NFITH=0
    IF (NFITI==1) NFITI=0
    IF (NFITF==1) NFITF=0
  ELSE
  ! Spectral fit on model geometry is excluded ; => change to 2 if not 0 :
    IF (NFITP /= 0) NFITP=2
    IF (NFITT /= 0) NFITT=2
    IF (NFITV /= 0) NFITV=2
    IF (NFITI /= 0) NFITI=2
    IF (NFITS /= 0) NFITS=2
    IF (NFITH /= 0) NFITH=2
    IF (NFITF /= 0) NFITF=2
  ENDIF

  IF (CFPFMT=='MODEL') THEN
  ! No "stretched" filters
    IFPFILMAX=2
  ELSE
    IFPFILMAX=3
  ENDIF
  NSPFILP=MIN(IFPFILMAX,MAX(1,NSPFILP))
  NSPFILT=MIN(IFPFILMAX,MAX(1,NSPFILT))
  NSPFILV=MIN(IFPFILMAX,MAX(1,NSPFILV))
  NSPFILI=MIN(IFPFILMAX,MAX(1,NSPFILI))
  NSPFILF=MIN(IFPFILMAX,MAX(1,NSPFILF))
  NSPFILH=MIN(IFPFILMAX,MAX(1,NSPFILH))
  NSPFILS=MIN(IFPFILMAX,MAX(1,NSPFILS))
  NSPFIL2=MIN(IFPFILMAX,MAX(1,NSPFIL2))

  IF (LLPRINT) THEN
    WRITE(UNIT=NULOUT,FMT='('' NFPLAKE = '',I2,'' NFPCLI = '',I1,'' NFPMASK = '',I3,'' NFPSURFEX = '',I2)') &
     & NFPLAKE, NFPCLI, NFPMASK, NFPSURFEX
    WRITE(UNIT=NULOUT,FMT='('' LFPCAPEX = '',L2,'' NFPCAPE = '',I3)') LFPCAPEX, NFPCAPE
    WRITE(UNIT=NULOUT,FMT='('' LFPISOPV = '',L2,'' NITERPV = '',I3)') LFPISOPV, NITERPV
    WRITE(UNIT=NULOUT,FMT='('' RSTRMMH = '',F9.2,'' RSRHH = '',F9.2)') RSTRMMH,RSRHH
    WRITE(UNIT=NULOUT,FMT='('' LPUTZS = '',L2,'' LCRITSNOWTEMP = '',L2)') LPUTZS, LCRITSNOWTEMP
    WRITE(UNIT=NULOUT,FMT='('' LISOT_ABOVEG = '',L2)') LISOT_ABOVEG
    WRITE(UNIT=NULOUT,FMT='('' LFPQ = '',L2,'' LFPLOSP = '',L2,'' LFPRH100 = '',L2,'' RFPCSAB = '',F9.6, &
     & '' RFPCD2  = '',F9.4,'' RFPCORR = '',F9.2)') LFPQ, LFPLOSP, LFPRH100,RFPCSAB,RFPCD2,RFPCORR
    WRITE(UNIT=NULOUT,FMT='('' RENTRA  = '',E13.6 )') RENTRA
    WRITE(UNIT=NULOUT,FMT='('' FPRHMIN  = '',E13.6,'' FPRHMAX  = '',E13.6 )') FPRHMIN,FPRHMAX
    WRITE (NULOUT,'('' NFPBOYD = '',I2,'' RFPBSCAL = '',E13.6)') NFPBOYD, RFPBSCAL
    WRITE(UNIT=NULOUT,FMT='('' NFPSWI = '',I3,'' TSRESERV1  = '',E13.6,'' TSRESERV2  = '',E13.6,&
     & '' TDELTA1  = '',E13.6,'' TDELTA2  = '',E13.6 )') NFPSWI,TSRESERV1,TSRESERV2,TDELTA1,TDELTA2
    WRITE(UNIT=NULOUT,FMT='('' RWPITPN  = '',E13.6,'' RWPITPX  = '',E13.6,&
     & '' RSNSTPN  = '',E13.6,'' RSNSTPX  = '',E13.6, '' RSNSMOD '',E13.6)') &
     & RWPITPN,RWPITPX, RSNSTPN, RSNSTPX, RSNSMOD
    WRITE(UNIT=NULOUT,FMT='('' LSATURCAP = '',L2, '' LFITBETWEEN = '',L2)')  LSATURCAP, LFITBETWEEN
    WRITE(UNIT=NULOUT,FMT='('' NFITP = '',I2,'' NFITT = '',I2,'' NFITV = '',I2,&
     & '' NFITI = '',I2,'' NFITH = '',I2,'' NFITS = '',I2,'' NFITF = '',I2)')&
     & NFITP, NFITT, NFITV,NFITI, NFITH, NFITS, NFITF
    WRITE(UNIT=NULOUT,FMT='('' NSPFILP = '',I2,'' NSPFILT = '',I2,'' NSPFILV = '', &
     & I2,'' NSPFILI = '',I2,'' NSPFILH = '',I2, '' NSPFILS = '',I2, '' NSPFILF = '',I2, &
     & '' NSPFIL2 = '',I2)') &
     & NSPFILP, NSPFILT, NSPFILV, NSPFILI, NSPFILH, NSPFILS, NSPFILF, NSPFIL2
    IF (NFPCLI > 1) THEN
      WRITE(UNIT=NULOUT,FMT='('' LCLIMALBEDOS = '',L2,'' LCLIMAEROSOL = '',L2,'' LCLIMOZONE = '',L2)') &
       & LCLIMALBEDOS, LCLIMAEROSOL, LCLIMOZONE
    ENDIF
  ENDIF

  IERR=0
  IF (NFPCAPE < 0.OR.NFPCAPE > 6.OR.NFPCAPE==4) THEN
    IERR=IERR+1
    WRITE(NULOUT,*) 'ILLEGAL VALUE NFPCAPE'
  ENDIF
  IF (NFPMASK < 0 .OR. NFPMASK > 2) THEN
    IERR=IERR+1
    WRITE(NULOUT,*) 'ILLEGAL VALUE NFPMASK'
  ENDIF
  IF (NFPMASK >= 2 .AND. NFPCLI > 2) THEN
    IERR=IERR+1
    WRITE(NULOUT,*) 'NFPMASK >= 2 NEEDS NFPCLI <= 2'
  ENDIF
  IF (ABS(NFPLAKE) > 1) THEN
    IERR=IERR+1
    WRITE(NULOUT,*) 'ILLEGAL VALUE NFPLAKE'
  ENDIF
  IF (NFPLAKE == 1 .AND. NFPCLI <= 1) THEN
    IERR=IERR+1
    WRITE(NULOUT,*) 'NFPLAKE = 1 NEEDS NFPCLI > 1'
  ENDIF
  IF (NFPLAKE /= 0 .AND. NFPMASK == 0) THEN
    IERR=IERR+1
    WRITE(NULOUT,*) 'NFPLAKE /= 0 NEEDS NFPMASK > 0'
  ENDIF
  IF (IERR > 0) THEN
    CALL ABOR1('SUFPC : ABOR1 CALLED')
  ENDIF

  ! When everything is OK, save the structure
  YDNAMFPSCI=YLNAMFPSCI

ENDIF


!       4.3 NAMFPINT (Horizontal interpolations parameters)

IF (PRESENT(YDNAMFPINT)) THEN

  IWARN=0
  IF (NFPINDYN /= 0 .AND. NFPINDYN /= 4 .AND. NFPINDYN /= 12) THEN
    NFPINDYN=12
    IWARN=IWARN+1
  ENDIF
  IF (NFPINPHY /= 0 .AND. NFPINPHY /= 4 .AND. NFPINPHY /= 12) THEN
    NFPINPHY=12
    IWARN=IWARN+1
  ENDIF
  IF (IWARN /= 0) WRITE(NULOUT,*) 'FULL-POS WARNING : UNKNOWN INTERPOLATION TYPE, RESET TO 12 POINTS'

  IF (LLPRINT) THEN
    WRITE(UNIT=NULOUT,FMT='('' LFPML_STD = '',L2,'' RFPCR_STD(1)='',E20.14,'', RFPCR_STD(2)='',E20.14)') &
     & LFPML_STD,RFPCR_STD(1),RFPCR_STD(2)
    WRITE(UNIT=NULOUT,FMT='('' LFPML_LAN = '',L2,'' RFPCR_LAN(1)='',E20.14,'', RFPCR_LAN(2)='',E20.14)') &
     & LFPML_LAN,RFPCR_LAN(1),RFPCR_LAN(2)
    WRITE(UNIT=NULOUT,FMT='('' LFPML_SEA = '',L2,'' RFPCR_SEA(1)='',E20.14,'', RFPCR_SEA(2)='',E20.14)') &
     & LFPML_SEA,RFPCR_SEA(1),RFPCR_SEA(2)
    WRITE(UNIT=NULOUT,FMT='(I3,'' POINTS INTERP. FOR DYN. FIELDS'')') NFPINDYN
    WRITE(UNIT=NULOUT,FMT='(I3,'' POINTS INTERP. FOR PHY. FIELDS'')') NFPINPHY
    WRITE(UNIT=NULOUT,FMT='('' NFPSFXINT = '',I2)') NFPSFXINT
  ENDIF

  IF (NFPSFXINT < 0 .OR. NFPSFXINT > 2) THEN
    CALL ABOR1('SUFPC : NFPSFXINT MUST BE EITHER 0, 1 OR 2')
  ENDIF

  ! When everything is OK, save the structure
  YDNAMFPINT=YLNAMFPINT

ENDIF


!       4.3 NAMFPL (List of output fields)
IF (PRESENT(YDNAMFPL)) THEN

  IF (.NOT.LARPEGEF) THEN

    IF (LLPRINT) THEN
      IF (NFP3DFS > 0) THEN
        WRITE(NULOUT,'('' THE FOLLOWING '',I6,'' 3DYN. FIELDS'','' WILL BE WRITTEN OUT ON MODEL LEVELS'')')NFP3DFS
        WRITE(NULOUT,'(20(1X,I6))') (MFP3DFS(J),J=1,NFP3DFS)
      ENDIF
      IF (NFP3DFH > 0) THEN
        WRITE(NULOUT,'('' THE FOLLOWING '',I6,'' 3DYN. FIELDS'','' WILL BE WRITTEN OUT ON H LEVELS'')')NFP3DFH
        WRITE(NULOUT,'(20(1X,I3))') (MFP3DFH(J),J=1,NFP3DFH)
      ENDIF
      IF (NFP3DFP > 0) THEN
        WRITE(NULOUT,'('' THE FOLLOWING '',I6,'' 3DYN. FIELDS'','' WILL BE WRITTEN OUT ON PRESSURE LEVELS'')')NFP3DFP
        WRITE(NULOUT,'(20(1X,I6))') (MFP3DFP(J),J=1,NFP3DFP)
      ENDIF
      IF (NFP3DFT > 0) THEN
        WRITE(NULOUT,'('' THE FOLLOWING '',I6,'' 3DYN. FIELDS'','' WILL BE WRITTEN OUT ON THETA LEVELS'')')NFP3DFT
        WRITE(NULOUT,'(20(1X,I3))') (MFP3DFT(J),J=1,NFP3DFT)
      ENDIF
      IF (NFP3DFV > 0) THEN
        WRITE(NULOUT,'('' THE FOLLOWING '',I6,'' 3DYN. FIELDS'','' WILL BE WRITTEN OUT ON PV LEVELS'')')NFP3DFV
        WRITE(NULOUT,'(20(1X,I3))') (MFP3DFV(J),J=1,NFP3DFV)
      ENDIF
      IF (NFP3DFI > 0) THEN
        WRITE(NULOUT,'('' THE FOLLOWING '',I6,'' 3DYN. FIELDS'','' WILL BE WRITTEN OUT ON TEMPERATURE LEVELS'')')NFP3DFI
        WRITE(NULOUT,'(20(1X,I3))') (MFP3DFI(J),J=1,NFP3DFI)
      ENDIF
      IF (NFP3DFF > 0) THEN
        WRITE(NULOUT,'('' THE FOLLOWING '',I6,'' 3DYN. FIELDS'','' WILL BE WRITTEN OUT ON FLIGHT LEVELS'')')NFP3DFF
        WRITE(NULOUT,'(20(1X,I3))') (MFP3DFF(J),J=1,NFP3DFF)
      ENDIF
      IF (NFP2DF > 0) THEN
        WRITE(NULOUT,'('' THE FOLLOWING '',I3,'' DYN.SURFACE FIELDS'','' WILL BE WRITTEN OUT '')')NFP2DF
        WRITE(NULOUT,'(20(1X,I3))') (MFP2DF(J),J=1,NFP2DF)
      ENDIF
      IF (NFPPHY > 0) THEN
        WRITE(NULOUT,'('' THE FOLLOWING '',I6,'' PHY.SURFACE FIELDS'','' WILL BE WRITTEN OUT '')')NFPPHY
        WRITE(NULOUT,'(20(1X,I6))') (MFPPHY(J),J=1,NFPPHY)
      ENDIF
    ENDIF

    IERR=0
    DO J=1, NFPPHY
      IF (MFPPHY(J) < 1) THEN
        WRITE(NULOUT,*) 'SUFPC: ILLEGAL GRIB CODE FOR PHYSICS FIELD NO',J,MFPPHY(J)
        IERR=IERR+1
      ENDIF
    ENDDO
    IF (IERR > 0) THEN
      CALL ABOR1(' SUFPC: ABOR1 CALLED')
    ENDIF

  ENDIF

  ! When everything is OK, save the structure
  YDNAMFPL=YLNAMFPL


ENDIF


!     ------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('SUFPC',1,ZHOOK_HANDLE)
END SUBROUTINE SUFPC
