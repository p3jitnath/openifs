SUBROUTINE SUXFU(YDGEOMETRY,YDXFU,YDRIP,YDPHY,KULOUT)

!**** *SUXFU  * - ROUTINE TO INITIALIZE THE CONTROL OF INSTANTANEOUS FLUXES

!     PURPOSE.
!     --------
!        SET DEFAULT VALUES, THEN READS NAMELIST NAMXFU AND INITIALIZE POINTERS

!**   INTERFACE.
!     ----------
!        *CALL* *SUXFU(...)*

!     EXPLICIT ARGUMENTS :  KULOUT
!     --------------------

!     IMPLICIT ARGUMENTS :
!     --------------------
!        COMMON  YOMCFU

!     METHOD.
!     -------

!     EXTERNALS.
!     ----------

!     REFERENCE.
!     ----------
!        ECMWF RESEARCH DEPARTMENT DOCUMENTATION OF THE IFS

!     AUTHOR.
!     -------
!      O.CLARY 06/92
!      ORIGINAL : 06/92

!     MODIFICATIONS.
!     --------------
!      R. El Khatib : 01-04-23 LXQCLS,HUn,HUx
!      R. El Khatib : 01-08-07 pruning options
!      R. El Khatib : 03-04-17 Fullpos improvments
!      M.Hamrud     : 01-Oct-2003 CY28 Cleaning
!      Y.Bouteloup  : 26-Mars-2004 introduction of MTS radiance
!      F.Bouyssel   : 04-09-15 LXFU forced to false if .NOT.LMPHYS
!      R. El Khatib : 05-02-21 specific frequency array to reset extreme T2m and HU2m
!      F.Bouyssel   : 05-11-10 Introduction of  QICE and QLI
!      Y.Seity      : 07-01-15  add Graupel and Hail in XFU
!      S. Riette    : 09-05-05 extreme gusts and mean wind added
!      S. Riette    : 24-02-2010 extreme gusts over NXGSTPERIOD seconds
!      Y.Seity      : 04-01-11  add Hail diagnostic in XFU
!      R. El Khatib : 10-Aug-2011 NIOLEVG management
!      R. El Khatib : 01-Mar-2012 LFPOS => NFPOS
!      T. Wilhelmsson (Sept 2013) Geometry and setup refactoring.
!      R. Brozkova  : 07-2018 Added mean radiant temperature.
!      I. Etchevers : 04-2019 Add Visibilities, Ptype (frequent and severe) and T'W at 2 meters
!-----------------------------------------------------------------------

USE YOMRIP       , ONLY : TRIP
USE YOMXFU       , ONLY : TXFU, JPFUXT
USE GEOMETRY_MOD , ONLY : GEOMETRY
USE PARKIND1     , ONLY : JPIM     ,JPRB
USE YOMHOOK      , ONLY : LHOOK    ,DR_HOOK, JPHOOK
USE YOMLUN       , ONLY : NULNAM
USE YOMPHY       , ONLY : TPHY
USE YOMCT0       , ONLY : NFPOS    ,LECMWF, NCONF
USE YOMFA        , ONLY : YFA_X10U ,YFA_X10V ,YFA_X10NU,YFA_X10NV,&
 &                        YFA_X2RH ,YFA_X2SH ,YFA_X2T  ,YFA_X2TPW ,YFA_XCAPE,&
 &                        YFA_XCTOP,YFA_XCCC ,YFA_XCLPH,YFA_XCP  ,YFA_XCSF ,YFA_XN2HU,&
 &                        YFA_XCVQ ,YFA_XCVS ,YFA_XCVU ,YFA_XCVV ,YFA_XGDU ,YFA_XGDV ,&
 &                        YFA_XHCC ,YFA_XLCC ,YFA_XLSP ,YFA_XLSS ,YFA_XLSG ,YFA_XCSG ,&
 &                        YFA_XLSH ,YFA_XCSH ,YFA_XMCC ,YFA_XX2HU,YFA_XTHW ,YFA_XXDIAGH,&
 &                        YFA_XMOCO,YFA_XN2T ,YFA_XSSR ,YFA_XSTR ,YFA_XTCC ,YFA_XTUQ ,&
 &                        YFA_XTUS ,YFA_XTUU ,YFA_XTUV ,YFA_XUGST,YFA_XVGST,&
 &                        YFA_XUGST2 ,YFA_XVGST2 ,YFA_XX2T ,YFA_XVEIN ,YFA_XMRT,&
 &                        YFA_VISICLD ,YFA_VISIHYD ,YFA_MXCLWC ,YFA_XPTYPE ,YFA_XPTYPESEV,&
 &                        YFA_VISICLD2,YFA_VISIHYD2,YFA_MXCLWC2,YFA_XPTYPE2,YFA_XPTYPESEV2

IMPLICIT NONE

TYPE(GEOMETRY)    , INTENT(INOUT) :: YDGEOMETRY
TYPE(TXFU),TARGET , INTENT(INOUT) :: YDXFU
TYPE(TRIP)        , INTENT(INOUT) :: YDRIP
TYPE(TPHY)        , INTENT(INOUT) :: YDPHY
INTEGER(KIND=JPIM), INTENT(IN)    :: KULOUT 

INTEGER(KIND=JPIM), PARAMETER :: JPKIND=6
INTEGER(KIND=JPIM) :: IP, J, IPAR, IK, INF550, INF850, JF, JL, IADD
INTEGER(KIND=JPIM) :: INLEV(YDGEOMETRY%YRDIMV%NFLEVG+1,JPKIND), ILEV(JPKIND)

CHARACTER(LEN=4) :: CLPRE(YDGEOMETRY%YRDIMV%NFLEVG+1,JPKIND), CLEVS(1)
CHARACTER(LEN=16) :: CLSU
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

LOGICAL, POINTER :: LXFU, LXTRD, LXTRC, LXTRT, LXPLC, LXPLCG, LXPLCH, LXPLS,&
 & LXPLSG, LXPLSH, LXR, LXNEBTT, LXNEBPA, LXCLS, LXTTCLS,LXHHCLS, LXTPWCLS, LXQCLS,&
 & LXSOIL, LTXTRD, LTXTRC, LTXTRT, LTXR, LTXNEB, LTXQICE, LTXQLI, LXICV, LXCTOP,&
 & LXCLP, LXTGST, LXXGST, LXXGST2, LXTHW, LXXDIAGH, LXMWINDCLS, LXNUVCLS,&
 & LXVEIN, LXMRT, LXVISI, LXVISI2
INTEGER(KIND=JPIM), POINTER :: NFRXFU, NXFUTS(:), NFRRAZ, NRAZTS(:), N1RAZ,&
 & MEANPERIOD, NXGSTPERIOD, NXGSTPERIOD2, NVISIPERIOD,NVISIPERIOD2

#include "namxfu.nam.h"

#include "posnam.intfb.h"
#include "seapre.intfb.h"
#include "sufpxfu.intfb.h"

!      ----------------------------------------------------------------

!*       1.    SET DEFAULT VALUES.
!              -------------------

!        1.1 Set implicit default values

IF (LHOOK) CALL DR_HOOK('SUXFU',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM=>YDGEOMETRY%YRDIM, &
  & YDDIMV=>YDGEOMETRY%YRDIMV,YDGEM=>YDGEOMETRY%YRGEM, &
  & YDMP=>YDGEOMETRY%YRMP, YDSTA=>YDGEOMETRY%YRSTA)
ASSOCIATE(NGPTOT=>YDGEM%NGPTOT, &
 & STPRE=>YDSTA%STPRE, & 
 & LREAXFU=>YDXFU%LREAXFU, &
 & NFDXFU=>YDXFU%NFDXFU, NMEANSTEPS=>YDXFU%NMEANSTEPS, &
 & NTYPXFU=>YDXFU%NTYPXFU, &
 & TYPE_XFU=>YDXFU%TYPE_XFU, &
 & NIOLEVG=>YDDIMV%NIOLEVG, NFLEVG=>YDDIMV%NFLEVG, &
 & LMPHYS=>YDPHY%LMPHYS, LNEIGE=>YDPHY%LNEIGE, &
 & LSOLV=>YDPHY%LSOLV,TSTEP=>YDRIP%TSTEP,&
 & LDPRECIPS=>YDPHY%LDPRECIPS,LDPRECIPS2=>YDPHY%LDPRECIPS2)
! Associate pointers for variables in namelist
LXFU        => YDXFU%LXFU
NFRXFU      => YDXFU%NFRXFU
NXFUTS      => YDXFU%NXFUTS
NFRRAZ      => YDXFU%NFRRAZ
NRAZTS      => YDXFU%NRAZTS
N1RAZ       => YDXFU%N1RAZ
LXTRD       => YDXFU%LXTRD
LXTRC       => YDXFU%LXTRC
LXTRT       => YDXFU%LXTRT
LXPLC       => YDXFU%LXPLC
LXPLCG      => YDXFU%LXPLCG
LXPLCH      => YDXFU%LXPLCH
LXPLS       => YDXFU%LXPLS
LXPLSG      => YDXFU%LXPLSG
LXPLSH      => YDXFU%LXPLSH
LXR         => YDXFU%LXR
LXNEBTT     => YDXFU%LXNEBTT
LXNEBPA     => YDXFU%LXNEBPA
LXCLS       => YDXFU%LXCLS
LXNUVCLS    => YDXFU%LXNUVCLS
LXTTCLS     => YDXFU%LXTTCLS
LXHHCLS     => YDXFU%LXHHCLS
LXTPWCLS    => YDXFU%LXTPWCLS
LXQCLS      => YDXFU%LXQCLS
LXSOIL      => YDXFU%LXSOIL
LTXTRD      => YDXFU%LTXTRD
LTXTRC      => YDXFU%LTXTRC
LTXTRT      => YDXFU%LTXTRT
LTXR        => YDXFU%LTXR
LTXNEB      => YDXFU%LTXNEB
LTXQICE     => YDXFU%LTXQICE
LTXQLI      => YDXFU%LTXQLI
LXICV       => YDXFU%LXICV
LXCTOP      => YDXFU%LXCTOP
LXCLP       => YDXFU%LXCLP
LXVEIN      => YDXFU%LXVEIN
LXMRT       => YDXFU%LXMRT
LXTGST      => YDXFU%LXTGST
LXXGST      => YDXFU%LXXGST
LXXGST2     => YDXFU%LXXGST2
LXTHW       => YDXFU%LXTHW
LXXDIAGH    => YDXFU%LXXDIAGH
LXMWINDCLS  => YDXFU%LXMWINDCLS
MEANPERIOD  => YDXFU%MEANPERIOD
NXGSTPERIOD => YDXFU%NXGSTPERIOD
NXGSTPERIOD2=> YDXFU%NXGSTPERIOD2
LXVISI      => YDXFU%LXVISI
LXVISI2     => YDXFU%LXVISI2
NVISIPERIOD => YDXFU%NVISIPERIOD
NVISIPERIOD2=> YDXFU%NVISIPERIOD2

LXFU   = .FALSE.
LREAXFU= .FALSE.
LXTRD  = .FALSE.
LTXTRD = .FALSE.
LXTRC  = .FALSE.
LTXTRC = .FALSE.
LXTRT  = .FALSE.
LTXTRT = .FALSE.
LXPLC  = .FALSE.
LXPLCH = .FALSE.
LXPLCG = .FALSE.
LXPLS  = .FALSE.
LXPLSH = .FALSE.
LXPLSG = .FALSE.
LXR    = .FALSE.
LTXR   = .FALSE.
LTXNEB = .FALSE.
LTXQICE= .FALSE.
LTXQLI = .FALSE.
LXNEBTT= .FALSE.
LXNEBPA= .FALSE.
LXSOIL = .FALSE.
LXICV  = .FALSE.
LXCTOP = .FALSE.
LXCLS  = .FALSE.
LXNUVCLS = .FALSE.
LXMWINDCLS = .FALSE.
LXQCLS = .FALSE.
LXTTCLS= .FALSE.
LXHHCLS= .FALSE.
LXTPWCLS= .FALSE.
LXCLP  = .FALSE.
LXVEIN = .FALSE.
LXMRT  = .FALSE.
LXTGST = .FALSE.
LXXGST = .FALSE.
LXXGST2 = .FALSE.
LXTHW  = .FALSE.
LXXDIAGH= .FALSE.
LXVISI = .FALSE.
LXVISI2= .FALSE.

DO J=0,JPFUXT
  NXFUTS(J)=0
  NRAZTS(J)=0
ENDDO
NFRXFU   =20
NFRRAZ   =20
N1RAZ    =1
NMEANSTEPS=1_JPIM
MEANPERIOD=600_JPIM
NXGSTPERIOD=3600_JPIM ! max gusts calculation over one hour until next IHISTS event...
NXGSTPERIOD2=900_JPIM ! max gusts calculation over 15 minutes (reset every 15')
NVISIPERIOD=3600_JPIM ! min visi and max CLWC over one hour
NVISIPERIOD2=900_JPIM ! min visi and max CLWC over 15 minutes

!        1.2 Modify default values according to LECMWF

IF (LECMWF) THEN
ELSE
  NFRXFU=9999999
  NXFUTS(0)=1
  NXFUTS(1)=1
  NRAZTS(0)=-27
! Reset every 3 hours until H54, then every 6 hours.
  NFRRAZ=1
  NRAZTS(1)=-0
  NRAZTS(2)=-3
  NRAZTS(3)=-6
  NRAZTS(4)=-9
  NRAZTS(5)=-12
  NRAZTS(6)=-15
  NRAZTS(7)=-18
  NRAZTS(8)=-21
  NRAZTS(9)=-24
  NRAZTS(10)=-27
  NRAZTS(11)=-30
  NRAZTS(12)=-33
  NRAZTS(13)=-36
  NRAZTS(14)=-39
  NRAZTS(15)=-42
  NRAZTS(16)=-45
  NRAZTS(17)=-48
  NRAZTS(18)=-51
  NRAZTS(19)=-54
  NRAZTS(20)=-60
  NRAZTS(21)=-66
  NRAZTS(22)=-72
  NRAZTS(23)=-78
  NRAZTS(24)=-84
  NRAZTS(25)=-90
  NRAZTS(26)=-96
  NRAZTS(27)=-102
ENDIF

!      ----------------------------------------------------------------

!*       2.    MODIFIES DEFAULT VALUES.
!              ------------------------

CALL POSNAM(NULNAM,'NAMXFU')
READ(NULNAM,NAMXFU)

IF (NFPOS /= 0) THEN
  CALL SUFPXFU(YDXFU,YDRIP,YDPHY)
ENDIF
IF (LXFU .AND. .NOT.LMPHYS) THEN
  LXFU=.FALSE.
ENDIF
   
LXTRD  =LXTRD  .AND.LXFU
LXTRC  =LXTRC  .AND.LXFU
LXTRT  =LXTRT  .AND.LXFU
LXPLC  =LXPLC  .AND.LXFU
LXPLS  =LXPLS  .AND.LXFU
LXR    =LXR    .AND.LXFU
LTXNEB =LTXNEB .AND.LXFU
LTXQICE=LTXQICE.AND.LXFU
LTXQLI =LTXQLI .AND.LXFU
LXNEBTT=LXNEBTT.AND.LXFU
LXNEBPA=LXNEBPA.AND.LXFU
LXSOIL =LXSOIL .AND.LXFU
LXICV  =LXICV  .AND.LXFU 
LXCTOP =LXCTOP .AND.LXFU 
LXCLS  =LXCLS  .AND.LXFU
LXNUVCLS=LXNUVCLS  .AND.LXFU
LXTTCLS=LXTTCLS.AND.LXFU
LXQCLS =LXQCLS .AND.LXFU
LXHHCLS=LXHHCLS.AND.LXFU
LXTPWCLS=LXTPWCLS.AND.LXFU
LXCLP  =LXCLP  .AND.LXFU
LXVEIN =LXVEIN .AND.LXFU
LXMRT  =LXMRT  .AND.LXFU
LXTGST =LXTGST .AND.LXFU
LXXGST =LXXGST .AND.LXFU
LXXGST2=LXXGST2.AND.LXFU
LXVISI =LXVISI .AND.LXFU
LXVISI2=LXVISI2.AND.LXFU
LXXDIAGH=LXXDIAGH.AND.LXFU
!mean winds require instanteneous winds
LXMWINDCLS=LXMWINDCLS.AND.(LXCLS.OR.LXNUVCLS)
IF (NCONF /= 903) THEN
  ! For compatibility with older files, in offline pp, a field should be deactivated unless explicitely requested.
  ! However, the activation of basic CLS fields will trigger tpwcls in other cases :
  LXTPWCLS=LXCLS.AND.LXFU
ENDIF

IF(LXXGST.AND.MOD(TSTEP,REAL(NXGSTPERIOD))/=0.) THEN
  WRITE(KULOUT,*)'WARNING NGSTPERIOD DO NOT EXACTLY DIVIDE TSTEP'
  WRITE(KULOUT,*)'MAX GUST CALCULATION MAY BE WRONG',&
                       &MOD(TSTEP,REAL(NXGSTPERIOD))
ENDIF
IF(LXXGST2.AND.MOD(TSTEP,REAL(NXGSTPERIOD2))/=0.) THEN
  WRITE(KULOUT,*)'WARNING NGSTPERIOD2 DO NOT EXACTLY DIVIDE TSTEP'
  WRITE(KULOUT,*)'MAX GUST CALCULATION MAY BE WRONG',&
                       &MOD(TSTEP,REAL(NXGSTPERIOD2))
ENDIF

IF(LXVISI.AND.MOD(TSTEP,REAL(NVISIPERIOD))/=0.) THEN
   WRITE(KULOUT,*)'WARNING NVISIPERIOD DO NOT EXACTLY DIVIDE TSTEP'
   WRITE(KULOUT,*)'MIN VISI CALCULATION MAY BE WRONG',&
                        &MOD(TSTEP,REAL(NVISIPERIOD))
ENDIF
IF(LXVISI2.AND.MOD(TSTEP,REAL(NVISIPERIOD2))/=0.) THEN
   WRITE(KULOUT,*)'WARNING NVISIPERIOD2 DO NOT EXACTLY DIVIDE TSTEP'
   WRITE(KULOUT,*)'MIN VISI CALCULATION MAY BE WRONG',&
                        &MOD(TSTEP,REAL(NVISIPERIOD2))
ENDIF

IF(LXXGST.AND.MOD(TSTEP,REAL(NXGSTPERIOD))/=0.) THEN
  WRITE(KULOUT,*)'WARNING NGSTPERIOD DO NOT EXACTLY DIVIDE TSTEP'
  WRITE(KULOUT,*)'MAX GUST CALCULATION MAY BE WRONG',&
                       &MOD(TSTEP,REAL(NXGSTPERIOD))
ENDIF
IF(LXXGST2.AND.MOD(TSTEP,REAL(NXGSTPERIOD2))/=0.) THEN
  WRITE(KULOUT,*)'WARNING NGSTPERIOD2 DO NOT EXACTLY DIVIDE TSTEP'
  WRITE(KULOUT,*)'MAX GUST CALCULATION MAY BE WRONG',&
                       &MOD(TSTEP,REAL(NXGSTPERIOD2))
ENDIF

IF(LXVISI.AND.MOD(TSTEP,REAL(NVISIPERIOD))/=0.) THEN
   WRITE(KULOUT,*)'WARNING NVISIPERIOD DO NOT EXACTLY DIVIDE TSTEP'
   WRITE(KULOUT,*)'MIN VISI CALCULATION MAY BE WRONG',&
                        &MOD(TSTEP,REAL(NVISIPERIOD))
ENDIF
IF(LXVISI2.AND.MOD(TSTEP,REAL(NVISIPERIOD2))/=0.) THEN
   WRITE(KULOUT,*)'WARNING NVISIPERIOD2 DO NOT EXACTLY DIVIDE TSTEP'
   WRITE(KULOUT,*)'MIN VISI CALCULATION MAY BE WRONG',&
                        &MOD(TSTEP,REAL(NVISIPERIOD2))
ENDIF


!      ----------------------------------------------------------------

!*       3.    INITIALIZE DESCRIPTORS
!              ----------------------

!        3.1 Preinitialize the vertical levels and prefixes for *FA* files

CLEVS(1) = 'S   '

! On each half level :
IK=1
ILEV(IK)=NFLEVG+1
DO J=1,ILEV(IK)
  CLPRE(J,IK)=CLEVS(1)
  INLEV(J,IK)=J-1+NIOLEVG-NFLEVG
ENDDO
! At the surface
IK=2
ILEV(IK)=1
CLPRE(1,IK)='SURF'
INLEV(1,IK)=NIOLEVG
! On each full level :
IK=3
ILEV(IK)=NFLEVG
DO J=1,ILEV(IK)
  CLPRE(J,IK)=CLEVS(1)
  INLEV(J,IK)=J+NIOLEVG-NFLEVG
ENDDO
! At the top and the surface :
IK=4
ILEV(IK)=2
CLPRE(1,IK)='SOMM'
CLPRE(2,IK)='SURF'
INLEV(1,IK)=NIOLEVG-NFLEVG
INLEV(2,IK)=NIOLEVG
! At a given model level
IK=5
ILEV(IK)=1
CLPRE(1,IK)=CLEVS(1)

!        3.2 Setup descripors

IP=0
IADD=1

!        3.2.1 STRess of gravity wave Drag :

YDXFU%YXFUPT%MXTRDU=0
YDXFU%YXFUPT%MXTRDV=0
IF (LXTRD) THEN
  IF (LTXTRD) THEN
!   On each half level : 
    IK=1 
  ELSE
!   At the surface :
    IK=2 
  ENDIF
  IPAR=1
  CLSU=YFA_XGDU%CLNAME(5:)
  CALL NEXTYPE3(CLPRE(1,IK),CLSU,INLEV(1,IK),ILEV(IK),IPAR,YDXFU%YXFUPT%MXTRDU)
  CLSU=YFA_XGDV%CLNAME(5:)
  CALL NEXTYPE3(CLPRE(1,IK),CLSU,INLEV(1,IK),ILEV(IK),IPAR,YDXFU%YXFUPT%MXTRDV)
ENDIF

!        3.2.2 STRess of Convection :

YDXFU%YXFUPT%MXTRCU=0
YDXFU%YXFUPT%MXTRCV=0
YDXFU%YXFUPT%MXDICQ=0
YDXFU%YXFUPT%MXDICS=0
IF (LXTRC) THEN
  IF (LTXTRC) THEN
!   On each half level :
    IK=1
  ELSE
!   At the model level the closest to 550 hPa :
    IK=5
    CALL SEAPRE(55000._JPRB,INF550,STPRE,NFLEVG)
    INLEV(1,IK)=INF550-1
  ENDIF
  CLSU=YFA_XCVU%CLNAME(5:)
  IPAR=1
  CALL NEXTYPE3(CLPRE(1,IK),CLSU,INLEV(1,IK),ILEV(IK),IPAR,YDXFU%YXFUPT%MXTRCU)
  CLSU=YFA_XCVV%CLNAME(5:)
  IPAR=1
  CALL NEXTYPE3(CLPRE(1,IK),CLSU,INLEV(1,IK),ILEV(IK),IPAR,YDXFU%YXFUPT%MXTRCV)
  IF (LTXTRC) THEN
!   On each half level :
    IK=1
  ELSE
!   At the model level the closest to 850 hPa :
    IK=5
    CALL SEAPRE(85000._JPRB,INF850,STPRE,NFLEVG)
    INLEV(1,IK)=INF850-1
  ENDIF
  CLSU=YFA_XCVQ%CLNAME(5:)
  IPAR=0
  CALL NEXTYPE3(CLPRE(1,IK),CLSU,INLEV(1,IK),ILEV(IK),IPAR,YDXFU%YXFUPT%MXDICQ)
  CLSU=YFA_XCVS%CLNAME(5:)
  IPAR=0
  CALL NEXTYPE3(CLPRE(1,IK),CLSU,INLEV(1,IK),ILEV(IK),IPAR,YDXFU%YXFUPT%MXDICS)
ENDIF

!        3.2.3 STRess of Turbulence, or Surface Fluxes

YDXFU%YXFUPT%MXTRTU=0
YDXFU%YXFUPT%MXTRTV=0
YDXFU%YXFUPT%MXDITQ=0
YDXFU%YXFUPT%MXDITS=0
IF (LXTRT) THEN
  IF (LTXTRT) THEN
!   On each half level :
    IK=1
  ELSE
!   At the surface :
    IK=2
  ENDIF
  CLSU=YFA_XTUU%CLNAME(5:)
  CALL NEXTYPE3(CLPRE(1,IK),CLSU,INLEV(1,IK),ILEV(IK),1,YDXFU%YXFUPT%MXTRTU)
  CLSU=YFA_XTUV%CLNAME(5:)
  CALL NEXTYPE3(CLPRE(1,IK),CLSU,INLEV(1,IK),ILEV(IK),1,YDXFU%YXFUPT%MXTRTV)
  IF (LXTRT) THEN
    CLSU=YFA_XTUQ%CLNAME(5:)
    CALL NEXTYPE3(CLPRE(1,IK),CLSU,INLEV(1,IK),ILEV(IK),0,YDXFU%YXFUPT%MXDITQ)
    CLSU=YFA_XTUS%CLNAME(5:)
    CALL NEXTYPE3(CLPRE(1,IK),CLSU,INLEV(1,IK),ILEV(IK),0,YDXFU%YXFUPT%MXDITS)
  ENDIF
ENDIF

!        3.2.4 Flux de PLuie  neige graupel et grele Convective 
!          (Convective rain snow graupel and hail) 

YDXFU%YXFUPT%MXPLCL=0
YDXFU%YXFUPT%MXPLCN=0
YDXFU%YXFUPT%MXPLCG=0
YDXFU%YXFUPT%MXPLCH=0
IF (LXPLC) THEN
  IK=2
  CLSU=YFA_XCP%CLNAME(5:)
  CALL NEXTYPE3(CLPRE(1,IK),CLSU,INLEV(1,IK),ILEV(IK),0,YDXFU%YXFUPT%MXPLCL)
  IF (LNEIGE) THEN
    CLSU=YFA_XCSF%CLNAME(5:)
    CALL NEXTYPE3(CLPRE(1,IK),CLSU,INLEV(1,IK),ILEV(IK),0,YDXFU%YXFUPT%MXPLCN)
  ENDIF
  IF (LXPLCG) THEN
    CLSU=YFA_XCSG%CLNAME(5:)
    CALL NEXTYPE3(CLPRE(1,IK),CLSU,INLEV(1,IK),ILEV(IK),0,YDXFU%YXFUPT%MXPLCG)
  ENDIF
  IF (LXPLCH) THEN
    CLSU=YFA_XCSH%CLNAME(5:)
    CALL NEXTYPE3(CLPRE(1,IK),CLSU,INLEV(1,IK),ILEV(IK),0,YDXFU%YXFUPT%MXPLCH)
  ENDIF
ENDIF

!        3.2.5 Flux de PLuie et neige graupel et grele Stratiforme
!               (Stratiform rain snow graupel and hail)

YDXFU%YXFUPT%MXPLSL=0
YDXFU%YXFUPT%MXPLSN=0
YDXFU%YXFUPT%MXPLSG=0
