CHARACTER(LEN=*),   INTENT(IN)  :: CDPREF(KLEV)
CHARACTER(LEN=*),   INTENT(IN)  :: CDSUFF
INTEGER(KIND=JPIM), INTENT(OUT) :: KX

INTEGER(KIND=JPIM) :: J, ILEN
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('SUXFU:NEXTYPE3',0,ZHOOK_HANDLE)
ASSOCIATE(TYPE_XFU=>YDXFU%TYPE_XFU)

IF (ALL((CDPREF(:) /= ' ' .AND. CDSUFF(:) /= ' '))) THEN
  IP=IP+1
  DO J=1,KLEV
    ILEN=MIN(4,LEN(CDPREF(J)))
    TYPE_XFU(IP)%CLPREF(J)=CDPREF(J)(1:ILEN)
    TYPE_XFU(IP)%INUMLEV(J)=KNLEV(J)
  ENDDO
  ILEN=MIN(16,LEN(CDSUFF))
  TYPE_XFU(IP)%CLSUFF=CDSUFF(1:ILEN)
  TYPE_XFU(IP)%ILEV=KLEV
  TYPE_XFU(IP)%IPARITY=KPAR
  KX=IADD
  IADD=IADD+KLEV
ENDIF

END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('SUXFU:NEXTYPE3',1,ZHOOK_HANDLE)

END SUBROUTINE NEXTYPE3

