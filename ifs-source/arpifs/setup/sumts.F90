SUBROUTINE SUMTS(YDML_GCONF)

!**** *SUMTS* - SET UPS FOR TOVS RAD. TRANSFER CODE

!     PURPOSE.
!     -------
!       SET UP CONSTANTS FOR MODEL TO SATELLITE CODE
!       1) READS IN REGRESSION COEFFICIENTS FOR EXTRAPOLATION TO 000.1 mb
!       2) RETURNS F00.M00. PRESSURE LEVEL INFORMATION
!       3) SETS UP DUMMY CLOUD AND EMISSIVITY PARAMETERS

!**   INTERFACE.
!**   ---------
!       *CALL* * SUMTS*  (FROM SUPHY)

!**   EXTERNALS
!     ---------
!        RTTVI   - READ TOVS COEFFICIENTS (RT-CODE)

!     AUTHOR.
!     -------
!        R. El Khatib *METEO-FRANCE*
!        ORIGINAL : 04-08-19

!     MODIFICATIONS.
!     --------------
!       V. Guidard: 21-Mar-2011  Add LISP_HYBRID
!       Z. SASSI  : 07-Mar-2013  INITIALIZE THE CONTROL OF POST-PROCESS OF PHYSICAL QUANTITIES 
!       K. Yessad (July 2014): Move some variables.
!     ------------------------------------------------------------------

USE MODEL_GENERAL_CONF_MOD , ONLY : MODEL_GENERAL_CONF_TYPE
USE PARKIND1 , ONLY : JPRB, JPIM
USE YOMHOOK  , ONLY : LHOOK, DR_HOOK, JPHOOK
USE YOMMTS   , ONLY : NTYPE, LMTS, LMTSCL, LUBIQUITAIRE, LISP_HYBRID
USE YOMLUN   , ONLY : NULNAM ,NULOUT, NULERR
USE YOMFA    , ONLY : YFA_MSAT7C1,YFA_MSAT7C2,&
 & YFA_MSAT8C1,YFA_MSAT8C2,YFA_MSAT8C3,YFA_MSAT8C4,&
 & YFA_MSAT8C5,YFA_MSAT8C6,YFA_MSAT8C7,YFA_MSAT8C8,&
 & YFA_MSAT9C1,YFA_MSAT9C2,YFA_MSAT9C3,YFA_MSAT9C4,&
 & YFA_MSAT9C5,YFA_MSAT9C6,YFA_MSAT9C7,YFA_MSAT9C8,&
 & YFA_GOES11C1,YFA_GOES11C2,YFA_GOES11C3,YFA_GOES11C4,&
 & YFA_GOES12C1,YFA_GOES12C2,YFA_GOES12C3,YFA_GOES12C4,&
 & YFA_MTSAT1C1,YFA_MTSAT1C2,YFA_MTSAT1C3,YFA_MTSAT1C4
USE YOMFPC   , ONLY : TNAMFPL

!     ------------------------------------------------------------------

IMPLICIT NONE
INTEGER(KIND=JPIM) :: JFLD, IFP2DF
TYPE(MODEL_GENERAL_CONF_TYPE),INTENT(INOUT):: YDML_GCONF
TYPE(TNAMFPL) :: YLNAMFPC
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!     ------------------------------------------------------------------

#include "nammts.nam.h"
#include "posnam.intfb.h"
#include "sufpc.intfb.h"
#include "abor1.intfb.h"

!     ------------------------------------------------------------------
IF (LHOOK) CALL DR_HOOK('SUMTS',0,ZHOOK_HANDLE)
ASSOCIATE(YA_NL=>YDML_GCONF%YGFL%YA_NL, YIRAD_NL=>YDML_GCONF%YGFL%YIRAD_NL, YI_NL=>YDML_GCONF%YGFL%YI_NL, &
 & YLRAD_NL=>YDML_GCONF%YGFL%YLRAD_NL, YL_NL=>YDML_GCONF%YGFL%YL_NL, &
 & NSTOP=>YDML_GCONF%YRRIP%NSTOP,CFP2DF=>YLNAMFPC%CFP2DF)
!     ------------------------------------------------------------------

NTYPE=7

!default value for LUBIQUITAIRE and LISP_HYBRID
LUBIQUITAIRE=.FALSE.
LISP_HYBRID=.FALSE.

!--------------INITIALIZE THE CONTROL OF POST-PROCESS---------
!-------------------OF PHYSICAL QUANTITIES--------------------
LMTS=.FALSE.
LMTSCL=.FALSE.
CALL SUFPC(YDNAMFPL=YLNAMFPC,LDPRINT=.FALSE.)
DO JFLD=1,SIZE(CFP2DF)
  IF (CFP2DF(JFLD) == ' ') EXIT
ENDDO
IFP2DF=JFLD-1
DO JFLD=1,IFP2DF
   IF (  CFP2DF(JFLD) == YFA_MSAT7C1%CLNAME .OR. CFP2DF(JFLD) == YFA_MSAT7C2%CLNAME .OR.&
     & CFP2DF(JFLD) == YFA_MSAT8C1%CLNAME .OR. CFP2DF(JFLD) == YFA_MSAT8C2%CLNAME .OR.&
     & CFP2DF(JFLD) == YFA_MSAT8C3%CLNAME .OR. CFP2DF(JFLD) == YFA_MSAT8C4%CLNAME .OR.&
     & CFP2DF(JFLD) == YFA_MSAT8C5%CLNAME .OR. CFP2DF(JFLD) == YFA_MSAT8C6%CLNAME .OR.&
     & CFP2DF(JFLD) == YFA_MSAT8C7%CLNAME .OR. CFP2DF(JFLD) == YFA_MSAT8C8%CLNAME .OR.&
     & CFP2DF(JFLD) == YFA_MSAT9C1%CLNAME .OR. CFP2DF(JFLD) == YFA_MSAT9C2%CLNAME .OR.&
     & CFP2DF(JFLD) == YFA_MSAT9C3%CLNAME .OR. CFP2DF(JFLD) == YFA_MSAT9C4%CLNAME .OR.&
     & CFP2DF(JFLD) == YFA_MSAT9C5%CLNAME .OR. CFP2DF(JFLD) == YFA_MSAT9C6%CLNAME .OR.&
     & CFP2DF(JFLD) == YFA_MSAT9C7%CLNAME .OR. CFP2DF(JFLD) == YFA_MSAT9C8%CLNAME .OR.&
     & CFP2DF(JFLD) == YFA_GOES11C1%CLNAME .OR. CFP2DF(JFLD) == YFA_GOES11C2%CLNAME .OR.&
     & CFP2DF(JFLD) == YFA_GOES11C3%CLNAME .OR. CFP2DF(JFLD) == YFA_GOES11C4%CLNAME .OR.&
     & CFP2DF(JFLD) == YFA_GOES12C1%CLNAME .OR. CFP2DF(JFLD) == YFA_GOES12C2%CLNAME .OR.&
     & CFP2DF(JFLD) == YFA_GOES12C3%CLNAME .OR. CFP2DF(JFLD) == YFA_GOES12C4%CLNAME .OR.&
     & CFP2DF(JFLD) == YFA_MTSAT1C1%CLNAME .OR. CFP2DF(JFLD) == YFA_MTSAT1C2%CLNAME .OR.&
     & CFP2DF(JFLD) == YFA_MTSAT1C3%CLNAME .OR. CFP2DF(JFLD) == YFA_MTSAT1C4%CLNAME&
     &) THEN
    LMTS=.TRUE.
    LMTSCL=.TRUE.
  ENDIF
ENDDO

!----------------------------------
CALL POSNAM(NULNAM,'NAMMTS')
READ(NULNAM,NAMMTS)

IF (LMTS) THEN
  IF (NSTOP > 0_JPIM) THEN
    IF(.NOT.((YIRAD_NL%LGP.OR.YI_NL%LGP).AND.(YLRAD_NL%LGP.OR.YL_NL%LGP)&
        &.AND.YA_NL%LGP)) THEN
      WRITE(NULERR,'(''LMTS REQUIRES YIRAD_NL%LGP.AND.YLRAD_NL%LGP.AND.YA_NL%LGP&
& in Fullpos inline'')')
      CALL ABOR1(' SUMTS')
    ENDIF
  ELSE
    IF (.NOT.((YIRAD_NL%LGP.OR.YI_NL%LGP).AND.(YLRAD_NL%LGP.OR.YL_NL%LGP)&
        &.AND.YA_NL%LGP)) THEN
     WRITE(NULERR,'(''LMTS REQUIRES YI(RAD)_NL%LGP.AND.YL(RAD)_NL%LGP.AND.YA_NL%LGP in&
&    Fullpos Offline'')')
      CALL ABOR1(' SUMTS')
    ENDIF
  ENDIF

  CALL PHRTSETUP(IFP2DF,CFP2DF(1:IFP2DF))
ENDIF

IF (LUBIQUITAIRE.AND.LISP_HYBRID) THEN
  WRITE(NULERR,*) 'LUBIQUITAIRE AND LISP_HYBRID CANNOT BE BOTH .TRUE.'
  CALL ABOR1('SUMTS')
ENDIF
   
WRITE(UNIT=NULOUT,FMT=*) 'LMTS = ',LMTS, 'LMTSCL = ',LMTSCL,&
 & 'LUBIQUITAIRE = ',LUBIQUITAIRE, 'LISP_HYBRID = ',LISP_HYBRID,&
 & 'NTYPE = ',NTYPE

!     ------------------------------------------------------------------
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('SUMTS',1,ZHOOK_HANDLE)
END SUBROUTINE SUMTS
