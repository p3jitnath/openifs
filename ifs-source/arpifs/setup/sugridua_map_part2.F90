SUBROUTINE SUGRIDUA_MAP_PART2(YDGEOMETRY,YDGFL,YGFL,KREP,YDSUGACTX)

!**** *SUGRIDUA_MAP_PART2*  - Wait for upper-air fields to be received

!     Author. 
!     ------- 
!      Philippe Marguinaud *METEO FRANCE* 
!      Original : 01-10-2014
!      O. Marsden: June 2015 CY42 YRGMV, YRGFL, YRSURF, YRGMV5, and YRGFL5 are now passed by argument


USE YOM_YGFL    , ONLY : TYPE_GFLD
USE GEOMETRY_MOD, ONLY : GEOMETRY
USE YOMGFL   , ONLY : TGFL
USE PARKIND1 , ONLY : JPIM, JPRB
USE YOMHOOK  , ONLY : LHOOK, DR_HOOK, JPHOOK
USE IOGRIDUA_MOD, ONLY : IOGRIDUA_COUNT,&
                       & IOGRIDUA_SELECTD,&
                       & NIOGRIDUACT_READ,&
                       & SUGRIDUA_MAP_CTX
USE IOGRID_MOD,   ONLY : IOGRID_SELECTF,&
                       & NIOGRIDCT_READ

USE YOMIO_SERV, ONLY : IO_SERV_C001
USE IOFLDPTR_MOD, ONLY : IOFLDPTR
USE IOFLDDESC_MOD, ONLY : OPERATOR (/=)

IMPLICIT NONE

TYPE(GEOMETRY)         , INTENT (IN)    :: YDGEOMETRY
TYPE(TGFL)             , INTENT (INOUT) :: YDGFL
TYPE(TYPE_GFLD)         ,INTENT(INOUT)  :: YGFL
INTEGER (KIND=JPIM)    , INTENT (OUT)   :: KREP
TYPE (SUGRIDUA_MAP_CTX), INTENT (INOUT) :: YDSUGACTX

REAL (KIND=JPRB), POINTER :: ZGPBUFL (:,:) ! data fields to be read from file
TYPE (IOFLDPTR),  ALLOCATABLE :: YLFLDGT (:)

INTEGER (KIND=JPIM) :: IFNUM
INTEGER (KIND=JPIM) :: JFLD
INTEGER (KIND=JPIM), PARAMETER :: IFILE = 9_JPIM

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

#include "io_serv_map_recv_part2.intfb.h"
#include "sugridua_fixup.intfb.h"
#include "abor1.intfb.h"

IF (LHOOK) CALL DR_HOOK('SUGRIDUA_MAP_PART2',0,ZHOOK_HANDLE)

KREP = 0

CALL IO_SERV_MAP_RECV_PART2 (IO_SERV_C001, YDSUGACTX%YIOSMPP, ZGPBUFL)

IF (.NOT. ASSOCIATED (ZGPBUFL)) THEN
  KREP = -1
  IF (LHOOK) CALL DR_HOOK('SUGRIDUA_MAP_PART2',1,ZHOOK_HANDLE)
  RETURN 
ENDIF

IFNUM = 0
CALL IOGRIDUA_COUNT(YDGEOMETRY,YDGFL,YGFL,NIOGRIDUACT_READ,IFNUM,KFILE=IFILE)

IF (IFNUM /= SIZE (YDSUGACTX%YFLDSC)) THEN
  CALL ABOR1 ('SUGRIDUA_MAP_PART2: FIELD LIST MISMATCH')
ENDIF

ALLOCATE (YLFLDGT (IFNUM))

CALL IOGRIDUA_SELECTD(YDGEOMETRY, YDGFL, YGFL, NIOGRIDUACT_READ, YLFLDGT, KFILE=IFILE)

DO JFLD = 1, IFNUM
  IF (YLFLDGT (JFLD)%YFLDDSC /= YDSUGACTX%YFLDSC (JFLD)) THEN
    CALL ABOR1 ('SUGRIDUA_MAP_PART2: FIELD LIST MISMATCH')
  ENDIF
ENDDO

CALL IOGRID_SELECTF (YDGEOMETRY, NIOGRIDCT_READ, ZGPBUFL, YLFLDGT)

CALL SUGRIDUA_FIXUP(YDGEOMETRY,YDGFL,YGFL,IFILE)

DEALLOCATE (ZGPBUFL)

DEALLOCATE (YDSUGACTX%YFLDSC)
NULLIFY (YDSUGACTX%YFLDSC)

DEALLOCATE (YLFLDGT)

IF (LHOOK) CALL DR_HOOK('SUGRIDUA_MAP_PART2',1,ZHOOK_HANDLE)

END SUBROUTINE SUGRIDUA_MAP_PART2

