SUBROUTINE SUGRIDA_FIXUP(YDGEOMETRY,YDSURF,YDMODEL,KFNUM,PBUFL,YDFLDSC)

!**** *SUGRIDA_FIXUP*  - Massage surface fields after reading them

!     Author. 
!     ------- 
!      Philippe Marguinaud *METEO FRANCE* 
!      Original : 01-10-2014

USE TYPE_MODEL         , ONLY : MODEL
USE GEOMETRY_MOD       , ONLY : GEOMETRY
USE SURFACE_FIELDS_MIX , ONLY : TSURF
USE PARKIND1           , ONLY : JPIM, JPRB
USE YOMHOOK            , ONLY : LHOOK, DR_HOOK, JPHOOK
USE IOFLDDESC_MOD      , ONLY : IOFLDDESC

IMPLICIT NONE

TYPE(GEOMETRY)     , INTENT (IN)    :: YDGEOMETRY
TYPE(TSURF)        , INTENT (INOUT) :: YDSURF
TYPE(MODEL)        , INTENT (INOUT) :: YDMODEL
INTEGER (KIND=JPIM), INTENT (IN)    :: KFNUM
REAL (KIND=JPRB)   , INTENT (IN)    :: PBUFL (YDGEOMETRY%YRGEM%NGPTOT,KFNUM)
TYPE (IOFLDDESC)   , INTENT (IN)    :: YDFLDSC (KFNUM) 
#include "sugrida_fix_toz.intfb.h"

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('SUGRIDA_FIXUP',0,ZHOOK_HANDLE)

CALL SUGRIDA_FIX_LSM(YDGEOMETRY%YRDIM,YDSURF,YDMODEL%YRML_PHY_MF%YRPARAR)
  
CALL SUGRIDA_FIX_SURFANALYSISMASK(YDGEOMETRY,YDSURF)

CALL SUGRIDA_FIX_TOZ(YDGEOMETRY,YDMODEL%YRML_CHEM%YROZO,YDMODEL%YRML_AOC%YRMCC,YDMODEL%YRML_PHY_G%YRDPHY, &
 & YDMODEL%YRML_PHY_MF%YRPHY,KFNUM,PBUFL,YDFLDSC)
  
IF (LHOOK) CALL DR_HOOK('SUGRIDA_FIXUP',1,ZHOOK_HANDLE)

CONTAINS

SUBROUTINE SUGRIDA_FIX_LSM(YDDIM,YDSURF,YDPARAR)

USE YOMDIM             , ONLY : TDIM
USE SURFACE_FIELDS_MIX , ONLY : TSURF
USE YOMCT0             , ONLY : LSFXLSM
USE YOMPARAR           , ONLY : TPARAR
USE YOMGPPB            , ONLY : GPARBUF



TYPE(TDIM)  ,INTENT(IN)    :: YDDIM
TYPE(TSURF) ,INTENT(INOUT) :: YDSURF
TYPE(TPARAR),INTENT(IN)    :: YDPARAR
REAL (KIND=JPRB), DIMENSION (:,:), ALLOCATABLE :: ZGPAR
INTEGER(KIND=JPIM) :: JSTGLO, IEND, IBL, JL, JV

REAL (KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK ('SUGRIDA_FIXUP:SUGRIDA_FIX_LSM',0,ZHOOK_HANDLE)


IF (LSFXLSM) THEN

  DO JV = 1, YDSURF%YSD_VFD%NUMFLDS

    IF (YDSURF%YSD_VF%YVF(JV)%CNAME == 'SURFIND.TERREMER') THEN

! If LSFXLSM=.true. (then LMS=.T.), 
! land-sea mask is read from SURFEX in suphmse and stored in GPARBUF 
  
      ALLOCATE (ZGPAR (YDGEOMETRY%YRDIM%NPROMA,YDPARAR%NGPAR))

      DO JSTGLO = 1, YDGEOMETRY%YRGEM%NGPTOT, YDGEOMETRY%YRDIM%NPROMA

        IEND = MIN (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRGEM%NGPTOT-JSTGLO+1)
        IBL  = (JSTGLO-1)/YDGEOMETRY%YRDIM%NPROMA+1

        ZGPAR = GPARBUF (:, :, 1+( JSTGLO-1)/YDGEOMETRY%YRDIM%NPROMA)

        DO JL = 1, IEND
          YDSURF%SD_VF (JL, JV, IBL) = ZGPAR (JL, YDPARAR%MLSM)
        ENDDO   

      ENDDO   

      DEALLOCATE (ZGPAR)

    ENDIF

  ENDDO

ENDIF


IF (LHOOK) CALL DR_HOOK ('SUGRIDA_FIXUP:SUGRIDA_FIX_LSM',1,ZHOOK_HANDLE)

END SUBROUTINE SUGRIDA_FIX_LSM

SUBROUTINE SUGRIDA_FIX_SURFANALYSISMASK(YDGEOMETRY,YDSURF)

USE GEOMETRY_MOD       , ONLY : GEOMETRY
USE SURFACE_FIELDS_MIX , ONLY : TSURF
USE YOMLUN             , ONLY : NULOUT
USE YOMCT0             , ONLY : LELAM
USE YOMMP0             , ONLY : MYPROC
USE DISGRID_MOD        , ONLY : DISGRID_SEND,&
 &                              DISGRID_RECV

TYPE(GEOMETRY), INTENT(IN)    :: YDGEOMETRY
TYPE(TSURF)   , INTENT(INOUT) :: YDSURF

REAL (KIND=JPRB) :: ZSANMASKG (YDGEOMETRY%YRGEM%NGPTOTG), ZSANMASKL (YDGEOMETRY%YRGEM%NGPTOT)
INTEGER(KIND=JPIM) :: JSTGLO, IEND, IBL, JL, IROF, JGL, JV, JLON

REAL (KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK ('SUGRIDA_FIXUP:SUGRIDA_FIX_SURFANALYSISMASK',0,ZHOOK_HANDLE)

DO JV = 1, YDSURF%YSP_CID%NUMFLDS

  IF (YDSURF%YSP_CI%YCI(JV)%CNAME == 'SURFANALYSISMASK') THEN

    IF (MYPROC == 1) THEN 
    
      ZSANMASKG = 1.0_JPRB
    
      WRITE (NULOUT,'(''         changed to 1. '')')
    
      IF (LELAM) THEN
    
    ! we won't want to deal with the E-domain ALADIN in CANARI
    
        IROF = 0  
        DO JGL = 1, YDGEOMETRY%YRDIM%NDGUXG
          IROF = IROF + YDGEOMETRY%YRDIM%NDLUXG
          DO JLON = YDGEOMETRY%YRDIM%NDLUXG+1, YDGEOMETRY%YRGEM%NLOENG (JGL)
            IROF = IROF + 1
            ZSANMASKG (IROF) = 0.0_JPRB
          ENDDO   
        ENDDO
    
        DO JGL = YDGEOMETRY%YRDIM%NDGUXG+1, YDGEOMETRY%YRDIM%NDGLG
          DO JLON = 1, YDGEOMETRY%YRGEM%NLOENG(JGL)
            IROF = IROF + 1
            ZSANMASKG (IROF) = 0.0_JPRB
          ENDDO   
        ENDDO   

      ENDIF   
    
      CALL DISGRID_SEND(YDGEOMETRY,1, ZSANMASKG, 0, ZSANMASKL)
    
    ELSE
    
      CALL DISGRID_RECV(YDGEOMETRY,1, 1, ZSANMASKL, 0)
    
    ENDIF   
    
    DO JSTGLO = 1, YDGEOMETRY%YRGEM%NGPTOT, YDGEOMETRY%YRDIM%NPROMA
    
      IEND = MIN (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRGEM%NGPTOT-JSTGLO+1)
      IBL  = (JSTGLO-1)/YDGEOMETRY%YRDIM%NPROMA+1
    
      DO JL = 1, IEND
        YDSURF%SP_CI (JL,JV,IBL) = ZSANMASKL (JSTGLO-1+JL)
      ENDDO   
    
    ENDDO   

  ENDIF

ENDDO

IF (LHOOK) CALL DR_HOOK ('SUGRIDA_FIXUP:SUGRIDA_FIX_SURFANALYSISMASK',1,ZHOOK_HANDLE)

END SUBROUTINE SUGRIDA_FIX_SURFANALYSISMASK

END SUBROUTINE SUGRIDA_FIXUP

