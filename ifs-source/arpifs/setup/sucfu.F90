    TYPE_CFU(IP)%CLPREF(J)=CDPREF(J)(1:ILEN)
    TYPE_CFU(IP)%INUMLEV(J)=KNLEV(J)
  ENDDO
  ILEN=MIN(16,LEN(CDSUFF))
  TYPE_CFU(IP)%CLSUFF=CDSUFF(1:ILEN)
  TYPE_CFU(IP)%ILEV=KLEV
  TYPE_CFU(IP)%IPARITY=KPAR
  KX=IADD
  IADD=IADD+KLEV
ENDIF

END ASSOCIATE
