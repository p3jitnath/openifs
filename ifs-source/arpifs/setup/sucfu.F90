SUBROUTINE NEXTYPE3(CDPREF,CDSUFF,KNLEV,KLEV,KPAR,KX)

INTEGER(KIND=JPIM), INTENT(IN)  :: KLEV,KPAR
INTEGER(KIND=JPIM), INTENT(IN)  :: KNLEV(KLEV)
CHARACTER(LEN=*),   INTENT(IN)  :: CDPREF(KLEV)
CHARACTER(LEN=*),   INTENT(IN)  :: CDSUFF
INTEGER(KIND=JPIM), INTENT(OUT) :: KX

INTEGER(KIND=JPIM) :: J, ILEN
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('SUCFU:NEXTYPE3',0,ZHOOK_HANDLE)
ASSOCIATE(TYPE_CFU=>YDCFU%TYPE_CFU)

