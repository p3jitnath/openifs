REAL(KIND=JPRB) :: ZAERBCA3D_VI(KDLON,0:KLEV),ZAERBCB3D_VI(KDLON,0:KLEV)
REAL(KIND=JPRB) :: ZAERORA3D_VI(KDLON,0:KLEV),ZAERORB3D_VI(KDLON,0:KLEV)
REAL(KIND=JPRB) :: ZAERSDA3D_VI(KDLON,0:KLEV),ZAERSDB3D_VI(KDLON,0:KLEV),ZAERSDC3D_VI(KDLON,0:KLEV)
REAL(KIND=JPRB) :: ZAERSSA3D_VI(KDLON,0:KLEV),ZAERSSB3D_VI(KDLON,0:KLEV),ZAERSSC3D_VI(KDLON,0:KLEV)
REAL(KIND=JPRB) :: ZAERSUA3D_VI(KDLON,0:KLEV)


REAL(KIND=JPRB) :: ZAERBCA(KDLON), ZAERBCB(KDLON)
REAL(KIND=JPRB) :: ZAERORA(KDLON), ZAERORB(KDLON)
REAL(KIND=JPRB) :: ZAERSDA(KDLON), ZAERSDB(KDLON), ZAERSDC(KDLON)
REAL(KIND=JPRB) :: ZAERSSA(KDLON), ZAERSSB(KDLON), ZAERSSC(KDLON)
REAL(KIND=JPRB) :: ZAERSUA(KDLON)

REAL(KIND=JPRB) :: ZAERS1O(KDLON), ZAERS2O(KDLON), ZAERS3O(KDLON)
REAL(KIND=JPRB) :: ZAERD1O(KDLON), ZAERD2O(KDLON), ZAERD3O(KDLON)
REAL(KIND=JPRB) :: ZAERO1O(KDLON), ZAERO2O(KDLON)
REAL(KIND=JPRB) :: ZAERB1O(KDLON), ZAERB2O(KDLON)
REAL(KIND=JPRB) :: ZAERSUO(KDLON)
REAL(KIND=JPRB) :: ZAERS1N(KDLON), ZAERS2N(KDLON), ZAERS3N(KDLON)
REAL(KIND=JPRB) :: ZAERD1N(KDLON), ZAERD2N(KDLON), ZAERD3N(KDLON)
REAL(KIND=JPRB) :: ZAERO1N(KDLON), ZAERO2N(KDLON)
REAL(KIND=JPRB) :: ZAERB1N(KDLON), ZAERB2N(KDLON)
REAL(KIND=JPRB) :: ZAERSUN(KDLON)

REAL(KIND=JPRB) :: ZAERBC1A, ZAERBC1B, ZAERBC2A, ZAERBC2B
REAL(KIND=JPRB) :: ZAEROR1A, ZAEROR1B, ZAEROR2A, ZAEROR2B
REAL(KIND=JPRB) :: ZAERSD1A, ZAERSD1B, ZAERSD2A, ZAERSD2B, ZAERSD3A, ZAERSD3B
REAL(KIND=JPRB) :: ZAERSS1A, ZAERSS1B, ZAERSS2A, ZAERSS2B, ZAERSS3A, ZAERSS3B
REAL(KIND=JPRB) :: ZAERSU1A, ZAERSU1B
REAL(KIND=JPRB) :: ZREF_AER_PRA, ZREF_AER_PRB, ZREF_AER_DPRA, ZREF_AER_DPRB
REAL(KIND=JPRB) :: ZAERSDT

!--

REAL(KIND=JPRB) :: ZAETRN(KDLON), ZAETRO(KDLON)

REAL(KIND=JPRB) :: ZPRS(KLON,0:KLEV)
REAL(KIND=JPRD) :: ZALP(66)
REAL(KIND=JPRB) :: ZDPN(KDLON)  , ZDPO(KDLON)
REAL(KIND=JPRB) :: ZFAED(21)    , ZFAEL(21)    , ZFAES(21)    , ZFAEU(21)
REAL(KIND=JPRB) :: ZFOZQ(11)    , ZFOZH(11)
REAL(KIND=JPRB) :: ZGRTH(KDLON)
REAL(KIND=JPRB) :: ZLON(KDLON)  , ZLONR(360)    , ZNLO1(KDLON) , ZNLO2(KDLON)

REAL(KIND=JPRB) :: ZOZH (KDLON) , ZOZQ (KDLON)
REAL(KIND=JPRB) :: ZQOFN(KDLON) , ZQOFO(KDLON)
REAL(KIND=JPRB) :: ZSILAT(KDLON), ZSINR(181), ZSTRDEP(KDLON)
REAL(KIND=JPRB) :: ZRH(KDLON)
INTEGER(KIND=JPIM) :: IRH(KDLON)

INTEGER(KIND=JPIM) :: IL, IMM, IMNC, IMNS, INLA, INLA1, INLA2, INLO1, INLO2, &
 & ITOTPT, JAER, JK, JL, JLR, JMM, JNN, JTAB, JV, &
 & INLATR, INLONR, ITOT, IC, JC
!Local SPP array integers
INTEGER(KIND=JPIM) :: IPZHSVDAERO, IPDELTAAERO  

REAL(KIND=JPRB) :: ZAETR, ZAETRV, ZCOS1, ZCOS10, ZCOS2, ZCOS3, ZCOS4,&
 & ZCOS5, ZCOS6, ZCOS7, ZCOS8, ZCOS9, ZCPHN3, &
 & ZCPHO3, ZDPNMO, ZDP, ZINT, ZGRIDR, ZLATR, ZSDPN3, ZSDPO3
REAL(KIND=JPRD) :: ZSIN, ZSIN1, ZSIN10, ZSIN2, ZSIN3, ZSIN4, &
 & ZSIN5, ZSIN6, ZSIN7, ZSIN8, ZSIN9  
REAL(KIND=JPRB) :: ZSSIN
REAL(KIND=JPRB) :: ZAERBC1, ZAERBC2, ZAEROR1, ZAEROR2, ZAERSD1, ZAERSD2, &
 & ZAERSS1, ZAERSS2, ZAERSU1, ZAERSU2, ZDSO4S
REAL(KIND=JPRB) :: ZVDAEL, ZVDAES, ZVDAEU, ZVDAED
REAL(KIND=JPRB) :: ZVDAESU, ZVDAESS, ZVDAEDU, ZVDAEOM, ZVDAEBC
REAL(KIND=JPRB) :: ZXAERSU(KDLON), ZXAERSS(KDLON), ZXAEROR(KDLON), ZXAERBC(KDLON), ZXAERSD(KDLON)
REAL(KIND=JPRB), DIMENSION(KDLON) :: ZXAERSUA, ZXAERSSA, ZXAERSSB, ZXAERSSC, ZXAERSDA, ZXAERSDB, ZXAERSDC, &
     &                               ZXAERORA, ZXAERORB, ZXAERBCA, ZXAERBCB
!Local SPP fields
INTEGER(KIND=JPIM) :: IPN
TYPE(SPP_PERT)     :: PN1ZHS_VD, PN1DELTA
REAL(KIND=JPRB) :: ZRRVD(KDLON), ZRDELTA
!Local switches to activate SPP perturbations
LOGICAL :: LLPERT_ZHS_VDAERO, LLPERT_DELTA_AERO

REAL(KIND=JPRB) :: ZDLONR
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE


!     ------------------------------------------------------------------

#include "abor1.intfb.h"
#include "legtri.intfb.h"

!     ------------------------------------------------------------------
IF (LHOOK) CALL DR_HOOK('RADACT',0,ZHOOK_HANDLE)
ASSOCIATE(CVDAED=>YDEAERD%CVDAED, CVDAEL=>YDEAERD%CVDAEL, &
 & CVDAES=>YDEAERD%CVDAES, CVDAEU=>YDEAERD%CVDAEU, RAEDC=>YDEAERD%RAEDC, &
 & RAEDS=>YDEAERD%RAEDS, RAELC=>YDEAERD%RAELC, RAELS=>YDEAERD%RAELS, &
 & RAESC=>YDEAERD%RAESC, RAESS=>YDEAERD%RAESS, RAEUC=>YDEAERD%RAEUC, &
 & RAEUS=>YDEAERD%RAEUS, RCAEOPD=>YDEAERD%RCAEOPD, RCAEOPL=>YDEAERD%RCAEOPL, &
 & RCAEOPS=>YDEAERD%RCAEOPS, RCAEOPU=>YDEAERD%RCAEOPU, &
 & RCSTBGA=>YDEAERD%RCSTBGA, RCTRBGA=>YDEAERD%RCTRBGA, RCTRPT=>YDEAERD%RCTRPT, &
 & RCVOBGA=>YDEAERD%RCVOBGA, RRHTAB=>YDEAERSNK%RRHTAB, &
 & CVDAEBC=>YDERAD%CVDAEBC, CVDAEDU=>YDERAD%CVDAEDU, CVDAEOM=>YDERAD%CVDAEOM, &
 & CVDAESS=>YDERAD%CVDAESS, CVDAESU=>YDERAD%CVDAESU, &
 & LAERADJDU=>YDERAD%LAERADJDU, LESO4HIS=>YDERAD%LESO4HIS, &
 & LHVOLCA=>YDERAD%LHVOLCA, LNEWAER=>YDERAD%LNEWAER, &
 & LVOLCDAMP=>YDERAD%LVOLCDAMP, LVOLCSPEC=>YDERAD%LVOLCSPEC, &
 & NAERMACC=>YDERAD%NAERMACC, NMCLAT=>YDERAD%NMCLAT, NMCLON=>YDERAD%NMCLON, &
 & NMCVAR=>YDERAD%NMCVAR, NVOLCVERT=>YDERAD%NVOLCVERT, &
 & RVOLCSPEC=>YDERAD%RVOLCSPEC, STBKG=>YDERAD%STBKG, &
 & NSTADD=>YDRIP%NSTADD,LAER3D=>YDERAD%LAER3D)
!     ------------------------------------------------------------------

!*         1.     "NEW AEROSOL DISTRIBUTION" PARAMETERS COMPUTATIONS
!                 --------------------------------------------------

!*         1.1    VOLCANIC AEROSOL DISTRIBUTION PARAMETERS
!                 ----------------------------------------

!                 GISS CLIMATOLOGY, APPLIED TO SULPHATE AEROSOLS
!                 ----------------------------------------------

IF (LHVOLCA) THEN
  INLATR=46
  ZGRIDR=180._JPRB/(INLATR-1)
  DO JLR=1,INLATR
    ZLATR=90._JPRB-(JLR-1)*ZGRIDR
    ZSINR(JLR)=SIN(ZLATR*RPI/180._JPRB)
  ENDDO

  IL=KSHIFT
  DO JL=KIDIA,KFDIA,KRINT
    IL=IL+1
    INLA=0
    ZSILAT(IL)=-9999._JPRB
    ZSIN=PSIN(JL)
    DO JLR=1,INLATR-1
      IF (ZSIN <= ZSINR(JLR) .AND. ZSIN > ZSINR(JLR+1)) THEN
        INLA=JLR
        ZSILAT(IL)=(ZSIN-ZSINR(INLA))/(ZSINR(INLA+1)-ZSINR(INLA))
        ZAERVO(IL)=YDRIP%YREAERC_TEGEN%RTAEVO(INLA)+ZSILAT(IL)*(YDRIP%YREAERC_TEGEN%RTAEVO(INLA+1)-YDRIP%YREAERC_TEGEN%RTAEVO(INLA))
      ENDIF
    ENDDO
    IF (ZSIN <= ZSINR(INLATR-1) .AND. ZSIN >= ZSINR(INLATR))THEN
      INLA=INLATR
      ZSILAT(IL)=(ZSIN-ZSINR(INLA-1))/(ZSINR(INLA)-ZSINR(INLA-1))
      ZAERVO(IL)=YDRIP%YREAERC_TEGEN%RTAEVO(INLA-1)&
       & +ZSILAT(IL)*(YDRIP%YREAERC_TEGEN%RTAEVO(INLA)-YDRIP%YREAERC_TEGEN%RTAEVO(INLA-1))  
    ENDIF
    IF (INLA == 0) THEN
      CALL ABOR1(' Problem with lat. interpolation in radact!')
    ENDIF
    IF(LVOLCSPEC) THEN
      IF(ZSIN > 0.64_JPRB) THEN
        ZAERVO(IL)=RVOLCSPEC(1)
      ELSEIF(ZSIN > 0.34_JPRB) THEN
        ZSILAT(IL)=(ZSIN-0.34_JPRB)/(0.3_JPRB)
        ZAERVO(IL)=RVOLCSPEC(2)+ZSILAT(IL)*(RVOLCSPEC(1)-RVOLCSPEC(2))
      ELSEIF(ZSIN > -0.34_JPRB) THEN
        ZAERVO(IL)=RVOLCSPEC(2)
      ELSEIF(ZSIN > -0.64_JPRB) THEN
        ZSILAT(IL)=(-ZSIN-0.34_JPRB)/(0.3_JPRB)
        ZAERVO(IL)=RVOLCSPEC(2)+ZSILAT(IL)*(RVOLCSPEC(3)-RVOLCSPEC(2))
      ELSE
        ZAERVO(IL)=RVOLCSPEC(3)
      ENDIF
      IF(LVOLCDAMP) THEN
        ZAERVO(IL)=STBKG+(ZAERVO(IL)-STBKG)*EXP(-NSTADD/400.0_JPRB)
      ENDIF
    ENDIF
    ZDPO(IL)=PAPRS(JL,1)
    ZAETRO(IL)=1.0_JPRB
    ZSTRDEP(IL)=0.0_JPRB
  ENDDO


!-- define how thick the stratosphere is in pressure units
  DO JK=1,KLEV
    IL=KSHIFT
    IF (KCF == 0) THEN
      DO JL=KIDIA,KFDIA,KRINT
        IL=IL+1
        ZGRTH(IL)= PTH(JL,JK)/PTH(JL,JK+1)
      ENDDO
    ELSEIF (KCF == 1) THEN
      DO JL=KIDIA,KFDIA,KRINT
        IL=IL+1
        ZGRTH(IL)= PTH(IL,JK)/PTH(IL,JK+1)
      ENDDO
    ENDIF
    IL=KSHIFT
    DO JL=KIDIA,KFDIA,KRINT
      IL=IL+1
      ZDPN(IL)=PAPRS(JL,JK+1)
      IF (0.5_JPRB*(PAPRS(JL,JK+1)+PAPRS(JL,JK)) < 999._JPRB) THEN
        ZAETRN(IL)=1.0_JPRB
      ELSE
        ZAETRN(IL)=ZAETRO(IL)*(MIN(1.0_JPRB, ZGRTH(IL )))**RCTRPT
      ENDIF
      ZAETR=SQRT(ZAETRN(IL)*ZAETRO(IL))

      IF(NVOLCVERT==0) THEN
        ZAETRV=ZAETR
      ELSEIF(NVOLCVERT==1) THEN
! Use temperature profile, but no volcanic aerosol above 10hPa
        IF (0.5_JPRB*(PAPRS(JL,JK)+PAPRS(JL,JK+1)) < 999._JPRB) THEN
          ZAETRV=0.0_JPRB
        ELSE
          ZAETRV=ZAETR
        ENDIF
      ELSEIF(NVOLCVERT==2) THEN
! Use prognostic ozone to define lower edge of aerosol distribution
        IF (0.5_JPRB*(PAPRS(JL,JK)+PAPRS(JL,JK+1)) < 999._JPRB) THEN
          ZAETRV=0.0_JPRB
        ELSEIF(0.5_JPRB*(PAPRS(JL,JK)+PAPRS(JL,JK+1)) < 7000._JPRB) THEN
          ZAETRV=1.0_JPRB
        ELSEIF(PAPRS(JL,JK) > 40000._JPRB) THEN
          ZAETRV=0.0_JPRB
        ELSE
          IF(PECPO3(JL,JK) > 0.4E-6_JPRB) THEN
            ZAETRV=1.0_JPRB
          ELSEIF(PECPO3(JL,JK) > 0.2E-6_JPRB) THEN
            ZAETRV=(PECPO3(JL,JK)-0.2E-6_JPRB)/0.2E-6_JPRB
          ELSE
            ZAETRV=0.0_JPRB
          ENDIF
        ENDIF
      ENDIF

      ZDPNMO=ZDPN(IL)-ZDPO(IL)
      ZSTRDEP(IL)=ZSTRDEP(IL)+ZAETRV*ZDPNMO
    ENDDO
    IL=KSHIFT
    DO JL=KIDIA,KFDIA,KRINT
      IL = IL+1
      ZDPO(IL)  =ZDPN(IL)
      ZAETRO(IL)=ZAETRN(IL)
    ENDDO
  ENDDO
!-- 
  IL = KSHIFT
  DO JL=KIDIA,KFDIA,KRINT
    IL = IL+1
    ZAERVO(IL)=ZAERVO(IL)/ZSTRDEP(IL)
  ENDDO

!                 TANRE ET AL. CLIMATOLOGY 
!                 ------------------------

ELSE
  IL = KSHIFT
  DO JL=KIDIA,KFDIA,KRINT
    IL = IL+1
    ZAERVO(IL)=RCSTBGA
  ENDDO
ENDIF
ITOTPT=IL

!*         1.2    TROPOSPHERIC AEROSOL DISTRIBUTION PARAMETERS
!                 --------------------------------------------

!-- if LNEWAER Tegen aerosol climatology is always done

IF (LNEWAER) THEN

!* -- Tegen et al. 1997 aerosol climatology


!-- latitude       
  INLATR=46
  ZGRIDR=180._JPRB/(INLATR-1)
  DO JLR=1,INLATR
    ZLATR=90._JPRB-(JLR-1)*ZGRIDR
    ZSINR(JLR)=SIN(ZLATR*RPI/180._JPRB)
  ENDDO
  INLONR=72
  ZDLONR=2._JPRB*RPI/INLONR
  DO JLR=1,INLONR
    ZLONR(JLR)=(JLR-1)*ZDLONR
  ENDDO

  IL=KSHIFT
  DO JL=KIDIA,KFDIA,KRINT         
    IL=IL+1
    IINLA1(IL)=0
    IINLA2(IL)=0
    ZSILAT(IL)=-9999._JPRB
    ZSIN=PSIN(JL)
    DO JLR=1,INLATR-1
