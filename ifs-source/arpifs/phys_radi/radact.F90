          ZAERSSC3D(IL,JK)=ZAERSS3A+ZLON(IL)*(ZAERSS3B-ZAERSS3A)
          
          !- SO4
          
          ZAERSU1A=YDRIP%YREAERC_MACC%RMAESU13D(INLO1,INLA1,JK) &
               & +ZSILAT(IL)*(YDRIP%YREAERC_MACC%RMAESU13D(INLO1,INLA2,JK)-YDRIP%YREAERC_MACC%RMAESU13D(INLO1,INLA1,JK))    
