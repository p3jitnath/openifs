ENDIF



!*         1.3    TROPOSPHERIC SULPHATE AEROSOL DISTRIBUTION PARAMETERS
!                 -----------------------------------------------------

IF (LNEWAER .AND. LESO4HIS) THEN

  IL=KSHIFT
  DO JL=KIDIA,KFDIA,KRINT        
    IL=IL+1
    ZSSIN = PSIN(JL)
