REAL(KIND=JPRB)   ,INTENT(IN),OPTIONAL    :: PPERT(KLON, YDSPP_CONFIG%SM%NRFTOTAL_RADGRID)   !SPP: 2D random pattern fields

!     -----------------------------------------------------------------

INTEGER(KIND=JPIM) :: IINLA1(KDLON), IINLA2(KDLON)
INTEGER(KIND=JPIM) :: IINLO1(KDLON), IINLO2(KDLON)

