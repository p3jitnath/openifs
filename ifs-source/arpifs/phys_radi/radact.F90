!     ------------------------------------------------------------------

!       3.     AEROSOLS
!              --------
!***
!       3.1     CALL TO LEGTRI

!***
CALL LEGTRI (ZSIN,11,66,ZALP)
!***

!       3.2     LEGENDRE TRANSFORM FOR AEROSOLS
!               -------------------------------

DO JMM=1,21
  ZFAES(JMM) = 0.0_JPRB
  ZFAEL(JMM) = 0.0_JPRB
  ZFAEU(JMM) = 0.0_JPRB
  ZFAED(JMM) = 0.0_JPRB
ENDDO
IMM  = 0
IMNC = 0
IMNS = 0
DO JMM=1,11
  IMM  = IMM+1
  DO JNN=JMM,11
    IMNC = IMNC+1
    ZFAES(IMM) = ZFAES(IMM)+ZALP(IMNC)*RAESC(IMNC)
    ZFAEL(IMM) = ZFAEL(IMM)+ZALP(IMNC)*RAELC(IMNC)
    ZFAEU(IMM) = ZFAEU(IMM)+ZALP(IMNC)*RAEUC(IMNC)
    ZFAED(IMM) = ZFAED(IMM)+ZALP(IMNC)*RAEDC(IMNC)
  ENDDO
  IF(JMM /= 1) THEN
    IMM  = IMM+1
    DO JNN=JMM,11
      IMNS = IMNS+1
      ZFAES(IMM) = ZFAES(IMM)+ZALP(IMNS+11)*RAESS(IMNS)
      ZFAEL(IMM) = ZFAEL(IMM)+ZALP(IMNS+11)*RAELS(IMNS)
      ZFAEU(IMM) = ZFAEU(IMM)+ZALP(IMNS+11)*RAEUS(IMNS)
      ZFAED(IMM) = ZFAED(IMM)+ZALP(IMNS+11)*RAEDS(IMNS)
    ENDDO
  ENDIF
ENDDO

!       3.3     FOURIER TRANSFORM FOR AEROSOLS
!               ------------------------------

IL = KSHIFT
DO JL=KIDIA,KFDIA,KRINT
  IL = IL+1
  ZCOS1    = PCLON(JL)
  ZSIN1    = PSLON(JL)
  ZCOS2    = ZCOS1*ZCOS1-ZSIN1*ZSIN1
  ZSIN2    = ZSIN1*ZCOS1+ZCOS1*ZSIN1
  ZCOS3    = ZCOS2*ZCOS1-ZSIN2*ZSIN1
  ZSIN3    = ZSIN2*ZCOS1+ZCOS2*ZSIN1
  ZCOS4    = ZCOS3*ZCOS1-ZSIN3*ZSIN1
  ZSIN4    = ZSIN3*ZCOS1+ZCOS3*ZSIN1
  ZCOS5    = ZCOS4*ZCOS1-ZSIN4*ZSIN1
  ZSIN5    = ZSIN4*ZCOS1+ZCOS4*ZSIN1
  ZCOS6    = ZCOS5*ZCOS1-ZSIN5*ZSIN1
  ZSIN6    = ZSIN5*ZCOS1+ZCOS5*ZSIN1
  ZCOS7    = ZCOS6*ZCOS1-ZSIN6*ZSIN1
  ZSIN7    = ZSIN6*ZCOS1+ZCOS6*ZSIN1
  ZCOS8    = ZCOS7*ZCOS1-ZSIN7*ZSIN1
  ZSIN8    = ZSIN7*ZCOS1+ZCOS7*ZSIN1
  ZCOS9    = ZCOS8*ZCOS1-ZSIN8*ZSIN1
  ZSIN9    = ZSIN8*ZCOS1+ZCOS8*ZSIN1
  ZCOS10   = ZCOS9*ZCOS1-ZSIN9*ZSIN1
  ZSIN10   = ZSIN9*ZCOS1+ZCOS9*ZSIN1
  ZAES(IL) = ZFAES(1) + 2.0_JPRB*&
   & ( ZFAES(2)*ZCOS1  + ZFAES(3)*ZSIN1  + ZFAES(4)*ZCOS2&
   & + ZFAES(5)*ZSIN2  + ZFAES(6)*ZCOS3  + ZFAES(7)*ZSIN3&
   & + ZFAES(8)*ZCOS4  + ZFAES(9)*ZSIN4  + ZFAES(10)*ZCOS5&
   & + ZFAES(11)*ZSIN5 + ZFAES(12)*ZCOS6 + ZFAES(13)*ZSIN6&
   & + ZFAES(14)*ZCOS7 + ZFAES(15)*ZSIN7 + ZFAES(16)*ZCOS8&
   & + ZFAES(17)*ZSIN8 + ZFAES(18)*ZCOS9 + ZFAES(19)*ZSIN9&
   & + ZFAES(20)*ZCOS10+ ZFAES(21)*ZSIN10                 )  
  ZAEL(IL) = ZFAEL(1) + 2.0_JPRB*&
   & ( ZFAEL(2)*ZCOS1  + ZFAEL(3)*ZSIN1  + ZFAEL(4)*ZCOS2&
   & + ZFAEL(5)*ZSIN2  + ZFAEL(6)*ZCOS3  + ZFAEL(7)*ZSIN3&
   & + ZFAEL(8)*ZCOS4  + ZFAEL(9)*ZSIN4  + ZFAEL(10)*ZCOS5&
   & + ZFAEL(11)*ZSIN5 + ZFAEL(12)*ZCOS6 + ZFAEL(13)*ZSIN6&
   & + ZFAEL(14)*ZCOS7 + ZFAEL(15)*ZSIN7 + ZFAEL(16)*ZCOS8&
   & + ZFAEL(17)*ZSIN8 + ZFAEL(18)*ZCOS9 + ZFAEL(19)*ZSIN9&
   & + ZFAEL(20)*ZCOS10+ ZFAEL(21)*ZSIN10                 )  
  ZAEU(IL) = ZFAEU(1) + 2.0_JPRB*&
   & ( ZFAEU(2)*ZCOS1  + ZFAEU(3)*ZSIN1  + ZFAEU(4)*ZCOS2&
   & + ZFAEU(5)*ZSIN2  + ZFAEU(6)*ZCOS3  + ZFAEU(7)*ZSIN3&
   & + ZFAEU(8)*ZCOS4  + ZFAEU(9)*ZSIN4  + ZFAEU(10)*ZCOS5&
   & + ZFAEU(11)*ZSIN5 + ZFAEU(12)*ZCOS6 + ZFAEU(13)*ZSIN6&
   & + ZFAEU(14)*ZCOS7 + ZFAEU(15)*ZSIN7 + ZFAEU(16)*ZCOS8&
   & + ZFAEU(17)*ZSIN8 + ZFAEU(18)*ZCOS9 + ZFAEU(19)*ZSIN9&
   & + ZFAEU(20)*ZCOS10+ ZFAEU(21)*ZSIN10                 )  
  ZAED(IL) = ZFAED(1) + 2.0_JPRB*&
   & ( ZFAED(2)*ZCOS1  + ZFAED(3)*ZSIN1  + ZFAED(4)*ZCOS2&
   & + ZFAED(5)*ZSIN2  + ZFAED(6)*ZCOS3  + ZFAED(7)*ZSIN3&
   & + ZFAED(8)*ZCOS4  + ZFAED(9)*ZSIN4  + ZFAED(10)*ZCOS5&
   & + ZFAED(11)*ZSIN5 + ZFAED(12)*ZCOS6 + ZFAED(13)*ZSIN6&
   & + ZFAED(14)*ZCOS7 + ZFAED(15)*ZSIN7 + ZFAED(16)*ZCOS8&
   & + ZFAED(17)*ZSIN8 + ZFAED(18)*ZCOS9 + ZFAED(19)*ZSIN9&
   & + ZFAED(20)*ZCOS10+ ZFAED(21)*ZSIN10                 )  
ENDDO

!       3.4     PREPARE PERTURBATIONS OF AEROSOL DISTRIBUTION
!               ---------------------------------------------

IF (YDSPP_CONFIG%LSPP) THEN

  IPN = YDSPP_CONFIG%PPTR%ZHS_VDAERO
  LLPERT_ZHS_VDAERO = IPN > 0
  IF (LLPERT_ZHS_VDAERO) THEN
    PN1ZHS_VD = YDSPP_CONFIG%SM%PN(IPN)
    IPZHSVDAERO=PN1ZHS_VD%MP_RADGRID
  ENDIF

  IPN = YDSPP_CONFIG%PPTR%DELTA_AERO
  LLPERT_DELTA_AERO = IPN > 0
  IF (LLPERT_DELTA_AERO) THEN
    PN1DELTA = YDSPP_CONFIG%SM%PN(IPN)
    IPDELTAAERO=PN1DELTA%MP_RADGRID
  ENDIF

ELSE
  LLPERT_ZHS_VDAERO = .FALSE.
  LLPERT_DELTA_AERO = .FALSE.
ENDIF

!     ------------------------------------------------------------------

!*       4.      VERTICAL DISTRIBUTION
!*               ---------------------
!* HH The code around LESO4HIS could be simpler; but it ensures bit-identical
!* HH results with CY36R4 when it is .false. (default)

IL=KSHIFT
DO JL=KIDIA,KFDIA,KRINT
  IL=IL+1
  ZDPO(IL)=PAPRS (JL,1)
  ZCPHO3=PAPRS (JL,1)**3
  ZSDPO3=SQRT  (ZCPHO3)
  IF (LNEWAER) THEN
    ZVDAEL=CVDAEL(1)
    ZVDAES=CVDAES(1)
    ZVDAEU=CVDAEU(1)
    ZVDAED=CVDAED(1)
    IF (NAERMACC == 1) THEN
      ZVDAESU=CVDAESU(1)
      ZVDAESS=CVDAESS(1)
      ZVDAEDU=CVDAEDU(1)
      ZVDAEOM=CVDAEOM(1)
      ZVDAEBC=CVDAEBC(1)
    ENDIF
    !
    !   SPP: perturb vertical length scales if LLPERT_ZHS_VDAERO=T
    !     ZVDAE. is replaced by ZVDAE**(1./r) with 1/r=ZRRVD
    !
    IF (LLPERT_ZHS_VDAERO) THEN
      ! distribution of r^-1
      ! random pattern stored in ZRRVD for later use
      ZRRVD(IL)=EXP(-(PN1ZHS_VD%MU(1)+PN1ZHS_VD%XMAG(1)*PPERT(JL,IPZHSVDAERO)))
      ZVDAEL=ZVDAEL**ZRRVD(IL)
      ZVDAES=ZVDAES**ZRRVD(IL)
      ZVDAEU=ZVDAEU**ZRRVD(IL)
      ZVDAED=ZVDAED**ZRRVD(IL)
      IF (NAERMACC == 1) THEN
        ZVDAESU=ZVDAESU**ZRRVD(IL)
        ZVDAESS=ZVDAESS**ZRRVD(IL)
        ZVDAEDU=ZVDAEDU**ZRRVD(IL)
        ZVDAEOM=ZVDAEOM**ZRRVD(IL)
        ZVDAEBC=ZVDAEBC**ZRRVD(IL)
      ENDIF
    ENDIF

    ZXAERSU(IL)=ZAERSU(IL)
    ZXAERSS(IL)=ZAERSS(IL)
    ZXAEROR(IL)=ZAEROR(IL)
    ZXAERBC(IL)=ZAERBC(IL)
    ZXAERSD(IL)=ZAERSD(IL)
    IF (NAERMACC == 1 .AND. .NOT. LAER3D) THEN
      ZXAERSUA(IL)=ZAERSUA(IL)
      ZXAERSSA(IL)=ZAERSSA(IL)
      ZXAERSSB(IL)=ZAERSSB(IL)
      ZXAERSSC(IL)=ZAERSSC(IL)
      ZXAERSDA(IL)=ZAERSDA(IL)
      ZXAERSDB(IL)=ZAERSDB(IL)
      ZXAERSDC(IL)=ZAERSDC(IL)
      ZXAERORA(IL)=ZAERORA(IL)
      ZXAERORB(IL)=ZAERORB(IL)
      ZXAERBCA(IL)=ZAERBCA(IL)
      ZXAERBCB(IL)=ZAERBCB(IL)
    ENDIF
    !
    !   SPP: perturb 2D horizontal distribution if LLPERT_DELTA_AERO=T
    !
    IF (LLPERT_DELTA_AERO) THEN
      ZRDELTA=EXP(PN1DELTA%MU(1)+PN1DELTA%XMAG(1)*PPERT(JL,IPDELTAAERO))
      ZXAERSU(IL)=ZXAERSU(IL)*ZRDELTA
      ZXAERSS(IL)=ZXAERSS(IL)*ZRDELTA
      ZXAEROR(IL)=ZXAEROR(IL)*ZRDELTA
      ZXAERBC(IL)=ZXAERBC(IL)*ZRDELTA
      ZXAERSD(IL)=ZXAERSD(IL)*ZRDELTA
      IF (NAERMACC == 1 .AND. .NOT. LAER3D) THEN
        ZXAERSUA(IL)=ZXAERSUA(IL)*ZRDELTA
        ZXAERSSA(IL)=ZXAERSSA(IL)*ZRDELTA
        ZXAERSSB(IL)=ZXAERSSB(IL)*ZRDELTA
        ZXAERSSC(IL)=ZXAERSSC(IL)*ZRDELTA
        ZXAERSDA(IL)=ZXAERSDA(IL)*ZRDELTA
        ZXAERSDB(IL)=ZXAERSDB(IL)*ZRDELTA
        ZXAERSDC(IL)=ZXAERSDC(IL)*ZRDELTA
        ZXAERORA(IL)=ZXAERORA(IL)*ZRDELTA
        ZXAERORB(IL)=ZXAERORB(IL)*ZRDELTA
        ZXAERBCA(IL)=ZXAERBCA(IL)*ZRDELTA
        ZXAERBCB(IL)=ZXAERBCB(IL)*ZRDELTA
      ENDIF
    ENDIF
      
    ZSO4SO(IL)= ZXAERSU(IL)*ZVDAEL
    ZAEQSO(IL)= ZXAERSS(IL)*ZVDAES
    IF (LESO4HIS) THEN
       ZAEQLO(IL)= ZXAEROR(IL)*ZVDAEL
    ELSE
       ZAEQLO(IL)= ZXAEROR(IL)*ZVDAEL + ZSO4SO(IL)
    ENDIF
    ZAEQUO(IL)= ZXAERBC(IL)*ZVDAEU
    ZAEQDO(IL)= ZXAERSD(IL)*ZVDAED
    IF (NAERMACC == 1 .AND. .NOT. LAER3D) THEN
      ZAERSUO(IL)= ZXAERSUA(IL)*ZVDAESU   
      ZAERS1O(IL)= ZXAERSSA(IL)*ZVDAESS   
      ZAERS2O(IL)= ZXAERSSB(IL)*ZVDAESS
      ZAERS3O(IL)= ZXAERSSC(IL)*ZVDAESS
      ZAERD1O(IL)= ZXAERSDA(IL)*ZVDAEDU
      ZAERD2O(IL)= ZXAERSDB(IL)*ZVDAEDU
      ZAERD3O(IL)= ZXAERSDC(IL)*ZVDAEDU
      ZAERO1O(IL)= ZXAERORA(IL)*ZVDAEOM
      ZAERO2O(IL)= ZXAERORB(IL)*ZVDAEOM
      ZAERB1O(IL)= ZXAERBCA(IL)*ZVDAEBC
      ZAERB2O(IL)= ZXAERBCB(IL)*ZVDAEBC
    ENDIF
  ELSE
    ZSO4SO(IL)=0.0_JPRB
    ZAEQSO(IL)=RCAEOPS*ZAES(IL)*CVDAES(1)
    ZAEQLO(IL)=RCAEOPL*ZAEL(IL)*CVDAEL(1)
    ZAEQUO(IL)=RCAEOPU*ZAEU(IL)*CVDAEU(1)
    ZAEQDO(IL)=RCAEOPD*ZAED(IL)*CVDAED(1)
  ENDIF  
  ZAETRO(IL)=1.0_JPRB
  ZQOFO(IL)=ZOZQ(IL)*ZSDPO3 / (ZSDPO3 + ZOZH(IL))
ENDDO

DO JK=1,KLEV
  IL=KSHIFT
  IF (KCF == 0) THEN
    DO JL=KIDIA,KFDIA,KRINT
      IL=IL+1
      ZGRTH(IL)= PTH(JL,JK)/PTH(JL,JK+1)
      ZRH(IL)  = PQ (JL,JK)/PQS(JL,JK)
    ENDDO
  ELSEIF (KCF == 1) THEN
    DO JL=KIDIA,KFDIA,KRINT
      IL=IL+1
      ZGRTH(IL)= PTH(IL,JK)/PTH(IL,JK+1)
      ZRH(IL)  = PQ (IL,JK)/PQS(IL,JK)
    ENDDO
  ENDIF

  IF(LESO4HIS) THEN
    IL=KSHIFT
    DO JL=KIDIA,KFDIA,KRINT
      IL=IL+1
      IRH(IL)=1
      DO JTAB=1,12
        IF (ZRH(IL)*100._JPRB > RRHTAB(JTAB)) THEN
          IRH(IL)=JTAB
        ENDIF
      ENDDO
    ENDDO
  ENDIF

  IL=KSHIFT
  DO JL=KIDIA,KFDIA,KRINT
    IL=IL+1
    ZDPN(IL)=PAPRS (JL,JK+1)
    ZCPHN3=PAPRS (JL,JK+1)**3
    ZSDPN3=SQRT  (ZCPHN3)
    IF (LNEWAER) THEN
      ZVDAEL=CVDAEL(JK+1)
      ZVDAES=CVDAES(JK+1)
      ZVDAEU=CVDAEU(JK+1)
      ZVDAED=CVDAED(JK+1)
      IF (NAERMACC == 1) THEN
        ZVDAESU=CVDAESU(JK+1)
        ZVDAESS=CVDAESS(JK+1)
        ZVDAEDU=CVDAEDU(JK+1)
        ZVDAEOM=CVDAEOM(JK+1)
        ZVDAEBC=CVDAEBC(JK+1)
      ENDIF
      !
      !   SPP: perturb vertical length scales if LLPERT_ZHS_VDAERO=T
      !     ZVDAE. is replaced by ZVDAE**(1./r) with 1/r=ZRRVD
      !
      IF (LLPERT_ZHS_VDAERO) THEN
        !
        ! distribution of r^-1:
        !   ZRRVD(IL) is pre-computed in block that deals with CVDAE.(1)
        !
        ZVDAEL=ZVDAEL**ZRRVD(IL)
        ZVDAES=ZVDAES**ZRRVD(IL)
        ZVDAEU=ZVDAEU**ZRRVD(IL)
        ZVDAED=ZVDAED**ZRRVD(IL)
        IF (NAERMACC == 1) THEN
          ZVDAESU=ZVDAESU**ZRRVD(IL)
          ZVDAESS=ZVDAESS**ZRRVD(IL)
          ZVDAEDU=ZVDAEDU**ZRRVD(IL)
          ZVDAEOM=ZVDAEOM**ZRRVD(IL)
          ZVDAEBC=ZVDAEBC**ZRRVD(IL)
        ENDIF
      ENDIF
      !
      !   ZXAER.. are unperturbed if LLPERT_DELTA_AERO=F
      !
      ZSO4SN(IL)= ZXAERSU(IL)*ZVDAEL
      ZAEQSN(IL)= ZXAERSS(IL)*ZVDAES
      IF (LESO4HIS) THEN
         ZAEQLN(IL)= ZXAEROR(IL)*ZVDAEL
      ELSE
         ZAEQLN(IL)= ZXAEROR(IL)*ZVDAEL + ZSO4SN(IL)
      ENDIF
      ZAEQUN(IL)= ZXAERBC(IL)*ZVDAEU
      ZAEQDN(IL)= ZXAERSD(IL)*ZVDAED
      IF (NAERMACC == 1 .AND. .NOT. LAER3D) THEN
        ZAERSUN(IL)= ZXAERSUA(IL)*ZVDAESU
        ZAERS1N(IL)= ZXAERSSA(IL)*ZVDAESS
        ZAERS2N(IL)= ZXAERSSB(IL)*ZVDAESS
        ZAERS3N(IL)= ZXAERSSC(IL)*ZVDAESS
        ZAERD1N(IL)= ZXAERSDA(IL)*ZVDAEDU
        ZAERD2N(IL)= ZXAERSDB(IL)*ZVDAEDU
        ZAERD3N(IL)= ZXAERSDC(IL)*ZVDAEDU
        ZAERO1N(IL)= ZXAERORA(IL)*ZVDAEOM
        ZAERO2N(IL)= ZXAERORB(IL)*ZVDAEOM
        ZAERB1N(IL)= ZXAERBCA(IL)*ZVDAEBC
        ZAERB2N(IL)= ZXAERBCB(IL)*ZVDAEBC
      ENDIF
    ELSE
      ZSO4SN(IL)=0.0_JPRB
      ZAEQSN(IL)=RCAEOPS*ZAES(IL)*CVDAES(JK+1)
      ZAEQLN(IL)=RCAEOPL*ZAEL(IL)*CVDAEL(JK+1)
      ZAEQUN(IL)=RCAEOPU*ZAEU(IL)*CVDAEU(JK+1)
      ZAEQDN(IL)=RCAEOPD*ZAED(IL)*CVDAED(JK+1)
    ENDIF  

    IF (0.5_JPRB*(PAPRS(JL,JK)+PAPRS(JL,JK+1)) < 999._JPRB) THEN
      ZAETRN(IL)=1.0_JPRB
    ELSE
      ZAETRN(IL)=ZAETRO(IL)*(MIN(1.0_JPRB,     ZGRTH(IL)         ))**RCTRPT
    ENDIF
    ZAETR=SQRT  (ZAETRN(IL)*ZAETRO(IL))
    IF(NVOLCVERT==0) THEN
      ZAETRV=ZAETR
    ELSEIF(NVOLCVERT==1) THEN
! Use temperature profile, but no volcanic aerosol above 10hPa
      IF (0.5_JPRB*(PAPRS(JL,JK)+PAPRS(JL,JK+1)) < 999._JPRB) THEN
        ZAETRV=0.0_JPRB
      ELSE
        ZAETRV=ZAETR
      ENDIF
    ELSEIF(NVOLCVERT==2) THEN
! Use prognostic ozone to define lower edge of aerosol distribution
      IF (0.5_JPRB*(PAPRS(JL,JK)+PAPRS(JL,JK+1)) < 999._JPRB) THEN
        ZAETRV=0.0_JPRB
      ELSEIF(0.5_JPRB*(PAPRS(JL,JK)+PAPRS(JL,JK+1)) < 7000._JPRB) THEN
        ZAETRV=1.0_JPRB
      ELSEIF(PAPRS(JL,JK) > 40000._JPRB) THEN
        ZAETRV=0.0_JPRB
      ELSE
        IF(PECPO3(JL,JK) > 0.4E-6_JPRB) THEN
          ZAETRV=1.0_JPRB
        ELSEIF(PECPO3(JL,JK) > 0.2E-6_JPRB) THEN
          ZAETRV=(PECPO3(JL,JK)-0.2E-6_JPRB)/0.2E-6_JPRB
        ELSE
          ZAETRV=0.0_JPRB
        ENDIF
      ENDIF
    ENDIF

    ZQOFN(IL)=ZOZQ(IL)*ZSDPN3/(ZSDPN3+ZOZH(IL))
    ZDPNMO    =ZDPN(IL)-ZDPO(IL)
    IF(LESO4HIS) THEN
       ZDSO4S = (ZSO4SN(IL)-ZSO4SO(IL))*ALF_SU(IRH(IL),9)
       PAER(IL,1,JK)=(1.0_JPRB-ZAETR)*(RCTRBGA*ZDPNMO+ ZAEQLN(IL)-ZAEQLO(IL) + ZDSO4S)
    ELSE
       PAER(IL,1,JK)=(1.0_JPRB-ZAETR)*(RCTRBGA*ZDPNMO+ ZAEQLN(IL)-ZAEQLO(IL))
    ENDIF
!fixed tropospheric background only in case of MACC climatology
    IF (NAERMACC == 1) PAER(IL,1,JK)=(1.0_JPRB-ZAETR)*(RCTRBGA*ZDPNMO)
    PAER(IL,2,JK)=(1.0_JPRB-ZAETR)*(ZAEQSN(IL)-ZAEQSO(IL))
    PAER(IL,3,JK)=(1.0_JPRB-ZAETR)*(ZAEQDN(IL)-ZAEQDO(IL))
    PAER(IL,4,JK)=(1.0_JPRB-ZAETR)*(ZAEQUN(IL)-ZAEQUO(IL))
! volcanic ash at fixed (low) background value
    PAER(IL,5,JK)=   ZAETRV  * RCVOBGA*ZDPNMO
! volcanic sulphate aerosol either fixed or varying according to GISS 
    PAER(IL,6,JK)=   ZAETRV  * ZAERVO(IL) * ZDPNMO
    POZON(IL,JK)=ZQOFN(IL)-ZQOFO(IL)
!**** **************************************************
!**** **************************************************
    IF (NAERMACC == 1) THEN
       IF (.NOT.LAER3D) THEN
!**** input data are in total integrated mass (kg/m2). The vertical profile is mass mixing ratio (kg/kg)
!**** hence the scaling by dp/g

          ZDP = (PAPRS(JL,JK+1)-PAPRS(JL,JK))/RG
          
          PAERO(IL,JK,1) = (1.0_JPRB-ZAETR)*(ZAERS1N(IL)-ZAERS1O(IL)) / ZDP
          PAERO(IL,JK,2) = (1.0_JPRB-ZAETR)*(ZAERS2N(IL)-ZAERS2O(IL)) / ZDP 
          PAERO(IL,JK,3) = (1.0_JPRB-ZAETR)*(ZAERS3N(IL)-ZAERS3O(IL)) / ZDP
          PAERO(IL,JK,4) = (1.0_JPRB-ZAETR)*(ZAERD1N(IL)-ZAERD1O(IL)) / ZDP
          PAERO(IL,JK,5) = (1.0_JPRB-ZAETR)*(ZAERD2N(IL)-ZAERD2O(IL)) / ZDP
          PAERO(IL,JK,6) = (1.0_JPRB-ZAETR)*(ZAERD3N(IL)-ZAERD3O(IL)) / ZDP
          PAERO(IL,JK,7) = (1.0_JPRB-ZAETR)*(ZAERO1N(IL)-ZAERO1O(IL)) / ZDP
          PAERO(IL,JK,8) = (1.0_JPRB-ZAETR)*(ZAERO2N(IL)-ZAERO2O(IL)) / ZDP
          PAERO(IL,JK,9) = (1.0_JPRB-ZAETR)*(ZAERB1N(IL)-ZAERB1O(IL)) / ZDP
          PAERO(IL,JK,10)= (1.0_JPRB-ZAETR)*(ZAERB2N(IL)-ZAERB2O(IL)) / ZDP
          PAERO(IL,JK,11)= (1.0_JPRB-ZAETR)*(ZAERSUN(IL)-ZAERSUO(IL)) / ZDP
          PAERO(IL,JK,12)= 0._JPRB
       ENDIF
    ENDIF
 ENDDO
  IL=KSHIFT
  DO JL=KIDIA,KFDIA,KRINT
    IL=IL+1
    ZDPO(IL)=ZDPN(IL)
    ZQOFO(IL)=ZQOFN(IL)

    ZAEQSO(IL)=ZAEQSN(IL)
    ZAEQLO(IL)=ZAEQLN(IL)
    ZSO4SO(IL)=ZSO4SN(IL)
    ZAEQUO(IL)=ZAEQUN(IL)
    ZAEQDO(IL)=ZAEQDN(IL)
    ZAETRO(IL)=ZAETRN(IL)

    IF (NAERMACC == 1 .AND. .NOT. LAER3D) THEN
      ZAERSUO(IL)= ZAERSUN(IL)
      ZAERS1O(IL)= ZAERS1N(IL)
      ZAERS2O(IL)= ZAERS2N(IL)
      ZAERS3O(IL)= ZAERS3N(IL)
      ZAERD1O(IL)= ZAERD1N(IL)
      ZAERD2O(IL)= ZAERD2N(IL)
      ZAERD3O(IL)= ZAERD3N(IL)
      ZAERO1O(IL)= ZAERO1N(IL)
      ZAERO2O(IL)= ZAERO2N(IL)
      ZAERB1O(IL)= ZAERB1N(IL)
      ZAERB2O(IL)= ZAERB2N(IL)
    ENDIF
  ENDDO
  
!-- diagnostics on Tegen climatology in case of problem  
  DO JAER=1,6
    IL=KSHIFT
    DO JL=KIDIA,KFDIA,KRINT
      IL=IL+1
      PAER(IL,JAER,JK)=MAX(PAER(IL,JAER,JK),YDRIP%YREAERC_TEGEN%REPAER)
    ENDDO
    ITOT=IL
  ENDDO   
!--
   
ENDDO

!-- Initialization of arrays used in the interpolation
!-- of 3D aerosol climatology
ZAERBCA3D_VI_input(KIDIA:KFDIA,0) = 0._JPRB
ZAERBCA3D_VI(KIDIA:KFDIA,0) = 0._JPRB
ZAERBCB3D_VI_input(KIDIA:KFDIA,0) = 0._JPRB
ZAERBCB3D_VI(KIDIA:KFDIA,0) = 0._JPRB

ZAERORA3D_VI_input(KIDIA:KFDIA,0) = 0._JPRB
ZAERORA3D_VI(KIDIA:KFDIA,0) = 0._JPRB
ZAERORB3D_VI_input(KIDIA:KFDIA,0) = 0._JPRB
ZAERORB3D_VI(KIDIA:KFDIA,0) = 0._JPRB

ZAERSDA3D_VI_input(KIDIA:KFDIA,0) = 0._JPRB
ZAERSDA3D_VI(KIDIA:KFDIA,0) = 0._JPRB
ZAERSDB3D_VI_input(KIDIA:KFDIA,0) = 0._JPRB
ZAERSDB3D_VI(KIDIA:KFDIA,0) = 0._JPRB
ZAERSDC3D_VI_input(KIDIA:KFDIA,0) = 0._JPRB
ZAERSDC3D_VI(KIDIA:KFDIA,0) = 0._JPRB

ZAERSSA3D_VI_input(KIDIA:KFDIA,0) = 0._JPRB
ZAERSSA3D_VI(KIDIA:KFDIA,0) = 0._JPRB
ZAERSSB3D_VI_input(KIDIA:KFDIA,0) = 0._JPRB
ZAERSSB3D_VI(KIDIA:KFDIA,0) = 0._JPRB
ZAERSSC3D_VI_input(KIDIA:KFDIA,0) = 0._JPRB
ZAERSSC3D_VI(KIDIA:KFDIA,0) = 0._JPRB

ZAERSUA3D_VI_input(KIDIA:KFDIA,0) = 0._JPRB
ZAERSUA3D_VI(KIDIA:KFDIA,0) = 0._JPRB

ZREF_AER_PR(KIDIA:KFDIA,0) = 0._JPRB !to define!!!!!

!shift needed because otherwise DP is computed as JK+1 - JK
!while aerosol MMR as JK - JK-1
ZPRS(:,0:KLEV)=PAPRS(:,1:KLEV+1)
IF (LAER3D .AND. NAERMACC==1) THEN
!vertical interpolation of the input 3D climatology
!files come in already in layer mass kg/m2 (mmr*dp/g).
!1)compute the cumulative vertical profile on the original grid
   DO JK=1,KLEV_AER
      ZAERBCA3D_VI_input(KIDIA:KFDIA,JK) = ZAERBCA3D_VI_input(KIDIA:KFDIA,JK-1) + ZAERBCA3D(KIDIA:KFDIA,JK)
      ZAERBCB3D_VI_input(KIDIA:KFDIA,JK) = ZAERBCB3D_VI_input(KIDIA:KFDIA,JK-1) + ZAERBCB3D(KIDIA:KFDIA,JK)

      ZAERORA3D_VI_input(KIDIA:KFDIA,JK) = ZAERORA3D_VI_input(KIDIA:KFDIA,JK-1) + ZAERORA3D(KIDIA:KFDIA,JK)
      ZAERORB3D_VI_input(KIDIA:KFDIA,JK) = ZAERORB3D_VI_input(KIDIA:KFDIA,JK-1) + ZAERORB3D(KIDIA:KFDIA,JK)

      ZAERSDA3D_VI_input(KIDIA:KFDIA,JK) = ZAERSDA3D_VI_input(KIDIA:KFDIA,JK-1) + ZAERSDA3D(KIDIA:KFDIA,JK)
      ZAERSDB3D_VI_input(KIDIA:KFDIA,JK) = ZAERSDB3D_VI_input(KIDIA:KFDIA,JK-1) + ZAERSDB3D(KIDIA:KFDIA,JK)
      ZAERSDC3D_VI_input(KIDIA:KFDIA,JK) = ZAERSDC3D_VI_input(KIDIA:KFDIA,JK-1) + ZAERSDC3D(KIDIA:KFDIA,JK)

      ZAERSSA3D_VI_input(KIDIA:KFDIA,JK) = ZAERSSA3D_VI_input(KIDIA:KFDIA,JK-1) + ZAERSSA3D(KIDIA:KFDIA,JK)
      ZAERSSB3D_VI_input(KIDIA:KFDIA,JK) = ZAERSSB3D_VI_input(KIDIA:KFDIA,JK-1) + ZAERSSB3D(KIDIA:KFDIA,JK)
      ZAERSSC3D_VI_input(KIDIA:KFDIA,JK) = ZAERSSC3D_VI_input(KIDIA:KFDIA,JK-1) + ZAERSSC3D(KIDIA:KFDIA,JK)

      ZAERSUA3D_VI_input(KIDIA:KFDIA,JK) = ZAERSUA3D_VI_input(KIDIA:KFDIA,JK-1) + ZAERSUA3D(KIDIA:KFDIA,JK)
   ENDDO
!2)interpolate the cumulative vertical profile on the (vertical) model grid
   DO JK=1,KLEV
      IL=KSHIFT
      DO JL=KIDIA,KFDIA,KRINT
         IL=IL+1
         IC=KLEV_AER-1
         DO JC=0,KLEV_AER-1
            IF( ZPRS(JL,JK) >= ZREF_AER_PR(IL,JC) .AND. ZPRS(JL,JK) < ZREF_AER_PR(IL,JC+1)) THEN
               IC = JC
               EXIT
            ENDIF
         ENDDO
         ZINT=(ZPRS(JL,JK)-ZREF_AER_PR(IL,IC))/(ZREF_AER_PR(IL,IC+1)-ZREF_AER_PR(IL,IC))

         ZAERBCA3D_VI(IL,JK) = ZAERBCA3D_VI_input(IL,IC) + ZINT * (ZAERBCA3D_VI_input(IL,IC+1) -ZAERBCA3D_VI_input(IL,IC))
         ZAERBCB3D_VI(IL,JK) = ZAERBCB3D_VI_input(IL,IC) + ZINT * (ZAERBCB3D_VI_input(IL,IC+1) -ZAERBCB3D_VI_input(IL,IC))

         ZAERORA3D_VI(IL,JK) = ZAERORA3D_VI_input(IL,IC) + ZINT * (ZAERORA3D_VI_input(IL,IC+1) -ZAERORA3D_VI_input(IL,IC))
         ZAERORB3D_VI(IL,JK) = ZAERORB3D_VI_input(IL,IC) + ZINT * (ZAERORB3D_VI_input(IL,IC+1) -ZAERORB3D_VI_input(IL,IC))

         ZAERSDA3D_VI(IL,JK) = ZAERSDA3D_VI_input(IL,IC) + ZINT * (ZAERSDA3D_VI_input(IL,IC+1) -ZAERSDA3D_VI_input(IL,IC))
         ZAERSDB3D_VI(IL,JK) = ZAERSDB3D_VI_input(IL,IC) + ZINT * (ZAERSDB3D_VI_input(IL,IC+1) -ZAERSDB3D_VI_input(IL,IC))
         ZAERSDC3D_VI(IL,JK) = ZAERSDC3D_VI_input(IL,IC) + ZINT * (ZAERSDC3D_VI_input(IL,IC+1) -ZAERSDC3D_VI_input(IL,IC))

         ZAERSSA3D_VI(IL,JK) = ZAERSSA3D_VI_input(IL,IC) + ZINT * (ZAERSSA3D_VI_input(IL,IC+1) -ZAERSSA3D_VI_input(IL,IC))
         ZAERSSB3D_VI(IL,JK) = ZAERSSB3D_VI_input(IL,IC) + ZINT * (ZAERSSB3D_VI_input(IL,IC+1) -ZAERSSB3D_VI_input(IL,IC))
         ZAERSSC3D_VI(IL,JK) = ZAERSSC3D_VI_input(IL,IC) + ZINT * (ZAERSSC3D_VI_input(IL,IC+1) -ZAERSSC3D_VI_input(IL,IC))

         ZAERSUA3D_VI(IL,JK) = ZAERSUA3D_VI_input(IL,IC) + ZINT * (ZAERSUA3D_VI_input(IL,IC+1) -ZAERSUA3D_VI_input(IL,IC))
      ENDDO
   ENDDO
!3)compute the mass mixing ratio from the cumulative vertical profile
   DO JK=1,KLEV
      IL=KSHIFT
      DO JL=KIDIA,KFDIA,KRINT
         IL=IL+1
         ZDP=(ZPRS(JL,JK)-ZPRS(JL,JK-1))/RG !back to kg/kg from kg/m2

         PAERO(IL,JK,1) = (ZAERSSA3D_VI(IL,JK)-ZAERSSA3D_VI(IL,JK-1)) / ZDP
         PAERO(IL,JK,2) = (ZAERSSB3D_VI(IL,JK)-ZAERSSB3D_VI(IL,JK-1)) / ZDP
         PAERO(IL,JK,3) = (ZAERSSC3D_VI(IL,JK)-ZAERSSC3D_VI(IL,JK-1)) / ZDP

         PAERO(IL,JK,4) = (ZAERSDA3D_VI(IL,JK)-ZAERSDA3D_VI(IL,JK-1)) / ZDP
         PAERO(IL,JK,5) = (ZAERSDB3D_VI(IL,JK)-ZAERSDB3D_VI(IL,JK-1)) / ZDP
         PAERO(IL,JK,6) = (ZAERSDC3D_VI(IL,JK)-ZAERSDC3D_VI(IL,JK-1)) / ZDP

         PAERO(IL,JK,7) = (ZAERORA3D_VI(IL,JK)-ZAERORA3D_VI(IL,JK-1)) / ZDP
         PAERO(IL,JK,8) = (ZAERORB3D_VI(IL,JK)-ZAERORB3D_VI(IL,JK-1)) / ZDP

         PAERO(IL,JK,9) = (ZAERBCA3D_VI(IL,JK)-ZAERBCA3D_VI(IL,JK-1)) / ZDP
         PAERO(IL,JK,10) = (ZAERBCB3D_VI(IL,JK)-ZAERBCB3D_VI(IL,JK-1)) / ZDP

         PAERO(IL,JK,11) = (ZAERSUA3D_VI(IL,JK)-ZAERSUA3D_VI(IL,JK-1)) / ZDP
         PAERO(IL,JK,12) = 0._JPRB
      ENDDO
   ENDDO
ENDIF

!     ------------------------------------------------------------------

END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('RADACT',1,ZHOOK_HANDLE)
END SUBROUTINE RADACT
