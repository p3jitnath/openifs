SUBROUTINE COMMSPNORM(YDGEOMETRY,LDPRTAL,LDOROG,KSMAX,&
 & KIOMASTER,KFTHER,KNUMSPFLDS,&
 & PNVOR  ,PNVORG  ,PMVOR  ,PMVORG,&
 & PNDIV  ,PNDIVG  ,PMDIV  ,PMDIVG,&
 & PNKE   ,PNKEG   ,PMKE   ,PMKEG,&
 & PNTHE  ,PNTHEG  ,PMTHE  ,PMTHEG,&
 & PNGFL  ,PNGFLG  ,PMGFL  ,PMGFLG,&
 & PNSP   ,PNSPG   ,PMSP   ,PMSPG,&
 & PNSPOR ,PNSPORG ,PMSPOR ,PMSPORG,&
 & PNTHEDE,PNTHEDEG,PMTHEDE,PMTHEDEG,&
 & PNGFLDE,PNGFLDEG,PMGFLDE,PMGFLDEG,&
 & PNSPDE ,PNSPDEG ,PMSPDE ,PMSPDEG)  

!**** *COMMSPNORM* Communicates spectral norm data

!     Purpose.
!     --------

!**   Interface.
!     ----------
!        *CALL* *COMMSPNORM(...)

!        Explicit arguments : 
!        -------------------- LDPRTAL - .T. if highest level output is required (input)
!                             LDOROG  - .T. if orography has to be communicated (input)
!                             KSMAX   - truncation
!                             KFTHER  - Number of thermodynamic fields
!                             KNUMSPFLDS  - Number of spectral gfl fields
!                             KIOMASTER - processor collecting global data
!                             PNxxx   - local n wave data   (input)
!                             PNxxxG  - global n wave data  (output)
!                             PMxxx   - local m wave data   (input)
!                             PMxxxG  - global m wave data  (output)

!        Implicit arguments :
!        --------------------

!     Method.
!     -------
!        See documentation

!     Externals.
!     ----------
!        MPL_SEND, MPL_RECV  -  communication interface

!     Reference.
!     ----------
!        ECMWF Research Department documentation of the IFS

!     Author.
!     -------
!      L. Isaksen  *ECMWF*
!      Original: 97-02-02 

!     Modifications.
!     --------------
!      M.Hamrud      01-Oct-2003 CY28 Cleaning
!      K.Yessad: Apr 2005 Bug corrections.
!      G.Mozdzynski: Apr 2012 Optimise comms to use a 2D gather scheme
!      T. Wilhelmsson and K. Yessad (Oct 2013) Geometry and setup refactoring.
!     ------------------------------------------------------------------

USE GEOMETRY_MOD , ONLY : GEOMETRY
USE PARKIND1     , ONLY : JPIM, JPRB
USE YOMHOOK      , ONLY : LHOOK, DR_HOOK, JPHOOK
USE YOMCT0       , ONLY : NSPPR
USE YOMMP0       , ONLY : MYPROC, NPRCIDS, MYSETW, NPROC, NPRTRW, NPRTRV
USE YOMTAG       , ONLY : MTAGSPNORM
USE MPL_MODULE

!     ------------------------------------------------------------------

IMPLICIT NONE

TYPE(GEOMETRY)    ,INTENT(IN)    :: YDGEOMETRY
INTEGER(KIND=JPIM),INTENT(IN)    :: KSMAX 
LOGICAL           ,INTENT(IN)    :: LDPRTAL 
LOGICAL           ,INTENT(IN)    :: LDOROG 
INTEGER(KIND=JPIM),INTENT(IN)    :: KIOMASTER 
INTEGER(KIND=JPIM),INTENT(IN)    :: KFTHER 
INTEGER(KIND=JPIM),INTENT(IN)    :: KNUMSPFLDS 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PNVOR(YDGEOMETRY%YRDIMV%NFLEVL,YDGEOMETRY%YRDIM%NUMP) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PNVORG(YDGEOMETRY%YRDIMV%NFLEVG,0:KSMAX) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PMVOR(YDGEOMETRY%YRDIMV%NFLEVL,YDGEOMETRY%YRDIM%NUMP) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PMVORG(YDGEOMETRY%YRDIMV%NFLEVG,0:KSMAX) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PNDIV(YDGEOMETRY%YRDIMV%NFLEVL,YDGEOMETRY%YRDIM%NUMP) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PNDIVG(YDGEOMETRY%YRDIMV%NFLEVG,0:KSMAX) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PMDIV(YDGEOMETRY%YRDIMV%NFLEVL,YDGEOMETRY%YRDIM%NUMP) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PMDIVG(YDGEOMETRY%YRDIMV%NFLEVG,0:KSMAX) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PNKE(YDGEOMETRY%YRDIMV%NFLEVL,YDGEOMETRY%YRDIM%NUMP) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PNKEG(YDGEOMETRY%YRDIMV%NFLEVG,0:KSMAX) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PMKE(YDGEOMETRY%YRDIMV%NFLEVL,YDGEOMETRY%YRDIM%NUMP) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PMKEG(YDGEOMETRY%YRDIMV%NFLEVG,0:KSMAX) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PNTHE(YDGEOMETRY%YRDIMV%NFLEVL,YDGEOMETRY%YRDIM%NUMP,KFTHER) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PNTHEG(YDGEOMETRY%YRDIMV%NFLEVG,0:KSMAX,KFTHER) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PMTHE(YDGEOMETRY%YRDIMV%NFLEVL,YDGEOMETRY%YRDIM%NUMP,KFTHER) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PMTHEG(YDGEOMETRY%YRDIMV%NFLEVG,0:KSMAX,KFTHER) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PNGFL(YDGEOMETRY%YRDIMV%NFLEVL,YDGEOMETRY%YRDIM%NUMP,KNUMSPFLDS) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PNGFLG(YDGEOMETRY%YRDIMV%NFLEVG,0:KSMAX,KNUMSPFLDS) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PMGFL(YDGEOMETRY%YRDIMV%NFLEVL,YDGEOMETRY%YRDIM%NUMP,KNUMSPFLDS) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PMGFLG(YDGEOMETRY%YRDIMV%NFLEVG,0:KSMAX,KNUMSPFLDS) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PNSP(YDGEOMETRY%YRDIM%NUMP) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PNSPG(0:KSMAX) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PMSP(YDGEOMETRY%YRDIM%NUMP) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PMSPG(0:KSMAX) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PNSPOR(YDGEOMETRY%YRDIM%NUMP) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PNSPORG(0:KSMAX) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PMSPOR(YDGEOMETRY%YRDIM%NUMP) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PMSPORG(0:KSMAX) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PNTHEDE(YDGEOMETRY%YRDIMV%NFLEVL,YDGEOMETRY%YRDIM%NUMP,KFTHER) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PNTHEDEG(YDGEOMETRY%YRDIMV%NFLEVG,0:KSMAX,KFTHER) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PMTHEDE(YDGEOMETRY%YRDIMV%NFLEVL,YDGEOMETRY%YRDIM%NUMP,KFTHER) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PMTHEDEG(YDGEOMETRY%YRDIMV%NFLEVG,0:KSMAX,KFTHER) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PNGFLDE(YDGEOMETRY%YRDIMV%NFLEVL,YDGEOMETRY%YRDIM%NUMP,KNUMSPFLDS) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PNGFLDEG(YDGEOMETRY%YRDIMV%NFLEVG,0:KSMAX,KNUMSPFLDS) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PMGFLDE(YDGEOMETRY%YRDIMV%NFLEVL,YDGEOMETRY%YRDIM%NUMP,KNUMSPFLDS) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PMGFLDEG(YDGEOMETRY%YRDIMV%NFLEVG,0:KSMAX,KNUMSPFLDS) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PNSPDE(YDGEOMETRY%YRDIM%NUMP) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PNSPDEG(0:KSMAX) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PMSPDE(YDGEOMETRY%YRDIM%NUMP) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PMSPDEG(0:KSMAX) 

!     ------------------------------------------------------------------

REAL(KIND=JPRB) :: ZSENDBUF(((3+2*KFTHER+KNUMSPFLDS+1)*YDGEOMETRY%YRDIMV%NFLEVL+3*YDGEOMETRY%YRMP%NPSP)*YDGEOMETRY%YRDIM%NUMP)
REAL(KIND=JPRB),ALLOCATABLE :: ZRECVBUF(:)
REAL(KIND=JPRB),ALLOCATABLE :: ZBUFW(:)

INTEGER(KIND=JPIM) :: IA, IB, IBUFLENR, IBUFLENS, ILEV, IM, IOFF, IOFFDIV, IOFFKE,&
 & IOFFSP, IOFFSPDE, IOFFSPOR, IOFFTHE,&
 & IOFFTHEDE, IOFFGFLDE, IOFFVOR, IRECVLEV, IRECVNUMP,&
 & IRECVSETA, IRECVSETB, ISTLEV, ISTOTAL,&
 & ISZPS, ISZPSOR, ISZSP, ISZSPR, ISZTHE,&
 & ITAG, ITOTAL, JF, JL, JMLOC, JROC,IOFFB,ISZGFL,IOFFGFL  

INTEGER(KIND=JPIM) :: IREQ(NPROC+1)
INTEGER(KIND=JPIM) :: IMASTER,IR,ITOT,IINC,IRECV,JSETW
INTEGER(KIND=JPIM) :: IBEG,IEND,IBEGOFF,IENDOFF,ISETW,ISETV

INTEGER(KIND=JPIM) :: IRECVCOUNTS(NPROC)

LOGICAL :: LLHASPS
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
#ifdef __INTEL_COMPILER
INTRINSIC :: SHAPE ! Fails with Intel compiler as it thinks we use ATLAS shape function
#endif


!     ------------------------------------------------------------------

#include "abor1.intfb.h"
#include "pe2set.intfb.h"
#include "set2pe.intfb.h"

!     ------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('COMMSPNORM',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM=>YDGEOMETRY%YRDIM,YDDIMV=>YDGEOMETRY%YRDIMV,YDGEM=>YDGEOMETRY%YRGEM, YDMP=>YDGEOMETRY%YRMP)
ASSOCIATE(NUMP=>YDDIM%NUMP, &
 & NFLEVG=>YDDIMV%NFLEVG, NFLEVL=>YDDIMV%NFLEVL, &
 & NALLMS=>YDMP%NALLMS, NPSP=>YDMP%NPSP, NPSURF=>YDMP%NPSURF, &
 & NPTRLL=>YDMP%NPTRLL, NPTRMS=>YDMP%NPTRMS, NUMLL=>YDMP%NUMLL, &
 & NUMPP=>YDMP%NUMPP)

!     ------------------------------------------------------------------


IBUFLENS=SIZE(ZSENDBUF)

! ------------------------------------------------------------------

!     1.    Shift N-fields from 1..NUMP to 0..KSMAX
!           ---------------------------------------

IF (NPROC == 1.AND.NSPPR >= 2) THEN
  PNVORG(:,0:KSMAX) = PNVOR(:,1:NUMP)
  PNDIVG(:,0:KSMAX) = PNDIV(:,1:NUMP)
  PNKEG(:,0:KSMAX) = PNKE(:,1:NUMP)
  IF (KFTHER > 0) THEN
    PNTHEG(:,0:KSMAX,:) = PNTHE(:,1:NUMP,:)
  ENDIF
  IF (KNUMSPFLDS > 0) THEN
    PNGFLG(:,0:KSMAX,:) = PNGFL(:,1:NUMP,:)
  ENDIF
  PNSPG(0:KSMAX) = PNSP(1:NUMP)
  IF (LDOROG) PNSPORG(0:KSMAX) = PNSPOR(1:NUMP)
  IF (LDPRTAL) THEN
    IF (KFTHER > 0) THEN
      PNTHEDEG(:,0:KSMAX,:) = PNTHEDE(:,1:NUMP,:)
    ENDIF
    IF (KNUMSPFLDS > 0) THEN
      PNGFLDEG(:,0:KSMAX,:) = PNGFLDE(:,1:NUMP,:)
    ENDIF
    PNSPDEG(0:KSMAX) = PNSPDE(1:NUMP)
  ENDIF
ENDIF

! ------------------------------------------------------------------

!     2.    Send local data to the KIOMASTER PE
!           ----------------------------------

ISZSP = SIZE(PMVOR)

IOFF = 0
ZSENDBUF(IOFF+1:IOFF+ISZSP) =&
 & RESHAPE(PMVOR,SHAPE(ZSENDBUF(IOFF+1:IOFF+ISZSP)))  
IOFF = IOFF+ISZSP

ZSENDBUF(IOFF+1:IOFF+ISZSP) =&
 & RESHAPE(PMDIV,SHAPE(ZSENDBUF(IOFF+1:IOFF+ISZSP)))  
IOFF = IOFF+ISZSP

ZSENDBUF(IOFF+1:IOFF+ISZSP) =RESHAPE(PMKE,SHAPE(ZSENDBUF(IOFF+1:IOFF+ISZSP)))
IOFF = IOFF+ISZSP

IF (KFTHER > 0) THEN
  ISZTHE = SIZE(PMTHE)
  ZSENDBUF(IOFF+1:IOFF+ISZTHE) =&
   & RESHAPE(PMTHE,SHAPE(ZSENDBUF(IOFF+1:IOFF+ISZTHE)))  
  IOFF = IOFF+ISZTHE
ENDIF

IF (KNUMSPFLDS > 0) THEN
  ISZGFL = SIZE(PMGFL)
  ZSENDBUF(IOFF+1:IOFF+ISZGFL) =&
   & RESHAPE(PMGFL,SHAPE(ZSENDBUF(IOFF+1:IOFF+ISZGFL)))  
  IOFF = IOFF+ISZGFL
ENDIF

IF (NPSP == 1) THEN
  ISZPS = SIZE(PMSP)
  ZSENDBUF(IOFF+1:IOFF+ISZPS) =&
   & RESHAPE(PMSP,SHAPE(ZSENDBUF(IOFF+1:IOFF+ISZPS)))  
  IOFF = IOFF+ISZPS
ENDIF

IF (NPSP == 1.AND.LDOROG) THEN
  ISZPSOR = SIZE(PMSPOR)
  ZSENDBUF(IOFF+1:IOFF+ISZPSOR) =&
   & RESHAPE(PMSPOR,SHAPE(ZSENDBUF(IOFF+1:IOFF+ISZPSOR)))  
  IOFF = IOFF+ISZPSOR
ENDIF

IF (LDPRTAL) THEN
  IF (KFTHER > 0) THEN
    ISZTHE = SIZE(PMTHEDE)
    ZSENDBUF(IOFF+1:IOFF+ISZTHE) =&
     & RESHAPE(PMTHEDE,SHAPE(ZSENDBUF(IOFF+1:IOFF+ISZTHE)))  
    IOFF = IOFF+ISZTHE
  ENDIF
  IF (KNUMSPFLDS > 0) THEN
    ISZGFL = SIZE(PMGFLDE)
    ZSENDBUF(IOFF+1:IOFF+ISZGFL) =&
     & RESHAPE(PMGFLDE,SHAPE(ZSENDBUF(IOFF+1:IOFF+ISZGFL)))  
    IOFF = IOFF+ISZGFL
  ENDIF

  IF (NPSP == 1) THEN
    ISZPS = SIZE(PMSPDE)
    ZSENDBUF(IOFF+1:IOFF+ISZPS) =&
     & RESHAPE(PMSPDE,SHAPE(ZSENDBUF(IOFF+1:IOFF+ISZPS)))  
    IOFF = IOFF+ISZPS
  ENDIF
ENDIF

ISTOTAL=IOFF

IF (ISTOTAL > IBUFLENS) CALL ABOR1(' COMMSPNORM : SEND BUFFER '&
 & // 'TOO SMALL ')  

IF (NPROC > 1) THEN
  IBUFLENR=0
  DO JROC=1,NPROC
    CALL PE2SET(JROC,IA,IB,IRECVSETA,IRECVSETB)
    IRECVLEV=NUMLL(IRECVSETB)
    IRECVNUMP=NUMPP(IRECVSETA)
    LLHASPS=NPSURF(IRECVSETB) == 1

    ISTLEV=NPTRLL(IRECVSETB)
    ISZSPR=IRECVLEV*IRECVNUMP

    IOFFVOR =  0
    IOFFDIV = IOFFVOR+IRECVLEV*IRECVNUMP
    IOFFKE  = IOFFDIV+IRECVLEV*IRECVNUMP
    IOFFTHE = IOFFKE+IRECVLEV*IRECVNUMP
    IOFFGFL = IOFFTHE+IRECVLEV*IRECVNUMP*KFTHER
    IOFFSP  = IOFFGFL+IRECVLEV*IRECVNUMP*KNUMSPFLDS
    IF (LDOROG) THEN
      IOFFSPOR = IOFFSP+IRECVNUMP*NPSURF(IRECVSETB)
    ELSE
      IOFFSPOR  = IOFFSP
    ENDIF
    IF (LDPRTAL) THEN
      IOFFTHEDE = IOFFSPOR+IRECVNUMP*NPSURF(IRECVSETB)
      IOFFGFLDE = IOFFTHEDE+IRECVLEV*IRECVNUMP*KFTHER
      IOFFSPDE  = IOFFGFLDE+IRECVLEV*IRECVNUMP*KNUMSPFLDS
      ITOTAL    = IOFFSPDE+IRECVNUMP*NPSURF(IRECVSETB)
    ELSE
      ITOTAL    = IOFFSPOR+IRECVNUMP*NPSURF(IRECVSETB)
    ENDIF
    IRECVCOUNTS(JROC) = ITOTAL
    IBUFLENR=IBUFLENR+ITOTAL
  ENDDO
  CALL GSTATS_BARRIER(773)
  CALL GSTATS_BARRIER(702) !Second GSTATS barrier to test "real" time of barrier
  CALL GSTATS(607,0)
  ALLOCATE(ZRECVBUF(IBUFLENR))
  IMASTER=1
  IBEG=(MYSETW-1)*NPRTRV+1
  IEND=IBEG+NPRTRV-1
  ALLOCATE(ZBUFW(SUM(IRECVCOUNTS(IBEG:IEND))))
  CALL MPL_GATHERV(ZSENDBUF(1:ISTOTAL),KROOT=IMASTER,&
   & PRECVBUF=ZBUFW,KRECVCOUNTS=IRECVCOUNTS(IBEG:IEND), &
   & KCOMM=MPL_ALL_LEVS_COMM,CDSTRING='COMMSPNORM:')  
  CALL PE2SET(MYPROC,IA,IB,ISETW,ISETV)
  ITAG=MTAGSPNORM
  IR=0
  IF( MYPROC==KIOMASTER )THEN
    ITOT=0
    DO JSETW=1,NPRTRW
      CALL SET2PE(IRECV,0,0,JSETW,1)
      IR=IR+1
      IBEG=(JSETW-1)*NPRTRV+1
      IEND=IBEG+NPRTRV-1
      IINC=SUM(IRECVCOUNTS(IBEG:IEND))
      IBEGOFF=1+ITOT
      IENDOFF=IBEGOFF+IINC-1
      CALL MPL_RECV(ZRECVBUF(IBEGOFF:IENDOFF),KSOURCE=NPRCIDS(IRECV),&
        & KMP_TYPE=JP_NON_BLOCKING_STANDARD,KREQUEST=IREQ(IR),&
        & KTAG=ITAG,CDSTRING='COMMSPNORM:' )
      ITOT=ITOT+IINC
    ENDDO
  ENDIF
  IF( ISETV==1 )THEN
    IR=IR+1
    CALL MPL_SEND(ZBUFW,KDEST=NPRCIDS(KIOMASTER), &
      & KMP_TYPE=JP_NON_BLOCKING_STANDARD,KREQUEST=IREQ(IR), &
      & KTAG=ITAG,CDSTRING='COMMSPNORM:' )
  ENDIF
  IF(IR > 0) THEN
    CALL MPL_WAIT(KREQUEST=IREQ(1:IR), &
      & CDSTRING='COMMSPNORM: WAIT FOR SENDS AND RECEIVES')
  ENDIF
  DEALLOCATE(ZBUFW)
  CALL GSTATS(607,1)
  CALL GSTATS_BARRIER2(773)
ELSE
  IRECVCOUNTS(1) = ISTOTAL
  IBUFLENR=ISTOTAL
  ALLOCATE(ZRECVBUF(IBUFLENR))
  ZRECVBUF(1:ISTOTAL)=ZSENDBUF(1:ISTOTAL)
ENDIF

! ------------------------------------------------------------------

!     3.    Receive and store data in global version. Local data 
!           is treated as if it was received from another PE
!           ------------------------------------------------

IF (MYPROC == KIOMASTER) THEN
  CALL GSTATS(1931,0)
  IOFFB=0
  DO JROC=1,NPROC

    CALL PE2SET(JROC,IA,IB,IRECVSETA,IRECVSETB)
    IRECVLEV=NUMLL(IRECVSETB)
    IRECVNUMP=NUMPP(IRECVSETA)
    LLHASPS=NPSURF(IRECVSETB) == 1

    ISTLEV=NPTRLL(IRECVSETB)
    ISZSPR=IRECVLEV*IRECVNUMP

    IOFFVOR =  0
    IOFFDIV = IOFFVOR+IRECVLEV*IRECVNUMP
    IOFFKE  = IOFFDIV+IRECVLEV*IRECVNUMP
    IOFFTHE = IOFFKE+IRECVLEV*IRECVNUMP
    IOFFGFL = IOFFTHE+IRECVLEV*IRECVNUMP*KFTHER
    IOFFSP  = IOFFGFL+IRECVLEV*IRECVNUMP*KNUMSPFLDS
    IF (LDOROG) THEN
      IOFFSPOR = IOFFSP+IRECVNUMP*NPSURF(IRECVSETB)
    ELSE
      IOFFSPOR  = IOFFSP
    ENDIF
    IF (LDPRTAL) THEN
      IOFFTHEDE = IOFFSPOR+IRECVNUMP*NPSURF(IRECVSETB)
      IOFFGFLDE = IOFFTHEDE+IRECVLEV*IRECVNUMP*KFTHER
      IOFFSPDE  = IOFFGFLDE+IRECVLEV*IRECVNUMP*KNUMSPFLDS
    ENDIF

    DO JMLOC=1,IRECVNUMP
      IM=NALLMS(NPTRMS(IRECVSETA)-1+JMLOC)
      DO JL=1,IRECVLEV
        ILEV=ISTLEV+JL-1
        PMVORG(ILEV,IM)=ZRECVBUF(IOFFB+IOFFVOR+(JMLOC-1)*IRECVLEV+JL)
        PMDIVG(ILEV,IM)=ZRECVBUF(IOFFB+IOFFDIV+(JMLOC-1)*IRECVLEV+JL)
        PMKEG (ILEV,IM)=ZRECVBUF(IOFFB+IOFFKE +(JMLOC-1)*IRECVLEV+JL)
        DO JF=1,KFTHER
          PMTHEG(ILEV,IM,JF)=ZRECVBUF(IOFFB+IOFFTHE+(JF-1)*&
           & IRECVLEV*IRECVNUMP+(JMLOC-1)*IRECVLEV+JL)  
        ENDDO
        DO JF=1,KNUMSPFLDS
          PMGFLG(ILEV,IM,JF)=ZRECVBUF(IOFFB+IOFFGFL+(JF-1)*&
           & IRECVLEV*IRECVNUMP+(JMLOC-1)*IRECVLEV+JL)  
        ENDDO
      ENDDO
      IF (LLHASPS) THEN
        PMSPG(IM) = ZRECVBUF(IOFFB+IOFFSP+JMLOC)
      ENDIF
      IF (LLHASPS.AND.LDOROG) THEN
        PMSPORG(IM) = ZRECVBUF(IOFFB+IOFFSPOR+JMLOC)
      ENDIF
    ENDDO
    IF (LDPRTAL) THEN
      DO JMLOC=1,IRECVNUMP
        IM=NALLMS(NPTRMS(IRECVSETA)-1+JMLOC)
        DO JL=1,IRECVLEV
          ILEV=ISTLEV+JL-1
          DO JF=1,KFTHER
            PMTHEDEG(ILEV,IM,JF)=ZRECVBUF(IOFFB+IOFFTHEDE+(JF-1)*&
             & IRECVLEV*IRECVNUMP+(JMLOC-1)*IRECVLEV+JL)  
          ENDDO
          DO JF=1,KNUMSPFLDS
            PMGFLDEG(ILEV,IM,JF)=ZRECVBUF(IOFFB+IOFFGFLDE+(JF-1)*&
             & IRECVLEV*IRECVNUMP+(JMLOC-1)*IRECVLEV+JL)  
          ENDDO
        ENDDO
        IF (LLHASPS) THEN
          PMSPDEG(IM) = ZRECVBUF(IOFFB+IOFFSPDE+JMLOC)
        ENDIF
      ENDDO
    ENDIF
    IOFFB=IOFFB+IRECVCOUNTS(JROC)
  ENDDO
  CALL GSTATS(1931,1)
ENDIF

DEALLOCATE(ZRECVBUF)

!     ------------------------------------------------------------------

END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('COMMSPNORM',1,ZHOOK_HANDLE)
END SUBROUTINE COMMSPNORM
