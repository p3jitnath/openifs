SUBROUTINE STOM_UNPACK(KJR,KRECVSET,KPOS,KRECV_START,KRECV_END,KSPNUM,IRECVCOUNT,NPRTRV,PCOMBUFR)

INTEGER(KIND=JPIM),INTENT(IN)    :: KJR
INTEGER(KIND=JPIM),INTENT(IN)    :: KRECVSET
INTEGER(KIND=JPIM),INTENT(INOUT) :: KPOS
INTEGER(KIND=JPIM),INTENT(INOUT) :: KRECV_START
INTEGER(KIND=JPIM),INTENT(IN)    :: KRECV_END
INTEGER(KIND=JPIM),INTENT(INOUT) :: KSPNUM
INTEGER(KIND=JPIM),INTENT(IN)    :: IRECVCOUNT
INTEGER(KIND=JPIM),INTENT(IN)    :: NPRTRV
REAL(KIND=JPRB), INTENT(IN)      :: PCOMBUFR(-1:IRECVCOUNT,NPRTRV-1)

INTEGER(KIND=JPIM) :: JSP,JGFL,JFIELD
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('TRSTOM:STOM_UNPACK',0,ZHOOK_HANDLE)

ASSOCIATE(NFLEVL=>YDGEOMETRY%YRDIMV%NFLEVL, NFLEVG=>YDGEOMETRY%YRDIMV%NFLEVG, &
 & NPTRLL=>YDGEOMETRY%YRMP%NPTRLL, NPSP=>YDGEOMETRY%YRMP%NPSP, &
 & NPTRSV=>YDGEOMETRY%YRMP%NPTRSV, NPTRSVF=>YDGEOMETRY%YRMP%NPTRSVF, &
 & NSPEC2V=>YDGEOMETRY%YRMP%NSPEC2V, NPSURF=>YDGEOMETRY%YRMP%NPSURF, &
 & NUMLL=>YDGEOMETRY%YRMP%NUMLL, NSPEC2VF=>YDGEOMETRY%YRMP%NSPEC2VF)

IF (KRECV_START == 0 ) THEN
  ! * buffer contains extra surface spectral field
  IF (LLSP) THEN
    DO JSP=IPTRSV(KRECVSET),IPTRSV(KRECVSET+1)-1
      KPOS=KPOS+1
      PSPSP(JSP)=PCOMBUFR(KPOS,KJR)
    ENDDO
    KRECV_START = 1
  ENDIF
  IF (LLSEL2D) THEN
    DO JFIELD=1,UBOUND(PSPSEL2D,2)
      IF (LDSELECT2D(JFIELD)) THEN
        DO JSP=IPTRSV(KRECVSET),IPTRSV(KRECVSET+1)-1
          KPOS=KPOS+1 !NB: Assumes only one 3d field selected
          PSPSEL2D(JSP,JFIELD) = PCOMBUFR(KPOS,KJR)
        ENDDO
      ENDIF
    ENDDO
    KRECV_START = 1
  ENDIF
ENDIF

DO JSP=IPTRSV(KRECVSET),IPTRSV(KRECVSET+1)-1
  IF (KSPNUM == KRECV_END) EXIT
  KSPNUM=KSPNUM+1
  IF (KSPNUM >= KRECV_START) THEN
    IF (LLVOR)  PSPVOR(1:NFLEVL,JSP)=PCOMBUFR(KPOS+1:KPOS+NFLEVL,KJR)
    IF (LLVOR)  KPOS=KPOS+NFLEVL
    IF (LLDIV)  PSPDIV(1:NFLEVL,JSP)=PCOMBUFR(KPOS+1:KPOS+NFLEVL,KJR)
    IF (LLDIV)  KPOS=KPOS+NFLEVL
    IF (LLT)    PSPT(1:NFLEVL,JSP)=PCOMBUFR(KPOS+1:KPOS+NFLEVL,KJR)
    IF (LLT)    KPOS=KPOS+NFLEVL
    IF (LLSPD)  PSPSPD(1:NFLEVL,JSP)=PCOMBUFR(KPOS+1:KPOS+NFLEVL,KJR)
    IF (LLSPD)  KPOS=KPOS+NFLEVL
    IF (LLSVD)  PSPSVD(1:NFLEVL,JSP)=PCOMBUFR(KPOS+1:KPOS+NFLEVL,KJR)
    IF (LLSVD)  KPOS=KPOS+NFLEVL
    IF (LLSNHX) PSPSNHX(1:NFLEVL,JSP)=PCOMBUFR(KPOS+1:KPOS+NFLEVL,KJR)
    IF (LLSNHX) KPOS=KPOS+NFLEVL
    IF (LLAUX)  PSPAUX(1:NFLEVL,JSP)=PCOMBUFR(KPOS+1:KPOS+NFLEVL,KJR)
    IF (LLAUX)  KPOS=KPOS+NFLEVL
    IF (LLGFL) THEN
      DO JGFL=1,INUMSPGFL
        PSPGFL(1:NFLEVL,JSP,JGFL)=PCOMBUFR(KPOS+1:KPOS+NFLEVL,KJR)
        KPOS=KPOS+NFLEVL
      ENDDO
    ENDIF

    IF (LLSEL3D) THEN
      DO JFIELD=1,UBOUND(PSPSEL3D,3)
        IF (LDSELECT3D(JFIELD)) THEN
          PSPSEL3D(1:NFLEVL,JSP,JFIELD) = PCOMBUFR(KPOS+1:KPOS+NFLEVL,KJR)
          KPOS=KPOS+NFLEVL
        ENDIF
      ENDDO
    ENDIF
  ENDIF
ENDDO

END ASSOCIATE

IF (LHOOK) CALL DR_HOOK('TRSTOM:STOM_UNPACK',1,ZHOOK_HANDLE)
END SUBROUTINE STOM_UNPACK

!     ------------------------------------------------------------------
END SUBROUTINE TRSTOM
