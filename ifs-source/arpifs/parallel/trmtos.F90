SUBROUTINE MTOS_UNPACK(KJR,KRECVSET,KPOS,KRECV_START,KRECV_END,IRECVCOUNT,NPRTRV,PCOMBUFR)

INTEGER(KIND=JPIM),INTENT(IN)    :: KJR
INTEGER(KIND=JPIM),INTENT(IN)    :: KRECVSET
INTEGER(KIND=JPIM),INTENT(INOUT) :: KPOS
INTEGER(KIND=JPIM),INTENT(INOUT) :: KRECV_START
INTEGER(KIND=JPIM),INTENT(IN)    :: KRECV_END
INTEGER(KIND=JPIM),INTENT(IN)    :: IRECVCOUNT
INTEGER(KIND=JPIM),INTENT(IN)    :: NPRTRV
REAL(KIND=JPRB), INTENT(IN)      :: PCOMBUFR(-1:IRECVCOUNT,NPRTRV-1)

INTEGER(KIND=JPIM) :: JSP,JGFL,ISEL,JFIELD,IL1,IL2,ILN

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('TRMTOS:MTOS_UNPACK',0,ZHOOK_HANDLE)

ASSOCIATE(NFLEVL=>YDGEOMETRY%YRDIMV%NFLEVL, NFLEVG=>YDGEOMETRY%YRDIMV%NFLEVG, &
 & NPTRLL=>YDGEOMETRY%YRMP%NPTRLL, NPSP=>YDGEOMETRY%YRMP%NPSP, &
 & NPTRSV=>YDGEOMETRY%YRMP%NPTRSV, NPTRSVF=>YDGEOMETRY%YRMP%NPTRSVF, &
 & NPSURF=>YDGEOMETRY%YRMP%NPSURF, NSPEC2V=>YDGEOMETRY%YRMP%NSPEC2V, &
 & NSPEC2VF=>YDGEOMETRY%YRMP%NSPEC2VF)

IF (KRECV_START == 0 ) THEN
!            buffer contains extra 2d spectral field
  IF (LLSP) THEN
    DO JSP=1,ISPEC2V
      KPOS=KPOS+1
      PSPSPG(JSP)=PCOMBUFR(KPOS,KJR)
    ENDDO
    KRECV_START = 1
  ENDIF
  IF (LLSEL2D) THEN
    ISEL=0
    DO JFIELD=1,UBOUND(PSPSEL2D,2)
      IF (LDSELECT2D(JFIELD)) THEN
        ISEL=ISEL+1
        DO JSP=1,ISPEC2V
          KPOS=KPOS+1 ! Assumes only one field selected
          PSPSEL2DG(JSP,ISEL)= PCOMBUFR(KPOS,KJR)
        ENDDO
      ENDIF
    ENDDO
    KRECV_START = 1
  ENDIF
ENDIF
DO JSP=KRECV_START,KRECV_END
  IL1=NPTRLL(KRECVSET)
  IL2=NPTRLL(KRECVSET+1)-1
  ILN=IL2-IL1+1
  IF (LLVOR)  PSPVORG(IL1:IL2,JSP)=PCOMBUFR(KPOS+1:KPOS+ILN,KJR)
  IF (LLVOR)  KPOS=KPOS+ILN
  IF (LLDIV)  PSPDIVG(IL1:IL2,JSP)=PCOMBUFR(KPOS+1:KPOS+ILN,KJR)
  IF (LLDIV)  KPOS=KPOS+ILN
  IF (LLT)    PSPTG(IL1:IL2,JSP)=PCOMBUFR(KPOS+1:KPOS+ILN,KJR)
  IF (LLT)    KPOS=KPOS+ILN
  IF (LLSPD)  PSPSPDG(IL1:IL2,JSP)=PCOMBUFR(KPOS+1:KPOS+ILN,KJR)
  IF (LLSPD)  KPOS=KPOS+ILN
  IF (LLSVD)  PSPSVDG(IL1:IL2,JSP)=PCOMBUFR(KPOS+1:KPOS+ILN,KJR)
  IF (LLSVD)  KPOS=KPOS+ILN
  IF (LLSNHX) PSPSNHXG(IL1:IL2,JSP)=PCOMBUFR(KPOS+1:KPOS+ILN,KJR)
  IF (LLSNHX) KPOS=KPOS+ILN
  IF (LLAUX)  PSPAUXG(IL1:IL2,JSP)=PCOMBUFR(KPOS+1:KPOS+ILN,KJR)
  IF (LLAUX)  KPOS=KPOS+ILN
  IF (LLGFL) THEN
    DO JGFL=1,INUMSPGFL
      PSPGFLG(IL1:IL2,JSP,JGFL)=PCOMBUFR(KPOS+1:KPOS+ILN,KJR)
      KPOS=KPOS+ILN
    ENDDO
  ENDIF

  IF (LLSEL3D) THEN
    ISEL=0
    DO JFIELD=1,UBOUND(PSPSEL3D,3)
      IF (LDSELECT3D(JFIELD)) THEN
        ISEL=ISEL+1
        PSPSEL3DG(IL1:IL2,JSP,ISEL) = PCOMBUFR(KPOS+1:KPOS+ILN,KJR)
        KPOS=KPOS+ILN
      ENDIF
    ENDDO
  ENDIF
ENDDO

END ASSOCIATE

IF (LHOOK) CALL DR_HOOK('TRMTOS:MTOS_UNPACK',1,ZHOOK_HANDLE)

END SUBROUTINE MTOS_UNPACK

!     ------------------------------------------------------------------

END SUBROUTINE TRMTOS
