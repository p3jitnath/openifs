SUBROUTINE WRCPL(YDGEOMETRY,YDMCC,YDDPHY,YDRIP)

!**** * WRCPL  * - ROUTINE TO WRITE THE COUPLED FIELDS (OASIS)

!**   INTERFACE.
!     ----------
!        *CALL* *WRCPL*

!     EXPLICIT ARGUMENTS :  NONE
!     --------------------

!     IMPLICIT ARGUMENTS :
!     --------------------
!        COMMON  YOMCPL

!     METHOD.
!     -------
!     EXTERNALS.
!     ----------

!     REFERENCE.
!     ----------
!        ECMWF RESEARCH DEPARTMENT DOCUMENTATION OF THE IFS

!     AUTHOR.
!     -------
!        CNRM/GMGEC/EAC/JPh Piedelievre.
!        ORIGINAL : 00-11-27 (D'APRES WRCODM)

!     MODIFICATIONS.
!     --------------
!        Modified 03-12-05 by J.P. Piedelievre : bugfix
!        M.Hamrud      01-Oct-2003 CY28 Cleaning
!        A.Alias 14-oct-2009 ocean currents+gelato (J.F. Gueremy)
!                            IPCC AR5 fields under LGELATO (E.Maisonnave)
!        R. El Khatib : 23-Apr-2010 use diwgrid_mod instead of diwgrid
!        R. El Khatib : 03-Jun-2010 Remove dead call to clim_export
!        K. Yessad (July 2014): Move some variables.
!-----------------------------------------------------------------------

USE YOMDPHY      , ONLY : TDPHY
USE GEOMETRY_MOD , ONLY : GEOMETRY
USE PARKIND1 , ONLY : JPIM, JPRB
USE YOMHOOK  , ONLY : LHOOK, DR_HOOK, JPHOOK
USE YOMCPL   , ONLY : FCSTSU, FCSTSV, FCCHAS, FCRSOS, FCHUMS, FCRUIS, FCCHSS,&
 & FCHUML, FCHUMN, FCTSFL, FCWIMO, FCSUBL
USE YOMMCC   , ONLY : TMCC
USE YOMLUN   , ONLY : NULOUT
USE YOMRIP   , ONLY : TRIP
USE YOMCT3   , ONLY : NSTEP
USE YOMMP0   , ONLY : MYPROC
USE YOMCST   , ONLY : RDAY
USE DIWRGRID_MOD, ONLY : DIWRGRID_SEND, DIWRGRID_RECV

!*  Addition for SVIPC coupling technique
USE YOM_OAS  , ONLY : CPHAN
USE PAR_COU  , ONLY : JPFLDA2O
USE YOM_CPL

!     -------------------------------------------------------------

IMPLICIT NONE

!*  Addition for infos contained within the field brick if cpmodinf='YES'
!   Experiment name in the field brick, if any
!CHARACTER(len=3) CLJOBNAM_R
!   infos in the field brick, if any
TYPE(GEOMETRY), INTENT(IN)   :: YDGEOMETRY
TYPE(TDPHY)    ,INTENT(INOUT):: YDDPHY
TYPE(TMCC)     ,INTENT(INOUT):: YDMCC
TYPE(TRIP)     ,INTENT(INOUT):: YDRIP
INTEGER(KIND=JPIM), DIMENSION(3) :: INFOS
!CLIM
!MPI1
INTEGER(KIND=JPIM) :: ITIM

!INTEGER_M :: JPNCHP

!PARAMETER(JPNCHP=99)
!CHARACTER*13 CLNCHP(JPNCHP)

REAL(KIND=JPRB) :: ZREALG(YDGEOMETRY%YRDIM%NDLON*YDGEOMETRY%YRDIM%NDGLG)
INTEGER(KIND=JPIM) :: INDEX, IDIMG

INTEGER(KIND=JPIM) :: IFLD, INDEX1, INDEX2, IOPROC, ISTEP, ISTEPCPL, JNULCL
INTEGER(KIND=JPIM) :: IULUSR1

LOGICAL :: LLOP

REAL(KIND=JPRB) :: ZCUMUL
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!     -------------------------------------------------------------

#include "abor1.intfb.h"
#include "sipc_write_model.intfb.h"
#include "sucpl0.intfb.h"

!     -------------------------------------------------------------
IF (LHOOK) CALL DR_HOOK('WRCPL',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM=>YDGEOMETRY%YRDIM,YDDIMV=>YDGEOMETRY%YRDIMV,YDGEM=>YDGEOMETRY%YRGEM, YDMP=>YDGEOMETRY%YRMP)
ASSOCIATE(NDGLG=>YDDIM%NDGLG, NDLON=>YDDIM%NDLON, &
 & LGELATO=>YDMCC%LGELATO, NFRCPL=>YDMCC%NFRCPL, NOACOMM=>YDMCC%NOACOMM, &
 & NSTOP=>YDRIP%NSTOP, TSTEP=>YDRIP%TSTEP)
!     -------------------------------------------------------------

!*       1.    PREPARATIONS.
!              --------------

ISTEP=MAX(0,NSTEP+1)

!CLIM
ISTEPCPL=NSTEP+1
IF(NSTEP == 0) ISTEPCPL=0
!ENDCLIM
WRITE(NULOUT,*)' NSTEP,ISTEP,ISTEPCPL=',NSTEP,ISTEP,ISTEPCPL
!MPI1
ITIM=NSTEP*TSTEP
IF(NSTEP == 0) ITIM=0
IF (NOACOMM == 5) THEN
  WRITE(NULOUT,'(A,I8)')'Sending fluxes to OASIS3 for time : ',ITIM
ENDIF
!ENDMPI1

!*       1.1   LISTE DES CHAMPS A TRANSMETTRE A OASIS.

!CLNCHP( 1)='STRESS U'
!CLNCHP( 2)='STRESS V'
!CLNCHP( 3)='FLUX CHALEUR'
!CLNCHP( 4)='RAYT.SOLAIRE'
!CLNCHP( 5)='FLUX HUMI'
!CLNCHP( 6)='RUISSEL.'
!CLNCHP( 7)='FLU.CHA.SENS'
!CLNCHP( 8)='PLUIE TOTALE'
!CLNCHP( 9)='NEIGE TOTALE'
!CLNCHP(10)='TEMPERATURE'
!CLNCHP(11)='TSTS'
!CLNCHP(12)='TSFL'

!*       1.2   PROCESSEUR D'IO

! IOPROC MUST BE 1
IOPROC=1
IDIMG= NDGLG*NDLON
IF (MYPROC == IOPROC) THEN

! Reopen flxatmos to avoid any I/O conflict (?!)

  IF (ISTEP == NSTOP) THEN
    DO JNULCL=1,99
      INQUIRE(UNIT=JNULCL,OPENED=LLOP)
      IF (.NOT.LLOP) EXIT
    ENDDO
    IULUSR1=JNULCL
    OPEN (IULUSR1,FILE='flxatmos',FORM='UNFORMATTED')
  ENDIF

ENDIF ! MYPROC == 1

!     ---------------------------------------------------------------

!*    2.0   WRITE GRID POINT FIELDS ON *FA* FILE
!     -------------------------------------

IFLD=0
ZREALG=0.0_JPRB
ZCUMUL=REAL(NFRCPL)*TSTEP

!*       2.1   WRITE 2 D FIELDS

      INDEX1=1
      INDEX2=3

!IFLW=1
! FCSTSU
IF (NOACOMM == 5) THEN
  CALL ABOR1('WRCPL : ABOR1 CALL OASIS3 NOT IMPLEMENTED')
ELSE
IFLD=IFLD+1
IF (MYPROC /= IOPROC) THEN
  CALL DIWRGRID_SEND(YDGEOMETRY%YRGEM,IOPROC,1,FCSTSU,IFLD)
ELSE
  CALL DIWRGRID_RECV(YDGEOMETRY,1,FCSTSU,IFLD,ZREALG)
  IF (ISTEP > 1) THEN
    ZREALG=ZREALG/ZCUMUL
    IF (ISTEP == NSTOP) THEN
      WRITE(IULUSR1) 'COZOTAUX'
      WRITE(IULUSR1) ZREALG
      WRITE(IULUSR1) 'COZOTAUV'
      WRITE(IULUSR1) ZREALG
    ELSE
      INDEX1=5
      INDEX2=7
      IF (CPHAN == 'SIPC') THEN
        CALL SIPC_WRITE_MODEL(INDEX1,IDIMG,INFOS,ZREALG)
        CALL SIPC_WRITE_MODEL(INDEX2,IDIMG,INFOS,ZREALG)
      ENDIF
    ENDIF
  ENDIF
ENDIF
ENDIF

      INDEX1=2
      INDEX2=4

!IFLW=2
! FCSTSV
IF (NOACOMM == 5) THEN
  CALL ABOR1('WRCPL : ABOR1 CALL OASIS3 NOT IMPLEMENTED')
ELSE
IFLD=IFLD+1
IF (MYPROC /= IOPROC) THEN
  CALL DIWRGRID_SEND(YDGEOMETRY%YRGEM,IOPROC,1,FCSTSV,IFLD)
ELSE
  CALL DIWRGRID_RECV(YDGEOMETRY,1,FCSTSV,IFLD,ZREALG)
  IF (ISTEP > 1) THEN
    ZREALG=ZREALG/ZCUMUL
    IF (ISTEP == NSTOP) THEN
      WRITE(IULUSR1) 'COMETAUY'
      WRITE(IULUSR1) ZREALG
      WRITE(IULUSR1) 'COMETAUU'
      WRITE(IULUSR1) ZREALG
    ELSE
      INDEX1=6
      INDEX2=8
      IF (CPHAN == 'SIPC') THEN
        CALL SIPC_WRITE_MODEL(INDEX1,IDIMG,INFOS,ZREALG)
        CALL SIPC_WRITE_MODEL(INDEX2,IDIMG,INFOS,ZREALG)
      ENDIF
    ENDIF
  ENDIF
ENDIF
ENDIF

      INDEX=5

!IFLW=3
! FCCHAS
IF (NOACOMM == 5) THEN
  CALL ABOR1('WRCPL : ABOR1 CALL OASIS3 NOT IMPLEMENTED')
ELSE
IFLD=IFLD+1
IF (MYPROC /= IOPROC) THEN
  CALL DIWRGRID_SEND(YDGEOMETRY%YRGEM,IOPROC,1,FCCHAS(1,1),IFLD)
ELSE
  CALL DIWRGRID_RECV(YDGEOMETRY,1,FCCHAS(:,1),IFLD,ZREALG)
  IF (ISTEP > 1) THEN
    ZREALG=ZREALG/ZCUMUL
    IF (ISTEP == NSTOP) THEN
      WRITE(IULUSR1) 'CONSFTOT'
      WRITE(IULUSR1) ZREALG
    ELSE
      INDEX=1
      IF (CPHAN == 'SIPC') THEN
        CALL SIPC_WRITE_MODEL(INDEX,IDIMG,INFOS,ZREALG)
      ENDIF
    ENDIF
  ENDIF
ENDIF
ENDIF

      INDEX=6

!IFLW=4
! FCRSOS
IF (NOACOMM == 5) THEN
  CALL ABOR1('WRCPL : ABOR1 CALL OASIS3 NOT IMPLEMENTED')
ELSE
IFLD=IFLD+1
IF (MYPROC /= IOPROC) THEN
  CALL DIWRGRID_SEND(YDGEOMETRY%YRGEM,IOPROC,1,FCRSOS(1,1),IFLD)
ELSE
  CALL DIWRGRID_RECV(YDGEOMETRY,1,FCRSOS(:,1),IFLD,ZREALG)
  IF (ISTEP > 1) THEN
    ZREALG=ZREALG/ZCUMUL
    IF (ISTEP == NSTOP) THEN
      WRITE(IULUSR1) 'COSHFTOT'
      WRITE(IULUSR1) ZREALG
    ELSE
      INDEX=2
      IF (CPHAN == 'SIPC') THEN
        CALL SIPC_WRITE_MODEL(INDEX,IDIMG,INFOS,ZREALG)
      ENDIF
    ENDIF
  ENDIF
ENDIF
ENDIF

      INDEX=7

!IFLW=5
! FCHUMS
IF (NOACOMM == 5) THEN
  CALL ABOR1('WRCPL : ABOR1 CALL OASIS3 NOT IMPLEMENTED')
ELSE
IFLD=IFLD+1
IF (MYPROC /= IOPROC) THEN
  CALL DIWRGRID_SEND(YDGEOMETRY%YRGEM,IOPROC,1,FCHUMS(1,1),IFLD)
ELSE
  CALL DIWRGRID_RECV(YDGEOMETRY,1,FCHUMS(:,1),IFLD,ZREALG)
  IF (ISTEP > 1) THEN

!* Total water flux 'COWATFLU'

!  WARNING !!!! ===>>> values are sent in mm/day for all water fluxes
!  ------------------------------------------------------------------

    ZREALG=ZREALG/ZCUMUL*RDAY
    IF (ISTEP == NSTOP) THEN
      WRITE(IULUSR1) 'COWATFLU'
      WRITE(IULUSR1) ZREALG
    ELSE
      INDEX=3
      IF (CPHAN == 'SIPC') THEN
        CALL SIPC_WRITE_MODEL(INDEX,IDIMG,INFOS,ZREALG)
      ENDIF
    ENDIF
  ENDIF
ENDIF
ENDIF

      INDEX=8

!IFLW=6
! FCRUIS
IF (NOACOMM == 5) THEN
  CALL ABOR1('WRCPL : ABOR1 CALL OASIS3 NOT IMPLEMENTED')
ELSE
IFLD=IFLD+1
IF (MYPROC /= IOPROC) THEN
  CALL DIWRGRID_SEND(YDGEOMETRY%YRGEM,IOPROC,1,FCRUIS,IFLD)
ELSE
  CALL DIWRGRID_RECV(YDGEOMETRY,1,FCRUIS,IFLD,ZREALG)
  IF (ISTEP > 1) THEN

!* Run-off 'CORUNOFF'

!  WARNING !!!! ===>>> values are sent in mm/day for all water fluxes
!  ------------------------------------------------------------------

    ZREALG=ZREALG/ZCUMUL*RDAY
    IF (ISTEP == NSTOP) THEN
      WRITE(IULUSR1) 'CORUNOFF'
      WRITE(IULUSR1) ZREALG
    ELSE
      INDEX=4
      IF (CPHAN == 'SIPC') THEN
        CALL SIPC_WRITE_MODEL(INDEX,IDIMG,INFOS,ZREALG)
      ENDIF
    ENDIF
  ENDIF
ENDIF
ENDIF

      INDEX=9

!IFLW=7
! FCWIMO
IF (NOACOMM == 5) THEN
  CALL ABOR1('WRCPL : ABOR1 CALL OASIS3 NOT IMPLEMENTED')
ELSE
IFLD=IFLD+1
IF (MYPROC /= IOPROC) THEN
  CALL DIWRGRID_SEND(YDGEOMETRY%YRGEM,IOPROC,1,FCWIMO,IFLD)
ELSE
  CALL DIWRGRID_RECV(YDGEOMETRY,1,FCWIMO,IFLD,ZREALG)
  IF (ISTEP > 1) THEN
    ZREALG=ZREALG/ZCUMUL
    IF (ISTEP == NSTOP) THEN
      WRITE(IULUSR1) 'COWINMOD'
      WRITE(IULUSR1) ZREALG
    ELSE
      INDEX=9
      IF (CPHAN == 'SIPC') THEN
        CALL SIPC_WRITE_MODEL(INDEX,IDIMG,INFOS,ZREALG)
      ENDIF
    ENDIF
  ENDIF
ENDIF
ENDIF


!ADDITIONNAL FLUXES FOR GELATO
IF (LGELATO) THEN

      INDEX=10

!IFLW=8
! FCCHSS
IF (NOACOMM == 5) THEN
  CALL ABOR1('WRCPL : ABOR1 CALL OASIS3 NOT IMPLEMENTED')
ELSE
IFLD=IFLD+1
IF (MYPROC /= IOPROC) THEN
  CALL DIWRGRID_SEND(YDGEOMETRY%YRGEM,IOPROC,1,FCCHSS(1,1),IFLD)
ELSE
  CALL DIWRGRID_RECV(YDGEOMETRY,1,FCCHSS(:,1),IFLD,ZREALG)
  IF (ISTEP > 1.AND.JPFLDA2O >= INDEX) THEN

!*         'COTHSHSU'
!  WARNING !!!! ===>>>  values are sent in mm/day for all water fluxes
!  ------------------------------------------------------------------

    ZREALG=ZREALG/ZCUMUL*RDAY
    IF (ISTEP == NSTOP) THEN
      WRITE(IULUSR1) 'COTHSHSU'
      WRITE(IULUSR1) ZREALG(:IDIMG)
    ELSE
      IF (CPHAN == 'SIPC') THEN
        CALL SIPC_WRITE_MODEL(INDEX,IDIMG,INFOS,ZREALG)
      ENDIF
    ENDIF
  ENDIF
ENDIF
ENDIF

      INDEX=11

!IFLW=9
! FCHUML
IF (NOACOMM == 5) THEN
  CALL ABOR1('WRCPL : ABOR1 CALL OASIS3 NOT IMPLEMENTED')
ELSE
IFLD=IFLD+1
IF (MYPROC /= IOPROC) THEN
  CALL DIWRGRID_SEND(YDGEOMETRY%YRGEM,IOPROC,1,FCHUML,IFLD)
ELSE
  CALL DIWRGRID_RECV(YDGEOMETRY,1,FCHUML,IFLD,ZREALG)
  IF (ISTEP > 1.AND.JPFLDA2O >= INDEX) THEN

!*         'COTOLIPR'
!  WARNING !!!! ===>>> values are sent in mm/day for all water fluxes
!  ------------------------------------------------------------------

    ZREALG=ZREALG/ZCUMUL*RDAY
    IF (ISTEP == NSTOP) THEN
      WRITE(IULUSR1) 'COTOLIPR'
      WRITE(IULUSR1) ZREALG
    ELSE
      IF (CPHAN == 'SIPC') THEN
        CALL SIPC_WRITE_MODEL(INDEX,IDIMG,INFOS,ZREALG)
      ENDIF
    ENDIF
  ENDIF
ENDIF
ENDIF

      INDEX=12
      INDEX1=15
      INDEX2=16

!IFLW=10
! FCHUMN
IF (NOACOMM == 5) THEN
  CALL ABOR1('WRCPL : ABOR1 CALL OASIS3 NOT IMPLEMENTED')
ELSE
IFLD=IFLD+1
IF (MYPROC /= IOPROC) THEN
  CALL DIWRGRID_SEND(YDGEOMETRY%YRGEM,IOPROC,1,FCHUMN,IFLD)
ELSE
  CALL DIWRGRID_RECV(YDGEOMETRY,1,FCHUMN,IFLD,ZREALG)
  IF (ISTEP > 1.AND.JPFLDA2O >= INDEX) THEN

!*         'COTOSOPR'
!  WARNING !!!! ===>>> values are sent in mm/day for all water fluxes
!  ------------------------------------------------------------------

    ZREALG=ZREALG/ZCUMUL*RDAY
    IF (ISTEP == NSTOP) THEN
      WRITE(IULUSR1) 'COTOSOPR'
      WRITE(IULUSR1) ZREALG
      WRITE(IULUSR1) 'COCALANT'
      WRITE(IULUSR1) ZREALG
      WRITE(IULUSR1) 'COCALVIN'
      WRITE(IULUSR1) ZREALG
    ELSE
      IF (CPHAN == 'SIPC') THEN
        CALL SIPC_WRITE_MODEL(INDEX,IDIMG,INFOS,ZREALG)
        CALL SIPC_WRITE_MODEL(INDEX1,IDIMG,INFOS,ZREALG)
        CALL SIPC_WRITE_MODEL(INDEX2,IDIMG,INFOS,ZREALG)
      ENDIF
    ENDIF
  ENDIF
ENDIF
ENDIF

      INDEX=13

!IFLW=11
! FCSUBL
IF (NOACOMM == 5) THEN
  CALL ABOR1('WRCPL : ABOR1 CALL OASIS3 NOT IMPLEMENTED')
ELSE
IFLD=IFLD+1
IF (MYPROC /= IOPROC) THEN
  CALL DIWRGRID_SEND(YDGEOMETRY%YRGEM,IOPROC,1,FCSUBL,IFLD)
ELSE
  CALL DIWRGRID_RECV(YDGEOMETRY,1,FCSUBL,IFLD,ZREALG)
  IF (ISTEP > 1.AND.JPFLDA2O >= INDEX) THEN

!*         'COSUBLIM'
!  WARNING !!!! ===>>> values are sent in mm/day for all water fluxes
!  ------------------------------------------------------------------

    ZREALG=ZREALG/ZCUMUL*RDAY
    IF (ISTEP == NSTOP) THEN
      WRITE(IULUSR1) 'COSUBLIM'
      WRITE(IULUSR1) ZREALG
    ELSE
      IF (CPHAN == 'SIPC') THEN
        CALL SIPC_WRITE_MODEL(INDEX,IDIMG,INFOS,ZREALG)
      ENDIF
    ENDIF
  ENDIF
ENDIF
ENDIF

      INDEX=14

!IFLW=12
! FCTSFL
IF (NOACOMM == 5) THEN
  CALL ABOR1('WRCPL : ABOR1 CALL OASIS3 NOT IMPLEMENTED')
ELSE
IFLD=IFLD+1
IF (MYPROC /= IOPROC) THEN
  CALL DIWRGRID_SEND(YDGEOMETRY%YRGEM,IOPROC,1,FCTSFL,IFLD)
ELSE
  CALL DIWRGRID_RECV(YDGEOMETRY,1,FCTSFL,IFLD,ZREALG)
  IF (ISTEP > 1.AND.JPFLDA2O >= INDEX) THEN
    ZREALG=ZREALG/ZCUMUL
    IF (ISTEP == NSTOP) THEN
      WRITE(IULUSR1) 'CODFLXDT'
      WRITE(IULUSR1) ZREALG
    ELSE
      IF (CPHAN == 'SIPC') THEN
        CALL SIPC_WRITE_MODEL(INDEX,IDIMG,INFOS,ZREALG)
      ENDIF
    ENDIF
  ENDIF
ENDIF
ENDIF

!END ADDITIONNAL FLUXES FOR GELATO - IPCC AR5
ENDIF

!     ----------------------------------------------------------------

!*       4.    CLOSE FDB, SAVE FILES ETC.
!              --------------------------

IF (MYPROC == IOPROC) THEN
  CLOSE(IULUSR1)
ENDIF

!     ------------------------------------------------------------------

!*       5.    RESET TO ZERO
!              -------------

CALL SUCPL0(YDGEOMETRY,YDDPHY)

!     ------------------------------------------------------------------

END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('WRCPL',1,ZHOOK_HANDLE)
END SUBROUTINE WRCPL
