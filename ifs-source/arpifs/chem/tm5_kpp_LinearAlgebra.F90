SUBROUTINE TM5_KPP_KppDecomp_ORIG( PJVS, KERR )
! ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!        Sparse LU factorization
! ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

  USE TM5_KPP_Parameters, ONLY: LU_NONZERO, NVAR
  USE TM5_KPP_JacobianSP, ONLY:LU_DIAG, LU_CROW, LU_ICOL
  USE PARKIND1, ONLY : JPIM, JPRD, JPRB
  USE YOMHOOK   ,ONLY : LHOOK,   DR_HOOK, JPHOOK
 
  IMPLICIT NONE


  INTEGER(KIND=JPIM), INTENT(OUT) :: KERR
  REAL(KIND=JPRD),INTENT(INOUT) :: PJVS(LU_NONZERO)
  ! Local
  REAL(KIND=JPRD)     :: ZW(NVAR), Za
  INTEGER(KIND=JPIM)  :: Ik, Ikk, j, jj
  REAL(KIND=JPHOOK)     :: ZHOOK_HANDLE

#ifdef WITH_COMPO_DR_HOOK
  IF (LHOOK) CALL DR_HOOK('TM5_KPP_KPPDECOMP_ORIG',0,ZHOOK_HANDLE )
#endif

  Za = 0. ! mz_rs_20050606
  KERR = 0
  DO Ik=1,NVAR
    ! mz_rs_20050606: don't check if real value == 0
    ! IF ( PJVS( LU_DIAG(Ik) ) == 0. ) THEN
    IF ( ABS(PJVS(LU_DIAG(Ik))) < TINY(Za) ) THEN
        KERR = Ik
#ifdef WITH_COMPO_DR_HOOK
        IF (LHOOK) CALL DR_HOOK('TM5_KPP_KPPDECOMP_ORIG',1,ZHOOK_HANDLE )
#endif
        RETURN
    ENDIF
    DO Ikk = LU_CROW(Ik), LU_CROW(Ik+1)-1
      ZW( LU_ICOL(Ikk) ) = PJVS(Ikk)
    ENDDO
    DO Ikk = LU_CROW(Ik), LU_DIAG(Ik)-1
        j = LU_ICOL(Ikk)
        Za = -ZW(j) / PJVS( LU_DIAG(j) )
        ZW(j) = -Za
        DO jj = LU_DIAG(j)+1, LU_CROW(j+1)-1
          ZW( LU_ICOL(jj) ) = ZW( LU_ICOL(jj) ) + Za*PJVS(jj)
        ENDDO
     ENDDO
     DO Ikk = LU_CROW(Ik), LU_CROW(Ik+1)-1
        PJVS(Ikk) = ZW( LU_ICOL(Ikk) )
     ENDDO
  ENDDO
      
#ifdef WITH_COMPO_DR_HOOK
  IF (LHOOK) CALL DR_HOOK('TM5_KPP_KPPDECOMP_ORIG',1,ZHOOK_HANDLE )
#endif
END SUBROUTINE TM5_KPP_KppDecomp_ORIG


