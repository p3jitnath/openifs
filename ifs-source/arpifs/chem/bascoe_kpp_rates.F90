!-----------------------------------------------------------------------
    FUNCTION DARR_DP(PA,PB)
      IMPLICIT NONE

      REAL(KIND=JPRD),INTENT(IN) :: PA, PB
      REAL(KIND=JPRD) :: DARR_DP

      ! No DR_HOOK for performance
      
      DARR_DP = PA * EXP(PB/ZTEMP)

    END FUNCTION DARR_DP

!-----------------------------------------------------------------------
    FUNCTION TBODY( PSN, PK00, PN, PK0INF, PM )
      IMPLICIT NONE

      REAL(KIND=JPRB),INTENT(IN) ::PSN, PK00, PK0INF, PN, PM
      REAL(KIND=JPRB) :: TBODY,ZK0,ZKINF,ZX1

      ! No DR_HOOK for performance

      ZK0=PK00*PSN*(300./ZTEMP)**PN
      IF (PK0INF > 0.) THEN
        ZKINF=PK0INF*(300./ZTEMP)**PM
        ZX1=1.+(LOG10(ZK0/ZKINF))**2
        ZX1=1./ZX1
        TBODY=(ZK0/(1.+ZK0/ZKINF))*0.6**ZX1
      ELSE
        TBODY=ZK0
      ENDIF
    END FUNCTION TBODY

!-----------------------------------------------------------------------
    FUNCTION TB3( PSN, PK01, PK02, PK11,PK12,PK21,PK22 )
      IMPLICIT NONE

      REAL(KIND=JPRB), INTENT(IN) :: PSN,PK01,PK02,PK11,PK12,PK21,PK22
      REAL(KIND=JPRB) :: TB3,ZK0,ZK1,ZK2

      ! No DR_HOOK for performance
      
      ZK0=PK01*exp(PK02/ZTEMP)
      ZK1=PK11*exp(PK12/ZTEMP)
      ZK2=PK21*exp(PK22/ZTEMP)
      
      TB3=ZK0+(ZK2*PSN/(1._JPRB+(ZK2*PSN/ZK1)))

    END FUNCTION TB3

!-----------------------------------------------------------------------
    FUNCTION TBODY_CA( PSN, PK00, PN, PK0INF, PM )
      ! Three-body chemical activation rate constant defined in JPL2011 at top oHF
      ! (p.186 of pdf-file) especially for OH+CO+O2->HO2+CO2 (see note D1, p.194HF
      IMPLICIT NONE

      REAL(KIND=JPRB), INTENT(IN) :: PSN, PK00, PN, PK0INF, PM
      REAL(KIND=JPRB) :: TBODY_CA,ZK0, ZKINF, ZX1

      ! No DR_HOOK for performance
      
      ZK0=PK00*(300./ZTEMP)**PN
      IF (PK0INF > 0.) THEN
        ZKINF=PK0INF*(300./ZTEMP)**PM / PSN
        ZX1=1._JPRB+(LOG10(ZK0/ZKINF))**2
        ZX1=1._JPRB/ZX1
        TBODY_CA=(ZK0/(1._JPRB+ZK0/ZKINF))*0.6**ZX1
      ELSE
        TBODY_CA=ZK0
      ENDIF

      END FUNCTION TBODY_CA

!-----------------------------------------------------------------------
    FUNCTION K_HO2_HO2( PSN, Pn_H2O )
      ! Total effective reaction rate for HO2 + HO2 -> H2O2 + O2 
      ! as recommended by JPL2011, note B13, p.61
      IMPLICIT NONE

      ! air number density (molecules cm-3), water vapor number density (molecules cm-3)
      REAL(KIND=JPRB), INTENT(IN) :: PSN
      REAL(KIND=JPRD), INTENT(IN) :: Pn_H2O  
      REAL(KIND=JPRB)             :: K_HO2_HO2

      ! No DR_HOOK for performance
      
      !VH orig: K_HO2_HO2 = DARR(3.0E-13_JPRB, 460E0_JPRB ) + PSN*DARR(2.1E-33_JPRB, 920E0_JPRB )
      K_HO2_HO2 = 3.0E-13_JPRB * EXP( 460E0_JPRB/ZTEMP ) + PSN*2.1E-33_JPRB* EXP(920E0_JPRB/ZTEMP )
      K_HO2_HO2 = K_HO2_HO2* ( 1.0_JPRB + 1.4E-21_JPRB*Pn_H2O*exp(2200_JPRB/ZTEMP) )

      END FUNCTION K_HO2_HO2

