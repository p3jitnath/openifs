!--------------------------------------------------------------
      SUBROUTINE BASCOE_KPP_WCOPY(KN,PX,KincX,PY,KincY)
!--------------------------------------------------------------
!     copies Za vector, x, to Za vector, y:  y <- x
!     only for incX=KincY=1
!     after BLAS
!     replace this by the function from the optimized BLAS implementation:
!         CALL  SCOPY(N,X,1,Y,1)   or   CALL  DCOPY(N,X,1,Y,1)
!--------------------------------------------------------------
!     USE BASCOE_KPP_Precision
      USE PARKIND1  , ONLY : JPIM     ,JPRB, JPRD
      USE YOMHOOK   ,ONLY : LHOOK,   DR_HOOK, JPHOOK
     
      IMPLICIT NONE

      INTEGER(KIND=JPIM), INTENT(IN)    :: KincX,KincY,KN
      REAL(KIND=JPRD), INTENT(IN)  :: PX(KN)
      REAL(KIND=JPRD), INTENT(OUT) :: PY(KN)
      REAL(KIND=JPHOOK)    :: ZHOOK_HANDLE
      INTEGER(KIND=JPIM)    :: i,IM,IMP1

#ifdef WITH_COMPO_DR_HOOK
      IF (LHOOK) CALL DR_HOOK('BASCOE_KPP_WCOPY',0,ZHOOK_HANDLE )
#endif
      IF (KN<=0) THEN
#ifdef WITH_COMPO_DR_HOOK
        IF (LHOOK) CALL DR_HOOK('BASCOE_KPP_WCOPY',1,ZHOOK_HANDLE )
#endif
        RETURN
      ENDIF

      IM = MOD(KN,8)
      IF( IM /= 0 ) THEN
        DO i = 1,IM
          PY(i) = PX(i)
        ENDDO
        IF( KN < 8 ) THEN
#ifdef WITH_COMPO_DR_HOOK
          IF (LHOOK) CALL DR_HOOK('BASCOE_KPP_WCOPY',1,ZHOOK_HANDLE )
#endif
        RETURN
        ENDIF
      ENDIF    
      IMP1 = IM+1
      DO i = IMP1,KN,8
        PY(i) = PX(i)
        PY(i + 1) = PX(i + 1)
        PY(i + 2) = PX(i + 2)
        PY(i + 3) = PX(i + 3)
        PY(i + 4) = PX(i + 4)
        PY(i + 5) = PX(i + 5)
        PY(i + 6) = PX(i + 6)
        PY(i + 7) = PX(i + 7)
      ENDDO

#ifdef WITH_COMPO_DR_HOOK
      IF (LHOOK) CALL DR_HOOK('BASCOE_KPP_WCOPY',1,ZHOOK_HANDLE )
#endif

      END SUBROUTINE BASCOE_KPP_WCOPY


!--------------------------------------------------------------
      SUBROUTINE BASCOE_KPP_WAXPY(KN,PAlpha,PX,KincX,PY,KincY)
!--------------------------------------------------------------
!     constant times Za vector plus Za vector: y <- y + Alpha*x
!     only for KincX=KincY=1
!     after BLAS
!     replace this by the function from the optimized BLAS implementation:
!         CALL SAXPY(N,Alpha,X,1,Y,1) or  CALL DAXPY(N,Alpha,X,1,Y,1)
!--------------------------------------------------------------
      USE PARKIND1  , ONLY : JPIM     ,JPRB, JPRD
     
      IMPLICIT NONE

      INTEGER(KIND=JPIM), INTENT(IN)  :: KincX,KincY,KN
      REAL(KIND=JPRD), INTENT(IN)  :: PX(KN),PAlpha
      REAL(KIND=JPRD), INTENT(OUT) :: PY(KN)
      REAL(KIND=JPRD), PARAMETER :: ZERO = 0.0_JPRD
      INTEGER(KIND=JPIM) :: i,IM,IMP1

      ! No DR_HOOK for performance

      IF (PAlpha == ZERO) THEN 
        RETURN
      ENDIF
      IF (KN <= 0) THEN
        RETURN
      ENDIF

      IM = MOD(KN,4)
      IF( IM /= 0 ) THEN
        DO i = 1,IM
          PY(i) = PY(i) + PAlpha*PX(i)
        ENDDO
        IF( KN < 4 )THEN
        RETURN
        ENDIF
      ENDIF
      IMP1 = IM + 1
      DO i = IMP1,KN,4
        PY(i) = PY(i) + PAlpha*PX(i)
        PY(i + 1) = PY(i + 1) + PAlpha*PX(i + 1)
        PY(i + 2) = PY(i + 2) + PAlpha*PX(i + 2)
        PY(i + 3) = PY(i + 3) + PAlpha*PX(i + 3)
      ENDDO
      
      END SUBROUTINE BASCOE_KPP_WAXPY



!--------------------------------------------------------------
      SUBROUTINE BASCOE_KPP_WSCAL(KN,PAlpha,PX,KincX)
!--------------------------------------------------------------
!     constant times Za vector: x(1:N) <- Alpha*x(1:N) 
!     only for KincX=KincY=1
!     after BLAS
!     replace this by the function from the optimized BLAS implementation:
!         CALL SSCAL(N,Alpha,X,1) or  CALL DSCAL(N,Alpha,X,1)
!--------------------------------------------------------------
      USE PARKIND1  , ONLY : JPIM     ,JPRB, JPRD
      USE YOMHOOK   ,ONLY : LHOOK,   DR_HOOK, JPHOOK
     
      IMPLICIT NONE

      INTEGER(KIND=JPIM), INTENT(IN)  :: KincX,KN
      REAL(KIND=JPRD),INTENT(INOUT)   :: PX(KN)
      REAL(KIND=JPRD),INTENT(IN)      :: PAlpha
      REAL(KIND=JPRD), PARAMETER      :: ZERO=0.0_JPRD, Z_ONE=1.0_JPRD
      INTEGER(KIND=JPIM)              :: i,IM,IMP1

      REAL(KIND=JPHOOK)    :: ZHOOK_HANDLE

#ifdef WITH_COMPO_DR_HOOK
      IF (LHOOK) CALL DR_HOOK('BASCOE_KPP_WSCAL',0,ZHOOK_HANDLE )
#endif
      IF (PAlpha == Z_ONE) THEN
#ifdef WITH_COMPO_DR_HOOK
        IF (LHOOK) CALL DR_HOOK('BASCOE_KPP_WSCAL',1,ZHOOK_HANDLE )
#endif
        RETURN
      ENDIF
      IF (KN <= 0) THEN
#ifdef WITH_COMPO_DR_HOOK
        IF (LHOOK) CALL DR_HOOK('BASCOE_KPP_WSCAL',1,ZHOOK_HANDLE )
#endif
        RETURN
      ENDIF

      IM = MOD(KN,5)
      IF( IM /= 0 ) THEN
        IF (PAlpha == (-Z_ONE)) THEN
          DO i = 1,IM
            PX(i) = -PX(i)
          ENDDO
        ELSEIF (PAlpha == ZERO) THEN
          DO i = 1,IM
            PX(i) = ZERO
          ENDDO
        ELSE
          DO i = 1,IM
            PX(i) = PAlpha*PX(i)
          ENDDO
        ENDIF
        IF( KN < 5 ) THEN 
#ifdef WITH_COMPO_DR_HOOK
          IF (LHOOK) CALL DR_HOOK('BASCOE_KPP_WSCAL',1,ZHOOK_HANDLE )
#endif
          RETURN
       ENDIF
      ENDIF
      IMP1 = IM + 1
      IF (PAlpha == (-Z_ONE)) THEN
        DO i = IMP1,KN,5
          PX(i)     = -PX(i)
          PX(i + 1) = -PX(i + 1)
          PX(i + 2) = -PX(i + 2)
          PX(i + 3) = -PX(i + 3)
          PX(i + 4) = -PX(i + 4)
        ENDDO
      ELSEIF (PAlpha == ZERO) THEN
        DO i = IMP1,KN,5
          PX(i)     = ZERO
          PX(i + 1) = ZERO
          PX(i + 2) = ZERO
          PX(i + 3) = ZERO
          PX(i + 4) = ZERO
        ENDDO
      ELSE
        DO i = IMP1,KN,5
          PX(i)     = PAlpha*PX(i)
          PX(i + 1) = PAlpha*PX(i + 1)
          PX(i + 2) = PAlpha*PX(i + 2)
          PX(i + 3) = PAlpha*PX(i + 3)
          PX(i + 4) = PAlpha*PX(i + 4)
        ENDDO
      ENDIF

#ifdef WITH_COMPO_DR_HOOK
      IF (LHOOK) CALL DR_HOOK('BASCOE_KPP_WSCAL',1,ZHOOK_HANDLE )
#endif
      END SUBROUTINE BASCOE_KPP_WSCAL





