   & KCNTRL_U, PRCNTRL_U, KSTATUS_U, PRSTATUS_U, KERR_U, PVAR, PRCONST, PCFACTOR )

   USE PARKIND1  ,ONLY : JPIM, JPRB, JPRD
   USE YOMHOOK   ,ONLY : LHOOK, DR_HOOK, JPHOOK
   USE TM5_KPP_Parameters, ONLY: NVAR, NREACT, NHEXIT
   USE TM5_KPP_Global,     ONLY: RTOL, ATOL,STEPMIN

   IMPLICIT NONE

   REAL(KIND=JPRD), INTENT(IN) :: PTIN  ! Start Time
   REAL(KIND=JPRB), INTENT(IN) :: PTOUT ! End Time
   ! Optional input parameters and statistics
   INTEGER(KIND=JPIM),    INTENT(IN),  OPTIONAL :: KCNTRL_U(20)
   REAL(KIND=JPRD), INTENT(IN),  OPTIONAL :: PRCNTRL_U(20)
   INTEGER(KIND=JPIM),       INTENT(OUT), OPTIONAL :: KSTATUS_U(20)
   REAL(KIND=JPRD), INTENT(OUT), OPTIONAL :: PRSTATUS_U(20)
   INTEGER(KIND=JPIM), INTENT(OUT), OPTIONAL :: KERR_U

   REAL(KIND=JPRD),       INTENT(INOUT) :: PVAR(NVAR)
   REAL(KIND=JPRD),       INTENT(IN)    :: PRCONST(NREACT)
   REAL(KIND=JPRB),       INTENT(IN)    :: PCFACTOR

   REAL(KIND=JPRD) :: ZTOUT
   REAL(KIND=JPRD) :: ZRCNTRL(20), ZRSTATUS(20)
   INTEGER(KIND=JPIM)     :: ICNTRL(20), ISTATUS(20), IERR

   ! INTEGER(KIND=JPIM), SAVE :: Ntotal = 0

#ifdef WITH_COMPO_DR_HOOK
   REAL(KIND=JPHOOK)    :: ZHOOK_HANDLE
#endif

#ifdef WITH_COMPO_DR_HOOK
   IF (LHOOK) CALL DR_HOOK('TM5_KPP_INTEGRATOR',0,ZHOOK_HANDLE)
#endif

   ICNTRL(:)   = 0
   ZRCNTRL(:)  = 0.0_JPRD
   ISTATUS(:)  = 0
   ZRSTATUS(:) = 0.0_JPRD
   ZTOUT=PTOUT ! Convert to DP

    !~~~> fine-tune the integrator:
   ICNTRL(1) = 0        ! 0 - non-autonomous, 1 - autonomous
   ICNTRL(2) = 0        ! 0 - vector tolerances, 1 - scalars

   ! If optional parameters are given, and if they are >0, 
   ! then they overwrite default settings. 
   IF (PRESENT(KCNTRL_U)) THEN
     WHERE(KCNTRL_U(:) > 0) ICNTRL(:) = KCNTRL_U(:)
   ENDIF
   IF (PRESENT(PRCNTRL_U)) THEN
     WHERE(PRCNTRL_U(:) > 0) ZRCNTRL(:) = PRCNTRL_U(:)
   ENDIF

   CALL TM5_KPP_ROSENBROCK(PVAR,PRCONST, PTIN,ZTOUT,   &
      &   ATOL,RTOL, PCFACTOR, &
      &   ZRCNTRL,ICNTRL,ZRSTATUS,ISTATUS,IERR)

   !~~~> Debug option: show no of steps
   ! Ntotal = Ntotal + KSTATUS(Nstp)
   ! PRINT*,'NSTEPS=',KSTATUS(Nstp),' (',Ntotal,')','  O3=', VAR(ind_O3)

   STEPMIN = ZRSTATUS(Nhexit)
   ! if optional parameters are given for output they 
   ! are updated with the return information
   IF (PRESENT(KSTATUS_U)) KSTATUS_U(:) = ISTATUS(:)
   IF (PRESENT(PRSTATUS_U)) PRSTATUS_U(:) = ZRSTATUS(:)
   IF (PRESENT(KERR_U))    KERR_U       = IERR

#ifdef WITH_COMPO_DR_HOOK
   IF (LHOOK) CALL DR_HOOK('TM5_KPP_INTEGRATOR',1,ZHOOK_HANDLE)
#endif

END SUBROUTINE TM5_KPP_INTEGRATOR

!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
SUBROUTINE TM5_KPP_ROSENBROCK(PY,PRCONST, PTSTART,PTEND, &
      &     PABSTOL,PRELTOL, PCFACTOR,                        &
      &     PRCNTRL,KCNTRL,PRSTATUS,KSTATUS,KERR)
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!
!    Solves the system y'=F(t,y) using a Rosenbrock method defined by:
!
!     G = 1/(H*gamma(1)) - Jac(t0,Y0)
!     T_i = t0 + Alpha(i)*H
!     Y_i = Y0 + \sum_{j=1}^{i-1} A(i,j)*K_j
!     G * K_i = Fun( T_i, Y_i ) + \sum_{j=1}^S C(i,j)/H * K_j +
!         gamma(i)*dF/dT(t0, Y0)
!     Y1 = Y0 + \sum_{j=1}^S M(j)*K_j
!
!    For details on Rosenbrock methods and their implementation consult:
!      E. Hairer and G. Wanner
!      "Solving ODEs II. Stiff and differential-algebraic problems".
!      Springer series in computational mathematics, Springer-Verlag, 1996.
!    The codes contained in the book inspired this implementation.
!
!    (C)  Adrian Sandu, August 2004
!    Virginia Polytechnic Institute and State University
!    Contact: sandu@cs.vt.edu
!    Revised by Philipp Miehe and Adrian Sandu, May 2006                  
!    This implementation is part of KPP - the Kinetic PreProcessor
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!
!~~~>   INPUT ARGUMENTS:
!
!-     PY(N)    = vector of initial conditions (at T=Tstart)
!-    [Tstart,Tend]  = time range of integration
!     (if Tstart>Tend the integration is performed backwards in time)
!-    PRELTOL, PABSTOL = user precribed accuracy
!- SUBROUTINE  Fun( T, Y, Ydot ) = ODE function,
!                       returns Ydot = Y' = F(T,Y)
!- SUBROUTINE  Jac( T, Y, Jcb ) = Jacobian of the ODE function,
!                       returns Jcb = dFun/dY
!-    KCNTRL(1:20)    = integer inputs parameters
!-    PRCNTRL(1:20)    = real inputs parameters
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!
!~~~>     OUTPUT ARGUMENTS:
!
!-    Y(N)    -> vector of final states (at T->Tend)
!-    KSTATUS(1:20)   -> integer output parameters
!-    RSTATUS(1:20)   -> real output parameters
!-    KERR            -> job status upon return
!                        success (positive value) or
!                        failure (negative value)
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!
!~~~>     INPUT PARAMETERS:
!
!    Note: For input parameters equal to zero the default values of the
!       corresponding variables are used.
!
!    KCNTRL(1) = 1: F = F(y)   Independent of T (AUTONOMOUS)
!              = 0: F = F(t,y) Depends on T (NON-AUTONOMOUS)
!
!    KCNTRL(2) = 0: PABSTOL, PRELTOL are N-dimensional vectors
!              = 1: PABSTOL, PRELTOL are scalars
!
!    KCNTRL(3)  -> selection of a particular Rosenbrock method
!        = 0 :    Rodas3 (default)
!        = 1 :    Ros2
!        = 2 :    Ros3
!        = 3 :    Ros4
!        = 4 :    Rodas3
!        = 5 :    Rodas4
!
!    KCNTRL(4)  -> maximum number of integration steps
!        For KCNTRL(4)=0) the default value of 100000 is used
!
!    PRCNTRL(1)  -> Hmin, lower bound for the integration step size
!          It is strongly recommended to keep Hmin = ZERO
!    PRCNTRL(2)  -> Hmax, upper bound for the integration step size
!    PRCNTRL(3)  -> Hstart, starting value for the integration step size
!
!    PRCNTRL(4)  -> FacMin, lower bound on step decrease factor (default=0.2)
!    PRCNTRL(5)  -> FacMax, upper bound on step increase factor (default=6)
!    PRCNTRL(6)  -> FacRej, step decrease factor after multiple rejections
!                          (default=0.1)
!    PRCNTRL(7)  -> FacSafe, by which the new step is slightly smaller
!         than the predicted value  (default=0.9)
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!
!
!    OUTPUT ARGUMENTS:
!    -----------------
!
!    T           -> T value for which the solution has been computed
!                     (after successful return T=Tend).
!
!    PY(N)       -> Numerical solution at T
!
!    IDID        -> Reports on successfulness upon return:
!                    = 1 for success
!                    < 0 for error (value equals error code)
!
!    KSTATUS(1)  -> No. of function calls
!    KSTATUS(2)  -> No. of jacobian calls
!    KSTATUS(3)  -> No. of steps
!    KSTATUS(4)  -> No. of accepted steps
!    KSTATUS(5)  -> No. of rejected steps (except at very beginning)
!    KSTATUS(6)  -> No. of LU decompositions
!    KSTATUS(7)  -> No. of forward/backward substitutions
!    KSTATUS(8)  -> No. of singular matrix decompositions
!
!    RSTATUS(1)  -> Texit, the time corresponding to the
!                     computed Y upon return
!    RSTATUS(2)  -> Hexit, last accepted step before exit
!    RSTATUS(3)  -> Hnew, last predicted step (not yet taken)
!                   For multiple restarts, use Hnew as Hstart 
!                     in the subsequent run
!
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

  USE TM5_KPP_Parameters, only:NREACT,NVAR 
  USE TM5_KPP_Global, only : ROUNDOFF_STORE
  ! USE TM5_KPP_LinearAlgebra
  USE YOMHOOK   ,ONLY : LHOOK,   DR_HOOK, JPHOOK
  USE YOMLUN,     ONLY : NULERR
  USE PARKIND1  , ONLY : JPIM   ,JPRB  ,JPRD
  IMPLICIT NONE

!~~~>  Arguments
   REAL(KIND=JPRD), INTENT(INOUT) :: PY(NVAR)
   REAL(KIND=JPRD), INTENT(IN)    :: PRCONST(NREACT)
   REAL(KIND=JPRD), INTENT(IN)    :: PTSTART,PTEND
   REAL(KIND=JPRD), INTENT(IN)    :: PABSTOL(NVAR),PRELTOL(NVAR)
   INTEGER(KIND=JPIM),       INTENT(IN)    :: KCNTRL(20)
   REAL(KIND=JPRD), INTENT(IN)    :: PRCNTRL(20)
   INTEGER(KIND=JPIM),       INTENT(INOUT) :: KSTATUS(20)
   REAL(KIND=JPRB), INTENT(IN)    :: PCFACTOR
   REAL(KIND=JPRD), INTENT(INOUT) :: PRSTATUS(20)
   INTEGER(KIND=JPIM), INTENT(OUT)   :: KERR
!~~~>  Parameters of the Rosenbrock method, up to 6 stages
   INTEGER(KIND=JPIM) ::  IROS_S, IrosMethod
   INTEGER(KIND=JPIM), PARAMETER :: IRS2=1, IRS3=2, IRS4=3, IRD3=4, IRD4=5
   REAL(KIND=JPRD) :: Z_ROS_A(15), Z_ROS_C(15), Z_ROS_M(6), Z_ROS_E(6), &
&                    Z_ROS_ALPHA(6), Z_ROS_GAMMA(6), Z_ROS_ELO
   LOGICAL :: LL_ROS_NEWF(6)
   CHARACTER(LEN=12) :: CL_ROS_NAME
!~~~>  Local variables
   REAL(KIND=JPRD) :: ZRoundoff, ZFacMin, ZFacMax, ZFacRej, ZFacSafe
   REAL(KIND=JPRD) :: ZHmin, ZHmax, ZHstart
   REAL(KIND=JPRD) :: ZTexit
   INTEGER(KIND=JPIM)       :: i, IUplimTol, KMax_no_steps
   LOGICAL       :: LD_Autonomous, LD_VectorTol
!~~~>   Parameters
   REAL(KIND=JPRD), PARAMETER :: ZERO = 0.0_JPRD
   REAL(KIND=JPRD), PARAMETER :: ZDeltaMin = 1.0E-5_JPRD
#ifdef WITH_COMPO_DR_HOOK
   REAL(KIND=JPHOOK)    :: ZHOOK_HANDLE
#endif


#ifdef WITH_COMPO_DR_HOOK
   IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK',0,ZHOOK_HANDLE) 
#endif

!~~~>  Initialize statistics
   KSTATUS(1:8) = 0
   PRSTATUS(1:3) = ZERO

!~~~>  Autonomous or time dependent ODE. Default is time dependent.
   LD_Autonomous = .NOT.(KCNTRL(1) == 0)

!~~~>  For Scalar tolerances (KCNTRL(2).NE.0)  the code uses PABSTOL(1) and PRELTOL(1)
!   For Vector tolerances (KCNTRL(2) == 0) the code uses PABSTOL(1:NVAR) and PRELTOL(1:NVAR)
   IF (KCNTRL(2) == 0) THEN
      LD_VectorTol = .TRUE.
      IUplimTol  = NVAR
   ELSE
      LD_VectorTol = .FALSE.
      IUplimTol  = 1
   ENDIF

!~~~>   Initialize the particular Rosenbrock method selected
   SELECT CASE (KCNTRL(3))
     CASE (1)
       CALL Ros2
     CASE (2)
       CALL Ros3
     CASE (3)
       CALL Ros4
     CASE (0,4)
       CALL Rodas3
     CASE (5)
       CALL Rodas4
     CASE DEFAULT
       WRITE(NULERR,*) 'Unknown Rosenbrock method: KCNTRL(3)=',KCNTRL(3) 
       CALL ROS_ERRORMSG(-2,PTSTART,ZERO,KERR)
#ifdef WITH_COMPO_DR_HOOK
       IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK',1,ZHOOK_HANDLE) 
#endif
       RETURN
   END SELECT

!~~~>   The maximum number of steps admitted
   IF (KCNTRL(4) == 0) THEN
      KMax_no_steps = 200000
   ELSEIF (KCNTRL(4) > 0) THEN
      KMax_no_steps=KCNTRL(4)
   ELSE
      WRITE(NULERR,*)'User-selected max no. of steps: KCNTRL(4)=',KCNTRL(4)
      CALL ROS_ERRORMSG(-1,PTSTART,ZERO,KERR)
#ifdef WITH_COMPO_DR_HOOK
      IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK',1,ZHOOK_HANDLE) 
#endif
      RETURN
   ENDIF

!~~~>  Unit roundoff (1+Roundoff>1)
!VH There is an issue when runnng KPP in parallel with 'FIRST'
!VH   Roundoff = WLAMCH('E')
!VH Instead use 'stored' roundoff value, which is computed elsewhere.
    ZRoundoff = Roundoff_STORE

!~~~>  Lower bound on the step size: (positive value)
   IF (PRCNTRL(1) == ZERO) THEN
      ZHmin = ZERO
   ELSEIF (PRCNTRL(1) > ZERO) THEN
      ZHmin = PRCNTRL(1)
   ELSE
      WRITE(NULERR,*) 'User-selected Hmin: PRCNTRL(1)=', PRCNTRL(1)
      CALL ROS_ERRORMSG(-3,PTSTART,ZERO,KERR)
#ifdef WITH_COMPO_DR_HOOK
      IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK',1,ZHOOK_HANDLE) 
#endif
      RETURN
   ENDIF
!~~~>  Upper bound on the step size: (positive value)
   IF (PRCNTRL(2) == ZERO) THEN
      ZHmax = ABS(PTEND-PTSTART)
   ELSEIF (PRCNTRL(2) > ZERO) THEN
      ZHmax = MIN(ABS(PRCNTRL(2)),ABS(PTEND-PTSTART))
   ELSE
      WRITE(NULERR,*) 'User-selected Hmax: PRCNTRL(2)=', PRCNTRL(2)
      CALL ROS_ERRORMSG(-3,PTSTART,ZERO,KERR)
#ifdef WITH_COMPO_DR_HOOK
      IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK',1,ZHOOK_HANDLE) 
#endif
      RETURN
   ENDIF
!~~~>  Starting step size: (positive value)
   IF (PRCNTRL(3) == ZERO) THEN
      ZHstart = MAX(ZHmin,ZDeltaMin)
   ELSEIF (PRCNTRL(3) > ZERO) THEN
      ZHstart = MIN(ABS(PRCNTRL(3)),ABS(PTEND-PTSTART))
   ELSE
      WRITE(NULERR,*) 'User-selected Hstart: PRCNTRL(3)=', PRCNTRL(3)
      CALL ROS_ERRORMSG(-3,PTSTART,ZERO,KERR)
#ifdef WITH_COMPO_DR_HOOK
      IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK',1,ZHOOK_HANDLE) 
#endif
      RETURN
   ENDIF
!~~~>  Step size can be changed s.t.  FacMin < Hnew/Hold < FacMax
   IF (PRCNTRL(4) == ZERO) THEN
      ZFacMin = 0.2_JPRD
   ELSEIF (PRCNTRL(4) > ZERO) THEN
      ZFacMin = PRCNTRL(4)
   ELSE
      WRITE(NULERR,*) 'User-selected FacMin: PRCNTRL(4)=', PRCNTRL(4)
      CALL ROS_ERRORMSG(-4,PTSTART,ZERO,KERR)
#ifdef WITH_COMPO_DR_HOOK
      IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK',1,ZHOOK_HANDLE) 
#endif
      RETURN
   ENDIF
   IF (PRCNTRL(5) == ZERO) THEN
      ZFacMax = 6.0_JPRD
   ELSEIF (PRCNTRL(5) > ZERO) THEN
      ZFacMax = PRCNTRL(5)
   ELSE
      WRITE(NULERR,*) 'User-selected FacMax: PRCNTRL(5)=', PRCNTRL(5)
      CALL ROS_ERRORMSG(-4,PTSTART,ZERO,KERR)
#ifdef WITH_COMPO_DR_HOOK
      IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK',1,ZHOOK_HANDLE) 
#endif
      RETURN
   ENDIF
!~~~>   FacRej: Factor to decrease step after 2 succesive rejections
   IF (PRCNTRL(6) == ZERO) THEN
      ZFacRej = 0.1_JPRD
   ELSEIF (PRCNTRL(6) > ZERO) THEN
      ZFacRej = PRCNTRL(6)
   ELSE
      WRITE(NULERR,*) 'User-selected FacRej: PRCNTRL(6)=', PRCNTRL(6)
      CALL ROS_ERRORMSG(-4,PTstart,ZERO,KERR)
#ifdef WITH_COMPO_DR_HOOK
      IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK',1,ZHOOK_HANDLE) 
#endif
      RETURN
   ENDIF
!~~~>   FacSafe: Safety Factor in the computation of new step size
   IF (PRCNTRL(7) == ZERO) THEN
      ZFacSafe = 0.9_JPRD
   ELSEIF (PRCNTRL(7) > ZERO) THEN
      ZFacSafe = PRCNTRL(7)
   ELSE
      WRITE(NULERR,*) 'User-selected FacSafe: PRCNTRL(7)=', PRCNTRL(7)
      CALL ROS_ERRORMSG(-4,PTSTART,ZERO,KERR)
#ifdef WITH_COMPO_DR_HOOK
      IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK',1,ZHOOK_HANDLE) 
#endif
      RETURN
   ENDIF
!~~~>  Check if tolerances are reasonable
    DO i=1,IUplimTol
      IF ( (PABSTOL(i) <= ZERO) .OR. (PRELTOL(i) <= 10.0_JPRD*ZRoundoff) &
&         .OR. (PRELTOL(i) >= 1.0_JPRD) ) THEN
        WRITE(NULERR,*) ' AbsTol(',i,') = ',PABSTOL(i)
        WRITE(NULERR,*) ' RelTol(',i,') = ',PRELTOL(i)
        CALL ROS_ERRORMSG(-5,PTstart,ZERO,KERR)
#ifdef WITH_COMPO_DR_HOOK
        IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK',1,ZHOOK_HANDLE) 
#endif
       RETURN
      ENDIF
    ENDDO


!~~~>  CALL Rosenbrock method
   CALL TM5_KPP_ROS_INTEGRATOR(PY, PRCONST, PCFACTOR, PTSTART, PTEND, ZTEXIT,   &
    &    PABSTOL, PRELTOL,                        &
!  Integration parameters
    &    LD_Autonomous, LD_VectorTol, KMax_no_steps,     &
    &    ZRoundoff, ZHmin, ZHmax, ZHstart,        &
    &    ZFacMin, ZFacMax, ZFacRej, ZFacSafe,     &
!  Error indicator
    &    KERR)

#ifdef WITH_COMPO_DR_HOOK
IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK',1,ZHOOK_HANDLE) 
#endif

