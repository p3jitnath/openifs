#ifdef FULL_ALGEBRA    
   CALL  DGETRS( 'N', N , 1, PA, NVAR, KPIVOT, PB, NVAR, 0 )
#else   
   CALL TM5_KPP_KppSolve( PA, PB )
#endif

   KSTATUS(Nsol) = KSTATUS(Nsol) + 1
