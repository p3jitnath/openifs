!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~   
 SUBROUTINE ROS_ERRORMSG (K_Code,PT,PH,KERR)
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~   
!    Handles all error messages
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~   
  
   USE YOMHOOK   ,ONLY : LHOOK,   DR_HOOK, JPHOOK
   USE PARKIND1  ,ONLY : JPIM, JPRB,   JPRD
   USE YOMLUN    ,ONLY : NULERR
   
   REAL(KIND=JPRD), INTENT(IN) :: PT, PH
   INTEGER(KIND=JPIM), INTENT(IN)  :: K_Code
   INTEGER(KIND=JPIM), INTENT(OUT) :: KERR
#ifdef WITH_COMPO_DR_HOOK
   REAL(KIND=JPHOOK)    :: ZHOOK_HANDLE
#endif

#ifdef WITH_COMPO_DR_HOOK
   IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK:ROS_ERRORMSG',0,ZHOOK_HANDLE) 
#endif
   KERR = K_Code
   WRITE(NULERR,*)'Forced exit from Rosenbrock due to the following error:' 
     
   SELECT CASE (K_Code)
    CASE (-1)    
      WRITE(NULERR, *) '--> Improper value for maximal no of steps'
    CASE (-2)    
      WRITE(NULERR, *) '--> Selected Rosenbrock method not implemented'
    CASE (-3)    
      WRITE(NULERR, *) '--> Hmin/Hmax/Hstart must be positive'
    CASE (-4)    
      WRITE(NULERR, *) '--> FacMin/FacMax/FacRej must be positive'
    CASE (-5) 
      WRITE(NULERR, *) '--> Improper tolerance values'
    CASE (-6) 
      WRITE(NULERR, *) '--> No of steps exceeds maximum bound'
    CASE (-7) 
      WRITE(NULERR, *) '--> Step size too small: T + 10*H = T', &
         &   ' or H < Roundoff'
    CASE (-8)    
      WRITE(NULERR, *) '--> Matrix is repeatedly singular'
     CASE (-9)    
!      happens sometimes, should be reported by user's calling routine
!      WRITE(NULERR, *) , '       Irrealistic (out-of-range) values reached'
!      WRITE(NULERR,'(a,2(e9.1,a))')   &
!         & 'using range [',vmr_bad_small,',',vmr_bad_large,'] hardcoded in TM5_KPP_Integrator.f90'
   CASE DEFAULT
      WRITE(NULERR, *) 'Unknown Error code: ', K_Code
   END SELECT
   
   WRITE(NULERR,*) "T=", PT, "and H=", PH
     
#ifdef WITH_COMPO_DR_HOOK
   IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK:ROS_ERRORMSG',1,ZHOOK_HANDLE) 
#endif
 END SUBROUTINE ROS_ERRORMSG

!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
 SUBROUTINE TM5_KPP_ROS_INTEGRATOR (PY, PRCONST, PCFACTOR, PTSTART, PTEND, PT,  &
&        PABSTOL, PRELTOL,                        &
!~~~> Integration parameters
&        LD_Autonomous, LD_VectorTol, KMax_no_steps,     &
&        PRoundoff, PHmin, PHmax, PHstart,         &
&        PFacMin, PFacMax, PFacRej, PFacSafe,     &
!~~~> Error indicator
&        KERR )
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!   Template for the implementation of a generic Rosenbrock method
!      defined by IROS_S (no of stages)
!      and its coefficients ros_{A,C,M,E,Alpha,Gamma}
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

   USE YOMHOOK   ,ONLY : LHOOK,   DR_HOOK, JPHOOK
   USE PARKIND1  , ONLY : JPIM  , JPRB   ,JPRD
   USE TM5_KPP_Parameters, ONLY : NREACT,NVAR,NHEXIT,LU_NONZERO, NSTP,Nfun,Njac,Nacc,&
&      Nhnew,Ntexit,Nrej  
   USE CIFS_KPP_IntParam, ONLY : VMR_BAD_LARGE

  IMPLICIT NONE

!~~~> Input: the initial condition at Tstart; Output: the solution at T
   REAL(KIND=JPRD), INTENT(INOUT) :: PY(NVAR)
!~~~> Input: reaction rates
   REAL(KIND=JPRD), INTENT(IN) ::PRCONST(NREACT)
! Density (molec/cm3)
   REAL(KIND=JPRB), INTENT(IN) :: PCFACTOR
!~~~> Input: integration interval
   REAL(KIND=JPRD), INTENT(IN) :: PTSTART,PTEND
!~~~> Output: time at which the solution is returned (T=TENDIF success)
   REAL(KIND=JPRD), INTENT(OUT) ::  PT
!~~~> Input: tolerances
   REAL(KIND=JPRD), INTENT(IN) ::  PABSTOL(NVAR), PRELTOL(NVAR)
!~~~> Input: integration parameters
   LOGICAL, INTENT(IN) :: LD_Autonomous, LD_VectorTol
   REAL(KIND=JPRD), INTENT(IN) :: PHstart, PHmin, PHmax
   INTEGER(KIND=JPIM), INTENT(IN) :: KMax_no_steps
   REAL(KIND=JPRD), INTENT(IN) :: PRoundoff, PFacMin, PFacMax, PFacRej, PFacSafe
!~~~> Output: Error indicator
   INTEGER(KIND=JPIM), INTENT(OUT) :: KERR
! ~~~~ Local variables
   REAL(KIND=JPRD) :: ZYnew(NVAR), ZFcn0(NVAR), ZFcn(NVAR)
   REAL(KIND=JPRD) :: ZK(NVAR*IROS_S), ZdFdT(NVAR)
#ifdef FULL_ALGEBRA    
   REAL(KIND=JPRD) :: ZJac0(NVAR,NVAR), ZGhimj(NVAR,NVAR)
#else
   REAL(KIND=JPRD) :: ZJac0(LU_NONZERO), ZGhimj(LU_NONZERO)
#endif
   REAL(KIND=JPRD) :: ZH, ZHnew, ZHC, ZHG, ZFac, ZTau
   REAL(KIND=JPRD) :: ZErr, ZYerr(NVAR)
   INTEGER(KIND=JPIM) :: KPIVOT(NVAR), K_Direction, ioffset, j, istage
   LOGICAL :: LL_RejectLastH, LL_RejectMoreH, LL_Singular
!~~~>  Local parameters
   REAL(KIND=JPRD), PARAMETER :: ZERO = 0.0_JPRD, Z_ONE  = 1.0_JPRD
   REAL(KIND=JPRD), PARAMETER :: ZDeltaMin = 1.0E-5_JPRD
!~~~>  Locally called functions
!    REAL(KIND=JPRD) WLAMCH
!    EXTERNAL WLAMCH
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
#ifdef WITH_COMPO_DR_HOOK
   REAL(KIND=JPHOOK)    :: ZHOOK_HANDLE
#endif

#ifdef WITH_COMPO_DR_HOOK
   IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK:TM5_KPP_ROS_INTEGRATOR',0,ZHOOK_HANDLE) 
#endif

!~~~>  Initial preparations
   PT = PTSTART
   PRSTATUS(Nhexit) = ZERO
   ZH = MIN( MAX(ABS(PHmin),ABS(PHstart)) , ABS(PHmax) )
   IF (ABS(ZH) <= 10.0_JPRD*PRoundoff) ZH = ZDeltaMin

   IF (PTEND  >=  PTSTART) THEN
     K_Direction = +1
   ELSE
     K_Direction = -1
   ENDIF
   ZH = K_Direction*ZH

   LL_RejectLastH=.FALSE.
   LL_RejectMoreH=.FALSE.

!~~~> Time loop begins below

TimeLoop: DO WHILE ( (K_Direction > 0).AND.((PT-PTEND)+PRoundoff <= ZERO) &
    &   .OR. (K_Direction < 0).AND.((PTEND-PT)+PRoundoff <= ZERO) )

!   ~~~ YC20100816 : *** added a test on realistic values for number densities (Y)

    !VH - not necesary: IF( ANY (ISNAN(PY)) .OR. ANY (PY > (PCFACTOR*vmr_bad_large)) .OR. ANY (PY < PCFACTOR*vmr_bad_small) ) THEN
    IF( ANY (PY > (PCFACTOR*vmr_bad_large))  ) THEN
!        write(*,'(a,i)'), '********************* ROS_INTEGRATOR error: ISNAN(Y) or Y out-of-range at sub-step ',KSTATUS(nstp)
        CALL ROS_ERRORMSG(-9,PT,ZH,KERR)
#ifdef WITH_COMPO_DR_HOOK
        IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK:TM5_KPP_ROS_INTEGRATOR',1,ZHOOK_HANDLE) 
#endif
        RETURN
    ENDIF    

!   ~~~ YC20100816

   IF ( KSTATUS(Nstp) > KMax_no_steps ) THEN  ! Too many steps
      CALL ROS_ERRORMSG(-6,PT,ZH,KERR)
#ifdef WITH_COMPO_DR_HOOK
        IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK:TM5_KPP_ROS_INTEGRATOR',1,ZHOOK_HANDLE) 
#endif
      RETURN
   ENDIF
   IF ( ((PT+0.1_JPRD*ZH) == PT).OR.(ZH <= PRoundoff) ) THEN  ! Step size too small
      CALL ROS_ERRORMSG(-7,PT,ZH,KERR)
#ifdef WITH_COMPO_DR_HOOK
      IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK:TM5_KPP_ROS_INTEGRATOR',1,ZHOOK_HANDLE) 
#endif
      RETURN
   ENDIF

!~~~>  Limit H if necessary to avoid going beyond PTEND
   ZH = MIN(ZH,ABS(PTEND-PT))

!~~~>   Compute the function at current time
   CALL TM5_KPP_FunTemplate(PT,PY,ZFcn0, PRCONST)
   KSTATUS(Nfun) = KSTATUS(Nfun) + 1

!~~~>  Compute the function derivative with respect to T
   IF (.NOT.LD_Autonomous) THEN
      CALL TM5_KPP_ros_FunTimeDerivative ( PT, PRoundoff, PY, &
&                ZFcn0, ZdFdT, PRCONST )
   ENDIF

!~~~>   Compute the Jacobian at current time
   CALL TM5_KPP_JacTemplate(PT,PY,ZJac0,PRCONST)
   KSTATUS(Njac) = KSTATUS(Njac) + 1

!~~~>  Repeat step calculation until current step accepted
UntilAccepted: DO

   CALL TM5_KPP_ros_PrepareMatrix(ZH,K_Direction,Z_ROS_GAMMA(1), &
    &      ZJac0,ZGhimj,KPIVOT,LL_Singular)
   IF (LL_Singular) THEN ! More than 5 consecutive failed decompositions
       CALL ROS_ERRORMSG(-8,PT,ZH,KERR)
#ifdef WITH_COMPO_DR_HOOK
       IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK:TM5_KPP_ROS_INTEGRATOR',1,ZHOOK_HANDLE) 
#endif
       RETURN
   ENDIF

!~~~>   Compute the stages
Stage: DO istage = 1, IROS_S

      ! Current istage offset. Current istage vector is K(ioffset+1:ioffset+N)
       ioffset = NVAR*(istage-1)

      ! For the 1st istage the function has been computed previously
       IF ( istage == 1 ) THEN
         !slim CALL WCOPY(N,Fcn0,1,Fcn,1)
         ZFcn(1:NVAR) = ZFcn0(1:NVAR)
      ! istage>1 and a new function evaluation is needed at the current istage
       ELSEIF ( LL_ROS_NEWF(istage) ) THEN
         !slim CALL WCOPY(N,PY,1,Ynew,1)
         ZYnew(1:NVAR) = PY(1:NVAR)
         DO j = 1, istage-1
           CALL TM5_KPP_WAXPY(NVAR,Z_ROS_A((istage-1)*(istage-2)/2+j), &
&            ZK(NVAR*(j-1)+1),1,ZYnew,1)
         ENDDO
         ZTau = PT + Z_ROS_Alpha(istage)*K_Direction*ZH
         CALL TM5_KPP_FunTemplate(ZTau,ZYnew,ZFcn,PRCONST)
         KSTATUS(Nfun) = KSTATUS(Nfun) + 1
       ENDIF ! if istage == 1 elseif LL_ROS_NEWF(istage)
       !slim: CALL WCOPY(N,Fcn,1,K(ioffset+1),1)
       ZK(ioffset+1:ioffset+NVAR) = ZFcn(1:NVAR)
       DO j = 1, istage-1
         ZHC = Z_ROS_C((istage-1)*(istage-2)/2+j)/(K_Direction*ZH)
         CALL TM5_KPP_WAXPY(NVAR,ZHC,ZK(NVAR*(j-1)+1),1,ZK(ioffset+1),1)
       ENDDO
       IF ((.NOT. LD_Autonomous).AND.(Z_ROS_GAMMA(istage) /= ZERO)) THEN
         ZHG = K_Direction*ZH*Z_ROS_GAMMA(istage)
         CALL TM5_KPP_WAXPY(NVAR,ZHG,ZdFdT,1,ZK(ioffset+1),1)
       ENDIF
       CALL TM5_KPP_ROS_SOLVE(ZGhimj, KPIVOT, ZK(ioffset+1))

   ENDDO Stage


!~~~>  Compute the new solution
   !slim: CALL WCOPY(N,PY,1,Ynew,1)
   ZYnew(1:NVAR) = PY(1:NVAR)
   DO j=1,IROS_S
         CALL TM5_KPP_WAXPY(NVAR,Z_ROS_M(j),ZK(NVAR*(j-1)+1),1,ZYnew,1)
   ENDDO

!~~~>  Compute the error estimation
   !slim CALL WSCAL(NVAR,ZERO,Yerr,1)
   ZYerr(1:NVAR) = ZERO
   DO j=1,IROS_S
        CALL TM5_KPP_WAXPY(NVAR,Z_ROS_E(j),ZK(NVAR*(j-1)+1),1,ZYerr,1)
   ENDDO
   ZErr = TM5_KPP_ros_ErrorNorm ( PY, ZYnew, ZYerr, PABSTOL, PRELTOL, LD_VectorTol )

!~~~> New step size is bounded by FacMin <= Hnew/H <= FacMax
   ZFac  = MIN(PFacMax,MAX(PFacMin,PFacSafe/ZErr**(Z_ONE/Z_ROS_ELO)))
   ZHnew = ZH*ZFac

!~~~>  Check the error magnitude and adjust step size
   KSTATUS(Nstp) = KSTATUS(Nstp) + 1
   IF ( (ZErr <= Z_ONE).OR.(ZH <= PHmin) ) THEN  !~~~> Accept step
      KSTATUS(Nacc) = KSTATUS(Nacc) + 1
      ! mz_rs_20061212+
      ! old version did allow negative values here:
      !slim: CALL WCOPY(N,Ynew,1,Y,1)
      !Y(1:NVAR) = Ynew(1:NVAR)
      ! new value is positive definite:
      PY = MAX(ZYnew,ZERO)
      ! mz_rs_20061212-
      PT = PT + K_Direction*ZH
      ZHnew = MAX(ZHmin,MIN(ZHnew,ZHmax))
      IF (LL_RejectLastH) THEN  ! No step size increase after a rejected step
         ZHnew = MIN(ZHnew,ZH)
      ENDIF
      PRSTATUS(Nhexit) = ZH
      PRSTATUS(Nhnew)  = ZHnew
      PRSTATUS(Ntexit) = PT
      LL_RejectLastH = .FALSE.
      LL_RejectMoreH = .FALSE.
      ZH = ZHnew
      EXIT UntilAccepted ! EXIT THE LOOP: WHILE STEP NOT ACCEPTED
   ELSE           !~~~> Reject step
      IF (LL_RejectMoreH) THEN
         ZHnew = ZH*PFacRej
      ENDIF
      LL_RejectMoreH = LL_RejectLastH
      LL_RejectLastH = .TRUE.
      ZH = ZHnew
      IF (KSTATUS(Nacc) >= 1)  KSTATUS(Nrej) = KSTATUS(Nrej) + 1
   ENDIF ! Err <= 1

   ENDDO UntilAccepted

   ENDDO TimeLoop

!~~~> Succesful exit
   KERR = 1  !~~~> The integration was successful

#ifdef WITH_COMPO_DR_HOOK
   IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK:TM5_KPP_ROS_INTEGRATOR',1,ZHOOK_HANDLE) 
#endif
  END SUBROUTINE TM5_KPP_ROS_INTEGRATOR


!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
  REAL(KIND=JPRD) FUNCTION TM5_KPP_ros_ErrorNorm ( PY, PYnew, PYerr, &
&                               PABSTOL, PRELTOL, LD_VectorTol )
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!~~~> Computes the "scaled norm" of the error vector Yerr
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
   USE YOMHOOK   ,ONLY : LHOOK,   DR_HOOK, JPHOOK
   USE PARKIND1  , ONLY : JPIM     ,JPRD
   USE TM5_KPP_Parameters, ONLY : NVAR
   IMPLICIT NONE

! Input arguments
   REAL(KIND=JPRD), INTENT(IN) :: PY(NVAR), PYnew(NVAR), &
&          PYerr(NVAR), PABSTOL(NVAR), PRELTOL(NVAR)
   LOGICAL, INTENT(IN) ::  LD_VectorTol
! Local variables
   REAL(KIND=JPRD) :: ZErr, ZScale, ZYmax
   INTEGER(KIND=JPIM)  :: i
   REAL(KIND=JPRD), PARAMETER :: ZERO = 0.0_JPRD
#ifdef WITH_COMPO_DR_HOOK
   REAL(KIND=JPHOOK)    :: ZHOOK_HANDLE
#endif

#ifdef WITH_COMPO_DR_HOOK
   IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK:TM5_KPP_ROS_ERRORNORM',0,ZHOOK_HANDLE) 
#endif

   ZErr = ZERO
   DO i=1,NVAR
     ZYmax = MAX(ABS(PY(i)),ABS(PYnew(i)))
     IF (LD_VectorTol) THEN
       ZScale = PABSTOL(i)+PRELTOL(i)*ZYmax
     ELSE
       ZScale = PABSTOL(1)+PRELTOL(1)*ZYmax
     ENDIF
     ZErr = ZErr+(PYerr(i)/ZScale)**2
   ENDDO
   ZErr  = SQRT(ZErr/NVAR)

   TM5_KPP_ros_ErrorNorm = MAX(ZErr,1.0E-10_JPRD)
#ifdef WITH_COMPO_DR_HOOK
   IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK:TM5_KPP_ROS_ERRORNORM',1,ZHOOK_HANDLE) 
#endif

  END FUNCTION TM5_KPP_ros_ErrorNorm


!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
  SUBROUTINE TM5_KPP_ros_FunTimeDerivative ( PT, PRoundoff, PY, &
    &            PFcn0, PdFdT,PRCONST )
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!~~~> The time partial derivative of the function by finite differences
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
   USE YOMHOOK   ,ONLY : LHOOK,   DR_HOOK, JPHOOK
   USE PARKIND1  , ONLY : JPIM  ,JPRB   ,JPRD
   USE TM5_KPP_PARAMETERS, ONLY : NREACT,Nfun,NVAR
   IMPLICIT NONE

!~~~> Input arguments
   REAL(KIND=JPRD), INTENT(IN) :: PT, PRoundoff, PY(NVAR), PFcn0(NVAR),PRCONST(NREACT)
!~~~> Output arguments
   REAL(KIND=JPRD), INTENT(OUT) :: PdFdT(NVAR)
!~~~> Local variables
   REAL(KIND=JPRD) :: ZDelta
   REAL(KIND=JPRD), PARAMETER :: Z_ONE = 1.0_JPRD, ZDeltaMin = 1.0E-6_JPRD
#ifdef WITH_COMPO_DR_HOOK
   REAL(KIND=JPHOOK)    :: ZHOOK_HANDLE
#endif

#ifdef WITH_COMPO_DR_HOOK
   IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK:TM5_KPP_ROS_FUNTIMEDERIVATIVE',0,ZHOOK_HANDLE) 
#endif
   ZDelta = SQRT(PRoundoff)*MAX(ZDeltaMin,ABS(PT))
   CALL TM5_KPP_FunTemplate(PT+ZDelta,PY,PdFdT,PRCONST)
   KSTATUS(Nfun) = KSTATUS(Nfun) + 1
   CALL TM5_KPP_WAXPY(NVAR,(-Z_ONE),PFcn0,1,PdFdT,1)
   CALL TM5_KPP_WSCAL(NVAR,(Z_ONE/ZDelta),PdFdT,1)
#ifdef WITH_COMPO_DR_HOOK
   IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK:TM5_KPP_ROS_FUNTIMEDERIVATIVE',1,ZHOOK_HANDLE) 
#endif

  END SUBROUTINE TM5_KPP_ros_FunTimeDerivative


!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
  SUBROUTINE TM5_KPP_ros_PrepareMatrix ( PH, K_Direction, Pgam, &
     &        PJac0, PGhimj, KPIVOT, LD_Singular )
! --- --- --- --- --- --- --- --- --- --- --- --- ---
!  Prepares the LHS matrix for stage calculations
!  1.  Construct Ghimj = 1/(H*ham) - Jac0
!      "(Gamma H) Inverse Minus Jacobian"
!  2.  Repeat LU decomposition of Ghimj until successful.
!       -half the step size if LU decomposition fails and retry
!       -exit after 5 consecutive fails
! --- --- --- --- --- --- --- --- --- --- --- --- ---
   USE YOMHOOK,    ONLY : LHOOK,   DR_HOOK, JPHOOK
   USE YOMLUN,     ONLY : NULOUT
   USE PARKIND1,   ONLY : JPIM  , JPRB   ,JPRD
   USE TM5_KPP_JACOBIANSP, ONLY : LU_DIAG
   USE TM5_KPP_PARAMETERS, ONLY : LU_NONZERO,NVAR, Nsng
   IMPLICIT NONE

!~~~> Input arguments
#ifdef FULL_ALGEBRA    
   REAL(KIND=JPRD), INTENT(IN) ::  PJac0(NVAR,NVAR)
#else
   REAL(KIND=JPRD), INTENT(IN) ::  PJac0(LU_NONZERO)
#endif   
   REAL(KIND=JPRD), INTENT(IN) ::  Pgam
   INTEGER(KIND=JPIM), INTENT(IN) ::  K_Direction
!~~~> Output arguments
#ifdef FULL_ALGEBRA    
   REAL(KIND=JPRD), INTENT(OUT) :: PGhimj(NVAR,NVAR)
#else
   REAL(KIND=JPRD), INTENT(OUT) :: PGhimj(LU_NONZERO)
#endif   
   LOGICAL, INTENT(OUT) ::  LD_Singular
   INTEGER(KIND=JPIM), INTENT(OUT) ::  KPIVOT(NVAR)
!~~~> Inout arguments
   REAL(KIND=JPRD), INTENT(INOUT) :: PH   ! step size is decreased when LU fails
!~~~> Local variables
   INTEGER(KIND=JPIM)  :: i, ising, INconsecutive
   REAL(KIND=JPRD) :: Zghinv
   REAL(KIND=JPRD), PARAMETER :: Z_ONE  = 1.0_JPRD, ZHALF = 0.5_JPRD
#ifdef WITH_COMPO_DR_HOOK
   REAL(KIND=JPHOOK)    :: ZHOOK_HANDLE
#endif

#ifdef WITH_COMPO_DR_HOOK
   IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK:TM5_KPP_ROS_PREPAREMATRIX',0,ZHOOK_HANDLE) 
#endif

   INconsecutive = 0
   LD_Singular = .TRUE.

   DO WHILE (LD_Singular)

!~~~>    Construct Ghimj = 1/(H*gam) - Jac0
#ifdef FULL_ALGEBRA    
     !slim: CALL TM5_KPP_WCOPY(NVAR*NVAR,Jac0,1,PGhimj,1)
     !slim: CALL TM5_KPP_WSCAL(NVAR*NVAR,(-Z_ONE),PGhimj,1)
     PGhimj = -PJac0
     Zghinv = Z_ONE/(K_Direction*PH*gam)
     DO i=1,N
       PGhimj(i,i) = PGhimj(i,i)+Zghinv
     ENDDO
#else
     !slim: CALL TM5_KPP_WCOPY(LU_NONZERO,PJac0,1,PGhimj,1)
     !slim: CALL TM5_KPP_WSCAL(LU_NONZERO,(-Z_ONE),PGhimj,1)
     PGhimj(1:LU_NONZERO) = -PJac0(1:LU_NONZERO)
     Zghinv = Z_ONE/(K_Direction*PH*Pgam)
     DO i=1,NVAR
       PGhimj(LU_DIAG(i)) = PGhimj(LU_DIAG(i))+Zghinv
     ENDDO
#endif   
!~~~>    Compute LU decomposition
     CALL TM5_KPP_ros_Decomp( PGhimj, KPIVOT, ising )
     IF (ising == 0) THEN
!~~~>    If successful done
        LD_Singular = .FALSE.
     ELSE ! ising .ne. 0
!~~~>    If unsuccessful half the step size; if 5 consecutive fails then return
        KSTATUS(Nsng) = KSTATUS(Nsng) + 1
        INconsecutive = INconsecutive+1
        LD_Singular = .TRUE.
        WRITE(NULOUT,*) 'TM5_KPP Warning: LU Decomposition returned ising = ',ising
        IF (INconsecutive <= 5) THEN ! Less than 5 consecutive failed decompositions
           PH = PH*ZHALF
        ELSE  ! More than 5 consecutive failed decompositions
#ifdef WITH_COMPO_DR_HOOK
           IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK:TM5_KPP_ROS_PREPAREMATRIX',1,ZHOOK_HANDLE) 
#endif
           RETURN
        ENDIF  ! INconsecutive
      ENDIF    ! ising

   ENDDO ! WHILE Singular

#ifdef WITH_COMPO_DR_HOOK
   IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK:TM5_KPP_ROS_PREPAREMATRIX',1,ZHOOK_HANDLE) 
#endif

  END SUBROUTINE TM5_KPP_ros_PrepareMatrix


!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
  SUBROUTINE TM5_KPP_ROS_DECOMP( PA, KPIVOT, KSING )
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!  Template for the LU decomposition
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
   USE YOMHOOK,    ONLY : LHOOK,   DR_HOOK, JPHOOK
   USE PARKIND1,   ONLY : JPIM     ,JPRB, JPRD
   USE TM5_KPP_PARAMETERS, ONLY : NVAR,LU_NONZERO , Ndec
   IMPLICIT NONE
!~~~> Inout variables
#ifdef FULL_ALGEBRA    
   REAL(KIND=JPRD), INTENT(INOUT) :: PA(NVAR,NVAR)
#else   
   REAL(KIND=JPRD), INTENT(INOUT) :: PA(LU_NONZERO)
   REAL(KIND=JPRD)                :: ZA(LU_NONZERO)
#endif
!~~~> Output variables
   INTEGER(KIND=JPIM), INTENT(OUT) :: KPIVOT(NVAR), KSING
#ifdef WITH_COMPO_DR_HOOK
   REAL(KIND=JPHOOK)    :: ZHOOK_HANDLE
#endif

#ifdef WITH_COMPO_DR_HOOK
   IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK:TM5_KPP_ROS_DECOMP',0,ZHOOK_HANDLE) 
#endif

#ifdef FULL_ALGEBRA    
   CALL  DGETRF( N, N, PA, N, KPIVOT, KSING )
#else   
   IF (JPRD==JPRD) THEN
     CALL TM5_KPP_KppDecomp ( PA, KSING )
   ELSE
     ! Cast SP into DP variable..
     ZA(:)=PA(:)
     CALL TM5_KPP_KppDecomp ( ZA, KSING )
     PA(:)=ZA(:)
   ENDIF
   KPIVOT(1) = 1
#endif
   KSTATUS(Ndec) = KSTATUS(Ndec) + 1
#ifdef WITH_COMPO_DR_HOOK
   IF (LHOOK) CALL DR_HOOK('TM5_KPP_ROSENBROCK:TM5_KPP_ROS_DECOMP',1,ZHOOK_HANDLE) 
#endif

  END SUBROUTINE TM5_KPP_ROS_DECOMP


