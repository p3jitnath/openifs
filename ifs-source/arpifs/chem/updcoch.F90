SUBROUTINE UPDCOCH(YDGEOMETRY,YDDYNA,YDCHEM,YDRIP)

!**** *UPDCOCH*

!     PURPOSE.
!     --------

!     Updates the climatological CO data for the parametrization 
!     of carbon monoxyde chemistry

!**   INTERFACE.
!     ----------

!     CALL UPDCOCH

!     METHOD.
!     -------

!     Reads the monthly values of the 5 carbon monoxyde chemistry fields.
!     For a given day, the value is interpolated linearly between the 15th
!     of the nearest months. Values for all months are on unit NULCL1.

!     EXTERNALS.
!     ----------

!     UPDCAL

!     AUTHORS.
!     --------

!       S. MASSART (CERFACS/ECMWF) APR 11. (based on routines UPDO3CH)
!
!     MODIFICATIONS.
!     --------------
!
! ---------------------------------------------------------------------------

USE GEOMETRY_MOD , ONLY : GEOMETRY
USE PARKIND1 , ONLY : JPIM, JPRB, JPRD
USE YOMHOOK  , ONLY : LHOOK, DR_HOOK, JPHOOK
USE YOMLUN   , ONLY : NULOUT
USE YOMCST   , ONLY : RDAY
USE YOMCT0   , ONLY : LNF 
USE YOMCT2   , ONLY : NSTAR2
USE YOMRIP0  , ONLY : NINDAT
USE YOMRIP   , ONLY : TRIP
USE YOMMP0   , ONLY : MYPROC, NPROC
USE MPL_MODULE,ONLY : MPL_BROADCAST
USE YOMDYNA  , ONLY : TDYNA
USE YOMCHEM  , ONLY : TCHEM


! ---------------------------------------------------------------------------

IMPLICIT NONE

TYPE(GEOMETRY), INTENT(IN)   :: YDGEOMETRY
TYPE(TDYNA)   , INTENT(IN)   :: YDDYNA
TYPE(TCHEM)    ,INTENT(INOUT):: YDCHEM
TYPE(TRIP)     ,INTENT(INOUT):: YDRIP

! The unformatted READs filling these are of a double-precision data file
REAL(KIND=JPRD),ALLOCATABLE :: ZFLD1(:,:), ZFLD2(:,:)

INTEGER(KIND=JPIM) :: ILMOIS(12)
CHARACTER :: CLNOMF*34

INTEGER(KIND=JPIM) :: IA0, IAN, ICH, ICHF1, ICHF2, IJ0, IJOUR,&
 & IJT1, IJT2, ILATF1, ILATF2, ILEVF1, ILEVF2, &
 & IM0, IMOIS, IMONTH1, IMONTH2, IMT1, &
 & IMT2, IOMASTER, IPNCH1, IPNCH2, ITAG1, &
 & ITIME, IZT, ISTADD, JCH, JGL, JJ, JLEV, IDGLG, IERR 

REAL(KIND=JPRB) :: ZPOID1, ZPOID2
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE


! ---------------------------------------------------------------------------

#include "fcttim.func.h"

! ---------------------------------------------------------------------------

#include "abor1.intfb.h"
#include "updcal.intfb.h"

! ---------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('UPDCOCH',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM=>YDGEOMETRY%YRDIM,YDDIMV=>YDGEOMETRY%YRDIMV)
ASSOCIATE(NFLEVG=>YDDIMV%NFLEVG, &
  & NSTADD=>YDRIP%NSTADD, TSTEP=>YDRIP%TSTEP, &
  & NDGLG=>YDDIM%NDGLG, &
  & NUCOCH1=>YDCHEM%NUCOCH1, NUCOCH2=>YDCHEM%NUCOCH2, & 
  & NCHEM_LCOCOEF=>YDCHEM%NCHEM_LCOCOEF, CHEM_LCOVERS=>YDCHEM%CHEM_LCOVERS, &
  & LCHEM_LCOCSTCLIM=>YDCHEM%LCHEM_LCOCSTCLIM)
!!! DO I NEED TSTEP FROM YRRIP FROM AN ARGUMENT ??? 


! ---------------------------------------------------------------------------

!*     1. SETTING CONSTANT VALUES.
!     ---------------------------

IERR=0

IF(.NOT.LNF.AND.NSTADD == 0) THEN
! IN CASE OF RESTART:
  ITIME=NINT(TSTEP)
  IF (YDDYNA%LTWOTL) THEN
    IZT=NINT(TSTEP*(REAL(NSTAR2,JPRB)+0.5_JPRB))
  ELSE
    IZT=ITIME*NSTAR2
  ENDIF
  ISTADD=IZT/NINT(RDAY)
ELSE
  ISTADD=NSTADD
ENDIF

!*    1.1 Calendar

IJ0=NDD(NINDAT)
IM0=NMM(NINDAT)
IA0=NCCAA(NINDAT)
CALL UPDCAL(IJ0,IM0,IA0,ISTADD,IJOUR,IMOIS,IAN,ILMOIS,NULOUT)

IF(IJOUR >= 15)THEN
  IMT1=IMOIS
  IMT2=1+MOD(IMOIS,12)
  IJT1=15
  IJT2=15+ILMOIS(IMT1)
ELSE
  IMT1=1+MOD(IMOIS+10,12)
  IMT2=IMOIS
  IJT1=15-ILMOIS(IMT1)
  IJT2=15
ENDIF

!WRITE(NULOUT,*) ' UPDCOCH: IJOUR=',IJOUR,' IMOIS=',IMOIS,' IAN=',IAN
!WRITE(NULOUT,*) ' UPDCOCH: IMT1=',IMT1,' IMT2=',IMT2

! WEIGHTS FOR TIME INTERPOLATION

ZPOID1=REAL(IJT2-IJOUR,JPRB)/REAL(IJT2-IJT1,JPRB)
ZPOID2=1.0_JPRB-ZPOID1
!WRITE(NULOUT,*) ' ZPOID1=',ZPOID1,' ZPOID2=',ZPOID2


!*
!       2. READING MONTHLY VALUES AND TIME INTERPOLATION 
!       ------------------------------------------------

IDGLG=NDGLG
ALLOCATE(ZFLD1(IDGLG,NFLEVG))
ALLOCATE(ZFLD2(IDGLG,NFLEVG))
IOMASTER=MIN(1,NPROC)
IF(MYPROC == IOMASTER) THEN

!*      2.1 OPEN CLIMATE FILE MONTH 1

  WRITE(UNIT=NULOUT,FMT='(A)') ' READ CO CHEM. FIELDS '
  WRITE(UNIT=CLNOMF,FMT='(''COCHEM'',I2.2,A)') IMT1,CHEM_LCOVERS
  WRITE(UNIT=NULOUT,FMT='(A,A)')&
     & ' INITIAL DATA FOR MONTH 1 TO BE READ FROM FILE ',CLNOMF  
  WRITE(UNIT=NULOUT,FMT='(A,I4.4)')&
     & ' LOGICAL UNIT NUMBER ',NUCOCH1  
  

  OPEN(NUCOCH1,FILE=CLNOMF,FORM='UNFORMATTED',STATUS='OLD',IOSTAT=IERR,ACTION='READ')
  IF(IERR > 0) CALL ABOR1(' UPDCOCH: PROBLEM OPENING COCHEM FILE FOR FIRST MONTH' )
  READ(NUCOCH1) IMONTH1
  READ(NUCOCH1) IPNCH1

!*      2.2 OPEN CLIMATE FILE MONTH 2

  WRITE(UNIT=CLNOMF,FMT='(''COCHEM'',I2.2,A)') IMT2,CHEM_LCOVERS
  WRITE(UNIT=NULOUT,FMT='(A,A)')&
   & ' INITIAL DATA FOR MONTH 2 TO BE READ FROM FILE ',CLNOMF  
  WRITE(UNIT=NULOUT,FMT='(A,I4.4)')&
     & ' LOGICAL UNIT NUMBER ',NUCOCH2  

  OPEN(NUCOCH2,FILE=CLNOMF,FORM='UNFORMATTED',STATUS='OLD',IOSTAT=IERR,ACTION='READ')
  IF(IERR > 0) CALL ABOR1(' UPDCOCH: PROBLEM OPENING COCHEM FILE FOR SECOND MONTH' )
  READ(NUCOCH2) IMONTH2
  READ(NUCOCH2) IPNCH2

!  WRITE(NULOUT,*) ' UPDCOCH: IMONTH1=',IMONTH1,' IMT1=',IMT1
!  WRITE(NULOUT,*) ' UPDCOCH: IMONTH2=',IMONTH2,' IMT2=',IMT2
  IF(IPNCH2 /= IPNCH1) THEN
    CALL ABOR1(' UPDCOCH: IPNCH2 /= IPNCH1')
  ENDIF
!  WRITE(NULOUT,*) ' UPDCOCH: IPNCH1=',IPNCH1,' NCHEM_LCOCOEF=',NCHEM_LCOCOEF

!*      2.3 READ THE FIELDS FOR MONTH 1 AND MONTH 2

!    LOOP OVER FIELDS
  DO JCH=1,NCHEM_LCOCOEF

! MONTH 1

    READ(NUCOCH1)ICHF1,ILEVF1,ILATF1
!    WRITE(NULOUT,FMT='('' ICHF1'',I3,'' ILEVF1'',I3,'' ILATF1'',I4 &
!     & )')  ICHF1,ILEVF1,ILATF1  

    READ(NUCOCH1)

! MONTH 2

    READ(NUCOCH2)ICHF2,ILEVF2,ILATF2
!    WRITE(NULOUT,FMT='('' ICHF2'',I3,'' ILEVF2'',I3,'' ILATF2'',I4 &
!     & )')  ICHF2,ILEVF2,ILATF2  

    READ(NUCOCH2)

! CHECKING FOR CONSISTENCY
    IF(ICHF2 /= ICHF1.OR.ICHF1 /= JCH) THEN
      CALL ABOR1(' UPDCOCH: ICHF2 /= ICHF1.OR.ICHF1 /= JCH')
    ELSEIF (ILEVF2 /= ILEVF1.OR.ILEVF1 /= NFLEVG) THEN
      CALL ABOR1(' UPDCOCH: ILEVF2 /= ILEVF1.OR.ILEVF1 /= NFLEVG')
    ELSEIF (ILATF2 /= ILATF1.OR.ILATF1 /= IDGLG) THEN
      CALL ABOR1(' UPDCOCH: ILATF2 /= ILATF1.OR.ILATF1 /= NDGLG')
    ENDIF
    DO JLEV=1,ILEVF1
      READ (NUCOCH1)(ZFLD1(JJ,JLEV),JJ=1,ILATF1)
      READ (NUCOCH2)(ZFLD2(JJ,JLEV),JJ=1,ILATF2)

!  TIME INTERPOLATION AND WRITING TO COMMON YOMCO

      ICH=(ICHF1-1)*ILEVF1+JLEV

!  LATITUDExLEVEL COMMON 
!  ADAPTED FOR MESSAGE PASSING
        DO JGL=1,IDGLG
          YDCHEM%TCO2DG(ICH,JGL)=REAL(ZFLD1(JGL,JLEV),KIND=JPRB)*ZPOID1 + REAL(ZFLD2(JGL,JLEV),KIND=JPRB)*ZPOID2
        ENDDO
!    WRITE(NULOUT,*) 'TEST F1 F2 MAX ',JCH, JLEV, MAXVAL(ZFLD1(:,JLEV)), MAXVAL(ZFLD2(:,JLEV)) 
!    WRITE(NULOUT,*) 'TEST F1 F2 MIN ',JCH, JLEV, MINVAL(ZFLD1(:,JLEV)), MINVAL(ZFLD2(:,JLEV)) 
! END LOOP OVER LEVELS
    ENDDO
! END LOOP OVER FIELDS
  ENDDO

!*    2.4 CLOSE CLIMATE FILES

  CLOSE(NUCOCH1)
  CLOSE(NUCOCH2)

!*    TOP BOUNDARY CLIMATE READING

  IF (.NOT. LCHEM_LCOCSTCLIM) THEN
  
!*      2.5 OPEN TOP BOUNDARY CLIMATE FILE MONTH 1

    WRITE(UNIT=NULOUT,FMT='(A)') ' READ TOP CO FIELDS '
    WRITE(UNIT=CLNOMF,FMT='(''COTOPMLS_'',I2.2,A)') IMT1
    WRITE(UNIT=NULOUT,FMT='(A,A)')&
     & ' TOP DATA FOR MONTH 1 TO BE READ FROM FILE ',CLNOMF  
    WRITE(UNIT=NULOUT,FMT='(A,I4.4)')&
     & ' LOGICAL UNIT NUMBER ',NUCOCH1  
  

    OPEN(NUCOCH1,FILE=CLNOMF,FORM='UNFORMATTED',STATUS='OLD',IOSTAT=IERR,ACTION='READ')
    IF(IERR > 0) CALL ABOR1(' UPDCOCH: PROBLEM OPENING COTOPMLS FILE FOR FIRST MONTH' )

!*      2.6 OPEN TOP BOUNDARY CLIMATE FILE MONTH 2

    WRITE(UNIT=CLNOMF,FMT='(''COTOPMLS_'',I2.2,A)') IMT2
    WRITE(UNIT=NULOUT,FMT='(A,A)')&
     & ' TOP DATA FOR MONTH 2 TO BE READ FROM FILE ',CLNOMF  
    WRITE(UNIT=NULOUT,FMT='(A,I4.4)')&
       & ' LOGICAL UNIT NUMBER ',NUCOCH2  

    OPEN(NUCOCH2,FILE=CLNOMF,FORM='UNFORMATTED',STATUS='OLD',IOSTAT=IERR,ACTION='READ')
    IF(IERR > 0) CALL ABOR1(' UPDCOCH: PROBLEM OPENING COTOPMLS FILE FOR SECOND MONTH' )

!*      2.7 READ THE FIELDS FOR MONTH 1 AND MONTH 2

    READ (NUCOCH1)(ZFLD1(JJ,1),JJ=1,ILATF1)
    READ (NUCOCH2)(ZFLD2(JJ,1),JJ=1,ILATF2)

    DO JGL=1,IDGLG
      YDCHEM%TCOTOP(JGL) = REAL(ZFLD1(JGL,1),KIND=JPRB)*ZPOID1 + REAL(ZFLD2(JGL,1),KIND=JPRB)*ZPOID2
    ENDDO

!*    2.8 CLOSE CLIMATE FILES

    CLOSE(NUCOCH1)
    CLOSE(NUCOCH2)

  ENDIF !* .NOT. LCHEM_LCOCSTCLIM

  
ENDIF

!*    2.5 DISTRIBUTION OF FIELDS FROM IOMASTER TO ALL OTHER PE'S

IF(NPROC > 1) THEN
  CALL MPL_BROADCAST(YDCHEM%TCO2DG,KROOT=IOMASTER,KTAG=ITAG1,CDSTRING='UPDCOCH:')
  IF (.NOT. LCHEM_LCOCSTCLIM) &
 &  CALL MPL_BROADCAST(YDCHEM%TCOTOP,KROOT=IOMASTER,KTAG=ITAG1,CDSTRING='UPDCOCH:')
ENDIF

DEALLOCATE(ZFLD1)
DEALLOCATE(ZFLD2)

! ---------------------------------------------------------------------------

END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('UPDCOCH',1,ZHOOK_HANDLE)
END SUBROUTINE UPDCOCH
