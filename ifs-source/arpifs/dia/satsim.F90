SUBROUTINE SATSIM(YGFL,KPROMA,KIDIA,KFDIA,KSATSIM,KFLEVG, &
 & PGELAT,PGELAM,POROG,PLSM,PCI,PTRENU,PQLINU,PCAPE,PTCLS,PQCLS,PUCLS,PVCLS,PDSP, &
 & PRESF,PT,PQ,PO3,PA,PL,PI,PR,PS, &
 & PDCLBT,PDCSBT)

!**** *SATSIM*  - CALCULATE SIMULATED SATELLITE IMAGES WITH RTTOV

!     PURPOSE.
!     --------

!**   INTERFACE.
!     ----------
!        *CALL* *SUSATSIM*

!        EXPLICIT ARGUMENTS
!        --------------------
!        NONE

!        IMPLICIT ARGUMENTS
!        --------------------
!        NONE

!     METHOD.
!     -------
!        So far this is hard coded for Meteosat 10.

!     EXTERNALS.
!     ----------

!     REFERENCE.
!     ----------
!        ECMWF Research Department documentation of the IFS

!     AUTHOR.
!     -------
!      TOMAS WILHELMSSON *ECMWF*  2015-08-28

!     MODIFICATIONS.
!     --------------
!     ------------------------------------------------------------------

USE PARKIND1,     ONLY : JPIM, JPRB
USE YOMHOOK,      ONLY : LHOOK, DR_HOOK, JPHOOK
USE YOMLUN,       ONLY : NULERR
USE YOMCST,       ONLY : RPI
USE YOMSATSIM,    ONLY : NTOPLEVELS, NINST, NCHAN, MRTCHAN
USE RTTOV_CONST,  ONLY : ERRORSTATUS_SUCCESS 
USE RTTOV_EC_MOD, ONLY : RTTOV_EC_OPTS, IPAV
USE YOM_YGFL,     ONLY : TYPE_GFLD
USE YOMCST,       ONLY : RG

!     ------------------------------------------------------------------

IMPLICIT NONE

TYPE(TYPE_GFLD)    ,INTENT(IN)  :: YGFL
INTEGER(KIND=JPIM), INTENT(IN)  :: KPROMA
INTEGER(KIND=JPIM), INTENT(IN)  :: KIDIA
INTEGER(KIND=JPIM), INTENT(IN)  :: KFDIA
INTEGER(KIND=JPIM), INTENT(IN)  :: KSATSIM
INTEGER(KIND=JPIM), INTENT(IN)  :: KFLEVG
REAL(KIND=JPRB),    INTENT(IN)  :: PGELAM(KPROMA)
REAL(KIND=JPRB),    INTENT(IN)  :: PGELAT(KPROMA)
REAL(KIND=JPRB),   INTENT(IN)   :: POROG(KPROMA)
REAL(KIND=JPRB),    INTENT(IN)  :: PLSM(KPROMA)
REAL(KIND=JPRB),    INTENT(IN)  :: PCI(KPROMA)
REAL(KIND=JPRB),    INTENT(IN)  :: PTRENU(KPROMA)
REAL(KIND=JPRB),    INTENT(IN)  :: PQLINU(KPROMA)
REAL(KIND=JPRB),    INTENT(IN)  :: PCAPE(KPROMA)
REAL(KIND=JPRB),    INTENT(IN)  :: PTCLS(KPROMA)
REAL(KIND=JPRB),    INTENT(IN)  :: PQCLS(KPROMA)
REAL(KIND=JPRB),    INTENT(IN)  :: PUCLS(KPROMA)
REAL(KIND=JPRB),    INTENT(IN)  :: PVCLS(KPROMA)
REAL(KIND=JPRB),    INTENT(IN)  :: PDSP(KPROMA)
REAL(KIND=JPRB),    INTENT(IN)  :: PRESF(KPROMA,KFLEVG)
REAL(KIND=JPRB),    INTENT(IN)  :: PT(KPROMA,KFLEVG)
REAL(KIND=JPRB),    INTENT(IN)  :: PQ(KPROMA,KFLEVG)
REAL(KIND=JPRB),    INTENT(IN)  :: PO3(KPROMA,KFLEVG)
REAL(KIND=JPRB),    INTENT(IN)  :: PA(KPROMA,KFLEVG)
REAL(KIND=JPRB),    INTENT(IN)  :: PL(KPROMA,KFLEVG)
REAL(KIND=JPRB),    INTENT(IN)  :: PI(KPROMA,KFLEVG)
REAL(KIND=JPRB),    INTENT(IN)  :: PR(KPROMA,KFLEVG)
REAL(KIND=JPRB),    INTENT(IN)  :: PS(KPROMA,KFLEVG)
REAL(KIND=JPRB),    INTENT(OUT) :: PDCLBT(KPROMA,KSATSIM)
REAL(KIND=JPRB),    INTENT(OUT) :: PDCSBT(KPROMA,KSATSIM)

INTEGER(KIND=JPIM), PARAMETER :: IMJPNAV=6
REAL(KIND=JPRB),    PARAMETER :: ZTHEPWP2 = 0.1715_JPRB    ! from IFS tiles
! REAL(KIND=JPRB),    PARAMETER :: REARTH  = 6378.170_JPRB ! earth equatorial radius (km)
! REAL(KIND=JPRB),    PARAMETER :: SAT_LON = 0.0           ! SSP Longitude
! REAL(KIND=JPRB),    PARAMETER :: SAT_LAT = 0.0           ! SSP Latitude


INTEGER(KIND=JPIM) :: IERR, IINST, INPROF, IMXCHPF, INCHAN, IFPSIM, ITOPLEV
INTEGER(KIND=JPIM) :: JROF, JCHAN
REAL(KIND=JPRB) :: ZPSFC, ZWS
!REAL(KIND=JPRB) :: ZSATALT, ZDS, ZA, ZRL, ZRM, ZTANB, ZB, ZZ

INTEGER(KIND=JPIM) :: ISURF(KPROMA), ICHAN(KSATSIM*KPROMA), IPROF(KSATSIM*KPROMA)

REAL(KIND=JPRB) :: ZPRES(KFLEVG+1,KPROMA), ZAV(KFLEVG+1,IMJPNAV,KPROMA)
REAL(KIND=JPRB) :: ZT2M(KPROMA), ZQ2M(KPROMA), ZPSURF(KPROMA), ZTSKIN(KPROMA)
REAL(KIND=JPRB) :: ZU10M(KPROMA), ZV10M(KPROMA), ZCAPE(KPROMA)
REAL(KIND=JPRB) :: ZCTP(KPROMA), ZCFRAC(KPROMA), ZOROG(KPROMA)
REAL(KIND=JPRB) :: ZLAT(KPROMA), ZLON(KPROMA), ZEMIS(KSATSIM*KPROMA)
REAL(KIND=JPRB) :: ZANGL(KPROMA), ZANGA(KPROMA), ZANGS(KPROMA), ZANGSA(KPROMA)
REAL(KIND=JPRB) :: ZTB(KSATSIM*KPROMA), ZRAD(KSATSIM*KPROMA), ZTBCLD(KSATSIM*KPROMA)
REAL(KIND=JPRB) :: ZRADCLD(KSATSIM*KPROMA), ZRADCLD_ZCVM(KSATSIM*KPROMA)
REAL(KIND=JPRB) :: ZRADCLDLEV(KFLEVG+1,KSATSIM*KPROMA), ZTAU(KFLEVG+1,KSATSIM*KPROMA)
REAL(KIND=JPRB) :: ZTAUSFC(KSATSIM*KPROMA), ZOBS(KPROMA), ZVAROUT(4,KSATSIM*KPROMA)

LOGICAL :: LLRETR_EMIS_TSKIN = .FALSE.   ! Vertical interpolation in RTTOV

TYPE(RTTOV_EC_OPTS) :: EC_OPTS

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!     ------------------------------------------------------------------

#include "abor1.intfb.h"

#include "rttov_ec_alloc.h"
#include "rttov_ec.h"

IF (LHOOK) CALL DR_HOOK('SATSIM',0,ZHOOK_HANDLE)

!----------------------------------------------------------------------
!                  5. RTTOV calculations
!                 ----------------------
!----------------------------------------------------------------------

INPROF = KFDIA - KIDIA + 1

DO JROF = KIDIA, KFDIA
  ! Ps (Pa) -> Ps (hPa) 
  ZPSFC = PDSP(JROF)*0.01_JPRB

  ZPRES(1:KFLEVG,JROF) = PRESF(JROF,1:KFLEVG)*0.01_JPRB
  ZPRES(KFLEVG+1,JROF) = ZPSFC

  ZAV(1:KFLEVG,IPAV%T,JROF) = PT(JROF,1:KFLEVG)
  ZAV(1:KFLEVG,IPAV%Q,JROF) = MAX(PQ(JROF,1:KFLEVG), 1.E-10_JPRB)
!!  IF (YGFL%YO3%LGP) THEN
!!    ZAV(1:KFLEVG,IPAV%O3,JROF) = MAX(PO3(JROF,1:KFLEVG), 1.E-10_JPRB)
!!  ELSE
    ZAV(1:KFLEVG,IPAV%O3,JROF) = 1.E-10_JPRB
!!  ENDIF
  ZAV(1:KFLEVG,IPAV%L,JROF) = PL(JROF,1:KFLEVG)
  ! Add cloud ice and snow together.
  ZAV(1:KFLEVG,IPAV%I,JROF) = PI(JROF,1:KFLEVG) + PS(JROF,1:KFLEVG)
  ZAV(1:KFLEVG,IPAV%A,JROF) = PA(JROF,1:KFLEVG)

  ! Take lowest level for the surface
  ZAV(KFLEVG+1,:,JROF) = ZAV(KFLEVG,:,JROF)

  ZT2M(JROF)   = PTCLS(JROF)
  ZQ2M(JROF)   = MAX(PQCLS(JROF), 1.E-10_JPRB)
  ZPSURF(JROF) = ZPSFC
  ZU10M(JROF)  = PUCLS(JROF)
  ZV10M(JROF)  = PVCLS(JROF)
  ZTSKIN(JROF) = PTRENU(JROF)
  ZCAPE(JROF)  = PCAPE(JROF)

  ZCTP(JROF)   = 500._JPRB
  ZCFRAC(JROF) = 0.0_JPRB      ! Deactivates simple cloud computations

  ZLAT(JROF)   = PGELAT(JROF)*180._JPRB/RPI
  ZLON(JROF)   = PGELAM(JROF)*180._JPRB/RPI
  ZOROG(JROF)  = POROG(JROF)/RG

  ISURF(JROF)  = 1 + NINT(PLSM(JROF))

  ! Set up zenith angle ...

  ! ! ... satellite altitude
  ! ZSATALT = 6.610839_JPRB*REARTH - REARTH

  ! ! ... distance on the ground
  ! ZDS = REARTH*2._JPRB*ASIN(SQRT((1._JPRB - SIN(PGELAT(JROF))*SIN(SAT_LAT) - &
  !  & COS(PGELAT(JROF))*COS(SAT_LAT)*COS(PGELAM(JROF) - SAT_LON))/2._JPRB))

  ! ! ... easy trigonometry
  ! ZA  = ZDS/REARTH
  ! ZRL = REARTH*SIN(ZA)
  ! ZRM = REARTH*COS(ZA)
  ! ZTANB = ZRL / (REARTH + ZSATALT - ZRM)
  ! ZB  = ATAN(ZTANB)
  ! ZZ  = ZA + ZB
  ! ZANGL(JROF) = ZZ/RPI*180._JPRB

  ! ! ... RTTOV is not supposed to work for large zenith angles
  ! ZANGL(JROF) = MIN (65._JPRB, ZANGL(JROF))
  ! ZANGA(JROF) = 0.0_JPRB

  ! Nadir view everywhere
  ZANGL(JROF)  = 0.0_JPRB
  ZANGA(JROF)  = 0.0_JPRB

  ! Ignore solar effects for now
  ZANGS(JROF)  = 0.0_JPRB
  ZANGSA(JROF) = 0.0_JPRB
ENDDO

IFPSIM = 0
DO IINST = 1, NINST
  ITOPLEV = NTOPLEVELS(IINST)
  INCHAN = NCHAN(IINST)
  IMXCHPF = INCHAN*INPROF
  EC_OPTS%LCLOUD     = .TRUE.
  EC_OPTS%LTRACER(:) = .FALSE.

  CALL RTTOV_EC_ALLOC(IERR, IINST, IMXCHPF, INPROF, KFLEVG+1, EC_OPTS, 1_JPIM)

  DO JROF = KIDIA, KFDIA
    ! Setup channel and profile index arrays
    DO JCHAN = 1, INCHAN
      ICHAN((JROF-1)*INCHAN+JCHAN) = MRTCHAN(IFPSIM+JCHAN)
      IPROF((JROF-1)*INCHAN+JCHAN) = JROF
    ENDDO

    ! Set up IR surface emissivity
    ZWS = PQLINU(JROF) / ZTHEPWP2 ! < 1 => desert
    IF (PLSM(JROF) < 0.5_JPRB) THEN
      ZEMIS(INCHAN*(JROF-1)+1:INCHAN*JROF) = 0.0_JPRB  ! sea
    ELSEIF ( ZWS > 1.0_JPRB) THEN
      ZEMIS(INCHAN*(JROF-1)+1:INCHAN*JROF) = 0.99_JPRB ! moist land
    ELSE
      ZEMIS(INCHAN*(JROF-1)+1:INCHAN*JROF) = 0.93_JPRB ! dry land
    ENDIF
  ENDDO

  CALL RTTOV_EC &
    & (IERR,IINST,IMXCHPF,INPROF,KFLEVG+1,ITOPLEV,IMJPNAV,ICHAN,IPROF, &
    & ZPRES,ZAV,ZT2M,ZQ2M,ZPSURF,ZU10M,ZV10M, &
    & ZCTP,ZCFRAC,ZTSKIN,ISURF,ZEMIS, &
    & ZANGL,ZANGA,ZANGS,ZANGSA, &
    & ZLAT,ZLON,ZOROG, &
    & EC_OPTS,ZTB,ZRAD,ZTBCLD,ZRADCLD, &
    & ZRADCLD_ZCVM,ZRADCLDLEV,ZTAU,ZTAUSFC,LLRETR_EMIS_TSKIN, &
    & ZOBS,ZVAROUT,PCAPE=ZCAPE)
  
  IF (IERR /= ERRORSTATUS_SUCCESS) THEN
    WRITE(NULERR,*) 'RTTOV_EC errorstatus:',IERR
    CALL ABOR1('SATSIM: RTTOV_EC PROBLEM')
  ELSE
    DO JROF = KIDIA, KFDIA
      PDCLBT(JROF,IFPSIM+1:IFPSIM+INCHAN) = ZTBCLD(INCHAN*(JROF-1)+1:INCHAN*JROF)
      PDCSBT(JROF,IFPSIM+1:IFPSIM+INCHAN) =    ZTB(INCHAN*(JROF-1)+1:INCHAN*JROF)
    ENDDO
  ENDIF
  IFPSIM = IFPSIM + INCHAN

  CALL RTTOV_EC_ALLOC(IERR, IINST, IMXCHPF, INPROF, KFLEVG+1, EC_OPTS, 0_JPIM)
ENDDO

IF (LHOOK) CALL DR_HOOK('SATSIM',1,ZHOOK_HANDLE)
END SUBROUTINE SATSIM
