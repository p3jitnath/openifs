SUBROUTINE CPPHDDHE(YDDIM,YDSURF,YDML_DIAG,YDDPHY,YDRIP,YDPHY,KPROMA, KSTART, KPROF, KFLEV, &
 !     INPUT.
 & PNORDL, PNORDM, PUZGEO, PVMGEO,&
 & PLON, PLSM, PVFCI, &
 & PDIFCQ, PDIFCS, PDIFTQ, PDIFTS, PFCQNG,&
 & PFPLCL, PFPLCN, PFPLSL, PFPLSN,&
 & PFRSO , PFRTH ,&
 & PFHPCL, PFHPCN, PFHPSL, PFHPSN,&
 & PSTRCU, PSTRCV, PSTRDU, PSTRDV, PSTRTU, PSTRTV,&
 & PFCQLNG, PFSQRF,   PFSQSF ,  PFCQRNG,  PFCQSNG,PFSQLTUR, PFSQITUR ,&
 & PFCQNNG,&
 & PFCQLF,PFCQIF,PFSQLF,PFSQIF,&
 & PDHTLS ,PDHTSS ,PDHTTS ,PDHTIS ,&
 & PDHSSS ,PDHIIS ,PDHWLS ,PDHFSS ,&
 !     OUTPUT.
 & PDHCV, PDHCS)  
!---------------------------------------------------------------------------
!**** *CPPHDDHE*  - CALCUL DES FLUX/TENDANCES DYNAMIQUES
!                  DIAGNOSTICS PAR DOMAINES HORIZONTAUX
! CALCULATE PHYSICAL FLUXES AND TENDENCIES FOR DDH
!---------------------------------------------------------------------------
!     BUT
!     ---
!      CALCULS PHYSIQUES POUR LES DIAGNOSTICS DDH
!      AINSI QUE DES VARIABLES AU SOL A L INSTANT COURANT
! CALCULATES PHYSICS FOR DDH AS WELL AS SURFACE VARIABLES AT CURRENT TIME STEP
!   ** SURVEILLER LE POSITIONNEMENT DES CHAMPS AU MOYEN DE L INDICATEUR
!                           IDHCV

!     ARGUMENTS D ENTREE
!     ------------------

! - DIMENSIONS

!     KPROMA : DIMENSION HORIZONTALE.
!     KSTART : DEBUT DE LA BOUCLE HORIZONTALE.
!     KPROF  : FIN DE LA BOUCLE HORIZONTALE.
!     KFLEV  : DIMENSION VERTICALE ET NB DE NIVEAUX

! - TABLEAUX DE SORTIES ET AUTRES INFORMATIONS DDH
!     PDHCV :  TABLEAUX DE RESULTAT INITIALISE A 0.
!     PDHCS :  TABLEAUX DE RESULTAT AU SOL INITIALISE A 0.

! - TABLEAUX DE DONNEES
!     PLON           : LONGITUDE
!     PNORDL, PNORDM : DIRECTION DU NORD GEOGRAPHIQUE
!     PUZGEO, PVMGEO : VENT ZONAL ET MERIDIEN VRAIS GEOGRAPHIQUES
!            (UNIQUEMENT SI LHDHKS OU LHDMCI OU LHDENT VRAIS)

! - 1D (GEOGRAPHIQUE) .

! PLSM       : land-sea mask (values are 0 or 1)
! PVFCI      : sea-ice fraction

! - TABLEAUX ISSUS DES PARAMETRISATIONS
! - 2D (0:KFLEV) .

! PDIFCQ     : FLUX CONVECTIF D'HUMIDITE SPECIFIQUE (HORS PLUIE/NEIGE).
! PDIFCS     : FLUX CONVECTIF D'ENTHALPIE (HORS PLUIE/NEIGE).
! PDIFTQ     : FLUX TURBULENT (ET Q NEGATIF) D'HUMIDITE SPECIFIQUE.
! PDIFTS     : FLUX TURBULENT D'ENTHALPIE (OU D'ENERGIE STATIQUE SECHE).
! PFCQNG     : FLUX FICTIF D'EAU POUR CORRIGER LES Q<0.
! PFPLCL     : PRECIPITATIONS CONVECTIVES SOUS FORME LIQUIDE.
! PFPLCN     : PRECIPITATIONS CONVECTIVES SOUS FORME NEIGE.
! PFPLSL     : PRECIPITATIONS STRATIFORMES SOUS FORME LIQUIDE.
! PFPLSN     : PRECIPITATIONS STRATIFORMES SOUS FORME NEIGE.
! PFRSO      : FLUX DE RAYONNEMENT SOLAIRE.
! PFRTH      : FLUX DE RAYONNEMENT THERMIQUE.
! PSTRCU     : FLUX CONVECTIF DE QUANTITE DE MOUVEMENT "U".
! PSTRCV     : FLUX CONVECTIF DE QUANTITE DE MOUVEMENT "V".
! PSTRDU     : FLUX "GRAVITY WAVE DRAG" "U".
! PSTRDV     : FLUX "GRAVITY WAVE DRAG" "V".
! PSTRTU     : FLUX TURBULENT DE QUANTITE DE MOUVEMENT "U".
! PSTRTV     : FLUX TURBULENT DE QUANTITE DE MOUVEMENT "V".

! - TABLEAUX ISSUS DES CALCULS DE TENDANCES
! - 2D (0:KFLEV) .

! PFHPCL     : FLUX ENTHALPIE CONVECTIVES SOUS FORME LIQUIDE.
! PFHPCN     : FLUX ENTHALPIE CONVECTIVES SOUS FORME NEIGE.
! PFHPSL     : FLUX ENTHALPIE STRATIFORMES SOUS FORME LIQUIDE.
! PFHPSN     : FLUX ENTHALPIE STRATIFORMES SOUS FORME NEIGE.

! - 1D (DIAGNOSTIQUE) .

! PFCQLNG    : FLUX D'EAU LIQUIDE DE CORR. DES VALEURS < 0.
! PFCQNNG    : FLUX D'EAU NEIGEUSE DE CORR. DES VALEURS < 0.
! PFCQLF     : FLUX DE CONDENSATION CONV. LIQ.
! PFCQIF     : FLUX DE CONDENSATION CONV. NEI.
! PFSQLF     : FLUX DE CONDENSATION STRA. LIQ.
! PFSQIF     : FLUX DE CONDENSATION STRA. NEI.
! PFCQRNG    : FLUX RAIN DE CORR. DES VALEURS < 0.
! PFCQSNG    : FLUX SNOW DE CORR. DES VALEURS < 0.
! PFSQRF     : RAIN FLUX DUE TO SCLOUD
! PFSQSF     : SNOW SLUF DUE TO SCLOUD
! PFSQLTUR   : TURBULENT FLUX OF LIQUID DUE TO VDF
! PFSQITUR   : TURBULENT FLUX OF LIQUID DUE TO VDF
!     --- NEW SURFACE DIAGNOSTICS

! PDHTLS     : Variables and fluxes for individual tiles
! PDHTSS     : Variables and fluxes for snow energy budget
! PDHTTS     : Variables and fluxes for soil energy budget
! PDHTIS     : Variables and fluxes for sea-ice energy budget
! PDHSSS     : Variables and fluxes for snow water budget
! PDHIIS     : Variables and fluxes for interception layer water budget
! PDHWLS     : Variables and fluxes for soil water budget
! PDHFSS     : Free-style variables and fluxes 

!     ARGUMENTS IMPLICITES
!     --------------------
!       UNITE NULOUT (/YOMLUN/)
!       CONSTANTES UNIVERSELLES, COMMON /YOMCST/
!       FONCTIONS THERMODYNAMIQUES, COMDECK *FCTTRM*

!       CONTROLE PHYSIQUE DE BASE : YOMPHY : NDPSFI
!       CONSTANTES PHYSIQUES (NORMALEMENT INUTILES)
!       YOMPHY0, YOMPHY1, YOMPHY2, YOMPHY3

!       ELEMENTS DE CONTROLE DDH
!       INDICATEURS LOGIQUES, COMMON /YOMLDDH/
!        DANS LE COMDECK * YOMMDDH * POUR LES MASQUES
!       DIMENSIONS PROPRES AUX MASQUES DIAGNOSTIQUES DDH, COMMON /DDHDIM/
!       ET DIMENSIONS POUR LES TABLEAUX DE CUMULS
!       INDICATEURS DE POSITIONS DANS LES TABLEAUX DE CUMULS

!     AUTRES ENTREES
!     --------------

!     SORTIES
!     -------
!      DANS PDHCV, FLUX/TENDANCES PHYSIQUES INSTANTANEES
!      DANS PDHCS  VARIABLES ET FLUX AU SOL

!     ORIGINAL
!     --------
!     91-03-04, Alain Joly.

!     MODIFICATIONS
!     -------------
!      N. Wedi and K. Yessad (Jan 2008):different dev for NH model and PC scheme
!      M. Ahlgrimm  31-Oct-2011 add rain, snow and PEXTRA to DDH output
!      K. Yessad (July 2014): Move some variables.
!      K. Yessad (Feb 2018): remove deep-layer formulations.
!     ------------------------------------------------------------------

USE MODEL_DIAGNOSTICS_MOD , ONLY : MODEL_DIAGNOSTICS_TYPE
USE YOMDIM                , ONLY : TDIM
USE SURFACE_FIELDS_MIX    , ONLY : TSURF
USE PARKIND1              , ONLY : JPIM     ,JPRB
USE YOMHOOK               , ONLY : LHOOK    ,DR_HOOK, JPHOOK
USE YOMRIP0               , ONLY : NSSSSS
USE YOMRIP                , ONLY : TRIP
USE YOMPHY                , ONLY : TPHY
USE YOMCST                , ONLY : ROMEGA   ,RG
USE YOMDPHY               , ONLY : TDPHY

!     ------------------------------------------------------------------

IMPLICIT NONE

TYPE(TDIM)        , INTENT(IN)    :: YDDIM
TYPE(TSURF)       , INTENT(INOUT) :: YDSURF
TYPE(TDPHY)        ,INTENT(IN)    :: YDDPHY
TYPE(MODEL_DIAGNOSTICS_TYPE),INTENT(IN):: YDML_DIAG
TYPE(TPHY)         ,INTENT(IN)    :: YDPHY
TYPE(TRIP)         ,INTENT(IN)    :: YDRIP
INTEGER(KIND=JPIM), INTENT(IN)    :: KPROMA 
INTEGER(KIND=JPIM), INTENT(IN)    :: KSTART 
INTEGER(KIND=JPIM), INTENT(IN)    :: KPROF 
INTEGER(KIND=JPIM), INTENT(IN)    :: KFLEV 
REAL(KIND=JPRB)   , INTENT(IN)    :: PNORDL(KPROMA) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PNORDM(KPROMA) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PUZGEO(KPROMA,KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PVMGEO(KPROMA,KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PLON(KPROMA) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PLSM(KPROMA) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PVFCI(KPROMA) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PDIFCQ(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PDIFCS(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PDIFTQ(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PDIFTS(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PFCQNG(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PFPLCL(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PFPLCN(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PFPLSL(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PFPLSN(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PFRSO(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PFRTH(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PFHPCL(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PFHPCN(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PFHPSL(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PFHPSN(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PSTRCU(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PSTRCV(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PSTRDU(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PSTRDV(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PSTRTU(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PSTRTV(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PFCQLNG(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PFCQNNG(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PFSQRF(KPROMA,0:KFLEV)
REAL(KIND=JPRB)   , INTENT(IN)    :: PFSQSF(KPROMA,0:KFLEV)
REAL(KIND=JPRB)   , INTENT(IN)    :: PFCQRNG(KPROMA,0:KFLEV)
REAL(KIND=JPRB)   , INTENT(IN)    :: PFCQSNG(KPROMA,0:KFLEV)
REAL(KIND=JPRB)   , INTENT(IN)    :: PFSQLTUR(KPROMA,0:KFLEV)
REAL(KIND=JPRB)   , INTENT(IN)    :: PFSQITUR(KPROMA,0:KFLEV)
REAL(KIND=JPRB)   , INTENT(IN)    :: PFCQLF(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PFCQIF(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PFSQLF(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PFSQIF(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PDHTLS(KPROMA,YDDPHY%NTILES,YDML_DIAG%YRMDDH%NDHVTLS+YDML_DIAG%YRMDDH%NDHFTLS) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PDHTSS(KPROMA,YDDPHY%NCSNEC,YDML_DIAG%YRMDDH%NDHVTSS+YDML_DIAG%YRMDDH%NDHFTSS) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PDHTTS(KPROMA,YDSURF%YSP_SBD%NLEVS,YDML_DIAG%YRMDDH%NDHVTTS+YDML_DIAG%YRMDDH%NDHFTTS) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PDHTIS(KPROMA,YDDPHY%NCSI,YDML_DIAG%YRMDDH%NDHVTIS+YDML_DIAG%YRMDDH%NDHFTIS) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PDHSSS(KPROMA,YDDPHY%NCSNEC,YDML_DIAG%YRMDDH%NDHVSSS+YDML_DIAG%YRMDDH%NDHFSSS) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PDHIIS(KPROMA,YDML_DIAG%YRMDDH%NDHVIIS+YDML_DIAG%YRMDDH%NDHFIIS) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PDHWLS(KPROMA,YDSURF%YSP_SBD%NLEVS,YDML_DIAG%YRMDDH%NDHVWLS+YDML_DIAG%YRMDDH%NDHFWLS) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PDHFSS(KPROMA,YDML_DIAG%YRMDDH%NDHVFS+YDML_DIAG%YRMDDH%NDHFFS) 
REAL(KIND=JPRB)   , INTENT(OUT)   :: PDHCV(KPROMA,0:KFLEV,YDML_DIAG%YRMDDH%NDHCVSU) 
REAL(KIND=JPRB)   , INTENT(OUT)   :: PDHCS(KPROMA,YDML_DIAG%YRMDDH%NDHCSSU) 
!     ------------------------------------------------------------------
REAL(KIND=JPRB) :: ZCLON(KPROMA), ZSLON(KPROMA)

!     FLUX DE QTE DE MVT TURBULENTS DANS LE REPERE LOCAL GEOGRAPHIQUE
REAL(KIND=JPRB) :: ZSTTUG(KPROMA,2), ZSTTVG(KPROMA,2)
!     FLUX DE QTE DE MVT CONVECTIFS DANS LE REPERE LOCAL GEOGRAPHIQUE
REAL(KIND=JPRB) :: ZSTCUG(KPROMA,2), ZSTCVG(KPROMA,2)
!     FLUX DE QTE DE MVT ONDES OROG.DANS LE REPERE LOCAL GEOGRAPHIQUE
REAL(KIND=JPRB) :: ZSTDUG(KPROMA,2), ZSTDVG(KPROMA,2)

REAL(KIND=JPRB) :: ZLSM(KPROMA) 

INTEGER(KIND=JPIM) :: IDHCS, IDHCV, ILEN, JLEV, JROF, JVAR, JTILE, JFLX
INTEGER(KIND=JPIM) :: ICURLEV, ILEVM1
REAL(KIND=JPRB) :: ZDISKC, ZDISKD, ZDISKT, ZDPSFI, ZFPLL, ZFPLN, ZFPL, ZUSRG  
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!     ------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('CPPHDDHE',0,ZHOOK_HANDLE)
ASSOCIATE(NDPSFI=>YDPHY%NDPSFI, &
 & NCSI=>YDDPHY%NCSI, NCSNEC=>YDDPHY%NCSNEC, NTILES=>YDDPHY%NTILES, &
 & LHDHKS=>YDML_DIAG%YRLDDH%LHDHKS, LHDMCI=>YDML_DIAG%YRLDDH%LHDMCI, &
 & NDHAVP=>YDML_DIAG%YRMDDH%NDHAVP, NDHCSSU=>YDML_DIAG%YRMDDH%NDHCSSU, NDHCVSU=>YDML_DIAG%YRMDDH%NDHCVSU, &
 & NDHFFS=>YDML_DIAG%YRMDDH%NDHFFS, NDHFIIS=>YDML_DIAG%YRMDDH%NDHFIIS, NDHFSD=>YDML_DIAG%YRMDDH%NDHFSD, &
 & NDHFSP=>YDML_DIAG%YRMDDH%NDHFSP, NDHFSSS=>YDML_DIAG%YRMDDH%NDHFSSS, NDHFTIS=>YDML_DIAG%YRMDDH%NDHFTIS, &
 & NDHFTLS=>YDML_DIAG%YRMDDH%NDHFTLS, NDHFTSS=>YDML_DIAG%YRMDDH%NDHFTSS, NDHFTTS=>YDML_DIAG%YRMDDH%NDHFTTS, &
 & NDHFWLS=>YDML_DIAG%YRMDDH%NDHFWLS, NDHVFS=>YDML_DIAG%YRMDDH%NDHVFS, NDHVIIS=>YDML_DIAG%YRMDDH%NDHVIIS, &
 & NDHVS=>YDML_DIAG%YRMDDH%NDHVS, NDHVSSS=>YDML_DIAG%YRMDDH%NDHVSSS, NDHVTIS=>YDML_DIAG%YRMDDH%NDHVTIS, &
 & NDHVTLS=>YDML_DIAG%YRMDDH%NDHVTLS, NDHVTSS=>YDML_DIAG%YRMDDH%NDHVTSS, NDHVTTS=>YDML_DIAG%YRMDDH%NDHVTTS, &
 & NDHVWLS=>YDML_DIAG%YRMDDH%NDHVWLS, &
 & RSTATI=>YDRIP%RSTATI, &
 & LHDQLN=>YDML_DIAG%YRSDDH%LHDQLN, &
 & YSP_SBD=>YDSURF%YSP_SBD)
!     ------------------------------------------------------------------
!**   1.- INITIALISATIONS DIVERSES

ZUSRG = 1.0_JPRB/RG
ZDPSFI = REAL(NDPSFI,JPRB)
ILEN=KPROF-KSTART+1

IF ( LHDMCI ) THEN
  DO JROF=KSTART,KPROF
    ZCLON(JROF) = COS(PLON(JROF)+ROMEGA*(RSTATI-NSSSSS))
    ZSLON(JROF) = SIN(PLON(JROF)+ROMEGA*(RSTATI-NSSSSS))
  ENDDO
ENDIF

ICURLEV = 0
DO JLEV = 0, KFLEV
  IDHCV = 0
  ICURLEV = MOD(ICURLEV,2)+1
  ILEVM1  = MOD(ICURLEV,2)+1
  DO JROF=KSTART,KPROF
    ZSTTUG(JROF,ICURLEV) = PNORDM(JROF)*PSTRTU(JROF,JLEV)-&
     & PNORDL(JROF)*PSTRTV(JROF,JLEV)  
    ZSTTVG(JROF,ICURLEV) = PNORDL(JROF)*PSTRTU(JROF,JLEV)+&
     & PNORDM(JROF)*PSTRTV(JROF,JLEV)  
    ZSTCUG(JROF,ICURLEV) = PNORDM(JROF)*PSTRCU(JROF,JLEV)-&
     & PNORDL(JROF)*PSTRCV(JROF,JLEV)  
    ZSTCVG(JROF,ICURLEV) = PNORDL(JROF)*PSTRCU(JROF,JLEV)+&
     & PNORDM(JROF)*PSTRCV(JROF,JLEV)  
    ZSTDUG(JROF,ICURLEV) = PNORDM(JROF)*PSTRDU(JROF,JLEV)-&
     & PNORDL(JROF)*PSTRDV(JROF,JLEV)  
    ZSTDVG(JROF,ICURLEV) = PNORDL(JROF)*PSTRDU(JROF,JLEV)+&
     & PNORDM(JROF)*PSTRDV(JROF,JLEV)
    IF ( LHDHKS ) THEN
       ! Assignments moved here to avoid unnecessary loads
       PDHCV(JROF,JLEV,IDHCV+9)  = ZSTTUG(JROF,ICURLEV)
       PDHCV(JROF,JLEV,IDHCV+10) = ZSTTVG(JROF,ICURLEV)
       !     FUUTURCONV
       !     FVVTURCONV
       PDHCV(JROF,JLEV,IDHCV+11) = ZSTCUG(JROF,ICURLEV)
       PDHCV(JROF,JLEV,IDHCV+12) = ZSTCVG(JROF,ICURLEV)
       !     FUUONDEGREL
       !     FVVONDEGREL
       PDHCV(JROF,JLEV,IDHCV+13) = ZSTDUG(JROF,ICURLEV)
       PDHCV(JROF,JLEV,IDHCV+14) = ZSTDVG(JROF,ICURLEV)
    END IF
  ENDDO

  !     ------------------------------------------------------------------
!**   3.- CALCUL DES TENDANCES PHYSIQUES

  IDHCV = NDHAVP

!*         - DISSIPATION D'ENERGIE CINETIQUE.
  ! Loop moved to better stay in L1 cache for ZSTTVG(:,ICURLEV)
  IF ( LHDHKS .AND. JLEV >0) THEN
    DO JROF=KSTART,KPROF
!     TKKDISSIPTUR
      ZDISKT = -( PUZGEO(JROF,JLEV)*&
       & (ZSTTUG(JROF,ICURLEV)-ZSTTUG(JROF,ILEVM1)) + PVMGEO(JROF,JLEV)&
       & *(ZSTTVG(JROF,ICURLEV)-ZSTTVG(JROF,ILEVM1)) )  
      PDHCV(JROF,JLEV,IDHCV+1) = ZDISKT
!     TKKDISSIPCONV
      ZDISKC = -( PUZGEO(JROF,JLEV)*&
       & (ZSTCUG(JROF,ICURLEV)-ZSTCUG(JROF,ILEVM1)) + PVMGEO(JROF,JLEV)&
       & *(ZSTCVG(JROF,ICURLEV)-ZSTCVG(JROF,ILEVM1)) )  
      PDHCV(JROF,JLEV,IDHCV+2) = ZDISKC
!     TKKDISSIPGREL
      ZDISKD = -( PUZGEO(JROF,JLEV)*&
       & (ZSTDUG(JROF,ICURLEV)-ZSTDUG(JROF,ILEVM1)) + PVMGEO(JROF,JLEV)&
       & *(ZSTDVG(JROF,ICURLEV)-ZSTDVG(JROF,ILEVM1)) )  
      PDHCV(JROF,JLEV,IDHCV+3) = ZDISKD
    ENDDO
  ENDIF
ENDDO  

!     ------------------------------------------------------------------
!**   2.- CALCUL DES FLUX PHYSIQUES
! indices for physical fluxes start counting at ndhvv+ndhfvd
!    IDHCV=NDHVV+NDHFVD
  IDHCV = 0
  IF ( LHDHKS ) THEN
     DO JLEV = 0, KFLEV
        DO JROF=KSTART,KPROF
           !     FPPSUMFPL
           ZFPLL = PFPLCL(JROF,JLEV) + PFPLSL(JROF,JLEV)
           ZFPLN = PFPLCN(JROF,JLEV) + PFPLSN(JROF,JLEV)
           ZFPL  = ZFPLL  + ZFPLN
           PDHCV(JROF,JLEV,IDHCV+1)  = ZDPSFI*ZFPL
           !     FQVPRECISTL
           PDHCV(JROF,JLEV,IDHCV+4)  = PFPLSL(JROF,JLEV)
           !     FQVPRECISTN
           PDHCV(JROF,JLEV,IDHCV+5)  = PFPLSN(JROF,JLEV)
           !     FQVPRECICOL
           PDHCV(JROF,JLEV,IDHCV+6)  = PFPLCL(JROF,JLEV)
           !     FQVPRECICON
           PDHCV(JROF,JLEV,IDHCV+7)  = PFPLCN(JROF,JLEV)
        ENDDO
     ENDDO

     DO JLEV = 0, KFLEV
        DO JROF=KSTART,KPROF
           !     FQVTUR
           PDHCV(JROF,JLEV,IDHCV+2)  = PDIFTQ(JROF,JLEV)
        ENDDO
     ENDDO
     DO JLEV = 0, KFLEV
        DO JROF=KSTART,KPROF
           !     FQVTURCONV
           PDHCV(JROF,JLEV,IDHCV+3)  = PDIFCQ(JROF,JLEV)
        ENDDO
     ENDDO
     DO JLEV = 0, KFLEV
        DO JROF=KSTART,KPROF
           !     FQVTURQNEGAT
           PDHCV(JROF,JLEV,IDHCV+8)  = PFCQNG(JROF,JLEV)
        ENDDO
     ENDDO
     !     FUUTUR
     ! Assingments moved to previous loop to avoid reloading the entries
     !     FVVTUR
     ! PDHCV(JROF,JLEV,IDHCV+9)  = ZSTTUG(JROF,ICURLEV)
     ! PDHCV(JROF,JLEV,IDHCV+10) = ZSTTVG(JROF,ICURLEV)
     !     FUUTURCONV
     !     FVVTURCONV
     ! PDHCV(JROF,JLEV,IDHCV+11) = ZSTCUG(JROF,ICURLEV)
     ! PDHCV(JROF,JLEV,IDHCV+12) = ZSTCVG(JROF,ICURLEV)
     !     FUUONDEGREL
     !     FVVONDEGREL
     ! PDHCV(JROF,JLEV,IDHCV+13) = ZSTDUG(JROF,ICURLEV)
     ! PDHCV(JROF,JLEV,IDHCV+14) = ZSTDVG(JROF,ICURLEV)
     DO JLEV = 0, KFLEV
