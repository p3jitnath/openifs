SUBROUTINE PPFIDHEC(YDDIMV,YDSURF,YDDPHY,YDML_DIAG,YDPHY,PDHNOM,PDDHCV,PDDHCS,KNUM,PDDHCV_TOT,PDDHCS_TOT,KDIMDOM)
!-----------------------------------------------------------------------
!**** PPFIDH * ECRITURE DES RESULTATS POUR UN DOMAINE SUR UN FICHIER
!              DIAGNOSTICS PAR DOMAINES HORIZONTAUX
!              Write results for one domain on a DDH file.
!-----------------------------------------------------------------------
!     BUT
!     ---
!      TRANSFERT DES RESULTATS OBTENUS POUR UN DOMAINE IDENTIFIE
!     PAR PDHNOM SUR UN FICHIER LFA

!     ARGUMENTS D'ENTREE
!     ------------------
!      PDHNOM(11) : IDENTIFICATION DU DOMAINE
!                  PDHNOM(11) EST LE TYPE DE DOMAINE
!      PDDHCV(0:NFLEVG,NDHCV+NDHVV) : PROFILS VERTICAUX
!                  LA STRUCTURE DU TABLEAU EST RESUMEE DANS
!                  PPSYDH, SINON VOIR YOMTDDH ET CPDYDDH/CPPHDDH
!      PDDHCS(NDHCV+NDHVV) : SOL
!      KNUM : NUMERO DU DOMAINE DANS UNE SERIE
!             (PAR EXEMPLE ENSEMBLE DES BANDES DE LATITUDE)
!      KDIMDOM : NOMBRE DE DOMAINES

!     ARGUMENTS DE SORTIE
!     -------------------
!     FICHIER LFA

!     ORIGINAL
!     --------
!     91-02-01, Alain Joly.

!     MODIFICATIONS
!     -------------
!      M.Hamrud      01-Oct-2003 CY28 Cleaning
!      05-06-20, F. Bouyssel: Lopez's prognostic scheme
!      2005-04-16, T. Kovacic: AROME DDH.
!      06-01-25, F. Bouyssel: Change name of Lopez's water fluxes
!      2006-08-22, J.M. Piriou: write DDH on LFA files.
!      01-oct-08 O.Riviere introduction of new ddh dataflow in Arome; removal of old one
!      K. Yessad (Dec 2008): cleanings, remove tabulations.
!      2008-01-05, J.M. Piriou: cleaning: suppress use of NPHY, correct LHDQLN bug.
!      2011-05-19, R. Brozkova: temporary fix of ALARO ddh.
!      2011-10-31  M. Ahlgrimm: add rain, snow and PEXTRA to DDH output
!      K. Yessad (July 2014): Move some variables.
!      O. Marsden: June 2015 CY42 YRGMV, YRGFL, YRSURF, YRGMV5, and YRGFL5 are now passed by argument
!-----------------------------------------------------------------------

USE MODEL_DIAGNOSTICS_MOD , ONLY : MODEL_DIAGNOSTICS_TYPE
USE YOMPHY                , ONLY : TPHY, JPBP002
USE YOMDIMV               , ONLY : TDIMV
USE SURFACE_FIELDS_MIX    , ONLY : TSURF
USE PARKIND1              , ONLY : JPIM     ,JPRB
USE YOMHOOK               , ONLY : LHOOK    ,DR_HOOK, JPHOOK
USE YOMLUN                , ONLY : NULOUT   ,NPODDH
USE YOMMP0                , ONLY : NPRINTLEV
USE YOMCT0                , ONLY : LECMWF
USE YOMDPHY               , ONLY : TDPHY

USE DDH_MIX, ONLY: NTOTFIELD,RDDH_DESCR,NTOTSURF,RDDHSURF_DESCR,&
& NTOTSVAR, NTOTSVFS
USE YOMPHFT,  ONLY  : NDDHFT, YAPFT

!-----------------------------------------------------------------------

IMPLICIT NONE

TYPE(TDIMV)                 , INTENT(IN)    :: YDDIMV
TYPE(TSURF)                 , INTENT(INOUT) :: YDSURF
TYPE(TDPHY)                 , INTENT(IN)    :: YDDPHY
TYPE(MODEL_DIAGNOSTICS_TYPE), INTENT(INOUT) :: YDML_DIAG
TYPE(TPHY)                  , INTENT(INOUT) :: YDPHY
INTEGER(KIND=JPIM)          , INTENT(IN)    :: KNUM, KDIMDOM
REAL(KIND=JPRB)             , INTENT(IN)    :: PDHNOM(11)
REAL(KIND=JPRB)             , INTENT(IN)    :: PDDHCV(0:YDDIMV%NFLEVG,YDML_DIAG%YRMDDH%NDHCVSU+YDML_DIAG%YRMDDH%NDHVV)
REAL(KIND=JPRB)             , INTENT(IN)    :: PDDHCS(YDML_DIAG%YRMDDH%NDHCSSU+YDML_DIAG%YRMDDH%NDHVS)
REAL(KIND=JPRB)             , INTENT(IN)    :: PDDHCV_TOT((YDDIMV%NFLEVG+1)*KDIMDOM*(YDML_DIAG%YRMDDH%NDHCVSU+&
 &                                                        YDML_DIAG%YRMDDH%NDHVV))
REAL(KIND=JPRB)             , INTENT(IN)    :: PDDHCS_TOT(KDIMDOM*(YDML_DIAG%YRMDDH%NDHCSSU+YDML_DIAG%YRMDDH%NDHVS))
!-----------------------------------------------------------------------
INTEGER(KIND=JPIM) :: JVAR, JFLX, JFIELD
REAL(KIND=JPRB) :: ZEROV(YDDIMV%NFLEVG)
REAL(KIND=JPRB) :: ZEROF(0:YDDIMV%NFLEVG)
REAL(KIND=JPRB) :: ZMAR0 , ZMAR1
CHARACTER :: CLNOMA*16
CHARACTER :: CL0*10, CL1*10, CL2*2, CLDIVOR*10, CLFLUVD*10
CHARACTER :: CLFLUPL*10
INTEGER(KIND=JPIM) :: IDHCS, IDHCV, INBCUMS, JJPASS, JLEV, IPOINT,IVAR, JI
INTEGER(KIND=JPIM) :: IPLFIS, IPLVAS, IPLVFS, IVARS
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!-----------------------------------------------------------------------

#include "addpgr.intfb.h"

!-----------------------------------------------------------------------
IF (LHOOK) CALL DR_HOOK('PPFIDHEC',0,ZHOOK_HANDLE)
ASSOCIATE(YDCDDH=>YDML_DIAG%YRCDDH, YDLDDH=>YDML_DIAG%YRLDDH, &
 & YDMDDH=>YDML_DIAG%YRMDDH, YDSDDH=>YDML_DIAG%YRSDDH)
ASSOCIATE(NFLEVG=>YDDIMV%NFLEVG, &
 & NCSI=>YDDPHY%NCSI, NCSNEC=>YDDPHY%NCSNEC, NTILES=>YDDPHY%NTILES, &
 & LFLEXDIA=>YDLDDH%LFLEXDIA, LHDENT=>YDLDDH%LHDENT, LHDHKS=>YDLDDH%LHDHKS, &
 & LHDLFA=>YDLDDH%LHDLFA, LHDLIST=>YDLDDH%LHDLIST, LHDMCI=>YDLDDH%LHDMCI, &
 & LONLYVAR=>YDML_DIAG%YRLDDH%LONLYVAR, &
 & NDHAHKD=>YDMDDH%NDHAHKD, NDHAHKP=>YDMDDH%NDHAHKP, NDHAMCD=>YDMDDH%NDHAMCD, &
 & NDHAVD=>YDMDDH%NDHAVD, NDHAVP=>YDMDDH%NDHAVP, NDHBHKD=>YDMDDH%NDHBHKD, &
 & NDHBHKP=>YDMDDH%NDHBHKP, NDHBMCD=>YDMDDH%NDHBMCD, NDHBMCP=>YDMDDH%NDHBMCP, &
 & NDHCS=>YDMDDH%NDHCS, NDHCSSU=>YDMDDH%NDHCSSU, NDHCV=>YDMDDH%NDHCV, &
 & NDHCVSU=>YDMDDH%NDHCVSU, NDHFFS=>YDMDDH%NDHFFS, NDHFIIS=>YDMDDH%NDHFIIS, &
 & NDHFSD=>YDMDDH%NDHFSD, NDHFSP=>YDMDDH%NDHFSP, NDHFSSS=>YDMDDH%NDHFSSS, &
 & NDHFTIS=>YDMDDH%NDHFTIS, NDHFTLS=>YDMDDH%NDHFTLS, NDHFTSS=>YDMDDH%NDHFTSS, &
 & NDHFTTS=>YDMDDH%NDHFTTS, NDHFVD=>YDMDDH%NDHFVD, NDHFWLS=>YDMDDH%NDHFWLS, &
 & NDHVFS=>YDMDDH%NDHVFS, NDHVHK=>YDMDDH%NDHVHK, NDHVIIS=>YDMDDH%NDHVIIS, &
 & NDHVMC=>YDMDDH%NDHVMC, NDHVS=>YDMDDH%NDHVS, NDHVSSS=>YDMDDH%NDHVSSS, &
 & NDHVTIS=>YDMDDH%NDHVTIS, NDHVTLS=>YDMDDH%NDHVTLS, NDHVTSS=>YDMDDH%NDHVTSS, &
 & NDHVTTS=>YDMDDH%NDHVTTS, NDHVV=>YDMDDH%NDHVV, NDHVWLS=>YDMDDH%NDHVWLS, &
 & LHDPAS=>YDSDDH%LHDPAS, LHDQLN=>YDSDDH%LHDQLN, NHDPASVA=>YDSDDH%NHDPASVA, &
 & NHDQLNVA=>YDSDDH%NHDQLNVA, &
 & YSP_SBD=>YDSURF%YSP_SBD, &
 & L3MT=>YDPHY%L3MT, LCONDWT=>YDPHY%LCONDWT, LPROCLD=>YDPHY%LPROCLD, &
 & LSTRAPRO=>YDPHY%LSTRAPRO, NPHY=>YDPHY%NPHY)
!-----------------------------------------------------------------------

CLNOMA = ' '
CL0     = '0         '
CL1     = '1         '
CLDIVOR = 'DIVFLUHOR '
CLFLUVD = 'FLUVERTDYN'
CLFLUPL = 'FLUDUAPLUI'

DO JLEV=1,NFLEVG
  ZEROV(JLEV)=0.0_JPRB
ENDDO
DO JLEV=0,NFLEVG
  ZEROF(JLEV)=0.0_JPRB
ENDDO

ZMAR0=0._JPRB
DO JLEV=0, NFLEVG
  ZMAR0=ZMAR0+ PDDHCV(JLEV,1)
ENDDO

ZMAR1=0._JPRB
DO JLEV=0, NFLEVG
  ZMAR1=ZMAR1+ PDDHCV(JLEV,NDHCV+1)
ENDDO


!     ------------------------------------------------------------------
!*    1.- ARTICLE DOCUMENTAIRE
IF (NPRINTLEV >= 1) THEN
  WRITE (NULOUT,*) ' DDH * PPFIDH KNUM = ',KNUM,' NPODDH = ',NPODDH
ENDIF

IF(LHDLFA) THEN
  ! ON ECRIT SUR FICHIER LFA UN ARTICLE DOCD PAR DOMAINE.
  CALL LFAECRR(NPODDH,CLNOMA,PDHNOM,11)
ELSE
  CALL ADDPGR('DOCD',KNUM,PDHNOM,11)
ENDIF
! ON N'ECRIT SUR FICHIER LFA QU'UN ARTICLE SCIENTIFIQUE
! POUR TOUS LES DOMAINES. ON N'ECRIT DONC CI-DESSOUS
! L'ARTICLE SCIENTIFIQUE QUE SI LE DOMAINE COURANT EST LE DERNIER.

IF(.NOT.LHDLFA .OR. KNUM == KDIMDOM) THEN 

  IF (LFLEXDIA) THEN

    IVAR=0
    DO JFIELD=1,NTOTFIELD
      IPOINT=JFIELD
      SELECT CASE(RDDH_DESCR(JFIELD)%CFLUX)
      CASE('V')
       CALL ADDPGR(RDDH_DESCR(JFIELD)%CNAME//CL1    ,KNUM,PDDHCV(1,IPOINT),-999)
       
        IVAR=IVAR+1
        IPOINT=IVAR+NTOTFIELD
       CALL ADDPGR(RDDH_DESCR(JFIELD)%CNAME//CL0 ,KNUM,PDDHCV(1,IPOINT),-999)

      CASE('T')
       CALL ADDPGR(RDDH_DESCR(JFIELD)%CNAME//' '    ,KNUM,PDDHCV(1,IPOINT),-999)

      CASE('F')
       CALL ADDPGR(RDDH_DESCR(JFIELD)%CNAME//' '     ,KNUM,PDDHCV(1,IPOINT),1+NFLEVG)

      END SELECT
    ENDDO

  ELSE !LFLEXDIA

    !     ------------------------------------------------------------------
    !*    2.- ARTICLES DE VARIABLES

    IDHCV=0

    !     INSTANT INITIAL
    !     ---------------
    IF ( LHDHKS ) THEN
      IDHCV=0
      IPOINT=IDHCV+1
       CALL ADDPGR('VPP'//CL0,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+2
       CALL ADDPGR('VQV'//CL0,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+3
       CALL ADDPGR('VUU'//CL0,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+4
       CALL ADDPGR('VVV'//CL0,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+5
       CALL ADDPGR('VKK'//CL0,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+6
       CALL ADDPGR('VCT'//CL0,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+7
       CALL ADDPGR('VEP'//CL0,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+8
       CALL ADDPGR('VHR'//CL0,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IF(LHDQLN) THEN
        IPOINT=IDHCV+9
       CALL ADDPGR('VQL'//CL0,KNUM,PDDHCV(1,IPOINT),NFLEVG)

        IPOINT=IDHCV+10
       CALL ADDPGR('VQN'//CL0,KNUM,PDDHCV(1,IPOINT),NFLEVG)
        IPOINT=IDHCV+11
       CALL ADDPGR('VQR'//CL0,KNUM,PDDHCV(1,IPOINT),NFLEVG)
        
        IPOINT=IDHCV+12
       CALL ADDPGR('VQS'//CL0,KNUM,PDDHCV(1,IPOINT),NFLEVG)
      ENDIF

      IF(LHDPAS) THEN
        DO JJPASS=1,NHDPASVA
          WRITE(CL2,'(I2.2)') JJPASS
          IPOINT=IDHCV+9+NHDQLNVA+JJPASS-1
       CALL ADDPGR('V'//CL2//CL0,KNUM,PDDHCV(1,IPOINT),NFLEVG)
        ENDDO
      ENDIF

      IPOINT=IDHCV+9+NHDQLNVA+NHDPASVA
       CALL ADDPGR('VNT'//CL0,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+10+NHDQLNVA+NHDPASVA
       CALL ADDPGR('VOM'//CL0,KNUM,PDDHCV(1,IPOINT),NFLEVG)

    ENDIF

    IF ( LHDMCI ) THEN
      IDHCV=NDHVHK
      IPOINT=IDHCV+1
       CALL ADDPGR('VA1'//CL0,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+2
       CALL ADDPGR('VA2'//CL0,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+3
       CALL ADDPGR('VA3'//CL0,KNUM,PDDHCV(1,IPOINT),NFLEVG)
    ENDIF

    IF ( LHDENT ) THEN
      IDHCV=NDHVHK+NDHVMC
      IPOINT=IDHCV+1
       CALL ADDPGR('VSS'//CL0,KNUM,PDDHCV(1,IPOINT),NFLEVG)
    ENDIF

    !     INSTANT FINAL
    !     -------------
    IF ( LHDHKS ) THEN

      IDHCV=NDHCV
      IPOINT=IDHCV+1
       CALL ADDPGR('VPP'//CL1,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+2
       CALL ADDPGR('VQV'//CL1,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+3
       CALL ADDPGR('VUU'//CL1,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+4
       CALL ADDPGR('VVV'//CL1,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+5
       CALL ADDPGR('VKK'//CL1,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+6
       CALL ADDPGR('VCT'//CL1,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+7
       CALL ADDPGR('VEP'//CL1,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+8
       CALL ADDPGR('VHR'//CL1,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IF(LHDQLN) THEN
        IPOINT=IDHCV+9
       CALL ADDPGR('VQL'//CL1,KNUM,PDDHCV(1,IPOINT),NFLEVG)

        IPOINT=IDHCV+10
       CALL ADDPGR('VQN'//CL1,KNUM,PDDHCV(1,IPOINT),NFLEVG)
        IPOINT=IDHCV+11
       CALL ADDPGR('VQR'//CL1,KNUM,PDDHCV(1,IPOINT),NFLEVG)
        
        IPOINT=IDHCV+12
       CALL ADDPGR('VQS'//CL1,KNUM,PDDHCV(1,IPOINT),NFLEVG)
      ENDIF

      IF(LHDPAS) THEN
        DO JJPASS=1,NHDPASVA
          WRITE(CL2,'(I2.2)') JJPASS
          IPOINT=IDHCV+9+NHDQLNVA+JJPASS-1
       CALL ADDPGR('V'//CL2//CL1,KNUM,PDDHCV(1,IPOINT),NFLEVG)
        ENDDO
      ENDIF

      IPOINT=IDHCV+9+NHDQLNVA+NHDPASVA
       CALL ADDPGR('VNT'//CL1,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+10+NHDQLNVA+NHDPASVA
       CALL ADDPGR('VOM'//CL1,KNUM,PDDHCV(1,IPOINT),NFLEVG)

    ENDIF

    IF ( LHDMCI ) THEN
      IDHCV=NDHCV+NDHVHK
      IPOINT=IDHCV+1
       CALL ADDPGR('VA1'//CL1,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+2
       CALL ADDPGR('VA2'//CL1,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+3
       CALL ADDPGR('VA3'//CL1,KNUM,PDDHCV(1,IPOINT),NFLEVG)
    ENDIF

    IF ( LHDENT ) THEN
      IDHCV=NDHCV+NDHVHK+NDHVMC
      IPOINT=IDHCV+1
       CALL ADDPGR('VSS'//CL1,KNUM,PDDHCV(1,IPOINT),NFLEVG)
    ENDIF

    !     ------------------------------------------------------------------
    !*    3.- TENDANCES ET FLUX DYNAMIQUES

    IF ( LHDHKS ) THEN
      IDHCV=NDHVV
      IPOINT=IDHCV+1
       CALL ADDPGR('TPP'//CLDIVOR,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+2
       CALL ADDPGR('TQV'//CLDIVOR,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+3
       CALL ADDPGR('TUU'//CLDIVOR,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+4
       CALL ADDPGR('TVV'//CLDIVOR,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+5
       CALL ADDPGR('TUUFFVGADPSG ',KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+6
       CALL ADDPGR('TVVFFUGADPSG ',KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+7
       CALL ADDPGR('TKK'//CLDIVOR,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+8
       CALL ADDPGR('TKKCONVERSI1 ',KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+9
       CALL ADDPGR('TCT'//CLDIVOR,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+10
       CALL ADDPGR('TCTCONVERSI2 ',KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+11
       CALL ADDPGR('TCTCONVERSI3 ',KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+12
       CALL ADDPGR('TEP'//CLDIVOR,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+13
       CALL ADDPGR('PPP          ',KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IF(LHDQLN) THEN
        IPOINT=IDHCV+14
       CALL ADDPGR('TQL'//CLDIVOR,KNUM,PDDHCV(1,IPOINT),NFLEVG)

        IPOINT=IDHCV+15
       CALL ADDPGR('TQN'//CLDIVOR,KNUM,PDDHCV(1,IPOINT),NFLEVG)
        IPOINT=IDHCV+16
       CALL ADDPGR('TQR'//CLDIVOR,KNUM,PDDHCV(1,IPOINT),NFLEVG)
 
        IPOINT=IDHCV+17
       CALL ADDPGR('TQS'//CLDIVOR,KNUM,PDDHCV(1,IPOINT),NFLEVG)
      ENDIF
    ENDIF

    IF ( LHDMCI ) THEN
      IDHCV=NDHVV+NDHAHKD
      IPOINT=IDHCV+1
       CALL ADDPGR('TA1'//CLDIVOR,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+2
       CALL ADDPGR('TA2'//CLDIVOR,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+3
       CALL ADDPGR('TA3'//CLDIVOR,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+4
       CALL ADDPGR('TA1ADJUST    ',KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+5
       CALL ADDPGR('TA2ADJUST    ',KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+6
       CALL ADDPGR('TA3ADJUST    ',KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+7
       CALL ADDPGR('TA1NONAX     ',KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+8
       CALL ADDPGR('TA2NONAX     ',KNUM,PDDHCV(1,IPOINT),NFLEVG)
    ENDIF

    IF ( LHDENT ) THEN
      IDHCV=NDHVV+NDHAHKD+NDHAMCD
      IPOINT=IDHCV+1
       CALL ADDPGR('TSS'//CLDIVOR,KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+2
       CALL ADDPGR('TSSPRECICT   ',KNUM,PDDHCV(1,IPOINT),NFLEVG)
    ENDIF

    IF ( LHDHKS ) THEN
      IDHCV=NDHVV+NDHAVD
      IPOINT=IDHCV+1
       CALL ADDPGR('FPP'//CLFLUVD,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+2
       CALL ADDPGR('FQV'//CLFLUVD,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+3
       CALL ADDPGR('FUU'//CLFLUVD,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+4
       CALL ADDPGR('FVV'//CLFLUVD,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+5
       CALL ADDPGR('FUU'//CLFLUPL,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+6
       CALL ADDPGR('FVV'//CLFLUPL,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+7
       CALL ADDPGR('FKK'//CLFLUVD,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+8
       CALL ADDPGR('FKK'//CLFLUPL,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+9
       CALL ADDPGR('FCT'//CLFLUVD,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+10
       CALL ADDPGR('FEPCONVERSIFL',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IF(LHDQLN) THEN
        IPOINT=IDHCV+11
       CALL ADDPGR('FQL'//CLFLUVD,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

        IPOINT=IDHCV+12
       CALL ADDPGR('FQN'//CLFLUVD,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)
        IPOINT=IDHCV+13
       CALL ADDPGR('FQR'//CLFLUVD,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)
 
        IPOINT=IDHCV+14
       CALL ADDPGR('FQS'//CLFLUVD,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)
      ENDIF
    ENDIF

    IF ( LHDMCI ) THEN
      IDHCV=NDHVV+NDHAVD+NDHBHKD
      IPOINT=IDHCV+1
       CALL ADDPGR('FA1'//CLFLUVD,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+2
       CALL ADDPGR('FA2'//CLFLUVD,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+3
       CALL ADDPGR('FA3'//CLFLUVD,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+4
       CALL ADDPGR('FA1GRAV      ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+5
       CALL ADDPGR('FA2GRAV      ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+6
       CALL ADDPGR('FA3GRAV      ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+7
       CALL ADDPGR('FA1'//CLFLUPL,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+8
       CALL ADDPGR('FA2'//CLFLUPL,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+9
       CALL ADDPGR('FA3'//CLFLUPL,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)
    ENDIF

    IF ( LHDENT ) THEN
      IDHCV=NDHVV+NDHAVD+NDHBHKD+NDHBMCD
      IPOINT=IDHCV+1
       CALL ADDPGR('FSS'//CLFLUVD,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+1
       CALL ADDPGR('FSSPRECISS   ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)
    ENDIF

    !     ------------------------------------------------------------------
    !*    4.- FLUX ET TENDANCES PHYSIQUES
    
    IF ( L3MT.OR.LSTRAPRO) NPHY=JPBP002

    IF ( LHDHKS ) THEN
      IDHCV=NDHVV+NDHFVD

      SELECT CASE (NPHY)
      CASE( JPBP002 )
      IF(.NOT.LONLYVAR) THEN
        DO JI= 1, NDDHFT 
          IPOINT=IDHCV+JI
       CALL ADDPGR(YAPFT(JI)%CFT//YAPFT(JI)%CVAR//YAPFT(JI)%CNAME,KNUM,PDDHCV(0,IPOINT),NFLEVG+1)
        ENDDO
      ENDIF
      CASE DEFAULT

      IPOINT=IDHCV+1
       CALL ADDPGR('FPPSUMFPL    ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+2
       CALL ADDPGR('FQVTUR       ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+3
       CALL ADDPGR('FQVTURCONV   ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+4
       CALL ADDPGR('FQTPRECISTL  ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+5
       CALL ADDPGR('FQTPRECISTN  ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+6
       CALL ADDPGR('FQTPRECICOL  ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+7
       CALL ADDPGR('FQTPRECICON  ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+8
       CALL ADDPGR('FQVTURQNEGAT ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+9
       CALL ADDPGR('FUUTUR       ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+10
       CALL ADDPGR('FVVTUR       ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+11
       CALL ADDPGR('FUUTURCONV   ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+12
       CALL ADDPGR('FVVTURCONV   ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+13
       CALL ADDPGR('FUUONDEGREL  ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+14
       CALL ADDPGR('FVVONDEGREL  ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+15
       CALL ADDPGR('FCTTUR       ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+16
       CALL ADDPGR('FCTTURCONV   ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+17
       CALL ADDPGR('FCTRAYSOL1   ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+18
       CALL ADDPGR('FCTRAYTER1   ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+19
       CALL ADDPGR('FCTPRECISTL  ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+20
       CALL ADDPGR('FCTPRECISTN  ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+21
       CALL ADDPGR('FCTPRECICOL  ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+22
       CALL ADDPGR('FCTPRECICON  ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+23
       CALL ADDPGR('FCTPRECCSCOL ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+24
       CALL ADDPGR('FCTPRECCSCON ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+25
       CALL ADDPGR('FCTPRECCSSTL ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+26
       CALL ADDPGR('FCTPRECCSSTN ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IF(LHDQLN) THEN

        IPOINT=IDHCV+27
       CALL ADDPGR('FQLTUR       ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

        IPOINT=IDHCV+28
       CALL ADDPGR('FQNTUR       ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

        IPOINT=IDHCV+29
       CALL ADDPGR('FQLTURCONV   ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

        IPOINT=IDHCV+30
       CALL ADDPGR('FQNTURCONV   ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

        IPOINT=IDHCV+31
       CALL ADDPGR('FQLTURQNEGAT ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

        IPOINT=IDHCV+32
       CALL ADDPGR('FQNTURQNEGAT ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

        IPOINT=IDHCV+33
       CALL ADDPGR('FQTCONDECOL  ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

        IPOINT=IDHCV+34
       CALL ADDPGR('FQTCONDECON  ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

        IPOINT=IDHCV+35
       CALL ADDPGR('FQTCONDESTL  ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

        IPOINT=IDHCV+36
       CALL ADDPGR('FQTCONDESTN  ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

        IPOINT=IDHCV+37
       CALL ADDPGR('FQRTURQNEGAT ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)
 
        IPOINT=IDHCV+38
       CALL ADDPGR('FQSTURQNEGAT ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)
  
        IPOINT=IDHCV+39
       CALL ADDPGR('FQTCONDESTR  ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)
 
        IPOINT=IDHCV+40
       CALL ADDPGR('FQTCONDESTS  ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

        IF (LCONDWT.AND.LPROCLD) THEN
           IPOINT=IDHCV+37
       CALL ADDPGR('FQVEVPL      ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)
             
           IPOINT=IDHCV+38
       CALL ADDPGR('FQVEVPN      ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)
             
           IPOINT=IDHCV+39
       CALL ADDPGR('FQLFPL       ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)
             
           IPOINT=IDHCV+40
       CALL ADDPGR('FQNFPN       ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)
        ENDIF
      ENDIF
    END SELECT

    ENDIF ! LHDHKS

    IF ( LHDMCI ) THEN
      IDHCV=NDHVV+NDHFVD+NDHAHKP
      IPOINT=IDHCV+1
       CALL ADDPGR('FA1TUR       ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+2
       CALL ADDPGR('FA2TUR       ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+3
       CALL ADDPGR('FA3TUR       ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+4
       CALL ADDPGR('FA1TURCONV   ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+5
       CALL ADDPGR('FA2TURCONV   ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+6
       CALL ADDPGR('FA3TURCONV   ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+7
       CALL ADDPGR('FA1ONDEGREL  ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+8
       CALL ADDPGR('FA2ONDEGREL  ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)

      IPOINT=IDHCV+9
       CALL ADDPGR('FA3ONDEGREL  ',KNUM,PDDHCV(0,IPOINT),NFLEVG+1)
    ENDIF

    IF ( LHDHKS ) THEN
      IDHCV=NDHVV+NDHFVD+NDHAVP
      IPOINT=IDHCV+1
       CALL ADDPGR('TKKDISSIPTUR ',KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+2
       CALL ADDPGR('TKKDISSIPCONV',KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+3
       CALL ADDPGR('TKKDISSIPGREL',KNUM,PDDHCV(1,IPOINT),NFLEVG)
    ENDIF

    IF ( LHDENT ) THEN
      IDHCV=NDHVV+NDHFVD+NDHAVP+NDHBHKP+NDHBMCP
      IPOINT=IDHCV+1
       CALL ADDPGR('TSSDISSIPTUR ',KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+2
       CALL ADDPGR('TSSDISSIPCONV',KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+3
       CALL ADDPGR('TSSDISSIPGREL',KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+4
       CALL ADDPGR('TSSTURQVTOT  ',KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+5
       CALL ADDPGR('TSSRAY1      ',KNUM,PDDHCV(1,IPOINT),NFLEVG)

      IPOINT=IDHCV+6
       CALL ADDPGR('TSSTURCTTOT  ',KNUM,PDDHCV(1,IPOINT),NFLEVG)
    ENDIF

  ENDIF !FLEXDIA

    !     ------------------------------------------------------------------
    !*    5.- SOL

    IDHCS = 0
    INBCUMS = NDHFSD + NDHFSP

    IF ( LHDHKS .AND. ((NDHVS /= 0) .OR. (NDHCS /= 0)) ) THEN

      IF(LECMWF) THEN

        ! Write initial values of all standard variables

        ! Tiles

        DO JVAR=1,NDHVTLS
       CALL ADDPGR(TRIM(YDCDDH%CADHTLS(JVAR))//'0',KNUM,PDDHCS(IDHCS+1),NTILES)
          IDHCS=IDHCS+NTILES
        ENDDO

        IDHCS=NDHVTLS*NTILES

        ! Snow energy budget

        DO JVAR=1,NDHVTSS
       CALL ADDPGR(TRIM(YDCDDH%CADHTSS(JVAR))//'0',KNUM,PDDHCS(IDHCS+1),NCSNEC)
          IDHCS=IDHCS+NCSNEC
        ENDDO

        IDHCS=NDHVTLS*NTILES+NDHVTSS*NCSNEC

        ! Soil energy budget

        DO JVAR=1,NDHVTTS
       CALL ADDPGR(TRIM(YDCDDH%CADHTTS(JVAR))//'0',KNUM,PDDHCS(IDHCS+1),YSP_SBD%NLEVS)
          IDHCS=IDHCS+YSP_SBD%NLEVS
        ENDDO

        IDHCS=NDHVTLS*NTILES+NDHVTSS*NCSNEC+NDHVTTS*YSP_SBD%NLEVS

        ! Sea-ice energy budget

        DO JVAR=1,NDHVTIS
       CALL ADDPGR(TRIM(YDCDDH%CADHTIS(JVAR))//'0',KNUM,PDDHCS(IDHCS+1),NCSI)
          IDHCS=IDHCS+NCSI
        ENDDO

        IDHCS=NDHVTLS*NTILES+NDHVTSS*NCSNEC+NDHVTTS*YSP_SBD%NLEVS+&
         & NDHVTIS*NCSI

        ! Snow water budget

        DO JVAR=1,NDHVSSS
       CALL ADDPGR(TRIM(YDCDDH%CADHSSS(JVAR))//'0',KNUM,PDDHCS(IDHCS+1),NCSNEC)
          IDHCS=IDHCS+NCSNEC
        ENDDO

        IDHCS=NDHVTLS*NTILES+NDHVTSS*NCSNEC+NDHVTTS*YSP_SBD%NLEVS+&
         & NDHVTIS*NCSI+NDHVSSS*NCSNEC

        ! Interception layer water budget

        DO JVAR=1,NDHVIIS
       CALL ADDPGR(TRIM(YDCDDH%CADHIIS(JVAR))//'0',KNUM,PDDHCS(IDHCS+1),1)
          IDHCS=IDHCS+1
        ENDDO

        IDHCS=NDHVTLS*NTILES+NDHVTSS*NCSNEC+NDHVTTS*YSP_SBD%NLEVS+&
         & NDHVTIS*NCSI+NDHVSSS*NCSNEC+NDHVIIS

        ! Soil water budget

        DO JVAR=1,NDHVWLS
       CALL ADDPGR(TRIM(YDCDDH%CADHWLS(JVAR))//'0',KNUM,PDDHCS(IDHCS+1),YSP_SBD%NLEVS)
          IDHCS=IDHCS+YSP_SBD%NLEVS
        ENDDO

        !-------------------------------------------------------------------------

        ! Write current values of all standard variables

        ! set pointer

        IDHCS = NDHCS

        ! Tiles

        DO JVAR=1,NDHVTLS
       CALL ADDPGR(TRIM(YDCDDH%CADHTLS(JVAR))//'1',KNUM,PDDHCS(IDHCS+1),NTILES)
          IDHCS=IDHCS+NTILES
        ENDDO

        IDHCS=NDHCS+NDHVTLS*NTILES

        ! Snow energy budget

        DO JVAR=1,NDHVTSS
       CALL ADDPGR(TRIM(YDCDDH%CADHTSS(JVAR))//'1',KNUM,PDDHCS(IDHCS+1),NCSNEC)
          IDHCS=IDHCS+NCSNEC
        ENDDO

        IDHCS=NDHCS+NDHVTLS*NTILES+NDHVTSS*NCSNEC

        ! Soil energy budget

        DO JVAR=1,NDHVTTS
       CALL ADDPGR(TRIM(YDCDDH%CADHTTS(JVAR))//'1',KNUM,PDDHCS(IDHCS+1),YSP_SBD%NLEVS)
          IDHCS=IDHCS+YSP_SBD%NLEVS
        ENDDO

        IDHCS=NDHCS+NDHVTLS*NTILES+NDHVTSS*NCSNEC+NDHVTTS*YSP_SBD%NLEVS

        ! Sea-ice energy budget

        DO JVAR=1,NDHVTIS
       CALL ADDPGR(TRIM(YDCDDH%CADHTIS(JVAR))//'1',KNUM,PDDHCS(IDHCS+1),NCSI)
          IDHCS=IDHCS+NCSI
        ENDDO

        IDHCS=NDHCS+NDHVTLS*NTILES+NDHVTSS*NCSNEC+NDHVTTS*YSP_SBD%NLEVS+&
         & NDHVTIS*NCSI

        ! Snow water budget

        DO JVAR=1,NDHVSSS
       CALL ADDPGR(TRIM(YDCDDH%CADHSSS(JVAR))//'1',KNUM,PDDHCS(IDHCS+1),NCSNEC)
          IDHCS=IDHCS+NCSNEC
        ENDDO

        IDHCS=NDHCS+NDHVTLS*NTILES+NDHVTSS*NCSNEC+NDHVTTS*YSP_SBD%NLEVS+&
         & NDHVTIS*NCSI+NDHVSSS*NCSNEC

        ! Interception layer water budget

        DO JVAR=1,NDHVIIS
       CALL ADDPGR(TRIM(YDCDDH%CADHIIS(JVAR))//'1',KNUM,PDDHCS(IDHCS+1),1)
          IDHCS=IDHCS+1
        ENDDO

        IDHCS=NDHCS+NDHVTLS*NTILES+NDHVTSS*NCSNEC+NDHVTTS*YSP_SBD%NLEVS+&
         & NDHVTIS*NCSI+NDHVSSS*NCSNEC+NDHVIIS

        ! Soil water budget

        DO JVAR=1,NDHVWLS
       CALL ADDPGR(TRIM(YDCDDH%CADHWLS(JVAR))//'1',KNUM,PDDHCS(IDHCS+1),YSP_SBD%NLEVS)
          IDHCS=IDHCS+YSP_SBD%NLEVS
        ENDDO

        !-------------------------------------------------------------------------

        ! Write free-style variables

        IF (NDHVFS /= 0) THEN

          ! Set pointer

          IDHCS = NDHVS

          ! Write

          DO JVAR=1,NDHVFS
       CALL ADDPGR(TRIM(YDCDDH%CADHFSS(JVAR)),KNUM,PDDHCS(IDHCS+1),1)
            IDHCS=IDHCS+1
          ENDDO

        ENDIF
        !-------------------------------------------------------------------------

        ! Write standard fluxes

        ! Set pointer

        IDHCS = NDHVS + NDHVFS

        ! Tiles

        DO JFLX=NDHVTLS+1,NDHVTLS+NDHFTLS
       CALL ADDPGR(TRIM(YDCDDH%CADHTLS(JFLX)),KNUM,PDDHCS(IDHCS+1),NTILES)
          IDHCS=IDHCS+NTILES
        ENDDO

        IDHCS=NDHVS+NDHVFS+NDHFTLS*NTILES

        ! Snow energy budget

        DO JFLX=NDHVTSS+1,NDHVTSS+NDHFTSS
       CALL ADDPGR(TRIM(YDCDDH%CADHTSS(JFLX)),KNUM,PDDHCS(IDHCS+1),NCSNEC)
          IDHCS=IDHCS+NCSNEC
        ENDDO

        IDHCS=NDHVS+NDHVFS+NDHFTLS*NTILES+NDHFTSS*NCSNEC

        ! Soil energy budget

        DO JFLX=NDHVTTS+1,NDHVTTS+NDHFTTS
       CALL ADDPGR(TRIM(YDCDDH%CADHTTS(JFLX)),KNUM,PDDHCS(IDHCS+1),YSP_SBD%NLEVS)
          IDHCS=IDHCS+YSP_SBD%NLEVS
        ENDDO

        IDHCS=NDHVS+NDHVFS+NDHFTLS*NTILES+NDHFTSS*NCSNEC+NDHFTTS*YSP_SBD%NLEVS

        ! Sea-ice energy budget

        DO JFLX=NDHVTIS+1,NDHVTIS+NDHFTIS
       CALL ADDPGR(TRIM(YDCDDH%CADHTIS(JFLX)),KNUM,PDDHCS(IDHCS+1),NCSI)
          IDHCS=IDHCS+NCSI
        ENDDO

        IDHCS=NDHVS+NDHVFS+NDHFTLS*NTILES+NDHFTSS*NCSNEC+NDHFTTS*YSP_SBD%NLEVS+&
         & NDHFTIS*NCSI

        ! Snow water budget

        DO JFLX=NDHVSSS+1,NDHVSSS+NDHFSSS
       CALL ADDPGR(TRIM(YDCDDH%CADHSSS(JFLX)),KNUM,PDDHCS(IDHCS+1),NCSNEC)
          IDHCS=IDHCS+NCSNEC
        ENDDO

        IDHCS=NDHVS+NDHVFS+NDHFTLS*NTILES+NDHFTSS*NCSNEC+NDHFTTS*YSP_SBD%NLEVS+&
         & NDHFTIS*NCSI+NDHFSSS*NCSNEC

        ! Interception layer water budget

        DO JFLX=NDHVIIS+1,NDHVIIS+NDHFIIS
       CALL ADDPGR(TRIM(YDCDDH%CADHIIS(JFLX)),KNUM,PDDHCS(IDHCS+1),1)
          IDHCS=IDHCS+1
        ENDDO

        IDHCS=NDHVS+NDHVFS+NDHFTLS*NTILES+NDHFTSS*NCSNEC+NDHFTTS*YSP_SBD%NLEVS+&
         & NDHFTIS*NCSI+NDHFSSS*NCSNEC+NDHFIIS

        ! Soil water budget

        DO JFLX=NDHVWLS+1,NDHVWLS+NDHFWLS
       CALL ADDPGR(TRIM(YDCDDH%CADHWLS(JFLX)),KNUM,PDDHCS(IDHCS+1),YSP_SBD%NLEVS)
          IDHCS=IDHCS+YSP_SBD%NLEVS
        ENDDO

        !-------------------------------------------------------------------------

        ! Write free-style fluxes

        IF (NDHFFS /= 0) THEN

          ! Set pointer

          IDHCS = NDHVS + NDHVFS + NDHFSD + NDHFSP

          ! Write

          DO JFLX=NDHVFS+1,NDHVFS+NDHFFS
       CALL ADDPGR(TRIM(YDCDDH%CADHFSS(JFLX)),KNUM,PDDHCS(IDHCS+1),1)
            IDHCS=IDHCS+1
          ENDDO

        ENDIF

        !-------------------------------------------------------------------------

      ELSE ! Case .NOT.LECMWF
        
        IF (LFLEXDIA) THEN

          IVARS=0
          IPLVAS=NTOTSVAR
          IPLVFS=NTOTSVFS
          IPLFIS=NTOTSURF-NTOTSVAR-NTOTSVFS

          DO JFIELD=1,NTOTSURF
            IPOINT=JFIELD
            SELECT CASE(RDDHSURF_DESCR(JFIELD)%CFLUX)
            CASE('V')
       CALL ADDPGR(RDDHSURF_DESCR(JFIELD)%CNAME//'1 ',KNUM,PDDHCS(IPOINT),IPLVAS)

              IVARS=IVARS+1
              IPOINT=IVARS+NTOTSURF
       CALL ADDPGR(RDDHSURF_DESCR(JFIELD)%CNAME//'0 ',KNUM,PDDHCS(IPOINT),IPLVAS)

            CASE('S')
       CALL ADDPGR(RDDHSURF_DESCR(JFIELD)%CNAME//' ',KNUM,PDDHCS(IPOINT),IPLVFS)

            CASE('F')
       CALL ADDPGR(RDDHSURF_DESCR(JFIELD)%CNAME//' ',KNUM,PDDHCS(IPOINT),IPLFIS)

            END SELECT
          ENDDO

        ELSE !LFLEXDIA

          IDHCS = 0
 
          !       ENSEMBLE DES VARIABLES INITIALES
          DO JVAR=1,NDHVS
            WRITE (CLNOMA,FMT='(A,I2.2,A)') 'S',JVAR,'_0'
            IPOINT=IDHCS+JVAR
       CALL ADDPGR(CLNOMA,KNUM,PDDHCS(IPOINT),NDHVS)
          ENDDO

          !       ENSEMBLE DES VARIABLES COURANTES
          IDHCS = NDHCS
          DO JVAR=1,NDHVS
            WRITE (CLNOMA,FMT='(A,I2.2,A)') 'S',JVAR,'_1'
            IPOINT=IDHCS+JVAR
       CALL ADDPGR(CLNOMA,KNUM,PDDHCS(IPOINT),NDHVS)
          ENDDO

          !       ENSEMBLE DES CUMULS
          IDHCS = NDHVS + NDHVFS
          DO JVAR=1,INBCUMS
            WRITE (CLNOMA,FMT='(A,I2.2)') 'G',JVAR
            IPOINT=IDHCS+JVAR
       CALL ADDPGR(CLNOMA,KNUM,PDDHCS(IPOINT),INBCUMS)
          ENDDO

          !       Set of free-style surface variables
          IF (NDHVFS /= 0) THEN
            IDHCS = NDHVS
            DO JVAR=1,NDHVFS
              WRITE (CLNOMA,FMT='(A,I2.2)') 'SVGFS',JVAR
              IPOINT=IDHCS+JVAR
       CALL ADDPGR(CLNOMA,KNUM,PDDHCS(IPOINT),NDHVFS)
            ENDDO
          ENDIF

          !       Set of free-style surface fluxes
          IF (NDHFFS /= 0) THEN
            IDHCS = NDHVS + NDHVFS + NDHFSD + NDHFSP
            DO JVAR=1,NDHFFS
              WRITE (CLNOMA,FMT='(A,I2.2)') 'SFGFS',JVAR
              IPOINT=IDHCS+JVAR
       CALL ADDPGR(CLNOMA,KNUM,PDDHCS(IPOINT),NDHFFS)
            ENDDO
          ENDIF
        ENDIF !LFLEXDIA
      ENDIF ! LECMWF
    ENDIF ! ( LHDHKS .AND. ((NDHVS /= 0) .OR. (NDHCS /= 0)) )

    IF ( LHDLIST ) THEN
      WRITE (NULOUT,*) ' DDH * PPFIDH FINI IDHCV=',IDHCV,&
       & ' IDHCS = ',IDHCS,' INBCUMS = ',INBCUMS
    ENDIF

ENDIF ! (.NOT.LHDLFA .OR. KNUM == KDIMDOM)

4000 FORMAT (I3.3,A3,A9)
5000 FORMAT (I3.3,A1,A2,A10)
6000 FORMAT (I3.3,A11,A2)
7000 FORMAT (I3.3,A4,A2)
8000 FORMAT (I3.3,A5,A2)
9000 FORMAT (I3.3,A9,A2)

!-----------------------------------------------------------------------
END ASSOCIATE
END ASSOCIATE
