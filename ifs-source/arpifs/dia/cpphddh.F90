REAL(KIND=JPRB)   ,INTENT(IN)    :: PFCQSNG(KPROMA,0:KFLEV)
REAL(KIND=JPRB)   ,INTENT(IN)    :: PHI0(KPROMA,0:KFLEV) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PUCLS(KPROMA) 
REAL(KIND=JPRB)   ,INTENT(IN)    :: PVCLS(KPROMA) 
