SUBROUTINE CPPHDDH( &
 !     INPUT.
 & YDML_DIAG,YDEPHY,YDRIP,YDML_PHY_MF,KPROMA,KSTART,KPROF,KFLEV,LDDYN,&
 & YDGSGEOM,PUZGEO,PVMGEO,&
 & PSNS,PTP,PTS,&
 & PWP,PWS,PT0,PLSM,&
 & PDIFCQ, PDIFCS, PDIFTQ, PDIFTS, PFCQNG,&
 & PFPLCL, PFPLCN, PFPLSL, PFPLSN, PFRSO, PFRTH,&
 & PFRTHDS,PFHPCL, PFHPCN, PFHPSL, PFHPSN,&
 & PSTRCU, PSTRCV, PSTRDU, PSTRDV, PSTRTU, PSTRTV,&
 & PCT,&
 & PFCHSP, PFCLL, PFCLN, PFCS, PFEVL, PFEVN, PFLWSP,&
 & PFONTE, PRUISP, PRUISS,&
 & PENTRA, PENTRV,&
 & PDIFTQL,PDIFTQN,PDIFCQL,PDIFCQN,PFCQLNG,PFCQNNG,&
 & PFHSCL,PFHSCN,PFHSSL,PFHSSN, PFEPFP, PFCMPCQ, PFCMPSN, PFCMPSL,&
 & PFCQLF,PFCQIF,PFSQLF,PFSQIF,&
 & PFPFPL,PFPFPN,PFPFPCL,PFPFPCN,&
 & PFPEVPL,PFPEVPN,PFPEVPCL,PFPEVPCN,&
 & PFCQRNG, PFCQSNG,&
 & PHI0,PUCLS,PVCLS,PTCLS,PQCLS,PCLPH,PALB,PGZ0,PGZ0H,&
 !     OUTPUT.
 & PDHCV,PDHCS,PFTCNS)

!---------------------------------------------------------------------------
!**** *CPPHDDH*  - CALCUL DES FLUX/TENDANCES DYNAMIQUES
!                  DIAGNOSTICS PAR DOMAINES HORIZONTAUX
!---------------------------------------------------------------------------
!     BUT
!     ---
!      CALCULS PHYSIQUES POUR LES DIAGNOSTICS DDH
!      AINSI QUE DES VARIABLES AU SOL A L'INSTANT COURANT

!   ** SURVEILLER LE POSITIONNEMENT DES CHAMPS AU MOYEN DE L'INDICATEUR
!                           IDHCV

!     ARGUMENTS D ENTREE (INPUT)
!     --------------------------

! - DIMENSIONS
!     KPROMA : DIMENSION HORIZONTALE.
!     KSTART : DEBUT DE LA BOUCLE HORIZONTALE.
!     KPROF  : FIN DE LA BOUCLE HORIZONTALE.
!     KFLEV  : DIMENSION VERTICALE ET NB DE NIVEAUX

! - LOGIQUES DE CONTROLE
!     LDDYN  : activation des DDH pour la dynamique.

! - TABLEAUX DE DONNEES
!     YDGSGEOM       : structure for geographical sphere horizontal geometry.
!     PUZGEO, PVMGEO : VENT ZONAL ET MERIDIEN VRAIS GEOGRAPHIQUES
!                      (UNIQUEMENT SI LHDHKS OU LHDMCI OU LHDENT VRAIS)
!     PSNS       : MASSE PAR UNITE DE SURFACE DE LA COUCHE NEIGEUSE.
!     PTP        : TEMPERATURE EN PROFONDEUR.
!     PTS        : TEMPERATURE DE SURFACE.
!     PWP        : CONTENU EN EAU DU RESERVOIR EN PROFONDEUR.
!     PWS        : CONTENU EN EAU DU RESERVOIR DE SURFACE.
!     PT0        : TEMPERATURE A INSTANT 0 / TEMPERATURE AT TIME LEVEL 0
!     PHI0       : OROGRAPHIE (HAUTEUR GEOPOTENTIELLE "gz" A L'INSTANT t SUR LES INTERCOUCHES).
!     PLSM       : INDICE TERRE/MER.
!     PUCLS      : COMPOSANTE U DU VENT A 10 METRES (DIAGNOSTIQUE).
!     PVCLS      : COMPOSANTE V DU VENT A 10 METRES (DIAGNOSTIQUE).
!     PTCLS      : TEMPERATURE A 2 METRES (DIAGNOSTIQUE).
!     PQCLS      : HUMIDITE SPECIFIQUE A 2 METRES (DIAGNOSTIQUE).
!     PCLPH      : HAUTEUR (EN METRES) DE LA COUCHE LIMITE PLANETAIRE.
!     PALB       : ALBEDO DE SURFACE COURANT.
!     PGZ0       : G*LONGUEUR DE RUGOSITE DYNAMIQUE.
!     PGZ0H      : G*LONGUEUR DE RUGOSITE THERMIQUE.
!     PDIFCQ     : FLUX CONVECTIF D'HUMIDITE SPECIFIQUE (HORS PLUIE/NEIGE).
!     PDIFCS     : FLUX CONVECTIF D'ENTHALPIE (HORS PLUIE/NEIGE).
!     PDIFTQ     : FLUX TURBULENT (ET Q NEGATIF) D'HUMIDITE SPECIFIQUE.
!     PDIFTS     : FLUX TURBULENT D'ENTHALPIE (OU D'ENERGIE STATIQUE SECHE).
!     PFCQNG     : FLUX FICTIF D'EAU POUR CORRIGER LES Q<0.
!     PFPLCL     : PRECIPITATIONS CONVECTIVES SOUS FORME LIQUIDE.
!     PFPLCN     : PRECIPITATIONS CONVECTIVES SOUS FORME NEIGE.
!     PFPLSL     : PRECIPITATIONS STRATIFORMES SOUS FORME LIQUIDE.
!     PFPLSN     : PRECIPITATIONS STRATIFORMES SOUS FORME NEIGE.
!     PFRSO      : FLUX DE RAYONNEMENT SOLAIRE.
!     PFRTH      : FLUX DE RAYONNEMENT THERMIQUE.
!     PFRTHDS    : FLUX DE RAYONNEMENT THERMIQUE DESCENDANT A LA SURFACE.
!     PFHPCL     : FLUX ENTHALPIE CONVECTIVES SOUS FORME LIQUIDE.
!     PFHPCN     : FLUX ENTHALPIE CONVECTIVES SOUS FORME NEIGE.
!     PFHPSL     : FLUX ENTHALPIE STRATIFORMES SOUS FORME LIQUIDE.
!     PFHPSN     : FLUX ENTHALPIE STRATIFORMES SOUS FORME NEIGE.
!     PSTRCU     : FLUX CONVECTIF DE QUANTITE DE MOUVEMENT "U".
!     PSTRCV     : FLUX CONVECTIF DE QUANTITE DE MOUVEMENT "V".
!     PSTRDU     : FLUX "GRAVITY WAVE DRAG" "U".
!     PSTRDV     : FLUX "GRAVITY WAVE DRAG" "V".
!     PSTRTU     : FLUX TURBULENT DE QUANTITE DE MOUVEMENT "U".
!     PSTRTV     : FLUX TURBULENT DE QUANTITE DE MOUVEMENT "V".
!     PCT        : ???
!     PFCHSP     : FLUX DE CHALEUR DE LA SURFACE VERS LE SOL PROFOND.
!     PFCLL      : FLUX DE CHALEUR LATENTE SUR EAU LIQUIDE (OU SOL HUMIDE).
!     PFCLN      : FLUX DE CHALEUR LATENTE SUR NEIGE (OU GLACE).
!     PFCS       : FLUX DE CHALEUR SENSIBLE EN SURFACE.
!     PFEVL      : FLUX DE VAPEUR D'EAU SUR EAU LIQUIDE (OU SOL HUMIDE).
!     PFEVN      : FLUX DE VAPEUR D'EAU SUR NEIGE (OU GLACE).
!     PFLWSP     : FLUX D'EAU LIQUIDE DE LA SURFACE VERS LE SOL PROFOND.
!     PFONTE     : FLUX D'EAU CORRESPONDANT A LA FONTE DE NEIGE EN SURFACE.
!     PRUISP     : FLUX DE RUISSELLEMENT EN PROFONDEUR.
!     PRUISS     : FLUX DE RUISSELLEMENT EN SURFACE.
!     PENTRA     : ENTROPIE AIR SEC / SPECIFIC ENTROPY OF DRY AIR
!     PENTRV     : ENTROPIE VAPEUR D'EAU / SPECIFIC ENTROPY OF WATER VAPOUR
!     PDIFTQL    : FLUX D'EAU LIQUIDE TURBULENT.
!     PDIFTQN    : FLUX D'EAU NEIGEUSE TURBULENT.
!     PDIFCQL    : FLUX D'EAU LIQUIDE TURBULENT CONVECTIF.
!     PDIFCQN    : FLUX D'EAU NEIGEUSE TURBULENT CONVECTIF.
!     PFCQLNG    : FLUX D'EAU LIQUIDE DE CORR. DES VALEURS < 0.
!     PFCQNNG    : FLUX D'EAU NEIGEUSE DE CORR. DES VALEURS < 0.
!     PFHSCL     : FLUX DE CHALEUR SENSIBLE LIE AUX PRECIPITATIONS CONV. LIQ.
!     PFHSCN     : FLUX DE CHALEUR SENSIBLE LIE AUX PRECIPITATIONS CONV. NEI.
!     PFHSSL     : FLUX DE CHALEUR SENSIBLE LIE AUX PRECIPITATIONS STRA. LIQ.
!     PFHSSN     : FLUX DE CHALEUR SENSIBLE LIE AUX PRECIPITATIONS STRA. NEI.
!     PFEPFP     : PSEUDO FLUX D'ENTHALPIE LIE A L'ENERGIE PRECIPITATIONS.
!     PFCQLF     : FLUX DE CONDENSATION CONV. LIQ.
!     PFCQIF     : FLUX DE CONDENSATION CONV. NEI.
!     PFSQLF     : FLUX DE CONDENSATION STRA. LIQ.
!     PFSQIF     : FLUX DE CONDENSATION STRA. NEI.

!     ENTREES IMPLICITES
!     ------------------
!       UNITE NULOUT (/YOMLUN/)
!       CONSTANTES UNIVERSELLES, COMMON /YOMCST/
!       FONCTIONS THERMODYNAMIQUES, COMDECK *FCTTRM*

!       CONTROLE PHYSIQUE DE BASE : YOMPHY : NDPSFI
!       CONSTANTES PHYSIQUES (NORMALEMENT INUTILES)
!       YOMPHY0, YOMPHY1, YOMPHY2, YOMPHY3

!       ELEMENTS DE CONTROLE DDH
!       INDICATEURS LOGIQUES, COMMON /YOMLDDH/
!        DANS LE COMDECK * YOMMDDH * POUR LES MASQUES
!       DIMENSIONS PROPRES AUX MASQUES DIAGNOSTIQUES DDH, COMMON /DDHDIM/
!       ET DIMENSIONS POUR LES TABLEAUX DE CUMULS
!       INDICATEURS DE POSITIONS DANS LES TABLEAUX DE CUMUL

!     ARGUMENTS DE SORTIES (OUTPUT)
!     -----------------------------
!      DANS PDHCV, FLUX/TENDANCES PHYSIQUES INSTANTANEES
!      DANS PDHCS  VARIABLES ET FLUX AU SOL

!     REMARQUES SUPPLEMENTAIRES
!     -------------------------

