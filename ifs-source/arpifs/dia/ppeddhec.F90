SUBROUTINE PPEDDHEC(YDDIMV,YDSURF,YDML_DIAG,YDDPHY,YDML_GCONF,PDHNOM,PDDHCV,PDDHCS,CDNAMX,KDATEF)
!-----------------------------------------------------------------------
!**** PPEDDH * IMPRESSION DE BILANS EN MOYENNE VERTICALE POUR UN DOMAINE
!              DIAGNOSTICS PAR DOMAINES HORIZONTAUX
!-----------------------------------------------------------------------
!     BUT
!     ---
!     EDITION SUR LISTING DES BILANS EN MOYENNE VERTICALE
!     DU DOMAINE IDENTIFIE PAR PDHNOM

!     ARGUMENTS D'ENTREE
!     ------------------
!      PDHNOM(11) : IDENTIFICATION DU DOMAINE
!                  PDHNOM(11) EST LE TYPE DE DOMAINE
!      PDDHCV(0:NFLEVG,NDHCV+NDHVV) : PROFILS VERTICAUX
!                  LA STRUCTURE DU TABLEAU EST RESUMEE DANS
!                  PPSYDH, SINON VOIR YOMTDDH ET CPDYDDH/CPPHDDH
!      PDDHCS(NDHCV+NDHVV) : SOL
!      CDNAMX*4 : NOM DE L EXPERIENCE
!      KDATEF(11) : TABLEAU DE DATE DECOMPOSEE

!     ARGUMENTS DE SORTIE
!     -------------------
!     LISTING, UNITE NULOUT

!     ORIGINAL
!     --------
!     2000-06-29, C. Jakob, started as copy of PPEDDH
!     MODIFICATIONS
!     --------
!     2006-09-07, S.Serrar, DDH prints for GHG and SF6
!     JJMorcrette 20061002 DDH for aerosol physics
!     M. Ahlgrimm  31-Oct-2011 add rain, snow and PEXTRA to DDH output
!     K. Yessad (July 2014): Move some variables.
!-----------------------------------------------------------------------

USE MODEL_DIAGNOSTICS_MOD  , ONLY : MODEL_DIAGNOSTICS_TYPE
USE MODEL_GENERAL_CONF_MOD , ONLY : MODEL_GENERAL_CONF_TYPE
USE YOMDIMV                , ONLY : TDIMV
USE SURFACE_FIELDS_MIX     , ONLY : TSURF
USE PARKIND1               , ONLY : JPIM, JPRB
USE YOMHOOK                , ONLY : LHOOK, DR_HOOK, JPHOOK
USE YOMLUN                 , ONLY : NULOUT
USE YOMRIP0                , ONLY : NSSSSS
USE YOMCT3                 , ONLY : NSTEP
USE YOMCST                 , ONLY : RPI      ,ROMEGA   ,RLVTT    ,RLSTT    ,RLMLT
USE YOMDPHY                , ONLY : TDPHY

!     ------------------------------------------------------------------

IMPLICIT NONE

TYPE(TDIMV)       , INTENT(IN)    :: YDDIMV
TYPE(TSURF)       , INTENT(INOUT) :: YDSURF
TYPE(TDPHY)       , INTENT(IN)    :: YDDPHY
TYPE(MODEL_DIAGNOSTICS_TYPE) ,INTENT(INOUT):: YDML_DIAG
TYPE(MODEL_GENERAL_CONF_TYPE),INTENT(IN)   :: YDML_GCONF
REAL(KIND=JPRB)   , INTENT(IN)    :: PDHNOM(11) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PDDHCV(0:YDDIMV%NFLEVG,YDML_DIAG%YRMDDH%NDHCVSU+YDML_DIAG%YRMDDH%NDHVV) 
REAL(KIND=JPRB)   , INTENT(IN)    :: PDDHCS(YDML_DIAG%YRMDDH%NDHCSSU+YDML_DIAG%YRMDDH%NDHVS) 
CHARACTER         , INTENT(IN)    :: CDNAMX*4 
INTEGER(KIND=JPIM), INTENT(IN)    :: KDATEF(11) 
!     ------------------------------------------------------------------
CHARACTER :: CLDOM*20, CL24C*24, CL10C*10, CLOUT*10
REAL(KIND=JPRB) :: ZDHIV (YDML_DIAG%YRMDDH%NDHCV+YDML_DIAG%YRMDDH%NDHVV)

INTEGER(KIND=JPIM) :: IDHCS, IDHCS_2,IDHCV, ISAERO, ISEN, ISHK, ISMC, ITYPE, JCV, JJPASS, JLEV
INTEGER(KIND=JPIM) :: JVAR, JFLX, JTILE

!     --- NEW SURFACE FIELDS 

REAL(KIND=JPRB) :: ZDHTLS0(YDDPHY%NTILES,YDML_DIAG%YRMDDH%NDHVTLS)
REAL(KIND=JPRB) :: ZDHTSS0(YDDPHY%NCSNEC,YDML_DIAG%YRMDDH%NDHVTSS)
REAL(KIND=JPRB) :: ZDHTTS0(YDSURF%YSP_SBD%NLEVS,YDML_DIAG%YRMDDH%NDHVTTS)
REAL(KIND=JPRB) :: ZDHTIS0(YDDPHY%NCSI,YDML_DIAG%YRMDDH%NDHVTIS)
REAL(KIND=JPRB) :: ZDHSSS0(YDDPHY%NCSNEC,YDML_DIAG%YRMDDH%NDHVSSS)
REAL(KIND=JPRB) :: ZDHIIS0(YDML_DIAG%YRMDDH%NDHVIIS)
REAL(KIND=JPRB) :: ZDHWLS0(YDSURF%YSP_SBD%NLEVS,YDML_DIAG%YRMDDH%NDHVWLS)
REAL(KIND=JPRB) :: ZDHTLS(YDDPHY%NTILES,YDML_DIAG%YRMDDH%NDHVTLS+YDML_DIAG%YRMDDH%NDHFTLS)
REAL(KIND=JPRB) :: ZDHTSS(YDDPHY%NCSNEC,YDML_DIAG%YRMDDH%NDHVTSS+YDML_DIAG%YRMDDH%NDHFTSS)
REAL(KIND=JPRB) :: ZDHTTS(YDSURF%YSP_SBD%NLEVS,YDML_DIAG%YRMDDH%NDHVTTS+YDML_DIAG%YRMDDH%NDHFTTS)
REAL(KIND=JPRB) :: ZDHTIS(YDDPHY%NCSI,YDML_DIAG%YRMDDH%NDHVTIS+YDML_DIAG%YRMDDH%NDHFTIS)
REAL(KIND=JPRB) :: ZDHSSS(YDDPHY%NCSNEC,YDML_DIAG%YRMDDH%NDHVSSS+YDML_DIAG%YRMDDH%NDHFSSS)
REAL(KIND=JPRB) :: ZDHIIS(YDML_DIAG%YRMDDH%NDHVIIS+YDML_DIAG%YRMDDH%NDHFIIS)
REAL(KIND=JPRB) :: ZDHWLS(YDSURF%YSP_SBD%NLEVS,YDML_DIAG%YRMDDH%NDHVWLS+YDML_DIAG%YRMDDH%NDHFWLS)
REAL(KIND=JPRB) :: ZDHFSS(YDML_DIAG%YRMDDH%NDHVFS+YDML_DIAG%YRMDDH%NDHFFS)

REAL(KIND=JPRB) :: ZA10, ZA11, ZA1AD, ZA1C0, ZA1CS, ZA1D0, ZA1DH,&
 & ZA1DS, ZA1FC, ZA1FD, ZA1FG, ZA1FO, ZA1FP,&
 & ZA1FT, ZA1G0, ZA1GS, ZA1NA, ZA1O0, ZA1OS,&
 & ZA1P0, ZA1PS, ZA1T0, ZA1TS, ZA20, ZA21, ZA2AD,&
 & ZA2C0, ZA2CS, ZA2D0, ZA2DH, ZA2DS, ZA2FC,&
 & ZA2FD, ZA2FG, ZA2FO, ZA2FP, ZA2FT, ZA2G0,&
 & ZA2GS, ZA2NA, ZA2O0, ZA2OS, ZA2P0, ZA2PS,&
 & ZA2T0, ZA2TS, ZA30, ZA31, ZA3AD, ZA3C0, ZA3CS,&
 & ZA3D0, ZA3DH, ZA3DS, ZA3FC, ZA3FD, ZA3FG,&
 & ZA3FO, ZA3FP, ZA3FT, ZA3G0, ZA3GS, ZA3O0,&
 & ZA3OS, ZA3P0, ZA3PS, ZA3T0, ZA3TS, ZANM,&
 & ZBA1, ZBA2, ZBA3, ZBCOV, ZBCT, ZBEN, ZBKK,&
 & ZBMAR, ZBQL, ZBQN, ZBQV, ZCOVCT, ZCT0, ZCT1,&
 & ZCTCCL, ZCTCCL0, ZCTCCLS, ZCTCCN, ZCTCCN0,&
 & ZCTCCNS, ZCTCOP, ZCTCOV, ZCTCSL,&
 & ZCTCSL0, ZCTCSLS, ZCTCSN, ZCTCSN0, ZCTCSNS,&
 & ZCTDH, ZCTDISS, ZCTFD, ZCTFD0, ZCTFDS, ZCTFT,&
 & ZCTFT0, ZCTFTC, ZCTFTC0, ZCTFTCS, ZCTFTS,&
 & ZCTPCL, ZCTPCL0, ZCTPCLS,&
 & ZCTPCN, ZCTPCN0, ZCTPCNS,&
 & ZCTPSL, ZCTPSL0, ZCTPSLS,&
 & ZCTPSN, ZCTPSN0, ZCTPSNS,&
 & ZCTRAS, ZCTRAS0, ZCTRASS, ZCTRAT, ZCTRAT0,&
 & ZCTRATS, ZEN0, ZEN1, ZENCT, ZEND0,&
 & ZENDH, ZENDIC, ZENDID, ZENDISS, ZENDIT, ZENDS,&
 & ZENFD, ZENHUM, ZENP, ZENP0, ZENPCT, ZENPS,&
 & ZENRAD, ZEP0, ZEP1, ZEPDH, ZEPFD, ZEPFD0,&
 & ZEPFDS, ZERO, ZGMTSEC, ZHEURE, ZHR0, ZHR1,&
 & ZKK0, ZKK1, ZKKCOV, ZKKDH, ZKKDIC, ZKKDID,&
 & ZKKDIT, ZKKFD, ZKKFD0, ZKKFDM, ZKKFDM0, ZKKFDMS,&
 & ZKKFDS, ZLAT0, ZLFTT, ZLON0, ZLSTT, ZLVTT,&
 & ZMAR0, ZMAR1, ZMARDH, ZMARFD, ZMARFD0, ZMARFDS,&
 & ZMARFP, ZMARFP0, ZMARFPS, ZMNORM, ZNORM,&
 & ZNORMEQ, ZNT0, ZNT1, ZNT0_2, ZNT1_2, ZOM0, ZOM1, ZOUT, ZPASS0,&
 & ZPASS1, ZQL0, ZQL1, ZQLDH, ZQLFD,&
 & ZQLFNG, ZQLFT, ZQLFTC, ZQN0, ZQN1, ZQNDH,&
 & ZQNFD, ZQNFNG, ZQNFT, ZQNFTC, ZQNORM, ZQTCCL,&
 & ZQTCCN, ZQTCSL, ZQTCSN, ZQTPCL, ZQTPCL0,&
 & ZQTPCLS, ZQTPCN, ZQTPCN0,&
 & ZQTPCNS, ZQTPSL, ZQTPSL0,&
 & ZQTPSLS, ZQTPSN, ZQTPSN0,&
 & ZQTPSNS, ZQV0,   ZQV1,&
 & ZQVDH, ZQVFD, ZQVFD0, ZQVFDS, ZQVFNG, ZQVFNG0,&
 & ZQVFNGS, ZQVFT, ZQVFT0, ZQVFTC, ZQVFTC0,&
 & ZQVFTCS, ZQVFTS,&
 & ZSSS, ZUSSSS, ZLFRAC,&
 & ZIFRAC, ZSCALEL, ZSCALEI , ZNTS,&
 & ZBQR, ZQR0, ZQR1, ZQRFNG, ZQTCSR, ZQRDH, ZQRFD,&
 & ZBQS, ZQS0, ZQS1, ZQSFNG, ZQTCSS, ZQSDH, ZQSFD
REAL(KIND=JPRB) :: ZPHY1, ZPHY2, ZPHY3, ZPHY4, ZPHY5, ZPHY6, ZPHYT, ZTAUT 
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!     ------------------------------------------------------------------
IF (LHOOK) CALL DR_HOOK('PPEDDHEC',0,ZHOOK_HANDLE)
ASSOCIATE(NACTAERO=>YDML_GCONF%YGFL%NACTAERO, NGHG=>YDML_GCONF%YGFL%NGHG, &
 & YAERO=>YDML_GCONF%YGFL%YAERO, YGHG=>YDML_GCONF%YGFL%YGHG, &
 & NFLEVG=>YDDIMV%NFLEVG, &
 & NCSI=>YDDPHY%NCSI, NCSNEC=>YDDPHY%NCSNEC, NTILES=>YDDPHY%NTILES, &
 & PDDHFSVI=>YDML_DIAG%YRTDDH%PDDHFSVI, &
 & LHDENT=>YDML_DIAG%YRLDDH%LHDENT, LHDHKS=>YDML_DIAG%YRLDDH%LHDHKS, LHDMCI=>YDML_DIAG%YRLDDH%LHDMCI, &
 & NDHAHKD=>YDML_DIAG%YRMDDH%NDHAHKD, NDHAHKP=>YDML_DIAG%YRMDDH%NDHAHKP, NDHAMCD=>YDML_DIAG%YRMDDH%NDHAMCD, &
 & NDHAVD=>YDML_DIAG%YRMDDH%NDHAVD, NDHAVP=>YDML_DIAG%YRMDDH%NDHAVP, NDHBHKD=>YDML_DIAG%YRMDDH%NDHBHKD, &
 & NDHBHKP=>YDML_DIAG%YRMDDH%NDHBHKP, NDHBMCD=>YDML_DIAG%YRMDDH%NDHBMCD, NDHBMCP=>YDML_DIAG%YRMDDH%NDHBMCP, &
 & NDHCS=>YDML_DIAG%YRMDDH%NDHCS, NDHCSSU=>YDML_DIAG%YRMDDH%NDHCSSU, NDHCV=>YDML_DIAG%YRMDDH%NDHCV, &
 & NDHCVSU=>YDML_DIAG%YRMDDH%NDHCVSU, NDHFFS=>YDML_DIAG%YRMDDH%NDHFFS, NDHFIIS=>YDML_DIAG%YRMDDH%NDHFIIS, &
 & NDHFSD=>YDML_DIAG%YRMDDH%NDHFSD, NDHFSP=>YDML_DIAG%YRMDDH%NDHFSP, NDHFSSS=>YDML_DIAG%YRMDDH%NDHFSSS, &
 & NDHFTIS=>YDML_DIAG%YRMDDH%NDHFTIS, NDHFTLS=>YDML_DIAG%YRMDDH%NDHFTLS, NDHFTSS=>YDML_DIAG%YRMDDH%NDHFTSS, &
 & NDHFTTS=>YDML_DIAG%YRMDDH%NDHFTTS, NDHFVD=>YDML_DIAG%YRMDDH%NDHFVD, NDHFWLS=>YDML_DIAG%YRMDDH%NDHFWLS, &
 & NDHVFS=>YDML_DIAG%YRMDDH%NDHVFS, NDHVHK=>YDML_DIAG%YRMDDH%NDHVHK, NDHVIIS=>YDML_DIAG%YRMDDH%NDHVIIS, &
 & NDHVMC=>YDML_DIAG%YRMDDH%NDHVMC, NDHVS=>YDML_DIAG%YRMDDH%NDHVS, NDHVSSS=>YDML_DIAG%YRMDDH%NDHVSSS, &
 & NDHVTIS=>YDML_DIAG%YRMDDH%NDHVTIS, NDHVTLS=>YDML_DIAG%YRMDDH%NDHVTLS, NDHVTSS=>YDML_DIAG%YRMDDH%NDHVTSS, &
 & NDHVTTS=>YDML_DIAG%YRMDDH%NDHVTTS, NDHVV=>YDML_DIAG%YRMDDH%NDHVV, NDHVWLS=>YDML_DIAG%YRMDDH%NDHVWLS, &
 & NFSFLX_AERO=>YDML_DIAG%YRMDDH%NFSFLX_AERO, NFSVAR_AERO=>YDML_DIAG%YRMDDH%NFSVAR_AERO, &
 & RSTATI=>YDML_GCONF%YRRIP%RSTATI, TSTEP=>YDML_GCONF%YRRIP%TSTEP, &
 & LHDPAS=>YDML_DIAG%YRSDDH%LHDPAS, LHDQLN=>YDML_DIAG%YRSDDH%LHDQLN, NHDPASVA=>YDML_DIAG%YRSDDH%NHDPASVA, &
 & NHDQLNVA=>YDML_DIAG%YRSDDH%NHDQLNVA, &
 & YSP_SBD=>YDSURF%YSP_SBD)
!     ------------------------------------------------------------------

CALL GSTATS(1913,0)
WRITE (NULOUT,*) ' '

!     ------------------------------------------------------------------
!**   0.- INITIALISATIONS DE SECURITE.
!         AFIN DE CONTOURNER LE MESSAGE DE WARNING PRODUIT PAR
!         LE LOGICIEL DE CONTROLE DES INITIALISATIONS DE VARIABLES
!         EN CAS DE VARIABLES INITIALISEES A L'INTERIEUR DE
!         TESTS IF-ENDIF, ON PORTE UNE VALEUR QUELCONQUE
!         DANS LESDITES VARIABLES.

ZLAT0=0.0_JPRB
ZLON0=0.0_JPRB

!     ------------------------------------------------------------------
!**   1.- IDENTIFICATION DE LA SORTIE

ITYPE=NINT(PDHNOM(11))
WRITE (NULOUT,*) ' DDH * DDH * DDH * DDH * DDH * DDH * DDH '
WRITE (NULOUT,*) ' '
WRITE (NULOUT,*) '      ---------------------------- DDH'
ZSSS=REAL(NSTEP,JPRB)*TSTEP
ZHEURE=ZSSS/3600._JPRB
ZUSSSS=1.0_JPRB/ZSSS
IF(ZHEURE > 72._JPRB) THEN
  ZOUT=.001_JPRB*NINT(ZHEURE/24._JPRB*1000._JPRB)
  CLOUT=' DAYS.'
ELSE
  ZOUT=.001_JPRB*NINT(ZHEURE*1000._JPRB)
  IF(ZOUT >= 2.0_JPRB) THEN
    CLOUT=' HOURS.'
  ELSE
    CLOUT=' HOUR.'
  ENDIF
ENDIF
WRITE (NULOUT,*) '       VERTICALLY INTEGRATED BUDGET   ',&
 & ' EXPERIMENT ',CDNAMX,&
 & ' INITIAL DATE ',KDATEF(3),'/',KDATEF(2),'/',KDATEF(1),&
 & ' ELAPSED TIME : ',ZOUT,CLOUT  
WRITE (NULOUT,*) '      ---------------------------- DDH'
WRITE (NULOUT,*) ' '
IF ( ITYPE == 5 ) THEN
  WRITE (NULOUT,*) ' DDH * AREA    GLOBAL '
  WRITE (NULOUT,*) '               ------ '
  WRITE (CLDOM,'(A20)') 'GLOBAL '
ENDIF
IF ( ITYPE == 6 ) THEN
  WRITE (NULOUT,*) ' DDH * LATITUDE BAND     '
  WRITE (NULOUT,*) ' BAND  ',PDHNOM(2),' TO  ',PDHNOM(3),&
   & ' SINUS(LATITUDE GEO)=',PDHNOM(4)  
  WRITE (NULOUT,*) ' '
  WRITE (CLDOM,'(A8,F4.0,8X)') 'LATITUDE ',PDHNOM(2)
ENDIF
IF ( ITYPE == 1.OR. ITYPE == 4 ) THEN
  WRITE (NULOUT,*) ' DDH * POINT '
  WRITE (NULOUT,*) '      -------'
  WRITE (NULOUT,*) ' PLANE',PDHNOM(1),' AREA    ',PDHNOM(2),&
   & ' INDICES JLON=',PDHNOM(5),' JGL=',PDHNOM(6)  
  WRITE (NULOUT,*) ' LONGITUDE GEO=',PDHNOM(3),&
   & ' SINUS(LATITUDE GEO)=',PDHNOM(4)  
  WRITE (NULOUT,*) ' '
  WRITE (CLDOM,'(A7,F5.0,A1,F5.0,A1,1X)')&
   & 'POINT (',PDHNOM(5),',',PDHNOM(6),')'  
ENDIF
IF ( ITYPE == 2.OR. ITYPE == 3 ) THEN
  WRITE (NULOUT,*) ' DDH * LIMITED AREA   '
  WRITE (NULOUT,*) '      ----------------'
  WRITE (NULOUT,*) ' PLANE',PDHNOM(1),' AREA    ',PDHNOM(2)
  WRITE (NULOUT,5012) PDHNOM(3), PDHNOM(4), PDHNOM(5),&
   & PDHNOM(6), PDHNOM(7), PDHNOM(8), PDHNOM(9), PDHNOM(10)  
  WRITE (NULOUT,*) ' '
  WRITE (CLDOM,'(A8,F3.0,A1,F5.0,A1,2X)')&
   & 'LIMITE (',PDHNOM(1),',',PDHNOM(2),')'  
ENDIF
WRITE (NULOUT,*) ' DDH * BUDGET AT NSTEP=',NSTEP,&
 & ' I.E. ',ZSSS,' S ',' I.E. ',&
 & ZOUT,CLOUT  
WRITE (NULOUT,*) ' '
ISHK=0
ISMC=0
ISEN=0

!     ------------------------------------------------------------------
!**   2.- CALCUL DES MOYENNES VERTICALES INDIFFERENCIEES
!        OPERATION QUI A UN SENS SUR LES VARIABLES ET TENDANCES
!      MAIS NON SUR LES FLUX !

DO JCV=1, NDHCV+NDHVV
  ZDHIV(JCV)=0.0_JPRB
ENDDO
DO JLEV=0, NFLEVG
  DO JCV=1, NDHCV+NDHVV
    ZDHIV(JCV)=ZDHIV(JCV) + PDDHCV(JLEV,JCV)
  ENDDO
ENDDO

!     ------------------------------------------------------------------
!**   3.- IMPRESSION DU BILAN MASSES, ENERGIES, SOL

IDHCV=0

IF ( LHDHKS ) THEN
  ISHK=1
!    ----------------------
!*    BILAN DE MASSE D'AIR
!    ----------------------
  IDHCV=0
  ZMAR0=ZDHIV(IDHCV+1)

  IDHCV=NDHVV
  ZMARDH=ZDHIV(IDHCV+1)

  IDHCV=NDHVV + NDHAVD
  ZMARFD0=PDDHCV(0,IDHCV+1)
  ZMARFDS=PDDHCV(NFLEVG,IDHCV+1)
  ZMARFD=ZMARFD0 - ZMARFDS

  IDHCV=NDHVV + NDHFVD
  ZMARFP0=PDDHCV(0,IDHCV+1)
  ZMARFPS=PDDHCV(NFLEVG,IDHCV+1)
  ZMARFP=ZMARFP0 - ZMARFPS

  IDHCV=NDHCV

  ZMAR1=ZDHIV(IDHCV+1)
!     BILAN
  ZBMAR=ZMAR1 - ZMAR0 - ZMARDH - ZMARFD - ZMARFP

  ZMNORM=1.E+06_JPRB

!    -------------------------------
!*    BILAN DE MASSE DE VAPEUR D'EAU
!    -------------------------------
  IDHCV=0
  ZQV0=ZDHIV(IDHCV+2)

  IDHCV=NDHVV
  ZQVDH=ZDHIV(IDHCV+2)

  IDHCV=NDHVV + NDHAVD
  ZQVFD0=PDDHCV(0,IDHCV+2)
  ZQVFDS=PDDHCV(NFLEVG,IDHCV+2)
  ZQVFD=ZQVFD0 - ZQVFDS

  IDHCV=NDHVV + NDHFVD
  ZQVFT0=PDDHCV(0,IDHCV+2)
  ZQVFTS=PDDHCV(NFLEVG,IDHCV+2)
  ZQVFT=ZQVFT0 - ZQVFTS
  ZQVFTC0=PDDHCV(0,IDHCV+3)
  ZQVFTCS=PDDHCV(NFLEVG,IDHCV+3)
  ZQVFTC=ZQVFTC0 - ZQVFTCS
  ZQTPSL0=PDDHCV(0,IDHCV+4)
  ZQTPSLS=PDDHCV(NFLEVG,IDHCV+4)
  ZQTPSL=ZQTPSL0 - ZQTPSLS
  ZQTPSN0=PDDHCV(0,IDHCV+5)
  ZQTPSNS=PDDHCV(NFLEVG,IDHCV+5)
  ZQTPSN=ZQTPSN0 - ZQTPSNS
  ZQTPCL0=PDDHCV(0,IDHCV+6)
  ZQTPCLS=PDDHCV(NFLEVG,IDHCV+6)
  ZQTPCL=ZQTPCL0 - ZQTPCLS
  ZQTPCN0=PDDHCV(0,IDHCV+7)
  ZQTPCNS=PDDHCV(NFLEVG,IDHCV+7)
  ZQTPCN=ZQTPCN0 - ZQTPCNS
  ZQVFNG0=PDDHCV(0,IDHCV+8)
  ZQVFNGS=PDDHCV(NFLEVG,IDHCV+8)
  ZQVFNG=ZQVFNG0 - ZQVFNGS

  IDHCV=NDHCV
  ZQV1=ZDHIV(IDHCV+2)
!     BILAN
  ZBQV=ZQV1 - ZQV0 - ZQVDH - ZQVFD - ZQVFT -&
   & ZQVFTC - ZQTPSL - ZQTPSN - ZQTPCL - ZQTPCN - ZQVFNG  

!    --------------------------------
!*    BILAN D'EAU LIQUIDE ET NEIGEUSE
!    --------------------------------

  IF(LHDQLN) THEN

    IDHCV=0
    ZQL0=ZDHIV(IDHCV+9)
    ZQN0=ZDHIV(IDHCV+10)
    ZQR0=ZDHIV(IDHCV+11)
    ZQS0=ZDHIV(IDHCV+12)

    IDHCV=NDHCV
    ZQL1=ZDHIV(IDHCV+9)
    ZQN1=ZDHIV(IDHCV+10)
    ZQR1=ZDHIV(IDHCV+11)
    ZQS1=ZDHIV(IDHCV+12)

    IDHCV=NDHVV
    ZQLDH=ZDHIV(IDHCV+14)
    ZQNDH=ZDHIV(IDHCV+15)
    ZQRDH=ZDHIV(IDHCV+16)
    ZQSDH=ZDHIV(IDHCV+17)

    IDHCV=NDHVV + NDHAVD
    ZQLFD=PDDHCV(0,IDHCV+11)-PDDHCV(NFLEVG,IDHCV+11)
    ZQNFD=PDDHCV(0,IDHCV+12)-PDDHCV(NFLEVG,IDHCV+12)
    ZQRFD=PDDHCV(0,IDHCV+13)-PDDHCV(NFLEVG,IDHCV+13)
    ZQSFD=PDDHCV(0,IDHCV+14)-PDDHCV(NFLEVG,IDHCV+14)

    IDHCV=NDHVV + NDHFVD
    ZQLFT=PDDHCV(0,IDHCV+27)-PDDHCV(NFLEVG,IDHCV+27)
    ZQNFT=PDDHCV(0,IDHCV+28)-PDDHCV(NFLEVG,IDHCV+28)
    ZQLFTC=PDDHCV(0,IDHCV+29)-PDDHCV(NFLEVG,IDHCV+29)
    ZQNFTC=PDDHCV(0,IDHCV+30)-PDDHCV(NFLEVG,IDHCV+30)
    ZQLFNG=PDDHCV(0,IDHCV+31)-PDDHCV(NFLEVG,IDHCV+31)
    ZQNFNG=PDDHCV(0,IDHCV+32)-PDDHCV(NFLEVG,IDHCV+32)
    ZQTCCL=PDDHCV(0,IDHCV+33)-PDDHCV(NFLEVG,IDHCV+33)
    ZQTCCN=PDDHCV(0,IDHCV+34)-PDDHCV(NFLEVG,IDHCV+34)
    ZQTCSL=PDDHCV(0,IDHCV+35)-PDDHCV(NFLEVG,IDHCV+35)
    ZQTCSN=PDDHCV(0,IDHCV+36)-PDDHCV(NFLEVG,IDHCV+36)
