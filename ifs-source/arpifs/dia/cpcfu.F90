      ZGFU(KST:KND,IFLD)=ZGFU(KST:KND,IFLD)+PFRSGNI(KST:KND)*TSTEP
    ELSEIF (IADD == YDCFU%YCFUPT%MFRSOPS) THEN
      ZGFU(KST:KND,IFLD)=ZGFU(KST:KND,IFLD)+PFRSOPS(KST:KND)*TSTEP
    ELSEIF (IADD == YDCFU%YCFUPT%MFRSOPT) THEN
      ZGFU(KST:KND,IFLD)=ZGFU(KST:KND,IFLD)+PFRSOPT(KST:KND)*TSTEP
    ELSEIF (IADD == YDCFU%YCFUPT%MFRTHDS) THEN
      ZGFU(KST:KND,IFLD)=ZGFU(KST:KND,IFLD)+PFRTHDS(KST:KND)*TSTEP
    ELSEIF (IADD == YDCFU%YCFUPT%MFRSOLU) THEN
      ZGFU(KST:KND,IFLD)=ZGFU(KST:KND,IFLD)+PFRSOLU(KST:KND)*TSTEP
    ELSEIF (IADD == YDCFU%YCFUPT%MNEBCON) THEN
      ZGFU(KST:KND,IFLD)=ZGFU(KST:KND,IFLD)+PCLCC(KST:KND)*TSTEP
    ELSEIF (IADD == YDCFU%YCFUPT%MNEBHAU) THEN
      ZGFU(KST:KND,IFLD)=ZGFU(KST:KND,IFLD)+PCLCH(KST:KND)*TSTEP
    ELSEIF (IADD == YDCFU%YCFUPT%MNEBMOY) THEN
      ZGFU(KST:KND,IFLD)=ZGFU(KST:KND,IFLD)+PCLCM(KST:KND)*TSTEP
    ELSEIF (IADD == YDCFU%YCFUPT%MNEBBAS) THEN
      ZGFU(KST:KND,IFLD)=ZGFU(KST:KND,IFLD)+PCLCL(KST:KND)*TSTEP
    ELSEIF (IADD == YDCFU%YCFUPT%MFRSOC0) THEN
      IF (MOD(NSTEP,NFRRC) == 0) THEN
        ZGFU(KST:KND,IFLD)=ZGFU(KST:KND,IFLD)+PFRSOC(KST:KND,0)*TSTEP*NFRRC
      ENDIF
    ELSEIF (IADD == YDCFU%YCFUPT%MFRSOC1) THEN
      IF (MOD(NSTEP,NFRRC) == 0) THEN
        ZGFU(KST:KND,IFLD)=ZGFU(KST:KND,IFLD)+PFRSOC(KST:KND,1)*TSTEP*NFRRC
      ENDIF
    ELSEIF (IADD == YDCFU%YCFUPT%MFRTHC0) THEN
      IF (MOD(NSTEP,NFRRC) == 0) THEN
        ZGFU(KST:KND,IFLD)=ZGFU(KST:KND,IFLD)+PFRTHC(KST:KND,0)*TSTEP*NFRRC
      ENDIF
    ELSEIF (IADD == YDCFU%YCFUPT%MFRTHC1) THEN
      IF (MOD(NSTEP,NFRRC) == 0) THEN
        ZGFU(KST:KND,IFLD)=ZGFU(KST:KND,IFLD)+PFRTHC(KST:KND,1)*TSTEP*NFRRC
      ENDIF
    ELSEIF (IADD == YDCFU%YCFUPT%MFLASH) THEN
      ZGFU(KST:KND,IFLD)=ZGFU(KST:KND,IFLD)+PFLASH(KST:KND)*TSTEP    
    ELSEIF (IADD == YDCFU%YCFUPT%MFDUTP) THEN
      DO JI=KST,KND
        IF (ZFPREC9(JI) > ZEPS) THEN
          ZGFU(JI,IFLD)=ZGFU(JI,IFLD)*((ZFPREC9(JI)+ZFPREC0(JI))**2)&
           & /((ZFPREC9(JI)**2)+(ZGFU(JI,IFLD)*ZSQPRE(JI)))  
        ELSEIF (ZFPREC0(JI) > ZEPS) THEN
          ZGFU(JI,IFLD)=TSTEP
        ELSE
          ZGFU(JI,IFLD)=0.0_JPRB
        ENDIF
      ENDDO
    ELSE
      CALL ABOR1('CPCFU : ERROR IN POINTERS') 
    ENDIF

  ENDDO
  IOFF=IOFF+TYPE_CFU(JF)%ILEV
ENDDO

