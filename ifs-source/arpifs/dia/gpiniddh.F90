SUBROUTINE GPINIDDH(YDGEOMETRY,YDMDDH,KST,KEND,&
 & KDDHI,PDHSF,PDHCV,PNEB,PCLCT,&
 & KSTGLO)  
!****
!     ------------------------------------------------------------------

!     INITIALIZING THE LOCATION & WEIGHT VECTORS.
!     DIAGNOSTICS PAR DOMAINES HORIZONTAUX
!     ------------------------------------

!       -------------------------------------------------------
!     GOAL
!     ----

!     EXPLICIT ARGUMENTS
!     ------------------
!     KDDHI(NPROMA) : DISTRIBUTION OF INTERNAL AREAS INDEXES
!     PDHSF(NPROMA) : DISTRIBUTION OF HORIZONTAL MEANS WEIGHTS
!     PDHCV(NPROMA,0:NFLEVG,NDHCVSU): VERTICAL FIELDS FOR ACCUMULATION

!     ARGUMENTS IMPLICITES
!     --------------------

!     AUTRES ENTREES
!     --------------

!     OUTPUT
!     ------

!     WRITTEN BY
!     ----------

!     MODIFICATIONS
!     -------------
!      R. El Khatib : 01-08-07 Pruning options
!     ------------------------------------------------------------------

USE GEOMETRY_MOD , ONLY : GEOMETRY
USE PARKIND1     , ONLY : JPIM, JPRB
USE YOMHOOK      , ONLY : LHOOK, DR_HOOK, JPHOOK
USE YOMMDDH      , ONLY : TMDDH

IMPLICIT NONE

TYPE(GEOMETRY)    ,INTENT(IN)    :: YDGEOMETRY
TYPE(TMDDH)       ,INTENT(INOUT) :: YDMDDH
INTEGER(KIND=JPIM),INTENT(IN)    :: KST 
INTEGER(KIND=JPIM),INTENT(IN)    :: KEND 
INTEGER(KIND=JPIM),INTENT(OUT)   :: KDDHI(YDGEOMETRY%YRDIM%NPROMA) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PDHSF(YDGEOMETRY%YRDIM%NPROMA) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PDHCV(YDGEOMETRY%YRDIM%NPROMA,0:YDGEOMETRY%YRDIMV%NFLEVG,YDMDDH%NDHCVSUN) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PNEB(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG) 
REAL(KIND=JPRB)   ,INTENT(OUT)   :: PCLCT(YDGEOMETRY%YRDIM%NPROMA) 
INTEGER(KIND=JPIM),INTENT(IN)    :: KSTGLO 

INTEGER(KIND=JPIM) :: JLEV, JROF, JSU
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('GPINIDDH',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM=>YDGEOMETRY%YRDIM,YDDIMV=>YDGEOMETRY%YRDIMV,YDGEM=>YDGEOMETRY%YRGEM, YDMP=>YDGEOMETRY%YRMP)
ASSOCIATE(NPROMA=>YDDIM%NPROMA, &
 & NFLEVG=>YDDIMV%NFLEVG, &
 & HDSF=>YDMDDH%HDSF, NDDHI=>YDMDDH%NDDHI, NDHCVSUN=>YDMDDH%NDHCVSUN)
DO JROF=KST,KEND
  KDDHI(JROF)=NDDHI(JROF+KSTGLO-1)
  PDHSF(JROF)=HDSF(JROF+KSTGLO-1)
ENDDO

!        INITIALISATION OF INTERMEDIATE COMPUTATION ARRAYS
!        IN ORDER TO MAKE THE ACCUMULATION EASIER

DO JSU=1,NDHCVSUN
  DO JROF=KST,KEND
    PDHCV(JROF,0   ,JSU)=0.0_JPRB
  ENDDO
ENDDO

DO  JLEV=1,NFLEVG
  DO JROF=KST,KEND
    PNEB(JROF,JLEV)=0.0_JPRB
  ENDDO
ENDDO

DO JROF=KST,KEND
  PCLCT(JROF)=0.0_JPRB
ENDDO

END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('GPINIDDH',1,ZHOOK_HANDLE)
END SUBROUTINE GPINIDDH

