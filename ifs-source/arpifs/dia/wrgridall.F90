SUBROUTINE WRGRIDALL(YDGEOMETRY,YDGFL,YDSURF,YDCFU,YDXFU,YDMODEL,YDFACTX,YDFLAG,CDFIC)

!**** *WRGRIDALL*  - IO of all grid-point data

!     Author. 
!     ------- 
!      Philippe Marguinaud *METEO FRANCE*
!      Original : 01-01-2011

!      Modifications :
!      P.Marguinaud : 18-06-2012 : Refactor
!      P.Marguinaud : 11-09-2012 : Use MTAG_WRGRIDALL tag + IOFLDDESC_MOD
!      P.Marguinaud : 10-10-2013 : Use FACTX
!      O. Marsden: June 2015 CY42 YRGMV, YRGFL, YRSURF, YRGMV5, and YRGFL5 are now passed by argument


USE TYPE_MODEL         , ONLY : MODEL
USE GEOMETRY_MOD       , ONLY : GEOMETRY
USE SURFACE_FIELDS_MIX , ONLY : TSURF
USE YOMGFL             , ONLY : TGFL
USE YOMCFU             , ONLY : TCFU
USE YOMXFU             , ONLY : TXFU
USE PARKIND1           , ONLY : JPRB, JPIM
USE YOMHOOK            , ONLY : LHOOK, DR_HOOK, JPHOOK
USE IOFU_MOD           , ONLY : IOFU_COUNT,     IOFU_SELECTD,     NIOFUACT_WRITE
USE IOGRIDA_MOD        , ONLY : IOGRIDA_COUNT,  IOGRIDA_SELECTD,  NIOGRIDACT_WRITE
USE IOGRIDUA_MOD       , ONLY : IOGRIDUA_COUNT, IOGRIDUA_SELECTD, NIOGRIDUACT_WRITE
USE IOXFU_MOD          , ONLY : IOXFU_COUNT,    IOXFU_SELECTD,    NIOXFUACT_WRITE
USE IOGRID_MOD         , ONLY :                 IOGRID_SELECTF,   NIOGRIDCT_WRITE
USE YOMTAG             , ONLY : MTAG_MFIO_WRGRIDALL
USE YOMFP_SERV         , ONLY : FP_SERV_C001

USE IOFLDPTR_MOD       , ONLY : IOFLDPTR
USE MFIOOPTS_MOD       , ONLY : MFIOFLAG

USE FACTX_MOD          , ONLY : FACTX

IMPLICIT NONE

TYPE (GEOMETRY)  ,INTENT (IN)    :: YDGEOMETRY
TYPE (TGFL)      ,INTENT (INOUT) :: YDGFL
TYPE (TSURF)     ,INTENT (INOUT) :: YDSURF
TYPE(TCFU)       ,INTENT (INOUT) :: YDCFU
TYPE(TXFU)       ,INTENT (INOUT) :: YDXFU
TYPE(MODEL)      ,INTENT (INOUT) :: YDMODEL
TYPE (FACTX)     ,INTENT (INOUT) :: YDFACTX
TYPE (MFIOFLAG)  ,INTENT (IN)    :: YDFLAG
CHARACTER(LEN=*),INTENT(IN)      :: CDFIC

#include "wrgp2fa.intfb.h"
#include "wrgridall_fp.intfb.h"

REAL (KIND=JPRB), ALLOCATABLE :: ZREAL(:,:)
TYPE (IOFLDPTR),  ALLOCATABLE :: YLFLDGT (:) 
INTEGER (KIND=JPIM)           :: IFNUM
INTEGER (KIND=JPIM)           :: IFCUR

REAL (KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK ('WRGRIDALL',0,ZHOOK_HANDLE)

IF (FP_SERV_C001%LFP_CLIENT) THEN
  CALL WRGRIDALL_FP(FP_SERV_C001,YDGEOMETRY,YDGFL,YDSURF,YDCFU,YDXFU,YDMODEL,YDFLAG)
  IF (.NOT. FP_SERV_C001%LFP_CLIENT_WRITE) THEN
    IF (LHOOK) CALL DR_HOOK ('WRGRIDALL',1,ZHOOK_HANDLE)
    RETURN
  ENDIF
ENDIF

IFNUM = 0
IF (YDFLAG%LLWRFU)     CALL IOFU_COUNT(YDGEOMETRY,YDCFU,NIOFUACT_WRITE,IFNUM)
IF (YDFLAG%LLWRGRIDA)  CALL IOGRIDA_COUNT(YDGEOMETRY,YDSURF,YDMODEL%YRML_AOC%YRMCC,YDMODEL%YRML_PHY_G%YRDPHY, &
 & YDMODEL%YRML_PHY_MF%YRPHY,YDMODEL%YRML_PHY_MF%YRPHYDS,NIOGRIDACT_WRITE,IFNUM)
IF (YDFLAG%LLWRGRIDUA) CALL IOGRIDUA_COUNT(YDGEOMETRY,YDGFL,YDMODEL%YRML_GCONF%YGFL,NIOGRIDUACT_WRITE,IFNUM)
IF (YDFLAG%LLWRXFU)    CALL IOXFU_COUNT(YDGEOMETRY,YDXFU,NIOXFUACT_WRITE,IFNUM)

IF (IFNUM > 0) THEN

  ALLOCATE (YLFLDGT (IFNUM))

  IFCUR = 0
  IF (YDFLAG%LLWRFU)     CALL IOFU_SELECTD(YDGEOMETRY, YDCFU,  NIOFUACT_WRITE,    YLFLDGT, IFCUR)
  IF (YDFLAG%LLWRGRIDA)  CALL IOGRIDA_SELECTD(YDGEOMETRY, YDSURF,YDMODEL%YRML_AOC%YRMCC, YDMODEL%YRML_PHY_G%YRDPHY, &
 & YDMODEL%YRML_PHY_MF%YRPHY, YDMODEL%YRML_PHY_MF%YRPHYDS, NIOGRIDACT_WRITE,  YLFLDGT, IFCUR)
  IF (YDFLAG%LLWRGRIDUA) CALL IOGRIDUA_SELECTD(YDGEOMETRY, YDGFL, YDMODEL%YRML_GCONF%YGFL, NIOGRIDUACT_WRITE, YLFLDGT, IFCUR)
  IF (YDFLAG%LLWRXFU)    CALL IOXFU_SELECTD(YDGEOMETRY, YDXFU,  NIOXFUACT_WRITE,   YLFLDGT, IFCUR)

  ALLOCATE (ZREAL (YDGEOMETRY%YRGEM%NGPTOT, IFNUM))

  CALL IOGRID_SELECTF (YDGEOMETRY, NIOGRIDCT_WRITE, ZREAL, YLFLDGT)

  CALL WRGP2FA(YDGEOMETRY,YDMODEL%YRML_GCONF%YRRIP,IFNUM,ZREAL,YLFLDGT%YFLDDSC,YDFACTX, &
 & CDFIC,KTAG=MTAG_MFIO_WRGRIDALL)

  DEALLOCATE (ZREAL)

  DEALLOCATE (YLFLDGT)

ENDIF

IF (LHOOK) CALL DR_HOOK ('WRGRIDALL',1,ZHOOK_HANDLE)

END SUBROUTINE WRGRIDALL

