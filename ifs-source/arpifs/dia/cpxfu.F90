SUBROUTINE MEANWIND(KMEANSTEPS,KXU,KXV,PXU,PXV,PMWINDCALC,ZUM,ZVM)
! The algorithm is the same as the one used to compute observed mean wind

INTEGER(KIND=JPIM) ,INTENT(IN) :: KMEANSTEPS
INTEGER(KIND=JPIM) ,INTENT(IN) :: KXU ,KXV
REAL(KIND=JPRB) ,INTENT(IN) ,DIMENSION(KST:KND) :: PXU ,PXV
REAL(KIND=JPRB) ,INTENT(INOUT) :: PMWINDCALC(KST+KSTGLO-KST:KND+KSTGLO-KST)
REAL(KIND=JPRB) ,INTENT(OUT) ,DIMENSION(KST:KND) :: ZUM ,ZVM

REAL(KIND=JPRB) :: ZMEANMODULE(KST:KND)
REAL(KIND=JPRB) :: ZMEANANGLE
REAL(KIND=JPRB) :: ZANGLE
REAL(KIND=JPRB) :: ZMEANCANGLE
REAL(KIND=JPRB) :: ZMEANSANGLE
REAL(KIND=JPRB) :: ZEPS

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('CPXFU:MEANWIND',0,ZHOOK_HANDLE)

ZEPS=1.E-12
IF (KMEANSTEPS==1) THEN
  ZUM(KST:KND)=PXU(KST:KND)
  ZVM(KST:KND)=PXV(KST:KND)
  PMWINDCALC(KST+KSTGLO-KST:KND+KSTGLO-KST)=1.0_JPRB
ELSE
  DO J=KST,KND
    ! Module of mean wind is the mean of the modules
    ZMEANMODULE(J)=SQRT(ZGFU(J,KXU)**2+ZGFU(J,KXV)**2)
  ENDDO
  DO J=KST,KND
    ! Module of mean wind is the mean of the modules
    ZMEANMODULE(J)= ZMEANMODULE(J)*REAL(KMEANSTEPS-1,JPRB) + SQRT(PXU(J)**2+PXV(J)**2)
  ENDDO
  DO J=KST,KND
    ! We compute mean of COS and mean of SIN
    ! Direction of mean wind is the direction of vector [mean COS, mean SIN]
    IF (ABS(ZGFU(J,KXV))<ZEPS.AND.ABS(ZGFU(J,KXU))<ZEPS) THEN
      ZMEANANGLE=0.0_JPRB
    ELSE
      ZMEANANGLE=ATAN2(ZGFU(J,KXV),ZGFU(J,KXU))
    ENDIF
    IF (ABS(PXV(J))<ZEPS.AND.ABS(PXU(J))<ZEPS) THEN
      ZANGLE=0.0_JPRB
    ELSE
      ZANGLE=ATAN2(PXV(J),PXU(J))
    ENDIF
    ZMEANCANGLE=( COS(ZMEANANGLE)*REAL(KMEANSTEPS-1,JPRB) * &
     & PMWINDCALC(J+KSTGLO-KST)+ &
     & COS(ZANGLE) ) / REAL(KMEANSTEPS,JPRB)
    ZMEANSANGLE=( SIN(ZMEANANGLE)*REAL(KMEANSTEPS-1,JPRB) * &
     & PMWINDCALC(J+KSTGLO-KST)+ &
     & SIN(ZANGLE) ) / REAL(KMEANSTEPS,JPRB)
    PMWINDCALC(J+KSTGLO-KST)=SQRT(ZMEANCANGLE**2+ZMEANSANGLE**2)
    IF (ABS(ZMEANSANGLE)<ZEPS.AND.ABS(ZMEANCANGLE)<ZEPS) THEN
      ZMEANANGLE=0.0_JPRB
    ELSE
      ZMEANANGLE=ATAN2(ZMEANSANGLE,ZMEANCANGLE)
    ENDIF
    ZUM(J)=ZMEANMODULE(J)*COS(ZMEANANGLE)/ REAL(KMEANSTEPS,JPRB)
    ZVM(J)=ZMEANMODULE(J)*SIN(ZMEANANGLE)/ REAL(KMEANSTEPS,JPRB)
  ENDDO
ENDIF

IF (LHOOK) CALL DR_HOOK('CPXFU:MEANWIND',1,ZHOOK_HANDLE)

END SUBROUTINE MEANWIND 

