SUBROUTINE PPFIDH(YDDIMV,YDSURF,YDDPHY,YDML_DIAG,YDPHY,PDHNOM,PDDHCV,PDDHCS,KNUM,PDDHCV_TOT,PDDHCS_TOT,KDIMDOM)
!-----------------------------------------------------------------------
!**** PPFIDH * ECRITURE DES RESULTATS POUR UN DOMAINE SUR UN FICHIER
!              DIAGNOSTICS PAR DOMAINES HORIZONTAUX
!              Write results for one domain on a DDH file.
!-----------------------------------------------------------------------
!     BUT
!     ---
!      TRANSFERT DES RESULTATS OBTENUS POUR UN DOMAINE IDENTIFIE
!     PAR PDHNOM SUR UN FICHIER LFA

!     ARGUMENTS D'ENTREE
!     ------------------
!      PDHNOM(11) : IDENTIFICATION DU DOMAINE
!                  PDHNOM(11) EST LE TYPE DE DOMAINE
!      PDDHCV(0:NFLEVG,NDHCV+NDHVV) : PROFILS VERTICAUX
!                  LA STRUCTURE DU TABLEAU EST RESUMEE DANS
!                  PPSYDH, SINON VOIR YOMTDDH ET CPDYDDH/CPPHDDH
!      PDDHCS(NDHCV+NDHVV) : SOL
!      KNUM : NUMERO DU DOMAINE DANS UNE SERIE
!             (PAR EXEMPLE ENSEMBLE DES BANDES DE LATITUDE)
!      KDIMDOM : NOMBRE DE DOMAINES

!     ARGUMENTS DE SORTIE
!     -------------------
!     FICHIER LFA

!     ORIGINAL
!     --------
!     91-02-01, Alain Joly.

!     MODIFICATIONS
!     -------------
!      M.Hamrud      01-Oct-2003 CY28 Cleaning
!      05-06-20, F. Bouyssel: Lopez's prognostic scheme
!      2005-04-16, T. Kovacic: AROME DDH.
!      06-01-25, F. Bouyssel: Change name of Lopez's water fluxes
!      2006-08-22, J.M. Piriou: write DDH on LFA files.
!      01-oct-08 O.Riviere introduction of new ddh dataflow in Arome; removal of old one
!      K. Yessad (Dec 2008): cleanings, remove tabulations.
!      2008-01-05, J.M. Piriou: cleaning: suppress use of NPHY, correct LHDQLN bug.
!      2011-05-19, R. Brozkova: temporary fix of ALARO ddh.
!      2011-10-31  M. Ahlgrimm: add rain, snow and PEXTRA to DDH output
!      K. Yessad (July 2014): Move some variables.
!      2019-09-24, J.M. Piriou: authorize more than 1000 domains.
!      M. Hrastinski Sep-2019: TKE and TTE terms for ALARO DDH
!-----------------------------------------------------------------------

USE MODEL_DIAGNOSTICS_MOD , ONLY : MODEL_DIAGNOSTICS_TYPE
USE YOMPHY                , ONLY : TPHY, JPBP002
USE YOMDIMV               , ONLY : TDIMV
USE SURFACE_FIELDS_MIX    , ONLY : TSURF
USE PARKIND1              , ONLY : JPIM     ,JPRB
USE YOMHOOK               , ONLY : LHOOK    ,DR_HOOK, JPHOOK
USE YOMLUN                , ONLY : NULOUT   ,NPODDH
USE YOMMP0                , ONLY : NPRINTLEV
USE YOMCT0                , ONLY : LECMWF
USE YOMDPHY               , ONLY : TDPHY
USE DDH_MIX               , ONLY : NTOTFIELD,RDDH_DESCR,NTOTSURF,RDDHSURF_DESCR,&
&                                  NTOTSVAR, NTOTSVFS
USE YOMPHFT               , ONLY : NDDHFT, YAPFT

!-----------------------------------------------------------------------

IMPLICIT NONE

TYPE(TDIMV)                 , INTENT(IN)    :: YDDIMV
TYPE(TSURF)                 , INTENT(INOUT) :: YDSURF
TYPE(TDPHY)                 , INTENT(IN)    :: YDDPHY
TYPE(MODEL_DIAGNOSTICS_TYPE), INTENT(INOUT) :: YDML_DIAG
TYPE(TPHY)                  , INTENT(INOUT) :: YDPHY
INTEGER(KIND=JPIM)          , INTENT(IN)    :: KNUM, KDIMDOM
REAL(KIND=JPRB)             , INTENT(IN)    :: PDHNOM(11)
REAL(KIND=JPRB)             , INTENT(IN)    :: PDDHCV(0:YDDIMV%NFLEVG,YDML_DIAG%YRMDDH%NDHCVSU+YDML_DIAG%YRMDDH%NDHVV)
REAL(KIND=JPRB)             , INTENT(IN)    :: PDDHCS(YDML_DIAG%YRMDDH%NDHCSSU+YDML_DIAG%YRMDDH%NDHVS)
REAL(KIND=JPRB)             , INTENT(IN)    :: PDDHCV_TOT((YDDIMV%NFLEVG+1)*KDIMDOM*(YDML_DIAG%YRMDDH%NDHCVSU+ &
 &                                                        YDML_DIAG%YRMDDH%NDHVV))
REAL(KIND=JPRB)             , INTENT(IN)    :: PDDHCS_TOT(KDIMDOM*(YDML_DIAG%YRMDDH%NDHCSSU+YDML_DIAG%YRMDDH%NDHVS))
!-----------------------------------------------------------------------
INTEGER(KIND=JPIM) :: JVAR, JFLX, JFIELD
REAL(KIND=JPRB) :: ZEROV(YDDIMV%NFLEVG)
REAL(KIND=JPRB) :: ZEROF(0:YDDIMV%NFLEVG)
REAL(KIND=JPRB) :: ZMAR0 , ZMAR1
CHARACTER :: CLNOMA*16
CHARACTER :: CL0*10, CL1*10, CL2*2, CLDIVOR*10, CLFLUVD*10
CHARACTER :: CLFLUPL*10
CHARACTER(LEN=200) :: CLNUM
INTEGER(KIND=JPIM) :: IDHCS, IDHCV, INBCUMS, JJPASS, JLEV, IPOINT,IVAR, JI
INTEGER(KIND=JPIM) :: IPLFIS, IPLVAS, IPLVFS, IVARS
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!-----------------------------------------------------------------------

#include "addpgr.intfb.h"
#include "wrifdh.intfb.h"

!-----------------------------------------------------------------------
IF (LHOOK) CALL DR_HOOK('PPFIDH',0,ZHOOK_HANDLE)
ASSOCIATE(YDCDDH=>YDML_DIAG%YRCDDH,YDLDDH=>YDML_DIAG%YRLDDH,YDMDDH=>YDML_DIAG%YRMDDH, &
 & YDSDDH=>YDML_DIAG%YRSDDH,YDTDDH=>YDML_DIAG%YRTDDH )
ASSOCIATE(NFLEVG=>YDDIMV%NFLEVG, &
 & NCSI=>YDDPHY%NCSI, NCSNEC=>YDDPHY%NCSNEC, NTILES=>YDDPHY%NTILES, &
 & LFLEXDIA=>YDLDDH%LFLEXDIA, LHDENT=>YDLDDH%LHDENT, LHDHKS=>YDLDDH%LHDHKS, &
 & LHDLFA=>YDLDDH%LHDLFA, LHDLIST=>YDLDDH%LHDLIST, LHDMCI=>YDLDDH%LHDMCI, &
 & LONLYVAR=>YDLDDH%LONLYVAR, LDDH_OMP=>YDLDDH%LDDH_OMP, &
 & NDHAHKD=>YDMDDH%NDHAHKD, NDHAHKP=>YDMDDH%NDHAHKP, NDHAMCD=>YDMDDH%NDHAMCD, &
 & NDHAVD=>YDMDDH%NDHAVD, NDHAVP=>YDMDDH%NDHAVP, NDHBHKD=>YDMDDH%NDHBHKD, &
 & NDHBHKP=>YDMDDH%NDHBHKP, NDHBMCD=>YDMDDH%NDHBMCD, NDHBMCP=>YDMDDH%NDHBMCP, &
 & NDHCS=>YDMDDH%NDHCS, NDHCSSU=>YDMDDH%NDHCSSU, NDHCV=>YDMDDH%NDHCV, &
 & NDHCVSU=>YDMDDH%NDHCVSU, NDHFFS=>YDMDDH%NDHFFS, NDHFIIS=>YDMDDH%NDHFIIS, &
 & NDHFSD=>YDMDDH%NDHFSD, NDHFSP=>YDMDDH%NDHFSP, NDHFSSS=>YDMDDH%NDHFSSS, &
 & NDHFTIS=>YDMDDH%NDHFTIS, NDHFTLS=>YDMDDH%NDHFTLS, NDHFTSS=>YDMDDH%NDHFTSS, &
 & NDHFTTS=>YDMDDH%NDHFTTS, NDHFVD=>YDMDDH%NDHFVD, NDHFWLS=>YDMDDH%NDHFWLS, &
 & NDHVFS=>YDMDDH%NDHVFS, NDHVHK=>YDMDDH%NDHVHK, NDHVIIS=>YDMDDH%NDHVIIS, &
 & NDHVMC=>YDMDDH%NDHVMC, NDHVS=>YDMDDH%NDHVS, NDHVSSS=>YDMDDH%NDHVSSS, &
 & NDHVTIS=>YDMDDH%NDHVTIS, NDHVTLS=>YDMDDH%NDHVTLS, NDHVTSS=>YDMDDH%NDHVTSS, &
 & NDHVTTS=>YDMDDH%NDHVTTS, NDHVV=>YDMDDH%NDHVV, NDHVWLS=>YDMDDH%NDHVWLS, &
 & LHDPAS=>YDSDDH%LHDPAS, LHDQLN=>YDSDDH%LHDQLN, NHDPASVA=>YDSDDH%NHDPASVA, &
 & NHDQLNVA=>YDSDDH%NHDQLNVA, &
 & YSP_SBD=>YDSURF%YSP_SBD, &
 & L3MT=>YDPHY%L3MT, LCONDWT=>YDPHY%LCONDWT, LPROCLD=>YDPHY%LPROCLD, &
 & LSTRAPRO=>YDPHY%LSTRAPRO, NPHY=>YDPHY%NPHY, LPTKE=>YDPHY%LPTKE, &
 & LCOEFK_PTTE=>YDPHY%LCOEFK_PTTE)
!-----------------------------------------------------------------------

CLNOMA = ' '
CL0     = '0         '
CL1     = '1         '
CLDIVOR = 'DIVFLUHOR '
CLFLUVD = 'FLUVERTDYN'
CLFLUPL = 'FLUDUAPLUI'

DO JLEV=1,NFLEVG
  ZEROV(JLEV)=0.0_JPRB
ENDDO
DO JLEV=0,NFLEVG
  ZEROF(JLEV)=0.0_JPRB
ENDDO

ZMAR0=0._JPRB
DO JLEV=0, NFLEVG
  ZMAR0=ZMAR0+ PDDHCV(JLEV,1)
ENDDO

ZMAR1=0._JPRB
DO JLEV=0, NFLEVG
  ZMAR1=ZMAR1+ PDDHCV(JLEV,NDHCV+1)
ENDDO


!     ------------------------------------------------------------------
!*    1.- ARTICLE DOCUMENTAIRE
IF (NPRINTLEV >= 1) THEN
  WRITE (NULOUT,*) ' DDH * PPFIDH KNUM = ',KNUM,' NPODDH = ',NPODDH
ENDIF

IF(LHDLFA) THEN
  ! ON ECRIT SUR FICHIER LFA UN ARTICLE DOCD PAR DOMAINE.
  IF(KNUM < 1000) THEN
    WRITE (CLNOMA,'(''DOCD'',I3.3)') KNUM
  ELSE
    WRITE(CLNUM,FMT=*) KNUM
    WRITE(CLNOMA,FMT='(2A)') 'DOCD',TRIM(ADJUSTL(CLNUM))
  ENDIF
  CALL LFAECRR(NPODDH,CLNOMA,PDHNOM,11)
ELSE
  CALL ADDPGR('DOCD',KNUM,PDHNOM,11)
ENDIF
! ON N'ECRIT SUR FICHIER LFA QU'UN ARTICLE SCIENTIFIQUE
! POUR TOUS LES DOMAINES. ON N'ECRIT DONC CI-DESSOUS
! L'ARTICLE SCIENTIFIQUE QUE SI LE DOMAINE COURANT EST LE DERNIER.

IF(.NOT.LHDLFA .OR. KNUM == KDIMDOM) THEN 

  IF (LFLEXDIA) THEN
    IF (LDDH_OMP) THEN
      IVAR=0
      DO JFIELD=1,NTOTFIELD
        IPOINT=JFIELD
        SELECT CASE(YDTDDH%CFLDTYPES3D(JFIELD))
        CASE('V')
          WRITE (CLNOMA,4000) KNUM,YDTDDH%CFLDNAMES3D(JFIELD),CL1
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          (1+NFLEVG)*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),-999)

          WRITE (CLNOMA,4000) KNUM,YDTDDH%CFLDNAMES3D(JFIELD),CL0
          IVAR=IVAR+1
          IPOINT=IVAR+NTOTFIELD
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          (1+NFLEVG)*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),-999)

        CASE('T')
          WRITE (CLNOMA,6000) KNUM,YDTDDH%CFLDNAMES3D(JFIELD),' '
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          (1+NFLEVG)*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),-999)

        CASE('F')
          WRITE (CLNOMA,6000) KNUM,YDTDDH%CFLDNAMES3D(JFIELD),' '
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          (1+NFLEVG)*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),1+NFLEVG)

        END SELECT
      ENDDO
    ELSE
      IVAR=0
      DO JFIELD=1,NTOTFIELD
        IPOINT=JFIELD
        SELECT CASE(RDDH_DESCR(JFIELD)%CFLUX)
        CASE('V')
          WRITE (CLNOMA,4000) KNUM,RDDH_DESCR(JFIELD)%CNAME,CL1    
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          (1+NFLEVG)*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),-999)
       
          WRITE (CLNOMA,4000) KNUM,RDDH_DESCR(JFIELD)%CNAME,CL0 
          IVAR=IVAR+1
          IPOINT=IVAR+NTOTFIELD
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          (1+NFLEVG)*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),-999)

        CASE('T')
          WRITE (CLNOMA,6000) KNUM,RDDH_DESCR(JFIELD)%CNAME,' '    
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          (1+NFLEVG)*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),-999)

        CASE('F')
          WRITE (CLNOMA,6000) KNUM,RDDH_DESCR(JFIELD)%CNAME,' '     
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          (1+NFLEVG)*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),1+NFLEVG)

        END SELECT
      ENDDO
    ENDIF
  ELSE !LFLEXDIA

    !     ------------------------------------------------------------------
    !*    2.- ARTICLES DE VARIABLES

    IDHCV=0

    !     INSTANT INITIAL
    !     ---------------
    IF ( LHDHKS ) THEN
      IDHCV=0
      WRITE (CLNOMA,5000) KNUM,'V','PP',CL0
      IPOINT=IDHCV+1
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      WRITE (CLNOMA,5000) KNUM,'V','QV',CL0
      IPOINT=IDHCV+2
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      WRITE (CLNOMA,5000) KNUM,'V','UU',CL0
      IPOINT=IDHCV+3
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      WRITE (CLNOMA,5000) KNUM,'V','VV',CL0
      IPOINT=IDHCV+4
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      WRITE (CLNOMA,5000) KNUM,'V','KK',CL0
      IPOINT=IDHCV+5
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      WRITE (CLNOMA,5000) KNUM,'V','CT',CL0
      IPOINT=IDHCV+6
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      WRITE (CLNOMA,5000) KNUM,'V','EP',CL0
      IPOINT=IDHCV+7
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      WRITE (CLNOMA,5000) KNUM,'V','HR',CL0
      IPOINT=IDHCV+8
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      IF(LHDQLN) THEN
        WRITE (CLNOMA,5000) KNUM,'V','QL',CL0
        IPOINT=IDHCV+9
        CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
         &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

        WRITE (CLNOMA,5000) KNUM,'V','QN',CL0
        IPOINT=IDHCV+10
        CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
         &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)
        WRITE (CLNOMA,5000) KNUM,'V','QR',CL0
        IPOINT=IDHCV+11
        CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
         &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)
        
        WRITE (CLNOMA,5000) KNUM,'V','QS',CL0
        IPOINT=IDHCV+12
        CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
         &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)
      ENDIF

      IF(LHDPAS) THEN
        DO JJPASS=1,NHDPASVA
          WRITE(CL2,'(I2.2)') JJPASS
          WRITE (CLNOMA,5000) KNUM,'V',CL2,CL0
          IPOINT=IDHCV+9+NHDQLNVA+JJPASS-1
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)
        ENDDO
      ENDIF

      WRITE (CLNOMA,5000) KNUM,'V','NT',CL0
      IPOINT=IDHCV+9+NHDQLNVA+NHDPASVA
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      WRITE (CLNOMA,5000) KNUM,'V','OM',CL0
      IPOINT=IDHCV+10+NHDQLNVA+NHDPASVA
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      ! TKE (ALARO)
      IF (LPTKE) THEN
        WRITE (CLNOMA,5000) KNUM,'V','TK',CL0
        IPOINT=IDHCV+10+NHDQLNVA+NHDPASVA+1
        CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
         &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

        ! TTE (ALARO) 
        IF (LCOEFK_PTTE) THEN
          WRITE (CLNOMA,5000) KNUM,'V','TT',CL0
          IPOINT=IDHCV+10+NHDQLNVA+NHDPASVA+2
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)
        ENDIF

      ENDIF

    ENDIF

    IF ( LHDMCI ) THEN
      IDHCV=NDHVHK
      WRITE (CLNOMA,5000) KNUM,'V','A1',CL0
      IPOINT=IDHCV+1
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      WRITE (CLNOMA,5000) KNUM,'V','A2',CL0
      IPOINT=IDHCV+2
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      WRITE (CLNOMA,5000) KNUM,'V','A3',CL0
      IPOINT=IDHCV+3
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)
    ENDIF

    IF ( LHDENT ) THEN
      IDHCV=NDHVHK+NDHVMC
      WRITE (CLNOMA,5000) KNUM,'V','SS',CL0
      IPOINT=IDHCV+1
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)
    ENDIF

    !     INSTANT FINAL
    !     -------------
    IF ( LHDHKS ) THEN

      IDHCV=NDHCV
      WRITE (CLNOMA,5000) KNUM,'V','PP',CL1
      IPOINT=IDHCV+1
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      WRITE (CLNOMA,5000) KNUM,'V','QV',CL1
      IPOINT=IDHCV+2
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      WRITE (CLNOMA,5000) KNUM,'V','UU',CL1
      IPOINT=IDHCV+3
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      WRITE (CLNOMA,5000) KNUM,'V','VV',CL1
      IPOINT=IDHCV+4
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      WRITE (CLNOMA,5000) KNUM,'V','KK',CL1
      IPOINT=IDHCV+5
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      WRITE (CLNOMA,5000) KNUM,'V','CT',CL1
      IPOINT=IDHCV+6
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      WRITE (CLNOMA,5000) KNUM,'V','EP',CL1
      IPOINT=IDHCV+7
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

