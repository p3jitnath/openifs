      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(2+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          (NFLEVG+1)*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      WRITE (CLNOMA,5000) KNUM,'T','SS','RAY1      '
      IPOINT=IDHCV+5
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(2+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          (NFLEVG+1)*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)

      WRITE (CLNOMA,5000) KNUM,'T','SS','TURCTTOT  '
      IPOINT=IDHCV+6
      CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(2+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
       &          (NFLEVG+1)*KDIMDOM,NPODDH,CLNOMA,PDDHCV(1,IPOINT),NFLEVG)
    ENDIF

  ENDIF !FLEXDIA

    !     ------------------------------------------------------------------
    !*    5.- SOL

    IDHCS = 0
    INBCUMS = NDHFSD + NDHFSP

    IF ( LHDHKS .AND. ((NDHVS /= 0) .OR. (NDHCS /= 0)) ) THEN

      IF(LECMWF) THEN

        ! Write initial values of all standard variables

        ! Tiles

        DO JVAR=1,NDHVTLS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHTLS(JVAR))//'0'
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),NTILES)
          IDHCS=IDHCS+NTILES
        ENDDO

        IDHCS=NDHVTLS*NTILES

        ! Snow energy budget

        DO JVAR=1,NDHVTSS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHTSS(JVAR))//'0'
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),NCSNEC)
          IDHCS=IDHCS+NCSNEC
        ENDDO

        IDHCS=NDHVTLS*NTILES+NDHVTSS*NCSNEC

        ! Soil energy budget

        DO JVAR=1,NDHVTTS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHTTS(JVAR))//'0'
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),YSP_SBD%NLEVS)
          IDHCS=IDHCS+YSP_SBD%NLEVS
        ENDDO

        IDHCS=NDHVTLS*NTILES+NDHVTSS*NCSNEC+NDHVTTS*YSP_SBD%NLEVS

        ! Sea-ice energy budget

        DO JVAR=1,NDHVTIS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHTIS(JVAR))//'0'
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),NCSI)
          IDHCS=IDHCS+NCSI
        ENDDO

        IDHCS=NDHVTLS*NTILES+NDHVTSS*NCSNEC+NDHVTTS*YSP_SBD%NLEVS+&
         & NDHVTIS*NCSI

        ! Snow water budget

        DO JVAR=1,NDHVSSS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHSSS(JVAR))//'0'
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),NCSNEC)
          IDHCS=IDHCS+NCSNEC
        ENDDO

        IDHCS=NDHVTLS*NTILES+NDHVTSS*NCSNEC+NDHVTTS*YSP_SBD%NLEVS+&
         & NDHVTIS*NCSI+NDHVSSS*NCSNEC

        ! Interception layer water budget

        DO JVAR=1,NDHVIIS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHIIS(JVAR))//'0'
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),1)
          IDHCS=IDHCS+1
        ENDDO

        IDHCS=NDHVTLS*NTILES+NDHVTSS*NCSNEC+NDHVTTS*YSP_SBD%NLEVS+&
         & NDHVTIS*NCSI+NDHVSSS*NCSNEC+NDHVIIS

        ! Soil water budget

        DO JVAR=1,NDHVWLS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHWLS(JVAR))//'0'
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),YSP_SBD%NLEVS)
          IDHCS=IDHCS+YSP_SBD%NLEVS
        ENDDO

        !-------------------------------------------------------------------------

        ! Write current values of all standard variables

        ! set pointer

        IDHCS = NDHCS

        ! Tiles

        DO JVAR=1,NDHVTLS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHTLS(JVAR))//'1'
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),NTILES)
          IDHCS=IDHCS+NTILES
        ENDDO

        IDHCS=NDHCS+NDHVTLS*NTILES

        ! Snow energy budget

        DO JVAR=1,NDHVTSS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHTSS(JVAR))//'1'
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),NCSNEC)
          IDHCS=IDHCS+NCSNEC
        ENDDO

        IDHCS=NDHCS+NDHVTLS*NTILES+NDHVTSS*NCSNEC

        ! Soil energy budget

        DO JVAR=1,NDHVTTS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHTTS(JVAR))//'1'
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),YSP_SBD%NLEVS)
          IDHCS=IDHCS+YSP_SBD%NLEVS
        ENDDO

        IDHCS=NDHCS+NDHVTLS*NTILES+NDHVTSS*NCSNEC+NDHVTTS*YSP_SBD%NLEVS

        ! Sea-ice energy budget

        DO JVAR=1,NDHVTIS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHTIS(JVAR))//'1'
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),NCSI)
          IDHCS=IDHCS+NCSI
        ENDDO

        IDHCS=NDHCS+NDHVTLS*NTILES+NDHVTSS*NCSNEC+NDHVTTS*YSP_SBD%NLEVS+&
         & NDHVTIS*NCSI

        ! Snow water budget

        DO JVAR=1,NDHVSSS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHSSS(JVAR))//'1'
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),NCSNEC)
          IDHCS=IDHCS+NCSNEC
        ENDDO

        IDHCS=NDHCS+NDHVTLS*NTILES+NDHVTSS*NCSNEC+NDHVTTS*YSP_SBD%NLEVS+&
         & NDHVTIS*NCSI+NDHVSSS*NCSNEC

        ! Interception layer water budget

        DO JVAR=1,NDHVIIS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHIIS(JVAR))//'1'
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),1)
          IDHCS=IDHCS+1
        ENDDO

        IDHCS=NDHCS+NDHVTLS*NTILES+NDHVTSS*NCSNEC+NDHVTTS*YSP_SBD%NLEVS+&
         & NDHVTIS*NCSI+NDHVSSS*NCSNEC+NDHVIIS

        ! Soil water budget

        DO JVAR=1,NDHVWLS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHWLS(JVAR))//'1'
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),YSP_SBD%NLEVS)
          IDHCS=IDHCS+YSP_SBD%NLEVS
        ENDDO

        !-------------------------------------------------------------------------

        ! Write free-style variables

        IF (NDHVFS /= 0) THEN

          ! Set pointer

          IDHCS = NDHVS

          ! Write

          DO JVAR=1,NDHVFS
            WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHFSS(JVAR))
            CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
             &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),1)
            IDHCS=IDHCS+1
          ENDDO

        ENDIF
        !-------------------------------------------------------------------------

        ! Write standard fluxes

        ! Set pointer

        IDHCS = NDHVS + NDHVFS

        ! Tiles

        DO JFLX=NDHVTLS+1,NDHVTLS+NDHFTLS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHTLS(JFLX))
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),NTILES)
          IDHCS=IDHCS+NTILES
        ENDDO

        IDHCS=NDHVS+NDHVFS+NDHFTLS*NTILES

        ! Snow energy budget

        DO JFLX=NDHVTSS+1,NDHVTSS+NDHFTSS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHTSS(JFLX))
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),NCSNEC)
          IDHCS=IDHCS+NCSNEC
        ENDDO

        IDHCS=NDHVS+NDHVFS+NDHFTLS*NTILES+NDHFTSS*NCSNEC

        ! Soil energy budget

        DO JFLX=NDHVTTS+1,NDHVTTS+NDHFTTS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHTTS(JFLX))
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),YSP_SBD%NLEVS)
          IDHCS=IDHCS+YSP_SBD%NLEVS
        ENDDO

        IDHCS=NDHVS+NDHVFS+NDHFTLS*NTILES+NDHFTSS*NCSNEC+NDHFTTS*YSP_SBD%NLEVS

        ! Sea-ice energy budget

        DO JFLX=NDHVTIS+1,NDHVTIS+NDHFTIS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHTIS(JFLX))
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),NCSI)
          IDHCS=IDHCS+NCSI
        ENDDO

        IDHCS=NDHVS+NDHVFS+NDHFTLS*NTILES+NDHFTSS*NCSNEC+NDHFTTS*YSP_SBD%NLEVS+&
         & NDHFTIS*NCSI

        ! Snow water budget

        DO JFLX=NDHVSSS+1,NDHVSSS+NDHFSSS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHSSS(JFLX))
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),NCSNEC)
          IDHCS=IDHCS+NCSNEC
        ENDDO

        IDHCS=NDHVS+NDHVFS+NDHFTLS*NTILES+NDHFTSS*NCSNEC+NDHFTTS*YSP_SBD%NLEVS+&
         & NDHFTIS*NCSI+NDHFSSS*NCSNEC

        ! Interception layer water budget

        DO JFLX=NDHVIIS+1,NDHVIIS+NDHFIIS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHIIS(JFLX))
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),1)
          IDHCS=IDHCS+1
        ENDDO

        IDHCS=NDHVS+NDHVFS+NDHFTLS*NTILES+NDHFTSS*NCSNEC+NDHFTTS*YSP_SBD%NLEVS+&
         & NDHFTIS*NCSI+NDHFSSS*NCSNEC+NDHFIIS

        ! Soil water budget

        DO JFLX=NDHVWLS+1,NDHVWLS+NDHFWLS
          WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHWLS(JFLX))
          CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
           &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),YSP_SBD%NLEVS)
          IDHCS=IDHCS+YSP_SBD%NLEVS
        ENDDO

        !-------------------------------------------------------------------------

        ! Write free-style fluxes

        IF (NDHFFS /= 0) THEN

          ! Set pointer

          IDHCS = NDHVS + NDHVFS + NDHFSD + NDHFSP

          ! Write

          DO JFLX=NDHVFS+1,NDHVFS+NDHFFS
            WRITE (CLNOMA,'(I3.3,A)') KNUM,TRIM(YDCDDH%CADHFSS(JFLX))
            CALL WRIFDH(YDDIMV,YDLDDH,PDDHCV_TOT(1+(NFLEVG+1)*KDIMDOM*(IPOINT-1)),&
             &          NFLEVG*KDIMDOM,NPODDH,CLNOMA,PDDHCS(IDHCS+1),1)
            IDHCS=IDHCS+1
          ENDDO

        ENDIF

        !-------------------------------------------------------------------------

      ELSE ! Case .NOT.LECMWF
        
        IF (LFLEXDIA) THEN
          IF (LDDH_OMP) THEN

            IVARS=0
            IPLVAS=NTOTSVAR
            IPLVFS=NTOTSVFS
            IPLFIS=NTOTSURF-NTOTSVAR-NTOTSVFS

            DO JFIELD=1,NTOTSURF
              IPOINT=JFIELD
              SELECT CASE(YDTDDH%CFLDTYPES2D(JFIELD))
              CASE('V')
                WRITE (CLNOMA,7000) KNUM,YDTDDH%CFLDNAMES2D(JFIELD),'1 '
                CALL WRIFDH(YDDIMV,YDLDDH,PDDHCS_TOT(1+KDIMDOM*(IPOINT-1)),&
                 &          KDIMDOM,NPODDH,CLNOMA,PDDHCS(IPOINT),IPLVAS)

                WRITE (CLNOMA,7000) KNUM,YDTDDH%CFLDNAMES2D(JFIELD),'0 '
                IVARS=IVARS+1
                IPOINT=IVARS+NTOTSURF
                CALL WRIFDH(YDDIMV,YDLDDH,PDDHCS_TOT(1+KDIMDOM*(IPOINT-1)),&
                 &          KDIMDOM,NPODDH,CLNOMA,PDDHCS(IPOINT),IPLVAS)

              CASE('S')
                WRITE (CLNOMA,8000) KNUM,YDTDDH%CFLDNAMES2D(JFIELD),' '
                CALL WRIFDH(YDDIMV,YDLDDH,PDDHCS_TOT(1+KDIMDOM*(IPOINT-1)),&
                 &          KDIMDOM,NPODDH,CLNOMA,PDDHCS(IPOINT),IPLVFS)

              CASE('F')
                WRITE (CLNOMA,9000) KNUM,YDTDDH%CFLDNAMES2D(JFIELD),' '
                CALL WRIFDH(YDDIMV,YDLDDH,PDDHCS_TOT(1+KDIMDOM*(IPOINT-1)),&
                 &          KDIMDOM,NPODDH,CLNOMA,PDDHCS(IPOINT),IPLFIS)

              END SELECT
            ENDDO

          ELSE
            IVARS=0
            IPLVAS=NTOTSVAR
            IPLVFS=NTOTSVFS
            IPLFIS=NTOTSURF-NTOTSVAR-NTOTSVFS

            DO JFIELD=1,NTOTSURF
              IPOINT=JFIELD
              SELECT CASE(RDDHSURF_DESCR(JFIELD)%CFLUX)
              CASE('V')
                WRITE (CLNOMA,7000) KNUM,RDDHSURF_DESCR(JFIELD)%CNAME,'1 '
                CALL WRIFDH(YDDIMV,YDLDDH,PDDHCS_TOT(1+KDIMDOM*(IPOINT-1)),&
                 &          KDIMDOM,NPODDH,CLNOMA,PDDHCS(IPOINT),IPLVAS)
  
                WRITE (CLNOMA,7000) KNUM,RDDHSURF_DESCR(JFIELD)%CNAME,'0 '
                IVARS=IVARS+1
                IPOINT=IVARS+NTOTSURF
                CALL WRIFDH(YDDIMV,YDLDDH,PDDHCS_TOT(1+KDIMDOM*(IPOINT-1)),&
                 &          KDIMDOM,NPODDH,CLNOMA,PDDHCS(IPOINT),IPLVAS)
  
              CASE('S')
                WRITE (CLNOMA,8000) KNUM,RDDHSURF_DESCR(JFIELD)%CNAME,' '
                CALL WRIFDH(YDDIMV,YDLDDH,PDDHCS_TOT(1+KDIMDOM*(IPOINT-1)),&
                 &          KDIMDOM,NPODDH,CLNOMA,PDDHCS(IPOINT),IPLVFS)

              CASE('F')
                WRITE (CLNOMA,9000) KNUM,RDDHSURF_DESCR(JFIELD)%CNAME,' '
                CALL WRIFDH(YDDIMV,YDLDDH,PDDHCS_TOT(1+KDIMDOM*(IPOINT-1)),&
                 &          KDIMDOM,NPODDH,CLNOMA,PDDHCS(IPOINT),IPLFIS)

              END SELECT
            ENDDO
          ENDIF
        ELSE !LFLEXDIA

          IDHCS = 0
 
          !       ENSEMBLE DES VARIABLES INITIALES
          DO JVAR=1,NDHVS
            WRITE (CLNOMA,FMT='(I3.3,A,I2.2,A)') KNUM,'S',JVAR,'_0'
            IPOINT=IDHCS+JVAR
            CALL WRIFDH(YDDIMV,YDLDDH,PDDHCS_TOT(1+KDIMDOM*(IPOINT-1)),&
             & KDIMDOM,NPODDH,CLNOMA,PDDHCS(IPOINT),NDHVS)
          ENDDO

          !       ENSEMBLE DES VARIABLES COURANTES
          IDHCS = NDHCS
          DO JVAR=1,NDHVS
            WRITE (CLNOMA,FMT='(I3.3,A,I2.2,A)') KNUM,'S',JVAR,'_1'
            IPOINT=IDHCS+JVAR
            CALL WRIFDH(YDDIMV,YDLDDH,PDDHCS_TOT(1+KDIMDOM*(IPOINT-1)),&
             &          KDIMDOM,NPODDH,CLNOMA,PDDHCS(IPOINT),NDHVS)
          ENDDO

          !       ENSEMBLE DES CUMULS
          IDHCS = NDHVS + NDHVFS
          DO JVAR=1,INBCUMS
            WRITE (CLNOMA,FMT='(I3.3,A,I2.2)') KNUM,'G',JVAR
            IPOINT=IDHCS+JVAR
            CALL WRIFDH(YDDIMV,YDLDDH,PDDHCS_TOT(1+KDIMDOM*(IPOINT-1)),&
             &          KDIMDOM,NPODDH,CLNOMA,PDDHCS(IPOINT),INBCUMS)
          ENDDO

          !       Set of free-style surface variables
          IF (NDHVFS /= 0) THEN
            IDHCS = NDHVS
            DO JVAR=1,NDHVFS
              WRITE (CLNOMA,FMT='(I3.3,A,I2.2)') KNUM,'SVGFS',JVAR
              IPOINT=IDHCS+JVAR
              CALL WRIFDH(YDDIMV,YDLDDH,PDDHCS_TOT(1+KDIMDOM*(IPOINT-1)),&
               &          KDIMDOM,NPODDH,CLNOMA,PDDHCS(IPOINT),NDHVFS)
            ENDDO
          ENDIF

          !       Set of free-style surface fluxes
          IF (NDHFFS /= 0) THEN
            IDHCS = NDHVS + NDHVFS + NDHFSD + NDHFSP
            DO JVAR=1,NDHFFS
              WRITE (CLNOMA,FMT='(I3.3,A,I2.2)') KNUM,'SFGFS',JVAR
              IPOINT=IDHCS+JVAR
              CALL WRIFDH(YDDIMV,YDLDDH,PDDHCS_TOT(1+KDIMDOM*(IPOINT-1)),&
               &          KDIMDOM,NPODDH,CLNOMA,PDDHCS(IPOINT),NDHFFS)
            ENDDO
          ENDIF
        ENDIF !LFLEXDIA
      ENDIF ! LECMWF
    ENDIF ! ( LHDHKS .AND. ((NDHVS /= 0) .OR. (NDHCS /= 0)) )

    IF ( LHDLIST ) THEN
      WRITE (NULOUT,*) ' DDH * PPFIDH FINI IDHCV=',IDHCV,&
       & ' IDHCS = ',IDHCS,' INBCUMS = ',INBCUMS
    ENDIF

ENDIF ! (.NOT.LHDLFA .OR. KNUM == KDIMDOM)

4000 FORMAT (I3.3,A3,A9)
5000 FORMAT (I3.3,A1,A2,A10)
6000 FORMAT (I3.3,A11,A2)
7000 FORMAT (I3.3,A4,A2)
8000 FORMAT (I3.3,A5,A2)
9000 FORMAT (I3.3,A9,A2)

!-----------------------------------------------------------------------
END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('PPFIDH',1,ZHOOK_HANDLE)
END SUBROUTINE PPFIDH
