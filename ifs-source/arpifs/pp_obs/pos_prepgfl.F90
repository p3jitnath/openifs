SUBROUTINE POS_PREPGFL(YDQTYPE,YDTFP,YGFL,KPPGFL,YDIN,YDGFL,YDPPIC,LD_GFL,LDIN_GFL,KGFLCOD,KDIN_GFL)

!**** *POS_PREPGFL* - post-processing

!     PURPOSE.
!     -------
!           Post-processing calculations: preliminary calculations for GFL

!**   INTERFACE.
!     ---------
!        *CALL* *POS_PREPGFL(...)

!        EXPLICIT ARGUMENTS :
!        ------------------
!        * INPUT:
!        KPPGFL    : dimension (number of GFL variables required for post-processing)
!        YDIN      : pointers allowing to retrieve individual fields in PGFL (in POS)
!        YDGFL     : says if GFL is active or not
!        YDPPIC    : pointers allowing to retrieve individual fields in ZPPGFL (in POS)
!        * OUTPUT:
!        LD_GFL    : says if pp of GFL is required or not
!        LDIN_GFL  : ldin_gfl(jvar_[X])=YDGFL%LL[X]
!        KGFLCOD   : stores YDTFP%[X]%ICOD
!        KDIN_GFL  : allows to find correspondance between ordering GFL in PGFL
!                    and ordering GFL in ZGFLT0, in POS
!                    zgflt0(jvar)=pgfl(kdin_gfl(jvar)).

!     EXTERNALS.
!     ---------

! Modifications
! -------------
!   ORIGINAL : August 2009 (K. Yessad)
! End Modifications
!------------------------------------------------------------------------------

USE PARKIND1 , ONLY : JPIM, JPRB
USE YOMHOOK  , ONLY : LHOOK, DR_HOOK, JPHOOK
USE TYPE_FPRQDYNS, ONLY : TYPE_FPRQDYN
USE YOMAFN, ONLY : ALL_FULLPOS_TYPES
USE YOM_YGFL , ONLY : TYPE_GFLD
USE TYPE_GFLFLDS, ONLY: TYPE_IGFLFLD,TYPE_IGFLFLDD,TYPE_LGFLFLDD
USE YOE_AERODIAG, ONLY : NPAERAOT, NPAERLISI

!     ------------------------------------------------------------------

IMPLICIT NONE

TYPE (TYPE_FPRQDYN),  INTENT(IN) :: YDQTYPE
TYPE(ALL_FULLPOS_TYPES), INTENT(IN) :: YDTFP
TYPE(TYPE_GFLD)   ,INTENT(IN)    :: YGFL
INTEGER(KIND=JPIM),INTENT(IN)    :: KPPGFL
TYPE(TYPE_IGFLFLDD),INTENT(IN)   :: YDIN
TYPE(TYPE_LGFLFLDD),INTENT(IN)   :: YDGFL
TYPE(TYPE_IGFLFLD),INTENT(IN)    :: YDPPIC
LOGICAL,INTENT(OUT)              :: LD_GFL(KPPGFL)
LOGICAL,INTENT(OUT)              :: LDIN_GFL(KPPGFL)
INTEGER(KIND=JPIM),INTENT(OUT)   :: KGFLCOD(KPPGFL)
INTEGER(KIND=JPIM),INTENT(OUT)   :: KDIN_GFL(KPPGFL)

!     ------------------------------------------------------------------

INTEGER(KIND=JPIM) :: JVAR
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!     ------------------------------------------------------------------
IF (LHOOK) CALL DR_HOOK('POS_PREPGFL',0,ZHOOK_HANDLE)
ASSOCIATE(NAERO=>YGFL%NAERO, NAEROUT=>YGFL%NAEROUT, NCHEM=>YGFL%NCHEM, &
 & NERA40=>YGFL%NERA40, NGFL_EXT=>YGFL%NGFL_EXT, NGHG=>YGFL%NGHG, &
 & NEMIS3D=>YGFL%NEMIS3D, &
 & NGFL_EZDIAG=>YGFL%NGFL_EZDIAG, &
 & NNOGW=>YGFL%NNOGW, NUVP=>YGFL%NUVP, YLRCH4=>YGFL%YLRCH4, &
 & YLRCH4_NL=>YGFL%YLRCH4_NL, YEMIS3D=>YGFL%YEMIS3D, NEDRP=>YGFL%NEDRP,&
 & LAERAOT=>YGFL%LAERAOT, LAERLISI=>YGFL%LAERLISI)
!     ------------------------------------------------------------------

!     1.     LD_GFL

LD_GFL(YDPPIC%IQ)    = YDQTYPE%LL(YDTFP%Q%ICOD).OR.YDQTYPE%LL(YDTFP%THPW%ICOD)
LD_GFL(YDPPIC%IL)    = YDQTYPE%LL(YDTFP%L%ICOD).OR.YDQTYPE%LL(YDTFP%THPW%ICOD)
LD_GFL(YDPPIC%ILRAD) = YDQTYPE%LL(YDTFP%LRAD%ICOD)
LD_GFL(YDPPIC%II)    = YDQTYPE%LL(YDTFP%I%ICOD).OR.YDQTYPE%LL(YDTFP%THPW%ICOD)
LD_GFL(YDPPIC%IIRAD) = YDQTYPE%LL(YDTFP%IRAD%ICOD)
LD_GFL(YDPPIC%IA)    = YDQTYPE%LL(YDTFP%CLF%ICOD)
LD_GFL(YDPPIC%IO3)   = YDQTYPE%LL(YDTFP%O3MX%ICOD)
LD_GFL(YDPPIC%ICPF)   = YDQTYPE%LL(YDTFP%CPF%ICOD)
LD_GFL(YDPPIC%ISPF)   = YDQTYPE%LL(YDTFP%SPF%ICOD)
LD_GFL(YDPPIC%IDAL)  = YDQTYPE%LL(YDTFP%DAL%ICOD)
LD_GFL(YDPPIC%IDOM)  = YDQTYPE%LL(YDTFP%DOM%ICOD)
LD_GFL(YDPPIC%IUAL)  = YDQTYPE%LL(YDTFP%UAL%ICOD)
LD_GFL(YDPPIC%IUOM)  = YDQTYPE%LL(YDTFP%UOM%ICOD)
LD_GFL(YDPPIC%ILCONV)= YDQTYPE%LL(YDTFP%LCONV%ICOD)
LD_GFL(YDPPIC%IICONV)= YDQTYPE%LL(YDTFP%ICONV%ICOD)
LD_GFL(YDPPIC%IRCONV)= YDQTYPE%LL(YDTFP%RCONV%ICOD)
LD_GFL(YDPPIC%ISCONV)= YDQTYPE%LL(YDTFP%SCONV%ICOD)
LD_GFL(YDPPIC%IUEN)  = YDQTYPE%LL(YDTFP%UEN%ICOD)
LD_GFL(YDPPIC%IUNEBH)= YDQTYPE%LL(YDTFP%UNEBH%ICOD)
LD_GFL(YDPPIC%IS)    = YDQTYPE%LL(YDTFP%SN%ICOD)
LD_GFL(YDPPIC%IRR)   = YDQTYPE%LL(YDTFP%RR%ICOD)
LD_GFL(YDPPIC%IG)    = YDQTYPE%LL(YDTFP%GR%ICOD)
LD_GFL(YDPPIC%IH)    = YDQTYPE%LL(YDTFP%HL%ICOD)
LD_GFL(YDPPIC%ITKE)  = YDQTYPE%LL(YDTFP%TKE%ICOD)
LD_GFL(YDPPIC%IPHYCTY)=YDQTYPE%LL(YDTFP%PHYCTY%ICOD)
! should be equivalent to write LD_GFL(YDPPIC%ILRCH4)=YDQTYPE%LL(YDTFP%LRCH4%ICOD)
IF(NGHG>0 .AND. YLRCH4_NL%LGP) THEN
  IF (YLRCH4%LACTIVE) LD_GFL(YDPPIC%ILRCH4)=YLRCH4%LACTIVE
ENDIF
DO JVAR=1,NEMIS3D
  LD_GFL(YDPPIC%IEMIS3D+JVAR-1)=YDQTYPE%LL(YDTFP%EMIS3D(JVAR)%ICOD)
ENDDO
DO JVAR=1,NGHG
  LD_GFL(YDPPIC%IGHG+JVAR-1)=YDQTYPE%LL(YDTFP%GHG(JVAR)%ICOD)
ENDDO
DO JVAR=1,NGFL_EZDIAG
  LD_GFL(YDPPIC%IEZDIAG+JVAR-1)=YDQTYPE%LL(YDTFP%EZDIAG(JVAR)%ICOD)
ENDDO
DO JVAR=1,NAERO
  LD_GFL(YDPPIC%IAERO+JVAR-1)=YDQTYPE%LL(YDTFP%AERO(JVAR)%ICOD)
ENDDO
DO JVAR=1,NCHEM
  LD_GFL(YDPPIC%ICHEM+JVAR-1)=YDQTYPE%LL(YDTFP%CHEM(JVAR)%ICOD)
ENDDO
DO JVAR=1,NGFL_EXT
  LD_GFL(YDPPIC%IEXT+JVAR-1)=YDQTYPE%LL(YDTFP%EXT(JVAR)%ICOD)
ENDDO
DO JVAR=1,NNOGW
  LD_GFL(YDPPIC%INOGW+JVAR-1)=YDQTYPE%LL(YDTFP%NOGW(JVAR)%ICOD)
ENDDO
DO JVAR=1,NEDRP
  LD_GFL(YDPPIC%IEDRP+JVAR-1)=YDQTYPE%LL(YDTFP%EDRP(JVAR)%ICOD)
ENDDO
IF (LAERAOT) THEN
  DO JVAR=1,NPAERAOT
    LD_GFL(YDPPIC%IAERAOT+JVAR-1)=YDQTYPE%LL(YDTFP%AERAOT(JVAR)%ICOD)
  ENDDO
ENDIF
IF (LAERLISI) THEN
  DO JVAR=1,NPAERLISI
    LD_GFL(YDPPIC%IAERLISI+JVAR-1)=YDQTYPE%LL(YDTFP%AERLISI(JVAR)%ICOD)
  ENDDO
ENDIF
DO JVAR=1,NAEROUT
  LD_GFL(YDPPIC%IAEROUT+JVAR-1)=YDQTYPE%LL(YDTFP%AEROUT(JVAR)%ICOD)
ENDDO
DO JVAR=1,NUVP
  LD_GFL(YDPPIC%IUVP+JVAR-1)=YDQTYPE%LL(YDTFP%UVP(JVAR)%ICOD)
ENDDO
DO JVAR=1,NERA40
  LD_GFL(YDPPIC%IERA40+JVAR-1)=YDQTYPE%LL(YDTFP%ERA40(JVAR)%ICOD)
ENDDO

!     3.     KGFLCOD

KGFLCOD(YDPPIC%IQ)=YDTFP%Q%ICOD
KGFLCOD(YDPPIC%IL)=YDTFP%L%ICOD
KGFLCOD(YDPPIC%ILRAD)=YDTFP%LRAD%ICOD
KGFLCOD(YDPPIC%II)=YDTFP%I%ICOD
KGFLCOD(YDPPIC%IIRAD)=YDTFP%IRAD%ICOD
KGFLCOD(YDPPIC%IA)=YDTFP%CLF%ICOD
KGFLCOD(YDPPIC%IO3)=YDTFP%O3MX%ICOD
KGFLCOD(YDPPIC%ICPF)=YDTFP%CPF%ICOD
KGFLCOD(YDPPIC%ISPF)=YDTFP%SPF%ICOD
KGFLCOD(YDPPIC%IDAL)=YDTFP%DAL%ICOD
KGFLCOD(YDPPIC%IDOM)=YDTFP%DOM%ICOD
KGFLCOD(YDPPIC%IUAL)=YDTFP%UAL%ICOD
KGFLCOD(YDPPIC%IUOM)=YDTFP%UOM%ICOD
KGFLCOD(YDPPIC%ILCONV)=YDTFP%LCONV%ICOD
KGFLCOD(YDPPIC%IICONV)=YDTFP%ICONV%ICOD
KGFLCOD(YDPPIC%IRCONV)=YDTFP%RCONV%ICOD
KGFLCOD(YDPPIC%ISCONV)=YDTFP%SCONV%ICOD
KGFLCOD(YDPPIC%IUEN)=YDTFP%UEN%ICOD
KGFLCOD(YDPPIC%IUNEBH)=YDTFP%UNEBH%ICOD
KGFLCOD(YDPPIC%IS)=YDTFP%SN%ICOD
KGFLCOD(YDPPIC%IRR)=YDTFP%RR%ICOD
KGFLCOD(YDPPIC%IG)=YDTFP%GR%ICOD
KGFLCOD(YDPPIC%IH)=YDTFP%HL%ICOD
KGFLCOD(YDPPIC%ITKE)=YDTFP%TKE%ICOD
KGFLCOD(YDPPIC%IPHYCTY)=YDTFP%PHYCTY%ICOD
IF(NGHG>0 .AND. YLRCH4_NL%LGP) KGFLCOD(YDPPIC%ILRCH4)=YDTFP%LRCH4%ICOD
DO JVAR=1,NEMIS3D
  KGFLCOD(YDPPIC%IEMIS3D+JVAR-1)=YDTFP%EMIS3D(JVAR)%ICOD
ENDDO
DO JVAR=1,NGHG
  KGFLCOD(YDPPIC%IGHG+JVAR-1)=YDTFP%GHG(JVAR)%ICOD
ENDDO
DO JVAR=1,NGFL_EZDIAG
  KGFLCOD(YDPPIC%IEZDIAG+JVAR-1)=YDTFP%EZDIAG(JVAR)%ICOD
ENDDO
DO JVAR=1,NAERO
  KGFLCOD(YDPPIC%IAERO+JVAR-1)=YDTFP%AERO(JVAR)%ICOD
ENDDO
DO JVAR=1,NCHEM
  KGFLCOD(YDPPIC%ICHEM+JVAR-1)=YDTFP%CHEM(JVAR)%ICOD
ENDDO
DO JVAR=1,NGFL_EXT
  KGFLCOD(YDPPIC%IEXT+JVAR-1)=YDTFP%EXT(JVAR)%ICOD
ENDDO
DO JVAR=1,NNOGW
  KGFLCOD(YDPPIC%INOGW+JVAR-1)=YDTFP%NOGW(JVAR)%ICOD
ENDDO
DO JVAR=1,NEDRP
  KGFLCOD(YDPPIC%IEDRP+JVAR-1)=YDTFP%EDRP(JVAR)%ICOD
ENDDO
IF (LAERAOT) THEN
  DO JVAR=1,NPAERAOT
    KGFLCOD(YDPPIC%IAERAOT+JVAR-1)=YDTFP%AERAOT(JVAR)%ICOD
  ENDDO
ENDIF
IF (LAERLISI) THEN
  DO JVAR=1,NPAERLISI
    KGFLCOD(YDPPIC%IAERLISI+JVAR-1)=YDTFP%AERLISI(JVAR)%ICOD
  ENDDO
ENDIF
DO JVAR=1,NAEROUT
  KGFLCOD(YDPPIC%IAEROUT+JVAR-1)=YDTFP%AEROUT(JVAR)%ICOD
ENDDO
DO JVAR=1,NUVP
  KGFLCOD(YDPPIC%IUVP+JVAR-1)=YDTFP%UVP(JVAR)%ICOD
ENDDO
DO JVAR=1,NERA40
  KGFLCOD(YDPPIC%IERA40+JVAR-1)=YDTFP%ERA40(JVAR)%ICOD
ENDDO

!     4.     KDIN_GFL

KDIN_GFL(YDPPIC%IQ)=YDIN%IQ
KDIN_GFL(YDPPIC%IL)=YDIN%IL
KDIN_GFL(YDPPIC%ILRAD)=YDIN%ILRAD
KDIN_GFL(YDPPIC%II)=YDIN%II
KDIN_GFL(YDPPIC%IIRAD)=YDIN%IIRAD
KDIN_GFL(YDPPIC%IA)=YDIN%IA
KDIN_GFL(YDPPIC%IO3)=YDIN%IO3
KDIN_GFL(YDPPIC%ICPF)=YDIN%ICPF
KDIN_GFL(YDPPIC%ISPF)=YDIN%ISPF
KDIN_GFL(YDPPIC%IDAL)=YDIN%IDAL
KDIN_GFL(YDPPIC%IDOM)=YDIN%IDOM
KDIN_GFL(YDPPIC%IUAL)=YDIN%IUAL
KDIN_GFL(YDPPIC%IUOM)=YDIN%IUOM
KDIN_GFL(YDPPIC%ILCONV)=YDIN%ILCONV
KDIN_GFL(YDPPIC%IICONV)=YDIN%IICONV
KDIN_GFL(YDPPIC%IRCONV)=YDIN%IRCONV
KDIN_GFL(YDPPIC%ISCONV)=YDIN%ISCONV
KDIN_GFL(YDPPIC%IUEN)=YDIN%IUEN
KDIN_GFL(YDPPIC%IUNEBH)=YDIN%IUNEBH
KDIN_GFL(YDPPIC%IS)=YDIN%IS
KDIN_GFL(YDPPIC%IRR)=YDIN%IRR
KDIN_GFL(YDPPIC%IG)=YDIN%IG
KDIN_GFL(YDPPIC%IH)=YDIN%IH
KDIN_GFL(YDPPIC%ITKE)=YDIN%ITKE
KDIN_GFL(YDPPIC%IPHYCTY)=YDIN%IPHYCTY
IF(NGHG>0 .AND. YLRCH4_NL%LGP) KDIN_GFL(YDPPIC%ILRCH4)=YDIN%ILRCH4
DO JVAR=1,NEMIS3D
  KDIN_GFL(YDPPIC%IEMIS3D+JVAR-1)=YDIN%IEMIS3D+JVAR-1
ENDDO
DO JVAR=1,NGHG
  KDIN_GFL(YDPPIC%IGHG+JVAR-1)=YDIN%IGHG+JVAR-1
ENDDO
DO JVAR=1,NGFL_EZDIAG
  KDIN_GFL(YDPPIC%IEZDIAG+JVAR-1)=YDIN%IEZDIAG+JVAR-1
ENDDO
DO JVAR=1,NAERO
  KDIN_GFL(YDPPIC%IAERO+JVAR-1)=YDIN%IAERO+JVAR-1
ENDDO
DO JVAR=1,NCHEM
  KDIN_GFL(YDPPIC%ICHEM+JVAR-1)=YDIN%ICHEM+JVAR-1
ENDDO
DO JVAR=1,NGFL_EXT
  KDIN_GFL(YDPPIC%IEXT+JVAR-1)=YDIN%IEXT+JVAR-1
ENDDO
DO JVAR=1,NNOGW
  KDIN_GFL(YDPPIC%INOGW+JVAR-1)=YDIN%INOGW+JVAR-1
ENDDO
DO JVAR=1,NEDRP
  KDIN_GFL(YDPPIC%IEDRP+JVAR-1)=YDIN%IEDRP+JVAR-1
ENDDO
IF (LAERAOT) THEN
  DO JVAR=1,NPAERAOT
    KDIN_GFL(YDPPIC%IAERAOT+JVAR-1)=YDIN%IAERAOT+JVAR-1
  ENDDO
ENDIF
IF (LAERLISI) THEN
  DO JVAR=1,NPAERLISI
    KDIN_GFL(YDPPIC%IAERLISI+JVAR-1)=YDIN%IAERLISI+JVAR-1
  ENDDO
ENDIF
DO JVAR=1,NAEROUT
  KDIN_GFL(YDPPIC%IAEROUT+JVAR-1)=YDIN%IAEROUT+JVAR-1
ENDDO
DO JVAR=1,NUVP
  KDIN_GFL(YDPPIC%IUVP+JVAR-1)=YDIN%IUVP+JVAR-1
ENDDO
DO JVAR=1,NERA40
  KDIN_GFL(YDPPIC%IERA40+JVAR-1)=YDIN%IERA40+JVAR-1
ENDDO

!     5.     LDIN_GFL

LDIN_GFL(YDPPIC%IQ)=YDGFL%LLQ
LDIN_GFL(YDPPIC%IL)=YDGFL%LLL
LDIN_GFL(YDPPIC%ILRAD)=YDGFL%LLLRAD
LDIN_GFL(YDPPIC%II)=YDGFL%LLI
LDIN_GFL(YDPPIC%IIRAD)=YDGFL%LLIRAD
LDIN_GFL(YDPPIC%IA)=YDGFL%LLA
LDIN_GFL(YDPPIC%IO3)=YDGFL%LLO3
LDIN_GFL(YDPPIC%ICPF)=YDGFL%LLCPF
LDIN_GFL(YDPPIC%ISPF)=YDGFL%LLSPF
LDIN_GFL(YDPPIC%IDAL)=YDGFL%LLDAL
LDIN_GFL(YDPPIC%IDOM)=YDGFL%LLDOM
LDIN_GFL(YDPPIC%IUAL)=YDGFL%LLUAL
LDIN_GFL(YDPPIC%IUOM)=YDGFL%LLUOM
LDIN_GFL(YDPPIC%ILCONV)=YDGFL%LLLCONV
LDIN_GFL(YDPPIC%IICONV)=YDGFL%LLICONV
LDIN_GFL(YDPPIC%IRCONV)=YDGFL%LLRCONV
LDIN_GFL(YDPPIC%ISCONV)=YDGFL%LLSCONV
LDIN_GFL(YDPPIC%IUEN)=YDGFL%LLUEN
LDIN_GFL(YDPPIC%IUNEBH)=YDGFL%LLUNEBH
LDIN_GFL(YDPPIC%IS)=YDGFL%LLS
LDIN_GFL(YDPPIC%IRR)=YDGFL%LLRR
LDIN_GFL(YDPPIC%IG)=YDGFL%LLG
LDIN_GFL(YDPPIC%IH)=YDGFL%LLH
LDIN_GFL(YDPPIC%ITKE)=YDGFL%LLTKE
LDIN_GFL(YDPPIC%IPHYCTY)=YDGFL%LLPHYCTY
IF(NGHG>0 .AND. YLRCH4_NL%LGP) LDIN_GFL(YDPPIC%ILRCH4)=YDGFL%LLLRCH4
DO JVAR=1,NEMIS3D
  LDIN_GFL(YDPPIC%IEMIS3D+JVAR-1)=YDGFL%LLEMIS3D(JVAR)
ENDDO
DO JVAR=1,NGHG
  LDIN_GFL(YDPPIC%IGHG+JVAR-1)=YDGFL%LLGHG(JVAR)
ENDDO
DO JVAR=1,NGFL_EZDIAG
  LDIN_GFL(YDPPIC%IEZDIAG+JVAR-1)=YDGFL%LLEZDIAG(JVAR)
ENDDO
DO JVAR=1,NAERO
  LDIN_GFL(YDPPIC%IAERO+JVAR-1)=YDGFL%LLAERO(JVAR)
ENDDO
DO JVAR=1,NCHEM
  LDIN_GFL(YDPPIC%ICHEM+JVAR-1)=YDGFL%LLCHEM(JVAR)
ENDDO
DO JVAR=1,NGFL_EXT
  LDIN_GFL(YDPPIC%IEXT+JVAR-1)=YDGFL%LLEXT(JVAR)
ENDDO
DO JVAR=1,NNOGW
  LDIN_GFL(YDPPIC%INOGW+JVAR-1)=YDGFL%LLNOGW(JVAR)
ENDDO
DO JVAR=1,NEDRP
  LDIN_GFL(YDPPIC%IEDRP+JVAR-1)=YDGFL%LLEDRP(JVAR)
ENDDO
IF (LAERAOT) THEN
  DO JVAR=1,NPAERAOT
    LDIN_GFL(YDPPIC%IAERAOT+JVAR-1)=YDGFL%LLAERAOT(JVAR)
  ENDDO
ENDIF
IF (LAERLISI) THEN
  DO JVAR=1,NPAERLISI
    LDIN_GFL(YDPPIC%IAERLISI+JVAR-1)=YDGFL%LLAERLISI(JVAR)
  ENDDO
ENDIF
DO JVAR=1,NAEROUT
  LDIN_GFL(YDPPIC%IAEROUT+JVAR-1)=YDGFL%LLAEROUT(JVAR)
ENDDO
DO JVAR=1,NUVP
  LDIN_GFL(YDPPIC%IUVP+JVAR-1)=YDGFL%LLUVP(JVAR)
ENDDO
DO JVAR=1,NERA40
  LDIN_GFL(YDPPIC%IERA40+JVAR-1)=YDGFL%LLERA40(JVAR)
ENDDO

!     ------------------------------------------------------------------
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('POS_PREPGFL',1,ZHOOK_HANDLE)
END SUBROUTINE POS_PREPGFL
