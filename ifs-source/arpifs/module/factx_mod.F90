END SUBROUTINE FAOFF

SUBROUTINE COMPACTFLD (CDFPCA, YDFACTX, YDFLDSC, PFIELD, YDCPDSC, PVALCO, KMASK)

USE IOFLDDESC_MOD, ONLY : IOFLDDESC
USE IOCPTDESC_MOD, ONLY : IOCPTDESC
USE FA_MOD, ONLY : FA_COM_DEFAULT, FA_COM
USE OML_MOD, ONLY : OML_MY_THREAD

CHARACTER(LEN=*),    INTENT (IN)             :: CDFPCA
TYPE (FACTX),        INTENT (INOUT)          :: YDFACTX
TYPE (IOFLDDESC),    INTENT (IN)             :: YDFLDSC (:)
REAL (KIND=JPRB),    INTENT (IN)             :: PFIELD (:,:)
TYPE (IOCPTDESC),    INTENT (INOUT)          :: YDCPDSC (:)
REAL (KIND=JPRB),    INTENT (OUT)            :: PVALCO (:,:)
INTEGER (KIND=JPIM), INTENT (IN),   OPTIONAL :: KMASK (:)

#include "wrspeca_compress_mt.intfb.h"
#include "wrgp2fa_compress_mt.intfb.h"
#include "abor1.intfb.h"

INTEGER (KIND=JPIM) :: JFLD, INFL, ITID
LOGICAL             :: LLFLD
TYPE (FA_COM), POINTER :: YLFA

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('FACTX_MOD:COMPACTFLD',0,ZHOOK_HANDLE)

INFL = SIZE (YDCPDSC)

LLFLD = INFL > 0
IF (PRESENT (KMASK)) THEN
  LLFLD = LLFLD .AND. ANY (KMASK /= 0)
ENDIF

IF (LLFLD) THEN

  CALL OPENCPFACTX (YDFACTX,CDFPCA)
  
!$OMP PARALLEL PRIVATE (ITID, JFLD, YLFA) IF (YDFACTX%YIOOPTS%LFACPT_OPENMP)
  
  
  IF (YDFACTX%YIOOPTS%LFACPT_OPENMP) THEN
    ITID = OML_MY_THREAD ()
    YLFA => YDFACTX%YFABYTID (ITID)
  ELSE
    YLFA => FA_COM_DEFAULT
  ENDIF
  
!$OMP DO
  DO JFLD = 1, INFL
  
    IF (PRESENT (KMASK)) THEN
      IF (KMASK (JFLD) == 0) CYCLE
    ENDIF
  
    IF (YDFLDSC (JFLD)%LSPEC) THEN
      CALL WRSPECA_COMPRESS_MT (YLFA, YDFACTX%IUNITCP, PFIELD(:,JFLD),&
                              & PVALCO(:,JFLD), YDFLDSC(JFLD), YDCPDSC (JFLD))
    ELSE
      CALL WRGP2FA_COMPRESS_MT (YLFA, YDFACTX%IUNITCP, YDFLDSC(JFLD),&
                              & PFIELD(:,JFLD), PVALCO(:,JFLD), YDCPDSC(JFLD))
    ENDIF
  
  ENDDO
!$OMP END DO
  
!$OMP END PARALLEL

ENDIF

IF (LHOOK) CALL DR_HOOK('FACTX_MOD:COMPACTFLD',1,ZHOOK_HANDLE)

END SUBROUTINE COMPACTFLD

SUBROUTINE WRITEFLD(YDFACTX, YDCPDSC, KMASK,PVALCO)

USE IOCPTDESC_MOD , ONLY : IOCPTDESC
USE FA_MOD        , ONLY : JPPRCM

TYPE (FACTX),        INTENT (INOUT) :: YDFACTX
TYPE (IOCPTDESC),    INTENT (IN)    :: YDCPDSC (:)
INTEGER (KIND=JPIM), INTENT (IN)    :: KMASK (:)
REAL (KIND=JPRB),    INTENT (IN)    :: PVALCO (:,:)

#include "abor1.intfb.h"

INTEGER (KIND=JPIM) :: JFLD, INFL, IREP

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('FACTX_MOD:WRITEFLD',0,ZHOOK_HANDLE)


INFL = SIZE (YDCPDSC)

IF ((INFL > 0) .AND. ANY (KMASK /= 0)) THEN

  IF (YDFACTX%YIOOPTS%LUCFA) THEN
  
    IF (.NOT. YDFACTX%LCFA) CALL ABOR1 ('FACTX_MOD:WRITEFLD: LUCFA SHOULD IMPLY LCFA')
  
    CALL WRFLDCW_WRITE (YDFACTX%YWCTX, YDCPDSC%CNOMA, YDCPDSC%ILNOMA,&
                      & PVALCO, YDCPDSC%ILONGD, KMASK)
  
  ELSE
  
    DO JFLD = 1, INFL
    
      IF (KMASK (JFLD) == 0) CYCLE
    
      CALL FAISAN (IREP, YDFACTX%IUNITWR, YDCPDSC (JFLD)%CNOMA(1:YDCPDSC (JFLD)%ILNOMA),&
                 & PVALCO (1:YDCPDSC (JFLD)%ILONGD*JPPRCM,JFLD), YDCPDSC (JFLD)%ILONGD)
    
    ENDDO
  
  ENDIF

ENDIF

IF (LHOOK) CALL DR_HOOK('FACTX_MOD:WRITEFLD',1,ZHOOK_HANDLE)

END SUBROUTINE WRITEFLD

SUBROUTINE FREEFACTX (YDFACTX)

USE OML_MOD, ONLY : OML_MY_THREAD
USE FADUP_MOD, ONLY : FADUPU3, FADUPU4
USE YOMCT1, ONLY : LRFILAF

TYPE (FACTX), INTENT(INOUT) :: YDFACTX

INTEGER (KIND=JPIM) :: INTROU, INARES, INAMAX, INALDO
INTEGER (KIND=JPIM) :: IREP
INTEGER (KIND=JPIM) :: IITID

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('FACTX_MOD:FREEFACTX',0,ZHOOK_HANDLE)

IF (YDFACTX%IUNITWR /= 0) THEN

  IF (YDFACTX%YIOOPTS%LUCFA) THEN
    CALL WRFLDCW_CLOSE (YDFACTX%YWCTX)
    YDFACTX%LCFA = .FALSE.
  ENDIF

  IF (LRFILAF) CALL LFILAF (IREP, YDFACTX%IUNITWR, .FALSE.)
  
  CALL LFINAF (IREP, YDFACTX%IUNITWR, INALDO, INTROU, INARES, INAMAX)

  IF ((.NOT. YDFACTX%LEXIST) .AND. (INALDO == YDFACTX%INIART)) THEN
    CALL FAIRME (IREP, YDFACTX%IUNITWR, 'DELETE')
  ELSE
    CALL FAIRME (IREP, YDFACTX%IUNITWR, 'KEEP')
  ENDIF
  
  YDFACTX%IUNITWR = 0
ENDIF

IF (YDFACTX%IUNITCP /= 0) THEN

  CALL FAIRNO (IREP, YDFACTX%IUNITCP, 'DELETE')

  YDFACTX%IUNITCP = 0

  IF (YDFACTX%YIOOPTS%LFACPT_OPENMP) THEN

!$OMP PARALLEL PRIVATE (IITID)

    IITID = OML_MY_THREAD ()

    CALL FADUPU3 (YDFACTX%YFABYTID (IITID), YDFACTX%YFAPARAM)

!$OMP END PARALLEL

    CALL FADUPU4 (YDFACTX%YFAPARAM)

  ENDIF

ENDIF

IF (LHOOK) CALL DR_HOOK('FACTX_MOD:FREEFACTX',1,ZHOOK_HANDLE)

END SUBROUTINE FREEFACTX

SUBROUTINE OPENCPFACTX (YDFACTX,CDFPCA)

USE OML_MOD, ONLY : OML_MAX_THREADS, OML_MY_THREAD
USE FADUP_MOD, ONLY : FADUPU1, FADUPU2
USE YOMFA, ONLY : CMODEL, NIDCEN, LEXTERN

! Model

USE YOMLUN, ONLY : NSCRTCH

TYPE (FACTX), INTENT(INOUT) :: YDFACTX
CHARACTER(LEN=*), INTENT(IN):: CDFPCA

#include "getfplun.intfb.h"

INTEGER (KIND=JPIM) :: IUNIT
INTEGER (KIND=JPIM) :: INTID, IITID
INTEGER (KIND=JPIM) :: INBARI, INBARP, IREP
INTEGER (KIND=JPIM) :: IULFP
CHARACTER(LEN=16) :: CLSCRTCH

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('FACTX_MOD:OPENCPFACTX',0,ZHOOK_HANDLE)

IF (YDFACTX%IUNITCP /= 0) THEN
  IF (LHOOK) CALL DR_HOOK('FACTX_MOD:OPENCPFACTX',1,ZHOOK_HANDLE)
  RETURN
ENDIF


IF (YDFACTX%IDOM == 0) THEN
  YDFACTX%IUNITCP=-NSCRTCH
ELSE
  CALL GETFPLUN(YDFACTX%IDOM,IULFP)
  YDFACTX%IUNITCP=-IULFP
ENDIF

IF (YDFACTX%YIOOPTS%LFACRW) YDFACTX%IUNITCP=-YDFACTX%IUNITCP

IUNIT=YDFACTX%IUNITCP

INBARP=0
INBARI=0
CALL FANOUV(IREP,IUNIT,.FALSE.,CLSCRTCH,'NEW',.TRUE.,&
         & .TRUE.,1_JPIM,INBARP,INBARI,CDFPCA)

! Set date used for compression
CALL FANDAX (IREP, IUNIT, YDFACTX%IDATEF)

! Other settings

CALL FAREGU (IUNIT, 'CMODEL='//TRIM (CMODEL), 1, 1)
CALL FAREGU (IUNIT, 'IDCEN', NIDCEN, 1)

IF (LEXTERN .AND. YDFACTX%IDOM > 0) THEN
  CALL FAREGU (IUNIT, 'EXTERN', 1_JPIM,  1_JPIM)
ENDIF

IF (YDFACTX%YIOOPTS%LFACPT_OPENMP) THEN

  INTID = OML_MAX_THREADS ()

  ALLOCATE (YDFACTX%YFABYTID (INTID))
  
  CALL FADUPU1 (YDFACTX%YFAPARAM, IUNIT)

  YDFACTX%YFAPARAM%LOUVR = .FALSE. 
  YDFACTX%YFAPARAM%LNOMM = .FALSE.

!$OMP PARALLEL PRIVATE (IITID)
    
  IITID = OML_MY_THREAD ()
   
  CALL FADUPU2 (YDFACTX%YFABYTID (IITID), YDFACTX%YFAPARAM, IUNIT)
    
!$OMP END PARALLEL
  
ENDIF

IF (LHOOK) CALL DR_HOOK('FACTX_MOD:OPENCPFACTX',1,ZHOOK_HANDLE)

END SUBROUTINE OPENCPFACTX

SUBROUTINE OPENWRFACTX(YDVAB,YDFACTX,CDFIC,CDFPCA,YDGEOMETRY,YDFPUSERGEO,KFPCHKDAT)

! Logical units
USE GEOMETRY_MOD , ONLY : GEOMETRY
USE YOMVERT  , ONLY : TVAB
USE YOMLUN       , ONLY : NULOUT, NTRJSH

! Model

USE YOMMP0, ONLY : MYPROC  
USE YOMCT0, ONLY : LELAM, NQUAD
USE YOMCST, ONLY : RDAY

! Fullpos

USE YOMFA, ONLY : LEXTERN
USE TYPE_FPUSERGEO, ONLY : TFPUSERGEO

! Arguments

TYPE(TVAB)     ,  INTENT(IN)    :: YDVAB
TYPE (FACTX),     INTENT (INOUT)      :: YDFACTX
CHARACTER(LEN=*), INTENT(IN) :: CDFIC
CHARACTER(LEN=*), INTENT(IN) :: CDFPCA
TYPE (GEOMETRY),  INTENT(IN), TARGET, OPTIONAL :: YDGEOMETRY
TYPE (TFPUSERGEO),INTENT(IN), OPTIONAL  :: YDFPUSERGEO
INTEGER(KIND=JPIM),INTENT(IN), OPTIONAL  :: KFPCHKDAT
! Model
#include "chien.h"

#include "cchien.intfb.h"

INTEGER(KIND=JPIM), PARAMETER :: JPLENPROC=6
CHARACTER (LEN=LEN_TRIM(CDFIC)+JPLENPROC) :: CLFIC
CHARACTER(LEN=16)  :: CLLEC
INTEGER(KIND=JPIM) :: IDATEF(22)
INTEGER(KIND=JPIM) :: INBARI, INBARP, INIMES, IREP
INTEGER(KIND=JPIM) :: ISSS, ISSSP, ITEST, IPPDATE(22)
INTEGER(KIND=JPIM) :: IUNIT, IFILE
INTEGER(KIND=JPIM) :: INTROU, INARES, INAMAX, INALDO
LOGICAL            :: LLEXIST, LLFICP 
REAL(KIND=JPRB)    :: ZEPS, ZFTIME, ZMTIME

! Fullpos

#include "getfplun.intfb.h"
#include "openfpfa.intfb.h"

#include "abor1.intfb.h"

CHARACTER(LEN=JPLENPROC)              :: CLPROC
TYPE (GEOMETRY), POINTER              :: YLGEOMETRY

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

#include "fcttim.func.h"

IF (LHOOK) CALL DR_HOOK('FACTX_MOD:OPENWRFACTX',0,ZHOOK_HANDLE)

IF (YDFACTX%IUNITWR /= 0) THEN
  IF (LHOOK) CALL DR_HOOK('FACTX_MOD:OPENWRFACTX',1,ZHOOK_HANDLE)
  RETURN
ENDIF

WRITE (CLPROC,'("_",I5.5)') MYPROC
IF (YDFACTX%YIOOPTS%LFILN) THEN
  CLFIC=TRIM(CDFIC)//CLPROC
ELSE
  CLFIC=TRIM(CDFIC)
ENDIF


IF (PRESENT(YDGEOMETRY)) THEN

  IPPDATE(:)=YDFACTX%IDATEF(:)
  INBARI=0
  INBARP=0
  INIMES=0

  
!            OPEN FILES & CHECK COHERENCY
!            ----------------------------
  
  YDFACTX%IUNITWR=NTRJSH
  IF (YDFACTX%YIOOPTS%LFACRW) YDFACTX%IUNITWR=-YDFACTX%IUNITWR
  
  IUNIT=YDFACTX%IUNITWR
  IF (YDFACTX%YIOOPTS%NFACTM /= 0) THEN
    CALL LFIAFM (IREP, IUNIT, YDFACTX%YIOOPTS%NFACTM)
  ENDIF
  
  INQUIRE (FILE=CLFIC, EXIST=LLEXIST) 
  
  IF (LLEXIST) THEN
    CLLEC ='CADRE.SORTIE    '
    CALL FAITOU (IREP,IUNIT,.TRUE.,CLFIC,'OLD',.TRUE.,.TRUE.,INIMES,INBARP,INBARI,CLLEC)
  
    ZEPS=EPSILON(1.0_JPRB)*10000.0_JPRB
    ITEST=0
    YLGEOMETRY => YDGEOMETRY
    IF (LELAM) THEN
      CALL CCHIEN (YLGEOMETRY, CLLEC, IUNIT, ITEST)
    ELSE
      CALL CHIEN(CLLEC,YLGEOMETRY%YRGEM%NSTTYP,YLGEOMETRY%YRGEM%RMUCEN,YLGEOMETRY%YRGEM%RLOCEN, &
       & YLGEOMETRY%YRGEM%RSTRET,YLGEOMETRY%YRDIM%NSMAX,&
       & YLGEOMETRY%YRDIM%NDGLG,YLGEOMETRY%YRDIM%NDLON,YLGEOMETRY%YRGEM%NLOENG(1:YLGEOMETRY%YRDIM%NDGLG),&
       & YLGEOMETRY%YRGEM%NMENG(1:YLGEOMETRY%YRDIM%NDGLG),YLGEOMETRY%YRGEM%NHTYP,UBOUND(YDVAB%VBH,DIM=1),VP00,&
       & YDVAB%VALH,YDVAB%VBH,NQUAD,ITEST,1,&
       & YLGEOMETRY%YRDIM%NDGLG,ZEPS,LLFICP,NULOUT)  
    ENDIF
  ELSE
    CALL FAITOU (IREP,IUNIT,.TRUE.,CLFIC,'UNKNOWN',.TRUE.,.TRUE., INIMES,INBARP,INBARI,CDFPCA)  
  ENDIF
  
  CALL LFIMST(IREP,IUNIT,.FALSE.)
  
  IF (LLEXIST) THEN
    CALL FADIEX(IREP,IUNIT,IDATEF)
    WRITE(NULOUT,*) 'YOU READ OUTPUT FIELDS OF THE : ',IDATEF(3),' ',&
         & IDATEF(2),' ',IDATEF(1)  
    WRITE(NULOUT,*) ' HOUR AND MINUTE : ',IDATEF(4),'  ',IDATEF(5)
    IF (IDATEF(6) == 1) THEN
      WRITE(NULOUT,*) ' FORECAST RANGE IN HOURS : ',IDATEF(7)
    ELSEIF (IDATEF(6) == 2) THEN
      WRITE(NULOUT,*) ' FORECAST RANGE IN DAYS : ',IDATEF(7)
      IDATEF(7)=IDATEF(7)*24
      WRITE(NULOUT,*) ' EQUIVALENCE IN DAYS : ',IDATEF(7)
    ELSE
      WRITE(NULOUT,*) ' ERROR IN KDATE(6) : ',IDATEF(6)
      CALL ABOR1('OPENWRFACTX : ABOR1 CALLED')
    ENDIF
!   Test time range
    ISSS=IDATEF(7)*3600+IDATEF(4)*3600+IDATEF(5)*60
    ISSSP=IPPDATE(7)*3600+IPPDATE(4)*3600+IPPDATE(5)*60
    ZFTIME=RTIME(IDATEF(1),IDATEF(2),IDATEF(3),ISSS,RDAY)
    ZMTIME=RTIME(IPPDATE(1),IPPDATE(2),IPPDATE(3),ISSSP,RDAY) 
!    ZMTIME=RTIME(IPPDATE(1),IPPDATE(2),IPPDATE(3),ISSS,RDAY)
    IF (ZMTIME > ZFTIME+1800.0_JPRB.OR.ZMTIME <= ZFTIME-1800.0_JPRB) THEN  
      WRITE(NULOUT,*) ' DATE INCOHERENCE BETWEEN OUTPUT FILE AND MODEL'
      WRITE(NULOUT,*) ' DATE OF THE MODEL : ',IPPDATE(3),' ',IPPDATE(2),' ',IPPDATE(1)
      WRITE(NULOUT,*) ' SECONDS IN THE DAY :',ISSSP
!      WRITE(NULOUT,*) ' SECONDS IN THE DAY :',ISSS
      WRITE(NULOUT,*) ' IN HOUR         :',NCTH(ISSSP)
!      WRITE(NULOUT,*) ' IN HOUR         :',NCTH(ISSS)
      CALL ABOR1('OPENWRFACTX : ABOR1 CALLED')
    ENDIF
  ENDIF
  
  CALL FANDAX(IREP,IUNIT,IPPDATE)
  
!  IDENTIFICATEUR DES FICHIERS D EBAUCHES
  
  IF (LELAM) THEN
    CALL FAUTIF(IREP,IUNIT,'ALD')
  ELSE
    CALL FAUTIF(IREP,IUNIT,'ARP')
  ENDIF
  
  YDFACTX%LEXIST=LLEXIST
  IF (.NOT. LLEXIST) THEN
    CALL LFINAF (IREP, IUNIT, INALDO, INTROU, INARES, INAMAX)
    YDFACTX%INIART=INALDO
  ENDIF

ELSEIF (PRESENT(YDFPUSERGEO)) THEN

  CALL GETFPLUN(YDFACTX%IDOM,YDFACTX%IUNITWR)
  IF (YDFACTX%YIOOPTS%LFACRW) YDFACTX%IUNITWR=-YDFACTX%IUNITWR
  IUNIT=YDFACTX%IUNITWR

  IFILE = 0
  IF (YDFPUSERGEO%CFPGRID=='GAUSS') THEN
    WRITE(UNIT=CLLEC,FMT='(''FULPOS.ARP.'',I1.1,I2.2,I2.2)') IFILE, YDFACTX%IDOM, IUNIT
  ELSE
    WRITE(UNIT=CLLEC,FMT='(''FULPOS.ALD.'',I1.1,I2.2,I2.2)') IFILE, YDFACTX%IDOM, IUNIT
  ENDIF
  CALL OPENFPFA (YDFPUSERGEO, IUNIT, CLFIC, CLLEC, KFILE=IFILE,&
               & KDATE=YDFACTX%IDATEF, CDFPCACREATE=CDFPCA, KFPCHKDAT=KFPCHKDAT,YDVAB=YDVAB)
 
  IF (LEXTERN) THEN
    CALL FAREGU (IUNIT, 'EXTERN', 1_JPIM,  1_JPIM)
  ENDIF

ELSE
  CALL ABOR1('FACTX_MOD:OPENWRFACTX : UNSPECIFIED GEOMETRY')
ENDIF

IF (YDFACTX%YIOOPTS%LUCFA) THEN
  CALL WRFLDCW_OPEN (YDFACTX%YWCTX, YDFACTX%IUNITWR)
  YDFACTX%LCFA = .TRUE.
ENDIF

IF (LHOOK) CALL DR_HOOK('FACTX_MOD:OPENWRFACTX',1,ZHOOK_HANDLE)

END SUBROUTINE OPENWRFACTX

SUBROUTINE NEWFACTX(YDFACTX, YDIOOPTS, CDCONF, KDOM)

!**** *NEWFACTX*  New model FA context

!     Purpose.
!     --------
!           To setup a new set of FA parameters from model or fullpos, and general options

!        Explicit arguments :
!        --------------------
!          YDIOOPTS : general I/O options
!          CDCONF   : configuration of IOPACK
!          KDOM     : rank of the current set
!          YDFACTX  : current FA context

!     Externals.
!     ----------

!     Author.
!     -------
!      R. El Khatib *METEO-FRANCE*
!      Original : 28-Apr-2017 re-write from older NEWFACTX

!     Modifications.
!     --------------
!     ------------------------------------------------------------------

TYPE (FACTX)       , INTENT(OUT)           :: YDFACTX
TYPE (MFIOOPTS)    , INTENT(IN)            :: YDIOOPTS
CHARACTER(LEN=1)   , INTENT(IN),  OPTIONAL :: CDCONF 
INTEGER (KIND=JPIM), INTENT(IN),  OPTIONAL :: KDOM

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

#include "abor1.intfb.h"

IF (LHOOK) CALL DR_HOOK('FACTX_MOD:NEWFACTX',0,ZHOOK_HANDLE)

IF (PRESENT (KDOM)) THEN

  ! Fullpos context

  IF (KDOM == 0) CALL ABOR1 ('FACTX_MOD:NEWFACTX: KDOM MUST BE POSITIVE')

  YDFACTX%IDOM       = KDOM

ELSEIF (PRESENT (CDCONF)) THEN

  ! model context

  YDFACTX%CONF       = CDCONF

ELSE

  CALL ABOR1 ('FACTX_MOD:NEWFACTX: EITHER KDOM OR CDCONF IS REQUIRED')

ENDIF

YDFACTX%YIOOPTS    = YDIOOPTS

IF (LHOOK) CALL DR_HOOK('FACTX_MOD:NEWFACTX',1,ZHOOK_HANDLE)

END SUBROUTINE NEWFACTX

END MODULE FACTX_MOD
