SUBROUTINE FAOFF (YDFACTX)

TYPE (FACTX), INTENT (INOUT) :: YDFACTX

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('FACTX_MOD:FAOFF',0,ZHOOK_HANDLE)

