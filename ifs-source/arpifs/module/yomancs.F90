!*
!      YOMANCS - ANALYSIS NUMERICAL CONSTANTS
!                     DEFINED IN SUBROUTINE SUANCT

!        D. VASILJEVIC     ECMWF    06/03/1990

!     ANALYSIS NUMERICAL CONSTANTS
!     VARIABLE / TYPE /                   MEANING                      /
!     --------   ----                     -------
!      NMDI       I      MISSING INDICATOR (INTEGER)
!      RMDI       I      MISSING INDICATOR (REAL)
!      RMDB       I      BUFR MISSING INDICATOR
!      RUPMLIM
!      RDWMLIM
!      EPSBUFR
!      NBITS   () I      MASKING CONSTANTS
!      NOBTIS     I      NO. OF BITS IN A WORD
!      NOBYTES    I       NO. OF BYTES PER WORD
!      NCMBYTES   I      NO. OF BYTES USED BY CMA
!      RADIANS    R      RADIANS
!      RDEGREES   R      DEGREES
!      EPSMAC     R      MACHINE PRECISION
!      RSQRT2PI   R      SQRT(2*PI), USED IN VARIOATIONAL QC
!      RNLATLIM   R      LAT. LIMIT NORTH
!      RSLATLIM   R      LAT. LIMIT SOUTH
!      RWLONLIM   R      LON. LIMIT WEST
!      RELONLIM   R      LON. LIMIT EAST
!      RSYNANMH   R      SYNOP (NON-SHIP) ANEMOMETER HEIGHT
!      RSYNBARH   R      SYNOP (NON-SHIP) BAROMETER HEIGHT
!      RSHIPDH    R      SHIP DECK HEIGHT
!      RSANMOMH   R      SHIP ANEMOMETER HEIGHT
!      RSBAROMH   R      SHIP BAROMETER HEIGHT
!      RDRIBUDH   R      DRIBU DECK HEIGHT
!      RDANMOMH   R      DRIBU ANEMOMETER HEIGHT
!      RDBAROMH   R      DRIBU BAROMETER HEIGHT

!      RDAYPYEA   I    NO. OF DAYS PER YEAR
!      RHOUPDAY   I    NO. HOURS PER DAY
!      NHOUPDAY   I    NO. HOURS PER DAY
!      RMINPHOU   I    NO. OF MINUTES PER HOUR
!      NMINPHOU   I    NO. OF MINUTES PER HOUR
!      RSECPMIN   I    NO. OF SECONDES PER HOUR
!      NSECPMIN   I    NO. OF SECONDES PER HOUR
!      RDAYFINH   I    FRACTION OF A DAY IN HOURS
!      NDAYFINH   I    FRACTION OF A DAY IN HOURS
!      RREDPERR   I    PERSISTENCE ERROR REDUCTION
!      RREDPEW1   I    PERSISTENCE ERROR WEIGHT REDUCTION 1
!      RREDPEW2   I    PERSISTENCE ERROR WEIGHT REDUCTION 2
!      RGWOSW     R    RATIO OF GEOSTROPHIC TO SURFACE WIND OVER SEA
!      RAIRDEN    R    DENSITY OF AIR
!      RSHPCO     R    CONSTANT FOR SHIP PRESSURE TENDENCY CORRECTION
!      RSINAG     R    SIN OF ANGLE BETWEEN ACTUAL AND GEOSTROPHIC WIND
!      RCOSAG     R    COS OF ANGLE BETWEEN ACTUAL AND GEOSTROPHIC WIND
!      RPAPM      R    PRESSURE CONVERSION

!      RT0        R
!      RKELVN     R
!      RTT        R    =RT0=273.16= TRIPLE POINT TEMPERATURE
!      R2ES       R    CONSTANTS USED FOR COMPUTATION OF SATURATION
!      R3LES      R    MIXING RATIO OVER LIQUID WATER(*R_LES*) OR
!      R3IES      R    ICE(*R_IES*).
!      R4LES      R
!      R4IES      R
!      RTWAT      R    RTWAT=RTT
!      RTICE      R    RTICE=RTT-23
!      RRD        R    DRY AIR GAS CONSTANT
!      RRV        R    WATER VAPOUR GAS CONSTANT
!
!     Cruse_dpdt  R    Assumed DpDt for crusing
!     Ascend_dpdt R    Assumed DpDt for ascending
!     Dscend_dpdt R    Assumed DpDt for descending


