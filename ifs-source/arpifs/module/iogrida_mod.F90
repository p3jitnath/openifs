SUBROUTINE IOGRIDA_COUNT(YDGEOMETRY,YDSURF,YDMCC,YDDPHY,YDPHY,YDPHYDS,KACTION,KFNUM)
!     O. Marsden: June 2015 CY42 YRGMV, YRGFL, YRSURF, YRGMV5, and YRGFL5 are now passed by argument

USE YOMDPHY            , ONLY : TDPHY
USE YOMMCC             , ONLY : TMCC
USE YOMPHY             , ONLY : TPHY
USE YOMPHYDS           , ONLY : TPHYDS
USE GEOMETRY_MOD       , ONLY : GEOMETRY
USE SURFACE_FIELDS_MIX , ONLY : TSURF
IMPLICIT NONE


TYPE (GEOMETRY),     INTENT (IN)    :: YDGEOMETRY
TYPE (TSURF),        INTENT (INOUT) :: YDSURF
TYPE (TDPHY),        INTENT (IN)    :: YDDPHY
TYPE (TMCC),         INTENT (IN)    :: YDMCC
TYPE (TPHY),         INTENT (IN)    :: YDPHY
TYPE (TPHYDS),       INTENT (IN)    :: YDPHYDS
INTEGER (KIND=JPIM), INTENT (IN)    :: KACTION
INTEGER (KIND=JPIM), INTENT (INOUT) :: KFNUM

INTEGER (KIND=JPIM) :: IFCUR

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('IOGRIDA_MOD:IOGRIDA_COUNT',0,ZHOOK_HANDLE)

IFCUR = 0
CALL IOGRIDA_SELECT_WH(YDGEOMETRY,YDSURF,YDMCC,YDDPHY,YDPHY,YDPHYDS,KACTION,KFCUR=IFCUR)
KFNUM = KFNUM + IFCUR

IF (LHOOK) CALL DR_HOOK('IOGRIDA_MOD:IOGRIDA_COUNT',1,ZHOOK_HANDLE)

END SUBROUTINE IOGRIDA_COUNT

