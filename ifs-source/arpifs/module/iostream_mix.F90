SUBROUTINE GRIB_UPDATE_MESSAGE(KGRIB_HANDLE,KGRIBCD,KLEV,CDREPR,CDLTYPE,PDATA,&
 & KSMAX,LDGRAD,KTOP,KBOT,KGRIB_LEN,KGRIB)
IMPLICIT NONE

INTEGER(KIND=JPIM),INTENT(IN)    :: KGRIB_HANDLE
INTEGER(KIND=JPIM),INTENT(IN)    :: KGRIBCD
INTEGER(KIND=JPIM),INTENT(IN)    :: KLEV
CHARACTER         ,INTENT(IN)    :: CDREPR*(*)
CHARACTER         ,INTENT(IN)    :: CDLTYPE*(*)
REAL(KIND=JPRB)   ,INTENT(IN)    :: PDATA(:)
INTEGER(KIND=JPIM),INTENT(IN)    :: KSMAX 
LOGICAL           ,INTENT(IN)    :: LDGRAD
INTEGER(KIND=JPIM),INTENT(IN)    :: KTOP
INTEGER(KIND=JPIM),INTENT(IN)    :: KBOT
INTEGER(KIND=JPKSIZE_T),INTENT(INOUT) :: KGRIB_LEN
INTEGER(KIND=JPIM),INTENT(OUT)   :: KGRIB(:)

INTEGER(KIND=JPIM) :: ILENINT
LOGICAL            :: LLSPEC
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
!----------------------------------------------------------------------------
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:GRIB_UPDATE_MESSAGE',0,ZHOOK_HANDLE)

! Packing
LLSPEC = CDREPR == 'SH'
CALL GRIB_SET_PACKING(KGRIB_HANDLE,KGRIBCD,CDLTYPE,LLSPEC,LDGRAD,PDATA,KSMAX)

!*        1.2   LEVEL(S)
!$OMP CRITICAL
CALL GRIB_SET_LEVELS(KGRIB_HANDLE,KGRIBCD,KLEV,KBOT,KTOP,CDLTYPE)
!$OMP END CRITICAL

!*       1.3   PARAMETER

CALL GRIB_SET_PARAMETER(KGRIB_HANDLE,KGRIBCD,KLEV,&
 & YIOS%MSERIES,YIOS%MSATID,YIOS%MINST,YIOS%MCHAN,YIOS%RCWN)

!*        1.4   VALUES

CALL IGRIB_SET_VALUE(KGRIB_HANDLE,'values',PDATA)
CALL IGRIB_GET_MESSAGE_SIZE(KGRIB_HANDLE,KGRIB_LEN)
ILENINT = (KGRIB_LEN+YIOS%NINTLEN-1)/YIOS%NINTLEN
IF(SIZE(KGRIB) < ILENINT) THEN
  WRITE(YIOS%NULERR,*) 'KGRIB TOO SMALL ',SIZE(KGRIB),ILENINT,KGRIB_LEN
  CALL ABOR1('IOSTREAM_MIX:GRIB_UPDATE_MESSAGE - KGRIB TOO SMALL ')
ENDIF
CALL IGRIB_GET_MESSAGE(KGRIB_HANDLE,KGRIB)
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:GRIB_UPDATE_MESSAGE',1,ZHOOK_HANDLE)
END SUBROUTINE GRIB_UPDATE_MESSAGE
