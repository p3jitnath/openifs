SUBROUTINE INQUIRE_GRIB(KGRIB_HANDLE,KPARAM,KLEV,KSMAX,KNVALS,&
 & CDREPRT,KLATS,KLONS,PLATS,&
 & KDATE,KTIME,KSTEP,KFLEV,PVALH,PVBH,PA,PB,KPTS,&
 & KSVCOMP,KRITZ1,KRITZ2,KNUMB)

!**** *INQUIRE_GRIB*  - Find info from GRIB header

! Purpose - Find info from GRIB header

! Explicit arguments :
! --------------------
! KRIB_HANDLE - Grib API handle
! KPARAM  - GRIB parameter number
! KLEV    - level
! KSMAX   - spectral truncation
! KNVALS  - number of values
! CDREPRT  - representation type (spectral, gridpoint)
! KLATS   - number of latitude rows
! KLONS   - number of points on each latitude row
! KPTS    - total number of points 
! PLATS   - latitude of each row [radians]
! KDATE   - date of data (yyyymmdd)
! KTIME   - time of data (seconds)
! KSTEP   - time of fc (seconds)
! PA, PB  - pressure on level is given by p = PA + ps*PB where ps is
!           surface pressure
!-------------------------------------------------------------------------

IMPLICIT NONE
INTEGER(KIND=JPIM),INTENT(IN) :: KGRIB_HANDLE

INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KPARAM
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KLEV
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KSMAX
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KNVALS
CHARACTER(LEN=*)  ,OPTIONAL,INTENT(OUT) :: CDREPRT
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KLATS
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KLONS(:)
REAL   (KIND=JPRB),OPTIONAL,INTENT(OUT) :: PLATS(:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KPTS
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KDATE
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KTIME
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KSTEP
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KFLEV
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KSVCOMP
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KRITZ1
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KRITZ2
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KNUMB
REAL   (KIND=JPRB),OPTIONAL,INTENT(OUT) :: PVALH(0:)
REAL   (KIND=JPRB),OPTIONAL,INTENT(OUT) :: PVBH (0:)
REAL   (KIND=JPRB),OPTIONAL,INTENT(OUT) :: PA
REAL   (KIND=JPRB),OPTIONAL,INTENT(OUT) :: PB

INTEGER(KIND=JPIM) :: ITIME
INTEGER(KIND=JPIM) :: ILATS,JLAT, IPAR, IFLEV
CHARACTER(LEN=32)  :: CLVAL
INTEGER(KIND=JPIM) :: IVAL,ILONS,IPLPRESENT,ILEV,INVALS
INTEGER(KIND=JPIM) :: IJP
REAL   (KIND=JPRB) :: ZDTOR, ZDLAT,ZNP,ZSP,ZINCJ
REAL   (KIND=JPRB),ALLOCATABLE :: ZVALS(:)
INTEGER(KIND=JPIM),ALLOCATABLE :: IREDGRID(:)
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!-------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:INQUIRE_GRIB',0,ZHOOK_HANDLE)

IF(PRESENT(KDATE)) CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'dataDate',KDATE)
IF(PRESENT(KTIME)) THEN
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'dataTime',ITIME)
  KTIME = (ITIME/100)*3600+MOD(ITIME,100)*60
ENDIF
IF(PRESENT(KSTEP)) THEN
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'step',IVAL)
  KSTEP=IVAL*3600
ENDIF
IF(PRESENT(KPARAM)) THEN
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'paramId',IVAL)
  IF (IVAL .EQ. 129172) THEN ! lsmgrd is currently used for C3_C4_map (photosynthesis pathway)
    KPARAM = IVAL
  ELSEIF (IVAL .EQ. 200199) THEN ! Vegdiff is currently used for urban
    KPARAM = IVAL
  ELSEIF (IVAL .EQ. 200026) THEN ! Lakediff is currently used for wetlands
    KPARAM = IVAL
  ELSEIF (IVAL > 129000 .AND. IVAL < 129999) THEN
    KPARAM = IVAL - 129000
  ELSEIF (IVAL > 200000 .AND. IVAL < 200999) THEN
    KPARAM = IVAL - 200000
  ELSEIF (IVAL > 211000 .AND. IVAL < 211999) THEN
    KPARAM = IVAL - 1000
  ELSE
    KPARAM = IVAL
  ENDIF
ENDIF

IF(PRESENT(KFLEV)) THEN
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'numberOfVerticalCoordinateValues',IVAL)
  KFLEV = IVAL/2-1
ENDIF
IF(PRESENT(PVALH)) THEN
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'numberOfVerticalCoordinateValues',IVAL)
  IFLEV = IVAL/2-1
  IF(UBOUND(PVALH,1) < IFLEV)&
   & CALL ABOR1 ('IOSTREAM_MIX:INQUIRE_GRIB - PVALH TOO SMALL')
  ALLOCATE(ZVALS(2*(IFLEV+1)))
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'pv',ZVALS)
  PVALH(0:IFLEV) =  ZVALS(1:IFLEV+1)
  DEALLOCATE(ZVALS)
ENDIF
IF(PRESENT(PVBH)) THEN
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'numberOfVerticalCoordinateValues',IVAL)
  IFLEV = IVAL/2-1
  IF(UBOUND(PVBH,1) < IFLEV)&
   & CALL ABOR1 ('IOSTREAM_MIX:INQUIRE_GRIB - PVBH TOO SMALL')
  ALLOCATE(ZVALS(2*(IFLEV+1)))
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'pv',ZVALS)
  PVBH(0:IFLEV) =  ZVALS(IFLEV+2:2*(IFLEV+1))
  DEALLOCATE(ZVALS)
ENDIF
IF(PRESENT(KLEV)) THEN
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'paramId',IPAR)
  IF(IPAR == NGRBBTMP) THEN                 ! TOVS channel number
    CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'channelNumber',IVAL)
  ELSE
    CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'level',IVAL)
  ENDIF
  KLEV = IVAL
ENDIF

IF(PRESENT(KSVCOMP)) THEN
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'numberOfSingularVectorsComputed',KSVCOMP)
ENDIF
IF(PRESENT(KRITZ1)) THEN
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'NINT_LOG10_RITZ',KRITZ1)
ENDIF
IF(PRESENT(KRITZ2)) THEN
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'NINT_RITZ_EXP',KRITZ2)
ENDIF
IF(PRESENT(KNUMB)) THEN
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'forecastOrSingularVectorNumber',KNUMB)
ENDIF

IF(PRESENT(CDREPRT)) THEN
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'gridType',CDREPRT)
ENDIF

!Grid INFO
IF(PRESENT(KLATS) .OR. PRESENT(KLONS) .OR. PRESENT(PLATS) .OR.&
 & PRESENT(KPTS) ) THEN
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'gridType',CLVAL)
  IF ( TRIM(CLVAL) /= 'sh' ) THEN
    CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'numberOfPointsAlongAMeridian',ILATS)
    IF(PRESENT(KLATS)) KLATS = ILATS
    IF(PRESENT(KLONS) .OR. PRESENT(PLATS) .OR. PRESENT(KPTS) ) THEN
      IF(PRESENT(KLONS)) THEN
        IF(SIZE(KLONS) < ILATS)&
         & CALL ABOR1('IOSTREAM_MIX:INQUIRE_GRIB - KLONS TOO SMALL')
      ENDIF
      IF(PRESENT(PLATS)) THEN
        IF(SIZE(PLATS) < ILATS)&
         & CALL ABOR1('IOSTREAM_MIX:INQUIRE_GRIB - PLATS TOO SMALL')
      ENDIF
      ZDTOR = YIOS%RPI/180._JPRB
      IF (TRIM(CLVAL) == 'regular_ll' .OR. TRIM(CLVAL) == 'reduced_ll') THEN ! Lat long grid
        CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'numberOfPointsAlongAParallel',ILONS)
        IF(PRESENT(KLONS)) KLONS(:) = ILONS
        IF(PRESENT(KPTS))  KPTS = ILATS*ILONS
        IF(PRESENT(PLATS))THEN
          CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'latitudeOfFirstGridPointInDegrees',ZNP)
          CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'jDirectionIncrementInDegrees',ZINCJ)
          CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'jScansPositively',IJP)
          IF (IJP .EQ. 1) THEN
            ZINCJ=-ZINCJ
          END IF
          DO JLAT=1,ILATS
            PLATS(JLAT)=ZDTOR*(ZNP-(JLAT-1)*ZINCJ)
          ENDDO
        ENDIF
      ELSEIF (TRIM(CLVAL) == 'regular_gg' .OR. TRIM(CLVAL) == 'reduced_gg') THEN ! Gaussian grid
        CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'latitudeOfFirstGridPointInDegrees',ZNP)
        CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'latitudeOfLastGridPointInDegrees',ZSP)
        ZDLAT = ZDTOR*(ZSP-ZNP) / REAL(ILATS-1,JPRB)
        IF(PRESENT(KLONS)) THEN
          CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'PLPresent',IPLPRESENT)
          IF (IPLPRESENT == 0) THEN
            CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'numberOfPointsAlongAParallel',ILONS)
            KLONS(:) = ILONS
            ELSE
            CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'pl',KLONS)
            ENDIF
        ENDIF
        IF(PRESENT(KPTS)) THEN
          CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'PLPresent',IPLPRESENT)
          IF (IPLPRESENT == 0) THEN
            CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'numberOfPointsAlongAParallel',ILONS)
            KPTS = ILONS*ILATS
            ELSE
            ALLOCATE(IREDGRID(ILATS))
            CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'pl',IREDGRID)
            KPTS = SUM(IREDGRID(1:ILATS))
            DEALLOCATE(IREDGRID)
            ENDIF
        ENDIF
        IF(PRESENT(PLATS))THEN
          DO JLAT=1,ILATS
            PLATS(JLAT) = ZDTOR*ZNP + (JLAT-1)*ZDLAT
          ENDDO
        ENDIF
      ENDIF
    ENDIF
  ELSE
    CALL ABOR1('IOSTREAM_MIX:INQUIRE_GRIB - GRID TYPE NOT RECOGNIZED')
  ENDIF
ENDIF

IF(PRESENT(PA) .AND. PRESENT(PB)) THEN
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'paramId',IPAR)
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'typeOfLevel',CLVAL)
  IF(IPAR == NGRBBTMP) THEN
    CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'channelNumber',IVAL)
    PA = IVAL                                  ! TOVS channel number
    PB = 0.0_JPRB
  ELSEIF (TRIM(CLVAL) == 'surface') THEN        ! Surface
    PA = 1.0_JPRB
    PB = 0.0_JPRB
  ELSEIF (TRIM(CLVAL) == 'isobaricInhPa') THEN  ! Pressure level
    CALL ABOR1('IOSTREAM_MIX:INQUIRE_GRIB - Pressure level')
  ELSEIF (TRIM(CLVAL) == 'hybrid') THEN         ! Model level
    CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'numberOfVerticalCoordinateValues',IVAL)
    CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'level',ILEV)
    IF (IVAL == 0) THEN
      PA = 0.5_JPRB*(YIOS%VAH(ILEV-1)+YIOS%VAH(ILEV  ))
      PB = 0.5_JPRB*(YIOS%VBH(ILEV-1)+YIOS%VBH(ILEV  ))
    ELSE
      IFLEV = IVAL/2-1
      ALLOCATE(ZVALS(2*(IFLEV+1)))
      CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'pv',ZVALS)
      PA = 0.5_JPRB*(ZVALS(ILEV)+ZVALS(ILEV+1))
      PB = 0.5_JPRB*(ZVALS(ILEV+IVAL/2)+ZVALS(ILEV+1+IVAL/2))
      DEALLOCATE(ZVALS)
    ENDIF
  ENDIF
ENDIF  

IF(PRESENT(KSMAX)) THEN
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'gridType',CLVAL)
  IF ( TRIM(CLVAL) /= 'sh' )&
   & CALL ABOR1('IOSTREAM_MIX:INQUIRE_GRIB - KSMAX ONLY FOR SPECTRAL')
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'pentagonalResolutionParameterJ',IVAL)
  KSMAX = IVAL
ENDIF
IF (PRESENT(KNVALS)) THEN
  CALL IGRIB_GET_VALUE(KGRIB_HANDLE,'numberOfValues',INVALS)
  KNVALS = INVALS
ENDIF
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:INQUIRE_GRIB',1,ZHOOK_HANDLE)

END SUBROUTINE INQUIRE_GRIB
