SUBROUTINE SETUP_IOSTREAM(YD,CDTYPE,CDPATH,CDMODE,KIOMASTER,&
 & KIOPROCS,LDNOHELP,KNVALS,LSERIAL)

!**** *SETUP_IOSTREAM*  - Setup IO stream

! Purpose - To initiate an io-stream, open file if applicable etc.

! Explicit arguments :
! --------------------
! YD        - IO stream structure
! CDTYPE    - IO type (PBIO,FDB ...)
! CDPATH    - File path name
! CDMODE    - read/write/append mode
! KIOMASTER - processor to perform IO (takes presedence over KIOPROCS if present)
! KIOPROCS  - processors to perform IO (in neither KIOPROCS or KIOMASTER present 
!             all processors do perform IO)
! LDNOHELP  - if true don't use io helpers
! KNVALS    -  numberOfValues for data in file

!-------------------------------------------------------------------------
IMPLICIT NONE


TYPE(TYPE_IOSTREAM)        ,INTENT(INOUT) :: YD
CHARACTER(LEN=*)           ,INTENT(IN)    :: CDTYPE
CHARACTER(LEN=*)  ,OPTIONAL,INTENT(IN)    :: CDPATH
CHARACTER(LEN=1)  ,OPTIONAL,INTENT(IN)    :: CDMODE
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN)    :: KIOMASTER
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN)    :: KIOPROCS(:)
LOGICAL           ,OPTIONAL,INTENT(IN)    :: LDNOHELP
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN)    :: KNVALS
LOGICAL           ,OPTIONAL,INTENT(IN)    :: LSERIAL

INTEGER(KIND=JPIM) :: IRET,JROC,IROC,INODE
INTEGER(KIND=JPIM) :: JHELP,IOPROC,JLOC,IPROC,IHELP,JNODE
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
REAL(KIND=JPRD)    :: ZTIME0,ZTIME1
LOGICAL            :: LLUSED(YIOS%NPROC)

!-------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:SETUP_IOSTREAM',0,ZHOOK_HANDLE)
IF(.NOT. LPACK_INIT) CALL ABOR1('IOSTREAM_MIX:SETUP_IOSTREAM - CALL INI_IOSTREAM FIRST')
CALL USER_CLOCK(PELAPSED_TIME=ZTIME0)

! Set defaults
YD%LEOF       = .FALSE.
YD%NRECS      = 0
YD%NBYTES     = 0
YD%LIOPROC    = .FALSE.
YD%LIOPROCHLP = .FALSE.
YD%LIONOHELP  = .FALSE.
YD%LIOUSEHELP = .FALSE.
YD%NUMIOPROCS = 0
YD%NUMIOPROCSHLP = 0
YD%MIOPROCHLP = -99
YD%CPATH      = 'UNDEF'
YD%CMODE      = 'r'
YD%IOMASTER   = JPNONDEF
YD%NVALS = JPNONDEF
YD%LNVALS=.FALSE.
YD%SERIAL=.FALSE.
!Statistics
YD%WALLCLOCK    = 0.0_JPRD
YD%TOTWALL      = 0.0_JPRD
NIOSTREAMC      = NIOSTREAMC+1
NOPENIOSTREAMS  = NOPENIOSTREAMS+1
NOPENEDIOSTREAMS  = NOPENEDIOSTREAMS+1
YD%IFILE_HANDLE = NIOSTREAMC

! Process arguments
IF(PRESENT(KIOMASTER)) THEN     ! Only one IO processor
  YD%IOMASTER = KIOMASTER
  YD%NUMIOPROCS = 1
  YD%NUMIOPROCSHLP = YIOS%NPROC
  ALLOCATE(YD%IOPROCS(YD%NUMIOPROCS))
  ALLOCATE(YD%IOPROCSHLP(YD%NUMIOPROCS,YD%NUMIOPROCSHLP))
  ALLOCATE(YD%NUMHLP(YD%NUMIOPROCS))
  YD%IOPROCS(1) = KIOMASTER
  YD%NUMHLP(1)=YD%NUMIOPROCSHLP
  YD%IOPROCSHLP(:,:)=0
  IROC=YD%IOPROCS(1)-1
  INODE=MPL_NODE(YD%IOPROCS(1))-1
  IHELP=1
  YD%IOPROCSHLP(1,IHELP)=YD%IOPROCS(1)
  OUTER:DO JNODE=1,MPL_NNODES
    INODE=INODE+1
    IF(INODE > MPL_NNODES) INODE=1
    DO JROC=1,MPL_TASK_PER_NODE(INODE)
      IF(MPL_NODE_TASKS(INODE,JROC) /= YD%IOPROCSHLP(1,1)) THEN
        IHELP =IHELP+1
        YD%IOPROCSHLP(1,IHELP)=MPL_NODE_TASKS(INODE,JROC)
      ENDIF
      IF(IHELP == YD%NUMIOPROCSHLP) EXIT OUTER
    ENDDO
  ENDDO OUTER
  IF(YD%IOMASTER == YIOS%MYPROC) THEN
    YD%LIOPROC  = .TRUE.
  ENDIF
  IF(PRESENT(LSERIAL)) YD%SERIAL=LSERIAL
ELSEIF(PRESENT(KIOPROCS)) THEN  ! Possibly multiple IO processors

  IF(KIOPROCS(1) == -1) THEN    ! All processors do IO
    YD%LIOPROC  = .TRUE.
    YD%NUMIOPROCS = YIOS%NPROC
    YD%NUMIOPROCSHLP = YIOS%NPROC
    ALLOCATE(YD%IOPROCS(YD%NUMIOPROCS))
    ALLOCATE(YD%NUMHLP(YD%NUMIOPROCS))
    ALLOCATE(YD%IOPROCSHLP(YD%NUMIOPROCS,YD%NUMIOPROCSHLP))
    YD%IOPROCSHLP(:,:)=0
    DO JROC=1,YD%NUMIOPROCS
      YD%IOPROCS(JROC) = JROC
      YD%NUMHLP(JROC)=1
      YD%IOPROCSHLP(JROC,JROC)=YD%IOPROCS(JROC)
    ENDDO
  ELSE
    IF(IFIND(YIOS%MYPROC,KIOPROCS(:)) >0) THEN
      YD%LIOPROC  = .TRUE.
    ENDIF
    YD%NUMIOPROCS = SIZE(KIOPROCS)
    ALLOCATE(YD%IOPROCS(YD%NUMIOPROCS))
    IHELP=YIOS%NPROC/YD%NUMIOPROCS
    YD%NUMIOPROCSHLP = YD%NUMIOPROCS*IHELP
    ALLOCATE(YD%NUMHLP(YD%NUMIOPROCS))
    ALLOCATE(YD%IOPROCSHLP(YD%NUMIOPROCS,YD%NUMIOPROCSHLP))
    YD%IOPROCSHLP(:,:)=0
    DO JROC=1,YD%NUMIOPROCS
      IF(KIOPROCS(JROC) < 1 .OR. KIOPROCS(JROC) > YIOS%NPROC) THEN
        WRITE(YIOS%NULERR,*) 'IOSTREAM_MIX:SETUP_IOSTREAM ',JROC,KIOPROCS(JROC)
        CALL ABOR1('IOSTREAM_MIX:SETUP_IOSTREAM - ILLEGAL  KIOPROCS')
      ENDIF
      YD%IOPROCS(JROC) = KIOPROCS(JROC)
    ENDDO
    LLUSED(:)=.FALSE.
    DO JROC=1,YD%NUMIOPROCS
      YD%NUMHLP(JROC)=1
      YD%IOPROCSHLP(JROC,1)=YD%IOPROCS(JROC)
      LLUSED(YD%IOPROCS(JROC))=.TRUE.
    ENDDO
    DO JHELP=1,IHELP
      DO JROC=1,YD%NUMIOPROCS
        IOPROC=YD%IOPROCS(JROC)
        INODE=MPL_NODE(IOPROC)
        DO JLOC=1,MPL_TASK_PER_NODE(INODE)
          IPROC=MPL_NODE_TASKS(INODE,JLOC)
          IF(IPROC > 0 .AND. IPROC <= YIOS%NPROC) THEN
            IF(.NOT.LLUSED(IPROC)) THEN
              YD%NUMHLP(JROC)=YD%NUMHLP(JROC)+1
              YD%IOPROCSHLP(JROC,YD%NUMHLP(JROC))=IPROC
              LLUSED(IPROC)=.TRUE.
            ENDIF
          ENDIF
        ENDDO
      ENDDO
    ENDDO
    ! This option does not work
    YD%LIONOHELP = .TRUE.
  ENDIF
ELSE    ! All processors do IO 
  YD%LIOPROC  = .TRUE.
  YD%NUMIOPROCS = YIOS%NPROC
  ALLOCATE(YD%IOPROCS(YD%NUMIOPROCS))
  YD%NUMIOPROCSHLP = YD%NUMIOPROCS
  ALLOCATE(YD%NUMHLP(YD%NUMIOPROCS))
  ALLOCATE(YD%IOPROCSHLP(YD%NUMIOPROCS,YD%NUMIOPROCSHLP))
  YD%IOPROCSHLP(:,:)=0
  DO JROC=1,YD%NUMIOPROCS
    YD%IOPROCS(JROC) = JROC
    YD%NUMHLP(JROC)=1
    YD%IOPROCSHLP(JROC,1)=YD%IOPROCS(JROC)
  ENDDO
ENDIF
IF (PRESENT(KNVALS)) THEN
  YD%NVALS=KNVALS
  YD%LNVALS=.TRUE.
ENDIF
DO JROC=1,YD%NUMIOPROCS
  IF(IFIND(YIOS%MYPROC,YD%IOPROCSHLP(JROC,:)) >0) THEN
    IF(YIOS%MYPROC /= YD%IOPROCS(JROC)) THEN
      YD%LIOPROCHLP  = .TRUE.
      YD%MIOPROCHLP  = YD%IOPROCS(JROC)
    ENDIF
  ENDIF
ENDDO
IF(PRESENT(CDMODE)) YD%CMODE = CDMODE
IF(PRESENT(LDNOHELP)) YD%LIONOHELP = LDNOHELP
IF(YD%LIONOHELP) YD%NUMHLP(:)=1
CALL GSTATS(1752,0)
! Set IO type
SELECT CASE(CDTYPE)
CASE('FDB')
  YD%IOTYPE = JPFDB
CASE('CIO')
  YD%IOTYPE = JPCIO
  IF(PRESENT(CDPATH)) THEN
    YD%CPATH = CDPATH 
  ELSE
    CALL ABOR1('IOSTREAM_MIX:SETUP_IOSTREAM - PATH REQUIRED')
  ENDIF
  IF( YD%LIOPROC ) THEN
    CALL IGRIB_OPEN_FILE(YD%IFILE_HANDLE,TRIM(YD%CPATH),YD%CMODE)
  ENDIF

CASE DEFAULT
  CALL ABOR1('IOSTREAM_MIX:SETUP_IOSTREAM - UNKNOWN IO TYPE')
END SELECT
CALL GSTATS(1752,1)

WRITE(YIOS%NULOUT,*) 'IO-STREAM SETUP - IOTYPE =',YD%IOTYPE,' NUMIOPROCS =',&
 & YD%NUMIOPROCS,' CPATH =',TRIM(YD%CPATH),' MODE=',YD%CMODE

CALL USER_CLOCK(PELAPSED_TIME=ZTIME1)
YD%TOTWALL = YD%TOTWALL+(ZTIME1-ZTIME0)
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:SETUP_IOSTREAM',1,ZHOOK_HANDLE)

END SUBROUTINE SETUP_IOSTREAM
