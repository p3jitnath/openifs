SUBROUTINE CLOSE_IOSTREAM(YD)

!**** *CLOSE_IOSTREAM*  - Close IO stream

! Purpose - To release an io-stream, close file if applicable etc.

! Explicit arguments :
! --------------------
! YD        - IO stream structure

!-------------------------------------------------------------------------
IMPLICIT NONE
TYPE(TYPE_IOSTREAM),INTENT(INOUT) :: YD

INTEGER(KIND=JPIM) :: JROC,ITAG,IROC,ISENDREQ
INTEGER(KIND=JPIB) :: IBYTES(YIOS%NPROC)
INTEGER(KIND=JPIM) :: IRECS(YIOS%NPROC)
REAL(KIND=JPRD)    :: ZMBYTES_PER_SEC,ZPERC
REAL(KIND=JPRD)    :: ZWALL(YIOS%NPROC),ZTOTWALL(YIOS%NPROC),ZBUF(4)
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
REAL(KIND=JPRD)    :: ZTIME0,ZTIME1

!-------------------------------------------------------------------------
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:CLOSE_IOSTREAM',0,ZHOOK_HANDLE)

IF(YD%LIOPROC) THEN
  CALL USER_CLOCK(PELAPSED_TIME=ZTIME0)
  ! Close file, handle etc.
  CALL GSTATS(1753,0)
  SELECT CASE(YD%IOTYPE)
  CASE(JPCIO)
    CALL IGRIB_CLOSE_FILE(YD%IFILE_HANDLE)
  CASE(JPFDB)
    CALL IFLUSHFDBSUBS()
  END SELECT
  CALL USER_CLOCK(PELAPSED_TIME=ZTIME1)
  YD%TOTWALL = YD%TOTWALL+(ZTIME1-ZTIME0)
  CALL GSTATS(1753,1)
ENDIF

! Write out IO statistics
ZWALL (YIOS%MYPROC) = YD%WALLCLOCK
ZTOTWALL (YIOS%MYPROC) = YD%TOTWALL
IRECS (YIOS%MYPROC) = YD%NRECS
IBYTES(YIOS%MYPROC) = YD%NBYTES
IF(YD%CMODE == 'r' .OR. YD%CMODE == 'R') THEN
  WALLCLOCK_IN = WALLCLOCK_IN+YD%WALLCLOCK
  TOTWALL_IN = TOTWALL_IN+YD%TOTWALL
  NRECS_IN  = NRECS_IN+YD%NRECS
  NBYTES_IN = NBYTES_IN+YD%NBYTES
ELSE
  WALLCLOCK_OUT = WALLCLOCK_OUT+YD%WALLCLOCK
  TOTWALL_OUT = TOTWALL_OUT+YD%TOTWALL
  NRECS_OUT  = NRECS_OUT+YD%NRECS
  NBYTES_OUT = NBYTES_OUT+YD%NBYTES
ENDIF
IF((YD%NUMIOPROCS > 1 .OR. YD%IOMASTER /= 1).AND..NOT.YD%SERIAL) THEN
  CALL GSTATS_BARRIER(745)
  CALL GSTATS(629,0)
  ITAG = 20000
  IF(YIOS%MYPROC == 1) THEN
    DO JROC=1,YD%NUMIOPROCS
      IF(YD%IOPROCS(JROC) /= YIOS%MYPROC) THEN
        CALL MPL_RECV(ZBUF,KSOURCE=YD%IOPROCS(JROC),KTAG=ITAG,&
         & KMP_TYPE=JP_BLOCKING_STANDARD,CDSTRING='IOSTREAM_MIX:CLOSE_IOSTREAM')
        IBYTES(YD%IOPROCS(JROC)) = ZBUF(1)
        IRECS (YD%IOPROCS(JROC)) = ZBUF(2)
        ZWALL (YD%IOPROCS(JROC)) = ZBUF(3)
        ZTOTWALL (YD%IOPROCS(JROC)) = ZBUF(4)
      ENDIF
    ENDDO
  ELSEIF(YD%LIOPROC) THEN
    ZBUF(1) = YD%NBYTES
    ZBUF(2) = YD%NRECS
    ZBUF(3) = YD%WALLCLOCK
    ZBUF(4) = YD%TOTWALL
    CALL MPL_SEND(ZBUF,KDEST=1,KTAG=ITAG,&
     & KMP_TYPE=JP_NON_BLOCKING_STANDARD,KREQUEST=ISENDREQ,&
     & CDSTRING='IOSTREAM_MIX:CLOSE_IOSTREAM')
    CALL MPL_WAIT(KREQUEST=ISENDREQ,&
     & CDSTRING='IOSTREAM_MIX:CLOSE_IOSTREAM')
  ENDIF
  CALL GSTATS(629,1)
  CALL GSTATS_BARRIER2(745)
ENDIF
IF(YIOS%MYPROC == 1) THEN
  WRITE(YIOS%NULOUT,*) 'IO-STREAM CLOSED - ',TRIM(YD%CPATH)
  DO JROC=1,YD%NUMIOPROCS
    IROC = YD%IOPROCS(JROC)
    IF(ZWALL(IROC) > 0.0_JPRD) THEN
      ZMBYTES_PER_SEC = IBYTES(IROC)/ZWALL(IROC)*1.E-6_JPRD
    ELSE
      ZMBYTES_PER_SEC = 0.0_JPRD
    ENDIF
    IF(ZTOTWALL(IROC) > 0.0_JPRD) THEN
      ZPERC = ZWALL(IROC)/ZTOTWALL(IROC)*100._JPRD
    ELSE
      ZPERC = 0.0_JPRB
    ENDIF
    WRITE(YIOS%NULOUT,'(1X,A,I4,A,I12,A,I6,A,F10.4,A,F8.1,A,F10.4,A,F4.1,A)')&
     & 'MPI-TASK:',IROC,' - ',&
     & IBYTES(IROC),' BYTES IN ' ,IRECS(IROC),&
     & ' RECORDS TRANSFERRED IN', ZWALL(IROC),' SECONDS',&
     & ZMBYTES_PER_SEC,' Mbytes/s, TOTAL TIME=',ZTOTWALL(IROC),&
     & '(',ZPERC,'%)'
  ENDDO
ENDIF

! Reset default values, free space
YD%IOTYPE = JPNONDEF
YD%IOMASTER = JPNONDEF
YD%LIOPROC = .FALSE.
DEALLOCATE(YD%IOPROCS)
DEALLOCATE(YD%IOPROCSHLP)
DEALLOCATE(YD%NUMHLP)
YD%NUMIOPROCS = JPNONDEF
YD%NUMIOPROCSHLP = JPNONDEF
YD%MIOPROCHLP = JPNONDEF
YD%IFILE_HANDLE = JPNONDEF
NOPENIOSTREAMS = NOPENIOSTREAMS-1

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:CLOSE_IOSTREAM',1,ZHOOK_HANDLE)

END SUBROUTINE CLOSE_IOSTREAM

