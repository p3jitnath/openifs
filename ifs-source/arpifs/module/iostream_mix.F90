SUBROUTINE ASSIGN_IOPROC(YD,KIOPROC,KMYCOUNT,KFLDPROC)

!**** *ASSIGN_IOPROC*  - Assign IO processor to fields

! Purpose - Assign IO processor to fields in round-robin fashion

! Explicit arguments :
! --------------------
! YD   - IO stream structure
! KIOPROC - IO processor for field
! KMYCOUNT - number of fields my processor is responsible for
!-------------------------------------------------------------------------
IMPLICIT NONE
TYPE(TYPE_IOSTREAM),INTENT(IN) :: YD
INTEGER(KIND=JPIM),INTENT(OUT) :: KIOPROC(:)
INTEGER(KIND=JPIM),INTENT(OUT) :: KMYCOUNT
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KFLDPROC(:)

INTEGER(KIND=JPIM) :: JF,IFLDS,INCR,IPROC
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
!-------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:ASSIGN_IOPROC',0,ZHOOK_HANDLE)
KMYCOUNT = 0
IF(PRESENT(KFLDPROC)) THEN
  IF(SIZE(KFLDPROC) < SIZE(KIOPROC))&
   & CALL ABOR1('IOSTREAM_MIX:ASSIGN_IOPROC - KFLDPROC TOO SMALL')
  DO JF=1,SIZE(KIOPROC)
    IF(IFIND(KFLDPROC(JF),YD%IOPROCS(:)) == 0)&
     & CALL ABOR1('IOSTREAM_MIX:ASSIGN_IOPROC - KFLDPROC PROC NOT IN YD%IOPROCS')
    KIOPROC(JF) = KFLDPROC(JF)
    IF(KIOPROC(JF) == YIOS%MYPROC) KMYCOUNT = KMYCOUNT+1
  ENDDO
ELSE
  IFLDS=SIZE(KIOPROC)
  INCR=YD%NUMIOPROCS/IFLDS
  IPROC=1
  DO JF=1,IFLDS
    IF(IFLDS >= YD%NUMIOPROCS)THEN
      KIOPROC(JF) = YD%IOPROCS(MOD(JF-1,YD%NUMIOPROCS)+1)
    ELSE
      KIOPROC(JF) = YD%IOPROCS(IPROC)
      IPROC = IPROC+INCR
    ENDIF
    IF(KIOPROC(JF) == YIOS%MYPROC) KMYCOUNT = KMYCOUNT+1
  ENDDO
ENDIF
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:ASSIGN_IOPROC',1,ZHOOK_HANDLE)

END SUBROUTINE ASSIGN_IOPROC
