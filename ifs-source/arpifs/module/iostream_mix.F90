! Explicit arguments :
! --------------------
! YDR  - IO request structure
! LDPR - logical to indicate real args present
! KPRD - dimensions of real args present
! LDKR - logical to indicate integer args present
! KKRD - dimensions of integer args present
! PR1  - real rank 1 array
