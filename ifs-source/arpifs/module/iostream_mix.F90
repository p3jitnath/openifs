SUBROUTINE SPEC_OUT(YD,YDR,PSPEC2,PSPEC3,KFLD2,KLEV,KFLD3)

!**** *SPEC_OUT*  - Outpout spectral data

! Purpose - Gather and write out spectral fields

! Explicit arguments :
! --------------------
! YD      - IO stream structure
! YDR     - IO request structure
! PSPEC2  - 2-D spectral fields
! PSPEC3  - 3-D spectral fields
! KFLD2   - number of 2-D spectral fields
! KLEV    - number of levels in 3-D spectral fields 
! KFLD3   - number of 3-D spectral fields
!-------------------------------------------------------------------------

IMPLICIT NONE
TYPE(TYPE_IOSTREAM) , INTENT(INOUT) :: YD
TYPE(TYPE_IOREQUEST), INTENT(INOUT) :: YDR
REAL(KIND=JPRB),OPTIONAL,INTENT(IN) :: PSPEC2(:,:)
REAL(KIND=JPRB),OPTIONAL,INTENT(IN) :: PSPEC3(:,:,:)
INTEGER(KIND=JPIM)  , INTENT(IN)    :: KFLD2,KLEV,KFLD3

INTEGER(KIND=JPIM) :: ITO2(KFLD2),IBSET2(KFLD2) 
INTEGER(KIND=JPIM) :: ITO3(KLEV),IBSET3(KLEV) 
INTEGER(KIND=JPIM) :: ISPEC2G,JFLD,JLEV,IMYCOUNT,IRET,IFLD,ILEV,JS,IHELP,IMYSETV
INTEGER(KIND=JPIM) :: ICHUNKSZ, IFI, ILA, IFLDS, IMYFLDS, JJFLD,ILFI,ILLA,JJ
INTEGER(KIND=JPIM) :: ILPT,ILPT2(KFLD2),ILPT3(KLEV)
LOGICAL :: LL2B(KFLD2), LL3B(KLEV)
REAL(KIND=JPRB),ALLOCATABLE :: ZSPECG(:,:),ZSPEC(:,:)
INTEGER(KIND=JPIM) :: ICOLOUR
INTEGER(KIND=JPIM) :: IERROR
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
!-------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:SPEC_OUT',0,ZHOOK_HANDLE)
CALL TRANS_INQ(KRESOL=YDR%NRESOL,KMYSETV=IMYSETV)
ISPEC2G = YDR%YSPF%ISPEC2G

IF(KFLD2 > 0) THEN
  IBSET2(:) = YDR%YMDL_2D%IBSET(:)
  LL2B(:) = .FALSE.
  ILPT = 0
  DO JJ=1,KFLD2
    IF(IBSET2(JJ) == IMYSETV) THEN
      ILPT = ILPT+1
      LL2B(JJ) = .TRUE. 
    ENDIF
    ILPT2(JJ) = ILPT
  ENDDO
  IF(YD%NUMIOPROCS > 1) THEN
    YD%LIOUSEHELP=.FALSE.
    CALL ASSIGN_IOPROC(YD,ITO2,IMYCOUNT)
    ICHUNKSZ = KFLD2
    ALLOCATE(ZSPECG(YDR%YSPF%ISPEC2G,IMYCOUNT))
    ALLOCATE(YDR%IGRBCODES(IMYCOUNT))
    ALLOCATE(YDR%ILEVS(IMYCOUNT))
    ALLOCATE(YDR%IHAVEFLD(KFLD2))
  ELSE
    IF(YDR%LDISTRIBUTE) THEN
      YD%LIOUSEHELP = .NOT.YD%LIONOHELP
      ITO2(:) = YD%IOPROCS(1)
    ELSE
      YD%LIOUSEHELP = .FALSE.
      ITO2(:) = YD%IOPROCS(1)
    ENDIF
    IF(YD%NUMHLP(1) == 1) THEN
      ICHUNKSZ = YDR%NCHUNKSIZE
    ELSE
      ICHUNKSZ = KFLD2
    ENDIF
    IF(YD%LIOPROC) THEN
      IMYCOUNT = KFLD2
    ELSE
      IMYCOUNT = 0
    ENDIF
    IHELP=YD%NUMHLP(1)-1
    IF(ANY(YD%IOPROCSHLP(1,:) == YIOS%MYPROC)) THEN
      ALLOCATE(ZSPECG(YDR%YSPF%ISPEC2G,ICHUNKSZ/MAX(1,IHELP)+1))
    ELSE
      ALLOCATE(ZSPECG(1,1))
    ENDIF
    ALLOCATE(YDR%IGRBCODES(ICHUNKSZ))
    ALLOCATE(YDR%IHAVEFLD(ICHUNKSZ))
    ALLOCATE(YDR%ILEVS(ICHUNKSZ))
  ENDIF

  IF( YD%IOTYPE == JPFDB) THEN
    IF(YD%LIOPROC) THEN
      ICOLOUR=1
    ELSE
      ICOLOUR=-1
    ENDIF
    CALL MPL_COMM_SPLIT(MPL_COMM, ICOLOUR, YIOS%MYPROC, YDR%IOCOMM, IERROR, &
        CDSTRING='IOSTREAM_MIX:SPEC_OUT')
  ENDIF
  DO JJFLD=1,KFLD2,ICHUNKSZ
    IFI = JJFLD
    ILA = MIN(JJFLD+ICHUNKSZ-1,KFLD2)
    ILLA = MAX(0,MAXVAL(ILPT2(IFI:ILA),MASK=LL2B(IFI:ILA)))
    ILFI = MIN(ILLA,MINVAL(ILPT2(IFI:ILA),MASK=LL2B(IFI:ILA)))
    IFLDS = ILA-IFI+1
    DO JFLD=1,IFLDS
      IF(YD%LIOUSEHELP) THEN
        YDR%IHAVEFLD(JFLD)=IGETPROCHLP(YD,JFLD)
      ELSE
        YDR%IHAVEFLD(JFLD)=ITO2(IFI+JFLD-1)
      ENDIF
    ENDDO
    IF(YD%NUMIOPROCS > 1) THEN
      IMYFLDS = IMYCOUNT
    ELSEIF(YD%LIOPROC) THEN
      IMYFLDS = IFLDS
    ELSE
      IMYFLDS = 0
    ENDIF
    IF(YDR%LDISTRIBUTE) THEN
      IF(ILLA >0) THEN
        CALL GATH_SPEC(PSPECG=ZSPECG,KFGATHG=IFLDS,KTO=YDR%IHAVEFLD(1:IFLDS),&
         & KVSET=IBSET2(IFI:ILA),KRESOL=YDR%NRESOL,PSPEC=PSPEC2(:,ILFI:ILLA),&
         & LDIM1_IS_FLD=.FALSE.,KSMAX=YDR%YSPF%ISMAX)
      ELSE
        CALL GATH_SPEC(PSPECG=ZSPECG,KFGATHG=IFLDS,KTO=YDR%IHAVEFLD(1:IFLDS),&
         & KVSET=IBSET2(IFI:ILA),KRESOL=YDR%NRESOL,&
         & LDIM1_IS_FLD=.FALSE.,KSMAX=YDR%YSPF%ISMAX)
      ENDIF
    ENDIF
    IFLD = 0
    DO JFLD=IFI,ILA
      IF(YDR%IHAVEFLD(JFLD-IFI+1) == YIOS%MYPROC) THEN
        IFLD = IFLD+1
        YDR%IGRBCODES(IFLD) = YDR%YMDL_2D%IGRIBCODE(JFLD)
        YDR%ILEVS(IFLD) = YDR%YMDL_2D%ILEVS(JFLD)
      ENDIF
    ENDDO
    IF(YDR%LDISTRIBUTE) THEN
      IF(YD%LIOUSEHELP) THEN
        IF(ANY(YD%IOPROCSHLP(1,:) == YIOS%MYPROC)) THEN
          CALL WRITE_RECORD(YD,YDR,IFLDS,PREC=ZSPECG)
        ENDIF
      ELSE
        CALL WRITE_RECORD(YD,YDR,IMYFLDS,PREC=ZSPECG)
      ENDIF
    ELSE
      CALL WRITE_RECORD(YD,YDR,IMYFLDS,PREC=PSPEC2(:,IFI:ILA))
    ENDIF
  ENDDO
  IF(ALLOCATED(ZSPECG)) DEALLOCATE(ZSPECG)
  DEALLOCATE(YDR%IGRBCODES)
  DEALLOCATE(YDR%IHAVEFLD)
  DEALLOCATE(YDR%ILEVS)
ENDIF

IF(YDR%YMDL_3D%NUMFLDS > 0) THEN
  IBSET3(:) = YDR%YMDL_3D%IBSET(:)
  LL3B(:) = .FALSE.
  ILPT = 0
  DO JJ=1,KLEV
    IF(IBSET3(JJ) == IMYSETV) THEN
      ILPT = ILPT+1
      LL3B(JJ) = .TRUE.
    ENDIF
    ILPT3(JJ) = ILPT
  ENDDO
  IF(YD%NUMIOPROCS > 1) THEN
    YD%LIOUSEHELP = .FALSE.
    CALL ASSIGN_IOPROC(YD,ITO3,IMYCOUNT)
    ICHUNKSZ = KLEV
    ALLOCATE(ZSPECG(YDR%YSPF%ISPEC2G,IMYCOUNT))
    ALLOCATE(YDR%IGRBCODES(IMYCOUNT))
    ALLOCATE(YDR%IHAVEFLD(KLEV))
    ALLOCATE(YDR%ILEVS(IMYCOUNT))
  ELSE
    ITO3(:) = YD%IOPROCS(1)
    YD%LIOUSEHELP = .NOT.YD%LIONOHELP
    IF(YD%NUMHLP(1) == 1) THEN
      ICHUNKSZ = YDR%NCHUNKSIZE
    ELSE
      ICHUNKSZ = KLEV
    ENDIF
    IF(YD%IOPROCS(1) == YIOS%MYPROC) THEN
      IMYCOUNT = KLEV
    ELSE
      IMYCOUNT = 0
    ENDIF
    IHELP=YD%NUMHLP(1)-1
    IF(ANY(YD%IOPROCSHLP(1,:) == YIOS%MYPROC)) THEN
      ALLOCATE(ZSPECG(YDR%YSPF%ISPEC2G,ICHUNKSZ/MAX(1,IHELP)+1))
    ELSE
      ALLOCATE(ZSPECG(1,1))
    ENDIF
    ALLOCATE(YDR%IGRBCODES(ICHUNKSZ))
    ALLOCATE(YDR%IHAVEFLD(ICHUNKSZ))
    ALLOCATE(YDR%ILEVS(ICHUNKSZ))
  ENDIF
  ALLOCATE(ZSPEC(YDR%YSPF%ISPEC2,SIZE(PSPEC3,1)))

  DO JFLD=1,YDR%YMDL_3D%NUMFLDS
    DO JJFLD=1,KLEV,ICHUNKSZ
      IFI = JJFLD
      ILA = MIN(JJFLD+ICHUNKSZ-1,KLEV)
      ILLA = MAX(0,MAXVAL(ILPT3(IFI:ILA),MASK=LL3B(IFI:ILA)))
      ILFI = MIN(ILLA,MINVAL(ILPT3(IFI:ILA),MASK=LL3B(IFI:ILA)))
      IFLDS = ILA-IFI+1
      IF(YD%LIOUSEHELP) THEN
        DO JLEV=1,IFLDS
          YDR%IHAVEFLD(JLEV)=IGETPROCHLP(YD,JLEV)
        ENDDO
      ENDIF
      DO JLEV=1,IFLDS
        IF(YD%LIOUSEHELP) THEN
          YDR%IHAVEFLD(JLEV)=IGETPROCHLP(YD,JLEV)
        ELSE
          YDR%IHAVEFLD(JLEV)=ITO3(IFI+JLEV-1)
        ENDIF
      ENDDO
      IF(YD%NUMIOPROCS > 1) THEN
        IMYFLDS = IMYCOUNT
      ELSEIF(YD%IOPROCS(1) == YIOS%MYPROC) THEN
        IMYFLDS = IFLDS
      ELSE
        IMYFLDS = 0
      ENDIF
      IF( SIZE(PSPEC3,1) > 0 .AND. ILFI > 0)THEN
        CALL GSTATS(1479,0)
!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(JLEV,ILEV,JS)
        DO JLEV=ILFI,ILLA
          ILEV = JLEV-ILFI+1
          DO JS=1,YDR%YSPF%ISPEC2
            ZSPEC(JS,ILEV) = PSPEC3(JLEV,JS,JFLD)
          ENDDO
        ENDDO
!$OMP END PARALLEL DO
        CALL GSTATS(1479,1)
      ENDIF
      IF(YDR%LDISTRIBUTE) THEN
        CALL GATH_SPEC(PSPECG=ZSPECG,KFGATHG=IFLDS,KTO=YDR%IHAVEFLD(1:IFLDS),&
         & KVSET=IBSET3(IFI:ILA),KRESOL=YDR%NRESOL,PSPEC=ZSPEC,&
         & KSMAX=YDR%YSPF%ISMAX,LDIM1_IS_FLD=.FALSE.)
        IFLD = 0
        DO JLEV=IFI,ILA
          IF(YDR%IHAVEFLD(JLEV-IFI+1) == YIOS%MYPROC) THEN
            IFLD = IFLD+1
            YDR%IGRBCODES(IFLD) = YDR%YMDL_3D%IGRIBCODE(JFLD)
            YDR%ILEVS(IFLD) = YDR%YMDL_3D%ILEVS(JLEV)
          ENDIF
        ENDDO
        IF(YD%LIOUSEHELP) THEN
          IF(ANY(YD%IOPROCSHLP(1,:) == YIOS%MYPROC)) THEN
            CALL WRITE_RECORD(YD,YDR,IFLDS,PREC=ZSPECG)
          ENDIF
        ELSE
          CALL WRITE_RECORD(YD,YDR,IMYFLDS,PREC=ZSPECG)
        ENDIF
      ELSE
        IFLD = 0
        DO JLEV=IFI,ILA
          IF(ITO3(JLEV) == YIOS%MYPROC) THEN
            IFLD = IFLD+1
            YDR%IGRBCODES(1) = YDR%YMDL_3D%IGRIBCODE(JFLD)
            YDR%ILEVS(1) = YDR%YMDL_3D%ILEVS(JLEV)
            CALL WRITE_RECORD(YD,YDR,1,PREC=PSPEC3(IFLD,:,JFLD:JFLD))
          ENDIF
        ENDDO
      ENDIF
    ENDDO
  ENDDO
  IF(ALLOCATED(ZSPECG)) DEALLOCATE(ZSPECG)
  IF(ALLOCATED(ZSPEC)) DEALLOCATE(ZSPEC)
  IF( YD%IOTYPE == JPFDB .AND. YD%LIOPROC .AND. KFLD2 > 0) THEN 
    CALL MPL_COMM_FREE(YDR%IOCOMM,IERROR,CDSTRING='IOSTREAM_MIX:SPEC_OUT')
  ENDIF

  DEALLOCATE(YDR%IGRBCODES)
  DEALLOCATE(YDR%IHAVEFLD)
  DEALLOCATE(YDR%ILEVS)
ENDIF

IF( YIOS%NPROC > 1) THEN
  CALL GSTATS(711,0)
  CALL MPL_BARRIER(CDSTRING='IOSTREAM_MIX:SPEC_OUT')
  CALL GSTATS(711,1)
ENDIF
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:SPEC_OUT',1,ZHOOK_HANDLE)
  
END SUBROUTINE SPEC_OUT
