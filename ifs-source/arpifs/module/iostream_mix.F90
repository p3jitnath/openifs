SUBROUTINE COPYGP_GLOB2LOC(PGLOB,PLOC,KPROMA,KPTS)

!**** *COPYGP*  - Copy grid-point array

! Purpose - Copy grid-point array

! Explicit arguments :
! --------------------
! PGLOB - input array
! PLOC  - output array
! KPROMA - blocking factor
! KPTS - number of points
!-------------------------------------------------------------------------

IMPLICIT NONE
REAL(KIND=JPRB),INTENT(IN)     :: PGLOB(:,:)
REAL(KIND=JPRB),INTENT(OUT)    :: PLOC(:,:,:)
INTEGER(KIND=JPIM),INTENT(IN)  :: KPROMA
INTEGER(KIND=JPIM),INTENT(IN)  :: KPTS

INTEGER(KIND=JPIM) :: IFIELDS,IBL,JFLD,JKGLO,JROF 
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!-------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:COPYGP_GLOB2LOC',0,ZHOOK_HANDLE)

IF(KPROMA*SIZE(PLOC,3) < KPTS)&
 & CALL ABOR1('IOSTREAM_MIX:COPYGP_GLOB2LOC - PLOC TOO SMALL')
IFIELDS = MIN(SIZE(PGLOB,2),SIZE(PLOC,2))
CALL GSTATS(1477,0)
!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(JFLD,JKGLO,IBL,JROF)
DO JKGLO=1,KPTS,KPROMA
  IBL = (JKGLO-1)/KPROMA+1
  DO JFLD=1,IFIELDS
    DO JROF=1,MIN(KPROMA,KPTS-JKGLO+1)
      PLOC(JROF,JFLD,IBL) =  PGLOB(JKGLO+JROF-1,JFLD)
    ENDDO
  ENDDO
ENDDO
!$OMP END PARALLEL DO
CALL GSTATS(1477,1)
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:COPYGP_GLOB2LOC',1,ZHOOK_HANDLE)
END SUBROUTINE COPYGP_GLOB2LOC
