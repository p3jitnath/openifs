SUBROUTINE GRID_OUT(YD,YDR,PGRID2,PGRID3,KFLD2,KLEV,KFLD3,KFLDPROC2D)

!**** *GRID_OUT*  - Output grid-point data

! Purpose - Gather and write out grid-point fields

! Explicit arguments :
! --------------------
! YD      - IO stream structure
! YDR     - IO request structure
! PGRID2  - 2-D grid-point fields
! PGRID3  - 3-D grid-point fields
! KFLD2   - number of 2-D grid-point fields
! KLEV    - number of levels in 3-D grid-point fields 
! KFLD3   - number of 3-D grid-point fields
!-------------------------------------------------------------------------
IMPLICIT NONE
TYPE(TYPE_IOSTREAM) , INTENT(INOUT)       :: YD
TYPE(TYPE_IOREQUEST), INTENT(INOUT)       :: YDR
REAL(KIND=JPRB)     , OPTIONAL,INTENT(IN) :: PGRID2(:,:,:)
REAL(KIND=JPRB)     , OPTIONAL,INTENT(IN) :: PGRID3(:,:,:,:)
INTEGER(KIND=JPIM)  , INTENT(IN)          :: KFLD2,KLEV,KFLD3
INTEGER(KIND=JPIM)  , OPTIONAL,INTENT(IN) :: KFLDPROC2D(:)

INTEGER(KIND=JPIM) :: ITO2(KFLD2)
INTEGER(KIND=JPIM) :: ITO3(KLEV)
INTEGER(KIND=JPIM) :: JFLD,JLEV,IMYCOUNT,IFLD
INTEGER(KIND=JPIM) :: ILATS,IGPTOTG
INTEGER(KIND=JPIM),ALLOCATABLE  :: ILONS(:)
INTEGER(KIND=JPIM) :: ICHUNKSZ, IFI, ILA, IFLDS, IMYFLDS, JJFLD,J3D
INTEGER(KIND=JPIM) :: IMYGATH,IHELP
REAL(KIND=JPRB),ALLOCATABLE :: ZLATS(:)
REAL(KIND=JPRB),ALLOCATABLE :: ZGRIDG(:,:)
REAL(KIND=JPRB),ALLOCATABLE :: ZZGRIDG(:)
INTEGER(KIND=JPIM) :: ICOLOUR, IRET
INTEGER(KIND=JPIM) :: IERROR
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
!-------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:GRID_OUT',0,ZHOOK_HANDLE)

IGPTOTG = YDR%YGPF%IGPTOTG
IF(KFLD2 > 0) THEN
  IF(YD%NUMIOPROCS > 1) THEN
    YD%LIOUSEHELP = .FALSE.
    CALL ASSIGN_IOPROC(YD,ITO2,IMYCOUNT,KFLDPROC2D)
    ICHUNKSZ = KFLD2
    IMYGATH=IMYCOUNT
    ALLOCATE(ZGRIDG(IGPTOTG,IMYCOUNT))
    ALLOCATE(YDR%IGRBCODES(KFLD2))
    ALLOCATE(YDR%IHAVEFLD(KFLD2))
    ALLOCATE(YDR%ILEVS(KFLD2))
    YDR%IHAVEFLD(:)=ITO2(:)
  ELSE
    IF(YDR%LDISTRIBUTE) THEN
      YD%LIOUSEHELP = .NOT.YD%LIONOHELP
    ELSE
      YD%LIOUSEHELP = .FALSE.
      ITO2(:) = YD%IOPROCS(1)
    ENDIF
    IF(YD%NUMHLP(1) == 1) THEN
      ICHUNKSZ = YDR%NCHUNKSIZE
    ELSE
      ICHUNKSZ = KFLD2
    ENDIF
    ALLOCATE(YDR%IGRBCODES(ICHUNKSZ))
    ALLOCATE(YDR%IHAVEFLD(ICHUNKSZ))
    ALLOCATE(YDR%ILEVS(ICHUNKSZ))
    ITO2(:) = YD%IOPROCS(1)
    IF(YD%IOPROCS(1) == YIOS%MYPROC) THEN
      IMYCOUNT = KFLD2
    ELSE
      IMYCOUNT = 0
    ENDIF
!    CALL ASSIGN_IOPROC(YD,YDR%IHAVEFLD,IMYGATH,KFLDPROC2D)
    IHELP=YD%NUMHLP(1)-1
    IF(ANY(YD%IOPROCSHLP(1,:) == YIOS%MYPROC)) THEN
      ALLOCATE(ZGRIDG(IGPTOTG,ICHUNKSZ/MAX(1,IHELP)+1))
    ELSE
      ALLOCATE(ZGRIDG(1,1))
    ENDIF
  ENDIF
  IF( YD%IOTYPE == JPFDB ) THEN
     IF(YD%LIOPROC) THEN
       ICOLOUR=1
     ELSE
       ICOLOUR=-1
     ENDIF
     CALL MPL_COMM_SPLIT(MPL_COMM, ICOLOUR, YIOS%MYPROC, YDR%IOCOMM, IERROR, &
         CDSTRING='IOSTREAM_MIX:GRID_OUT')
  ENDIF

  DO JJFLD=1,KFLD2,ICHUNKSZ
    IFI = JJFLD
    ILA = MIN(JJFLD+ICHUNKSZ-1,KFLD2)
    IFLDS = ILA-IFI+1
    DO JFLD=1,IFLDS
      IF(YD%LIOUSEHELP) THEN
        YDR%IHAVEFLD(JFLD)=IGETPROCHLP(YD,JFLD)
        IF(YDR%IHAVEFLD(JFLD) < 0)CALL ABOR1('IOSTREAM_MIX:GRID_OUT  - YDR%IHAVEFLD(JFLD) < 0 (1)')
      ELSE
        YDR%IHAVEFLD(JFLD)=ITO2(IFI+JFLD-1)
        IF(YDR%IHAVEFLD(JFLD) < 0)CALL ABOR1('IOSTREAM_MIX:GRID_OUT  - YDR%IHAVEFLD(JFLD) < 0 (2)')
      ENDIF
    ENDDO
    IF(YD%NUMIOPROCS > 1) THEN
      IMYFLDS = IMYCOUNT
    ELSEIF(YD%IOPROCS(1) == YIOS%MYPROC) THEN
      IMYFLDS = IFLDS
    ELSE
      IMYFLDS = 0
    ENDIF
    IF(YDR%LDISTRIBUTE) THEN
      IF(YDR%LFPDIST) THEN
        CALL GATH_GRID_FP(PGPG=ZGRIDG,KFGATHG=IFLDS,KTO=YDR%IHAVEFLD(1:IFLDS),&
         & PGP=PGRID2(:,IFI:ILA,:))
      ELSE
!        This is where it goes
        CALL GATH_GRID(PGPG=ZGRIDG,KFGATHG=IFLDS,KTO=YDR%IHAVEFLD(1:IFLDS),&
         & PGP=PGRID2(:,IFI:ILA,:),KPROMA=YDR%YGPF%NPROMA,KRESOL=YDR%NRESOL)
      ENDIF
    ELSEIF(YD%LIOPROC) THEN
      CALL COPYGP_LOC2GLOB(ZGRIDG,PGRID2(:,IFI:ILA,:),&
       & YDR%YGPF%NPROMA,IGPTOTG)
    ENDIF
    IFLD = 0
    DO JFLD=IFI,ILA
      IF(YDR%IHAVEFLD(JFLD-IFI+1) == YIOS%MYPROC) THEN
        IFLD = IFLD+1
        YDR%IGRBCODES(IFLD) = YDR%YMDL_2D%IGRIBCODE(JFLD)
        YDR%ILEVS(IFLD) = YDR%YMDL_2D%ILEVS(JFLD)
      ENDIF
    ENDDO

    IF(YDR%LINTERP) THEN
    ! Note order of statements, YDR%YGPF%IGPTOTG changes in call to INQUIRE_GRIB
      ALLOCATE(ZZGRIDG(IGPTOTG))
      IF(YIOS%NDGLG == YIOS%NUNDEF) CALL ABOR1('IOSTREAM_MIX:GRID_OUT - YIOS%NDGLG NOT DEFINED')
      IF(.NOT.ALLOCATED(YIOS%NLOENG)) CALL ABOR1('IOSTREAM_MIX:GRID_OUT - YIOS%NLOENG NOT DEFINED')
      IF(.NOT.ALLOCATED(YIOS%RLATIG)) CALL ABOR1('IOSTREAM_MIX:GRID_OUT - YIOS%RLATIG NOT DEFINED')
      ALLOCATE(ILONS(YIOS%NDGLG))
      ALLOCATE(ZLATS(YIOS%NDGLG))
      CALL INQUIRE_GRIB(YDR%IGRIB_HANDLE,KLATS=ILATS,KLONS=ILONS,PLATS=ZLATS,&
       & KPTS=YDR%YGPF%IGPTOTG)
      IFLD = 0
      DO JFLD=IFI,ILA
        IF(YDR%IHAVEFLD(JFLD-IFI+1) == YIOS%MYPROC) THEN
          IFLD = IFLD+1
          ZZGRIDG(:) = ZGRIDG(:,IFLD)
          ZGRIDG(:,IFLD) = 0.0_JPRB
          CALL INTERP_GP(YDR%NTYPE_INTERP,YDR%IGRBCODES(IFLD),YDR%ILEVS(IFLD),&
         & YIOS%NDGLG,YIOS%NLOENG(1:YIOS%NDGLG),YIOS%RLATIG(1:YIOS%NDGLG),&
         & ILATS,ILONS(1:ILATS),ZLATS(1:ILATS),&
         & YIOS%VAH,YIOS%VBH,&
         & ZZGRIDG(:),ZGRIDG(:,IFLD),YDR%CLEVTYPE)
        ENDIF
      ENDDO
      DEALLOCATE(ZZGRIDG)
      DEALLOCATE(ILONS)
      DEALLOCATE(ZLATS)
    ENDIF
    IF(YD%LIOUSEHELP) THEN
      IF(ANY(YD%IOPROCSHLP(1,:) == YIOS%MYPROC)) THEN
        CALL WRITE_RECORD(YD,YDR,IFLDS,PREC=ZGRIDG)
      ENDIF
    ELSE
      CALL WRITE_RECORD(YD,YDR,IMYFLDS,PREC=ZGRIDG)
    ENDIF
      
!     & KALLFLDS=IFLDS,KIOFLDS=ITO2(IFI:ILA),KHAVEFLDS=YDR%IHAVEFLD(IFI:ILA))
  ENDDO
  IF( YD%IOTYPE == JPFDB .AND. YD%LIOPROC .AND. KFLD2 > 0) THEN 
    CALL MPL_COMM_FREE(YDR%IOCOMM,IERROR,CDSTRING='IOSTREAM_MIX:GRID_OUT')
  ENDIF
  IF(ALLOCATED(ZGRIDG))  DEALLOCATE(ZGRIDG)
  DEALLOCATE(YDR%IGRBCODES)
  DEALLOCATE(YDR%IHAVEFLD)
  DEALLOCATE(YDR%ILEVS)
ENDIF

IF(YDR%YMDL_3D%NUMFLDS > 0) THEN
  IF(YDR%LINTERP)&
   & CALL ABOR1('IOSTREAM_MIX:IO_PUT - COMBINATION 3-D FIELDS AND LINTERP NOT SUPPORTED')
  IF(YD%NUMIOPROCS > 1) THEN
    YD%LIOUSEHELP = .FALSE.
    CALL ASSIGN_IOPROC(YD,ITO3,IMYCOUNT)
    ICHUNKSZ = KLEV
    ALLOCATE(ZGRIDG(YDR%YGPF%IGPTOTG,IMYCOUNT))
    ALLOCATE(YDR%IGRBCODES(IMYCOUNT))
    ALLOCATE(YDR%IHAVEFLD(IMYCOUNT))
    ALLOCATE(YDR%ILEVS(IMYCOUNT))
  ELSE
    IF(YDR%LDISTRIBUTE) THEN
      YD%LIOUSEHELP = .NOT.YD%LIONOHELP
    ELSE
      YD%LIOUSEHELP = .FALSE.
    ENDIF
    IF(YD%LIOUSEHELP) THEN
      IHELP=YD%NUMHLP(1)-1
      ICHUNKSZ=MIN(YD%NUMHLP(1)*YDR%NCHUNKSIZE,KLEV)
    ELSE
      IHELP=0
    ITO3(:) = YD%IOPROCS(1)
      ICHUNKSZ = YDR%NCHUNKSIZE
    ENDIF
    IF(YD%IOPROCS(1) == YIOS%MYPROC) THEN
      IMYCOUNT = KLEV
    ELSE
      IMYCOUNT = 0
    ENDIF
    IF(ANY(YD%IOPROCSHLP(1,:) == YIOS%MYPROC)) THEN
      ALLOCATE(ZGRIDG(IGPTOTG,ICHUNKSZ/MAX(1,IHELP)+1))
    ELSE
      ALLOCATE(ZGRIDG(1,1))
    ENDIF
    ALLOCATE(YDR%IGRBCODES(ICHUNKSZ))
    ALLOCATE(YDR%IHAVEFLD(ICHUNKSZ))
    ALLOCATE(YDR%ILEVS(ICHUNKSZ))
  ENDIF
  DO J3D=1,YDR%YMDL_3D%NUMFLDS
    DO JJFLD=1,KLEV,ICHUNKSZ
      IFI = JJFLD
      ILA = MIN(JJFLD+ICHUNKSZ-1,KLEV)
      IFLDS = ILA-IFI+1
      DO JFLD=1,IFLDS
        IF(YD%LIOUSEHELP) THEN
          YDR%IHAVEFLD(JFLD)=IGETPROCHLP(YD,JFLD)
      ELSE
          YDR%IHAVEFLD(JFLD)=ITO3(IFI+JFLD-1)
      ENDIF
      ENDDO
      IF(YDR%LDISTRIBUTE) THEN
        IF(YDR%LFPDIST) THEN
          CALL GATH_GRID_FP(PGPG=ZGRIDG,KFGATHG=IFLDS,KTO=YDR%IHAVEFLD(1:IFLDS),&
           & PGP=PGRID3(:,IFI:ILA,J3D,:))
        ELSE
          CALL GATH_GRID(PGPG=ZGRIDG,KFGATHG=IFLDS,KTO=YDR%IHAVEFLD(1:IFLDS),&
           & PGP=PGRID3(:,IFI:ILA,J3D,:),KPROMA=YDR%YGPF%NPROMA,KRESOL=YDR%NRESOL)
        ENDIF
      ELSEIF(YD%LIOPROC) THEN
        CALL COPYGP_LOC2GLOB(ZGRIDG,PGRID3(:,IFI:ILA,J3D,:),&
         & YDR%YGPF%NPROMA,YDR%YGPF%IGPTOTG)
      ENDIF
      IFLD = 0
      DO JLEV=IFI,ILA
        IF(YDR%IHAVEFLD(JLEV-IFI+1) == YIOS%MYPROC) THEN
          IFLD = IFLD+1
          YDR%IGRBCODES(IFLD) = YDR%YMDL_3D%IGRIBCODE(J3D)
          YDR%ILEVS(IFLD) = YDR%YMDL_3D%ILEVS(JLEV)
        ENDIF
      ENDDO
      IF(YD%LIOUSEHELP) THEN
        IF(ANY(YD%IOPROCSHLP(1,:) == YIOS%MYPROC)) THEN
          CALL WRITE_RECORD(YD,YDR,IFLDS,PREC=ZGRIDG)
        ENDIF
      ELSE
        IF(YD%NUMIOPROCS > 1) THEN
          IMYFLDS = IMYCOUNT
        ELSEIF(YD%IOPROCS(1) == YIOS%MYPROC) THEN
          IMYFLDS = IFLDS
        ELSE
          IMYFLDS = 0
        ENDIF
        CALL WRITE_RECORD(YD,YDR,IMYFLDS,PREC=ZGRIDG)
      ENDIF
    ENDDO
  ENDDO
  DEALLOCATE(ZGRIDG)
  DEALLOCATE(YDR%IGRBCODES)
  DEALLOCATE(YDR%IHAVEFLD)
  DEALLOCATE(YDR%ILEVS)
ENDIF
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:GRID_OUT',1,ZHOOK_HANDLE)
  
END SUBROUTINE GRID_OUT
!=================================================================
SUBROUTINE IO_GET(YD,YDR,PR1,PR2,PR3,PR4,KR1,KR2,KR3,KR4,CDR,&
 & LDMISSING_2D,LDMISSING_3D,KFLDPROC2D,KLEVPROC3D)

!**** *IO_GET*  - Input (read) data

! Purpose - To read in data .

! Explicit arguments :
! --------------------
! YD   - IO stream structure
! YDR  - IO request structure
! PR1  - real rank 1 array
! PR2  - real rank 2 array
! PR3  - real rank 3 array
! PR4  - real rank 4 array
! KR1  - integer rank 1 array
! KR2  - integer rank 2 array
! KR3  - integer rank 3 array
! KR4  - integer rank 4 array
! CDR  - character string
! LDMISSING_2D - missing 2-D fields (if not present abort will be called if field missing)
! LDMISSING_2D - missing 3-D fields (if not present abort will be called if field missing)
!-------------------------------------------------------------------------
IMPLICIT NONE
TYPE(TYPE_IOSTREAM),INTENT(INOUT) :: YD
TYPE(TYPE_IOREQUEST),INTENT(INOUT) :: YDR
REAL(KIND=JPRB),OPTIONAL,INTENT(INOUT) :: PR1(:)
REAL(KIND=JPRB),OPTIONAL,INTENT(INOUT) :: PR2(:,:)
REAL(KIND=JPRB),OPTIONAL,INTENT(INOUT) :: PR3(:,:,:)
REAL(KIND=JPRB),OPTIONAL,INTENT(INOUT) :: PR4(:,:,:,:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(INOUT) :: KR1(:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(INOUT) :: KR2(:,:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(INOUT) :: KR3(:,:,:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(INOUT) :: KR4(:,:,:,:)
CHARACTER(LEN=*)  ,OPTIONAL,INTENT(INOUT) :: CDR
LOGICAL,OPTIONAL,INTENT(OUT) :: LDMISSING_2D(:)
LOGICAL,OPTIONAL,INTENT(OUT) :: LDMISSING_3D(:,:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KFLDPROC2D(:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KLEVPROC3D(:)

LOGICAL :: LLPR(4),LLKR(4)
INTEGER(KIND=JPIM) :: IPRD(4,4),IKRD(4,4),ISIZE
INTEGER(KIND=JPIM),ALLOCATABLE :: IREC1(:)
REAL(KIND=JPRB)   ,ALLOCATABLE :: ZREC1(:)
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
REAL(KIND=JPRD)    :: ZTIME0,ZTIME1
!-------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:IO_GET',0,ZHOOK_HANDLE)
CALL USER_CLOCK(PELAPSED_TIME=ZTIME0)

IF(YD%NUMIOPROCS < 1 .OR. YD%NUMIOPROCS > YIOS%NPROC) THEN
  WRITE(YIOS%NULERR,*) 'IOSTREAM_MIX-IO_GET YD%NUMIOPROCS=',YD%NUMIOPROCS
  CALL ABOR1('IOSTREAM_MIX;IO_GET - IOSTREAM_MIX NOT SET UP')
ENDIF

CALL GET_PUT_ARGS(YDR,LLPR,IPRD,LLKR,IKRD,&
 & PR1,PR2,PR3,PR4,KR1,KR2,KR3,KR4)

SELECT CASE(YDR%IDATA_TYPE)
CASE(JPRAW)
  IF(YD%LIOPROC) THEN
    IF(LLPR(1)) THEN
      CALL READ_RECORD(YD,YDR,1,PREC1=PR1)
    ENDIF
    IF(LLPR(2)) THEN
      ISIZE = SIZE(PR2)
      ALLOCATE(ZREC1(ISIZE))
      CALL READ_RECORD(YD,YDR,1,PREC1=ZREC1)
      PR2(:,:) = RESHAPE(ZREC1,SHAPE(PR2))
      DEALLOCATE(ZREC1)
    ENDIF
    IF(LLPR(3)) THEN
      ISIZE = SIZE(PR3)
      ALLOCATE(ZREC1(ISIZE))
      CALL READ_RECORD(YD,YDR,1,PREC1=ZREC1)
      PR3(:,:,:) = RESHAPE(ZREC1,SHAPE(PR3))
      DEALLOCATE(ZREC1)
    ENDIF
    IF(LLPR(4)) THEN
      ISIZE = SIZE(PR4)
      ALLOCATE(ZREC1(ISIZE))
      CALL READ_RECORD(YD,YDR,1,PREC1=ZREC1)
      PR4(:,:,:,:) = RESHAPE(ZREC1,SHAPE(PR4))
      DEALLOCATE(ZREC1)
    ENDIF
    IF(LLKR(1)) THEN
      CALL READ_RECORD(YD,YDR,1,KREC1=KR1)
    ENDIF
    IF(LLKR(2)) THEN
      ISIZE = SIZE(KR2)
      ALLOCATE(IREC1(ISIZE))
      CALL READ_RECORD(YD,YDR,1,KREC1=IREC1)
      KR2(:,:) = RESHAPE(IREC1,SHAPE(KR2))
      DEALLOCATE(IREC1)
    ENDIF
    IF(LLKR(3)) THEN
      ISIZE = SIZE(KR3)
      ALLOCATE(IREC1(ISIZE))
      CALL READ_RECORD(YD,YDR,1,KREC1=IREC1)
      KR3(:,:,:) = RESHAPE(IREC1,SHAPE(KR3))
      DEALLOCATE(IREC1)
    ENDIF
    IF(LLKR(4)) THEN
      ISIZE = SIZE(KR4)
      ALLOCATE(IREC1(ISIZE))
      CALL READ_RECORD(YD,YDR,1,KREC1=IREC1)
      KR4(:,:,:,:) = RESHAPE(IREC1,SHAPE(KR4))
      DEALLOCATE(IREC1)
    ENDIF
    IF (PRESENT(CDR)) THEN
      CALL READ_RECORD(YD,YDR,1,CDREC=CDR)
    ENDIF
  ENDIF
CASE(JPSPEC)
  CALL SPEC_IN(YD,YDR,PR2,PR3,YDR%YMDL_2D%NUMFLDS,YDR%YMDL_3D%NLEVS,IPRD(3,3),&
   & LDMISSING_2D,LDMISSING_3D,KFLDPROC2D,KLEVPROC3D)  
CASE(JPGP)
  CALL GRID_IN(YD,YDR,PR3,PR4,IPRD(3,2),IPRD(4,2),IPRD(4,3),&
   & LDMISSING_2D,LDMISSING_3D,KFLDPROC2D)
END SELECT
CALL USER_CLOCK(PELAPSED_TIME=ZTIME1)
YD%TOTWALL = YD%TOTWALL+(ZTIME1-ZTIME0)
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:IO_GET',1,ZHOOK_HANDLE)

END SUBROUTINE IO_GET
!=================================================================
SUBROUTINE SPEC_IN(YD,YDR,PSPEC2,PSPEC3,KFLD2,KLEV,KFLD3,&
 & LDMISSING_2D,LDMISSING_3D,KFLDPROC2D,KLEVPROC3D)

!**** *SPEC_IN*  - Input spectral data

! Purpose - Read and distribute spectral fields

! Explicit arguments :
! --------------------
! YD      - IO stream structure
! YDR     - IO request structure
! PSPEC2  - 2-D spectral fields
! PSPEC3  - 3-D spectral fields
! KFLD2   - number of 2-D spectral fields
! KLEV    - number of levels in 3-D spectral fields 
! KFLD3   - number of 3-D spectral fields
! LDMISSING_2D - missing 2-D fields 
! LDMISSING_2D - missing 3-D fields 
!-------------------------------------------------------------------------
IMPLICIT NONE
TYPE(TYPE_IOSTREAM),INTENT(INOUT) :: YD
TYPE(TYPE_IOREQUEST),INTENT(INOUT) :: YDR
REAL(KIND=JPRB),OPTIONAL,INTENT(INOUT) :: PSPEC2(:,:)
REAL(KIND=JPRB),OPTIONAL,INTENT(INOUT) :: PSPEC3(:,:,:)
INTEGER(KIND=JPIM),INTENT(IN) :: KFLD2,KLEV,KFLD3
LOGICAL,OPTIONAL,INTENT(OUT) :: LDMISSING_2D(:)
LOGICAL,OPTIONAL,INTENT(OUT) :: LDMISSING_3D(:,:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KFLDPROC2D(:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KLEVPROC3D(:)

INTEGER(KIND=JPIM) :: IFROM2(KFLD2),IBSET2(KFLD2) 
INTEGER(KIND=JPIM) :: IFROM3(KLEV),IBSET3(KLEV) 
INTEGER(KIND=JPIM) :: ISPEC2G,JFLD,JLEV,IFF,ILEV,ILOCLV,IMISSING,&
 & IFLD,IMYCOUNT,II,IMISSINGX, INOFF2, INOFF3
INTEGER(KIND=JPIM) :: IFF2(JPMAXCHUNK),IFF3(JPMAXCHUNK),IFLD2(JPMAXCHUNK),IFLD3(JPMAXCHUNK),ILEV3(JPMAXCHUNK)
INTEGER(KIND=JPIM) :: ICHUNKSZ,ICHUNK,IIFLD
INTEGER(KIND=JPIM),ALLOCATABLE :: IMSG(:)
INTEGER(KIND=JPIM) :: IFROM(JPMAXCHUNK),IBSET(KFLD3*KLEV+KFLD2),IHELP,IIFROM(JPMAXCHUNK)
INTEGER(KIND=JPIM) :: JJ,IMYGFLD,IFLDG
REAL(KIND=JPRB),ALLOCATABLE :: ZSPECG(:,:),ZSPEC(:,:),ZSPECGT(:,:,:)
LOGICAL :: LLMISSING2D(KFLD2),LLMISSING3D(KLEV,KFLD3),LLFAIL
LOGICAL :: LLMISSING2DX(KFLD2),LLMISSING3DX(KLEV,KFLD3)
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!-------------------------------------------------------------------------
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:SPEC_IN',0,ZHOOK_HANDLE)

IF(YDR%LINTERP) THEN
  ISPEC2G=YDR%YSPF%IMAXSPEC2G
ELSE
  ISPEC2G=YDR%YSPF%ISPEC2G
ENDIF
IF(YDR%LRANDOM_ORDER) THEN
  IF(YD%NUMIOPROCS /= 1)&
   & CALL ABOR1('IOSTREAM_MIX:SPEC_IN - READING RANDOM FILE WITH > 1 IOPROCS')
  IF(YD%LIONOHELP) THEN
    YD%LIOUSEHELP = .FALSE.
  ELSE
    YD%LIOUSEHELP = YDR%LDISTRIBUTE
  ENDIF
  IF(YD%LIOUSEHELP) THEN
    IHELP=YD%NUMHLP(1)-1
  ELSE
    IHELP=0
  ENDIF
  IF(KFLD2 > 0) LLMISSING2D(:)   = .TRUE.
  IF(KFLD3 > 0) LLMISSING3D(:,:) = .TRUE.
  IF(KFLD2 > 0) LLMISSING2DX(:)   = .TRUE.
  IF(KFLD3 > 0) LLMISSING3DX(:,:) = .TRUE.
  IMISSING = KLEV*KFLD3+KFLD2
  IMISSINGX = KLEV*KFLD3+KFLD2
  ICHUNKSZ = YDR%NCHUNKSIZE*MAX(1,IHELP)
  ICHUNKSZ = MIN(ICHUNKSZ,IMISSING+5)
  IF(.NOT. YDR%LREADALL) ICHUNKSZ = MIN(ICHUNKSZ,IMISSING)
  IF(ICHUNKSZ > JPMAXCHUNK) CALL ABOR1('IOSTREAM_MIX:SPEC_IN - ICHUNKSZ > JPMAXCHUNK')
  ALLOCATE(ZSPECG(ISPEC2G,ICHUNKSZ/MAX(1,IHELP)+1))
  ALLOCATE(ZSPEC (YDR%YSPF%ISPEC2,ICHUNKSZ))
  ALLOCATE(IMSG(3*ICHUNKSZ))
  ALLOCATE(YDR%IGRBCODES(ICHUNKSZ))
  ALLOCATE(YDR%IHAVEFLD(ICHUNKSZ))
  ALLOCATE(YDR%ILEVS(ICHUNKSZ))
  IFROM(1:ICHUNKSZ) = YD%IOPROCS(1)
  DO 
    IF(YD%LEOF) EXIT
    IF(IMISSING == 0 .AND. .NOT. YDR%LREADALL) EXIT
    ICHUNK = MIN(ICHUNKSZ,IMISSING)
    IF(YDR%LREADALL) ICHUNK=ICHUNKSZ
    IF(ANY(YD%IOPROCSHLP(1,:) == YIOS%MYPROC)) THEN
      CALL GSTATS(1480,0)
!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(JFLD)
      DO JFLD=1,ICHUNKSZ/MAX(1,IHELP)+1
        ZSPECG(:,JFLD) = 0.0_JPRB
      ENDDO
!$OMP END PARALLEL DO
      CALL GSTATS(1480,1)
      CALL READ_RECORD(YD,YDR,ICHUNK,PREC=ZSPECG)
      IF(YD%LIOPROC .AND.YDR%LDISTHEADER) THEN
        II=0
        DO JFLD=1,MIN(YDR%IFIELDS_IO,ICHUNK)
          IMSG(II+1) = YDR%ILEVS(JFLD)
          IMSG(II+2) = YDR%IGRBCODES(JFLD)
          IMSG(II+3) = YDR%IHAVEFLD(JFLD)
          II=II+3
        ENDDO
        DO JFLD=YDR%IFIELDS_IO+1,ICHUNK
          IMSG(II+1) = -HUGE(IMSG)
          IMSG(II+2) = 0
          IMSG(II+3) = 0
          II=II+3
        ENDDO
      ENDIF
    ENDIF

    IF(YDR%LDISTHEADER) THEN
      CALL GSTATS_BARRIER(726)
      CALL GSTATS(631,0)
      CALL MPL_BROADCAST(IMSG,KTAG=99,KROOT=YD%IOPROCS(1),&
       & CDSTRING='IOSTREAM_MIX:SPEC_IN')
      CALL GSTATS(631,1)
      CALL GSTATS_BARRIER2(726)
      YDR%IFIELDS_IO = ICHUNK
      II=0
      DO JFLD=1,ICHUNK
        IF(IMSG(II+1) == -HUGE(IMSG)) THEN
          YDR%IFIELDS_IO = JFLD-1
          YD%LEOF = .TRUE.
          EXIT
        ELSE
          YDR%ILEVS(JFLD) = IMSG(II+1)
          YDR%IGRBCODES(JFLD) = IMSG(II+2)
          IFROM(JFLD) = IMSG(II+3)
        ENDIF
        II=II+3
      ENDDO
    ENDIF
    IF(YDR%LINTERP) THEN
      IFLD=0
!!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(JFLD)
      DO JFLD=1,YDR%IFIELDS_IO
        IF(IFROM(JFLD) == YIOS%MYPROC) THEN
          IFLD=IFLD+1
          CALL SPINT(ZSPECG(:,IFLD),ISPEC2G,&
           & YDR%IRESOLI,YDR%YSPF%ISMAX)
        ENDIF
      ENDDO
!!$OMP END PARALLEL DO
    ENDIF

    IFLD=0
    DO JFLD=1,YDR%IFIELDS_IO
      IF(IFROM(JFLD) == YIOS%MYPROC) THEN
        IFLD=IFLD+1
      ENDIF
    ENDDO
    IMYGFLD=IFLD

    IBSET(:)=-1
    IFLD=0
    IFLDG=0
    DO JFLD=1,YDR%IFIELDS_IO
      IF(IMISSINGX == 0 .AND. .NOT. YDR%LREADALL) EXIT
      IFLDG=IFLDG+1
      IF(IFROM(JFLD) == YIOS%MYPROC) THEN
        IFLD=IFLD+1
      ENDIF
      IF(KFLD2 > 0) THEN
        IF(ANY(YDR%YMDL_2D%ILEVS(:) > 0)) THEN
          IFF = IFIND2(YDR%IGRBCODES(JFLD),YDR%ILEVS(JFLD),&
           & YDR%YMDL_2D%IGRIBCODE(:),YDR%YMDL_2D%ILEVS(:))
        ELSE
          IFF = IFIND(YDR%IGRBCODES(JFLD),YDR%YMDL_2D%IGRIBCODE(:))
        ENDIF
        IF(IFF > 0 )THEN
          IF(LLMISSING2DX(IFF) .OR. YDR%LREADALL) THEN
            IBSET(IFLDG) = YDR%YMDL_2D%IBSET(IFF)
            IIFROM(IFLDG)=IFROM(JFLD)
            IMISSINGX = IMISSINGX-1
            LLMISSING2DX(IFF) = .FALSE.
            CYCLE
          ENDIF
        ENDIF
      ENDIF
      IF(YDR%YMDL_3D%NUMFLDS > 0) THEN
        IFF  = IFIND(YDR%IGRBCODES(JFLD),YDR%YMDL_3D%IGRIBCODE(:))
        ILEV = YDR%ILEVS(JFLD)
        IF(IFF > 0 )THEN
          IF( LLMISSING3DX(ILEV,IFF) .OR. YDR%LREADALL )THEN
            LLMISSING3DX(ILEV,IFF) = .FALSE.
            IMISSINGX = IMISSINGX-1
            IBSET(IFLDG) = YDR%YMDL_3D%IBSET(ILEV)
            IIFROM(IFLDG)=IFROM(JFLD)
            CYCLE
          ENDIF
        ENDIF
      ENDIF
! We don't seem to want this field
      WRITE(YIOS%NULOUT,*) 'IOSTREAM : SPEC_IN - SKIPPING FIELD ',YDR%IGRBCODES(JFLD),YDR%ILEVS(JFLD)
      IF(IBSET(IFLDG) == -1) THEN
        IF(IFROM(JFLD) == YIOS%MYPROC) THEN
          DO JJ=IFLD,IMYGFLD-1
            ZSPECG(:,JJ)=ZSPECG(:,JJ+1) 
          ENDDO
          IMYGFLD=IMYGFLD-1
          IFLD=IFLD-1
        ENDIF
        IFLDG=IFLDG-1
      ENDIF
    ENDDO
    
    IF(IFLDG > 0 .AND. YDR%LDISTRIBUTE ) THEN
      CALL DIST_SPEC(PSPECG=ZSPECG,KFDISTG=IFLDG,KFROM=IIFROM,KVSET=IBSET,&
       & KRESOL=YDR%NRESOL,PSPEC=ZSPEC,LDIM1_IS_FLD=.FALSE.,KSMAX=YDR%YSPF%ISMAX)
    ENDIF
    IIFLD=0
    INOFF2=0
    INOFF3=0
    DO JFLD=1,YDR%IFIELDS_IO
      IF(IMISSING == 0 .AND. .NOT. YDR%LREADALL) EXIT
      IF(KFLD2 > 0) THEN
        IF(ANY(YDR%YMDL_2D%ILEVS(:) > 0)) THEN
          IFF = IFIND2(YDR%IGRBCODES(JFLD),YDR%ILEVS(JFLD),&
           & YDR%YMDL_2D%IGRIBCODE(:),YDR%YMDL_2D%ILEVS(:))
        ELSE
          IFF = IFIND(YDR%IGRBCODES(JFLD),YDR%YMDL_2D%IGRIBCODE(:))
        ENDIF
        IF(IFF > 0) THEN
          IF(LLMISSING2D(IFF) .OR. YDR%LREADALL) THEN
            IMISSING = IMISSING-1
            LLMISSING2D(IFF) = .FALSE.
            IF(YDR%YMDL_2D%ILOCFLD(IFF) > 0 .OR. YD%SERIAL ) THEN
              INOFF2 = INOFF2 + 1
              IIFLD = IIFLD+1
              IFF2(INOFF2) = IFF
              IFLD2(INOFF2) = IIFLD
            ENDIF
            CYCLE
          ENDIF
        ENDIF
      ENDIF
      IF(YDR%YMDL_3D%NUMFLDS > 0) THEN
        IFF  = IFIND(YDR%IGRBCODES(JFLD),YDR%YMDL_3D%IGRIBCODE(:))
        ILEV = YDR%ILEVS(JFLD)
        IF(IFF > 0 )THEN
          IF(LLMISSING3D(ILEV,IFF) .OR. YDR%LREADALL) THEN
            LLMISSING3D(ILEV,IFF) = .FALSE.
            IMISSING = IMISSING-1
            IF(YD%SERIAL) THEN
              ILOCLV=ILEV
            ELSE
              ILOCLV = YDR%YMDL_3D%ILOCLEV(ILEV)
            ENDIF
            IF(ILOCLV > 0) THEN
              INOFF3 = INOFF3+1
              IIFLD    = IIFLD+1
              IFF3(INOFF3) = IFF
              IFLD3(INOFF3) = IIFLD
              ILEV3(INOFF3) = ILOCLV
            ENDIF
            CYCLE
          ENDIF
        ENDIF
      ENDIF
    ENDDO
    IF(YDR%LDISTRIBUTE) THEN
      DO JFLD=1,INOFF2
        IFF = IFF2(JFLD)
        IIFLD = IFLD2(JFLD)
        PSPEC2(:,YDR%YMDL_2D%ILOCFLD(IFF)) = ZSPEC(:,IIFLD)
      ENDDO
    ELSE
      DO JFLD=1,INOFF2
        IFF = IFF2(JFLD)
        IIFLD = IFLD2(JFLD)
        PSPEC2(:,IFF) = ZSPECG(:,IIFLD)
      ENDDO
    ENDIF
    IF(YDR%LDISTRIBUTE) THEN
      DO JFLD=1,INOFF3
        IFF = IFF3(JFLD)
        IIFLD = IFLD3(JFLD)
        ILOCLV = ILEV3(JFLD)
        PSPEC3(ILOCLV,:,IFF) = ZSPEC(:,IIFLD)
      ENDDO
    ELSE
      DO JFLD=1,INOFF3
        IFF = IFF3(JFLD)
        IIFLD = IFLD3(JFLD)
        ILOCLV = ILEV3(JFLD)
        PSPEC3(ILOCLV,:,IFF) = ZSPECG(:,IIFLD)
      ENDDO
    ENDIF

  ENDDO

  LLFAIL = .FALSE.
  IF(KFLD2 > 0)  THEN
    IF(PRESENT(LDMISSING_2D)) THEN
      IF(SIZE(LDMISSING_2D) < KFLD2)&
       & CALL ABOR1('IOSTREAM_MIX:SPEC_IN - LDMISSING_2D TOO SHORT')
      LDMISSING_2D(1:KFLD2) = LLMISSING2D(:)
    ELSE
      DO JFLD=1,KFLD2
        IF(LLMISSING2D(JFLD)) THEN
          WRITE(YIOS%NULOUT,*)'SPECTRAL 2D FIELD MISSING: ',&
           & YDR%YMDL_2D%IGRIBCODE(JFLD)
          LLFAIL = .TRUE.
        ENDIF
      ENDDO
    ENDIF
  ENDIF
  IF(KFLD3 > 0)  THEN
    IF(PRESENT(LDMISSING_3D)) THEN
      IF(SIZE(LDMISSING_3D,1) < KLEV)&
       & CALL ABOR1('IOSTREAM_MIX:SPEC_IN - LDMISSING_3D DIM 1 TOO SMALL')
      IF(SIZE(LDMISSING_3D,2) < KFLD3)&
       & CALL ABOR1('IOSTREAM_MIX:SPEC_IN - LDMISSING_3D DIM 2 TOO SMALL')
      LDMISSING_3D(1:KLEV,1:KFLD3) = LLMISSING3D(1:KLEV,1:KFLD3)
    ELSE
      DO JFLD=1,YDR%YMDL_3D%NUMFLDS
        DO JLEV=1,KLEV
          IF(LLMISSING3D(JLEV,JFLD)) THEN
            WRITE(YIOS%NULOUT,*)'SPECTRAL 3D FIELD MISSING: ',&
             & YDR%YMDL_3D%IGRIBCODE(JFLD),JLEV
            LLFAIL = .TRUE.
          ENDIF
        ENDDO
      ENDDO
    ENDIF
  ENDIF
  IF(LLFAIL) CALL ABOR1('IOSTREAM_MIX:SPEC_IN - MISSING FIELD')
  DEALLOCATE(ZSPECG)
  DEALLOCATE(ZSPEC)
  DEALLOCATE(IMSG)
  DEALLOCATE(YDR%IGRBCODES)
  DEALLOCATE(YDR%IHAVEFLD)
  DEALLOCATE(YDR%ILEVS)
ELSE

  IF(KFLD2 > 0) THEN
    CALL ASSIGN_IOPROC(YD,IFROM2,IMYCOUNT,KFLDPROC2D)
    IBSET2(:) = YDR%YMDL_2D%IBSET(:)
    ALLOCATE(ZSPECG(ISPEC2G,IMYCOUNT))
    ALLOCATE(YDR%IGRBCODES(IMYCOUNT))
    ALLOCATE(YDR%IHAVEFLD(IMYCOUNT))
    ALLOCATE(YDR%ILEVS(IMYCOUNT))
    CALL READ_RECORD(YD,YDR,IMYCOUNT,PREC=ZSPECG)
    IFLD = 0
    DO JFLD=1,KFLD2
      IF(IFROM2(JFLD) == YIOS%MYPROC) THEN
        IFLD = IFLD+1
        IF(YDR%LGRIB) THEN
          IF(YDR%IGRBCODES(IFLD) /= YDR%YMDL_2D%IGRIBCODE(JFLD)) THEN
            WRITE(YIOS%NULERR,*) 'IOSTREAM_MIX:SPEC_IN - EXPECTED CODE ',&
             & YDR%YMDL_2D%IGRIBCODE(JFLD),&
             & ' FOUND CODE ', YDR%IGRBCODES(IFLD)
            CALL ABOR1('IOSTREAM_MIX:SPEC_IN - UNEXPECTED GRIB CODE')
          ENDIF
          IF(ANY(YDR%YMDL_2D%ILEVS(:) > 0)) THEN
            IF(YDR%ILEVS(IFLD) /= YDR%YMDL_2D%ILEVS(JFLD)) THEN
              WRITE(YIOS%NULERR,*) 'IOSTREAM_MIX:SPEC_IN - EXPECTED LEVEL ',&
               & YDR%YMDL_2D%ILEVS(JFLD),&
               & ' FOUND LEVEL ', YDR%ILEVS(IFLD)
              CALL ABOR1('IOSTREAM_MIX:SPEC_IN - UNEXPECTED LEVEL')
            ENDIF
          ENDIF
        ENDIF
      ENDIF
    ENDDO
    CALL DIST_SPEC(PSPECG=ZSPECG,KFDISTG=KFLD2,KFROM=IFROM2,&
     & KVSET=IBSET2,PSPEC=PSPEC2,LDIM1_IS_FLD=.FALSE.,KSMAX=YDR%YSPF%ISMAX)
    DEALLOCATE(ZSPECG)
    DEALLOCATE(YDR%IGRBCODES)
    DEALLOCATE(YDR%IHAVEFLD)
    DEALLOCATE(YDR%ILEVS)
  ENDIF

  IF(YDR%YMDL_3D%NUMFLDS > 0) THEN
    CALL ASSIGN_IOPROC(YD,IFROM3,IMYCOUNT,KLEVPROC3D)
    IBSET3(:) = YDR%YMDL_3D%IBSET(:)
    ALLOCATE(ZSPECGT(IMYCOUNT,ISPEC2G,1))
    ALLOCATE(YDR%IGRBCODES(IMYCOUNT))
    ALLOCATE(YDR%IHAVEFLD(IMYCOUNT))
    ALLOCATE(YDR%ILEVS(IMYCOUNT))
    DO JFLD=1,YDR%YMDL_3D%NUMFLDS
      DO JLEV=1,IMYCOUNT
        CALL READ_RECORD(YD,YDR,1,PREC=ZSPECGT(JLEV,:,:))
      ENDDO
!!$      ILEV = 0
!!$      DO JLEV=1,YDR%YMDL_3D%NLEVS
!!$        IF(IFROM3(JLEV) == YIOS%MYPROC) THEN
!!$          ILEV = ILEV+1
!!$          IF(YDR%LGRIB) THEN
!!$            IF(YDR%IGRBCODES(ILEV) /= YDR%YMDL_3D%IGRIBCODE(JFLD)) THEN
!!$              WRITE(YIOS%NULERR,*) 'IOSTREAM_MIX:SPEC_IN - EXPECTED CODE ',&
!!$               & YDR%YMDL_3D%IGRIBCODE(JFLD),&
!!$               & ' FOUND CODE ', YDR%IGRBCODES(ILEV)
!!$              CALL ABOR1('IOSTREAM_MIX:SPEC_IN - UNEXPECTED GRIB CODE')
!!$            ENDIF
!!$          ENDIF
!!$        ENDIF
!!$      ENDDO
      CALL DIST_SPEC(PSPECG=ZSPECGT(:,:,1),KFDISTG=KLEV,KFROM=IFROM3,&
       & KVSET=IBSET3,PSPEC=PSPEC3(:,:,JFLD),KSMAX=YDR%YSPF%ISMAX)
    ENDDO
    DEALLOCATE(ZSPECGT)
    DEALLOCATE(YDR%IGRBCODES)
    DEALLOCATE(YDR%IHAVEFLD)
    DEALLOCATE(YDR%ILEVS)
  ENDIF
ENDIF
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:SPEC_IN',1,ZHOOK_HANDLE)
  
END SUBROUTINE SPEC_IN
!=================================================================
SUBROUTINE GRID_IN(YD,YDR,PGRID2,PGRID3,KFLD2,KLEV,KFLD3,&
 &LDMISSING_2D,LDMISSING_3D,KFLDPROC2D)

!**** *GRID_IN*  - Read in grid-point data

! Purpose - Read and distribute grid-point fields

! Explicit arguments :
! --------------------
! YD      - IO stream structure
! YDR     - IO request structure
! PGRID2  - 2-D grid-point fields
! PGRID3  - 3-D grid-point fields
! KFLD2   - number of 2-D grid-point fields
! KLEV    - number of levels in 3-D grid-point fields 
! KFLD3   - number of 3-D grid-point fields
! LDMISSING_2D - missing 2-D fields 
! LDMISSING_2D - missing 3-D fields 
! KFLDPROC2D - 
!-------------------------------------------------------------------------
IMPLICIT NONE
TYPE(TYPE_IOSTREAM) ,INTENT(INOUT) :: YD
TYPE(TYPE_IOREQUEST),INTENT(INOUT) :: YDR
REAL(KIND=JPRB),OPTIONAL,INTENT(INOUT) :: PGRID2(:,:,:)
REAL(KIND=JPRB),OPTIONAL,INTENT(INOUT) :: PGRID3(:,:,:,:)
INTEGER(KIND=JPIM),INTENT(IN) :: KFLD2,KLEV,KFLD3
LOGICAL,OPTIONAL,INTENT(OUT)  :: LDMISSING_2D(:)
LOGICAL,OPTIONAL,INTENT(OUT)  :: LDMISSING_3D(:,:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KFLDPROC2D(:)
INTEGER(KIND=JPIM) :: IFROM2(KFLD2)
INTEGER(KIND=JPIM) :: IFROM3(KLEV)
INTEGER(KIND=JPIM) :: IGRID2G,JFLD,JLEV,JBLK,IFF,ILEV,IMISSING,ICHUNKSZ,IGPBLKS,II
INTEGER(KIND=JPIM) :: ICHUNK,IHELP
INTEGER(KIND=JPIM),ALLOCATABLE :: IMSG(:)
INTEGER(KIND=JPIM) :: IFROM(JPMAXCHUNK)
INTEGER(KIND=JPIM) :: INOFF2, INOFF3, IFF2(JPMAXCHUNK),IFF3(JPMAXCHUNK),&
 & IFLD2(JPMAXCHUNK),IFLD3(JPMAXCHUNK)
INTEGER(KIND=JPIM) :: ILATS,IGP,IPTS,IFLD,IMYCOUNT
INTEGER(KIND=JPIB) :: IBUFLEN
INTEGER(KIND=JPIM),ALLOCATABLE  :: ILONS(:)
REAL(KIND=JPRB),ALLOCATABLE :: ZGRIDG(:,:),ZZGRIDG(:),ZGRIDL(:,:,:)
REAL(KIND=JPRB),ALLOCATABLE :: ZLATS(:)
LOGICAL :: LLMISSING2D(KFLD2),LLMISSING3D(KLEV,KFLD3),LLFAIL
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
!-------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:GRID_IN',0,ZHOOK_HANDLE)

IF(YDR%LRANDOM_ORDER) THEN
  IF(YD%NUMIOPROCS /= 1)&
   & CALL ABOR1('IOSTREAM_MIX:GRID_IN - READING RANDOM FILE WITH > 1 IOPROCS')
  IF(YD%LIONOHELP) THEN
    YD%LIOUSEHELP = .FALSE.
  ELSE
    YD%LIOUSEHELP = YDR%LDISTRIBUTE
  ENDIF
  IF(KFLD2 > 0) LLMISSING2D(:)   = .TRUE.
  IF(KFLD3 > 0) LLMISSING3D(:,:) = .TRUE.
  IMISSING = KLEV*KFLD3+KFLD2
  IF (YD%LNVALS) THEN
    IBUFLEN=YD%NVALS
  ELSEIF (YDR%LDISTRIBUTE.AND.(.NOT.YDR%LINTERP)) THEN
    IBUFLEN = YDR%YGPF%IGPTOTG
  ELSE
    IBUFLEN = YIOS%IMAXGRID
  ENDIF
  IF(YD%LIOUSEHELP) THEN
    IHELP=YD%NUMHLP(1)-1
  ELSE
    IHELP=0
  ENDIF
  ICHUNKSZ = YDR%NCHUNKSIZE*MAX(1,IHELP)
  ICHUNKSZ = MIN(ICHUNKSZ,IMISSING+5)
  IF(.NOT. YDR%LREADALL) ICHUNKSZ = MIN(ICHUNKSZ,IMISSING)
  IF(ICHUNKSZ > JPMAXCHUNK) CALL ABOR1('IOSTREAM_MIX:GRID_IN - ICHUNKSZ > JPMAXCHUNK')
  IF(ANY(YD%IOPROCSHLP(1,:) == YIOS%MYPROC)) THEN
    ALLOCATE(ZGRIDG(IBUFLEN,ICHUNKSZ/MAX(1,IHELP)+1))
  ELSE
    ALLOCATE(ZGRIDG(1,1))
  ENDIF
  IF(YDR%LDISTRIBUTE) THEN
    IGPBLKS = (YDR%YGPF%IGPTOT-1)/YDR%YGPF%NPROMA+1
    ALLOCATE(ZGRIDL(YDR%YGPF%NPROMA,ICHUNKSZ,IGPBLKS))
  ENDIF
  ALLOCATE(IMSG(3*ICHUNKSZ))
  ALLOCATE(YDR%IGRBCODES(ICHUNKSZ))
  ALLOCATE(YDR%ILEVS(ICHUNKSZ))
  ALLOCATE(YDR%IHAVEFLD(ICHUNKSZ))
  IFROM(1:ICHUNKSZ) = YD%IOPROCS(1)
  DO 
    IF(YD%LEOF) EXIT
    IF(IMISSING == 0 .AND. .NOT. YDR%LREADALL) EXIT
    ICHUNK = MIN(ICHUNKSZ,IMISSING)
    IF(YDR%LREADALL) ICHUNK=ICHUNKSZ
    IF(ANY(YD%IOPROCSHLP(1,:) == YIOS%MYPROC)) THEN
      CALL READ_RECORD(YD,YDR,ICHUNK,PREC=ZGRIDG)
      IF(YD%LIOPROC.AND.YDR%LDISTHEADER) THEN
        II=0
        DO JFLD=1,MIN(YDR%IFIELDS_IO,ICHUNK)
          IMSG(II+1) = YDR%ILEVS(JFLD)
          IMSG(II+2) = YDR%IGRBCODES(JFLD)
          IMSG(II+3) = YDR%IHAVEFLD(JFLD)
          II=II+3
        ENDDO
        DO JFLD=YDR%IFIELDS_IO+1,ICHUNK
          IMSG(II+1) = -HUGE(IMSG)
          IMSG(II+2) = 0
          IMSG(II+3) = 0
          II=II+3
        ENDDO
      ENDIF
    ENDIF
    IF(YDR%LDISTHEADER) THEN
      CALL GSTATS_BARRIER(727)
      CALL GSTATS(632,0)
      CALL MPL_BROADCAST(IMSG,KTAG=99,KROOT=YD%IOPROCS(1),&
       & CDSTRING='IOSTREAM_MIX:GRID_IN')
      CALL GSTATS(632,1)
      CALL GSTATS_BARRIER2(727)
      YDR%IFIELDS_IO = ICHUNK
      II=0
      DO JFLD=1,ICHUNK
        IF(IMSG(II+1) == -HUGE(IMSG)) THEN
          YDR%IFIELDS_IO = JFLD-1
          YD%LEOF = .TRUE.
          EXIT
        ELSE
          YDR%ILEVS(JFLD) = IMSG(II+1)
          YDR%IGRBCODES(JFLD) = IMSG(II+2)
          IFROM(JFLD) = IMSG(II+3)
        ENDIF
        II=II+3
      ENDDO
    ENDIF
    IF(YDR%IFIELDS_IO == 0) EXIT
    IF(YDR%LINTERP) THEN
      IF(YIOS%NDGLG == YIOS%NUNDEF) CALL ABOR1('IOSTREAM_MIX:GRID_IN - YIOS%NDGLG NOT DEFINED')
      IF(.NOT.ALLOCATED(YIOS%NLOENG)) CALL ABOR1('IOSTREAM_MIX:GRID_IN - YIOS%NLOENG NOT DEFINED')
      IF(.NOT.ALLOCATED(YIOS%RLATIG)) CALL ABOR1('IOSTREAM_MIX:GRID_IN - YIOS%RLATIG NOT DEFINED')
      IF(ANY(IFROM(1:YDR%IFIELDS_IO) == YIOS%MYPROC)) THEN
        CALL INQUIRE_GRIB(YDR%IGRIB_HANDLE,KLATS=ILATS)
        ALLOCATE(ILONS(ILATS))
        ALLOCATE(ZLATS(ILATS))
        CALL INQUIRE_GRIB(YDR%IGRIB_HANDLE,KLONS=ILONS,PLATS=ZLATS,KPTS=IGP)
        ALLOCATE(ZZGRIDG(IGP))
        IFLD=0
        DO JFLD=1,YDR%IFIELDS_IO
          IF(IFROM(JFLD) == YIOS%MYPROC) THEN
            IFLD=IFLD+1
            ZZGRIDG(1:IGP) = ZGRIDG(1:IGP,IFLD)
            CALL INTERP_GP(YDR%NTYPE_INTERP,YDR%IGRBCODES(JFLD),YDR%ILEVS(JFLD),&
             & ILATS,ILONS,ZLATS,YIOS%NDGLG,YIOS%NLOENG(1:YIOS%NDGLG),YIOS%RLATIG(1:YIOS%NDGLG),&
             & YIOS%VAH,YIOS%VBH, &
             & ZZGRIDG,ZGRIDG(1:YDR%YGPF%IGPTOTG,IFLD),YDR%CLEVTYPE)
          ENDIF
        ENDDO
        DEALLOCATE(ZZGRIDG)
        DEALLOCATE(ILONS)
        DEALLOCATE(ZLATS)
      ENDIF
    ENDIF

    IF(YDR%LDISTRIBUTE) THEN
      CALL DIST_GRID(PGPG=ZGRIDG,KFDISTG=YDR%IFIELDS_IO,KFROM=IFROM(1:YDR%IFIELDS_IO),&
       & PGP=ZGRIDL,KPROMA=YDR%YGPF%NPROMA,KRESOL=YDR%NRESOL)
    ENDIF

    INOFF2 = 0
    INOFF3 = 0
    DO JFLD=1,YDR%IFIELDS_IO
      IF(IMISSING == 0 .AND. .NOT. YDR%LREADALL) EXIT
      IF(KFLD2 > 0) THEN
        IF(ANY(YDR%YMDL_2D%ILEVS(:) > 0)) THEN
          IFF = IFIND2(YDR%IGRBCODES(JFLD),YDR%ILEVS(JFLD),&
           & YDR%YMDL_2D%IGRIBCODE(:),YDR%YMDL_2D%ILEVS(:))
        ELSE
          IFF = IFIND(YDR%IGRBCODES(JFLD),YDR%YMDL_2D%IGRIBCODE(:))
        ENDIF
        IF( IFF > 0 )THEN
          IF( LLMISSING2D(IFF) .OR. YDR%LREADALL ) THEN
            IMISSING = IMISSING-1
            LLMISSING2D(IFF) = .FALSE.
            INOFF2 = INOFF2+1
            IFF2(INOFF2) = IFF
            IFLD2(INOFF2) = JFLD
            CYCLE
          ENDIF
        ENDIF
      ENDIF

      IF(YDR%YMDL_3D%NUMFLDS > 0) THEN
        IFF  = IFIND(YDR%IGRBCODES(JFLD),YDR%YMDL_3D%IGRIBCODE(:))
        ILEV = IFIND(YDR%ILEVS(JFLD),YDR%YMDL_3D%ILEVS(:))
        IF( IFF > 0 .AND. ILEV > 0 )THEN
          IF( LLMISSING3D(ILEV,IFF) .OR. YDR%LREADALL )THEN
            LLMISSING3D(ILEV,IFF) = .FALSE.
            IMISSING = IMISSING-1
            INOFF3 = INOFF3+1
            IFF3(INOFF3) = IFF
            IFLD3(INOFF3) = JFLD
            CYCLE
          ENDIF
        ENDIF
      ENDIF
    ENDDO

    IF(YDR%LDISTRIBUTE) THEN
      CALL GSTATS(1481,0)
!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(JFLD,IFF,IFLD,JBLK)
      DO JFLD=1,INOFF2
        IFF  = IFF2(JFLD)
        IFLD = IFLD2(JFLD)
        DO JBLK=1,IGPBLKS
          PGRID2(:,IFF,JBLK) = ZGRIDL(:,IFLD,JBLK)
        ENDDO
      ENDDO
!$OMP END PARALLEL DO
      CALL GSTATS(1481,1)
    ELSEIF(YD%IOPROCS(1) == YIOS%MYPROC) THEN
      DO JFLD=1,INOFF2
        IFF  = IFF2(JFLD)
        IFLD = IFLD2(JFLD)
        IF (.NOT. YDR%LINTERP) THEN 
          CALL INQUIRE_GRIB(YDR%IGRIB_HANDLE,KPTS=IPTS)
          IF(SIZE(PGRID2,1) < IPTS) &
            & CALL ABOR1('IOSTREAM_MIX:GRID_IN - DIM 1 OF PGRID2 TOO SMALL')
        ELSE
          IPTS = YDR%YGPF%IGPTOTG
          IF (SIZE(PGRID2(:,IFF:IFF,:)) /= IPTS) &
           & CALL ABOR1('IOSTREAM_MIX:GRID_IN - DIMS OF PGRID2 WRONG')
        ENDIF 
        CALL COPYGP_GLOB2LOC(ZGRIDG(:,IFLD:IFLD),PGRID2(:,IFF:IFF,:),&
         & YDR%YGPF%NPROMA,IPTS)
      ENDDO
    ENDIF

    IF(YDR%LDISTRIBUTE) THEN
      CALL GSTATS(1481,0)
!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(JFLD,IFF,IFLD,ILEV,JBLK)
      DO JFLD=1,INOFF3
        IFF  = IFF3(JFLD)
        IFLD = IFLD3(JFLD)
        ILEV = YDR%ILEVS(IFLD)
        DO JBLK=1,IGPBLKS
          PGRID3(:,ILEV,IFF,JBLK) = ZGRIDL(:,IFLD,JBLK)
        ENDDO
      ENDDO
!$OMP END PARALLEL DO
      CALL GSTATS(1481,1)
    ELSEIF(YD%IOPROCS(1) == YIOS%MYPROC) THEN
      DO JFLD=1,INOFF3
        IFF  = IFF3(JFLD)
        IFLD = IFLD3(JFLD)
        ILEV = YDR%ILEVS(IFLD)
        IF (.NOT. YDR%LINTERP) THEN 
          CALL INQUIRE_GRIB(YDR%IGRIB_HANDLE,KPTS=IPTS)
          IF(SIZE(PGRID3,1) < IPTS)&
           & CALL ABOR1('IOSTREAM_MIX:GRID_IN - DIM 1 OF PGRID3 TOO SMALL')
        ELSE
          IPTS = YDR%YGPF%IGPTOTG
          IF (SIZE(PGRID3(:,ILEV:ILEV,IFF,:)) /= IPTS) &
           & CALL ABOR1('IOSTREAM_MIX:GRID_IN - DIMS OF PGRID3 WRONG')
        ENDIF 
        CALL COPYGP_GLOB2LOC(ZGRIDG(:,IFLD:IFLD),PGRID3(:,ILEV:ILEV,IFF,:),&
         & YDR%YGPF%NPROMA,IPTS)
      ENDDO
    ENDIF

  ENDDO

  LLFAIL = .FALSE.
  IF(KFLD2 > 0)  THEN
    IF(PRESENT(LDMISSING_2D)) THEN
      IF(SIZE(LDMISSING_2D) < KFLD2)&
       & CALL ABOR1('IOSTREAM_MIX:GRID_IN - LDMISSING_2D TOO SHORT')
      LDMISSING_2D(1:KFLD2) = LLMISSING2D(:)
    ELSE
      DO JFLD=1,KFLD2
        IF(LLMISSING2D(JFLD)) THEN
          WRITE(YIOS%NULOUT,*)'GRIDPOINT 2D FIELD MISSING: ',&
           & YDR%YMDL_2D%IGRIBCODE(JFLD)
          LLFAIL = .TRUE.
        ENDIF
      ENDDO
    ENDIF
  ENDIF
  IF(KFLD3 > 0)  THEN
    IF(PRESENT(LDMISSING_3D)) THEN
      IF(SIZE(LDMISSING_3D,1) < KLEV)&
       & CALL ABOR1('IOSTREAM_MIX:GRID_IN - LDMISSING_3D DIM 1 TOO SMALL')
      IF(SIZE(LDMISSING_3D,2) < KFLD3)&
       & CALL ABOR1('IOSTREAM_MIX:GRID_IN - LDMISSING_3D DIM 2 TOO SMALL')
      LDMISSING_3D(1:KLEV,1:KFLD3) = LLMISSING3D(:,:)
    ELSE
      DO JFLD=1,YDR%YMDL_3D%NUMFLDS
        DO JLEV=1,KLEV
          IF(LLMISSING3D(JLEV,JFLD)) THEN
            WRITE(YIOS%NULOUT,*)'GRIDPOINT 3D FIELD MISSING: ',&
             & YDR%YMDL_3D%IGRIBCODE(JFLD),JLEV
            LLFAIL = .TRUE.
          ENDIF
        ENDDO
      ENDDO
    ENDIF
  ENDIF
  IF(LLFAIL) CALL ABOR1('IOSTREAM_MIX:GRID_IN - MISSING FIELD')
  IF(ALLOCATED(ZGRIDG)) DEALLOCATE(ZGRIDG)
  IF(ALLOCATED(ZGRIDL)) DEALLOCATE(ZGRIDL)
  DEALLOCATE(IMSG)
  DEALLOCATE(YDR%IGRBCODES)
  DEALLOCATE(YDR%IHAVEFLD)
  DEALLOCATE(YDR%ILEVS)
ELSE

  IF(KFLD2 > 0) THEN
    CALL ASSIGN_IOPROC(YD,IFROM2,IMYCOUNT,KFLDPROC2D)
    IGRID2G = YDR%YGPF%IGPTOTG
    ALLOCATE(ZGRIDG(IGRID2G,IMYCOUNT))
    ALLOCATE(YDR%IGRBCODES(IMYCOUNT))
    ALLOCATE(YDR%IHAVEFLD(IMYCOUNT))
    ALLOCATE(YDR%ILEVS(IMYCOUNT))
    IF(YD%LIOPROC) THEN
      CALL READ_RECORD(YD,YDR,IMYCOUNT,PREC=ZGRIDG)
      IFLD = 0
      DO JFLD=1,KFLD2
        IF(IFROM2(JFLD) == YIOS%MYPROC) THEN
          IFLD = IFLD+1
          IF(YDR%LGRIB) THEN
            IF(YDR%IGRBCODES(IFLD) /= YDR%YMDL_2D%IGRIBCODE(JFLD)) THEN
              WRITE(YIOS%NULERR,*) 'IOSTREAM_MIX:GRID_IN - EXPECTED CODE ',&
               & YDR%YMDL_2D%IGRIBCODE(JFLD),&
               & ' FOUND CODE ', YDR%IGRBCODES(IFLD)
              CALL ABOR1('IOSTREAM_MIX:GRID_IN - UNEXPECTED GRIB CODE')
            ENDIF
            IF(ANY(YDR%YMDL_2D%ILEVS(:) > 0)) THEN
              IF(YDR%ILEVS(IFLD) /= YDR%YMDL_2D%ILEVS(JFLD)) THEN
                WRITE(YIOS%NULERR,*) 'IOSTREAM_MIX:GRID_IN - EXPECTED LEVEL ',&
                 & YDR%YMDL_2D%ILEVS(JFLD),&
                 & ' FOUND LEVEL ', YDR%ILEVS(IFLD)
                CALL ABOR1('IOSTREAM_MIX:GRID_IN - UNEXPECTED LEVEL')
              ENDIF
            ENDIF
          ENDIF
        ENDIF
      ENDDO
    ENDIF
    CALL DIST_GRID(PGPG=ZGRIDG,KFDISTG=KFLD2,KFROM=IFROM2,&
     & PGP=PGRID2,KPROMA=YDR%YGPF%NPROMA,KRESOL=YDR%NRESOL)
    DEALLOCATE(ZGRIDG)
    DEALLOCATE(YDR%IGRBCODES)
    DEALLOCATE(YDR%IHAVEFLD)
    DEALLOCATE(YDR%ILEVS)
  ENDIF

  IF(YDR%YMDL_3D%NUMFLDS > 0) THEN
    CALL ASSIGN_IOPROC(YD,IFROM3,IMYCOUNT)
    IGRID2G = YDR%YGPF%IGPTOTG
    ALLOCATE(ZGRIDG(IGRID2G,IMYCOUNT))
    ALLOCATE(YDR%IGRBCODES(IMYCOUNT))
    ALLOCATE(YDR%IHAVEFLD(IMYCOUNT))
    ALLOCATE(YDR%ILEVS(IMYCOUNT))
    DO JFLD=1,YDR%YMDL_3D%NUMFLDS
      CALL READ_RECORD(YD,YDR,IMYCOUNT,PREC=ZGRIDG)
      CALL DIST_GRID(PGPG=ZGRIDG,KFDISTG=KLEV,KFROM=IFROM3,&
       & PGP=PGRID3(:,:,JFLD,:),KPROMA=YDR%YGPF%NPROMA,KRESOL=YDR%NRESOL)
    ENDDO
    DEALLOCATE(ZGRIDG)
    DEALLOCATE(YDR%IGRBCODES)
    DEALLOCATE(YDR%IHAVEFLD)
    DEALLOCATE(YDR%ILEVS)
  ENDIF

ENDIF
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:GRID_IN',1,ZHOOK_HANDLE)
END SUBROUTINE GRID_IN
!=============================================================
SUBROUTINE WRITE_RECORD(YD,YDR,KFIELDS,PREC,KREC,PREC1,KREC1,CDREC,&
 & KALLFLDS,KIOFLDS,KHAVEFLDS)

!**** *WRITE_RECORD*  - Write out record

! Purpose - To write a record to device .

! Explicit arguments :
! --------------------
! YD   - IO stream structure
! YDR  - IO request structure
! KFIELDS - number of records
! PREC - real data
! KREC = integer data
! CDREC = character string data
!-------------------------------------------------------------------------
IMPLICIT NONE
TYPE(TYPE_IOSTREAM) , INTENT(INOUT)       :: YD
TYPE(TYPE_IOREQUEST), INTENT(INOUT)       :: YDR
INTEGER(KIND=JPIM)  , INTENT(IN)          :: KFIELDS
REAL(KIND=JPRB)     , OPTIONAL,INTENT(IN) :: PREC(:,:)
INTEGER(KIND=JPIM)  , OPTIONAL,INTENT(IN) :: KREC(:,:)
REAL(KIND=JPRB)     , OPTIONAL,INTENT(IN) :: PREC1(:)
INTEGER(KIND=JPIM)  , OPTIONAL,INTENT(IN) :: KREC1(:)
CHARACTER(LEN=*)    , OPTIONAL,INTENT(IN) :: CDREC
INTEGER(KIND=JPIM)  , OPTIONAL,INTENT(IN) :: KALLFLDS
INTEGER(KIND=JPIM)  , OPTIONAL,INTENT(IN) :: KIOFLDS(:)
INTEGER(KIND=JPIM)  , OPTIONAL,INTENT(IN) :: KHAVEFLDS(:)


INTEGER(KIND=JPIM) :: IRET,JFLD,ILENINT,I,J,IHELP
INTEGER(KIND=JPIM) :: ISIZE
INTEGER(KIND=JPKSIZE_T) :: IBYTES(KFIELDS)
INTEGER(KIND=JPIB) :: IBYTESB(KFIELDS)
REAL(KIND=JPRD) :: ZTIME0,ZTIME1
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
REAL(KIND=JPRB),ALLOCATABLE :: ZREC(:)
CHARACTER(LEN=YIOS%NINTLEN) :: CLREC
INTEGER(KIND=JPIM), ALLOCATABLE :: IREC(:)
INTEGER(KIND=JPIM) :: IFIELDS_CODE,ICHUNKSZ,JJFLD,IFI,ILA,IFLDS,IFLD
INTEGER(KIND=JPIM) :: ITAG,IPROCHLP,ILRECV,INUM,ISENDREQ
INTEGER(KIND=JPIM) :: IACTUALFIELDS,IACTUALFIELDSSUM
!-------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:WRITE_RECORD',0,ZHOOK_HANDLE)

YDR%IFIELDS_IO = KFIELDS
IBYTES(:) = 0
IF(YD%LIOUSEHELP) THEN
  IFIELDS_CODE = COUNT(YDR%IHAVEFLD(1:KFIELDS) == YIOS%MYPROC)
  ICHUNKSZ = YDR%NCHUNKSIZE
ELSE
  IFIELDS_CODE=KFIELDS
  ICHUNKSZ = KFIELDS
ENDIF

IF(YDR%LGRIB.AND.IFIELDS_CODE>0) THEN
  IF(.NOT.PRESENT(PREC)) CALL ABOR1('IOSTREAM_MIX:WRITE_RECORD - PREC MISSING')
  IF(YD%LIOPROC.OR.(YD%LIOPROCHLP.AND.YD%LIOUSEHELP)) THEN
    CALL GRIB_ENCODE(YDR,PREC,IFIELDS_CODE)
  ENDIF
ENDIF

IFLD=0
IF(YD%LIOPROC) THEN
  INUM=IFIND(YIOS%MYPROC,YD%IOPROCS)
  IHELP=YD%NUMHLP(INUM)
ELSE
  IHELP=-97
ENDIF
DO JJFLD=1,KFIELDS,MAX(1,ICHUNKSZ)
  IFI = JJFLD
  ILA = MIN(JJFLD+ICHUNKSZ-1,KFIELDS)
  IFLDS = ILA-IFI+1
  IF(YD%LIOUSEHELP) THEN
    CALL GSTATS_BARRIER(738)
    IF((YD%LIOPROC .AND. IHELP > 1) .OR. (YD%LIOPROCHLP .AND. IFIELDS_CODE>0)) THEN
      CALL GSTATS(649,0)
      IF(YD%LIOPROC) CALL ALLO_IGRIB(YDR,IFLDS)
      DO JFLD=IFI,ILA
        ITAG=16000+JFLD
        IF(YDR%IHAVEFLD(JFLD) == YIOS%MYPROC) THEN
          IFLD=IFLD+1
          CALL MPL_SEND(NGRIBD(NGSTA(IFLD):NGSTA(IFLD+1)-1),&
           & KMP_TYPE=JP_NON_BLOCKING_STANDARD,KREQUEST=ISENDREQ,&
           & KDEST=YD%MIOPROCHLP,KTAG=ITAG,&
           & CDSTRING='IOSTREAM_MIX:WRITE_RECORD')
          CALL MPL_WAIT(KREQUEST=ISENDREQ,&
           & CDSTRING='IOSTREAM_MIX:WRITE_RECORD')
        ELSEIF(YD%LIOPROC) THEN
          IFLD=JFLD-IFI+1
          IPROCHLP=IGETPROCHLP(YD,JFLD)
          CALL MPL_RECV(NGRIBD(NGSTA(IFLD):),KSOURCE=IPROCHLP,KTAG=ITAG,&
           & KOUNT=ILRECV,KMP_TYPE=JP_BLOCKING_STANDARD,CDSTRING='IOSTREAM_MIX:WRITE_RECORD')
          NGSTA(IFLD+1) = NGSTA(IFLD)+ILRECV
        ENDIF
      ENDDO
      CALL GSTATS(649,1)
    ENDIF
    CALL GSTATS_BARRIER2(738)
  ENDIF
  IF(YD%LIOPROC) THEN
    CALL GSTATS(1764,0)
    CALL USER_CLOCK(PELAPSED_TIME=ZTIME0)
    SELECT CASE(YD%IOTYPE)
    CASE(JPCIO)
      IF(YDR%LGRIB) THEN
        DO JFLD=1,IFLDS
          IBYTES(1) = (NGSTA(JFLD+1)-NGSTA(JFLD))*YIOS%NINTLEN
          IF (IBYTES(1) > 0) THEN
            CALL IGRIB_WRITE_BYTES(YD%IFILE_HANDLE,NGRIBD(NGSTA(JFLD):NGSTA(JFLD+1)-1),IBYTES(1))
          ENDIF
        ENDDO
      ELSE
        IF(PRESENT(PREC)) THEN
          ISIZE = SIZE(PREC,1)
          IFLDS = SIZE(PREC,2)
          IBYTES(1) = 1_JPKSIZE_T*ISIZE*YIOS%NREALEN
          ALLOCATE (ZREC (ISIZE))
          DO J=1,IFLDS
            CALL ISWAP (ZREC, PREC(:,J), YIOS%NREALEN, ISIZE)
            CALL IGRIB_WRITE_BYTES(YD%IFILE_HANDLE,ZREC,IBYTES(1))
          ENDDO
          DEALLOCATE (ZREC)
          IBYTES(1)=IBYTES(1)*IFLDS
        ELSEIF(PRESENT(PREC1)) THEN
          ISIZE = SIZE(PREC1)
          IBYTES(1) = 1_JPKSIZE_T*ISIZE*YIOS%NREALEN
          ALLOCATE (ZREC (ISIZE))
          CALL ISWAP (ZREC, PREC1, YIOS%NREALEN, ISIZE)
          CALL IGRIB_WRITE_BYTES(YD%IFILE_HANDLE,ZREC,IBYTES(1))
          DEALLOCATE (ZREC)
        ELSEIF(PRESENT(KREC)) THEN
          ISIZE = SIZE(KREC,1)
          IFLDS = SIZE(KREC,2)
          IBYTES(1) = 1_JPKSIZE_T*ISIZE*YIOS%NINTLEN
          ALLOCATE (IREC (ISIZE))
          DO J=1,IFLDS
            CALL ISWAP (IREC, KREC (:,J), YIOS%NINTLEN, ISIZE)
            CALL IGRIB_WRITE_BYTES(YD%IFILE_HANDLE,IREC,IBYTES(1))
          ENDDO
          DEALLOCATE (IREC)
          IBYTES(1)=IBYTES(1)*IFLDS
        ELSEIF(PRESENT(KREC1)) THEN
          ISIZE = SIZE(KREC1)
          IBYTES(1) = 1_JPKSIZE_T*ISIZE*YIOS%NINTLEN
          ALLOCATE (IREC (ISIZE))
          CALL ISWAP (IREC, KREC1, YIOS%NINTLEN, ISIZE)
          CALL IGRIB_WRITE_BYTES(YD%IFILE_HANDLE,IREC,IBYTES(1))
          DEALLOCATE (IREC)
        ELSEIF(PRESENT(CDREC)) THEN
          IBYTES(1) = LEN(CDREC) !! NB: One-byte characters are assumed!
          ALLOCATE(IREC( (LEN(CDREC)+YIOS%NINTLEN-1)/YIOS%NINTLEN ))
          I=1
          DO J=1,SIZE(IREC)
            CLREC=CDREC(I:MIN(LEN(CDREC),I+YIOS%NINTLEN-1))
            READ(CLREC,'(A)') IREC(J)
            I=I+YIOS%NINTLEN
          ENDDO
          CALL IGRIB_WRITE_BYTES(YD%IFILE_HANDLE,IREC,IBYTES(1))
          DEALLOCATE(IREC)
        ENDIF
      ENDIF
    CASE(JPFDB)
      IACTUALFIELDS=0
      DO JFLD=1,KFIELDS
        IF(NGSTA(JFLD+1)-NGSTA(JFLD)>0) THEN
          IACTUALFIELDS=IACTUALFIELDS+1
        ENDIF
      ENDDO
      IACTUALFIELDSSUM=IACTUALFIELDS
      CALL MPL_ALLREDUCE(IACTUALFIELDSSUM,CDOPER='SUM',KCOMM=YDR%IOCOMM,CDSTRING='IOSTREAM_MIX:WRITE_RECORD')
      DO JFLD=1,KFIELDS
        ILENINT = NGSTA(JFLD+1)-NGSTA(JFLD)
        IBYTESB(JFLD) = 1_JPIB*ILENINT*YIOS%NINTLEN
        IF (ILENINT > 0) THEN
          IFLD = JFLD+IFI-1
          CALL IWRITEFDBSUBS(NGRIBD(NGSTA(JFLD):NGSTA(JFLD+1)-1),ILENINT,IRET)
          IF( IRET /= 0 )THEN
            CALL ABOR1('IOSTREAM_MIX:WRITE_RECORD - FAIL')
          ENDIF
        ENDIF
      ENDDO
    END SELECT
    CALL GSTATS(1764,1)
    CALL USER_CLOCK(PELAPSED_TIME=ZTIME1)
    YD%WALLCLOCK = YD%WALLCLOCK+(ZTIME1-ZTIME0)
    TOTAL_WALLCLOCK = TOTAL_WALLCLOCK+(ZTIME1-ZTIME0)
  ENDIF
  YD%NBYTES = YD%NBYTES+SUM(IBYTESB(1:YDR%IFIELDS_IO))
ENDDO
! Deal with cases where IOPROCs have no fields but still need to call allreduce
IF( KFIELDS==0 .AND. YD%LIOPROC .AND. YD%IOTYPE==JPFDB ) THEN
  IACTUALFIELDSSUM=0
  CALL MPL_ALLREDUCE(IACTUALFIELDSSUM,CDOPER='SUM',KCOMM=YDR%IOCOMM,CDSTRING='IOSTREAM_MIX:WRITE_RECORD')
ENDIF
YD%NRECS = YD%NRECS+YDR%IFIELDS_IO


IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:WRITE_RECORD',1,ZHOOK_HANDLE)

END SUBROUTINE WRITE_RECORD
!=============================================================
SUBROUTINE READ_RECORD(YD,YDR,KFIELDS,PREC,KREC,PREC1,KREC1,CDREC)

!**** *READ_RECORD*  - Read in record

! Purpose - To read a record from device .

! Explicit arguments :
! --------------------
! YD   - IO stream structure
! YDR  - IO request structure
! KFIELDS - number of records
! PREC - real data
! KREC = integer data
! CDREC = character string data
!-------------------------------------------------------------------------
IMPLICIT NONE
TYPE(TYPE_IOSTREAM),INTENT(INOUT) :: YD
TYPE(TYPE_IOREQUEST),INTENT(INOUT) :: YDR
INTEGER(KIND=JPIM),INTENT(IN) :: KFIELDS
REAL(KIND=JPRB),OPTIONAL,INTENT(OUT)    :: PREC(:,:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KREC(:,:)
REAL(KIND=JPRB),OPTIONAL,INTENT(OUT)    :: PREC1(:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KREC1(:)
CHARACTER(LEN=*)  ,OPTIONAL,INTENT(OUT) :: CDREC

INTEGER(KIND=JPIM) :: IRET,JFLD,I,J,JJFLD,ILENGRB,IMINBUF
INTEGER(KIND=JPIM) :: IMSG(2),ITAG,IFLD,IPROCHLP,ILRECV,INUM,ISEND,IOFF,IRBUF,ISIZEBUF
INTEGER(KIND=JPIM) :: ISIZE
INTEGER(KIND=JPKSIZE_T) :: ILEN_BYTES,IBYTES(KFIELDS)
INTEGER(KIND=JPIB) :: IBYTESB(KFIELDS)
REAL(KIND=JPRD) :: ZTIME0,ZTIME1
CHARACTER(LEN=YIOS%NINTLEN) :: CLREC
INTEGER(KIND=JPIM), ALLOCATABLE :: IREC(:),IGRIBMSG(:)
INTEGER(KIND=JPIM) :: ISENDREQ(KFIELDS),ILENSEND(KFIELDS),ITAGSEND(KFIELDS),IPROCSEND(KFIELDS)
INTEGER(KIND=JPIM) :: IOFFSEND(KFIELDS+1)
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
!-------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:READ_RECORD',0,ZHOOK_HANDLE)

IBYTES(:)=0
YD%LEOF = .FALSE.
IF(YDR%LGRIB) THEN
  IF(.NOT.PRESENT(PREC)) CALL ABOR1('IOSTREAM_MIX:READ_RECORD - PREC MISSING')
  ILENGRB = SIZE(PREC(:,1))+8000 ! for 64-bit IEEE 2*SIZE(PREC(:,1))+8000 ?
  IF(YD%LIOPROC) THEN
    INUM=IFIND(YIOS%MYPROC,YD%IOPROCS)
    IF(YD%NUMHLP(INUM) > 1 .AND. YD%LIOUSEHELP) THEN
      IMINBUF=(1024*1024*10)/YIOS%NINTLEN ! 10 Mbytes
      ALLOCATE(IGRIBMSG(MAX(ILENGRB*YDR%NCHUNKSIZE,IMINBUF)))
      ISIZEBUF=SIZE(IGRIBMSG)
      IOFFSEND(1)=0
    ELSE
      CALL ALLO_IGRIB(YDR,KFIELDS,ILENGRB)
    ENDIF
  ENDIF
  IF(YD%LIOPROCHLP) THEN
    CALL ALLO_IGRIB(YDR,YDR%NCHUNKSIZE+1,ILENGRB)
  ENDIF
ENDIF
IF(YD%LIOPROC) THEN
  YDR%IFIELDS_IO = KFIELDS
ELSE
  YDR%IFIELDS_IO = 0
ENDIF
YDR%IFIELDS_HAVE=0
INUM=IFIND(YIOS%MYPROC,YD%IOPROCS)
ISEND=0
CALL USER_CLOCK(PELAPSED_TIME=ZTIME0)
IF(YD%LIOUSEHELP) CALL GSTATS_BARRIER(739)
CALL GSTATS(650,0)
CALL GSTATS(650,2)
CALL GSTATS(1765,0)
DO JFLD=1,YDR%IFIELDS_IO
  SELECT CASE(YD%IOTYPE)
  CASE(JPCIO)
    IF(YDR%LGRIB) THEN
      IF(YD%NUMHLP(INUM) == 1 .OR. .NOT. YD%LIOUSEHELP) THEN
        ILEN_BYTES = SIZE(NGRIBD(NGSTA(JFLD):))*YIOS%NINTLEN
        CALL IGRIB_READ_FROM_FILE(YD%IFILE_HANDLE,NGRIBD(NGSTA(JFLD):),ILEN_BYTES,IRET)
        IBYTES(JFLD)=ILEN_BYTES
        IF(IRET >= 0) THEN
          NGSTA(JFLD+1) = NGSTA(JFLD)+(IBYTES(JFLD)+YIOS%NINTLEN-1)/YIOS%NINTLEN
          YDR%IFIELDS_HAVE=YDR%IFIELDS_HAVE+1
          YDR%IHAVEFLD(JFLD)=YIOS%MYPROC
        ELSEIF(IRET < -1) THEN
           WRITE(YIOS%NULERR,*) 'IOSTREAM_MIX: READ_FROM_FILE ',IRET,ILEN_BYTES
           CALL IGRIB_ERROR_MESSAGE(IRET)
           CALL ABOR1('IOSTREAM_MIX:READ_FROM_FILE - FAIL')
        ENDIF
      ELSE
        ISEND=ISEND+1
        ILEN_BYTES = (ISIZEBUF-IOFFSEND(ISEND))*YIOS%NINTLEN
        CALL IGRIB_READ_FROM_FILE(YD%IFILE_HANDLE,IGRIBMSG(IOFFSEND(ISEND)+1:),ILEN_BYTES,IRET)
        IBYTES(JFLD)=ILEN_BYTES
        IF( IRET < 0 )THEN
          IF(IRET == -1 .AND. YDR%LRANDOM_ORDER) THEN
            ILENSEND(ISEND)=1
          ELSE
            WRITE(YIOS%NULERR,*) 'IOSTREAM_MIX:READ_FROM_FILE ',IRET,ILEN_BYTES
            CALL IGRIB_ERROR_MESSAGE(IRET)
            CALL ABOR1('IOSTREAM_MIX:READ_FROM_FILE - FAIL')
          ENDIF
        ELSE
          ILENSEND(ISEND)=(IBYTES(JFLD)+YIOS%NINTLEN-1)/YIOS%NINTLEN
        ENDIF
        IOFFSEND(ISEND+1)=IOFFSEND(ISEND)+ILENSEND(ISEND)
        IRBUF=ISIZEBUF-IOFFSEND(ISEND+1)
        ITAG=15000+JFLD
        ITAGSEND(ISEND)=ITAG
        IPROCSEND(ISEND)=IGETPROCHLP(YD,JFLD)
        CALL USER_CLOCK(PELAPSED_TIME=ZTIME1)
        YD%WALLCLOCK = YD%WALLCLOCK+(ZTIME1-ZTIME0)
        CALL GSTATS(1765,2)
        CALL GSTATS(650,3)
        IOFF=IOFFSEND(ISEND)
        CALL MPL_SEND(IGRIBMSG(IOFF+1:IOFF+ILENSEND(ISEND)),KDEST=IPROCSEND(ISEND),KTAG=ITAGSEND(ISEND),&
         & KMP_TYPE=JP_NON_BLOCKING_STANDARD,KREQUEST=ISENDREQ(ISEND),&
         & CDSTRING='IOSTREAM_MIX:READ_RECORD')
        CALL USER_CLOCK(PELAPSED_TIME=ZTIME0)
        IF(JFLD == YDR%IFIELDS_IO .OR. IRBUF < ILENGRB .OR. ILENSEND(ISEND) == 1) THEN
          CALL USER_CLOCK(PELAPSED_TIME=ZTIME1)
          YD%WALLCLOCK = YD%WALLCLOCK+(ZTIME1-ZTIME0)
          CALL MPL_WAIT(KREQUEST=ISENDREQ(1:ISEND),CDSTRING='IOSTREAM_MIX:READ_RECORD')
! Sign off helpers
          IF(ILENSEND(ISEND) == 1) THEN
            ISEND=0
            DO JJFLD=JFLD+1,YDR%IFIELDS_IO
              IF(IGETHLP(YD,JFLD) == IGETHLP(YD,JJFLD) ) EXIT
              ISEND=ISEND+1
              ITAG=15000+JJFLD
              IPROCHLP=IGETPROCHLP(YD,JJFLD)
              CALL MPL_SEND(IGRIBMSG(IOFF+1:IOFF+1),KDEST=IPROCHLP,KTAG=ITAG,&
               & KMP_TYPE=JP_NON_BLOCKING_STANDARD,KREQUEST=ISENDREQ(ISEND),&
               & CDSTRING='IOSTREAM_MIX:READ_RECORD')
            ENDDO
            CALL MPL_WAIT(KREQUEST=ISENDREQ(1:ISEND),CDSTRING='IOSTREAM_MIX:READ_RECORD')
          ENDIF
          ISEND=0
          CALL USER_CLOCK(PELAPSED_TIME=ZTIME0)
        ENDIF
        CALL GSTATS(650,2)
        CALL GSTATS(1765,3)
      ENDIF
    ELSE
      IF(PRESENT(PREC)) THEN
        ISIZE = SIZE(PREC,1)
        IBYTES(JFLD) = 1_JPKSIZE_T*ISIZE*YIOS%NREALEN
        CALL IGRIB_READ_BYTES(YD%IFILE_HANDLE,PREC(:,JFLD),IBYTES(JFLD),IRET)
        CALL ISWAP (PREC(:,JFLD), PREC(:,JFLD), YIOS%NREALEN, ISIZE)
      ELSEIF(PRESENT(PREC1)) THEN
        ISIZE = SIZE(PREC1)
        IBYTES(JFLD) = 1_JPKSIZE_T*ISIZE*YIOS%NREALEN
        CALL IGRIB_READ_BYTES(YD%IFILE_HANDLE,PREC1,IBYTES(JFLD),IRET)
        CALL ISWAP (PREC1, PREC1, YIOS%NREALEN, ISIZE)
      ELSEIF(PRESENT(KREC)) THEN
        ISIZE = SIZE(KREC,1)
        IBYTES(JFLD) = 1_JPKSIZE_T*ISIZE*YIOS%NINTLEN
        CALL IGRIB_READ_BYTES(YD%IFILE_HANDLE,KREC(:,JFLD),IBYTES(JFLD),IRET)
        CALL ISWAP (KREC (:,JFLD), KREC (:,JFLD), YIOS%NINTLEN, ISIZE)
      ELSEIF(PRESENT(KREC1)) THEN
        ISIZE = SIZE(KREC1)
        IBYTES(JFLD) = 1_JPKSIZE_T*ISIZE*YIOS%NINTLEN
        CALL IGRIB_READ_BYTES(YD%IFILE_HANDLE,KREC1,IBYTES(JFLD),IRET)
        CALL ISWAP (KREC1, KREC1, YIOS%NINTLEN, ISIZE)
      ELSEIF(PRESENT(CDREC)) THEN
        IBYTES(JFLD) = LEN(CDREC) !! NB: One-byte characters are assumed!
        ALLOCATE(IREC( (LEN(CDREC)+YIOS%NINTLEN-1)/YIOS%NINTLEN ))
        CALL IGRIB_READ_BYTES(YD%IFILE_HANDLE,IREC,IBYTES(JFLD),IRET)
        I=1
        DO J=1,SIZE(IREC)
          WRITE(CLREC,'(A)') IREC(J)
          CDREC(I:MIN(LEN(CDREC),I+YIOS%NINTLEN-1))=CLREC
          I=I+YIOS%NINTLEN
        ENDDO
        DEALLOCATE(IREC)
      ENDIF
    ENDIF
  END SELECT
  IF( IRET < 0 )THEN
    IF(IRET == -1 .AND. YDR%LRANDOM_ORDER) THEN
      YD%LEOF = .TRUE.
      YDR%IFIELDS_IO = JFLD-1
      EXIT
    ELSE
      WRITE(YIOS%NULERR,*) 'IOSTREAM_MIX:READ_RECORD ',IRET,KFIELDS,JFLD,SIZE(PREC,1)
      CALL IGRIB_ERROR_MESSAGE(IRET)
      CALL ABOR1('IOSTREAM_MIX:READ_RECORD - FAIL')
    ENDIF
  ENDIF
ENDDO
CALL GSTATS(1765,1) 
CALL GSTATS(650,3)
CALL GSTATS(650,1)
CALL USER_CLOCK(PELAPSED_TIME=ZTIME1)
YD%WALLCLOCK = YD%WALLCLOCK+(ZTIME1-ZTIME0)
TOTAL_WALLCLOCK = TOTAL_WALLCLOCK+(ZTIME1-ZTIME0)
YD%NRECS = YD%NRECS+YDR%IFIELDS_IO
IBYTESB(1:YDR%IFIELDS_IO)=IBYTES(1:YDR%IFIELDS_IO)
YD%NBYTES = YD%NBYTES+SUM(IBYTESB(1:YDR%IFIELDS_IO))

IF(YDR%LGRIB .AND. KFIELDS >0) THEN
  IF( YD%LIOUSEHELP ) THEN
    IF( YD%LIOPROCHLP) THEN
      CALL GSTATS(650,0)
      YDR%IFIELDS_HAVE=0
      IFLD=0
      RLOOP:DO JFLD=1,KFIELDS
        ITAG=15000+JFLD
        IPROCHLP=IGETPROCHLP(YD,JFLD)
        IF(IPROCHLP == YIOS%MYPROC) THEN
          IFLD=IFLD+1
          CALL MPL_RECV(NGRIBD(NGSTA(IFLD):),KSOURCE=YD%MIOPROCHLP,KTAG=ITAG,&
           & KOUNT=ILRECV,CDSTRING='IOSTREAM_MIX:READ_RECORD')
          IF(ILRECV == 1) THEN
            YD%LEOF = .TRUE.
            EXIT RLOOP
          ELSE
            NGSTA(IFLD+1) = NGSTA(IFLD)+ILRECV
            YDR%IFIELDS_HAVE=YDR%IFIELDS_HAVE+1
            CALL GSTATS(650,2)
            CALL GRIB_DECODE(YDR,PREC)
            CALL GSTATS(650,3)
          ENDIF
        ENDIF
      ENDDO RLOOP
      CALL GSTATS(650,1)
    ENDIF
  ENDIF

  CALL GRIB_DECODE(YDR,PREC)

  IF(YD%LIOUSEHELP) THEN
    CALL GSTATS(648,0)
    IF(YD%LIOPROC) THEN
      INUM=IFIND(YIOS%MYPROC,YD%IOPROCS)
      IF(YD%NUMHLP(INUM) > 1) THEN
        DO JFLD=1,YDR%IFIELDS_IO
          ITAG=16000+JFLD
          IPROCHLP=IGETPROCHLP(YD,JFLD)
          CALL MPL_RECV(IMSG,KSOURCE=IPROCHLP,KTAG=ITAG,&
           & CDSTRING='IOSTREAM_MIX:READ_RECORD')
          YDR%IGRBCODES(JFLD)=IMSG(1)
          YDR%ILEVS(JFLD) = IMSG(2)
          YDR%IHAVEFLD(JFLD) = IPROCHLP
        ENDDO
      ENDIF
    ELSEIF(YD%LIOPROCHLP) THEN
      IFLD=0
      MLOOP:DO JFLD=1,KFIELDS
        ITAG=16000+JFLD
        IPROCHLP=IGETPROCHLP(YD,JFLD)
        IF(IPROCHLP == YIOS%MYPROC) THEN
          IFLD=IFLD+1
          IF(IFLD > YDR%IFIELDS_HAVE) EXIT MLOOP
          IMSG(1)=YDR%IGRBCODES(IFLD)
          IMSG(2)=YDR%ILEVS(IFLD)
          CALL MPL_SEND(IMSG,KDEST=YD%MIOPROCHLP,KTAG=ITAG,&
           & CDSTRING='IOSTREAM_MIX:READ_RECORD')
        ENDIF
      ENDDO MLOOP
    ENDIF
    CALL GSTATS(648,1)
  ENDIF
ENDIF
IF(YD%LIOUSEHELP) CALL GSTATS_BARRIER2(739)
IF(ALLOCATED(IGRIBMSG)) DEALLOCATE(IGRIBMSG)

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:READ_RECORD',1,ZHOOK_HANDLE)

END SUBROUTINE READ_RECORD

