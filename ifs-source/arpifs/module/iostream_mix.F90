! PR3  - real rank 3 array
! PR4  - real rank 4 array
! KR1  - integer rank 1 array
! KR2  - integer rank 2 array
! KR3  - integer rank 3 array
! KR4  - integer rank 4 array

!-------------------------------------------------------------------------
IMPLICIT NONE
TYPE(TYPE_IOREQUEST),INTENT(IN) :: YDR
LOGICAL,INTENT(OUT) :: LDPR(4)
INTEGER(KIND=JPIM),INTENT(OUT) :: KPRD(4,4)
LOGICAL,INTENT(OUT) :: LDKR(4)
INTEGER(KIND=JPIM),INTENT(OUT) :: KKRD(4,4)
REAL(KIND=JPRB),OPTIONAL,INTENT(IN) :: PR1(:)
REAL(KIND=JPRB),OPTIONAL,INTENT(IN) :: PR2(:,:)
REAL(KIND=JPRB),OPTIONAL,INTENT(IN) :: PR3(:,:,:)
REAL(KIND=JPRB),OPTIONAL,INTENT(IN) :: PR4(:,:,:,:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KR1(:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KR2(:,:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KR3(:,:,:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KR4(:,:,:,:)

INTEGER(KIND=JPIM) :: IF2,IF3,ILV3,IA2,IA3
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
!-------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:GET_PUT_ARGS',0,ZHOOK_HANDLE)

LDPR(:) = .FALSE.
KPRD(:,:) = 0
IF(PRESENT(PR1)) THEN
  LDPR(1)   = .TRUE.
  KPRD(1,1) = UBOUND(PR1,1)
ENDIF  
IF(PRESENT(PR2)) THEN
  LDPR(2)   = .TRUE.
  KPRD(2,1) = UBOUND(PR2,1)
  KPRD(2,2) = UBOUND(PR2,2)
ENDIF  
IF(PRESENT(PR3)) THEN
  LDPR(3)   = .TRUE.
  KPRD(3,1) = UBOUND(PR3,1)
  KPRD(3,2) = UBOUND(PR3,2)
  KPRD(3,3) = UBOUND(PR3,3)
ENDIF  
IF(PRESENT(PR4)) THEN
  LDPR(4)   = .TRUE.
  KPRD(4,1) = UBOUND(PR4,1)
  KPRD(4,2) = UBOUND(PR4,2)
  KPRD(4,3) = UBOUND(PR4,3)
  KPRD(4,4) = UBOUND(PR4,4)
ENDIF
LDKR(:) = .FALSE.
KKRD(:,:) = 0
IF(PRESENT(KR1)) THEN
  LDKR(1)   = .TRUE.
  KKRD(1,1) = UBOUND(KR1,1)
ENDIF  
IF(PRESENT(KR2)) THEN
  LDKR(2)   = .TRUE.
  KKRD(2,1) = UBOUND(KR2,1)
  KKRD(2,2) = UBOUND(KR2,2)
ENDIF  
IF(PRESENT(KR3)) THEN
  LDKR(3)   = .TRUE.
  KKRD(3,1) = UBOUND(KR3,1)
  KKRD(3,2) = UBOUND(KR3,2)
  KKRD(3,3) = UBOUND(KR3,3)
ENDIF  
IF(PRESENT(KR4)) THEN
  LDKR(4)   = .TRUE.
  KKRD(4,1) = UBOUND(KR4,1)
  KKRD(4,2) = UBOUND(KR4,2)
  KKRD(4,3) = UBOUND(KR4,3)
  KKRD(4,4) = UBOUND(KR4,4)
ENDIF
  
!Checks on arguments

IF(YDR%IDATA_TYPE == JPSPEC .OR. YDR%IDATA_TYPE == JPGP) THEN
  IF(YDR%IDATA_TYPE == JPSPEC ) THEN
    IA2 = 2
    IF2 = 2
    IA3 = 3
    IF3 = 3
    ILV3 = 1
  ELSE
    IA2 = 3
    IF2 = 2
    IF3 = 3
    IA3 = 4
    ILV3 = 2
  ENDIF
  IF(YDR%IDATA_TYPE == JPGP) THEN
    IF(LDPR(IA2)) THEN
      IF(YDR%YMDL_2D%NUMFLDS /= KPRD(IA2,IF2)) THEN
        WRITE(YIOS%NULERR,*) 'YDR%YMDL_2D%NUMFLDS  KPRD(IA2,IF2) ',&
         & YDR%YMDL_2D%NUMFLDS,KPRD(IA2,IF2),IA2,IF2
        CALL ABOR1('IOSTREAM_MIX:IO_GET_PUT_ARGS - YDR%YMDL_2D%NUMFLDS /= KPRD')
      ENDIF
    ELSE
      IF(YDR%YMDL_2D%NUMFLDS /= 0)&
       & CALL ABOR1('IOSTREAM_MIX:IO_GET_PUT_ARGS - 2D PR MISSING')
    ENDIF
  ENDIF
  IF(LDPR(IA3)) THEN
    IF(YDR%YMDL_3D%NUMFLDS /= KPRD(IA3,IF3)) THEN
      WRITE(YIOS%NULERR,*) 'YDR%YMDL_3D%NUMFLDS,KPRD(IA3,IF3) ',&
       & YDR%YMDL_3D%NUMFLDS,KPRD(IA3,IF3),IA3,IF3
      CALL ABOR1('IOSTREAM_MIX:IO_GET_PUT_ARGS - YDR%YMDL_3D%NUMFLDS /= IPR')
    ENDIF
  ENDIF
ENDIF

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:GET_PUT_ARGS',1,ZHOOK_HANDLE)
END SUBROUTINE GET_PUT_ARGS
