SUBROUTINE GET_PUT_ARGS(YDR,LDPR,KPRD,LDKR,KKRD,&
 &PR1,PR2,PR3,PR4,KR1,KR2,KR3,KR4)

!**** *GET_PUT_ARGS*  - Check args for IO_PUT or IO_GET

