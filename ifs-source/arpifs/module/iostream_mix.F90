SUBROUTINE IO_PUT(YD,YDR,PR1,PR2,PR3,PR4,KR1,KR2,KR3,KR4,CDR,&
                & KFLDPROC2D,KLEVPROC3D)

!**** *IO_PUT*  - Output(write) data

! Purpose - To write out data .

! Explicit arguments :
! --------------------
! YD   - IO stream structure
! YDR  - IO request structure
! PR1  - real rank 1 array
! PR2  - real rank 2 array
! PR3  - real rank 3 array
! PR4  - real rank 4 array
! KR1  - integer rank 1 array
! KR2  - integer rank 2 array
! KR3  - integer rank 3 array
! KR4  - integer rank 4 array
! CDR  - character string
!-------------------------------------------------------------------------
IMPLICIT NONE
TYPE(TYPE_IOSTREAM) ,INTENT(INOUT)     :: YD
TYPE(TYPE_IOREQUEST),INTENT(INOUT)     :: YDR
REAL(KIND=JPRB)   ,OPTIONAL,INTENT(IN) :: PR1(:)
REAL(KIND=JPRB)   ,OPTIONAL,INTENT(IN) :: PR2(:,:)
REAL(KIND=JPRB)   ,OPTIONAL,INTENT(IN) :: PR3(:,:,:)
REAL(KIND=JPRB)   ,OPTIONAL,INTENT(IN) :: PR4(:,:,:,:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KR1(:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KR2(:,:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KR3(:,:,:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KR4(:,:,:,:)
CHARACTER(LEN=*)  ,OPTIONAL,INTENT(IN) :: CDR
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KFLDPROC2D(:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KLEVPROC3D(:)

INTEGER(KIND=JPIM) :: IPRD(4,4),IKRD(4,4),ISIZE
LOGICAL :: LLPR(4),LLKR(4)
INTEGER(KIND=JPIM),ALLOCATABLE :: IREC1(:)
REAL(KIND=JPRB)   ,ALLOCATABLE :: ZREC1(:)
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
REAL(KIND=JPRD)    :: ZTIME0,ZTIME1

!-------------------------------------------------------------------------
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:IO_PUT',0,ZHOOK_HANDLE)
CALL USER_CLOCK(PELAPSED_TIME=ZTIME0)

IF(YD%NUMIOPROCS < 1 .OR. YD%NUMIOPROCS > YIOS%NPROC) THEN
  WRITE(YIOS%NULERR,*) 'IOSTREAM_MIX-IO_PUT YD%NUMIOPROCS=',YD%NUMIOPROCS
  CALL ABOR1('IOSTREAM_MIX:IO_PUT - IOSTREAM_MIX NOT SET UP')
ENDIF
! Check out optional arguments
CALL GET_PUT_ARGS(YDR,LLPR,IPRD,LLKR,IKRD,&
 & PR1,PR2,PR3,PR4,KR1,KR2,KR3,KR4)

IF(YDR%LGRIB) THEN
  IF(YDR%LGEOPRESET) THEN
    IF(YDR%IGRIB_HANDLE < 0 )&
     & CALL ABOR1('IOSTREAM_MIX:IO_PUT - GRIB HANDLE NOT DEFINED')
  ELSE
    IF(.NOT. YIOS%LIFS_INTERNAL) CALL ABOR1('IOSTREAM_MIX:IO_PUT - GRIB HANDLE NOT DEFINED, NOT IFS')
    YDR%IGRIB_HANDLE=-1
    IF(YDR%IDATA_TYPE == JPSPEC) THEN
      IF(YDR%CLEVTYPE == 'ML') THEN
        IF(YGBH%NGRIB_HANDLE_SH_ML < 0) CALL ABOR1('IOSTREAM_MIX:IO_PUT - YGBH%NGRIB_HANDLE_SH_ML NOT SET UP')
        CALL IGRIB_CLONE(YGBH%NGRIB_HANDLE_SH_ML,YDR%IGRIB_HANDLE)
      ELSE
        IF(YGBH%NGRIB_HANDLE_SH < 0) CALL ABOR1('IOSTREAM_MIX:IO_PUT - YGBH%NGRIB_HANDLE_SH NOT SET UP')
        CALL IGRIB_CLONE(YGBH%NGRIB_HANDLE_SH,YDR%IGRIB_HANDLE)
      ENDIF
    ELSEIF(YDR%IDATA_TYPE == JPGP) THEN
      IF(YDR%CLEVTYPE == 'ML') THEN
        IF(YGBH%NGRIB_HANDLE_GG_ML < 0) CALL ABOR1('IOSTREAM_MIX:IO_PUT - YGBH%NGRIB_HANDLE_GG_ML NOT SET UP')
        CALL IGRIB_CLONE(YGBH%NGRIB_HANDLE_GG_ML,YDR%IGRIB_HANDLE)
      ELSE
        IF(YGBH%NGRIB_HANDLE_GG < 0) CALL ABOR1('IOSTREAM_MIX:IO_PUT - YGBH%NGRIB_HANDLE_GG NOT SET UP')
        CALL IGRIB_CLONE(YGBH%NGRIB_HANDLE_GG,YDR%IGRIB_HANDLE)
        IF(YGBH%NGRIB_HANDLE_GG2 < 0) CALL ABOR1('IOSTREAM_MIX:IO_PUT - YGBH%NGRIB_HANDLE_GG2 NOT SET UP')
        CALL IGRIB_CLONE(YGBH%NGRIB_HANDLE_GG2,YDR%IGRIB_HANDLE2)
      ENDIF
    ENDIF
  ENDIF
ENDIF

SELECT CASE(YDR%IDATA_TYPE)
! Write raw (unstructured) data
CASE(JPRAW)
  IF(YD%LIOPROC) THEN
    IF(LLPR(1)) THEN
      CALL WRITE_RECORD(YD,YDR,1,PREC1=PR1)
    ENDIF
    IF(LLPR(2)) THEN
      ISIZE = SIZE(PR2)
      ALLOCATE(ZREC1(ISIZE))
      ZREC1(:) = RESHAPE(PR2,(/ISIZE/))
      CALL WRITE_RECORD(YD,YDR,1,PREC1=ZREC1)
      DEALLOCATE(ZREC1)
    ENDIF
    IF(LLPR(3)) THEN
      ISIZE = SIZE(PR3)
      ALLOCATE(ZREC1(ISIZE))
      ZREC1(:) = RESHAPE(PR3,(/ISIZE/))
      CALL WRITE_RECORD(YD,YDR,1,PREC1=ZREC1)
      DEALLOCATE(ZREC1)
    ENDIF
    IF(LLPR(4)) THEN
      ISIZE = SIZE(PR4)
      ALLOCATE(ZREC1(ISIZE))
      ZREC1(:) = RESHAPE(PR4,(/ISIZE/))
      CALL WRITE_RECORD(YD,YDR,1,PREC1=ZREC1)
      DEALLOCATE(ZREC1)
    ENDIF
    IF(LLKR(1)) THEN
      CALL WRITE_RECORD(YD,YDR,1,KREC1=KR1)
    ENDIF
    IF(LLKR(2)) THEN
      ISIZE = SIZE(KR2)
      ALLOCATE(IREC1(ISIZE))
      IREC1(:) = RESHAPE(KR2,(/ISIZE/))
      CALL WRITE_RECORD(YD,YDR,1,KREC1=IREC1)
      DEALLOCATE(IREC1)
    ENDIF
    IF(LLKR(3)) THEN
      ISIZE = SIZE(KR3)
      ALLOCATE(IREC1(ISIZE))
      IREC1(:) = RESHAPE(KR3,(/ISIZE/))
      CALL WRITE_RECORD(YD,YDR,1,KREC1=IREC1)
      DEALLOCATE(IREC1)
    ENDIF
    IF(LLKR(4)) THEN
      ISIZE = SIZE(KR4)
      ALLOCATE(IREC1(ISIZE))
      IREC1(:) = RESHAPE(KR4,(/ISIZE/))
      CALL WRITE_RECORD(YD,YDR,1,KREC1=IREC1)
      DEALLOCATE(IREC1)
    ENDIF
    IF (PRESENT(CDR)) THEN
      CALL WRITE_RECORD(YD,YDR,1,CDREC=CDR)
    ENDIF
  ENDIF
CASE(JPSPEC)
!Write out spectral fields
  CALL SPEC_OUT(YD,YDR,PR2,PR3,YDR%YMDL_2D%NUMFLDS,YDR%YMDL_3D%NLEVS,IPRD(3,3))
CASE(JPGP)
!Write out grid-point fields
  CALL GRID_OUT(YD,YDR,PR3,PR4,IPRD(3,2),IPRD(4,2),IPRD(4,3),KFLDPROC2D)
END SELECT

CALL IGRIB_RELEASE(YDR%IGRIB_HANDLE)
IF(YDR%IDATA_TYPE == JPGP .AND. YDR%CLEVTYPE /= 'ML') CALL IGRIB_RELEASE(YDR%IGRIB_HANDLE2)

IF(YIOS%NDGLG > YIOS%NSMAX+1) THEN
! special, makes cubic grid work, stops mixing gg/sh fields of potentially different sizes in fdb!
   CALL FLUSH_IOSTREAM(YD)
ENDIF

CALL USER_CLOCK(PELAPSED_TIME=ZTIME1)
YD%TOTWALL = YD%TOTWALL+(ZTIME1-ZTIME0)
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:IO_PUT',1,ZHOOK_HANDLE)

END SUBROUTINE IO_PUT
!=============================================================
SUBROUTINE IO_INQUIRE(YD,YDR,LDREAD,&
 & KPARAMS,KNLEVS,KNVARS,KSMAX,KNVALS,KLATS,KLONS,PLATS,&
 & KDATE,KTIME,KSTEP,KFLEV,PVALH,PVBH,PA,PB,KPTS,KLEVS,&
 & KSVCOMP,KRITZ1,KRITZ2,KNUMB,KGRIB_HANDLE)

!**** *IO_INQUIRE*  - Find out contents of file

! Purpose - To return certain characteristics of data in a (GRIB)file

! Explicit arguments :
! --------------------
! YD      - IO stream structure
! YDR     - IO request structure
! LDREAD  - read in record (default true)
! KPARAMS - GRIB parameter numbers
! KSMAX   - spectral truncation
! KNVALS  - numberOfValues
!! CDREPRT  - representation type (spectral, gridpoint)
! KLATS   - number of latitude rows
! KLONS   - number of points on each latitude row
! PLATS   - latitude of each row [radians]
! KDATE   - date of data (yyyymmdd)
! KTIME   - time of data (seconds)
! KSTEP   - current fc time (seconds)
! PA, PB  - pressure on level is given by p = PA + ps*PB where ps is
!           surface pressure
! KPTS    - total number of points 
! KLEVS   - generalized "levels"
! KGRIB_HANDLE   - handle to GRIB_API field
!-------------------------------------------------------------------------

IMPLICIT NONE
TYPE(TYPE_IOSTREAM),INTENT(INOUT)  :: YD
TYPE(TYPE_IOREQUEST),INTENT(INOUT) :: YDR
LOGICAL,OPTIONAL,INTENT(IN) :: LDREAD
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KPARAMS(:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KNLEVS(:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KNVARS
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KSMAX
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KNVALS
!CHARACTER(LEN=*)  ,OPTIONAL,INTENT(OUT) :: CDREPRT
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KLATS
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KLONS(:)
REAL   (KIND=JPRB),OPTIONAL,INTENT(OUT) :: PLATS(:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KDATE
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KTIME
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KSTEP
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KFLEV
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KSVCOMP
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KRITZ1
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KRITZ2
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KNUMB
REAL   (KIND=JPRB),OPTIONAL,INTENT(OUT) :: PVALH(0:)
REAL   (KIND=JPRB),OPTIONAL,INTENT(OUT) :: PVBH (0:)
REAL   (KIND=JPRB),OPTIONAL,INTENT(OUT) :: PA(:,:)
REAL   (KIND=JPRB),OPTIONAL,INTENT(OUT) :: PB(:,:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KPTS
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KLEVS(:,:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(OUT) :: KGRIB_HANDLE

INTEGER(KIND=JPIM) :: IPARAMS,IFF,IMAXPARAMS,IMAXNLEVS,IPARAM
INTEGER(KIND=JPIM) :: JVAR, IRET, JLEV,ILEV,ITAG,ILENINT
INTEGER(KIND=JPKSIZE_T) :: IGRIB_LEN
INTEGER(KIND=JPIM) :: IBUF(12)
REAL   (KIND=JPRB) :: ZA,ZB
INTEGER(KIND=JPIM),ALLOCATABLE :: INLEVS(:)
INTEGER(KIND=JPIM),ALLOCATABLE :: IRANK(:)
INTEGER(KIND=JPIM),ALLOCATABLE :: INDEX(:)
INTEGER(KIND=JPIM),ALLOCATABLE :: ILEVS(:)
!REAL   (KIND=JPRB),ALLOCATABLE :: ZREC(:,:)
REAL   (KIND=JPRB),ALLOCATABLE :: ZPRES(:)
INTEGER(KIND=JPIM),ALLOCATABLE :: IGRIBMSG(:)
LOGICAL :: LLFIRST,LLREAD
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
REAL(KIND=JPRD)    :: ZTIME0,ZTIME1,ZTIME2,ZTIME3

!-------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:IO_INQUIRE',0,ZHOOK_HANDLE)
CALL USER_CLOCK(PELAPSED_TIME=ZTIME0)

IF(YD%NUMIOPROCS < 1 .OR. YD%NUMIOPROCS > YIOS%NPROC) THEN
  WRITE(YIOS%NULERR,*) 'IOSTREAM_MIX-IO_INQUIRE YD%NUMIOPROCS=',YD%NUMIOPROCS
  CALL ABOR1('IOSTREAM_MIX:IO_INQUIRE - IOSTREAM_MIX NOT SET UP')
ENDIF
IF(.NOT. YDR%LGRIB) CALL ABOR1('IOSTREAM_MIX:IO_INQUIRE - REQUIRES GRIB FILE')
YDR%LINQUIRE = .TRUE.
LLREAD = .TRUE.
IF(PRESENT(LDREAD)) LLREAD = LDREAD

IF(YD%LIOPROC) THEN
  
  IPARAMS=0
  IF(PRESENT(KPARAMS)) THEN
    IMAXPARAMS=SIZE(KPARAMS)
  ELSE
    IMAXPARAMS=0
  ENDIF
  IF(PRESENT(KNLEVS)) THEN
    IF(SIZE(KNLEVS) /= IMAXPARAMS)&
     &CALL ABOR1('IOSTREAM_MIX:IO_INQUIRE - SIZE(KNLEVS) /= IMAXPARAMS')
  ENDIF
  IF(PRESENT(PA)) THEN
    IF(.NOT.PRESENT(PB))&
     & CALL ABOR1('IOSTREAM_MIX:IO_INQUIRE - PA BUT NOT PB PRESENT') 
    IMAXNLEVS = SIZE(PA(:,1))
    IF(SIZE(PB(:,1)) /= IMAXNLEVS)&
     & CALL ABOR1('IOSTREAM_MIX:IO_INQUIRE - SIZE(PB(:,1)) /= IMAXNLEVS')
    IF(SIZE(PA(1,:)) /= IMAXPARAMS)&
     & CALL ABOR1('IOSTREAM_MIX:IO_INQUIRE - SIZE(PA(1,:)) /= IMAXPARAMS')
    IF(SIZE(PB(1,:)) /= IMAXPARAMS)&
     & CALL ABOR1('IOSTREAM_MIX:IO_INQUIRE - SIZE(PB(1,:)) /= IMAXPARAMS')
  ENDIF
  IF(PRESENT(KLEVS)) THEN
    IF(SIZE(KLEVS(1,:)) /= IMAXPARAMS)&
     & CALL ABOR1('IOSTREAM_MIX:IO_INQUIRE - SIZE(KLEVS(1,:)) /= IMAXPARAMS')
  ENDIF
   
  IF(PRESENT(PA) .OR. PRESENT(KLEVS)) THEN
    ALLOCATE(INLEVS(IMAXPARAMS))
    INLEVS(:) = 0
  ENDIF

  LLFIRST = .TRUE.

!  ALLOCATE(ZREC(YIOS%IMAXGRID,1))
  ALLOCATE(YDR%IGRBCODES(1))
  ALLOCATE(YDR%IHAVEFLD(1))
  ALLOCATE(YDR%ILEVS(1))

  DO 
    IF(LLREAD) THEN
!      CALL READ_RECORD(YD,YDR,1,PREC=ZREC)

      CALL USER_CLOCK(PELAPSED_TIME=ZTIME2)
      CALL IGRIB_NEW_FROM_FILE(YD%IFILE_HANDLE,YDR%IGRIB_HANDLE,IRET)
      CALL USER_CLOCK(PELAPSED_TIME=ZTIME3)
      YD%WALLCLOCK = YD%WALLCLOCK+(ZTIME3-ZTIME2)
      TOTAL_WALLCLOCK = TOTAL_WALLCLOCK+(ZTIME3-ZTIME2)

      IF(IRET == JPGRIB_END_OF_FILE) THEN
        YD%LEOF = .TRUE.
      ELSEIF(IRET /= JPGRIB_SUCCESS) THEN
        CALL IGRIB_ERROR_MESSAGE(IRET)
        CALL ABOR1('IOSTREAM_MIX:IO_INQUIRE READ FAILED')
      ENDIF
      IF (YD%LEOF) EXIT
    ENDIF
    IF(LLFIRST) THEN
      CALL INQUIRE_GRIB(YDR%IGRIB_HANDLE,KSMAX=KSMAX,&
       & KNVALS=KNVALS,&
       & KLATS=KLATS,KLONS=KLONS,PLATS=PLATS,&
       & KDATE=KDATE,KTIME=KTIME,KSTEP=KSTEP,&
       & KFLEV=KFLEV,PVALH=PVALH,PVBH=PVBH,KPTS=KPTS,&
       & KSVCOMP=KSVCOMP,KRITZ1=KRITZ1,KRITZ2=KRITZ2,KNUMB=KNUMB)
      IF(PRESENT(KGRIB_HANDLE)) THEN
        KGRIB_HANDLE = -99
        CALL IGRIB_CLONE(YDR%IGRIB_HANDLE,KGRIB_HANDLE)
      ENDIF
      LLFIRST = .FALSE.
    ENDIF
    IF(PRESENT(KPARAMS)) THEN
      CALL INQUIRE_GRIB(YDR%IGRIB_HANDLE,KPARAM=IPARAM)
      IFF = IFIND(IPARAM,KPARAMS(1:IPARAMS))
      IF(IFF == 0) THEN
        IPARAMS = IPARAMS+1
        IFF = IPARAMS
        IF(IPARAMS > IMAXPARAMS) CALL ABOR1('IOSTREAM_MIX:IO_INQUIRE - KPARAMS TOO SMALL')
        KPARAMS(IPARAMS) = IPARAM
      ENDIF
    ELSE
      EXIT
    ENDIF

    IF(PRESENT(PA)) THEN
      CALL INQUIRE_GRIB(YDR%IGRIB_HANDLE,PA=ZA,PB=ZB)
      IF(IFINDR(ZA,PA(1:INLEVS(IFF),IFF)) == 0 .OR.&
       & IFINDR(ZB,PB(1:INLEVS(IFF),IFF)) == 0) THEN
        INLEVS(IFF) = INLEVS(IFF)+1
        IF(INLEVS(IFF) > IMAXNLEVS)&
         & CALL ABOR1('IOSTREAM_MIX:IO_INQUIRE - INLEVS(IFF) > IMAXNLEVS')
        PA(INLEVS(IFF),IFF) = ZA
        PB(INLEVS(IFF),IFF) = ZB
      ENDIF
    ENDIF
    IF(PRESENT(KLEVS)) THEN
      CALL INQUIRE_GRIB(YDR%IGRIB_HANDLE,KLEV=ILEV)
      IF(IFIND(ILEV,KLEVS(1:INLEVS(IFF),IFF)) == 0) THEN
        IF(.NOT.PRESENT(PA)) INLEVS(IFF) = INLEVS(IFF)+1
        IF(INLEVS(IFF) > SIZE(KLEVS(:,1)))&
         & CALL ABOR1('IOSTREAM_MIX:IO_INQUIRE - INLEVS(IFF) > SIZE(KLEVS(:,1)')
        KLEVS(INLEVS(IFF),IFF) = ILEV
      ENDIF
    ENDIF
  ENDDO


  IF(PRESENT(KNLEVS)) THEN
    KNLEVS(1:IPARAMS) = INLEVS(1:IPARAMS)
  ENDIF
  IF(PRESENT(KNVARS)) KNVARS = IPARAMS

  IF(PRESENT(PA)) THEN
!        SORT PA AND PB IN ORDER OF INCREASING PRESSURE

    DO JVAR=1,IPARAMS
      ALLOCATE (ZPRES(INLEVS(JVAR)),IRANK(INLEVS(JVAR)),INDEX(INLEVS(JVAR)))
      ZPRES(1:INLEVS(JVAR)) = PA(1:INLEVS(JVAR),JVAR)+YIOS%SIPR*PB(1:INLEVS(JVAR),JVAR)
      
      CALL KEYSORT(IRET,ZPRES,INLEVS(JVAR),INDEX=INDEX,INIT=.TRUE.)
      CALL GET_RANK(INDEX, IRANK)
!      DO JLEV=1,INLEVS(JVAR)
!        IRANK(INDEX(JLEV)) = JLEV
!      ENDDO
      
      DO JLEV=1,INLEVS(JVAR)
        ZPRES(JLEV) = PA(IRANK(JLEV),JVAR)
      ENDDO
      PA(1:INLEVS(JVAR),JVAR) = ZPRES(:)
      
      DO JLEV=1,INLEVS(JVAR)
        ZPRES(JLEV) = PB(IRANK(JLEV),JVAR)
      ENDDO
      PB(1:INLEVS(JVAR),JVAR) = ZPRES(:)

      IF(PRESENT(KLEVS)) THEN
        ALLOCATE(ILEVS(INLEVS(JVAR)))
        DO JLEV=1,INLEVS(JVAR)
          ILEVS(JLEV) = KLEVS(IRANK(JLEV),JVAR)
        ENDDO
        KLEVS(1:INLEVS(JVAR),JVAR) = ILEVS(:)
        DEALLOCATE(ILEVS)
      ENDIF
      DEALLOCATE (ZPRES,IRANK,INDEX)
    ENDDO
    DEALLOCATE (INLEVS)
  ENDIF
!  IF(ALLOCATED(ZREC)) DEALLOCATE(ZREC)
  DEALLOCATE(YDR%IGRBCODES)
  DEALLOCATE(YDR%IHAVEFLD)
  DEALLOCATE(YDR%ILEVS)
ENDIF
IF(YDR%LDISTRIBUTE) THEN
  CALL GSTATS_BARRIER(707)
  CALL GSTATS(628,0)
  ITAG = 1000
  IF(PRESENT(KPARAMS)) CALL MPL_BROADCAST(KPARAMS,ITAG,YD%IOMASTER,&
   & CDSTRING='IOSTREAM_MIX:IO_INQUIRE')
  IF(PRESENT(KNLEVS))  CALL MPL_BROADCAST(KNLEVS ,ITAG,YD%IOMASTER,&
   & CDSTRING='IOSTREAM_MIX:IO_INQUIRE')
  IF(PRESENT(KLONS))   CALL MPL_BROADCAST(KLONS  ,ITAG,YD%IOMASTER,&
   & CDSTRING='IOSTREAM_MIX:IO_INQUIRE')
  IF(PRESENT(KLEVS))   CALL MPL_BROADCAST(KLEVS  ,ITAG,YD%IOMASTER,&
   & CDSTRING='IOSTREAM_MIX:IO_INQUIRE')
  IF(PRESENT(PLATS))   CALL MPL_BROADCAST(PLATS  ,ITAG,YD%IOMASTER,&
   & CDSTRING='IOSTREAM_MIX:IO_INQUIRE')
  IF(PRESENT(PVALH))   CALL MPL_BROADCAST(PVALH  ,ITAG,YD%IOMASTER,&
   & CDSTRING='IOSTREAM_MIX:IO_INQUIRE')
  IF(PRESENT(PVBH ))   CALL MPL_BROADCAST(PVBH   ,ITAG,YD%IOMASTER,&
   & CDSTRING='IOSTREAM_MIX:IO_INQUIRE')
  IF(PRESENT(PA))      CALL MPL_BROADCAST(PA     ,ITAG,YD%IOMASTER,&
   & CDSTRING='IOSTREAM_MIX:IO_INQUIRE')
  IF(PRESENT(PB))      CALL MPL_BROADCAST(PB     ,ITAG,YD%IOMASTER,&
   & CDSTRING='IOSTREAM_MIX:IO_INQUIRE')
  IF(PRESENT(KNVARS))  IBUF( 1) = KNVARS
  IF(PRESENT(KSMAX))   IBUF( 2) = KSMAX
  IF(PRESENT(KLATS))   IBUF( 3) = KLATS
  IF(PRESENT(KDATE))   IBUF( 4) = KDATE
  IF(PRESENT(KTIME))   IBUF( 5) = KTIME
  IF(PRESENT(KFLEV))   IBUF( 6) = KFLEV
  IF(PRESENT(KSTEP))   IBUF( 7) = KSTEP
  IF(PRESENT(KSVCOMP)) IBUF( 8) = KSVCOMP
  IF(PRESENT(KNUMB))   IBUF( 9) = KNUMB
  IF(PRESENT(KRITZ1))  IBUF(10) = KRITZ1
  IF(PRESENT(KRITZ2))  IBUF(11) = KRITZ2
  IF(PRESENT(KNVALS))  IBUF(12) = KNVALS
  CALL MPL_BROADCAST(IBUF,ITAG,YD%IOMASTER,CDSTRING='IOSTREAM_MIX:IO_INQUIRE')
  IF(PRESENT(KNVARS))  KNVARS  = IBUF( 1)
  IF(PRESENT(KSMAX))   KSMAX   = IBUF( 2)
  IF(PRESENT(KLATS))   KLATS   = IBUF( 3)
  IF(PRESENT(KDATE))   KDATE   = IBUF( 4)
  IF(PRESENT(KTIME))   KTIME   = IBUF( 5)
  IF(PRESENT(KFLEV))   KFLEV   = IBUF( 6)
  IF(PRESENT(KSTEP))   KSTEP   = IBUF( 7)
  IF(PRESENT(KSVCOMP)) KSVCOMP = IBUF( 8)
  IF(PRESENT(KNUMB))   KNUMB   = IBUF( 9)
  IF(PRESENT(KRITZ1))  KRITZ1  = IBUF(10)
  IF(PRESENT(KRITZ2))  KRITZ2 =  IBUF(11)
  IF(PRESENT(KNVALS))  KNVALS =  IBUF(12)
  CALL GSTATS(628,1)
  CALL GSTATS_BARRIER2(707)
  IF(PRESENT(KGRIB_HANDLE)) THEN
    IF(YIOS%MYPROC == YD%IOMASTER) THEN
      CALL IGRIB_GET_MESSAGE_SIZE(KGRIB_HANDLE,IGRIB_LEN)
      ILENINT = (IGRIB_LEN+YIOS%NINTLEN-1)/YIOS%NINTLEN
      ALLOCATE(IGRIBMSG(ILENINT))
      CALL IGRIB_GET_MESSAGE(KGRIB_HANDLE,IGRIBMSG)
    ENDIF
    CALL MPL_BROADCAST(ILENINT,ITAG,YD%IOMASTER,CDSTRING='IOSTREAM_MIX:IO_INQUIRE')
    IF(YIOS%MYPROC /= YD%IOMASTER) ALLOCATE(IGRIBMSG(ILENINT))
    CALL MPL_BROADCAST(IGRIBMSG,ITAG,YD%IOMASTER,CDSTRING='IOSTREAM_MIX:IO_INQUIRE')
    IF(YIOS%MYPROC /= YD%IOMASTER) THEN
      CALL IGRIB_NEW_FROM_MESSAGE(KGRIB_HANDLE,IGRIBMSG)
    ENDIF
    DEALLOCATE(IGRIBMSG)
  ENDIF
ENDIF
YDR%LINQUIRE = .FALSE.
CALL USER_CLOCK(PELAPSED_TIME=ZTIME1)
YD%TOTWALL = YD%TOTWALL+(ZTIME1-ZTIME0)
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:IO_INQUIRE',1,ZHOOK_HANDLE)
END SUBROUTINE IO_INQUIRE
