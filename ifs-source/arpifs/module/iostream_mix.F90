SUBROUTINE SETUP_IOREQUEST(YDR,CDTYPE,KGRIB3D,KLEVS3D,KGRIB2D,KLEVS2D,KBSET2D,KBSET3D,&
 & LDGRIB,KPROMA,KGPTOTG,KRESOL,CDLEVTYPE,LDFPDIST,LDINTERP,KTYPE_INTERP,LDISTRIBUTE,&
 & LDISTHEADER,KGRIB_HANDLE,KGRIB_HANDLE2,LDREADALL,LDLATLONG,LDINTONLY,KSMAX,KMAXSPEC2G,&
 & LDRANDOM_ORDER,KCHUNKSIZE,PTSTEP)

!**** *SETUP_IOREQUEST*  - Setup IO request

! Purpose - To initiate an io-request, select kind of data, etc.

! Explicit arguments :
! --------------------
! YDR          - IO request structure
! CDTYPE       - type of data ('RAW','SPECTRAL_FIELDS','GRIDPOINT_FIELDS')
! KGRIB3D      - GRIB parameter codes for 3-D data (points,levels and parameters)
! KLEVS3D      - level list for 3-D data
! KGRIB2D      - GRIB parameter codes for 2-D data
! KLEVS2D      - level list for 2-D data
! KBSET2D      - B-set for 2-D data(spectral only)
! KBSET3D      - B-set for 3-D data(spectral only)
! LDGRIB       - TRUE if data in GRIB form
! KPROMA       - blocking size for gridpoint data
! KGPTOTG      - number of points for gridpoint data
! KRESOL       - resolution indentifier
! CDLEVTYPE    - level type (model,pressure etc.)
! LDFPDIST     - Full-pos distribution of data
! LDINTERP     - data to be horizontally interpolated on read/write
! KTYPE_INTERP - interpolation method
! LDISTRIBUTE  - data to be distrubuted
! LDISTHEADER  - header data to be distrubuted
! KGRIB_HANDLE - Grib header template handle
! KGRIB_HANDLE2- Grib header template additional to set grib2 messages
! LDREADALL    - read all file (do not stop whean request satisfied)
! LDLATLONG    - data is in lat/long format (not reduced grid)
! LDINTONLY    - request for interogating only

!-------------------------------------------------------------------------
IMPLICIT NONE
TYPE(TYPE_IOREQUEST),INTENT(INOUT) :: YDR
CHARACTER(LEN=*)           ,INTENT(IN) :: CDTYPE
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KGRIB3D(:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KLEVS3D(:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KGRIB2D(:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KLEVS2D(:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KBSET2D(:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KBSET3D(:)
LOGICAL           ,OPTIONAL,INTENT(IN) :: LDGRIB
LOGICAL           ,OPTIONAL,INTENT(IN) :: LDFPDIST
LOGICAL           ,OPTIONAL,INTENT(IN) :: LDINTERP
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KTYPE_INTERP
LOGICAL           ,OPTIONAL,INTENT(IN) :: LDISTRIBUTE
LOGICAL           ,OPTIONAL,INTENT(IN) :: LDISTHEADER
LOGICAL           ,OPTIONAL,INTENT(IN) :: LDREADALL
LOGICAL           ,OPTIONAL,INTENT(IN) :: LDLATLONG
LOGICAL           ,OPTIONAL,INTENT(IN) :: LDINTONLY
LOGICAL           ,OPTIONAL,INTENT(IN) :: LDRANDOM_ORDER
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KPROMA
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KGPTOTG
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KSMAX
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KMAXSPEC2G
CHARACTER(LEN=*)  ,OPTIONAL,INTENT(IN) :: CDLEVTYPE
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KGRIB_HANDLE
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KGRIB_HANDLE2
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KCHUNKSIZE
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KRESOL
REAL(KIND=JPRB)   ,OPTIONAL,INTENT(IN) :: PTSTEP

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
INTEGER(KIND=JPIM) :: JLEV,ILEN,ISMAX,IPRTRW,IMYSETW,IMYSETV,ILOCLEV

#include "ini_spec_dist.h"

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:SETUP_IOREQUEST',0,ZHOOK_HANDLE)

!Preset defaults
YDR%LGRIB         = .FALSE.
YDR%LRANDOM_ORDER = .FALSE.
YDR%LFPDIST       = .FALSE.
YDR%LINTERP       = .FALSE.
YDR%NTYPE_INTERP  = 0
YDR%LDISTRIBUTE   = .TRUE.
YDR%LDISTHEADER   = .TRUE.
YDR%LGEOPRESET    = .FALSE.
YDR%LREADALL      = .FALSE.
YDR%LINQUIRE      = .FALSE.
YDR%LATLONG       = .FALSE.
YDR%LINTONLY      = .FALSE.
YDR%CLEVTYPE      = 'SFC'
YDR%NCHUNKSIZE    = 8
YDR%IGRIB_HANDLE  = -1
YDR%IGRIB_HANDLE2 = -1
YDR%NRESOL        = 1
YDR%TSTEP         = 3600.0
YDR%IOCOMM        = MPI_COMM_NULL

! Set logical switches according to arguments
IF(PRESENT(LDGRIB))      YDR%LGRIB       = LDGRIB
IF(PRESENT(CDLEVTYPE))   YDR%CLEVTYPE    = CDLEVTYPE
IF(PRESENT(LDFPDIST))    YDR%LFPDIST     = LDFPDIST
IF(PRESENT(LDINTERP))    THEN
  IF(LDINTERP .AND. .NOT.YIOS%LIFS_INTERNAL)&
   & CALL ABOR1('IOSTREAM_MIX:SETUP_IOREQUEST - LDINTERP=TRUE ONLY INSIDE IFS')
  YDR%LINTERP     = LDINTERP 
ENDIF
IF(PRESENT(LDISTRIBUTE)) YDR%LDISTRIBUTE = LDISTRIBUTE
IF(PRESENT(LDISTHEADER)) YDR%LDISTHEADER = LDISTHEADER
IF(PRESENT(LDREADALL))   YDR%LREADALL    = LDREADALL
IF(PRESENT(LDLATLONG))   YDR%LATLONG     = LDLATLONG
IF(PRESENT(LDINTONLY))   YDR%LINTONLY    = LDINTONLY

IF(YDR%LGRIB)  YDR%LRANDOM_ORDER = .TRUE.
IF(PRESENT(LDRANDOM_ORDER))   YDR%LRANDOM_ORDER   = LDRANDOM_ORDER

IF(PRESENT(KTYPE_INTERP)) YDR%NTYPE_INTERP = KTYPE_INTERP

ISMAX = YIOS%NSMAX

IF(PRESENT(KRESOL)) THEN
  YDR%NRESOL = KRESOL
  CALL TRANS_INQ(KRESOL=YDR%NRESOL,KSMAX=ISMAX)
ENDIF

IF(PRESENT(KSMAX)) ISMAX = KSMAX
IF(PRESENT(KCHUNKSIZE)) YDR%NCHUNKSIZE = KCHUNKSIZE

IF(YDR%NCHUNKSIZE > JPMAXCHUNK)&
 & CALL ABOR1('IOSTREAM_MIX:SETUP_IOREQUEST -NCHUNKSIZE > JPMAXCHUNK')
!Set data type and dependent varaibles
SELECT CASE(CDTYPE)
CASE('RAW')
  YDR%IDATA_TYPE = JPRAW
CASE('SPECTRAL_FIELDS')
  YDR%IDATA_TYPE = JPSPEC
  IF(PRESENT(KMAXSPEC2G)) THEN
    YDR%YSPF%IMAXSPEC2G=KMAXSPEC2G
  ELSE
    YDR%YSPF%IMAXSPEC2G=YIOS%IMAXSPEC2G
  ENDIF
  IF(.NOT. YDR%LINTONLY) THEN
    IF(YDR%LFPDIST) THEN
      IF(.NOT.ALLOCATED(YIOS%NFPMAX)) CALL ABOR1('IOSTREAM_MIX:SETUP_IOREQUEST - YIOS%NFPMAX NOT DEFINED')
      YDR%YSPF%ISMAX = YIOS%NFPMAX(1)
      YDR%YSPF%ISPEC2G=(YIOS%NFPMAX(1)+1)*(YIOS%NFPMAX(1)+2)
    ELSEIF(ISMAX /= YIOS%NSMAX) THEN
      YDR%YSPF%ISMAX = ISMAX
      YDR%YSPF%ISPEC2G = (ISMAX+1)*(ISMAX+2)
      CALL TRANS_INQ(KRESOL=YDR%NRESOL,KPRTRW=IPRTRW,KMYSETW=IMYSETW)
      CALL INI_SPEC_DIST(ISMAX,ISMAX,IPRTRW,IMYSETW,KSPEC2=YDR%YSPF%ISPEC2)
    ELSE
      YDR%YSPF%ISMAX = YIOS%NSMAX
      CALL TRANS_INQ(KRESOL=YDR%NRESOL,KSPEC2=YDR%YSPF%ISPEC2,KSPEC2G=YDR%YSPF%ISPEC2G)
    ENDIF
  ENDIF
CASE('GRIDPOINT_FIELDS')
  YDR%IDATA_TYPE = JPGP
  IF(YDR%LINTONLY) THEN
    YDR%YGPF%IGPTOT = -99
    YDR%YGPF%IGPTOTG = -99
  ELSEIF(YDR%LATLONG) THEN
    CALL TRANS_INQ(KRESOL=YDR%NRESOL,KGPTOT=YDR%YGPF%IGPTOT,KGPTOTG&
        &=YDR%YGPF%IGPTOTG)
  ELSE
    IF(YDR%LFPDIST) THEN
      IF(YIOS%NFPRGPL == YIOS%NUNDEF) CALL ABOR1('IOSTREAM_MIX:SETUP_IOREQUEST - YIOS%NFPRGPL NOT DEFINED')
      IF(YIOS%NFPRGPG == YIOS%NUNDEF) CALL ABOR1('IOSTREAM_MIX:SETUP_IOREQUEST - YIOS%NFPRGPG NOT DEFINED')
      YDR%YGPF%IGPTOT = YIOS%NFPRGPL
      YDR%YGPF%IGPTOTG = YIOS%NFPRGPG
    ELSE
      CALL TRANS_INQ(KRESOL=YDR%NRESOL,KGPTOT=YDR%YGPF%IGPTOT,KGPTOTG=YDR%YGPF%IGPTOTG)
    ENDIF
  ENDIF
  IF(PRESENT(KPROMA)) THEN
    YDR%YGPF%NPROMA=KPROMA
  ELSE
    YDR%YGPF%NPROMA=YDR%YGPF%IGPTOT
  ENDIF
  IF(PRESENT(KGPTOTG)) YDR%YGPF%IGPTOTG = KGPTOTG
CASE DEFAULT
  CALL ABOR1('IOSTREAM_MIX:SETUP_IOREQUEST - UNKNOWN DATA TYPE')
END SELECT

! For spectral and gridpoint data optionally set parameter lists etc.
IF(YDR%IDATA_TYPE == JPSPEC .OR. YDR%IDATA_TYPE == JPGP) THEN
  IF(PRESENT(KGRIB3D)) THEN
    YDR%YMDL_3D%NUMFLDS = UBOUND(KGRIB3D,1)
    ALLOCATE(YDR%YMDL_3D%IGRIBCODE(YDR%YMDL_3D%NUMFLDS))
    YDR%YMDL_3D%IGRIBCODE(:) = KGRIB3D(:)
    IF(.NOT.PRESENT(KLEVS3D)) THEN
      WRITE(YIOS%NULOUT,*)&
       & 'IOSTREAM_MIX:SETUP_IOREQUEST - KLEVS3D MISSING, ASSUMING MODEL LEVELS'
      IF(YIOS%NFLEVG == YIOS%NUNDEF)&
       & CALL ABOR1('IOSTREAM_MIX:SETUP_IOREQUEST - YIOS%NFLEVG NOT INITIALIZED')
      YDR%YMDL_3D%NLEVS = YIOS%NFLEVG
      ALLOCATE(YDR%YMDL_3D%ILEVS(YDR%YMDL_3D%NLEVS))
      DO JLEV=1,YDR%YMDL_3D%NLEVS
        YDR%YMDL_3D%ILEVS(JLEV) = JLEV
      ENDDO
    ELSE  
      YDR%YMDL_3D%NLEVS = UBOUND(KLEVS3D,1)
      ALLOCATE(YDR%YMDL_3D%ILEVS(YDR%YMDL_3D%NLEVS))
      YDR%YMDL_3D%ILEVS(:) = KLEVS3D(:)
    ENDIF
    IF(YDR%IDATA_TYPE == JPSPEC) THEN
      ALLOCATE(YDR%YMDL_3D%IBSET(YDR%YMDL_3D%NLEVS))
      ALLOCATE(YDR%YMDL_3D%ILOCLEV(YDR%YMDL_3D%NLEVS))
      YDR%YMDL_3D%ILOCLEV(:)=-HUGE(JLEV)
      IF(PRESENT(KBSET3D)) THEN
        IF(SIZE(KBSET3D) < YDR%YMDL_3D%NLEVS)&
         & CALL ABOR1('IOSTREAM_MIX:SETUP_IOREQUEST - KBSET3D TOO SMALL')
        YDR%YMDL_3D%IBSET(:) = KBSET3D(1:YDR%YMDL_3D%NLEVS)
      ELSE
        IF(YDR%YMDL_3D%NLEVS /= YIOS%NFLEVG)&
         & CALL ABOR1('IOSTREAM_MIX:SETUP_IOREQUEST - KBSET3D REQUIRED')
        YDR%YMDL_3D%IBSET(:) = YIOS%NBSETLEV(:)
      ENDIF
      CALL TRANS_INQ(KRESOL=YDR%NRESOL,KMYSETV=IMYSETV)
      ILOCLEV=0
      DO JLEV=1,YDR%YMDL_3D%NLEVS
        IF(YDR%YMDL_3D%IBSET(JLEV) == IMYSETV) THEN
          ILOCLEV=ILOCLEV+1
          YDR%YMDL_3D%ILOCLEV(JLEV)=ILOCLEV
        ENDIF
      ENDDO
    ENDIF
  ELSE
    YDR%YMDL_3D%NUMFLDS = 0
    YDR%YMDL_3D%NLEVS = 0
  ENDIF

  IF(PRESENT(KGRIB2D)) THEN
    ILEN = UBOUND(KGRIB2D,1)
    YDR%YMDL_2D%NUMFLDS = ILEN
    ALLOCATE(YDR%YMDL_2D%IGRIBCODE(ILEN))
    YDR%YMDL_2D%IGRIBCODE(:) = KGRIB2D(:)
    ALLOCATE(YDR%YMDL_2D%ILEVS(ILEN))
    ALLOCATE(YDR%YMDL_2D%IBSET(ILEN))
    ALLOCATE(YDR%YMDL_2D%ILOCFLD(ILEN))
    YDR%YMDL_2D%ILOCFLD(:)=-HUGE(JLEV)
    IF(PRESENT(KLEVS2D)) THEN
      IF(SIZE(KLEVS2D) < ILEN)&
       & CALL ABOR1('IOSTREAM_MIX:SETUP_IOREQUEST - KLEVS2D TOO SMALL')
      YDR%YMDL_2D%ILEVS(:) = KLEVS2D(1:ILEN)
    ELSE
      YDR%YMDL_2D%ILEVS(:)= 0
    ENDIF
    IF(YDR%CLEVTYPE == 'ML') THEN
      YDR%YMDL_2D%ILEVS(:)=MAX(YDR%YMDL_2D%ILEVS(:),1) ! Make sure "surface" model level fields are assigned
                                                       ! to level 1
    ENDIF
    IF(PRESENT(KBSET2D)) THEN
      IF(SIZE(KBSET2D) < ILEN)&
       & CALL ABOR1('IOSTREAM_MIX:SETUP_IOREQUEST - KBSET2D TOO SMALL')
      YDR%YMDL_2D%IBSET(:) = KBSET2D(1:ILEN)
    ELSE
      IF(YIOS%NBSETSP == YIOS%NUNDEF)&
       &  CALL ABOR1('IOSTREAM_MIX:SETUP_IOREQUEST - YIOS%NBSETSP NOT DEFINED')
      YDR%YMDL_2D%IBSET(:) = YIOS%NBSETSP
    ENDIF
    CALL TRANS_INQ(KRESOL=YDR%NRESOL,KMYSETV=IMYSETV)
    ILOCLEV=0
    DO JLEV=1,YDR%YMDL_2D%NUMFLDS
      IF(YDR%YMDL_2D%IBSET(JLEV) == IMYSETV) THEN
        ILOCLEV=ILOCLEV+1
        YDR%YMDL_2D%ILOCFLD(JLEV)=ILOCLEV
      ENDIF
    ENDDO
  ELSE
    YDR%YMDL_2D%NUMFLDS = 0
  ENDIF
ENDIF

! For data in GRIB format allocate(or clone) GRIB header struct.
IF(YDR%LGRIB)THEN
  IF(PRESENT(PTSTEP)) YDR%TSTEP=PTSTEP
  IF(PRESENT(KGRIB_HANDLE)) THEN
    CALL IGRIB_CLONE(KGRIB_HANDLE,YDR%IGRIB_HANDLE)
    YDR%LGEOPRESET = .TRUE.
  ENDIF
  IF(PRESENT(KGRIB_HANDLE2)) THEN
    CALL IGRIB_CLONE(KGRIB_HANDLE2,YDR%IGRIB_HANDLE2)
    YDR%LGEOPRESET = .TRUE.
  ENDIF
ENDIF

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:SETUP_IOREQUEST',1,ZHOOK_HANDLE)
END SUBROUTINE SETUP_IOREQUEST

