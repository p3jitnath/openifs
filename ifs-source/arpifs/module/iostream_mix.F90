SUBROUTINE GRIB_ENCODE(YDR,PREC,KFIELDS)

!**** *GRIB_ENCODE*  - Encode data in GRIB

! Purpose - To encode data in GRIB

! Explicit arguments :
! --------------------
! YDR  - IO request structure
! PREC - real data
!-------------------------------------------------------------------------
IMPLICIT NONE
TYPE(TYPE_IOREQUEST), INTENT(INOUT) :: YDR
REAL(KIND=JPRB)     , INTENT(IN)    :: PREC(:,:)
INTEGER(KIND=JPIM)  , INTENT(IN)    :: KFIELDS
INTEGER(KIND=JPIM) :: JFLD,ILEN,IBOT,ITOP,IGRIBHANDLE
INTEGER(KIND=JPKSIZE_T) :: IGRIBMSGLEN
INTEGER(KIND=JPIM), ALLOCATABLE :: IGRIB2D(:,:)
INTEGER(KIND=JPIM) :: IFIELD_LENGTHS(KFIELDS)
INTEGER(KIND=JPIM) :: I,IGRIBD_LEN,IGRIB2D_LEN
LOGICAL :: LLGRAD, LLVALID
CHARACTER(LEN=3) :: CLREPR

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
!-------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:GRIB_ENCODE',0,ZHOOK_HANDLE)

IF(.NOT.YIOS%LIFS_INTERNAL) THEN
  CALL ABOR1(' IOSTREAM_MIX:GRIB_DECODE - SHOULD NOT BE CALLED WHEN YIOS%LIFS_INTERNAL = FALSE')
ENDIF

CALL GSTATS(1484,0)
CALL ALLO_IGRIB(YDR,KFIELDS)
SELECT CASE(YDR%IDATA_TYPE)
CASE(JPSPEC)
  CLREPR = 'SH'
  ILEN = YDR%YSPF%ISPEC2G
CASE(JPGP)
  CLREPR = 'GG'
  ILEN = YDR%YGPF%IGPTOTG
CASE DEFAULT
  CLREPR = 'SH'
  ILEN = YDR%YSPF%ISPEC2G
END SELECT

IGRIBD_LEN=SIZE(NGRIBD)
IGRIB2D_LEN=IGRIBD_LEN/KFIELDS
ALLOCATE(IGRIB2D(IGRIB2D_LEN,KFIELDS))

!$OMP PARALLEL DO SCHEDULE(DYNAMIC)&
!$OMP PRIVATE(JFLD,ITOP,IBOT,LLGRAD,IGRIBHANDLE,IGRIBMSGLEN,LLVALID)
DO JFLD =1,KFIELDS
  ITOP = -1
  IBOT = -1
  LLGRAD = .FALSE.

!Need a critical region to avoid crash while setting up grib meta data
!$OMP CRITICAL (GRIB_ENCODE_CLONE)
  SELECT CASE(IGRIB_EDITION(YDR%IGRBCODES(JFLD),YDR%ILEVS(JFLD)))
  CASE(1)
    CALL IGRIB_CLONE(YDR%IGRIB_HANDLE,IGRIBHANDLE)
  CASE(2)
    IF (YDR%IGRIB_HANDLE2 == -1 .AND. YDR%CLEVTYPE == 'ML') THEN
      ! For ML, IGRIB_HANDLE will be GRIB2 anyway and IGRIB_HANDLE2 unset.
      CALL IGRIB_CLONE(YDR%IGRIB_HANDLE,IGRIBHANDLE)
    ELSE
      CALL IGRIB_CLONE(YDR%IGRIB_HANDLE2,IGRIBHANDLE)
    ENDIF
  CASE DEFAULT
    CALL ABOR1('IOSTREAM_MIX:GRIB_ENCODE - UNKNOWN GRIB EDITION')
  END SELECT
  CALL GRIB_CODE_MESSAGE(YDR%TSTEP,IGRIBHANDLE,YDR%IGRBCODES(JFLD),YDR%ILEVS(JFLD),&
       & CLREPR,YDR%CLEVTYPE,LLGRAD,LLVALID,ITOP,IBOT,KSMAX=YDR%YSPF%ISMAX)
!$OMP END CRITICAL (GRIB_ENCODE_CLONE)
  IGRIBMSGLEN=0
  IF (LLVALID) THEN
    CALL GRIB_UPDATE_MESSAGE(IGRIBHANDLE,YDR%IGRBCODES(JFLD),YDR%ILEVS(JFLD),&
      & CLREPR,YDR%CLEVTYPE,PREC(1:ILEN,JFLD),YDR%YSPF%ISMAX,&
      & LLGRAD,ITOP,IBOT,IGRIBMSGLEN,IGRIB2D(:,JFLD))
    IFIELD_LENGTHS(JFLD)=(IGRIBMSGLEN+YIOS%NINTLEN-1)/YIOS%NINTLEN
  ELSE
    IFIELD_LENGTHS(JFLD)=0
  ENDIF

!Need a critical region to avoid crash while releasing grib meta data
!$OMP CRITICAL (GRIB_ENCODE_RELEASE)
  CALL IGRIB_RELEASE(IGRIBHANDLE)
!$OMP END CRITICAL (GRIB_ENCODE_RELEASE)
ENDDO
!$OMP END PARALLEL DO

DO JFLD=1,KFIELDS
  NGSTA(JFLD+1) = NGSTA(JFLD) + IFIELD_LENGTHS(JFLD)
ENDDO

!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(JFLD,I)
DO JFLD=1,KFIELDS
  DO I=1,IFIELD_LENGTHS(JFLD)
    NGRIBD(NGSTA(JFLD)-1+I) = IGRIB2D(I,JFLD)
  ENDDO
ENDDO
!$OMP END PARALLEL DO


DEALLOCATE(IGRIB2D)
CALL GSTATS(1484,1)
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:GRIB_ENCODE',1,ZHOOK_HANDLE)

END SUBROUTINE GRIB_ENCODE
