SUBROUTINE CLOSE_IOREQUEST(YDR)
USE DEALLOCATE_IF_ALLOCATED_MOD, ONLY : DEALLOCATE_IF_ALLOCATED
IMPLICIT NONE
!**** *CLOSE_IOREQUEST*  - Close IO request

! Purpose - Release space, reset defaults

! Explicit arguments :
! --------------------
! YDR        - IO request structure
!-------------------------------------------------------------------------
TYPE(TYPE_IOREQUEST),INTENT(INOUT) :: YDR
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
!-------------------------------------------------------------------------
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:CLOSE_IOREQUEST',0,ZHOOK_HANDLE)
CALL DEALLOCATE_IF_ALLOCATED(YDR%YMDL_3D%ILEVS)
CALL DEALLOCATE_IF_ALLOCATED(YDR%YMDL_2D%ILEVS)
CALL DEALLOCATE_IF_ALLOCATED(YDR%YMDL_3D%IBSET)
CALL DEALLOCATE_IF_ALLOCATED(YDR%YMDL_2D%IBSET)
CALL DEALLOCATE_IF_ALLOCATED(YDR%YMDL_2D%ILOCFLD)
CALL DEALLOCATE_IF_ALLOCATED(YDR%YMDL_3D%IGRIBCODE)
CALL DEALLOCATE_IF_ALLOCATED(YDR%YMDL_2D%IGRIBCODE)
CALL DEALLOCATE_IF_ALLOCATED(YDR%YMDL_3D%ILOCLEV)
CALL DEALLOCATE_IF_ALLOCATED(YDR%ILEVS)
CALL DEALLOCATE_IF_ALLOCATED(YDR%IGRBCODES)
CALL DEALLOCATE_IF_ALLOCATED(YDR%IHAVEFLD)

IF(YDR%LGRIB)THEN
  YDR%LGRIB = .FALSE.
  IF(ALLOCATED(NGRIBD)) DEALLOCATE(NGRIBD)
  IF(ALLOCATED(NGSTA)) DEALLOCATE(NGSTA)
  IF(ALLOCATED(LGRIBDONE)) DEALLOCATE(LGRIBDONE)
  IF(ALLOCATED(NGRIBMSG)) DEALLOCATE(NGRIBMSG)
ENDIF
YDR%IDATA_TYPE = JPNONDEF 
IF (YDR%IGRIB_HANDLE /= -1) THEN
  CALL IGRIB_RELEASE(YDR%IGRIB_HANDLE)
  YDR%IGRIB_HANDLE=-1
ENDIF
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:CLOSE_IOREQUEST',1,ZHOOK_HANDLE)
END SUBROUTINE CLOSE_IOREQUEST

