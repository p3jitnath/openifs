SUBROUTINE GATH_GRID_FP(PGPG,KFGATHG,KTO,PGP)

!**** *GATH_GRID_FP*  - Gather grid-point fields

! Purpose - Gather grid-point fields distributed with "Full-Pos distribution"

! Explicit arguments :
! --------------------
! PGPG - global grid-point fields
! KFGATHG - number of global grid-point fields
! KTO - processor(s) to which to send data
! PGP - local grid-point fields
! 
!-------------------------------------------------------------------------
IMPLICIT NONE
REAL(KIND=JPRB)    , INTENT(OUT) :: PGPG(:,:)
INTEGER(KIND=JPIM) , INTENT(IN)  :: KFGATHG
INTEGER(KIND=JPIM) , INTENT(IN)  :: KTO(:)
REAL(KIND=JPRB)    , INTENT(IN)  :: PGP(:,:,:)
REAL(KIND=JPRB),ALLOCATABLE :: ZGP(:,:)
REAL(KIND=JPRB),ALLOCATABLE :: ZRCV(:,:,:)
INTEGER(KIND=JPIM) :: ISREQ(KFGATHG),IPT(YIOS%NFPRGPG)
INTEGER(KIND=JPIM) :: JFLD, JJ, ILENR0(YIOS%NPROC), IRCV,IFLD,ITO,JROC,ITAG
LOGICAL :: LLSAME
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
!-------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:GATH_GRID_FP',0,ZHOOK_HANDLE)
ITAG=11000
IF(UBOUND(KTO,1) < KFGATHG) THEN
  CALL ABOR1('IOSTREAM_MIX:GATH_GRID_FP - KTO TOO SHORT!')
ENDIF
IF(YIOS%NFPRGPG == YIOS%NUNDEF) CALL ABOR1('IOSTREAM_MIX:GATH_GRID_FP - YIOS%NFPRGPG NOT DEFINED')
IF(.NOT.ALLOCATED(YIOS%NUMPROCFP)) CALL ABOR1('IOSTREAM_MIX:GATH_GRID_FP - YIOS%NUMPROCFP NOT DEFINED')
ILENR0(:) = 0
DO JJ=1,YIOS%NFPRGPG
  IRCV = YIOS%NUMPROCFP(JJ)
  ILENR0(IRCV) = ILENR0(IRCV)+1
  IPT(JJ) = ILENR0(IRCV)
ENDDO
LLSAME = .TRUE.
ITO = KTO(1)
DO JFLD=2,KFGATHG
  IF(KTO(JFLD) /= ITO) THEN
    LLSAME = .FALSE.
    EXIT
  ENDIF
ENDDO
IFLD = 0
DO JFLD=1,KFGATHG
  IF(KTO(JFLD) == YIOS%MYPROC) THEN
    IFLD = IFLD+1
  ENDIF
ENDDO
ALLOCATE(ZRCV(YIOS%NFPRGPLX,IFLD,YIOS%NPROC))
CALL GSTATS_BARRIER(728)
CALL GSTATS(633,0)
IF(LLSAME) THEN
  ALLOCATE(ZGP(YIOS%NFPRGPLX,KFGATHG))
  DO JFLD=1,KFGATHG
    ZGP(1:SIZE(PGP,1),JFLD) = PGP(1:SIZE(PGP,1),JFLD,1)
  ENDDO
  CALL MPL_SEND(ZGP,KDEST=KTO(1),&
   & KMP_TYPE=JP_NON_BLOCKING_STANDARD,KREQUEST=ISREQ(1),&
   & KTAG=ITAG,CDSTRING='IOSTREAM_MIX:GATH_GRID_FP')
ELSE
  DO JFLD=1,KFGATHG
    CALL MPL_SEND(PGP(:,JFLD,1),KDEST=KTO(JFLD),&
     & KMP_TYPE=JP_NON_BLOCKING_STANDARD,KREQUEST=ISREQ(JFLD),&
     & KTAG=ITAG+JFLD,CDSTRING='IOSTREAM_MIX:GATH_GRID_FP')
  ENDDO
ENDIF
IF (LLSAME) THEN
  IFLD = 0
  IF(KTO(1) == YIOS%MYPROC) THEN
    IFLD = KFGATHG
    DO JROC=1,YIOS%NPROC
      IRCV = JROC
      CALL MPL_RECV(ZRCV(:,:,IRCV),&
       & KSOURCE=IRCV,KTAG=ITAG,CDSTRING='IOSTREAM_MIX:GATH_GRID_FP')
    ENDDO
  ENDIF
  CALL MPL_WAIT(KREQUEST=ISREQ(1),&
   & CDSTRING='IOSTREAM_MIX:GATH_GRID_FP')
  DEALLOCATE(ZGP)
ELSE
  IFLD = 0
  DO JFLD=1,KFGATHG
    IF(KTO(JFLD) == YIOS%MYPROC) THEN
      IFLD = IFLD+1
      DO JROC=1,YIOS%NPROC
        IRCV = JROC
        CALL MPL_RECV(ZRCV(:,IFLD,IRCV),&
         & KSOURCE=IRCV,KTAG=ITAG+JFLD,CDSTRING='IOSTREAM_MIX:GATH_GRID_FP')
      ENDDO
    ENDIF
  ENDDO
  CALL MPL_WAIT(KREQUEST=ISREQ(:),&
   & CDSTRING='IOSTREAM_MIX:GATH_GRID_FP')
ENDIF
CALL GSTATS(633,1)
CALL GSTATS_BARRIER2(728)

CALL GSTATS(1482,0)
!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(JFLD,JJ)
DO JFLD=1,IFLD
    DO JJ=1,YIOS%NFPRGPG
      PGPG(JJ,JFLD) = ZRCV(IPT(JJ),JFLD,YIOS%NUMPROCFP(JJ))
    ENDDO
ENDDO
!$OMP END PARALLEL DO
CALL GSTATS(1482,1)
DEALLOCATE(ZRCV)

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:GATH_GRID_FP',1,ZHOOK_HANDLE)
END SUBROUTINE GATH_GRID_FP
