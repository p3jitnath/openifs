SUBROUTINE GRIB_DECODE(YDR,PREC)

!**** *GRIB_DECODE*  - Decode GRIB data

! Purpose - Decode GRIB data

! Explicit arguments :
! --------------------
! YDR  - IO request structure
! PREC - real data
!-------------------------------------------------------------------------
IMPLICIT NONE
TYPE(TYPE_IOREQUEST),INTENT(INOUT) :: YDR
REAL(KIND=JPRB),INTENT(OUT) :: PREC(:,:)

INTEGER(KIND=JPIM) :: JFLD,IVALUES
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE,ZHOOK_HANDLE2
!INTEGER(KIND=JPIB), EXTERNAL :: GETHWM, GETRSS, GETCURHEAP, GETSTK, GETMAXSTK
CHARACTER(LEN=1) :: CLDECODE
!-------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:GRIB_DECODE',0,ZHOOK_HANDLE)
IF(YDR%LINQUIRE) THEN
  CLDECODE = 'I'
ELSE
  CLDECODE = 'D'
ENDIF
CALL GSTATS(1879,0)
DO JFLD =1,YDR%IFIELDS_HAVE
  IF(LGRIBDONE(JFLD)) CYCLE
  IF (YDR%IGRIB_HANDLE /= -1) THEN
    CALL IGRIB_RELEASE(YDR%IGRIB_HANDLE)
    YDR%IGRIB_HANDLE=-1
  ENDIF
  CALL IGRIB_NEW_FROM_MESSAGE(YDR%IGRIB_HANDLE,NGRIBD(NGSTA(JFLD):NGSTA(JFLD+1)-1))
  CALL IGRIB_SET_VALUE(YDR%IGRIB_HANDLE,'missingValue',-9999)
  CALL INQUIRE_GRIB(YDR%IGRIB_HANDLE,KPARAM=YDR%IGRBCODE,KLEV=YDR%ILEV,CDREPRT=YDR%CREPRT)
  IF(TRIM(YDR%CREPRT) == 'sh') THEN
    CALL INQUIRE_GRIB(YDR%IGRIB_HANDLE,KSMAX=YDR%IRESOLI)
  ENDIF
  CALL IGRIB_GET_VALUE(YDR%IGRIB_HANDLE,'numberOfPoints',IVALUES)
  YDR%IVALUES = IVALUES
  IF(.NOT. YDR%LINQUIRE) THEN
    IF(IVALUES > 0) THEN
      IF(SIZE(PREC(:,JFLD)) < IVALUES) THEN
        WRITE(YIOS%NULERR,*)' SIZE(PREC(:,JFLD) ,IVALUES ', SIZE(PREC(:,JFLD)) ,IVALUES
        CALL ABOR1(' IOSTREAM_MIX:GRIB_DECODE - PREC TO SMALL')
      ENDIF
    ENDIF
    PREC(1:IVALUES,JFLD) = -1.0_JPRB
    IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:GRIB_GET_VALUES',0,ZHOOK_HANDLE2)
    CALL IGRIB_GET_VALUE(YDR%IGRIB_HANDLE,'values',PREC(1:IVALUES,JFLD))
    IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:GRIB_GET_VALUES',1,ZHOOK_HANDLE2)
  ENDIF
  YDR%IGRBCODES(JFLD) = YDR%IGRBCODE
  YDR%ILEVS(JFLD) = YDR%ILEV
  LGRIBDONE(JFLD) = .TRUE.
  CALL GRIB_CHECKS(YDR)
ENDDO
CALL GSTATS(1879,1)
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:GRIB_DECODE',1,ZHOOK_HANDLE)

END SUBROUTINE GRIB_DECODE
!============================================================= 
SUBROUTINE GRIB_CHECKS(YDR)

!**** *GRIB_CHECKS*  - Some consistency checks on GRIB header

! Purpose - Check GRIB header info

! Explicit arguments :
! --------------------
! YDR  - IO request structure
!-------------------------------------------------------------------------
IMPLICIT NONE

TYPE(TYPE_IOREQUEST),INTENT(IN) :: YDR

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
!-------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:GRIB_CHECKS',0,ZHOOK_HANDLE)

SELECT CASE(YDR%IDATA_TYPE)
CASE(JPSPEC)
  IF(TRIM(YDR%CREPRT) /= 'sh') THEN
    WRITE(YIOS%NULERR,'(" IOSTREAM_MIX :UNEXPECTED DATA ",&
     & "REPRESENTATION TYPE",A)') TRIM(YDR%CREPRT)
    CALL ABOR1('IOSTREAM_MIX:GRIB_CHECKS - INVALID DATA REPRESENTATION TYPE FOR SH')
  ENDIF
  IF(.NOT. YDR%LINTERP .AND. .NOT. YDR%LINTONLY .AND.&
   & YDR%IRESOLI /= YDR%YSPF%ISMAX) THEN
    WRITE(YIOS%NULERR,*)' IOSTREAM_MIX :RESOLUTION OF MODEL ',YDR%YSPF%ISMAX,&
     & ' ,OF INITIAL DATA ',YDR%IRESOLI
    CALL ABOR1('IOSTREAM_MIX:GRIB_CHECKS -  DATA HAS INCORRECT RESOLUTION')
  ENDIF
CASE(JPGP)
  IF(YDR%LATLONG) THEN
    IF(TRIM(YDR%CREPRT) /= 'regular_ll' .AND. TRIM(YDR%CREPRT) /= 'reduced_ll') THEN
      WRITE(YIOS%NULERR,'(" IOSTREAM_MIX :UNEXPECTED DATA ",&
       & "REPRESENTATION TYPE",A)') TRIM(YDR%CREPRT)
      CALL ABOR1('IOSTREAM_MIX:GRIB_CHECKS - INVALID DATA REPRESENTATION TYPE FOR LAT/LONG')
    ENDIF
  ELSE
    IF(TRIM(YDR%CREPRT) /= 'regular_gg' .AND. TRIM(YDR%CREPRT) /= 'reduced_gg') THEN
    WRITE(YIOS%NULERR,'(" IOSTREAM_MIX :UNEXPECTED DATA ",&
       & "REPRESENTATION TYPE",A)') TRIM(YDR%CREPRT)
      CALL ABOR1('IOSTREAM_MIX:GRIB_CHECKS - INVALID DATA REPRESENTATION TYPE FOR GG')
    ENDIF
  ENDIF
END SELECT

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:GRIB_CHECKS',1,ZHOOK_HANDLE)

END SUBROUTINE GRIB_CHECKS
!============================================================= 
SUBROUTINE ALLO_IGRIB(YDR,KFIELDS,KLENGRB)

!**** *ALLO_IGRIB*  - Allocate array to keep GRIB data

! Purpose - Allocate GRIB array

! Explicit arguments :
! --------------------
! YDR  - IO request structure

!-------------------------------------------------------------------------
IMPLICIT NONE
TYPE(TYPE_IOREQUEST),INTENT(INOUT) :: YDR
INTEGER(KIND=JPIM),INTENT(IN) :: KFIELDS
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KLENGRB

INTEGER(KIND=JPIB) :: ILENGRB
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
!-------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:ALLO_IGRIB',0,ZHOOK_HANDLE)
IF(YDR%LGRIB) THEN
  IF(PRESENT(KLENGRB)) THEN
    ILENGRB = KLENGRB
  ELSEIF(YDR%LINTONLY) THEN
    ILENGRB = YIOS%IMAXGRID+11000
  ELSE    
    SELECT CASE(YDR%IDATA_TYPE)
    CASE(JPSPEC)
      ILENGRB = YDR%YSPF%ISPEC2G+11000
    CASE(JPGP)
      IF(YDR%LATLONG) THEN
        ILENGRB = YIOS%IMAXGRID+11000
      ELSE
        IF(LECMWF)THEN
!         ILENGRB = YDR%YGPF%IGPTOTG+8000
!          ILENGRB = YDR%YGPF%IGPTOTG+11000
!          ILENGRB = YDR%YGPF%IGPTOTG+18000
          ILENGRB = YDR%YGPF%IGPTOTG+36000
!
! WARNING : modification provisoire
!
        ELSE
          ILENGRB = JPMAXGRID+8000
        ENDIF
      ENDIF
    CASE DEFAULT
      ILENGRB = YDR%YGPF%IGPTOTG+11000
    END SELECT
  ENDIF

  IF(ALLOCATED(NGRIBD)) THEN
    IF(SIZE(NGRIBD) < ILENGRB*KFIELDS) THEN
      DEALLOCATE(NGRIBD)
      ALLOCATE(NGRIBD(ILENGRB*MAX(YDR%NCHUNKSIZE,KFIELDS)))
      DEALLOCATE(NGRIBMSG)
      ALLOCATE(NGRIBMSG(ILENGRB+1))
    ENDIF
  ELSE
    ALLOCATE(NGRIBD(ILENGRB*MAX(YDR%NCHUNKSIZE,KFIELDS)))
    ALLOCATE(NGRIBMSG(ILENGRB+1))
  ENDIF
  IF(ALLOCATED(NGSTA)) THEN
    IF(SIZE(NGSTA) < KFIELDS+1) THEN
      DEALLOCATE(NGSTA)
      DEALLOCATE(LGRIBDONE)
      ALLOCATE(NGSTA(MAX(YDR%NCHUNKSIZE,KFIELDS)+1))
      ALLOCATE(LGRIBDONE(MAX(YDR%NCHUNKSIZE,KFIELDS)+1))
    ENDIF
  ELSE
    ALLOCATE(NGSTA(MAX(YDR%NCHUNKSIZE,KFIELDS)+1))
    ALLOCATE(LGRIBDONE(MAX(YDR%NCHUNKSIZE,KFIELDS)+1))
  ENDIF
  NGSTA(1) = 1
  NGSTA(2:) = -HUGE(NGSTA(1))
  LGRIBDONE(:) = .FALSE.
ENDIF
IF (LHOOK) CALL DR_HOOK('IOSTREAM_MIX:ALLO_IGRIB',1,ZHOOK_HANDLE)

END SUBROUTINE ALLO_IGRIB
