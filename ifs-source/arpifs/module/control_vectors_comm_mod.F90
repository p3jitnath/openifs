SUBROUTINE GET_CVINDEX(YDGEOMETRY,YDCV, KINDX)
  ! ym: routine not used in CY46. To be updated with LENSCV if needed.
USE GEOMETRY_MOD , ONLY : GEOMETRY
TYPE(GEOMETRY)      , INTENT(IN)  :: YDGEOMETRY
TYPE(CONTROL_VECTOR), INTENT(IN)  :: YDCV
INTEGER(KIND=JPIM)  , INTENT(OUT) :: KINDX(:)

TYPE(SPECTRAL_FIELD) :: YLSP
INTEGER(KIND=JPIM) :: JA,JB,IA,IB,II,JF,JFOFF,JM,IM,ILEN,JSOFF,JN,IL,JL,JJ,&
                    & IFOFF,JCOMP
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('CONTROL_VECTORS_COMM_MOD:GET_CVINDEX',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM=>YDGEOMETRY%YRDIM,YDDIMV=>YDGEOMETRY%YRDIMV,YDGEM=>YDGEOMETRY%YRGEM, YDMP=>YDGEOMETRY%YRMP)
ASSOCIATE(NFLEVL=>YDDIMV%NFLEVL, NFLEVG=>YDDIMV%NFLEVG, &
 & NPROMA=>YDDIM%NPROMA, &
 & RMUCEN=>YDGEM%RMUCEN, &
 & NPTRLL=>YDMP%NPTRLL, NUMLL=>YDMP%NUMLL)

IF (SIZE(KINDX)/=YDCV%NSIZEL) CALL ABOR1('GET_CVINDEX: kindx wrong size')
IF (YDCV%INITCV%LWAVELETJB)  CALL ABOR1('GET_CVINDEX: not yet coded for wavelet Jb')

II=0
JFOFF=0
CALL PE2SET(MYPROC,IA,IB,JA,JB)

! initial condition
! -----------------
IF (YDCV%LINITCV) THEN
  CALL INQ_SPEC(YLSP,YDCV%STRUCT%NFLEVL,YDCV%STRUCT%NFLEVG,&
        & YDCV%STRUCT%NUMP,YDCV%STRUCT%MYMS,YDCV%NSMAX,YDCV%NMSMAX,&
        & YDCV%STRUCT%NALLMS,YDCV%STRUCT%NPTRMS,YDCV%STRUCT%NUMLL,&
        & YDCV%STRUCT%NPTRLL,YDCV%STRUCT%NPSURF,YDCV%NS3D,YDCV%NS2D)

  IF (.NOT.LELAM) THEN
    DO JF=1,YLSP%NS2D
      DO JM=1,YLSP%NUMP
        IM=YLSP%MS_PROC(JM,JA)
        ILEN=2*(YLSP%NSMAX-IM+1)
        JSOFF=JFOFF+YLSP%NASM0G(IM)-1
        DO JN=1,ILEN
          II=II+1
          KINDX(II)=JSOFF+JN
        ENDDO
      ENDDO
      JFOFF=JFOFF+YLSP%NSPEC2G
    ENDDO
  ELSE
    DO JF=1,YLSP%NS2D
      DO JM=1,YLSP%NUMP
        IM=YLSP%MS_PROC(JM,JA)
        ILEN=YLSP%NCPL4M(IM)
        JSOFF=JFOFF+YLSP%NESM0G(IM)-1
        DO JN=1,ILEN
          II=II+1
          KINDX(II)=JSOFF+JN
        ENDDO
      ENDDO
      JFOFF=JFOFF+YLSP%NSPEC2G
    ENDDO
  ENDIF
  JFOFF=YLSP%NSPEC2G*YLSP%NS2G
  IF (.NOT.LELAM) THEN
    DO JF=1,YLSP%NS3D
      DO JM=1,YLSP%NUMP
        IM=YLSP%MS_PROC(JM,JA)
        ILEN=2*(YLSP%NSMAX-IM+1)
        JSOFF=JFOFF+(YLSP%NASM0G(IM)-1)*YLSP%NFLEVG
        DO JN=1,ILEN
          DO JL=1,YLSP%NFLEVL
            IL=YLSP%NPTRLL(JB)+JL-1
            II=II+1
            KINDX(II)=JSOFF+IL
          ENDDO
          JSOFF=JSOFF+YLSP%NFLEVG
        ENDDO
      ENDDO
      JFOFF=JFOFF+YLSP%NSPEC2G*YLSP%NFLEVG
    ENDDO
  ELSE
    DO JF=1,YLSP%NS3D
      DO JM=1,YLSP%NUMP
        IM=YLSP%MS_PROC(JM,JA)
        ILEN=YLSP%NCPL4M(IM)
        JSOFF=JFOFF+(YLSP%NESM0G(IM)-1)*YLSP%NFLEVG
        DO JN=1,ILEN
          DO JL=1,YLSP%NFLEVL
            IL=YLSP%NPTRLL(JB)+JL-1
            II=II+1
            KINDX(II)=JSOFF+IL
          ENDDO
          JSOFF=JSOFF+YLSP%NFLEVG
        ENDDO
      ENDDO
      JFOFF=JFOFF+YLSP%NSPEC2G*YLSP%NFLEVG
    ENDDO
  ENDIF
  CALL DEALLOCATE_SPEC(YLSP)
ENDIF

! model error if needed
! ---------------------
IF (YDCV%NMODERR>0) THEN
  CALL INQ_SPEC(YLSP,YDCV%STRUCT%NFLEVL,YDCV%STRUCT%NFLEVG,&
        & YDCV%STRUCT%NUMP,YDCV%STRUCT%MYMS,YDCV%NSERR,YDCV%NSERR,&
        & YDCV%STRUCT%NALLMS,YDCV%STRUCT%NPTRMS,YDCV%STRUCT%NUMLL,&
        & YDCV%STRUCT%NPTRLL,YDCV%STRUCT%NPSURF,YDCV%NVE3D,YDCV%NVE2D)

  DO JCOMP=1,YDCV%NMODERR
    IFOFF=JFOFF
    IF (.NOT.LELAM) THEN
      DO JF=1,YLSP%NS2D
        DO JM=1,YLSP%NUMP
          IM=YLSP%MS_PROC(JM,JA)
          ILEN=2*(YLSP%NSMAX-IM+1)
          JSOFF=IFOFF+YLSP%NASM0G(IM)-1
          DO JN=1,ILEN
            II=II+1
            KINDX(II)=JSOFF+JN
          ENDDO
        ENDDO
        IFOFF=IFOFF+YLSP%NSPEC2G
      ENDDO
      JFOFF=JFOFF+YLSP%NSPEC2G*YLSP%NS2G
      DO JF=1,YLSP%NS3D
        DO JM=1,YLSP%NUMP
          IM=YLSP%MS_PROC(JM,JA)
          ILEN=2*(YLSP%NSMAX-IM+1)
          JSOFF=JFOFF+(YLSP%NASM0G(IM)-1)*YLSP%NFLEVG
          DO JN=1,ILEN
            DO JL=1,YLSP%NFLEVL
              IL=YLSP%NPTRLL(JB)+JL-1
              II=II+1
              KINDX(II)=JSOFF+IL
            ENDDO
            JSOFF=JSOFF+YLSP%NFLEVG
          ENDDO
        ENDDO
        JFOFF=JFOFF+YLSP%NSPEC2G*YLSP%NFLEVG
      ENDDO
    ELSE
      DO JF=1,YLSP%NS2D
        DO JM=1,YLSP%NUMP
          IM=YLSP%MS_PROC(JM,JA)
          ILEN=YLSP%NCPL4M(IM)
          JSOFF=IFOFF+YLSP%NESM0G(IM)-1
          DO JN=1,ILEN
            II=II+1
            KINDX(II)=JSOFF+JN
          ENDDO
        ENDDO
        IFOFF=IFOFF+YLSP%NSPEC2G
      ENDDO
      JFOFF=JFOFF+YLSP%NSPEC2G*YLSP%NS2G
      DO JF=1,YLSP%NS3D
        DO JM=1,YLSP%NUMP
          IM=YLSP%MS_PROC(JM,JA)
          ILEN=YLSP%NCPL4M(IM)
          JSOFF=JFOFF+(YLSP%NESM0G(IM)-1)*YLSP%NFLEVG
          DO JN=1,ILEN
            DO JL=1,YLSP%NFLEVL
              IL=YLSP%NPTRLL(JB)+JL-1
              II=II+1
              KINDX(II)=JSOFF+IL
            ENDDO
            JSOFF=JSOFF+YLSP%NFLEVG
          ENDDO
        ENDDO
        JFOFF=JFOFF+YLSP%NSPEC2G*YLSP%NFLEVG
      ENDDO
    ENDIF
  ENDDO
  CALL DEALLOCATE_SPEC(YLSP)
ENDIF

! TOVS variable if needed
! -----------------------
IF (YDCV%LTOVSCV) THEN
  DO JF=1,YDCV%STRUCT%NVATOVV
    DO JJ=1,YDCV%STRUCT%NLENTOVSCVP(MYPROC)
      II=II+1
      KINDX(II)=JFOFF+YDCV%STRUCT%NVATOVOFF(MYPROC)+JJ
    ENDDO
    JFOFF=JFOFF+YDCV%STRUCT%NLENTOVSCVG
  ENDDO
ENDIF
 
! LAM variable if needed
! -----------------------
IF (YDCV%LAM1D) THEN
  DO JF=1,YDCV%NS1D
    DO JL=1,NFLEVL
      IL=NPTRLL(JB)-1+JL
      II=II+1
      KINDX(II)=JFOFF+IL
    ENDDO
    JFOFF=JFOFF+NFLEVG
  ENDDO
ENDIF

! PARAM variable if needed
! ------------------------
IF (YDCV%NPARA>0) THEN
  DO JF=1,YDCV%NPARA
    II=II+1
    KINDX(II)=JFOFF+JF
  ENDDO
  JFOFF=JFOFF+YDCV%NPARA
ENDIF

! PARAM (ECV) variable if needed
! ------------------------
IF (YDCV%NPECV>0) THEN
  DO JF=1,YDCV%NPECV
    II=II+1
    KINDX(II)=JFOFF+JF
  ENDDO
  JFOFF=JFOFF+YDCV%NPECV
ENDIF

IF (II/=YDCV%NSIZEL) CALL ABOR1('GET_CVINDEX: ii /= nsizel')

END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('CONTROL_VECTORS_COMM_MOD:GET_CVINDEX',1,ZHOOK_HANDLE)
END SUBROUTINE GET_CVINDEX

