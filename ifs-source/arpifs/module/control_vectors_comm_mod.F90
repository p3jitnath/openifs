SUBROUTINE MULTISCATTER_CTLVEC(YDGEOMETRY,YDCV, KPROC, PCV, KSTART, KEND)

!     Purpose.
!     --------
!       Distribute control variables from multiple PE.

!     Author.
!     -------
!       Tomas Wilhelmsson *ECMWF*

!     Modifications.
!     --------------
!       Original  : 09-08-28
! ----------------------------------------------

USE GEOMETRY_MOD , ONLY : GEOMETRY
TYPE(GEOMETRY)      ,INTENT(IN)    :: YDGEOMETRY
INTEGER(KIND=JPIM)  ,INTENT(IN)    :: KSTART, KEND 
TYPE(CONTROL_VECTOR),INTENT(INOUT) :: YDCV(KSTART:KEND)
INTEGER(KIND=JPIM)  ,INTENT(IN)    :: KPROC(KSTART:KEND)
REAL(KIND=JPRB)     ,INTENT(IN)    :: PCV(:)

REAL(KIND=JPRB), ALLOCATABLE :: ZSENDBUF(:),ZRECVBUF(:)
INTEGER(KIND=JPIM) :: ISENDCOUNTS(NPROC), IRECVCOUNTS(NPROC)
INTEGER(KIND=JPIM) :: ILENS(NPROC,KSTART:KEND),IDISPS(NPROC)
INTEGER(KIND=JPIM) :: ILENG(KSTART:KEND),JP,JJ,J,ICNT
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('CONTROL_VECTORS_COMM_MOD:MULTISCATTER_CTLVEC',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM=>YDGEOMETRY%YRDIM,YDDIMV=>YDGEOMETRY%YRDIMV,YDGEM=>YDGEOMETRY%YRGEM, YDMP=>YDGEOMETRY%YRMP)
ASSOCIATE(NFLEVL=>YDDIMV%NFLEVL, NFLEVG=>YDDIMV%NFLEVG, &
 & NPROMA=>YDDIM%NPROMA, &
 & RMUCEN=>YDGEM%RMUCEN, &
 & NPTRLL=>YDMP%NPTRLL, NUMLL=>YDMP%NUMLL)

CALL GSTATS(257,0)

DO JJ = KSTART, KEND
  IF (MYPROC==KPROC(JJ) .AND. SIZE(PCV)/=YDCV(JJ)%NSIZEG) THEN
    WRITE (NULERR,*) 'jj=',JJ,'SIZE(pcv)=',SIZE(PCV),' ydcv(jj)%nsizeg=',YDCV(JJ)%NSIZEG
    CALL ABOR1('MULTISCATTER_CTLVEC: pcv wrong size')
  ENDIF

  ! Set indices and allocate sendbuffer
  ! -----------------------------------
  CALL GET_CVLENGTHS(YDGEOMETRY,YDCV(JJ), ILENS(:,JJ))

  ILENG(JJ)=SUM(ILENS(:,JJ)) 

  IF (MYPROC==KPROC(JJ)) THEN
    ALLOCATE(ZSENDBUF(ILENG(JJ)))
    DO JP = 1, NPROC
       ISENDCOUNTS(JP) = ILENS(JP,JJ)
    ENDDO

    IDISPS(1)=0
    DO JP=2,NPROC
       IDISPS(JP)=IDISPS(JP-1)+ILENS(JP-1,JJ)
    ENDDO
  
    ! Pack data on root processor
    ! ---------------------------
    CALL GSTATS(1847,0)
    CALL PACK_CV(YDGEOMETRY,YDCV(JJ),PCV,ILENG(JJ),ILENS(:,JJ),IDISPS,ZSENDBUF)
    CALL GSTATS(1847,1)
  ENDIF
ENDDO

IF (.NOT. ALLOCATED(ZSENDBUF)) THEN
  ALLOCATE(ZSENDBUF(1))
  ISENDCOUNTS(1:NPROC) = 0
ENDIF

! Allocate receive buffer
! -----------------------
ALLOCATE(ZRECVBUF(SUM(ILENS(MYPROC,:))))

IRECVCOUNTS(1:NPROC) = 0
DO JJ = KSTART, KEND
  IRECVCOUNTS(KPROC(JJ)) = SIZE(YDCV(JJ)%DATA)
ENDDO

! Exchange data
! -------------
CALL GSTATS_BARRIER(705)
CALL GSTATS(525,0)
CALL MPL_ALLTOALLV(ZSENDBUF, ISENDCOUNTS, ZRECVBUF, IRECVCOUNTS,&
                 & CDSTRING='CONTROL_VECTORS_COMM_MOD:MULTISCATTER_CTLVEC')  
CALL GSTATS(525,1)
CALL GSTATS_BARRIER2(705)

ICNT = 0
DO JJ = KSTART, KEND
  DO J=1, SIZE(YDCV(JJ)%DATA)
    ICNT = ICNT + 1
    YDCV(JJ)%DATA(J) = ZRECVBUF(ICNT)
  ENDDO
ENDDO

IF (SUM(ILENS(MYPROC,:))/=ICNT) CALL ABOR1('MULTISCATTER_CTLVEC: recvbuf wrong size')

DEALLOCATE(ZSENDBUF)
DEALLOCATE(ZRECVBUF)
CALL GSTATS(257,1)

END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('CONTROL_VECTORS_COMM_MOD:MULTISCATTER_CTLVEC',1,ZHOOK_HANDLE)
END SUBROUTINE MULTISCATTER_CTLVEC

