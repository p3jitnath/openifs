SUBROUTINE GATHER_CTLVEC(YDGEOMETRY,YDCV, KPROC, PCV)

!     Purpose.
!     --------
!       Gather control variable on one PE.

!     Author.
!     -------
!       Yannick Tremolet *ECMWF*

!     Modifications.
!     --------------
!       Original  : 02-08-07
! ----------------------------------------------

USE GEOMETRY_MOD , ONLY : GEOMETRY
TYPE(GEOMETRY)      , INTENT(IN)  :: YDGEOMETRY
TYPE(CONTROL_VECTOR), INTENT(IN)  :: YDCV
INTEGER(KIND=JPIM)  , INTENT(IN)  :: KPROC
REAL(KIND=JPRB)     , INTENT(OUT) :: PCV(:)

REAL(KIND=JPRB), ALLOCATABLE :: ZBUF(:)
INTEGER(KIND=JPIM) :: ILENR(NPROC),IDISPR(NPROC),ILENG,JP
