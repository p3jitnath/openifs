SUBROUTINE ALLGATHER_CVSECTION(YDGEOMETRY,YDCV, PSEC, KSTART, KEND)

!     Purpose.
!     --------
!       Gather part of the control variable on all PEs.
!       Replaces DVSECTION with a name that makes it explicit
!       that this is an expensive function.

!     Author.
!     -------
!       Yannick Tremolet

!     Modifications.
!     --------------
!       Original  : 02-08-12
! ----------------------------------------------

USE GEOMETRY_MOD , ONLY : GEOMETRY
TYPE(GEOMETRY)      , INTENT(IN)  :: YDGEOMETRY
TYPE(CONTROL_VECTOR), INTENT(IN)  :: YDCV
INTEGER(KIND=JPIM)  , INTENT(IN)  :: KSTART, KEND
REAL(KIND=JPRB)     , INTENT(OUT) :: PSEC(KSTART:KEND)
REAL(KIND=JPRB), ALLOCATABLE :: ZWORK(:)
INTEGER(KIND=JPIM) :: IV,IW,JV,JW,JJ,IT,JM,IM,JP,ISPLEN,JPT,IOFF1,IOFF3,IPT,IOMASTER,ITAG,ITO(1)
TYPE(SPECTRAL_FIELD) :: YLSP
REAL(KIND=JPRB), ALLOCATABLE :: ZLOCGP(:,:,:), ZGLOBGP(:,:)
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('CONTROL_VECTORS_COMM_MOD:ALLGATHER_CVSECTION',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM=>YDGEOMETRY%YRDIM,YDDIMV=>YDGEOMETRY%YRDIMV,YDGEM=>YDGEOMETRY%YRGEM, YDMP=>YDGEOMETRY%YRMP)
ASSOCIATE(NFLEVL=>YDDIMV%NFLEVL, NFLEVG=>YDDIMV%NFLEVG, &
 & NPROMA=>YDDIM%NPROMA, &
 & RMUCEN=>YDGEM%RMUCEN, &
 & NPTRLL=>YDMP%NPTRLL, NUMLL=>YDMP%NUMLL)

IF (KSTART<1)       CALL ABOR1('ALLGATHER_CVSECTION: kstart<1')
IF (KEND>YDCV%NSIZEG) CALL ABOR1('ALLGATHER_CVSECTION: kend>sizeg')
IF (KSTART>KEND   ) CALL ABOR1('ALLGATHER_CVSECTION: kstart>kend')

IF (YDCV%INITCV%LWAVELETJB)  THEN

! We arbitrarily define the 'first' elements of the control vector to
! be the first elements of the first global field.

  IF (KSTART>JB_STRUCT%GRID_DEFINITION(1)%NGPTOTG)&
    & CALL ABOR1('ALLGATHER_CVSECTION: kstart>GRID_DEFINITION(1)%NGPTOTG .AND. ydcv%lwaveletjb')

  IF (KEND>JB_STRUCT%GRID_DEFINITION(1)%NGPTOTG)&
    & CALL ABOR1('ALLGATHER_CVSECTION: kend>GRID_DEFINITION(1)%NGPTOTG .AND. ydcv%lwaveletjb')

  IF (YDCV%NSCALES/=YDCV%STRUCT%NSCALES) CALL ABOR1 ('ALLGATHER_CVSECTION: ydcv%nscales/=ydcv%struct%NSCALES')

  ALLOCATE (ZLOCGP(JB_STRUCT%GRID_DEFINITION(1)%NGPTOT,1,1))
  ALLOCATE (ZGLOBGP(JB_STRUCT%GRID_DEFINITION(1)%NGPTOTG,1))

  IOFF1=0
  IOFF3=0

  DO JPT=1,JB_STRUCT%GRID_DEFINITION(1)%NGPTOT
    IPT=YDCV%INITCV%IBGN_SCALE(1)+IOFF1+IOFF3
    ZLOCGP(JPT,1,1) = YDCV%INITCV%DATA(IPT)
    IOFF1=IOFF1+1
    IF (MOD(IOFF1,NPROMA)==0) THEN
      IOFF1=0
      IOFF3=IOFF3 + NPROMA*(NFLEVG*YDCV%NS3D+YDCV%NS2D)
    ENDIF
  ENDDO

  IOMASTER=1
  ITO(1)=IOMASTER
  IF (NPROC==1) THEN
    PSEC(KSTART:KEND) = ZLOCGP(KSTART:KEND,1,1) 
  ELSE
    CALL GSTATS_BARRIER(722)
!!!    CALL GSTATS(537,0)
    CALL GATH_GRID(PGPG=ZGLOBGP,PGP=ZLOCGP,KTO=ITO,KFGATHG=1,&
     & KRESOL=JB_STRUCT%GRID_DEFINITION(1)%NRESOL_ID)
     PSEC(KSTART:KEND) = ZGLOBGP(KSTART:KEND,1) 
    ITAG=99
    CALL MPL_BROADCAST(PSEC(KSTART:KEND),KROOT=IOMASTER,KTAG=ITAG,&
     & CDSTRING='CONTROL_VECTORS_COMM_MOD:ALLGATHER_CVSECTION')
!!!    CALL GSTATS(537,1)
    CALL GSTATS_BARRIER2(722)
  ENDIF

  DEALLOCATE (ZLOCGP)
  DEALLOCATE (ZGLOBGP)

ELSE

! Using the fact the the first elements of the control vector
! are the M=0 components of the first 2D field. They are 2 times
! NSMAX+1 elements on the same processor. In current 4D-Var this
! is more than we need.
! In Aladin, this is 4 times NSMAX+1 for wavenumber M=0.

  IF (LELAM) THEN
    ISPLEN=4*(YDCV%NSMAX+1)
  ELSE
    ISPLEN=2*(YDCV%NSMAX+1)
  ENDIF

  IF (KEND<=ISPLEN) THEN
    CALL INQ_SPEC(YLSP,YDCV%STRUCT%NFLEVL,YDCV%STRUCT%NFLEVG,&
        & YDCV%STRUCT%NUMP,YDCV%STRUCT%MYMS,YDCV%NSMAX,YDCV%NMSMAX,&
        & YDCV%STRUCT%NALLMS,YDCV%STRUCT%NPTRMS,YDCV%STRUCT%NUMLL,&
        & YDCV%STRUCT%NPTRLL,YDCV%STRUCT%NPSURF,YDCV%NS3D,YDCV%NS2D)

    JJ=0
    DO IV=1,NPRTRV
      IF (YLSP%NS2L(IV)>0) THEN
        JV=IV
        JJ=JJ+1
      ENDIF
    ENDDO
    IF (JJ/=1) CALL ABOR1('Error finding jv')
    JJ=0
    DO IW=1,NPRTRW
      DO JM=1,YLSP%NUMPP(IW)
        IM=YLSP%MS_PROC(JM,IW)
        IF (IM==0) THEN
          JW=IW
          JJ=JJ+1
        ENDIF
      ENDDO
    ENDDO
    IF (JJ/=1) CALL ABOR1('Error finding jw')
    CALL DEALLOCATE_SPEC(YLSP)
  
    CALL SET2PE(JP,0,0,JW,JV)
  
    IF (MYPROC==JP) THEN
      PSEC(KSTART:KEND)=YDCV%DATA(KSTART:KEND)
    ENDIF
    IT=MT_DISTRIBUTED_VECTOR
    CALL GSTATS_BARRIER(722)
    CALL GSTATS(615,0)
    CALL MPL_BROADCAST (PSEC(KSTART:KEND),KROOT=JP,KTAG=IT,&
                      & CDSTRING='CONTROL_VECTORS_COMM_MOD:ALLGATHER_CVSECTION')
    CALL GSTATS(615,1)
    CALL GSTATS_BARRIER2(722)
  ELSE
    WRITE(NULOUT,*)'WARNING: CVSECTION using inefficient version, '//&
     & 'kstart, kend=',KSTART,KEND  
    ALLOCATE(ZWORK(YDCV%NSIZEG))
    CALL ALLGATHER_CTLVEC(YDGEOMETRY,YDCV, ZWORK)
    PSEC(KSTART:KEND)=ZWORK(KSTART:KEND)
    DEALLOCATE(ZWORK)
  ENDIF
ENDIF

END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('CONTROL_VECTORS_COMM_MOD:ALLGATHER_CVSECTION',1,ZHOOK_HANDLE)
END SUBROUTINE ALLGATHER_CVSECTION

