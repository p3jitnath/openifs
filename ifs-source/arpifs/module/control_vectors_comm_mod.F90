SUBROUTINE GET_CVLENGTHS(YDGEOMETRY,YDCV, KLEN)

! Get length of local component of ydcv on each proc.

USE GEOMETRY_MOD , ONLY : GEOMETRY
TYPE(GEOMETRY)      , INTENT(IN)  :: YDGEOMETRY
TYPE(CONTROL_VECTOR), INTENT(IN)  :: YDCV
INTEGER(KIND=JPIM)  , INTENT(OUT) :: KLEN(NPROC)

TYPE(SPECTRAL_FIELD) :: YLSP
INTEGER(KIND=JPIM) :: JP,JA,JB,IA,IB,ISPEC2,JM,IM,ILEN,IMYLEN
INTEGER(KIND=JPIM) :: ITEMP

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE


IF (LHOOK) CALL DR_HOOK('CONTROL_VECTORS_COMM_MOD:GET_CVLENGTHS',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM=>YDGEOMETRY%YRDIM,YDDIMV=>YDGEOMETRY%YRDIMV,YDGEM=>YDGEOMETRY%YRGEM, YDMP=>YDGEOMETRY%YRMP)
ASSOCIATE(NFLEVL=>YDDIMV%NFLEVL, NFLEVG=>YDDIMV%NFLEVG, &
 & NPROMA=>YDDIM%NPROMA, &
 & RMUCEN=>YDGEM%RMUCEN, &
 & NPTRLL=>YDMP%NPTRLL, NUMLL=>YDMP%NUMLL)

DO JP=1,NPROC
  CALL PE2SET(JP,IA,IB,JA,JB)

! initial condition
! -----------------
  IF (YDCV%LINITCV) THEN
    IF (YDCV%INITCV%LWAVELETJB) THEN
      IF (YDCV%NSCALES==YDCV%STRUCT%NSCALES) THEN
        KLEN(JP) = NPROMA*(NFLEVG*YDCV%NS3D+YDCV%NS2D)*SUM((YDCV%STRUCT%NGPTOTL(:,IA,IB)+NPROMA-1)/NPROMA)
      ELSE
        ITEMP=3+YDCV%STRUCT%NSCALES-YDCV%NSCALES
        KLEN(JP) = NPROMA*(NFLEVG*YDCV%NS3D+YDCV%NS2D)&
                & *( 2*((YDCV%STRUCT%NGPTOTL(2+YDCV%STRUCT%NSCALES-YDCV%NSCALES,IA,IB)+NPROMA-1)/NPROMA)&
                &   +SUM((YDCV%STRUCT%NGPTOTL(ITEMP:,IA,IB)+NPROMA-1)/NPROMA))
      ENDIF

      IF (JP==MYPROC) THEN
        IF (YDCV%NSCALES==YDCV%STRUCT%NSCALES) THEN
          IMYLEN = NPROMA*(NFLEVG*YDCV%NS3D+YDCV%NS2D)*SUM(YDCV%STRUCT%NBLOCKSL(:))
        ELSE
          ITEMP=3+YDCV%STRUCT%NSCALES-YDCV%NSCALES
          IMYLEN = NPROMA*(NFLEVG*YDCV%NS3D+YDCV%NS2D)&
                & *(2*YDCV%STRUCT%NBLOCKSL(2+YDCV%STRUCT%NSCALES-YDCV%NSCALES)+SUM(YDCV%STRUCT%NBLOCKSL(ITEMP:)))
        ENDIF
        IF (KLEN(JP)/=IMYLEN) THEN
          WRITE (NULOUT,*) 'GET_CVLENGTHS: Error calculating initicv lengths'
          WRITE (NULOUT,*) 'GET_CVLENGTHS: jp=',JP,' klen(jp)=',KLEN(JP),' imylen=',IMYLEN
          CALL ABOR1 ('GET_CVLENGTHS: Error calculating initicv lengths')
        ENDIF
      ENDIF
    ELSE
      CALL INQ_SPEC(YLSP,YDCV%STRUCT%NFLEVL,YDCV%STRUCT%NFLEVG,&
        & YDCV%STRUCT%NUMP,YDCV%STRUCT%MYMS,YDCV%NSMAX,YDCV%NMSMAX,&
        & YDCV%STRUCT%NALLMS,YDCV%STRUCT%NPTRMS,YDCV%STRUCT%NUMLL,&
        & YDCV%STRUCT%NPTRLL,YDCV%STRUCT%NPSURF,YDCV%NS3D,YDCV%NS2D)

      ISPEC2=0
      IF (.NOT.LELAM) THEN
        DO JM=1,YLSP%NUMPP(JA)
          IM=YLSP%MS_PROC(JM,JA)
          ILEN=YLSP%NSMAX-IM+1
          ISPEC2=ISPEC2+2*ILEN
        ENDDO
      ELSE
        DO JM=1,YLSP%NUMPP(JA)
          IM=YLSP%MS_PROC(JM,JA)
          ILEN=YLSP%NCPL4M(IM)
          ISPEC2=ISPEC2+ILEN
        ENDDO
      ENDIF
      KLEN(JP)=ISPEC2*YLSP%NS3D*YLSP%NUMLL(JB)   ! 3D fields
      KLEN(JP)=KLEN(JP)+ISPEC2*YLSP%NS2L(JB)     ! 2D fields
      CALL DEALLOCATE_SPEC(YLSP)
    ENDIF
  ELSE
    KLEN(JP)=0
 ENDIF
 
! enscv
! -----------
 IF (YDCV%LENSCV) THEN
    CALL INQ_SPEC(YLSP,YDCV%STRUCT_ENS%NFLEVL,YDCV%STRUCT_ENS%NFLEVG,&
         & YDCV%STRUCT_ENS%NUMP,YDCV%STRUCT_ENS%MYMS,YDCV%NSMAX_ENS,YDCV%NMSMAX_ENS,&
         & YDCV%STRUCT_ENS%NALLMS,YDCV%STRUCT_ENS%NPTRMS,YDCV%STRUCT_ENS%NUMLL,&
         & YDCV%STRUCT_ENS%NPTRLL,YDCV%STRUCT_ENS%NPSURF,YDCV%NENS,YDCV%NENS)
    ISPEC2=0
    IF (.NOT.LELAM) THEN
       DO JM=1,YLSP%NUMPP(JA)
          IM=YLSP%MS_PROC(JM,JA)
          ILEN=YLSP%NSMAX-IM+1
          ISPEC2=ISPEC2+2*ILEN
       ENDDO
    ELSE
       DO JM=1,YLSP%NUMPP(JA)
          IM=YLSP%MS_PROC(JM,JA)
          ILEN=YLSP%NCPL4M(IM)
          ISPEC2=ISPEC2+ILEN
       ENDDO
    ENDIF
    KLEN(JP)=KLEN(JP)+ISPEC2*YLSP%NS3D*YLSP%NUMLL(JB)   ! 3D fields
    KLEN(JP)=KLEN(JP)+ISPEC2*YLSP%NS2L(JB)     ! 2D fields
    CALL DEALLOCATE_SPEC(YLSP)
 ENDIF
 
! model error
! -----------
  IF (YDCV%NMODERR>0) THEN
    CALL INQ_SPEC(YLSP,YDCV%STRUCT%NFLEVL,YDCV%STRUCT%NFLEVG,&
        & YDCV%STRUCT%NUMP,YDCV%STRUCT%MYMS,YDCV%NSERR,YDCV%NSERR,&
        & YDCV%STRUCT%NALLMS,YDCV%STRUCT%NPTRMS,YDCV%STRUCT%NUMLL,&
        & YDCV%STRUCT%NPTRLL,YDCV%STRUCT%NPSURF,YDCV%NVE3D,YDCV%NVE2D)

    ISPEC2=0
    IF (.NOT.LELAM) THEN
      DO JM=1,YLSP%NUMPP(JA)
        IM=YLSP%MS_PROC(JM,JA)
        ILEN=YLSP%NSMAX-IM+1
        ISPEC2=ISPEC2+2*ILEN
      ENDDO
    ELSE
      DO JM=1,YLSP%NUMPP(JA)
        IM=YLSP%MS_PROC(JM,JA)
        ILEN=YLSP%NCPL4M(IM)
        ISPEC2=ISPEC2+ILEN
      ENDDO
    ENDIF
    ISPEC2 = ISPEC2 * YDCV%NMODERR
    KLEN(JP)=KLEN(JP)+ISPEC2*YLSP%NS3D*YLSP%NUMLL(JB)  ! 3D fields
    KLEN(JP)=KLEN(JP)+ISPEC2*YLSP%NS2L(JB)             ! 2D fields
    CALL DEALLOCATE_SPEC(YLSP)
  ENDIF

! TOVS variable
! -------------
  IF (YDCV%LTOVSCV) THEN
    KLEN(JP)=KLEN(JP)+YDCV%STRUCT%NVATOVV*YDCV%STRUCT%NLENTOVSCVP(JP)
  ENDIF
 
! LAM variable
! ------------
  IF (YDCV%LAM1D) THEN
    KLEN(JP)=KLEN(JP)+YDCV%NS1D*NUMLL(JB)
  ENDIF

! PARAM variable
! --------------
  KLEN(JP)=KLEN(JP)+YDCV%NPARA

! PARAM variable (ECV)
! --------------
  KLEN(JP)=KLEN(JP)+YDCV%NPECV

ENDDO

END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('CONTROL_VECTORS_COMM_MOD:GET_CVLENGTHS',1,ZHOOK_HANDLE)
END SUBROUTINE GET_CVLENGTHS

! ------------------------------------------------------------------

SUBROUTINE PACK_CV(YDGEOMETRY,YDCV,PCV,KBUF,KLENS,KDISPS,PBUF)

! Arrange global cv in send buffer.

USE GEOMETRY_MOD , ONLY : GEOMETRY
TYPE(GEOMETRY)      , INTENT(IN)  :: YDGEOMETRY
TYPE(CONTROL_VECTOR), INTENT(IN)  :: YDCV
INTEGER(KIND=JPIM)  , INTENT(IN)  :: KBUF
INTEGER(KIND=JPIM)  , INTENT(IN)  :: KLENS(NPROC),KDISPS(NPROC)
REAL(KIND=JPRB)     , INTENT(IN)  :: PCV(YDCV%NSIZEG)
REAL(KIND=JPRB)     , INTENT(OUT) :: PBUF(KBUF)

TYPE(SPECTRAL_FIELD) :: YLSP, YLSP_ENS
INTEGER(KIND=JPIM) :: II(NPROC),JFOFF(NPROC)
INTEGER(KIND=JPIM) :: JP,JA,JB,IA,IB,JF,JM,IM,ILEN,JSOFF,JN,IL,JL,&
                    & JJ,IFOFF,JSCALE, JROF, ICEND, IBL, INFLDS, IGP,&
                    & IGPTOT, IAAAARRRRGH, IOFF, IGL2, IGL1, ILAST, JLON,&
                    & JGL, ILOFF, IGLOFF, JKGLO, ISCALE, JCOMP
INTEGER(KIND=JPIM), ALLOCATABLE :: IVATOVINDXINV(:)
REAL(KIND=JPRB), ALLOCATABLE :: ZGPLOC(:)
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('CONTROL_VECTORS_COMM_MOD:PACK_CV',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM=>YDGEOMETRY%YRDIM,YDDIMV=>YDGEOMETRY%YRDIMV,YDGEM=>YDGEOMETRY%YRGEM, YDMP=>YDGEOMETRY%YRMP)
ASSOCIATE(NFLEVL=>YDDIMV%NFLEVL, NFLEVG=>YDDIMV%NFLEVG, &
 & NPROMA=>YDDIM%NPROMA, &
 & RMUCEN=>YDGEM%RMUCEN, &
 & NPTRLL=>YDMP%NPTRLL, NUMLL=>YDMP%NUMLL)

IF (YDCV%LTOVSCV) THEN
  ALLOCATE(IVATOVINDXINV(YDCV%STRUCT%NLENTOVSCVG))
  DO JJ = 1, YDCV%STRUCT%NLENTOVSCVG
    IVATOVINDXINV(NVATOVINDX(JJ)) = JJ
  ENDDO
ENDIF

! Initialize II and JFOFF
!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1)&
!$OMP& PRIVATE(JP)
DO JP=1,NPROC
  II(JP)=KDISPS(JP)
  JFOFF(JP)=0
ENDDO
!$OMP END PARALLEL DO

! Pack initial condition (but not if wavelet Jb)
! ------------------------------------------------
IF (YDCV%LINITCV) THEN
  IF (.NOT. YDCV%INITCV%LWAVELETJB) THEN
    CALL INQ_SPEC(YLSP,YDCV%STRUCT%NFLEVL,YDCV%STRUCT%NFLEVG,&
        & YDCV%STRUCT%NUMP,YDCV%STRUCT%MYMS,YDCV%NSMAX,YDCV%NMSMAX,&
        & YDCV%STRUCT%NALLMS,YDCV%STRUCT%NPTRMS,YDCV%STRUCT%NUMLL,&
        & YDCV%STRUCT%NPTRLL,YDCV%STRUCT%NPSURF,YDCV%NS3D,YDCV%NS2D)
 ENDIF
 IF (YDCV%LENSCV) THEN
    CALL INQ_SPEC(YLSP_ENS,YDCV%STRUCT_ENS%NFLEVL,YDCV%STRUCT_ENS%NFLEVG,&
         & YDCV%STRUCT_ENS%NUMP,YDCV%STRUCT_ENS%MYMS,YDCV%NSMAX_ENS,YDCV%NMSMAX_ENS,&
         & YDCV%STRUCT_ENS%NALLMS,YDCV%STRUCT_ENS%NPTRMS,YDCV%STRUCT_ENS%NUMLL,&
         & YDCV%STRUCT_ENS%NPTRLL,YDCV%STRUCT_ENS%NPSURF,YDCV%NENS,YDCV%NENS)
 ENDIF
!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1)&
!$OMP& PRIVATE(JP,IA,IB,JA,JB,INFLDS,JSCALE,ISCALE,IGPTOT,ZGPLOC,JF,&
!$OMP& IGLOFF,IGL1,IGL2,IOFF,ILAST,JJ,ILEN,ILOFF,JGL,JLON,IAAAARRRRGH,IGP,JKGLO,&
!$OMP& ICEND,IBL,JROF,JM,IM,JSOFF,JN,JL,IL)
  DO JP=1,NPROC
    CALL PE2SET(JP,IA,IB,JA,JB)

    IF (YDCV%INITCV%LWAVELETJB) THEN

      INFLDS = NFLEVG*YDCV%NS3D+YDCV%NS2D

      DO JSCALE=1,YDCV%NSCALES
        ISCALE=MAX(JSCALE,2)+YDCV%STRUCT%NSCALES-YDCV%NSCALES

        IGPTOT=JB_STRUCT%GRID_DEFINITION(ISCALE)%NGPTOTL(IA,IB)
        ALLOCATE (ZGPLOC(IGPTOT))

        DO JF=1,INFLDS
          IGLOFF = JB_STRUCT%GRID_DEFINITION(ISCALE)%NPTRFRSTLAT(IA)
          IGL1 =   JB_STRUCT%GRID_DEFINITION(ISCALE)%NFRSTLAT(IA)
          IGL2 =   JB_STRUCT%GRID_DEFINITION(ISCALE)%NLSTLAT(IA)
          IOFF = 0

          IF (IA>1) THEN
            IF( JB_STRUCT%GRID_DEFINITION(ISCALE)%NLSTLAT(IA-1) == JB_STRUCT%GRID_DEFINITION(ISCALE)%NFRSTLAT(IA) )THEN
              ILAST = JB_STRUCT%GRID_DEFINITION(ISCALE)%NLSTLAT(IA-1)-1
            ELSE
              ILAST = JB_STRUCT%GRID_DEFINITION(ISCALE)%NLSTLAT(IA-1)
            ENDIF
            DO JJ=JB_STRUCT%GRID_DEFINITION(ISCALE)%NFRSTLAT(1),ILAST
              IOFF = IOFF+JB_STRUCT%GRID_DEFINITION(ISCALE)%NLOENG(JJ)
            ENDDO
          ENDIF
  
          ILEN = 0
          ILOFF = 0
          DO JGL=IGL1,IGL2
            DO JLON=1,JB_STRUCT%GRID_DEFINITION(ISCALE)%NONL(IGLOFF+JGL-IGL1,IB)
              IAAAARRRRGH = IOFF+ILOFF+JB_STRUCT%GRID_DEFINITION(ISCALE)%NSTA(IGLOFF+JGL-IGL1,IB)+JLON-1&
                           & + JFOFF(JP)
              ZGPLOC(ILEN+JLON) =PCV(IAAAARRRRGH)
            ENDDO
            ILEN = ILEN + JB_STRUCT%GRID_DEFINITION(ISCALE)%NONL(IGLOFF+JGL-IGL1,IB)
            ILOFF = ILOFF + JB_STRUCT%GRID_DEFINITION(ISCALE)%NLOENG(JGL)
          ENDDO

          IGP = 0
          DO JKGLO=1,IGPTOT,NPROMA
            ICEND=MIN(NPROMA,IGPTOT-JKGLO+1)
            IBL=(JKGLO-1)/NPROMA+1
            DO JROF=1,ICEND
              IGP = IGP + 1
              PBUF(II(JP)+JROF+NPROMA*(JF-1+INFLDS*(IBL-1))) = ZGPLOC(IGP)
            ENDDO
            DO JROF=ICEND+1,NPROMA
              PBUF(II(JP)+JROF+NPROMA*(JF-1+INFLDS*(IBL-1))) = 0.0_JPRB
            ENDDO
          ENDDO

!         increment the offset into the gathered control vector
          JFOFF(JP)=JFOFF(JP)+JB_STRUCT%GRID_DEFINITION(ISCALE)%NGPTOTG

        ENDDO

        DEALLOCATE (ZGPLOC)

!       increment the offset into the send buffer
        II(JP)=II(JP)+NPROMA*INFLDS*((IGPTOT+NPROMA-1)/NPROMA)
      ENDDO

    ELSE
      IF (.NOT.LELAM) THEN
        DO JF=1,YLSP%NS2L(JB)
          DO JM=1,YLSP%NUMPP(JA)
            IM=YLSP%MS_PROC(JM,JA)
            ILEN=2*(YLSP%NSMAX-IM+1)
            JSOFF=JFOFF(JP)+YLSP%NASM0G(IM)-1
            DO JN=1,ILEN
              II(JP)=II(JP)+1
              PBUF(II(JP))=PCV(JSOFF+JN)
            ENDDO
          ENDDO
          JFOFF(JP)=JFOFF(JP)+YLSP%NSPEC2G
        ENDDO
      ELSE
        DO JF=1,YLSP%NS2L(JB)
          DO JM=1,YLSP%NUMPP(JA)
            IM=YLSP%MS_PROC(JM,JA)
            ILEN=YLSP%NCPL4M(IM)
            JSOFF=JFOFF(JP)+YLSP%NESM0G(IM)-1
            DO JN=1,ILEN
              II(JP)=II(JP)+1
              PBUF(II(JP))=PCV(JSOFF+JN)
            ENDDO
          ENDDO
          JFOFF(JP)=JFOFF(JP)+YLSP%NSPEC2G
        ENDDO
      ENDIF
      JFOFF(JP)=YLSP%NSPEC2G*YLSP%NS2G
      IF (.NOT.LELAM) THEN
        DO JF=1,YLSP%NS3D
          DO JM=1,YLSP%NUMPP(JA)
            IM=YLSP%MS_PROC(JM,JA)
            ILEN=2*(YLSP%NSMAX-IM+1)
            JSOFF=JFOFF(JP)+(YLSP%NASM0G(IM)-1)*YLSP%NFLEVG
            DO JN=1,ILEN
              DO JL=1,YLSP%NUMLL(JB)
                IL=YLSP%NPTRLL(JB)+JL-1
                II(JP)=II(JP)+1
                PBUF(II(JP))=PCV(JSOFF+IL)
              ENDDO
              JSOFF=JSOFF+YLSP%NFLEVG
            ENDDO
          ENDDO
          JFOFF(JP)=JFOFF(JP)+YLSP%NSPEC2G*YLSP%NFLEVG
        ENDDO
      ELSE
        DO JF=1,YLSP%NS3D
          DO JM=1,YLSP%NUMPP(JA)
            IM=YLSP%MS_PROC(JM,JA)
            ILEN=YLSP%NCPL4M(IM)
            JSOFF=JFOFF(JP)+(YLSP%NESM0G(IM)-1)*YLSP%NFLEVG
            DO JN=1,ILEN
              DO JL=1,YLSP%NUMLL(JB)
                IL=YLSP%NPTRLL(JB)+JL-1
                II(JP)=II(JP)+1
                PBUF(II(JP))=PCV(JSOFF+IL)
              ENDDO
              JSOFF=JSOFF+YLSP%NFLEVG
            ENDDO
          ENDDO
          JFOFF(JP)=JFOFF(JP)+YLSP%NSPEC2G*YLSP%NFLEVG
        ENDDO
      ENDIF
    ENDIF
  ENDDO
!$OMP END PARALLEL DO

ENDIF

! Pack enscv if needed
! --------------------------
IF (YDCV%LENSCV) THEN
!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1)&
!$OMP& PRIVATE(JP,IA,IB,JA,JB,JCOMP,IFOFF,JF,JM,IM,ILEN,JSOFF,JN,JL,IL)
   DO JP=1,NPROC
      CALL PE2SET(JP,IA,IB,JA,JB)
      IFOFF=JFOFF(JP)
      IF (.NOT.LELAM) THEN
         DO JF=1,YLSP_ENS%NS2L(JB)
            DO JM=1,YLSP_ENS%NUMPP(JA)
               IM=YLSP_ENS%MS_PROC(JM,JA)
               ILEN=2*(YLSP_ENS%NSMAX-IM+1)
               JSOFF=IFOFF+YLSP_ENS%NASM0G(IM)-1
               DO JN=1,ILEN
                  II(JP)=II(JP)+1
                  PBUF(II(JP))=PCV(JSOFF+JN)
               ENDDO
            ENDDO
            IFOFF=IFOFF+YLSP_ENS%NSPEC2G
         ENDDO
         JFOFF(JP)=JFOFF(JP)+YLSP_ENS%NSPEC2G*YLSP_ENS%NS2G
         DO JF=1,YLSP_ENS%NS3D
            DO JM=1,YLSP_ENS%NUMPP(JA)
               IM=YLSP_ENS%MS_PROC(JM,JA)
               ILEN=2*(YLSP_ENS%NSMAX-IM+1)
               JSOFF=JFOFF(JP)+(YLSP_ENS%NASM0G(IM)-1)*YLSP_ENS%NFLEVG
               DO JN=1,ILEN
                  DO JL=1,YLSP_ENS%NUMLL(JB)
                     IL=YLSP_ENS%NPTRLL(JB)+JL-1
                     II(JP)=II(JP)+1
                     PBUF(II(JP))=PCV(JSOFF+IL)
                  ENDDO
                  JSOFF=JSOFF+YLSP_ENS%NFLEVG
               ENDDO
            ENDDO
            JFOFF(JP)=JFOFF(JP)+YLSP_ENS%NSPEC2G*YLSP_ENS%NFLEVG
         ENDDO
      ELSE
         DO JF=1,YLSP_ENS%NS2L(JB)
            DO JM=1,YLSP_ENS%NUMPP(JA)
               IM=YLSP_ENS%MS_PROC(JM,JA)
               ILEN=YLSP_ENS%NCPL4M(IM)
               JSOFF=IFOFF+YLSP_ENS%NESM0G(IM)-1
               DO JN=1,ILEN
                  II(JP)=II(JP)+1
                  PBUF(II(JP))=PCV(JSOFF+JN)
               ENDDO
            ENDDO
            IFOFF=IFOFF+YLSP_ENS%NSPEC2G
         ENDDO
         JFOFF(JP)=JFOFF(JP)+YLSP_ENS%NSPEC2G*YLSP_ENS%NS2G
         DO JF=1,YLSP_ENS%NS3D
            DO JM=1,YLSP_ENS%NUMPP(JA)
               IM=YLSP_ENS%MS_PROC(JM,JA)
               ILEN=YLSP_ENS%NCPL4M(IM)
               JSOFF=JFOFF(JP)+(YLSP_ENS%NESM0G(IM)-1)*YLSP_ENS%NFLEVG
               DO JN=1,ILEN
                  DO JL=1,YLSP_ENS%NUMLL(JB)
                     IL=YLSP_ENS%NPTRLL(JB)+JL-1
                     II(JP)=II(JP)+1
                     PBUF(II(JP))=PCV(JSOFF+IL)
                  ENDDO
                  JSOFF=JSOFF+YLSP_ENS%NFLEVG
               ENDDO
            ENDDO
            JFOFF(JP)=JFOFF(JP)+YLSP_ENS%NSPEC2G*YLSP_ENS%NFLEVG
         ENDDO
      ENDIF
   ENDDO
!$OMP END PARALLEL DO
ENDIF

! Pack model error if needed
! --------------------------
IF (YDCV%NMODERR>0) THEN
  CALL INQ_SPEC(YLSP,YDCV%STRUCT%NFLEVL,YDCV%STRUCT%NFLEVG,&
    & YDCV%STRUCT%NUMP,YDCV%STRUCT%MYMS,YDCV%NSERR,YDCV%NSERR,&
    & YDCV%STRUCT%NALLMS,YDCV%STRUCT%NPTRMS,YDCV%STRUCT%NUMLL,&
    & YDCV%STRUCT%NPTRLL,YDCV%STRUCT%NPSURF,YDCV%NVE3D,YDCV%NVE2D)
!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1)&
!$OMP& PRIVATE(JP,IA,IB,JA,JB,JCOMP,IFOFF,JF,JM,IM,ILEN,JSOFF,JN,JL,IL)
  DO JP=1,NPROC
    CALL PE2SET(JP,IA,IB,JA,JB)

    DO JCOMP=1,YDCV%NMODERR
      IFOFF=JFOFF(JP)
      IF (.NOT.LELAM) THEN
        DO JF=1,YLSP%NS2L(JB)
          DO JM=1,YLSP%NUMPP(JA)
            IM=YLSP%MS_PROC(JM,JA)
            ILEN=2*(YLSP%NSMAX-IM+1)
            JSOFF=IFOFF+YLSP%NASM0G(IM)-1
            DO JN=1,ILEN
              II(JP)=II(JP)+1
              PBUF(II(JP))=PCV(JSOFF+JN)
            ENDDO
          ENDDO
          IFOFF=IFOFF+YLSP%NSPEC2G
        ENDDO
        JFOFF(JP)=JFOFF(JP)+YLSP%NSPEC2G*YLSP%NS2G
        DO JF=1,YLSP%NS3D
          DO JM=1,YLSP%NUMPP(JA)
            IM=YLSP%MS_PROC(JM,JA)
            ILEN=2*(YLSP%NSMAX-IM+1)
            JSOFF=JFOFF(JP)+(YLSP%NASM0G(IM)-1)*YLSP%NFLEVG
            DO JN=1,ILEN
              DO JL=1,YLSP%NUMLL(JB)
                IL=YLSP%NPTRLL(JB)+JL-1
                II(JP)=II(JP)+1
                PBUF(II(JP))=PCV(JSOFF+IL)
              ENDDO
              JSOFF=JSOFF+YLSP%NFLEVG
            ENDDO
          ENDDO
          JFOFF(JP)=JFOFF(JP)+YLSP%NSPEC2G*YLSP%NFLEVG
        ENDDO
      ELSE
        DO JF=1,YLSP%NS2L(JB)
          DO JM=1,YLSP%NUMPP(JA)
            IM=YLSP%MS_PROC(JM,JA)
            ILEN=YLSP%NCPL4M(IM)
            JSOFF=IFOFF+YLSP%NESM0G(IM)-1
            DO JN=1,ILEN
              II(JP)=II(JP)+1
              PBUF(II(JP))=PCV(JSOFF+JN)
            ENDDO
          ENDDO
          IFOFF=IFOFF+YLSP%NSPEC2G
        ENDDO
        JFOFF(JP)=JFOFF(JP)+YLSP%NSPEC2G*YLSP%NS2G
        DO JF=1,YLSP%NS3D
          DO JM=1,YLSP%NUMPP(JA)
            IM=YLSP%MS_PROC(JM,JA)
            ILEN=YLSP%NCPL4M(IM)
            JSOFF=JFOFF(JP)+(YLSP%NESM0G(IM)-1)*YLSP%NFLEVG
            DO JN=1,ILEN
              DO JL=1,YLSP%NUMLL(JB)
                IL=YLSP%NPTRLL(JB)+JL-1
                II(JP)=II(JP)+1
                PBUF(II(JP))=PCV(JSOFF+IL)
              ENDDO
              JSOFF=JSOFF+YLSP%NFLEVG
            ENDDO
          ENDDO
          JFOFF(JP)=JFOFF(JP)+YLSP%NSPEC2G*YLSP%NFLEVG
        ENDDO
      ENDIF
    ENDDO
  ENDDO
!$OMP END PARALLEL DO
ENDIF

! Pack TOVS variable if needed
! ----------------------------
IF (YDCV%LTOVSCV) THEN
!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1)&
!$OMP& PRIVATE(JP,JF,JJ)
  DO JP=1,NPROC
    DO JF=1,YDCV%STRUCT%NVATOVV
      DO JJ=1,YDCV%STRUCT%NLENTOVSCVP(JP)
        II(JP)=II(JP)+1
         IF(LINC_TOVSCV)THEN
            PBUF(II(JP))=PCV(JFOFF(JP)+IVATOVINDXINV(YDCV%STRUCT%NVATOVOFF(JP)+JJ))
         ELSE
            PBUF(II(JP))=0.0_JPRB
         ENDIF
      ENDDO
      JFOFF(JP)=JFOFF(JP)+YDCV%STRUCT%NLENTOVSCVG
    ENDDO
  ENDDO
!$OMP END PARALLEL DO
ENDIF
 

! Pack LAM variable if needed
! ----------------------------
IF (YDCV%LAM1D) THEN
!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1)&
!$OMP& PRIVATE(JP,JF,IL)
  DO JP=1,NPROC
    DO JF=1,YDCV%NS1D
      DO JL=1,NUMLL(JB)
        IL=NPTRLL(JB)-1+JL
        II(JP)=II(JP)+1
        PBUF(II(JP))=PCV(JFOFF(JP)+IL)
      ENDDO
      JFOFF(JP)=JFOFF(JP)+NFLEVG
    ENDDO
  ENDDO
!$OMP END PARALLEL DO
ENDIF

! Pack PARAM variable if needed
! -----------------------------
IF (YDCV%NPARA>0) THEN
!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1)&
!$OMP& PRIVATE(JP,JF)
  DO JP=1,NPROC
    DO JF=1,YDCV%NPARA
      II(JP)=II(JP)+1
      PBUF(II(JP))=PCV(JFOFF(JP)+JF)
    ENDDO
    JFOFF(JP)=JFOFF(JP)+YDCV%NPARA
  ENDDO
!$OMP END PARALLEL DO
ENDIF

! Pack PARAM variable if needed
! -----------------------------
IF (YDCV%NPECV>0) THEN
!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1)&
!$OMP& PRIVATE(JP,JF)
  DO JP=1,NPROC
    DO JF=1,YDCV%NPECV
      II(JP)=II(JP)+1
      PBUF(II(JP))=PCV(JFOFF(JP)+JF)
    ENDDO
    JFOFF(JP)=JFOFF(JP)+YDCV%NPECV
  ENDDO
!$OMP END PARALLEL DO
ENDIF

!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1)&
!$OMP& PRIVATE(JP)
DO JP=1,NPROC
  IF( II(JP)-KDISPS(JP) > KLENS(JP) )THEN
    WRITE(NULOUT,'("CONTROL_VECTORS_COMM_MOD:PACK_CV ii-kdisps>klens: JP,II-KDISPS,KLENS=",&
     &3(2X,I10))')JP,II(JP)-KDISPS(JP),KLENS(JP)
    CALL ABOR1('CONTROL_VECTORS_COMM_MOD:PACK_CV  ii-kdisps>klens')
  ENDIF
ENDDO
!$OMP END PARALLEL DO

IF ((YDCV%LINITCV .AND. (.NOT. YDCV%INITCV%LWAVELETJB)) &
  & .OR. &
  & (YDCV%NMODERR>0)) THEN
  CALL DEALLOCATE_SPEC(YLSP)
ENDIF
IF (YDCV%LENSCV) THEN
   CALL DEALLOCATE_SPEC(YLSP_ENS)
ENDIF
IF (YDCV%LTOVSCV) THEN
  DEALLOCATE(IVATOVINDXINV)
ENDIF

END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('CONTROL_VECTORS_COMM_MOD:PACK_CV',1,ZHOOK_HANDLE)
END SUBROUTINE PACK_CV

! ------------------------------------------------------------------

SUBROUTINE UNPACK_CV(YDGEOMETRY,YDCV,KBUF,KLENR,KDISPR,PBUF,PCV)

! Arrange receive buffer in global cv.

USE GEOMETRY_MOD , ONLY : GEOMETRY
TYPE(GEOMETRY)      , INTENT(IN)  :: YDGEOMETRY
TYPE(CONTROL_VECTOR), INTENT(IN)  :: YDCV
INTEGER(KIND=JPIM)  , INTENT(IN)  :: KBUF
INTEGER(KIND=JPIM)  , INTENT(IN)  :: KLENR(NPROC),KDISPR(NPROC)
REAL(KIND=JPRB)     , INTENT(IN)  :: PBUF(KBUF)
REAL(KIND=JPRB)     , INTENT(OUT) :: PCV(YDCV%NSIZEG)

TYPE(SPECTRAL_FIELD) :: YLSP, YLSP_ENS
INTEGER(KIND=JPIM) :: II(NPROC),JFOFF(NPROC)
INTEGER(KIND=JPIM) :: JP,JA,JB,IA,IB,JF,JM,IM,ILEN,JSOFF,JN,IL,JL,&
                    & JJ,IFOFF,JSCALE, JROF, ICEND, IBL, INFLDS, IGP,&
                    & IGPTOT, IAAAARRRRGH, IOFF, IGL2, IGL1, ILAST, JLON,&
                    & JGL, ILOFF, IGLOFF, JKGLO, ISCALE, JCOMP
INTEGER(KIND=JPIM), ALLOCATABLE :: IVATOVINDXINV(:)
REAL(KIND=JPRB), ALLOCATABLE :: ZGPLOC(:)
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('CONTROL_VECTORS_COMM_MOD:UNPACK_CV',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM=>YDGEOMETRY%YRDIM,YDDIMV=>YDGEOMETRY%YRDIMV,YDGEM=>YDGEOMETRY%YRGEM, YDMP=>YDGEOMETRY%YRMP)
ASSOCIATE(NFLEVL=>YDDIMV%NFLEVL, NFLEVG=>YDDIMV%NFLEVG, &
 & NPROMA=>YDDIM%NPROMA, &
 & RMUCEN=>YDGEM%RMUCEN, &
 & NPTRLL=>YDMP%NPTRLL, NUMLL=>YDMP%NUMLL)

IF (YDCV%LTOVSCV) THEN
  ALLOCATE(IVATOVINDXINV(YDCV%STRUCT%NLENTOVSCVG))
  DO JJ = 1, YDCV%STRUCT%NLENTOVSCVG
    IVATOVINDXINV(NVATOVINDX(JJ)) = JJ
  ENDDO
ENDIF

! Initialize II and JFOFF
!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1)&
!$OMP& PRIVATE(JP)
DO JP=1,NPROC
  II(JP)=KDISPR(JP)
  JFOFF(JP)=0
ENDDO
!$OMP END PARALLEL DO

! Unpack initial condition (but not if wavelet Jb)
! ------------------------------------------------
IF (YDCV%LINITCV) THEN
  IF (.NOT. YDCV%INITCV%LWAVELETJB) THEN
    CALL INQ_SPEC(YLSP,YDCV%STRUCT%NFLEVL,YDCV%STRUCT%NFLEVG,&
        & YDCV%STRUCT%NUMP,YDCV%STRUCT%MYMS,YDCV%NSMAX,YDCV%NMSMAX,&
        & YDCV%STRUCT%NALLMS,YDCV%STRUCT%NPTRMS,YDCV%STRUCT%NUMLL,&
        & YDCV%STRUCT%NPTRLL,YDCV%STRUCT%NPSURF,YDCV%NS3D,YDCV%NS2D)
 ENDIF
 IF (YDCV%LENSCV) THEN
    CALL INQ_SPEC(YLSP_ENS,YDCV%STRUCT_ENS%NFLEVL,YDCV%STRUCT_ENS%NFLEVG,&
         & YDCV%STRUCT_ENS%NUMP,YDCV%STRUCT_ENS%MYMS,YDCV%NSMAX_ENS,YDCV%NMSMAX_ENS,&
         & YDCV%STRUCT_ENS%NALLMS,YDCV%STRUCT_ENS%NPTRMS,YDCV%STRUCT_ENS%NUMLL,&
         & YDCV%STRUCT_ENS%NPTRLL,YDCV%STRUCT_ENS%NPSURF,YDCV%NENS,YDCV%NENS)
 ENDIF
!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1)&
!$OMP& PRIVATE(JP,IA,IB,JA,JB,INFLDS,JSCALE,ISCALE,IGPTOT,ZGPLOC,JF,&
!$OMP& IGP,JKGLO,ICEND,IBL,JROF,IGLOFF,IGL1,IGL2,IOFF,ILAST,JJ,ILEN,ILOFF,&
!$OMP& JGL,JLON,IAAAARRRRGH,JM,IM,JSOFF,JN,JL,IL)
  DO JP=1,NPROC
    CALL PE2SET(JP,IA,IB,JA,JB)

    IF (YDCV%INITCV%LWAVELETJB) THEN

      INFLDS = NFLEVG*YDCV%NS3D+YDCV%NS2D

      DO JSCALE=1,YDCV%NSCALES
        ISCALE=MAX(JSCALE,2)+YDCV%STRUCT%NSCALES-YDCV%NSCALES

        IGPTOT=JB_STRUCT%GRID_DEFINITION(ISCALE)%NGPTOTL(IA,IB)
        ALLOCATE (ZGPLOC(IGPTOT))

        DO JF=1,INFLDS
          IGP = 0
          DO JKGLO=1,IGPTOT,NPROMA
            ICEND=MIN(NPROMA,IGPTOT-JKGLO+1)
            IBL=(JKGLO-1)/NPROMA+1
            DO JROF=1,ICEND
              IGP = IGP + 1
              ZGPLOC(IGP) = PBUF(II(JP)+JROF+NPROMA*(JF-1+INFLDS*(IBL-1)))
            ENDDO
          ENDDO

          IGLOFF = JB_STRUCT%GRID_DEFINITION(ISCALE)%NPTRFRSTLAT(IA)
          IGL1 =   JB_STRUCT%GRID_DEFINITION(ISCALE)%NFRSTLAT(IA)
          IGL2 =   JB_STRUCT%GRID_DEFINITION(ISCALE)%NLSTLAT(IA)
          IOFF = 0

          IF (IA>1) THEN
            IF( JB_STRUCT%GRID_DEFINITION(ISCALE)%NLSTLAT(IA-1) == JB_STRUCT%GRID_DEFINITION(ISCALE)%NFRSTLAT(IA) )THEN
              ILAST = JB_STRUCT%GRID_DEFINITION(ISCALE)%NLSTLAT(IA-1)-1
            ELSE
              ILAST = JB_STRUCT%GRID_DEFINITION(ISCALE)%NLSTLAT(IA-1)
            ENDIF
            DO JJ=JB_STRUCT%GRID_DEFINITION(ISCALE)%NFRSTLAT(1),ILAST
              IOFF = IOFF+JB_STRUCT%GRID_DEFINITION(ISCALE)%NLOENG(JJ)
            ENDDO
          ENDIF
  
          ILEN = 0
          ILOFF = 0
          DO JGL=IGL1,IGL2
            DO JLON=1,JB_STRUCT%GRID_DEFINITION(ISCALE)%NONL(IGLOFF+JGL-IGL1,IB)
              IAAAARRRRGH = IOFF+ILOFF+JB_STRUCT%GRID_DEFINITION(ISCALE)%NSTA(IGLOFF+JGL-IGL1,IB)+JLON-1&
                           & + JFOFF(JP)
              PCV(IAAAARRRRGH) = ZGPLOC(ILEN+JLON)
            ENDDO
            ILEN = ILEN + JB_STRUCT%GRID_DEFINITION(ISCALE)%NONL(IGLOFF+JGL-IGL1,IB)
            ILOFF = ILOFF + JB_STRUCT%GRID_DEFINITION(ISCALE)%NLOENG(JGL)
          ENDDO

!         increment the offset into the gathered control vector
          JFOFF(JP)=JFOFF(JP)+JB_STRUCT%GRID_DEFINITION(ISCALE)%NGPTOTG

        ENDDO

        DEALLOCATE (ZGPLOC)

!       increment the offset into the send buffer
        II(JP)=II(JP)+NPROMA*INFLDS*((IGPTOT+NPROMA-1)/NPROMA)
      ENDDO

    ELSE
      IF (.NOT.LELAM) THEN
        DO JF=1,YLSP%NS2L(JB)
          DO JM=1,YLSP%NUMPP(JA)
            IM=YLSP%MS_PROC(JM,JA)
            ILEN=2*(YLSP%NSMAX-IM+1)
            JSOFF=JFOFF(JP)+YLSP%NASM0G(IM)-1
            DO JN=1,ILEN
              II(JP)=II(JP)+1
              PCV(JSOFF+JN)=PBUF(II(JP))
            ENDDO
          ENDDO
          JFOFF(JP)=JFOFF(JP)+YLSP%NSPEC2G
        ENDDO
      ELSE
        DO JF=1,YLSP%NS2L(JB)
          DO JM=1,YLSP%NUMPP(JA)
            IM=YLSP%MS_PROC(JM,JA)
            ILEN=YLSP%NCPL4M(IM)
            JSOFF=JFOFF(JP)+YLSP%NESM0G(IM)-1
            DO JN=1,ILEN
              II(JP)=II(JP)+1
              PCV(JSOFF+JN)=PBUF(II(JP))
            ENDDO
          ENDDO
          JFOFF(JP)=JFOFF(JP)+YLSP%NSPEC2G
        ENDDO
      ENDIF
      JFOFF(JP)=YLSP%NSPEC2G*YLSP%NS2G
      IF (.NOT.LELAM) THEN
        DO JF=1,YLSP%NS3D
          DO JM=1,YLSP%NUMPP(JA)
            IM=YLSP%MS_PROC(JM,JA)
            ILEN=2*(YLSP%NSMAX-IM+1)
            JSOFF=JFOFF(JP)+(YLSP%NASM0G(IM)-1)*YLSP%NFLEVG
            DO JN=1,ILEN
              DO JL=1,YLSP%NUMLL(JB)
                IL=YLSP%NPTRLL(JB)+JL-1
                II(JP)=II(JP)+1
                PCV(JSOFF+IL)=PBUF(II(JP))
              ENDDO
              JSOFF=JSOFF+YLSP%NFLEVG
            ENDDO
          ENDDO
          JFOFF(JP)=JFOFF(JP)+YLSP%NSPEC2G*YLSP%NFLEVG
        ENDDO
      ELSE
        DO JF=1,YLSP%NS3D
          DO JM=1,YLSP%NUMPP(JA)
            IM=YLSP%MS_PROC(JM,JA)
            ILEN=YLSP%NCPL4M(IM)
            JSOFF=JFOFF(JP)+(YLSP%NESM0G(IM)-1)*YLSP%NFLEVG
            DO JN=1,ILEN
              DO JL=1,YLSP%NUMLL(JB)
                IL=YLSP%NPTRLL(JB)+JL-1
                II(JP)=II(JP)+1
                PCV(JSOFF+IL)=PBUF(II(JP))
              ENDDO
              JSOFF=JSOFF+YLSP%NFLEVG
            ENDDO
          ENDDO
          JFOFF(JP)=JFOFF(JP)+YLSP%NSPEC2G*YLSP%NFLEVG
        ENDDO
      ENDIF
    ENDIF
  ENDDO
!$OMP END PARALLEL DO
ENDIF

! Unpack enscv if needed
! --------------------------
IF (YDCV%LENSCV) THEN
   !$OMP PARALLEL DO SCHEDULE(DYNAMIC,1)&
   !$OMP& PRIVATE(JP,IA,IB,JA,JB,JCOMP,IFOFF,JF,JM,IM,ILEN,JSOFF,JN,JL,IL)
   DO JP=1,NPROC
      CALL PE2SET(JP,IA,IB,JA,JB)
      IFOFF=JFOFF(JP)
      IF (.NOT.LELAM) THEN
         DO JF=1,YLSP_ENS%NS2L(JB)
            DO JM=1,YLSP_ENS%NUMPP(JA)
               IM=YLSP_ENS%MS_PROC(JM,JA)
               ILEN=2*(YLSP_ENS%NSMAX-IM+1)
               JSOFF=IFOFF+YLSP_ENS%NASM0G(IM)-1
               DO JN=1,ILEN
                  II(JP)=II(JP)+1
                  PCV(JSOFF+JN)=PBUF(II(JP))
               ENDDO
            ENDDO
            IFOFF=IFOFF+YLSP_ENS%NSPEC2G
         ENDDO
         JFOFF(JP)=JFOFF(JP)+YLSP_ENS%NSPEC2G*YLSP_ENS%NS2G
         DO JF=1,YLSP_ENS%NS3D
            DO JM=1,YLSP_ENS%NUMPP(JA)
               IM=YLSP_ENS%MS_PROC(JM,JA)
               ILEN=2*(YLSP_ENS%NSMAX-IM+1)
               JSOFF=JFOFF(JP)+(YLSP_ENS%NASM0G(IM)-1)*YLSP_ENS%NFLEVG
               DO JN=1,ILEN
                  DO JL=1,YLSP_ENS%NUMLL(JB)
                     IL=YLSP_ENS%NPTRLL(JB)+JL-1
                     II(JP)=II(JP)+1
                     PCV(JSOFF+IL)=PBUF(II(JP))
                  ENDDO
                  JSOFF=JSOFF+YLSP_ENS%NFLEVG
               ENDDO
            ENDDO
            JFOFF(JP)=JFOFF(JP)+YLSP_ENS%NSPEC2G*YLSP_ENS%NFLEVG
         ENDDO
      ELSE
         DO JF=1,YLSP_ENS%NS2L(JB)
            DO JM=1,YLSP_ENS%NUMPP(JA)
               IM=YLSP_ENS%MS_PROC(JM,JA)
               ILEN=YLSP_ENS%NCPL4M(IM)
               JSOFF=IFOFF+YLSP_ENS%NESM0G(IM)-1
               DO JN=1,ILEN
                  II(JP)=II(JP)+1
                  PCV(JSOFF+JN)=PBUF(II(JP))
               ENDDO
            ENDDO
            IFOFF=IFOFF+YLSP_ENS%NSPEC2G
         ENDDO
         JFOFF(JP)=JFOFF(JP)+YLSP_ENS%NSPEC2G*YLSP_ENS%NS2G
         DO JF=1,YLSP_ENS%NS3D
            DO JM=1,YLSP_ENS%NUMPP(JA)
               IM=YLSP_ENS%MS_PROC(JM,JA)
               ILEN=YLSP_ENS%NCPL4M(IM)
               JSOFF=JFOFF(JP)+(YLSP_ENS%NESM0G(IM)-1)*YLSP_ENS%NFLEVG
               DO JN=1,ILEN
                  DO JL=1,YLSP_ENS%NUMLL(JB)
                     IL=YLSP_ENS%NPTRLL(JB)+JL-1
                     II(JP)=II(JP)+1
                     PCV(JSOFF+IL)=PBUF(II(JP))
                  ENDDO
                  JSOFF=JSOFF+YLSP_ENS%NFLEVG
               ENDDO
            ENDDO
            JFOFF(JP)=JFOFF(JP)+YLSP_ENS%NSPEC2G*YLSP_ENS%NFLEVG
         ENDDO
      ENDIF
   ENDDO
   !$OMP END PARALLEL DO
ENDIF

! Unpack model error if needed
! ----------------------------
IF (YDCV%NMODERR>0) THEN
  CALL INQ_SPEC(YLSP,YDCV%STRUCT%NFLEVL,YDCV%STRUCT%NFLEVG,&
    & YDCV%STRUCT%NUMP,YDCV%STRUCT%MYMS,YDCV%NSERR,YDCV%NSERR,&
    & YDCV%STRUCT%NALLMS,YDCV%STRUCT%NPTRMS,YDCV%STRUCT%NUMLL,&
    & YDCV%STRUCT%NPTRLL,YDCV%STRUCT%NPSURF,YDCV%NVE3D,YDCV%NVE2D)

!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1)&
!$OMP& PRIVATE(JP,IA,IB,JA,JB,JCOMP,IFOFF,JF,JM,IM,ILEN,JSOFF,JN,JL,IL)
  DO JP=1,NPROC
    CALL PE2SET(JP,IA,IB,JA,JB)

    DO JCOMP=1,YDCV%NMODERR
      IFOFF=JFOFF(JP)
      IF (.NOT.LELAM) THEN
        DO JF=1,YLSP%NS2L(JB)
          DO JM=1,YLSP%NUMPP(JA)
            IM=YLSP%MS_PROC(JM,JA)
            ILEN=2*(YLSP%NSMAX-IM+1)
            JSOFF=IFOFF+YLSP%NASM0G(IM)-1
            DO JN=1,ILEN
              II(JP)=II(JP)+1
              PCV(JSOFF+JN)=PBUF(II(JP))
            ENDDO
          ENDDO
          IFOFF=IFOFF+YLSP%NSPEC2G
        ENDDO
        JFOFF(JP)=JFOFF(JP)+YLSP%NSPEC2G*YLSP%NS2G
        DO JF=1,YLSP%NS3D
          DO JM=1,YLSP%NUMPP(JA)
            IM=YLSP%MS_PROC(JM,JA)
            ILEN=2*(YLSP%NSMAX-IM+1)
            JSOFF=JFOFF(JP)+(YLSP%NASM0G(IM)-1)*YLSP%NFLEVG
            DO JN=1,ILEN
              DO JL=1,YLSP%NUMLL(JB)
                IL=YLSP%NPTRLL(JB)+JL-1
                II(JP)=II(JP)+1
                PCV(JSOFF+IL)=PBUF(II(JP))
              ENDDO
              JSOFF=JSOFF+YLSP%NFLEVG
            ENDDO
          ENDDO
          JFOFF(JP)=JFOFF(JP)+YLSP%NSPEC2G*YLSP%NFLEVG
        ENDDO
      ELSE
        DO JF=1,YLSP%NS2L(JB)
          DO JM=1,YLSP%NUMPP(JA)
            IM=YLSP%MS_PROC(JM,JA)
            ILEN=YLSP%NCPL4M(IM)
            JSOFF=IFOFF+YLSP%NESM0G(IM)-1
            DO JN=1,ILEN
              II(JP)=II(JP)+1
              PCV(JSOFF+JN)=PBUF(II(JP))
            ENDDO
          ENDDO
          IFOFF=IFOFF+YLSP%NSPEC2G
        ENDDO
        JFOFF(JP)=JFOFF(JP)+YLSP%NSPEC2G*YLSP%NS2G
        DO JF=1,YLSP%NS3D
          DO JM=1,YLSP%NUMPP(JA)
            IM=YLSP%MS_PROC(JM,JA)
            ILEN=YLSP%NCPL4M(IM)
            JSOFF=JFOFF(JP)+(YLSP%NESM0G(IM)-1)*YLSP%NFLEVG
            DO JN=1,ILEN
              DO JL=1,YLSP%NUMLL(JB)
                IL=YLSP%NPTRLL(JB)+JL-1
                II(JP)=II(JP)+1
                PCV(JSOFF+IL)=PBUF(II(JP))
              ENDDO
              JSOFF=JSOFF+YLSP%NFLEVG
            ENDDO
          ENDDO
          JFOFF(JP)=JFOFF(JP)+YLSP%NSPEC2G*YLSP%NFLEVG
        ENDDO
      ENDIF
    ENDDO
  ENDDO
!$OMP END PARALLEL DO
ENDIF

! Unpack TOVS variable if needed
! ------------------------------
IF (YDCV%LTOVSCV) THEN
!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1)&
!$OMP& PRIVATE(JP,JF,JJ)
  DO JP=1,NPROC
    DO JF=1,YDCV%STRUCT%NVATOVV
      DO JJ=1,YDCV%STRUCT%NLENTOVSCVP(JP)
        II(JP)=II(JP)+1
        PCV(JFOFF(JP)+IVATOVINDXINV(YDCV%STRUCT%NVATOVOFF(JP)+JJ))=PBUF(II(JP))
      ENDDO
      JFOFF(JP)=JFOFF(JP)+YDCV%STRUCT%NLENTOVSCVG
    ENDDO
  ENDDO
!$OMP END PARALLEL DO
ENDIF


! Unpack LAM variable if needed
! -----------------------------
IF (YDCV%LAM1D) THEN
!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1)&
!$OMP& PRIVATE(JP,JF,JL,IL)
  DO JP=1,NPRTRV
    DO JF=1,YDCV%NS1D
      DO JL=1,NUMLL(JP)
        IL=NPTRLL(JP)-1+JL
        II(JP)=II(JP)+1
        PCV(JFOFF(JP)+IL)=PBUF(II(JP))
      ENDDO
      JFOFF(JP)=JFOFF(JP)+NFLEVG
    ENDDO
  ENDDO
!$OMP END PARALLEL DO
ENDIF

! Unpack PARAM variable if needed
! -------------------------------
IF (YDCV%NPARA>0) THEN
!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1)&
!$OMP& PRIVATE(JP,JF)
  DO JP=1,NPROC
    DO JF=1,YDCV%NPARA
      II(JP)=II(JP)+1
      PCV(JFOFF(JP)+JF)=PBUF(II(JP))
    ENDDO
    JFOFF(JP)=JFOFF(JP)+YDCV%NPARA
  ENDDO
!$OMP END PARALLEL DO
ENDIF

! Unpack PARAM variable if needed
! -------------------------------
IF (YDCV%NPECV>0) THEN
!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1)&
!$OMP& PRIVATE(JP,JF)
  DO JP=1,NPROC
    DO JF=1,YDCV%NPECV
      II(JP)=II(JP)+1
      PCV(JFOFF(JP)+JF)=PBUF(II(JP))
    ENDDO
    JFOFF(JP)=JFOFF(JP)+YDCV%NPECV
  ENDDO
!$OMP END PARALLEL DO
ENDIF

!$OMP PARALLEL DO SCHEDULE(DYNAMIC,1)&
!$OMP& PRIVATE(JP)
DO JP=1,NPROC
  IF( II(JP)-KDISPR(JP) > KLENR(JP) )THEN
    WRITE(NULOUT,'("CONTROL_VECTORS_COMM_MOD:UNPACK_CV ii-kdispr>klenr: JP,II-KDISPR,KLENR=",&
     &3(2X,I10))')JP,II(JP)-KDISPR(JP),KLENR(JP)
    CALL ABOR1('CONTROL_VECTORS_COMM_MOD:UNPACK_CV  ii-kdispr>klenr')
  ENDIF

ENDDO
!$OMP END PARALLEL DO

IF ((YDCV%LINITCV .AND. (.NOT. YDCV%INITCV%LWAVELETJB)) &
  & .OR. &
  & (YDCV%NMODERR>0)) THEN
  CALL DEALLOCATE_SPEC(YLSP)
ENDIF
IF (YDCV%LENSCV) THEN
   CALL DEALLOCATE_SPEC(YLSP_ENS)
ENDIF
IF (YDCV%LTOVSCV) THEN
  DEALLOCATE(IVATOVINDXINV)
ENDIF

END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('CONTROL_VECTORS_COMM_MOD:UNPACK_CV',1,ZHOOK_HANDLE)
END SUBROUTINE UNPACK_CV

! ------------------------------------------------------------------

END MODULE CONTROL_VECTORS_COMM_MOD
