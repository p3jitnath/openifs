SUBROUTINE MULTIGATHER_CTLVEC(YDGEOMETRY,YDCV, KPROC, PCV, KSTART, KEND)

!     Purpose.
!     --------
!       Gather multiple control variables on multiple PEs.

!     Author.
!     -------
!       Tomas Wilhelmsson *ECMWF*

!     Modifications.
!     --------------
!       Original  : 09-08-26
! ----------------------------------------------

USE GEOMETRY_MOD , ONLY : GEOMETRY
TYPE(GEOMETRY)      , INTENT(IN)  :: YDGEOMETRY
INTEGER(KIND=JPIM)  , INTENT(IN)  :: KSTART, KEND 
TYPE(CONTROL_VECTOR), INTENT(IN)  :: YDCV(KSTART:KEND)
INTEGER(KIND=JPIM)  , INTENT(IN)  :: KPROC(KSTART:KEND)
REAL(KIND=JPRB)     , INTENT(OUT) :: PCV(:)

REAL(KIND=JPRB), ALLOCATABLE :: ZSENDBUF(:),ZRECVBUF(:)
INTEGER(KIND=JPIM) :: ISENDCOUNTS(NPROC), IRECVCOUNTS(NPROC)
INTEGER(KIND=JPIM) :: ILENR(NPROC,KSTART:KEND),IDISPR(NPROC)
INTEGER(KIND=JPIM) :: ILENG(KSTART:KEND),JP,JJ,J,ICNT
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('CONTROL_VECTORS_COMM_MOD:MULTIGATHER_CTLVEC',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM=>YDGEOMETRY%YRDIM,YDDIMV=>YDGEOMETRY%YRDIMV,YDGEM=>YDGEOMETRY%YRGEM, YDMP=>YDGEOMETRY%YRMP)
ASSOCIATE(NFLEVL=>YDDIMV%NFLEVL, NFLEVG=>YDDIMV%NFLEVG, &
 & NPROMA=>YDDIM%NPROMA, &
 & RMUCEN=>YDGEM%RMUCEN, &
 & NPTRLL=>YDMP%NPTRLL, NUMLL=>YDMP%NUMLL)

CALL GSTATS(258,0)
DO JJ = KSTART, KEND
  IF (MYPROC==KPROC(JJ)) THEN
    IF (SIZE(PCV)/=YDCV(JJ)%NSIZEG) CALL ABOR1('MULTIGATHER_CTLVEC: pcv wrong size')
  ENDIF

  ! Allocate receive buffer
  ! -----------------------
  CALL GET_CVLENGTHS(YDGEOMETRY,YDCV(JJ), ILENR(:,JJ))

  ILENG(JJ)=SUM(ILENR(:,JJ))

  IF (MYPROC==KPROC(JJ)) THEN
    ALLOCATE(ZRECVBUF(ILENG(JJ)))
    DO JP = 1, NPROC
       IRECVCOUNTS(JP) = ILENR(JP,JJ)
    ENDDO
  ENDIF
ENDDO

IF (.NOT. ALLOCATED(ZRECVBUF)) THEN
  ALLOCATE(ZRECVBUF(1))
  IRECVCOUNTS(1:NPROC) = 0
ENDIF

! Allocate and fill send buffer
! -----------------------------

ALLOCATE(ZSENDBUF(SUM(ILENR(MYPROC,:))))

ISENDCOUNTS(1:NPROC) = 0
ICNT = 0
DO JJ = KSTART, KEND
  DO J=1, SIZE(YDCV(JJ)%DATA)
    ICNT = ICNT + 1
    ZSENDBUF(ICNT) = YDCV(JJ)%DATA(J)
  ENDDO
  ISENDCOUNTS(KPROC(JJ)) = SIZE(YDCV(JJ)%DATA)
ENDDO

IF (SUM(ILENR(MYPROC,:))/=ICNT) CALL ABOR1('MULTIGATHER_CTLVEC: sendbuf wrong size')

! Exchange data
! -------------
CALL GSTATS_BARRIER(724)
CALL GSTATS(536,0)
CALL MPL_ALLTOALLV(ZSENDBUF, ISENDCOUNTS, ZRECVBUF, IRECVCOUNTS,&
                 & CDSTRING='CONTROL_VECTORS_COMM_MOD:MULTIGATHER_CTLVEC')  
CALL GSTATS(536,1)
CALL GSTATS_BARRIER2(724)

! Unpack data on root processor
! -----------------------------

DO JJ = KSTART, KEND
  IF (MYPROC==KPROC(JJ)) THEN

    IDISPR(1)=0
    DO JP=2,NPROC
      IDISPR(JP)=IDISPR(JP-1)+ILENR(JP-1,JJ)
    ENDDO

    CALL GSTATS(1859,0)
    CALL UNPACK_CV(YDGEOMETRY,YDCV(JJ),ILENG(JJ),ILENR(:,JJ),IDISPR,ZRECVBUF,PCV)
    CALL GSTATS(1859,1)
  ENDIF
ENDDO

DEALLOCATE(ZSENDBUF)
DEALLOCATE(ZRECVBUF)
CALL GSTATS(258,1)

END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('CONTROL_VECTORS_COMM_MOD:MULTIGATHER_CTLVEC',1,ZHOOK_HANDLE)
END SUBROUTINE MULTIGATHER_CTLVEC

