SUBROUTINE SCATTER_CTLVEC(YDGEOMETRY,YDCV, KPROC, PCV)

!     Purpose.
!     --------
!       Distribute control variable from one PE.

!     Author.
!     -------
!       Yannick Tremolet *ECMWF*

!     Modifications.
!     --------------
!       Original  : 02-08-07
! ----------------------------------------------

USE GEOMETRY_MOD , ONLY : GEOMETRY
TYPE(GEOMETRY)      ,INTENT(IN)    :: YDGEOMETRY
TYPE(CONTROL_VECTOR),INTENT(INOUT) :: YDCV
INTEGER(KIND=JPIM)  ,INTENT(IN)    :: KPROC
REAL(KIND=JPRB)     ,INTENT(IN)    :: PCV(:)

REAL(KIND=JPRB), ALLOCATABLE :: ZBUF(:)
INTEGER(KIND=JPIM) :: ILENS(NPROC),IDISPS(NPROC),ILENR,ILENG,JP
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('CONTROL_VECTORS_COMM_MOD:SCATTER_CTLVEC',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM=>YDGEOMETRY%YRDIM,YDDIMV=>YDGEOMETRY%YRDIMV,YDGEM=>YDGEOMETRY%YRGEM, YDMP=>YDGEOMETRY%YRMP)
ASSOCIATE(NFLEVL=>YDDIMV%NFLEVL, NFLEVG=>YDDIMV%NFLEVG, &
 & NPROMA=>YDDIM%NPROMA, &
 & RMUCEN=>YDGEM%RMUCEN, &
 & NPTRLL=>YDMP%NPTRLL, NUMLL=>YDMP%NUMLL)

CALL GSTATS(254,0)
IF (MYPROC==KPROC .AND. SIZE(PCV)/=YDCV%NSIZEG) THEN
  WRITE (NULERR,*) 'SIZE(pcv)=',SIZE(PCV),' ydcv%nsizeg=',YDCV%NSIZEG
  CALL ABOR1('SCATTER_CTLVEC: pcv wrong size')
ENDIF

! Set indices and allocate buffer
! -------------------------------
CALL GET_CVLENGTHS(YDGEOMETRY,YDCV, ILENS)
IDISPS(1)=0
DO JP=2,NPROC
  IDISPS(JP)=IDISPS(JP-1)+ILENS(JP-1)
ENDDO
ILENR=YDCV%NSIZEL

ILENG=SUM(ILENS(:))  ! ileng is NOT ydcv%nsizeg because LAM and PARAM var are duplicated.
!?cf I don't understand this comment for the LAM ?
IF (MYPROC==KPROC) THEN
  ALLOCATE(ZBUF(ILENG))
ELSE
  ALLOCATE(ZBUF(1))
ENDIF

! Pack data on root processor
! ---------------------------
CALL GSTATS(1846,0)
IF (MYPROC==KPROC) CALL PACK_CV(YDGEOMETRY,YDCV,PCV,ILENG,ILENS,IDISPS,ZBUF)
CALL GSTATS(1846,1)

! Exchange data
! -------------
CALL GSTATS_BARRIER(714)
CALL GSTATS(524,0)
CALL MPL_SCATTERV(PRECVBUF=YDCV%DATA,KROOT=KPROC,PSENDBUF=ZBUF,&
 & KSENDCOUNTS=ILENS,CDSTRING='CONTROL_VECTORS_COMM_MOD:SCATTER_CTLVEC')  
CALL GSTATS(524,1)
CALL GSTATS_BARRIER2(714)

DEALLOCATE(ZBUF)
CALL GSTATS(254,1)

END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('CONTROL_VECTORS_COMM_MOD:SCATTER_CTLVEC',1,ZHOOK_HANDLE)
END SUBROUTINE SCATTER_CTLVEC

