SUBROUTINE FIELDS_FORCE_WITH(SELF,RHS,KFIDS)
USE PARKIND1, ONLY : JPRB
USE YOMHOOK,  ONLY : LHOOK, DR_HOOK, JPHOOK
USE YOMCT0,  ONLY : NINTERPTRAJ, LECMWF
USE YOMCT3,  ONLY : NSTEP
USE FIELD_CONTAINER_OPER_MOD
USE FIELD_DEFINITIONS, ONLY : FID
IMPLICIT NONE

TYPE(FIELDS)      , INTENT(INOUT) :: SELF
TYPE(FIELDS)      , INTENT(INOUT) :: RHS
INTEGER(KIND=JPIM), INTENT(IN)    :: KFIDS(:)

LOGICAL :: LLSAME_RESOL
INTEGER(KIND=JPIM),ALLOCATABLE :: IFIDS_GP(:),IFIDS_SPEC(:),IACTIVE(:),IFIDS_CLIM(:),IFIDS_SNOW(:)
CHARACTER(LEN=32) :: CLFNAME
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK('FIELDS_MOD:FIELDS_FORCE_WITH',0,ZHOOK_HANDLE)
LLSAME_RESOL = GEOMETRY_SAME(SELF%GEOM,RHS%GEOM)
WRITE(NULOUT,*) 'FIELDS_FORCE_WITH CALLED ', LLSAME_RESOL,SELF%GEOM%YRGEM%NGPTOTG,RHS%GEOM%YRGEM%NGPTOTG,NSTEP
CALL FLUSH(NULOUT)

!  Single level surface fields (stored as 2d fields in the field container)
! forced in the linearisation trajectory are interpolated using a consistent
! approach with upper air fields and multi-level surface fields (stored as
! 3d fields in the field container);
!  Extra caution is required when adding new surface fields to the linearisation
! trajectory: be aware that they will be intepolated consistently with
! all other fields.

IF(LECMWF) THEN
  CALL SPLIT_SPEC_GP(KFIDS,IFIDS_SPEC,IFIDS_GP)
ELSE
  IFIDS_SPEC=(/FID%U,FID%V,FID%T,FID%SP/)
  IFIDS_GP=(/FID%Q/) !This should not be hard-coded
ENDIF
WRITE(NULOUT,*) 'FIELDS_FORCE_WITH, IFIDS_SPEC ',IFIDS_SPEC
WRITE(NULOUT,*) 'FIELDS_FORCE_WITH, IFIDS_GP   ',IFIDS_GP
IF(SIZE(IFIDS_SPEC) > 0) THEN
  SELF%YRSPEC = RHS%YRSPEC
ENDIF
IF(LLSAME_RESOL) THEN
  CALL SELF%FIELD%FIELD_COPY(RHS%FIELD,KFIDS=KFIDS)
ELSE
  IF(NINTERPTRAJ == 1) THEN
    ! Bi-linear interpolation
     CALL FIELD_CONTAINER_INTERP(RHS%GEOM,SELF%GEOM,RHS%FIELD,SELF%FIELD, &
       &                         KFIDS_SPEC=IFIDS_SPEC,KFIDS=IFIDS_GP, &
       &                         KINTERPSURF=NINTERPTRAJ)
  ELSEIF(NINTERPTRAJ == 3) THEN
    ! Conserving interpolation
    IF (SELF%GEOM%YRDIM%NSMAX < RHS%GEOM%YRDIM%NSMAX) THEN
      CALL FIELD_CONTAINER_INTERP(RHS%GEOM,SELF%GEOM,RHS%FIELD,SELF%FIELD, &
        &                         KFIDS_SPEC=IFIDS_SPEC,KFIDS=IFIDS_GP,PSPSP=RHS%YRSPEC%SP, &
        &                         KINTERPSURF=NINTERPTRAJ)
    ELSE
     CALL FIELD_CONTAINER_INTERP(RHS%GEOM,SELF%GEOM,RHS%FIELD,SELF%FIELD, &
        &                        KFIDS_SPEC=IFIDS_SPEC,KFIDS=IFIDS_GP, &
        &                        KINTERPSURF=NINTERPTRAJ)
    ENDIF
  ELSE
    CALL ABOR1("FIELDS_MOD:FIELDS_FORCE_WITH: Invalid choice of interpolation type")
  ENDIF
  ! Snow multi-layer fields require vertical regridding (regularisation) after
  ! interpolation
  IFIDS_SNOW=(/FID%SG_F, FID%SG_R, FID%SG_T, FID%SG_W/) ! Pass in this order
  CALL SURF_VREGRID_SNOWML(SELF%GEOM,SELF%STATE_MODEL%YRML_PHY_EC%YREPHY%YSURF, &
    &                      SELF%YRSURF,SELF%FIELD,IFIDS_SNOW)
ENDIF

IF (LHOOK) CALL DR_HOOK('FIELDS_MOD:FIELDS_FORCE_WITH',1,ZHOOK_HANDLE)

END SUBROUTINE FIELDS_FORCE_WITH

