MODULE ERLBC_MOD

!**** *ERLBC_MOD*  - Save/restore model state (used for reading coupling data)

!     Author. 
!     ------- 
!      Philippe Marguinaud *METEO FRANCE* 
!      Original : 01-10-2014

USE PARKIND1    , ONLY : JPRB, JPIM
USE YOMHOOK     , ONLY : DR_HOOK, JPHOOK, LHOOK

USE YOMGFL      , ONLY : TGFL
USE IOGRIDUA_MOD, ONLY : SUGRIDUA_MAP_CTX
USE IOSPECA_MOD , ONLY : SUSPECA_MAP_CTX
USE SPECTRAL_FIELDS_MOD

IMPLICIT NONE

TYPE ERLBC_CTX
  REAL(KIND=JPRB), POINTER :: ZGPF(:,:) => NULL()
  REAL(KIND=JPRB), POINTER :: ZSPA3BC(:,:,:) => NULL()
  REAL(KIND=JPRB), POINTER :: ZSPA2BC(:,:) => NULL()
  REAL(KIND=JPRB), POINTER :: ZSPA1BC(:,:) => NULL()
END TYPE ERLBC_CTX

PRIVATE


TYPE (SUSPECA_MAP_CTX)  :: YSUSPCTX
TYPE (SUGRIDUA_MAP_CTX) :: YSUGACTX

PUBLIC :: ERLBC_CTX, ERLBC_SAVE_CTX, ERLBC_LOAD_CTX, YSUSPCTX, YSUGACTX

SAVE

CONTAINS

SUBROUTINE ERLBC_SAVE_CTX(YDGEOMETRY,YDGFL,YDML_GCONF,YDSPEC,YDCTX)

USE MODEL_GENERAL_CONF_MOD , ONLY : MODEL_GENERAL_CONF_TYPE
USE GEOMETRY_MOD , ONLY : GEOMETRY
TYPE(GEOMETRY)  ,INTENT(IN)    :: YDGEOMETRY
TYPE(TGFL)      ,INTENT(INOUT) :: YDGFL
TYPE(MODEL_GENERAL_CONF_TYPE),INTENT(INOUT):: YDML_GCONF
TYPE(SPECTRAL_FIELD),INTENT(INOUT) :: YDSPEC
TYPE (ERLBC_CTX),INTENT (INOUT):: YDCTX
INTEGER(KIND=JPIM) :: JGFL, IGFL, JLEV, IOFF, JKGLO, IBL, IST, IEND, JROF

REAL (KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK ('ERLBC_MOD:ERLBC_SAVE_CTX',0,ZHOOK_HANDLE)

ALLOCATE(YDCTX%ZSPA3BC(YDGEOMETRY%YRDIMV%NFLSUR,YDGEOMETRY%YRDIM%NSPEC2,YDML_GCONF%YRDIMF%NS3D))
ALLOCATE(YDCTX%ZSPA2BC(YDGEOMETRY%YRDIM%NSPEC2,YDML_GCONF%YRDIMF%NS2D))
ALLOCATE(YDCTX%ZSPA1BC(YDGEOMETRY%YRDIMV%NFLEVL,2))
ALLOCATE(YDCTX%ZGPF(YDGEOMETRY%YRGEM%NGPTOT,YDML_GCONF%YGFL%NUMGPFLDS*YDGEOMETRY%YRDIMV%NFLEVG))

YDCTX%ZSPA3BC = YDSPEC%SP3D
IF (YDGEOMETRY%YRMP%NPSP == 1) YDCTX%ZSPA2BC = YDSPEC%SP2D
YDCTX%ZSPA1BC = YDSPEC%SP1D

DO JKGLO=1,YDGEOMETRY%YRGEM%NGPTOT,YDGEOMETRY%YRDIM%NPROMA
  IBL=(JKGLO-1)/YDGEOMETRY%YRDIM%NPROMA+1
  IST=1
  IEND=MIN(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRGEM%NGPTOT-JKGLO+1)
  IGFL=0
  DO JGFL=1,YDML_GCONF%YGFL%NUMFLDS
    SELECT CASE (YDML_GCONF%YGFL%YCOMP(JGFL)%LGP)
    CASE(.TRUE.)
      IGFL=IGFL+1
      IOFF=(IGFL-1)*YDGEOMETRY%YRDIMV%NFLEVG
      DO JLEV=1,YDGEOMETRY%YRDIMV%NFLEVG
        DO JROF=IST,IEND
          YDCTX%ZGPF(JROF+JKGLO-1,IOFF+JLEV)=YDGFL%GFL(JROF,JLEV,YDML_GCONF%YGFL%YCOMP(JGFL)%MP,IBL)
        ENDDO
      ENDDO
    END SELECT
  ENDDO
ENDDO

IF (LHOOK) CALL DR_HOOK ('ERLBC_MOD:ERLBC_SAVE_CTX',1,ZHOOK_HANDLE)

END SUBROUTINE ERLBC_SAVE_CTX

SUBROUTINE ERLBC_LOAD_CTX(YDGEOMETRY,YDGFL,YGFL,YDSPEC,YDCTX)

USE YOM_YGFL     , ONLY : TYPE_GFLD
USE GEOMETRY_MOD , ONLY : GEOMETRY
TYPE(GEOMETRY)  ,INTENT(IN)     :: YDGEOMETRY
TYPE(TGFL)      ,INTENT(INOUT)  :: YDGFL
TYPE(TYPE_GFLD) ,INTENT(INOUT)  :: YGFL
TYPE(SPECTRAL_FIELD),INTENT(INOUT) :: YDSPEC
TYPE(ERLBC_CTX) ,INTENT (INOUT) :: YDCTX
INTEGER(KIND=JPIM) :: JGFL, IGFL, JLEV, IOFF, JKGLO, IBL, IST, IEND, JROF

REAL (KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK ('ERLBC_MOD:ERLBC_LOAD_CTX',0,ZHOOK_HANDLE)

YDSPEC%SP3D = YDCTX%ZSPA3BC
IF (YDGEOMETRY%YRMP%NPSP == 1) YDSPEC%SP2D = YDCTX%ZSPA2BC
YDSPEC%SP1D = YDCTX%ZSPA1BC

DO JKGLO=1,YDGEOMETRY%YRGEM%NGPTOT,YDGEOMETRY%YRDIM%NPROMA
IBL=(JKGLO-1)/YDGEOMETRY%YRDIM%NPROMA+1
  IST=1
  IEND=MIN(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRGEM%NGPTOT-JKGLO+1)
  IGFL=0
  DO JGFL=1,YGFL%NUMFLDS
    SELECT CASE (YGFL%YCOMP(JGFL)%LGP)
    CASE(.TRUE.)
      IGFL=IGFL+1
      IOFF=(IGFL-1)*YDGEOMETRY%YRDIMV%NFLEVG
      DO JLEV=1,YDGEOMETRY%YRDIMV%NFLEVG
        DO JROF=IST,IEND
          YDGFL%GFL(JROF,JLEV,YGFL%YCOMP(JGFL)%MP,IBL)=YDCTX%ZGPF(JROF+JKGLO-1,IOFF+JLEV)
        ENDDO
      ENDDO
    END SELECT
  ENDDO
ENDDO

DEALLOCATE(YDCTX%ZSPA3BC)
DEALLOCATE(YDCTX%ZSPA2BC)
DEALLOCATE(YDCTX%ZSPA1BC)
DEALLOCATE(YDCTX%ZGPF)

NULLIFY(YDCTX%ZSPA3BC)
NULLIFY(YDCTX%ZSPA2BC)
NULLIFY(YDCTX%ZSPA1BC)
NULLIFY(YDCTX%ZGPF)

IF (LHOOK) CALL DR_HOOK ('ERLBC_MOD:ERLBC_LOAD_CTX',1,ZHOOK_HANDLE)

END SUBROUTINE ERLBC_LOAD_CTX

END MODULE ERLBC_MOD

