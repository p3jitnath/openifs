      YDT9%MPREHYDS = IPTS
    ENDIF

  ENDIF

ENDIF

IF( YDDYNA%LGRADSP ) THEN
  IPT = IPT+1
  YDT9%MSGRTL = IPT
  IPT = IPT+1
  YDT9%MSGRTM = IPT
