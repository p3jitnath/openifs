YDT9%MCVNL = NUNDEFLD
YDT9%MCTNL = NUNDEFLD
YDT9%MCSPNL = NUNDEFLD
YDT9%MCVWVNL = NUNDEFLD
YDT9%MCSPDNL = NUNDEFLD
YDT9%MCUPT = NUNDEFLD
YDT9%MCVPT = NUNDEFLD
YDT9%MCTPT = NUNDEFLD
YDT9%MCSPDPT = NUNDEFLD
YDT9%MCSVDPT = NUNDEFLD
YDT9%MDPHI = NUNDEFLD

YDT9%MCURHS = NUNDEFLD
YDT9%MCVRHS = NUNDEFLD
YDT9%MCTRHS = NUNDEFLD
YDT9%MCSPDRHS = NUNDEFLD
YDT9%MCSVDRHS = NUNDEFLD
YDT9%MCSPRHS = NUNDEFLD
YDT9%MNHXNL = NUNDEFLD
YDT9%MCNHXNL = NUNDEFLD
YDT9%MSGRTL= NUNDEFLD
YDT9%MSGRTM= NUNDEFLD

! Field pointers in GMVS
YDT9%MSP = NUNDEFLD
YDT9%MSPL = NUNDEFLD
YDT9%MSPM = NUNDEFLD
YDT9%MCSPPT = NUNDEFLD
YDT9%MDBBC = NUNDEFLD
YDT9%MGWS = NUNDEFLD
YDT9%MSPNL2 = NUNDEFLD
YDT9%MCSPNL2 = NUNDEFLD
YDT9%MPREHYDS = NUNDEFLD

IPT  = YDT0%NDIM
IPTS = YDT0%NDIMS

IF (NCONF /= 701) THEN

  IF (YDDYNA%LTWOTL) THEN

    ! * GMV:
    IPT    = IPT+1
    YDT9%MU = IPT
    IPT    = IPT+1
    YDT9%MV = IPT
    IF (LRSHW) THEN
      IPT    = IPT+1
      YDT9%MSPNL = IPT
    ELSE
      IPT    = IPT+1
      YDT9%MEDOT = IPT
      IF( .NOT.YDDYNA%LNESC )THEN
        IPT = IPT+1
        YDT9%MUNL = IPT
        IPT = IPT+1
        YDT9%MVNL = IPT
        IPT = IPT+1
        YDT9%MTNL =IPT
        IF (YDDYNA%LSLINL) THEN
          IPT = IPT+1
          YDT9%MUNL_SI = IPT
          IPT = IPT+1
          YDT9%MVNL_SI = IPT
          IPT = IPT+1
          YDT9%MTNL_SI = IPT
        ENDIF
        IPT = IPT+1
        YDT9%MSPNL = IPT
        IF (YDDYNA%LSLINLC2) THEN
          IPT = IPT+1
          YDT9%MSPNL_SI = IPT
        ENDIF
      ENDIF
      IF(YDDYNA%LPC_FULL)THEN
        IPT = IPT+1
        YDT9%MT = IPT
        IPT = IPT+1
        YDT9%MTL = IPT
        IPT = IPT+1
        YDT9%MTM = IPT
        IF(YDDYNA%LNHDYN) THEN
          IPT = IPT+1
          YDT9%MSPD=IPT
          IPT = IPT+1
          YDT9%MSVD=IPT
          IPT = IPT+1
          YDT9%MSPDL=IPT
          IPT = IPT+1
          YDT9%MSVDL=IPT
          IPT = IPT+1
          YDT9%MSPDM=IPT
          IPT = IPT+1
          YDT9%MSVDM=IPT
          IF(YDDYNA%LGWADV)THEN
            IPT    = IPT+1
            YDT9%MGW= IPT
          ENDIF
        ENDIF
      ENDIF
      IF( YDDYNA%LPC_FULL )THEN
        IPT = IPT+1
        YDT9%MCUNL=IPT
        IPT = IPT+1
        YDT9%MCVNL=IPT
        IPT = IPT+1
        YDT9%MCTNL=IPT
        IPT = IPT+1
        YDT9%MCSPNL=IPT
        IF(YDDYNA%LNHDYN) THEN
          IPT = IPT+1
          YDT9%MCVWVNL=IPT
        ENDIF
        IF(YDDYNA%LNHEE) THEN
          IPT = IPT+1
          YDT9%MCSPDNL=IPT
        ENDIF
      ENDIF
      IF(YDDYNA%LPC_FULL.AND.(LMPHYS.OR.LEPHYS))THEN
        IPT = IPT+1
        YDT9%MCUPT = IPT
        IPT = IPT+1
        YDT9%MCVPT = IPT
        IPT = IPT+1
        YDT9%MCTPT = IPT
        IF(YDDYNA%LNHEE) THEN
          IPT = IPT+1
          YDT9%MCSPDPT = IPT
        ENDIF
        IF(YDDYNA%LNHDYN) THEN
          IPT = IPT+1
          YDT9%MCSVDPT = IPT
        ENDIF
      ENDIF
      IF(YDDYNA%LNHDYN) THEN
        IF( .NOT.YDDYNA%LNESC )THEN
          IPT = IPT+1
          YDT9%MVWVNL=IPT
          IF (YDDYNA%LNHEE) THEN
            IPT = IPT+1
            YDT9%MSPDNL=IPT
          ENDIF
          IF (YDDYNA%LSLINL) THEN
            IPT = IPT+1
            YDT9%MSVDNL_SI=IPT
          ENDIF
          IF (YDDYNA%LNHEE.AND.YDDYNA%LSLINL) THEN
            IPT = IPT+1
            YDT9%MSPDNL_SI=IPT
          ENDIF
        ENDIF
        IF (YDDYNA%NVDVAR==4) THEN
          IPT = IPT+1
          YDT9%MNHX=IPT
        ENDIF
      ENDIF
      IF(YDDYNA%LNHDYN.AND.YDDYNA%LRDBBC) THEN
        IPT = IPT+1
        YDT9%MDPHI = IPT
      ENDIF
    ENDIF

    ! * GMVS:
    IF(YDDYNA%LPC_FULL.OR.LDTENC)THEN
      IPTS    = IPTS+1
      YDT9%MSP  = IPTS
      IPTS    = IPTS+1
      YDT9%MSPM = IPTS
      IPTS    = IPTS+1
      YDT9%MSPL = IPTS
    ENDIF
    IF(YDDYNA%LPC_FULL.AND.LMPHYS)THEN
      IPTS    = IPTS+1
      YDT9%MCSPPT = IPTS
    ENDIF
    IF(YDDYNA%LNHDYN.AND.YDDYNA%LRDBBC) THEN
      IPTS    = IPTS+1
      YDT9%MDBBC = IPTS
      IF(YDDYNA%LPC_FULL) THEN
        IPTS    = IPTS+1
        YDT9%MGWS = IPTS
      ENDIF
    ENDIF
    IF(YDDYNA%LNHQE.AND.(.NOT.YDDYNA%LNHQE_SIHYD)) THEN
      IPTS    = IPTS+1
      YDT9%MPREHYDS = IPTS
    ENDIF

  ELSE

    ! LPC_FULL pointers only valid for Eulerian code only.
    ! LPC_FULL not yet coded for SL3TL.

    ! * GMV:
    IPT   = IPT+1
    YDT9%MDIV = IPT
    IPT   = IPT+1
    YDT9%MU = IPT
    IPT   = IPT+1
    YDT9%MV = IPT
    IF(NFTHER > 0) THEN
      IPT   = IPT+1
      YDT9%MT = IPT
      IPT   = IPT+1
      YDT9%MTL = IPT
      IPT   = IPT+1
      YDT9%MTM = IPT
    ENDIF
    IF(YDDYNA%LNHDYN) THEN
      IPT   = IPT+1
      YDT9%MSPD = IPT
      IPT   = IPT+1
      YDT9%MSVD = IPT
      IPT   = IPT+1
      YDT9%MSPDL = IPT
      IPT   = IPT+1
      YDT9%MSVDL = IPT
      IPT   = IPT+1
      YDT9%MSPDM = IPT
      IPT   = IPT+1
      YDT9%MSVDM = IPT
      IF (YDDYNA%NVDVAR == 4) THEN
        IPT   = IPT+1
        YDT9%MNHX= IPT
        IF(YDDYNA%LPC_FULL)THEN
          IPT = IPT+1
          YDT9%MNHXNL=IPT
          IPT = IPT+1
          YDT9%MCNHXNL=IPT
        ENDIF
      ENDIF
    ENDIF
    IF( YDDYNA%LPC_FULL )THEN
      IPT = IPT+1
      YDT9%MUNL = IPT
      IPT = IPT+1
      YDT9%MVNL = IPT
      IPT = IPT+1
      YDT9%MTNL =IPT
      IF( YDDYNA%LNHDYN )THEN
        IPT = IPT+1
