TYPE(TYPE_T9), INTENT(OUT) :: YDT9

INTEGER(KIND=JPIM) :: IPT,IPTS
LOGICAL :: LLP
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('GMV_SUBS_MOD:SETUP_T9',0,ZHOOK_HANDLE)
ASSOCIATE(NFTHER=>YDDIMF%NFTHER, &
