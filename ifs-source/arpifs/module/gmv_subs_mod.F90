SUBROUTINE SETUP_T0(YDDIMF,YDDYNA,YDT0)
USE YOMCT0 , ONLY : LECMWF
USE YOMDIMF, ONLY : TDIMF
TYPE(TDIMF)  ,INTENT(INOUT):: YDDIMF
TYPE(TDYNA)  ,INTENT(INOUT):: YDDYNA
TYPE(TYPE_T0),INTENT(OUT)  :: YDT0

INTEGER(KIND=JPIM) :: IPT,IPTS
LOGICAL :: LLP
REAL(KIND=JPHOOK)    :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('GMV_SUBS_MOD:SETUP_T0',0,ZHOOK_HANDLE)
ASSOCIATE(LADER=>YDDIMF%LADER, LUVDER=>YDDIMF%LUVDER)
LLP = NPRINTLEV > 1.OR. LALLOPR
YDT0%MU    = NUNDEFLD
YDT0%MV    = NUNDEFLD
YDT0%MT    = NUNDEFLD
YDT0%MTL   = NUNDEFLD
YDT0%MTM   = NUNDEFLD
YDT0%MDIV  = NUNDEFLD
YDT0%MVOR  = NUNDEFLD
YDT0%MUL   = NUNDEFLD
YDT0%MVL   = NUNDEFLD
YDT0%MSPD  = NUNDEFLD
YDT0%MSPDL = NUNDEFLD
YDT0%MSPDM = NUNDEFLD
YDT0%MSVD  = NUNDEFLD
YDT0%MSVDL = NUNDEFLD
YDT0%MSVDM = NUNDEFLD
YDT0%MNHX  = NUNDEFLD
YDT0%MNHXL = NUNDEFLD
YDT0%MNHXM = NUNDEFLD
YDT0%MEDOT = NUNDEFLD
YDT0%MSGRTL= NUNDEFLD
YDT0%MSGRTM= NUNDEFLD

! Field pointers in GMVS
YDT0%MSP   = NUNDEFLD
YDT0%MSPL  = NUNDEFLD
YDT0%MSPM  = NUNDEFLD

IPT  = 0
IPTS = 0
IF (LADER) THEN
  IPT = IPT+1
  YDT0%MVOR = IPT
  IPT = IPT+1
  YDT0%MDIV = IPT
ENDIF
IPT = IPT+1
YDT0%MU = IPT
IPT = IPT+1
YDT0%MV = IPT
IF ((.NOT.LECMWF).OR.LUVDER) THEN
  IPT = IPT+1
  YDT0%MUL = IPT
  IPT = IPT+1
  YDT0%MVL = IPT
ENDIF
YDT0%NDIMUV=IPT

IF (.NOT.LRSHW) THEN
  IPT = IPT+1
  YDT0%MT = IPT
  IF(YDDYNA%LNHDYN) THEN
    IPT = IPT+1
    YDT0%MSPD = IPT
    IPT = IPT+1
    YDT0%MSVD = IPT
    IF(YDDYNA%LNHX) THEN
      IPT = IPT+1
      YDT0%MNHX = IPT
    ENDIF
  ENDIF
  IF (LADER) THEN
    IPT = IPT+1
    YDT0%MTM = IPT
    IF(YDDYNA%LNHDYN) THEN
      IPT = IPT+1
      YDT0%MSPDM = IPT
      IPT = IPT+1
      YDT0%MSVDM = IPT
      IF(YDDYNA%LNHXDER) THEN
        IPT = IPT+1
        YDT0%MNHXM = IPT
      ENDIF
    ENDIF
  ENDIF
  IF (LADER) THEN
    IPT = IPT+1
    YDT0%MTL = IPT
    IF(YDDYNA%LNHDYN) THEN
      IPT = IPT+1
      YDT0%MSPDL = IPT
      IPT = IPT+1
      YDT0%MSVDL = IPT
      IF(YDDYNA%LNHXDER) THEN
        IPT = IPT+1
        YDT0%MNHXL = IPT
      ENDIF
    ENDIF
  ENDIF
ENDIF

IF (YDDYNA%LRUBC) THEN
  IPT = IPT+1
  YDT0%MEDOT = IPT
ENDIF

IPTS    = IPTS+1
YDT0%MSP  = IPTS
IF (LADER) THEN
  IPTS    = IPTS+1
  YDT0%MSPM = IPTS
  IPTS    = IPTS+1
  YDT0%MSPL = IPTS
ENDIF

IF( YDDYNA%LGRADSP ) THEN
  IPT = IPT+1
  YDT0%MSGRTL = IPT
  IPT = IPT+1
  YDT0%MSGRTM = IPT
ENDIF

YDT0%NDIM  = IPT
YDT0%NDIMS = IPTS

IF (LLP) WRITE(NULOUT,*)' MU=',YDT0%MU,'MV=',YDT0%MV,&
 & ' MT=',YDT0%MT,' MTL=',YDT0%MTL,' MTM=',YDT0%MTM
IF (LLP) WRITE(NULOUT,*)' MEDOT=',YDT0%MEDOT
IF (LLP) WRITE(NULOUT,*)' MDIV=',YDT0%MDIV,' MVOR=',YDT0%MVOR
IF (LLP) WRITE(NULOUT,*)' MUL=',YDT0%MUL,' MVL=',YDT0%MVL,' MSPD=',YDT0%MSPD,&
 & ' MSPDL=',YDT0%MSPDL,' MSPDM=',YDT0%MSPDM
IF (LLP) WRITE(NULOUT,*)' MSGRTL=',YDT0%MSGRTL,' MSGRTM=',YDT0%MSGRTM
IF (LLP) WRITE(NULOUT,*)' MSVD=',YDT0%MSVD,' MSVDL=',YDT0%MSVDL,' MSVDM=',YDT0%MSVDM
IF (LLP) WRITE(NULOUT,*)' MNHX=',YDT0%MNHX,' MNHXL=',YDT0%MNHXL,' MNHXM=',YDT0%MNHXM
IF (LLP) WRITE(NULOUT,*)' MSP=',YDT0%MSP,' MSPL=',YDT0%MSPL,' MSPM=',YDT0%MSPM

END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('GMV_SUBS_MOD:SETUP_T0',1,ZHOOK_HANDLE)

END SUBROUTINE SETUP_T0

