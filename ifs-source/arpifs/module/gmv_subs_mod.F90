YDT9%MTNL_SI = NUNDEFLD
YDT9%MSPNL = NUNDEFLD
YDT9%MSPNL_SI = NUNDEFLD
YDT9%MVWVNL = NUNDEFLD
YDT9%MSVDNL_SI = NUNDEFLD
YDT9%MSPDNL = NUNDEFLD
YDT9%MSPDNL_SI = NUNDEFLD
YDT9%MDIV = NUNDEFLD
YDT9%MVOR = NUNDEFLD
YDT9%MUL = NUNDEFLD
YDT9%MVL = NUNDEFLD
YDT9%MSPD = NUNDEFLD
YDT9%MSPDL = NUNDEFLD
