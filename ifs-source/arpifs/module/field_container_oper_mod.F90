CALL YDFIELDS(1)%OBJ%GET_ACTIVE_FIELDS(IACTIVE)
INFIELDS = SIZE(IACTIVE)

LL_SETUP_IOREQUEST=.TRUE.
IF(PRESENT(YD_IOSTREAM).AND.PRESENT(YD_IOREQUEST)) THEN
  YL_IOSTREAM=>YD_IOSTREAM        
  YL_IOREQUEST=>YD_IOREQUEST
  LL_SETUP_IOREQUEST=.FALSE.
ELSE  
  ALLOCATE(YL_IOSTREAM)        
  ALLOCATE(YL_IOREQUEST)
  CALL SETUP_IOSTREAM(YL_IOSTREAM,'CIO',TRIM(CLFN),CDMODE='w', &
  &                 KIOPROCS=NIOPROCS(1:NUM_IO_PROCS))
  CALL SETUP_IOREQUEST(YL_IOREQUEST,'RAW')
ENDIF  

IBUF(1) = NGPTOT
CALL IO_PUT(YL_IOSTREAM,YL_IOREQUEST,KR1=IBUF)

IBUF(1)=INFIELDS
CALL IO_PUT(YL_IOSTREAM,YL_IOREQUEST,KR1=IBUF)

IF(NUM_PROCS_PER_IO_PROC>1) THEN
  ! NDIMS=1
  ALLOCATE(ZGP3A(NPROMA,1,NGPBLKS))
  CALL YL_MULTI_IO_1D%SETUP('G',NPROMA*NGPBLKS,NIOPROCS,NUM_IO_PROCS)
  ! NDIMS=2
  ALLOCATE(ZGP3(NPROMA,NFLEVG,NGPBLKS))
  CALL YL_MULTI_IO_2D%SETUP('G',NPROMA*NFLEVG*NGPBLKS,NIOPROCS,NUM_IO_PROCS)
  DO JFLDS=1,INUMFLDS
    DO JFLD=1,INFIELDS
      ID = IACTIVE(JFLD)
      IF(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%NDIMS == 1) THEN
        DO JBLK=1,NGPBLKS
          IKGLO = 1+(JBLK-1)*NPROMA
          IST = 1
          IEND = MIN(NPROMA,NGPTOT-IKGLO+1)
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_OPEN_ONE(ID,JBLK,ZFIELD1)
          ZGP3A(IST:IEND,1,JBLK) = ZFIELD1(IST:IEND)
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_CLOSE_ONE(ID,JBLK,ZFIELD1)
        ENDDO
        CALL YL_MULTI_IO_1D%GATHER(ZGP3A)
        IBUF(1)=ID
        IBUF(2)=SIZE(YL_MULTI_IO_1D%ZBUF)
        CALL IO_PUT(YL_IOSTREAM,YL_IOREQUEST,KR1=IBUF)
        CALL IO_PUT(YL_IOSTREAM,YL_IOREQUEST,PR1=YL_MULTI_IO_1D%ZBUF)
      ELSEIF(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%NDIMS == 2) THEN
        INLEVS = YDFIELDS(JFLDS)%OBJ%NLEVELS(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%D2TYPE)
        DO JBLK=1,NGPBLKS
          IKGLO = 1+(JBLK-1)*NPROMA
          IST = 1
          IEND = MIN(NPROMA,NGPTOT-IKGLO+1)
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_OPEN_ONE(ID,JBLK,ZFIELD2)
          DO JF=1,INLEVS
            ZGP3(IST:IEND,JF,JBLK) = ZFIELD2(IST:IEND,JF)
          ENDDO
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_CLOSE_ONE(ID,JBLK,ZFIELD2)
        ENDDO
        CALL YL_MULTI_IO_2D%GATHER(ZGP3)
        IBUF(1)=ID
        IBUF(2)=SIZE(YL_MULTI_IO_2D%ZBUF)
        CALL IO_PUT(YL_IOSTREAM,YL_IOREQUEST,KR1=IBUF)
        CALL IO_PUT(YL_IOSTREAM,YL_IOREQUEST,PR1=YL_MULTI_IO_2D%ZBUF)
      ELSE
        INLEVS = YDFIELDS(JFLDS)%OBJ%NLEVELS(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%D2TYPE)
        IN3D   = YDFIELDS(JFLDS)%OBJ%NDIM3S(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%D3TYPE)
        ALLOCATE(ZGP4(NPROMA,INLEVS,IN3D,NGPBLKS))
        CALL YL_MULTI_IO_3D%SETUP('G',NPROMA*INLEVS*IN3D*NGPBLKS,NIOPROCS,NUM_IO_PROCS)
        DO JBLK=1,NGPBLKS
          IKGLO = 1+(JBLK-1)*NPROMA
          IST = 1
          IEND = MIN(NPROMA,NGPTOT-IKGLO+1)
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_OPEN_ONE(ID,JBLK,ZFIELD3)
          DO JF=1,IN3D
            DO JLEV=1,INLEVS
              ZGP4(IST:IEND,JLEV,JF,JBLK) = ZFIELD3(IST:IEND,JLEV,JF)
            ENDDO
          ENDDO
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_CLOSE_ONE(ID,JBLK,ZFIELD3)
        ENDDO
        CALL YL_MULTI_IO_3D%GATHER(ZGP4)
        IBUF(1)=ID
        IBUF(2)=SIZE(YL_MULTI_IO_3D%ZBUF)
        CALL IO_PUT(YL_IOSTREAM,YL_IOREQUEST,KR1=IBUF)
        CALL IO_PUT(YL_IOSTREAM,YL_IOREQUEST,PR1=YL_MULTI_IO_3D%ZBUF)
        DEALLOCATE(ZGP4)
        CALL YL_MULTI_IO_3D%RESET()
      ENDIF
    ENDDO
  ENDDO
  DEALLOCATE(ZGP3A,ZGP3)
  CALL YL_MULTI_IO_1D%RESET()
  CALL YL_MULTI_IO_2D%RESET()
ELSE
  DO JFLDS=1,INUMFLDS
    DO JFLD=1,INFIELDS
      ID = IACTIVE(JFLD)
      IBUF(:)=0_JPIM
      IF(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%NDIMS == 1) THEN
        ALLOCATE(ZGP3(NPROMA,1,NGPBLKS))
        DO JBLK=1,NGPBLKS
          IKGLO = 1+(JBLK-1)*NPROMA
          IST = 1
          IEND = MIN(NPROMA,NGPTOT-IKGLO+1)
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_OPEN_ONE(ID,JBLK,ZFIELD1)
          ZGP3(IST:IEND,1,JBLK) = ZFIELD1(IST:IEND)
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_CLOSE_ONE(ID,JBLK,ZFIELD1)
        ENDDO
        IBUF(1)=ID
        CALL IO_PUT(YL_IOSTREAM,YL_IOREQUEST,KR1=IBUF)
        CALL IO_PUT(YL_IOSTREAM,YL_IOREQUEST,PR3=ZGP3)
        DEALLOCATE(ZGP3)
      ELSEIF(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%NDIMS == 2) THEN
        INLEVS = YDFIELDS(JFLDS)%OBJ%NLEVELS(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%D2TYPE)
        ALLOCATE(ZGP3(NPROMA,INLEVS,NGPBLKS))
        DO JBLK=1,NGPBLKS
          IKGLO = 1+(JBLK-1)*NPROMA
          IST = 1
          IEND = MIN(NPROMA,NGPTOT-IKGLO+1)
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_OPEN_ONE(ID,JBLK,ZFIELD2)
          DO JF=1,INLEVS
            ZGP3(IST:IEND,JF,JBLK) = ZFIELD2(IST:IEND,JF)
          ENDDO
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_CLOSE_ONE(ID,JBLK,ZFIELD2)
        ENDDO
        IBUF(1)=ID
        CALL IO_PUT(YL_IOSTREAM,YL_IOREQUEST,KR1=IBUF)
        CALL IO_PUT(YL_IOSTREAM,YL_IOREQUEST,PR3=ZGP3)
        DEALLOCATE(ZGP3)
      ELSE
        INLEVS = YDFIELDS(JFLDS)%OBJ%NLEVELS(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%D2TYPE)
        IN3D   = YDFIELDS(JFLDS)%OBJ%NDIM3S(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%D3TYPE)
        ALLOCATE(ZGP4(NPROMA,INLEVS,IN3D,NGPBLKS))
        DO JBLK=1,NGPBLKS
          IKGLO = 1+(JBLK-1)*NPROMA
          IST = 1
          IEND = MIN(NPROMA,NGPTOT-IKGLO+1)
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_OPEN_ONE(ID,JBLK,ZFIELD3)
          DO JF=1,IN3D
            DO JLEV=1,INLEVS
              ZGP4(IST:IEND,JLEV,JF,JBLK) = ZFIELD3(IST:IEND,JLEV,JF)
            ENDDO
          ENDDO
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_CLOSE_ONE(ID,JBLK,ZFIELD3)
        ENDDO
        IBUF(1)=ID
        CALL IO_PUT(YL_IOSTREAM,YL_IOREQUEST,KR1=IBUF)
        CALL IO_PUT(YL_IOSTREAM,YL_IOREQUEST,PR4=ZGP4)
        DEALLOCATE(ZGP4)
      ENDIF
    ENDDO
  ENDDO
ENDIF

IF(LL_SETUP_IOREQUEST) THEN
  CALL CLOSE_IOREQUEST(YL_IOREQUEST)
  CALL CLOSE_IOSTREAM(YL_IOSTREAM)
  DEALLOCATE(YL_IOREQUEST)
  DEALLOCATE(YL_IOSTREAM)
