SUBROUTINE FIELD_CONTAINER_INTERP(YDGEOM_IN,YDGEOM_OUT,FIELD_IN,FIELD_OUT,&
 &                                KFIDS,KFIDS_SPEC,PSPSP,PGPSPGLO,&
 &                                KINTERPSURF)
IMPLICIT NONE
TYPE(GEOMETRY) , INTENT(IN)    :: YDGEOM_IN
TYPE(GEOMETRY) , INTENT(IN)    :: YDGEOM_OUT
TYPE(FIELD_CONTAINER_GP) , INTENT(INOUT)    :: FIELD_IN
TYPE(FIELD_CONTAINER_GP) , INTENT(INOUT) :: FIELD_OUT
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KFIDS(:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KFIDS_SPEC(:)
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN) :: KINTERPSURF
REAL(KIND=JPRB),OPTIONAL,   INTENT(IN) :: PSPSP(:)
REAL(KIND=JPRB),OPTIONAL,   INTENT(IN) :: PGPSPGLO(:,:)

REAL(KIND=JPRB),POINTER :: ZFIELD1(:),ZFIELD2(:,:),ZFIELD3(:,:,:)
REAL(KIND=JPRB),ALLOCATABLE :: ZGP3_IN(:,:,:),ZGP3_OUT(:,:,:)
REAL(KIND=JPRB),ALLOCATABLE :: ZGP4_IN(:,:,:,:),ZGP4_OUT(:,:,:,:)
INTEGER(KIND=JPIM),ALLOCATABLE :: IACTIVE(:),IFIDS_INTERP(:)
INTEGER(KIND=JPIM) :: INFIELDS,JFLD,IST,IEND,ID,IKGLO,JBLK,JF,INLEVS,INUM2D,JLEV,IN3D
INTEGER(KIND=JPIM) :: IPARAMID,INTERPSURF
CHARACTER(LEN=3)   :: CLLEVTYPE
REAL(KIND=JPHOOK)    :: ZHOOK_HANDLE

#include "grid_psglobal.intfb.h"

IF (LHOOK) CALL DR_HOOK('FIELD_CONTAINER_OPER_MOD:FIELD_CONTAINER_INTERP',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM_IN=>YDGEOM_IN%YRDIM,YDDIMV_IN=>YDGEOM_IN%YRDIMV,YDGEM_IN=>YDGEOM_IN%YRGEM,&
 & YDDIM_OUT=>YDGEOM_OUT%YRDIM,YDDIMV_OUT=>YDGEOM_OUT%YRDIMV,YDGEM_OUT=>YDGEOM_OUT%YRGEM)
ASSOCIATE(NGPBLKS_IN=>YDDIM_IN%NGPBLKS, NPROMA_IN=>YDDIM_IN%NPROMA, &
 & NGPTOT_IN=>YDGEM_IN%NGPTOT,NFLEVG_IN=>YDDIMV_IN%NFLEVG,NGPTOTG_IN=>YDGEM_IN%NGPTOTG,&
 & NGPBLKS_OUT=>YDDIM_OUT%NGPBLKS, NPROMA_OUT=>YDDIM_OUT%NPROMA,&
 & NGPTOT_OUT=>YDGEM_OUT%NGPTOT,NFLEVG_OUT=>YDDIMV_OUT%NFLEVG)

IF(PRESENT(KFIDS)) THEN
  ALLOCATE(IACTIVE(SIZE(KFIDS)))
  IACTIVE(:) = KFIDS(:)
ELSE
  CALL FIELD_OUT%GET_ACTIVE_FIELDS(IACTIVE)
ENDIF
INFIELDS = SIZE(IACTIVE)

IF(PRESENT(KFIDS_SPEC)) THEN
  CALL FIELD_CONTAINER_COPY_SPEC(YDGEOM_IN,YDGEOM_OUT,FIELD_IN,FIELD_OUT,KFIDS_SPEC)
ENDIF

! Single level surface fields are interpolated using nearest neighbour by default
INTERPSURF=0
IF(PRESENT(KINTERPSURF)) INTERPSURF=KINTERPSURF

IF (ALLOCATED(RGPPRS_HR)) DEALLOCATE(RGPPRS_HR)
IF (ALLOCATED(RGPPRS_LR)) DEALLOCATE(RGPPRS_LR)
IF(PRESENT(PSPSP)) THEN
  ALLOCATE(RGPPRS_HR(NGPTOTG_IN))
  CALL GRID_PSGLOBAL(YDGEOM_IN,PSPSP)
ELSEIF(PRESENT(PGPSPGLO)) THEN
  ALLOCATE(RGPPRS_HR(SIZE(PGPSPGLO,1)))
  RGPPRS_HR(:)=PGPSPGLO(:,1)
ENDIF

INUM2D = 0
DO JFLD=1,INFIELDS
  ID = IACTIVE(JFLD)
  IF(PRESENT(KFIDS_SPEC)) THEN
    IF(ANY(KFIDS_SPEC(:) == ID)) CYCLE
  ENDIF

  IF(FIELD_IN%METADATA(ID)%NDIMS == 1) THEN
    INUM2D = INUM2D+1
  ELSEIF(FIELD_IN%METADATA(ID)%NDIMS == 2) THEN
    IPARAMID = FIELD_IN%METADATA(ID)%IGRIBCODE
    CLLEVTYPE = FIELD_IN%CLEVTYPE(FIELD_IN%METADATA(ID)%D2TYPE)
    INLEVS = FIELD_IN%NLEVELS(FIELD_IN%METADATA(ID)%D2TYPE)
    ALLOCATE(ZGP3_IN(NPROMA_IN,INLEVS,NGPBLKS_IN))
    DO JBLK=1,NGPBLKS_IN
      IKGLO = 1+(JBLK-1)*NPROMA_IN
      IST = 1
      IEND = MIN(NPROMA_IN,NGPTOT_IN-IKGLO+1)
      CALL FIELD_IN%FIELD_OPEN_ONE(ID,JBLK,ZFIELD2)
      DO JF=1,INLEVS
        ZGP3_IN(IST:IEND,JF,JBLK) = ZFIELD2(IST:IEND,JF)
      ENDDO
      CALL FIELD_IN%FIELD_CLOSE_ONE(ID,JBLK,ZFIELD2)
    ENDDO
    ALLOCATE(ZGP3_OUT(NPROMA_OUT,INLEVS,NGPBLKS_OUT))
!    ZGP3_OUT = ZGP3_IN ! temporary
    CALL SINTERP(ID,YDGEOM_IN,YDGEOM_OUT,PGPIN=ZGP3_IN,PGPOUT=ZGP3_OUT, &
                & KPARAMID=IPARAMID, CDLEVTYPE=CLLEVTYPE)
    DO JBLK=1,NGPBLKS_OUT
      IKGLO = 1+(JBLK-1)*NPROMA_OUT
      IST = 1
      IEND = MIN(NPROMA_OUT,NGPTOT_OUT-IKGLO+1)
      CALL FIELD_OUT%FIELD_OPEN_ONE(ID,JBLK,ZFIELD2)
      DO JF=1,INLEVS
        ZFIELD2(IST:IEND,JF) = ZGP3_OUT(IST:IEND,JF,JBLK)
      ENDDO
      CALL FIELD_OUT%FIELD_CLOSE_ONE(ID,JBLK,ZFIELD2)
    ENDDO
    DEALLOCATE(ZGP3_IN)
    DEALLOCATE(ZGP3_OUT)
  ELSE
    IPARAMID = FIELD_IN%METADATA(ID)%IGRIBCODE
    CLLEVTYPE = FIELD_IN%CLEVTYPE(FIELD_IN%METADATA(ID)%D2TYPE)
    INLEVS = FIELD_IN%NLEVELS(FIELD_IN%METADATA(ID)%D2TYPE)
    IN3D   = FIELD_IN%NDIM3S(FIELD_IN%METADATA(ID)%D3TYPE)
    ALLOCATE(ZGP4_IN(NPROMA_IN,INLEVS,IN3D,NGPBLKS_IN))
    DO JBLK=1,NGPBLKS_IN
      IKGLO = 1+(JBLK-1)*NPROMA_IN
      IST = 1
      IEND = MIN(NPROMA_IN,NGPTOT_IN-IKGLO+1)
      CALL FIELD_IN%FIELD_OPEN_ONE(ID,JBLK,ZFIELD3)
      DO JF=1,IN3D
        DO JLEV=1,INLEVS
          ZGP4_IN(IST:IEND,JLEV,JF,JBLK) = ZFIELD3(IST:IEND,JLEV,JF)
        ENDDO
      ENDDO
      CALL FIELD_IN%FIELD_CLOSE_ONE(ID,JBLK,ZFIELD3)
    ENDDO
    ALLOCATE(ZGP4_OUT(NPROMA_OUT,INLEVS,IN3D,NGPBLKS_OUT))
!    ZGP3_OUT = ZGP3_IN ! temporary
    DO JF=1,IN3D
      CALL SINTERP(ID,YDGEOM_IN,YDGEOM_OUT,PGPIN=ZGP4_IN(:,:,JF,:),&
       & PGPOUT=ZGP4_OUT(:,:,JF,:), KPARAMID=IPARAMID, CDLEVTYPE=CLLEVTYPE)
    ENDDO
    DO JBLK=1,NGPBLKS_OUT
      IKGLO = 1+(JBLK-1)*NPROMA_OUT
      IST = 1
      IEND = MIN(NPROMA_OUT,NGPTOT_OUT-IKGLO+1)
      CALL FIELD_OUT%FIELD_OPEN_ONE(ID,JBLK,ZFIELD3)
      DO JF=1,IN3D
        DO JLEV=1,INLEVS
          ZFIELD3(IST:IEND,JLEV,JF) = ZGP4_OUT(IST:IEND,JLEV,JF,JBLK)
        ENDDO
      ENDDO
      CALL FIELD_OUT%FIELD_CLOSE_ONE(ID,JBLK,ZFIELD3)
    ENDDO
    DEALLOCATE(ZGP4_IN)
    DEALLOCATE(ZGP4_OUT)

  ENDIF
ENDDO

IF(INUM2D > 0) THEN
  ALLOCATE(ZGP3_IN(NPROMA_IN,INUM2D,NGPBLKS_IN))
  ALLOCATE(IFIDS_INTERP(INUM2D))

  INUM2D = 0
  DO JFLD=1,INFIELDS
    ID = IACTIVE(JFLD)
    IF(PRESENT(KFIDS_SPEC)) THEN
      IF(ANY(KFIDS_SPEC(:) == ID)) CYCLE
    ENDIF

    IF(FIELD_IN%METADATA(ID)%NDIMS == 1) THEN
      INUM2D = INUM2D+1
      IFIDS_INTERP(INUM2D) = ID
      DO JBLK=1,NGPBLKS_IN
        IKGLO = 1+(JBLK-1)*NPROMA_IN
        IST = 1
        IEND = MIN(NPROMA_IN,NGPTOT_IN-IKGLO+1)
        CALL FIELD_IN%FIELD_OPEN_ONE(ID,JBLK,ZFIELD1)
        ZGP3_IN(IST:IEND,INUM2D,JBLK) = ZFIELD1(IST:IEND)
        CALL FIELD_IN%FIELD_CLOSE_ONE(ID,JBLK,ZFIELD1)
      ENDDO
    ENDIF
  ENDDO

  ALLOCATE(ZGP3_OUT(NPROMA_OUT,INUM2D,NGPBLKS_OUT))
  ! FIXME: These ought to be passed through, but can't be because
  !   they are different for each field.
  ID = -1
  IPARAMID = -99
  CLLEVTYPE = 'SFC'
  CALL SINTERP(ID,YDGEOM_IN,YDGEOM_OUT,PGPIN=ZGP3_IN,PGPOUT=ZGP3_OUT, &
             & KPARAMID=IPARAMID, CDLEVTYPE=CLLEVTYPE, KINTERPSURF=INTERPSURF)
  DEALLOCATE(ZGP3_IN)

  INUM2D = 0
  DO JFLD=1,INFIELDS
    ID = IACTIVE(JFLD)
    IF(PRESENT(KFIDS_SPEC)) THEN
      IF(ANY(KFIDS_SPEC(:) == ID)) CYCLE
    ENDIF
    IF(FIELD_IN%METADATA(ID)%NDIMS == 1) THEN
      INUM2D = INUM2D+1
      DO JBLK=1,NGPBLKS_OUT
        IKGLO = 1+(JBLK-1)*NPROMA_OUT
        IST = 1
        IEND = MIN(NPROMA_OUT,NGPTOT_OUT-IKGLO+1)
        CALL FIELD_OUT%FIELD_OPEN_ONE(ID,JBLK,ZFIELD1)
        ZFIELD1(IST:IEND) = ZGP3_OUT(IST:IEND,INUM2D,JBLK)
        CALL FIELD_OUT%FIELD_CLOSE_ONE(ID,JBLK,ZFIELD1)
      ENDDO
    ENDIF
  ENDDO
  DEALLOCATE(ZGP3_OUT)
ENDIF

DEALLOCATE(IACTIVE)
END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('FIELD_CONTAINER_OPER_MOD:FIELD_CONTAINER_INTERP',1,ZHOOK_HANDLE)
END SUBROUTINE FIELD_CONTAINER_INTERP
