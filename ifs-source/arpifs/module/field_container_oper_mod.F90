SUBROUTINE FIELD_CONTAINER_WRITE_RESTART(YDGEOM,YDFIELDS,CDFNAME,KNUM_PROCS_PER_IO_PROC,&
 &                                       YD_IOSTREAM,YD_IOREQUEST)
USE YOMMP0, ONLY : MYPROC,NPROC
USE IOSTREAM_MIX , ONLY : SETUP_IOSTREAM, SETUP_IOREQUEST, IO_PUT,&
 &                        CLOSE_IOSTREAM, TYPE_IOSTREAM , TYPE_IOREQUEST,CLOSE_IOREQUEST
USE FIELD_CONTAINER_GP_MOD, ONLY : FIELD_CONTAINER_GP_CONTAINER
IMPLICIT NONE
TYPE(GEOMETRY),                     INTENT(IN)                     :: YDGEOM
TYPE(FIELD_CONTAINER_GP_CONTAINER), INTENT(INOUT)                  :: YDFIELDS(:)
CHARACTER (LEN=*),                  INTENT(IN)                     :: CDFNAME
INTEGER(KIND=JPIM),                 INTENT(IN),   OPTIONAL         :: KNUM_PROCS_PER_IO_PROC
TYPE(TYPE_IOSTREAM),                INTENT(IN),   OPTIONAL, TARGET :: YD_IOSTREAM
TYPE(TYPE_IOREQUEST),               INTENT(IN),   OPTIONAL, TARGET :: YD_IOREQUEST

LOGICAL :: LL_SETUP_IOREQUEST
CHARACTER (LEN = 256) ::  CLFN
CHARACTER (LEN = 5) ::  CLMYPROC
INTEGER(KIND=JPIM),ALLOCATABLE :: IACTIVE(:),IFIDSWRITE_RESTART(:)
INTEGER(KIND=JPIM) :: INFIELDS,JFLD,IST,IEND,ID,IKGLO,JBLK,JF,INLEVS,INUM2D,IBUF(2),IN3D,JLEV
INTEGER(KIND=JPIM) :: JK,IPROC,INUMFLDS,JFLDS
INTEGER(KIND=JPIM) :: NUM_PROCS_PER_IO_PROC,NUM_IO_PROCS
INTEGER(KIND=JPIM), ALLOCATABLE :: NIOPROCS(:)
REAL(KIND=JPRB),POINTER :: ZFIELD1(:),ZFIELD2(:,:),ZFIELD3(:,:,:)
REAL(KIND=JPRB),ALLOCATABLE :: ZGP3A(:,:,:),ZGP3(:,:,:),ZGP4(:,:,:,:)
TYPE(MULTI_IO) :: YL_MULTI_IO_1D, YL_MULTI_IO_2D, YL_MULTI_IO_3D
