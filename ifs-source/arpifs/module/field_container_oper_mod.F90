SUBROUTINE SINTERP(KID,YDGEOM_IN,YDGEOM_OUT,PGPIN,PGPOUT,KPARAMID,CDLEVTYPE,KINTERPSURF)
USE YOMMP0   , ONLY : MYPROC,NPROC
USE YOM_GRID_BICONSERV, ONLY : RGPPRS_HR
IMPLICIT NONE
INTEGER(KIND=JPIM), INTENT(IN)          :: KID
TYPE(GEOMETRY) , INTENT(IN)             :: YDGEOM_IN
TYPE(GEOMETRY) , INTENT(IN)             :: YDGEOM_OUT
REAL(KIND=JPRB),INTENT(IN)              :: PGPIN(:,:,:)
REAL(KIND=JPRB),INTENT(OUT)             :: PGPOUT(:,:,:)
INTEGER(KIND=JPIM),INTENT(IN)           :: KPARAMID
CHARACTER(LEN=3),INTENT(IN)             :: CDLEVTYPE
INTEGER(KIND=JPIM),OPTIONAL,INTENT(IN)  :: KINTERPSURF

LOGICAL :: LLINTERPSURF
REAL(KIND=JPRB),ALLOCATABLE :: ZGRIDG_IN(:,:),ZGRIDG_OUT(:,:)
INTEGER(KIND=JPIM),ALLOCATABLE :: ITO(:),ILEVS(:)
INTEGER(KIND=JPIM) :: IFLDSG,IFLDS,INTERP_TYPE,IDUM,JF,IL
REAL(KIND=JPHOOK)  :: ZHOOK_HANDLE

#include "gath_grid.h"
#include "dist_grid.h"
#include "interp_gp.intfb.h"

IF (LHOOK) CALL DR_HOOK('FIELD_CONTAINER_OPER_MOD:SINTERP',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM_IN=>YDGEOM_IN%YRDIM,YDDIMV_IN=>YDGEOM_IN%YRDIMV,YDGEM_IN=>YDGEOM_IN%YRGEM, &
 & YDCSGLEG_IN=>YDGEOM_IN%YRCSGLEG,YDVAB=>YDGEOM_IN%YRVAB, &
 & YDDIM_OUT=>YDGEOM_OUT%YRDIM,YDDIMV_OUT=>YDGEOM_OUT%YRDIMV,YDGEM_OUT=>YDGEOM_OUT%YRGEM,&
 & YDCSGLEG_OUT=>YDGEOM_OUT%YRCSGLEG)
ASSOCIATE(NRESOL_IN=>YDDIM_IN%NRESOL, NPROMA_IN=>YDDIM_IN%NPROMA, &
 & NGPTOT_IN=>YDGEM_IN%NGPTOT,NFLEVG_IN=>YDDIMV_IN%NFLEVG,&
 & NGPTOTG_IN=>YDGEM_IN%NGPTOTG,NDGLG_IN=>YDDIM_IN%NDGLG, &
 & NLOENG_IN=>YDGEM_IN%NLOENG,RLATIG_IN=>YDCSGLEG_IN%RLATIG, &
 & NRESOL_OUT=>YDDIM_OUT%NRESOL, NPROMA_OUT=>YDDIM_OUT%NPROMA,&
 & NGPTOT_OUT=>YDGEM_OUT%NGPTOT,NFLEVG_OUT=>YDDIMV_OUT%NFLEVG,&
 & NGPTOTG_OUT=>YDGEM_OUT%NGPTOTG,NDGLG_OUT=>YDDIM_OUT%NDGLG,&
 & NLOENG_OUT=>YDGEM_OUT%NLOENG,RLATIG_OUT=>YDCSGLEG_OUT%RLATIG)

! Check if performing interpolation of single level surface fields
LLINTERPSURF=.FALSE.
IF(PRESENT(KINTERPSURF)) LLINTERPSURF=.TRUE.

IFLDS = SIZE(PGPIN(1,:,1))
ALLOCATE(ITO(IFLDS))
DO JF=1,IFLDS
  ITO(JF) = MOD(JF-1,NPROC)+1
ENDDO
IFLDSG = COUNT(ITO(:) == MYPROC)
IF(IFLDSG > 0) THEN
  ALLOCATE(ZGRIDG_IN(NGPTOTG_IN,IFLDSG))
ELSE
  ALLOCATE(ZGRIDG_IN(1,1))
ENDIF
ALLOCATE(ILEVS(IFLDSG))
IF(LLINTERPSURF) THEN
  ILEVS(:) = 0
ELSE
  IL = 0
  DO JF=1,IFLDS
    IF(ITO(JF) == MYPROC) THEN
      IL = IL+1
      ILEVS(IL) = JF
    ENDIF
  ENDDO
ENDIF

CALL GATH_GRID(PGPG=ZGRIDG_IN,KFGATHG=IFLDS,KTO=ITO,PGP=PGPIN,KPROMA=NPROMA_IN,&
 & KRESOL=NRESOL_IN)
IF(LLINTERPSURF) THEN
  INTERP_TYPE = KINTERPSURF 
  IF (INTERP_TYPE == 3 .AND. .NOT. ALLOCATED(RGPPRS_HR)) &
   & CALL ABOR1("SINTERP: wrong interpolation method requested for surface fields") 
ELSE
  IF(ALLOCATED(RGPPRS_HR)) THEN
    INTERP_TYPE = 3 ! Conserving
  ELSE
    INTERP_TYPE = 1 ! Bi-linear
  ENDIF
ENDIF
IDUM=1
IF(IFLDSG > 0) THEN
  ALLOCATE(ZGRIDG_OUT(NGPTOTG_OUT,IFLDSG))
ELSE
  ALLOCATE(ZGRIDG_OUT(1,1))
ENDIF

DO JF=1,IFLDSG
  CALL INTERP_GP(INTERP_TYPE,KPARAMID,ILEVS(JF),&
   & NDGLG_IN,NLOENG_IN(1:),RLATIG_IN(1:),&
   & NDGLG_OUT,NLOENG_OUT(1:),RLATIG_OUT(1:),&
   & YDVAB%VAH,YDVAB%VBH, &
   & ZGRIDG_IN(:,JF),ZGRIDG_OUT(:,JF), &
   & CDLEVTYPE)
ENDDO
DEALLOCATE(ZGRIDG_IN)
CALL DIST_GRID(PGPG=ZGRIDG_OUT,KFDISTG=IFLDS,KFROM=ITO,PGP=PGPOUT,KPROMA=NPROMA_OUT,&
 & KRESOL=NRESOL_OUT)

END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('FIELD_CONTAINER_OPER_MOD:SINTERP',1,ZHOOK_HANDLE)
END SUBROUTINE SINTERP
