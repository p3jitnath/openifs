INTEGER(KIND=JPIM) :: INFIELDS,JFLD,IST,IEND,ID,IKGLO,JBLK,JF,INLEVS,INUM2D,IBUF(2),JLEV,IN3D
INTEGER(KIND=JPIM) :: JK,IPROC,INUMFLDS,JFLDS
INTEGER(KIND=JPIM) :: NUM_PROCS_PER_IO_PROC,NUM_IO_PROCS
INTEGER(KIND=JPIM), ALLOCATABLE :: NIOPROCS(:)
REAL(KIND=JPRB),POINTER :: ZFIELD1(:),ZFIELD2(:,:),ZFIELD3(:,:,:)
REAL(KIND=JPRB),ALLOCATABLE :: ZGP3A(:,:,:),ZGP3(:,:,:),ZGP4(:,:,:,:)
REAL(KIND=JPRB),ALLOCATABLE :: ZBUF(:),ZBUF2(:),ZBUF3(:)
TYPE(MULTI_IO) :: YL_MULTI_IO_1D, YL_MULTI_IO_2D, YL_MULTI_IO_3D
TYPE(TYPE_IOSTREAM), POINTER :: YL_IOSTREAM
TYPE(TYPE_IOREQUEST), POINTER :: YL_IOREQUEST
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

#include "abor1.intfb.h"

IF (LHOOK) CALL DR_HOOK('FIELD_CONTAINER_OPER_MOD:FIELD_CONTAINER_READ_RESTART',0,ZHOOK_HANDLE)

ASSOCIATE(YDDIM=>YDGEOM%YRDIM,YDDIMV=>YDGEOM%YRDIMV,YDGEM=>YDGEOM%YRGEM)
ASSOCIATE(NGPBLKS=>YDDIM%NGPBLKS, NPROMA=>YDDIM%NPROMA, &
 & NGPTOT=>YDGEM%NGPTOT,NFLEVG=>YDDIMV%NFLEVG,NGPTOTG=>YDGEM%NGPTOTG)

LL_IO_PROC=.FALSE.
NUM_PROCS_PER_IO_PROC=1
IF(PRESENT(KNUM_PROCS_PER_IO_PROC)) & 
  & NUM_PROCS_PER_IO_PROC=KNUM_PROCS_PER_IO_PROC
IF (MOD(NPROC,NUM_PROCS_PER_IO_PROC)/=0) &
  & CALL ABOR1('FIELD_CONTAINER_READ_RESTART WRONG I/O LAYOUT')
NUM_IO_PROCS=NPROC/NUM_PROCS_PER_IO_PROC
ALLOCATE(NIOPROCS(NUM_IO_PROCS))
IPROC=0
DO JK = NUM_PROCS_PER_IO_PROC,NPROC,NUM_PROCS_PER_IO_PROC
  IPROC=IPROC+1
  NIOPROCS(IPROC) = JK
  WRITE(CLMYPROC,'(A1,I4.4)')'.',MYPROC
  CLFN=TRIM(CDFNAME)//CLMYPROC
  IF (MYPROC == JK) THEN
    LL_IO_PROC=.TRUE.
  ENDIF
ENDDO
INUMFLDS=SIZE(YDFIELDS)

CALL YDFIELDS(1)%OBJ%GET_ACTIVE_FIELDS(IACTIVE)
INFIELDS = SIZE(IACTIVE)

LL_SETUP_IOREQUEST=.TRUE.
IF(PRESENT(YD_IOSTREAM).AND.PRESENT(YD_IOREQUEST)) THEN
  YL_IOSTREAM=>YD_IOSTREAM        
  YL_IOREQUEST=>YD_IOREQUEST
  LL_SETUP_IOREQUEST=.FALSE.
ELSE  
  ALLOCATE(YL_IOSTREAM)        
  ALLOCATE(YL_IOREQUEST)
  CALL SETUP_IOSTREAM(YL_IOSTREAM,'CIO',TRIM(CLFN),CDMODE='r', &
  &                 KIOPROCS=NIOPROCS(1:NUM_IO_PROCS))
  CALL SETUP_IOREQUEST(YL_IOREQUEST,'RAW')
ENDIF  

CALL IO_GET(YL_IOSTREAM,YL_IOREQUEST,KR1=IBUF)
IF(LL_IO_PROC .AND. IBUF(1) /= NGPTOT) &
  & CALL ABOR1('FIELD_CONTAINER_READ_RESTART: WRONG NGPTOT')
CALL IO_GET(YL_IOSTREAM,YL_IOREQUEST,KR1=IBUF)
IF(LL_IO_PROC .AND. IBUF(1) /= INFIELDS) &
  & CALL ABOR1('FIELD_CONTAINER_READ_RESTART: WRONG NUMBER OF FIELDS')

IF(NUM_PROCS_PER_IO_PROC>1) THEN
  ! NDIMS=1
  ALLOCATE(ZGP3A(NPROMA,1,NGPBLKS))
  CALL YL_MULTI_IO_1D%SETUP('S',NPROMA*NGPBLKS,NIOPROCS,NUM_IO_PROCS)
  ! NDIMS=2
  ALLOCATE(ZGP3(NPROMA,NFLEVG,NGPBLKS))
  CALL YL_MULTI_IO_2D%SETUP('S',NPROMA*NFLEVG*NGPBLKS,NIOPROCS,NUM_IO_PROCS)
  DO JFLDS=1,INUMFLDS
    DO JFLD=1,INFIELDS
      ID = IACTIVE(JFLD)
      IF(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%NDIMS == 1) THEN
        CALL IO_GET(YL_IOSTREAM,YL_IOREQUEST,KR1=IBUF)
        IF(LL_IO_PROC .AND. IBUF(1) /= ID) &
          & CALL ABOR1('FIELD_CONTAINER_READ_RESTART: WRONG 2D FIELD ID')
        IF(LL_IO_PROC .AND. IBUF(2)>0 .AND. .NOT.ALLOCATED(ZBUF2)) &
          & ALLOCATE(ZBUF2(IBUF(2)))
        CALL IO_GET(YL_IOSTREAM,YL_IOREQUEST,PR1=ZBUF2)
        CALL YL_MULTI_IO_1D%SCATTER(ZGP3A, ZBUF2, SIZE(ZBUF2))
        DO JBLK=1,NGPBLKS
          IKGLO = 1+(JBLK-1)*NPROMA
          IST = 1
          IEND = MIN(NPROMA,NGPTOT-IKGLO+1)
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_OPEN_ONE(ID,JBLK,ZFIELD1)
          ZFIELD1(IST:IEND) = ZGP3A(IST:IEND,1,JBLK)
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_CLOSE_ONE(ID,JBLK,ZFIELD1)
        ENDDO
      ELSEIF(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%NDIMS == 2) THEN
        INLEVS = YDFIELDS(JFLDS)%OBJ%NLEVELS(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%D2TYPE)
        CALL IO_GET(YL_IOSTREAM,YL_IOREQUEST,KR1=IBUF)
        IBUF(1)=ID
        IF(LL_IO_PROC .AND. IBUF(1) /= ID) &
          & CALL ABOR1('FIELD_CONTAINER_READ_RESTART: WRONG 3D FIELD ID')
        IF(LL_IO_PROC .AND. IBUF(2)>0 .AND. .NOT.ALLOCATED(ZBUF3)) &
          & ALLOCATE(ZBUF3(IBUF(2)))
        CALL IO_GET(YL_IOSTREAM,YL_IOREQUEST,PR1=ZBUF3)
        CALL YL_MULTI_IO_2D%SCATTER(ZGP3, ZBUF3, SIZE(ZBUF3))
        DO JBLK=1,NGPBLKS
          IKGLO = 1+(JBLK-1)*NPROMA
          IST = 1
          IEND = MIN(NPROMA,NGPTOT-IKGLO+1)
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_OPEN_ONE(ID,JBLK,ZFIELD2)
          DO JF=1,INLEVS
            ZFIELD2(IST:IEND,JF) = ZGP3(IST:IEND,JF,JBLK)
          ENDDO
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_CLOSE_ONE(ID,JBLK,ZFIELD2)
        ENDDO
      ELSE
        INLEVS = YDFIELDS(JFLDS)%OBJ%NLEVELS(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%D2TYPE)
        IN3D   = YDFIELDS(JFLDS)%OBJ%NDIM3S(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%D3TYPE)
        ALLOCATE(ZGP4(NPROMA,INLEVS,IN3D,NGPBLKS))
        CALL IO_GET(YL_IOSTREAM,YL_IOREQUEST,KR1=IBUF)
        IBUF(1)=ID
        IF(LL_IO_PROC .AND. IBUF(1) /= ID) &
          & CALL ABOR1('FIELD_CONTAINER_READ_RESTART: WRONG 3D FIELD ID')
        IF(IBUF(2)>0) ALLOCATE(ZBUF(IBUF(2)))
        CALL YL_MULTI_IO_3D%SETUP('S',NPROMA*INLEVS*IN3D*NGPBLKS,NIOPROCS,NUM_IO_PROCS)
        CALL IO_GET(YL_IOSTREAM,YL_IOREQUEST,PR1=ZBUF)
        CALL YL_MULTI_IO_3D%SCATTER(ZGP4, ZBUF, SIZE(ZBUF))
        DO JBLK=1,NGPBLKS
          IKGLO = 1+(JBLK-1)*NPROMA
          IST = 1
          IEND = MIN(NPROMA,NGPTOT-IKGLO+1)
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_OPEN_ONE(ID,JBLK,ZFIELD3)
          DO JF=1,IN3D
            DO JLEV=1,INLEVS
              ZFIELD3(IST:IEND,JLEV,JF) = ZGP4(IST:IEND,JLEV,JF,JBLK)
            ENDDO
          ENDDO
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_CLOSE_ONE(ID,JBLK,ZFIELD3)
        ENDDO
        DEALLOCATE(ZGP4)
        IF(ALLOCATED(ZBUF)) DEALLOCATE(ZBUF)
        CALL YL_MULTI_IO_3D%RESET()
      ENDIF
    ENDDO
  ENDDO
  !
  DEALLOCATE(ZGP3A)
  IF(ALLOCATED(ZBUF2)) DEALLOCATE(ZBUF2)
  CALL YL_MULTI_IO_1D%RESET()
  !
  DEALLOCATE(ZGP3)
  IF(ALLOCATED(ZBUF3)) DEALLOCATE(ZBUF3)
  CALL YL_MULTI_IO_2D%RESET()
ELSE
  DO JFLDS=1,INUMFLDS
    DO JFLD=1,INFIELDS
      ID = IACTIVE(JFLD)
    
      IF(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%NDIMS == 1) THEN
        ALLOCATE(ZGP3(NPROMA,1,NGPBLKS))
        CALL IO_GET(YL_IOSTREAM,YL_IOREQUEST,KR1=IBUF)
        IF(IBUF(1) /= ID) CALL ABOR1('FIELD_CONTAINER_READ_RESTART: WRONG 2D FIELD ID')
        CALL IO_GET(YL_IOSTREAM,YL_IOREQUEST,PR3=ZGP3)
        DO JBLK=1,NGPBLKS
          IKGLO = 1+(JBLK-1)*NPROMA
          IST = 1
          IEND = MIN(NPROMA,NGPTOT-IKGLO+1)
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_OPEN_ONE(ID,JBLK,ZFIELD1)
          ZFIELD1(IST:IEND) = ZGP3(IST:IEND,1,JBLK)
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_CLOSE_ONE(ID,JBLK,ZFIELD1)
        ENDDO
        DEALLOCATE(ZGP3)
      ELSEIF(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%NDIMS == 2) THEN
        INLEVS = YDFIELDS(JFLDS)%OBJ%NLEVELS(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%D2TYPE)
        ALLOCATE(ZGP3(NPROMA,INLEVS,NGPBLKS))
        IBUF(1)=ID
        CALL IO_GET(YL_IOSTREAM,YL_IOREQUEST,KR1=IBUF)
        IF(IBUF(1) /= ID) CALL ABOR1('FIELD_CONTAINER_READ_RESTART: WRONG 3D FIELD ID')
        CALL IO_GET(YL_IOSTREAM,YL_IOREQUEST,PR3=ZGP3)
        DO JBLK=1,NGPBLKS
          IKGLO = 1+(JBLK-1)*NPROMA
          IST = 1
          IEND = MIN(NPROMA,NGPTOT-IKGLO+1)
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_OPEN_ONE(ID,JBLK,ZFIELD2)
          DO JF=1,INLEVS
            ZFIELD2(IST:IEND,JF) = ZGP3(IST:IEND,JF,JBLK)
          ENDDO
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_CLOSE_ONE(ID,JBLK,ZFIELD2)
        ENDDO
        DEALLOCATE(ZGP3)
      ELSE
        INLEVS = YDFIELDS(JFLDS)%OBJ%NLEVELS(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%D2TYPE)
        IN3D   = YDFIELDS(JFLDS)%OBJ%NDIM3S(YDFIELDS(JFLDS)%OBJ%METADATA(ID)%D3TYPE)
        ALLOCATE(ZGP4(NPROMA,INLEVS,IN3D,NGPBLKS))
        IBUF(1)=ID
        CALL IO_GET(YL_IOSTREAM,YL_IOREQUEST,KR1=IBUF)
        IF(IBUF(1) /= ID) CALL ABOR1('FIELD_CONTAINER_READ_RESTART: WRONG 3D FIELD ID')
        CALL IO_GET(YL_IOSTREAM,YL_IOREQUEST,PR4=ZGP4)
        DO JBLK=1,NGPBLKS
          IKGLO = 1+(JBLK-1)*NPROMA
          IST = 1
          IEND = MIN(NPROMA,NGPTOT-IKGLO+1)
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_OPEN_ONE(ID,JBLK,ZFIELD3)
          DO JF=1,IN3D
            DO JLEV=1,INLEVS
              ZFIELD3(IST:IEND,JLEV,JF) = ZGP4(IST:IEND,JLEV,JF,JBLK)
            ENDDO
          ENDDO
          CALL YDFIELDS(JFLDS)%OBJ%FIELD_CLOSE_ONE(ID,JBLK,ZFIELD3)
        ENDDO
        DEALLOCATE(ZGP4)
      ENDIF
    ENDDO
  ENDDO
ENDIF

IF(LL_SETUP_IOREQUEST) THEN
  CALL CLOSE_IOREQUEST(YL_IOREQUEST)
  CALL CLOSE_IOSTREAM(YL_IOSTREAM)
  DEALLOCATE(YL_IOREQUEST)
  DEALLOCATE(YL_IOSTREAM)
ENDIF  

DEALLOCATE(IACTIVE)
END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('FIELD_CONTAINER_OPER_MOD:FIELD_CONTAINER_READ_RESTART',1,ZHOOK_HANDLE)
END SUBROUTINE FIELD_CONTAINER_READ_RESTART

