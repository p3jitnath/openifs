SUBROUTINE IOGRCLIA_SELECT_WH(YDGEOMETRY,YDSURF,YDMCC,KACTION,PREAL,YDFLDSC,KFCUR)

USE GEOMETRY_MOD , ONLY : GEOMETRY
USE SURFACE_FIELDS_MIX , ONLY : TSURF
USE YOMMCC, ONLY : TMCC

IMPLICIT NONE

TYPE(GEOMETRY)     , INTENT(IN)    :: YDGEOMETRY
TYPE(TSURF)        , INTENT(INOUT) :: YDSURF
TYPE(TMCC)          ,INTENT(INOUT) :: YDMCC
INTEGER (KIND=JPIM), INTENT (IN)   :: KACTION
REAL (KIND=JPRB),    OPTIONAL, INTENT (INOUT), TARGET :: PREAL (:,:)
TYPE (IOFLDDESC),    OPTIONAL, INTENT (INOUT), TARGET :: YDFLDSC (:) 
INTEGER (KIND=JPIM), OPTIONAL, INTENT (INOUT)         :: KFCUR
#include "abor1.intfb.h"

REAL (KIND=JPRB),         POINTER :: ZREAL (:,:)
CHARACTER (LEN=NPREFLEN), POINTER :: CLPREF (:)
CHARACTER (LEN=NSUFFLEN), POINTER :: CLSUFF (:)
INTEGER (KIND=JPIM),      POINTER :: ILEV (:)
LOGICAL,                  POINTER :: LLREQRD (:)

INTEGER(KIND=JPIM) :: IFLD, JV, IFCUR
LOGICAL :: LLCOPYD
LOGICAL :: LLCOPYF

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('IOGRCLIA_MOD:IOGRCLIA_SELECT_WH',0,ZHOOK_HANDLE)

LLCOPYD = PRESENT (YDFLDSC)
LLCOPYF = PRESENT (PREAL)

IFCUR = 0
IF (PRESENT (KFCUR)) IFCUR = KFCUR

ZREAL => NULL ()
IF (LLCOPYF) THEN
  ZREAL => PREAL (:,IFCUR+1:)
ENDIF

CLPREF  => NULL ()
CLSUFF  => NULL ()
ILEV    => NULL ()
LLREQRD => NULL ()
IF (LLCOPYD) THEN
  CLPREF  => YDFLDSC (IFCUR+1:)%CPREF 
  CLSUFF  => YDFLDSC (IFCUR+1:)%CSUFF 
  ILEV    => YDFLDSC (IFCUR+1:)%ILEVG
  LLREQRD => YDFLDSC (IFCUR+1:)%LREQD
ENDIF

IF (LLCOPYD) THEN
  IF (ASSOCIATED (ILEV))    ILEV (:)    = 0
  IF (ASSOCIATED (LLREQRD)) LLREQRD (:) = .TRUE.
ENDIF

IFLD=0

DO JV = 1, YDSURF%YSD_VXD%NUMFLDS
  CALL ACPY(YDGEOMETRY,YDSURF%YSD_VX%YVX(JV)%CNAME, PBUF2=YDSURF%SD_VX(:,JV,:), LDREQ=.NOT.YDMCC%LMCC01)
ENDDO

IF (PRESENT (KFCUR)) KFCUR = KFCUR + IFLD

IF (LHOOK) CALL DR_HOOK('IOGRCLIA_MOD:IOGRCLIA_SELECT_WH',1,ZHOOK_HANDLE)

CONTAINS

SUBROUTINE ACPY(YDGEOMETRY,CDNAME, PBUF2, LDREQ)


USE GEOMETRY_MOD , ONLY : GEOMETRY
TYPE(GEOMETRY)   , INTENT (IN)              :: YDGEOMETRY
CHARACTER (LEN=*), INTENT (IN)              :: CDNAME
REAL (KIND=JPRB) , INTENT (INOUT), OPTIONAL :: PBUF2 (:,:) ! with NPROMA blocks
LOGICAL,             INTENT (IN),    OPTIONAL :: LDREQ       ! field required

CHARACTER (LEN=*), PARAMETER :: CLBLANK = '                '
INTEGER (KIND=JPIM) :: JSTGLO, IEND, IBL, JL

REAL (KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK ('IOGRCLIA_MOD:ACPY',0,ZHOOK_HANDLE)

IF (CDNAME /= CLBLANK) THEN

  IFLD = IFLD + 1

  IF (LLCOPYF) THEN

    IF (IFLD > UBOUND (ZREAL,2)) CALL ABOR1 ('INPUT BUFFER TOO SMALL')

    IF (PRESENT (PBUF2)) THEN

      DO JSTGLO = 1, YDGEOMETRY%YRGEM%NGPTOT, YDGEOMETRY%YRDIM%NPROMA

        IEND = MIN (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRGEM%NGPTOT-JSTGLO+1)
        IBL  = (JSTGLO-1) / YDGEOMETRY%YRDIM%NPROMA+1

        DO JL = 1, IEND

          SELECT CASE (KACTION)

            CASE (NIOGRCLIACT_WRITE)

              ZREAL (JSTGLO+JL-1,IFLD) = PBUF2 (JL,IBL)

            CASE (NIOGRCLIACT_READ)

              PBUF2 (JL,IBL) = ZREAL (JSTGLO+JL-1,IFLD)

          END SELECT

        ENDDO

      ENDDO

    ENDIF


  ENDIF

  IF (LLCOPYD) THEN

    IF (IFLD > UBOUND (CLPREF,1)) CALL ABOR1 ('INPUT BUFFER TOO SMALL')

    CLPREF(IFLD) = CDNAME(1:4)
    CLSUFF(IFLD) = CDNAME(5:)

    IF (PRESENT (LDREQ)) THEN
      IF (ASSOCIATED (LLREQRD)) LLREQRD (IFLD) = LDREQ
    ENDIF

  ENDIF

ENDIF

IF (LHOOK) CALL DR_HOOK ('IOGRCLIA_MOD:ACPY',1,ZHOOK_HANDLE)

END SUBROUTINE ACPY

END SUBROUTINE IOGRCLIA_SELECT_WH

END MODULE IOGRCLIA_MOD

