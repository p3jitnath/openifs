CONTAINS

SUBROUTINE IOGRCLIA_COUNT(YDGEOMETRY,YDSURF,YDMCC,KACTION,KFNUM)

USE YOMMCC       , ONLY : TMCC
USE GEOMETRY_MOD , ONLY : GEOMETRY
USE SURFACE_FIELDS_MIX , ONLY : TSURF
IMPLICIT NONE


TYPE(GEOMETRY)     ,INTENT(IN)    :: YDGEOMETRY
TYPE(TSURF)        ,INTENT(INOUT) :: YDSURF
TYPE(TMCC)         ,INTENT(INOUT) :: YDMCC
INTEGER (KIND=JPIM),INTENT (IN)   :: KACTION
INTEGER (KIND=JPIM),INTENT (INOUT):: KFNUM
#include "abor1.intfb.h"

INTEGER (KIND=JPIM) :: IFCUR

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('IOGRCLIA_MOD:IOGRCLIA_COUNT',0,ZHOOK_HANDLE)

IF (KACTION /= NIOGRCLIACT_READ) CALL ABOR1 ('ONLY NIOGRCLIACT_READ IS IMPLEMENTED')

IFCUR = 0
CALL IOGRCLIA_SELECT_WH(YDGEOMETRY,YDSURF,YDMCC,KACTION,KFCUR=IFCUR)
KFNUM = KFNUM + IFCUR

IF (LHOOK) CALL DR_HOOK('IOGRCLIA_MOD:IOGRCLIA_COUNT',1,ZHOOK_HANDLE)

END SUBROUTINE IOGRCLIA_COUNT

