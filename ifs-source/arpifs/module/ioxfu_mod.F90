INTEGER (KIND=JPIM), PARAMETER :: NIOXFUACT_READ = 0, NIOXFUACT_WRITE = 1

INTERFACE IOXFU_SELECTD
  MODULE PROCEDURE IOXFU_SELECTDD, IOXFU_SELECTDP
END INTERFACE IOXFU_SELECTD

PRIVATE :: IOXFU_SELECTDD, IOXFU_SELECTDP, IOXFU_SELECT_WH

