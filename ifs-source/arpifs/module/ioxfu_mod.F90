SUBROUTINE IOXFU_SELECT_WH(YDGEOMETRY,YDXFU,KACTION,YDFLDSC,YDFLDGT,KFCUR)

USE GEOMETRY_MOD , ONLY : GEOMETRY
USE YOMXFU   , ONLY : TXFU
USE YOMCT0   , ONLY : LRPLANE
USE YOMOPH0  , ONLY : CNMCA
USE YOMFA    , ONLY : NVGRIB, NBITPG
USE FA_MOD   , ONLY : JPPRCM

!**** * IOXFU  * - ROUTINE TO EXTRAXCT AND WRITE INSTANTANEOUS FLUXES
!       History :
!       14/01/2011 : Creation from wrxfu.F90 (P.Marguinaud)
!        -----------------------------------------------------------

IMPLICIT NONE

TYPE (GEOMETRY),     INTENT (IN)                      :: YDGEOMETRY
TYPE(TXFU), TARGET  ,INTENT(INOUT)                    :: YDXFU
INTEGER (KIND=JPIM), INTENT (IN)                      :: KACTION
TYPE (IOFLDDESC),    INTENT (INOUT), OPTIONAL, TARGET :: YDFLDSC (:)
TYPE (IOFLDPTR),     INTENT (INOUT), OPTIONAL, TARGET :: YDFLDGT (:)
INTEGER(KIND=JPIM),  INTENT (INOUT), OPTIONAL         :: KFCUR

INTEGER (KIND=JPIM)       :: JF, IFLD, JL
INTEGER (KIND=JPIM)       :: IFCUR
TYPE (IOFLDDESC), POINTER :: YLFLDSC (:)
INTEGER (KIND=JPIM)       :: IREP, IPFAOVSZ

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('IOXFU_MOD:IOXFU_SELECT_WH',0,ZHOOK_HANDLE)

IF (PRESENT (YDFLDSC)) THEN
  YLFLDSC => YDFLDSC
ELSEIF (PRESENT (YDFLDGT)) THEN
  YLFLDSC => YDFLDGT%YFLDDSC
ELSE
  YLFLDSC => NULL ()
ENDIF

IFCUR = 0
IF (PRESENT (KFCUR)) IFCUR = KFCUR

CALL FASGRA (IREP, CNMCA, IPFAOVSZ)

IFLD = 0
DO JF = 1, YDXFU%NTYPXFU

  DO JL = 1, YDXFU%TYPE_XFU(JF)%ILEV
    IFLD = IFLD + 1
    IFCUR = IFCUR + 1

    IF (ASSOCIATED (YLFLDSC)) THEN

      YLFLDSC(IFCUR)%NFLSZ  = YDGEOMETRY%YRGEM%NGPTOTG
      YLFLDSC(IFCUR)%NSIZPK = YDGEOMETRY%YRGEM%NGPTOTG + JPPRCM * IPFAOVSZ
      YLFLDSC(IFCUR)%CPREF  = YDXFU%TYPE_XFU(JF)%CLPREF(JL)
      YLFLDSC(IFCUR)%ILEVG  = YDXFU%TYPE_XFU(JF)%INUMLEV(JL)
      YLFLDSC(IFCUR)%CSUFF  = YDXFU%TYPE_XFU(JF)%CLSUFF
      YLFLDSC(IFCUR)%NGRIBL = NVGRIB
      YLFLDSC(IFCUR)%JBITS  = NBITPG

      IF (YLFLDSC(IFCUR)%CSUFF == 'NEBULOSI.TOT') THEN
        YLFLDSC(IFCUR)%JBITS = 4
      ELSEIF (YLFLDSC(IFCUR)%CSUFF == 'HUMILIQU.TOT') THEN
        YLFLDSC(IFCUR)%JBITS = 10
      ELSEIF (YLFLDSC(IFCUR)%CSUFF == 'HUMISOLI.TOT') THEN
        YLFLDSC(IFCUR)%JBITS = 10
      ENDIF

    ENDIF
   
    IF (PRESENT (YDFLDGT)) THEN
      YDFLDGT(IFCUR)%ZFLDGP => YDXFU%XFUBUF (:,IFLD,:) 
! De-reduce vector fields
      YDFLDGT(IFCUR)%LDRDGP = LRPLANE .AND. (YDXFU%TYPE_XFU(JF)%IPARITY == 1)
    ENDIF

  ENDDO

ENDDO

IF (PRESENT (KFCUR)) KFCUR = IFCUR

IF (LHOOK) CALL DR_HOOK('IOXFU_MOD:IOXFU_SELECT_WH',1,ZHOOK_HANDLE)

END SUBROUTINE IOXFU_SELECT_WH

END MODULE IOXFU_MOD

