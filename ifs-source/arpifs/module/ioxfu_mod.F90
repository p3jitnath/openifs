CONTAINS

SUBROUTINE IOXFU_COUNT(YDGEOMETRY,YDXFU,KACTION,KFNUM)

USE YOMXFU      , ONLY : TXFU
USE GEOMETRY_MOD, ONLY : GEOMETRY
IMPLICIT NONE

TYPE (GEOMETRY),     INTENT (IN)    :: YDGEOMETRY
TYPE(TXFU)          ,INTENT(INOUT)  :: YDXFU
INTEGER (KIND=JPIM), INTENT (IN)    :: KACTION
INTEGER (KIND=JPIM), INTENT (INOUT) :: KFNUM

INTEGER (KIND=JPIM) :: IFCUR

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('IOXFU_MOD:IOXFU_COUNT',0,ZHOOK_HANDLE)

IFCUR = 0
CALL IOXFU_SELECT_WH(YDGEOMETRY,YDXFU,KACTION,KFCUR=IFCUR)
KFNUM = KFNUM + IFCUR

IF (LHOOK) CALL DR_HOOK('IOXFU_MOD:IOXFU_COUNT',1,ZHOOK_HANDLE)

END SUBROUTINE IOXFU_COUNT

