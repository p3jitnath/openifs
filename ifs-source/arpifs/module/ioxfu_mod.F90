INTEGER (KIND=JPIM), INTENT (IN)              :: KACTION
TYPE (IOFLDDESC),    INTENT (INOUT)           :: YDFLDSC (:)
INTEGER(KIND=JPIM),  INTENT (INOUT), OPTIONAL :: KFCUR

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

