       & NGPTOT=>YDGEOMETRY%YRGEM%NGPTOT, &
       & NFLEVG=>YDGEOMETRY%YRDIMV%NFLEVG)

KGRIBIOGG(:,:) = 0_JPIM
DO JLEV=1,NFLEVG
  KGRIBIOGG(1,JLEV) = KGRIBCD
  KGRIBIOGG(2,JLEV) = JLEV
  !
  CALL LOOKUP(JLEV,IPHYS,LLWRITE)
  JINDEX = YPHYS_ECV(IPHYS)%NINDEX
  !
  DO JSTGLO=1,NGPTOT,NPROMA
    IST=1
    IEND=MIN(NPROMA,NGPTOT-JSTGLO+1)
    IBL=(JSTGLO-1)/NPROMA+1
    DO J=IST,IEND
      IF (LLWRITE) THEN
        ! Land-sea mask
        IF (YPHYS_ECV(IPHYS)%LLAND) THEN
          ZMASK = YRECV0%RLSM(J,IBL)
        ELSE
          ZMASK = 1._JPRB
        ENDIF
        PREAL(JSTGLO-1+J,JLEV)=PECV(J,JINDEX,IBL)*ZMASK
      ! Not a level with parameter
      ELSE
        PREAL(JSTGLO-1+J,JLEV)=0._JPRB
      ENDIF
    ENDDO
  ENDDO
ENDDO

