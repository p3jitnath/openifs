SUBROUTINE ECPHYS_TO_ECV_SP3_AD(YDGEOMETRY,YDECV,KECV,PSP3)

  USE GEOMETRY_MOD, ONLY : GEOMETRY
  USE YOMJBECV,     ONLY : YRECV5, YRECV0, ECV_SPTOGP, ECV_SPTOGPAD, ECV_SPTOGP_INVAD
  USE TYPE_ECV,     ONLY : TECVDIM, ECV_CONTAINER
  USE YOMHOOK,      ONLY : LHOOK, DR_HOOK, JPHOOK

  IMPLICIT NONE

  TYPE(GEOMETRY)      ,INTENT(IN)    :: YDGEOMETRY
  TYPE(ECV_CONTAINER) ,INTENT(INOUT) :: YDECV
  INTEGER(KIND=JPIM)  ,INTENT(IN)    :: KECV
  REAL(KIND=JPRB)     ,INTENT(INOUT) :: PSP3(YDGEOMETRY%YRDIMV%NFLEVL,YDGEOMETRY%YRDIM%NSPEC2)


REAL(KIND=JPRB), ALLOCATABLE :: ZGPTMP(:,:,:)
REAL(KIND=JPRB), ALLOCATABLE :: ZGP5TMP(:,:,:)
INTEGER(KIND=JPIM)           :: JLEV, JSP, IPHYS, JINDEX
INTEGER(KIND=JPIM)           :: JKGLO, ICEND, IBL, JK
REAL(KIND=JPRB)              :: ZMASK
LOGICAL                      :: LLCHANGE(YDGEOMETRY%YRDIMV%NFLEVG)
LOGICAL                      :: LLCHANGELEV

REAL(KIND=JPHOOK)            :: ZHOOK_HANDLE

!     ------------------------------------------------------------------

#include "abor1.intfb.h"

!     ------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('YOMJBECPHYSECV:ECPHYS_TO_ECV_SP3_AD',0,ZHOOK_HANDLE)

ASSOCIATE(NPROMA=>YDGEOMETRY%YRDIM%NPROMA,&
        & NGPTOT=>YDGEOMETRY%YRGEM%NGPTOT,&
        & NSPEC2=>YDGEOMETRY%YRDIM%NSPEC2,&
        & NGPBLKS=>YDGEOMETRY%YRDIM%NGPBLKS,&
        & NFLEVL=>YDGEOMETRY%YRDIMV%NFLEVL, &
        & NFLEVG=>YDGEOMETRY%YRDIMV%NFLEVG)


! 2. CHECK IF LSM OR LOG CV IF NEEDED
DO JLEV=1, NFLEVG
  CALL LOOKUP(JLEV,IPHYS,LLCHANGELEV)
  IF (LLCHANGELEV .AND. (YPHYS_ECV(IPHYS)%LLAND.OR.YPHYS_ECV(IPHYS)%LLOGCV)) THEN
    LLCHANGE(JLEV) = .TRUE.
  ELSE
    LLCHANGE(JLEV) = .FALSE.
  ENDIF
ENDDO

! 3. APPLY LSM OR LOG CV IF NEEDED (grid point space)

IF (ANY(LLCHANGE)) THEN
  !
  ! Initialisation
  !
  ALLOCATE(ZGP5TMP(NPROMA,NFLEVG,NGPBLKS))
  ALLOCATE(ZGPTMP(NPROMA,NFLEVG,NGPBLKS))
  !
  ! Transform into grid point space
  !
  CALL ECV_SPTOGP(YDGEOMETRY,YRECV5,KECV,PGP3=ZGP5TMP)
  !
  ! Back to spectral space
  !
  CALL ECV_SPTOGP_INVAD(YDGEOMETRY,YDECV,KECV,PGP3=ZGPTMP)
  YDECV%RSPECV3D(KECV,:,:) =  0.0_JPRB
  !
  ! Apply change
  !
  DO JLEV=1, NFLEVG
    !
    CALL LOOKUP(JLEV,IPHYS,LLCHANGELEV)
    JINDEX = YPHYS_ECV(IPHYS)%NINDEX
    !
    IF (LLCHANGELEV) THEN
!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(JKGLO,ICEND,IBL,JK,ZMASK)
      DO JKGLO=1,NGPTOT,NPROMA
        ICEND=MIN(NPROMA,NGPTOT-JKGLO+1)
        IBL=(JKGLO-1)/NPROMA+1
        DO JK=1,ICEND
            ! Land-sea mask
            IF (YPHYS_ECV(IPHYS)%LLAND) THEN
              ZMASK = YRECV0%RLSM(JK,IBL)
            ELSE
              ZMASK = 1._JPRB
            ENDIF
            ! Log-normal control variable or not
            IF (YPHYS_ECV(IPHYS)%LLOGCV) THEN
              ZGPTMP(JK,JINDEX,IBL) = ZGPTMP(JK,JINDEX,IBL) &
                &                   * ZGP5TMP(JK,JINDEX,IBL)*ZMASK
            ELSE
              ZGPTMP(JK,JINDEX,IBL) = ZGPTMP(JK,JINDEX,IBL)*ZMASK
            ENDIF
        ENDDO
      ENDDO
!$OMP END PARALLEL DO
    ENDIF
  ENDDO
  !
  ! Transform into grid point space
  !
  CALL ECV_SPTOGPAD(YDGEOMETRY,YDECV,KECV,PGP3=ZGPTMP)
ENDIF

! 1. STORE

DO JLEV=1, NFLEVL
  DO JSP = 1, NSPEC2
    PSP3(JLEV,JSP)=PSP3(JLEV,JSP) + YDECV%RSPECV3D(KECV,JLEV,JSP)
    YDECV%RSPECV3D(KECV,JLEV,JSP)=0.0_JPRB
  ENDDO
ENDDO

END ASSOCIATE

IF (LHOOK) CALL DR_HOOK('YOMJBECPHYSECV:ECPHYS_TO_ECV_SP3_AD',1,ZHOOK_HANDLE)

END SUBROUTINE ECPHYS_TO_ECV_SP3_AD

