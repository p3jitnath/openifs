MODULE YOMJBECPHYSECV

!     Purpose.
!     --------
!       Data and controls for extended control variable used
!       for the optimisation of physical paremetrisation

!     Author.
!     -------
!       S. Massart

!     Modifications.
!     --------------
!       Original    April-2020
!       S. Massart  July-2021   Addition of 2D/3D Spectral fields
