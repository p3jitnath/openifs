SUBROUTINE ECPHYS_TO_ECV_SP2(YDGEOMETRY,YDECV,KECV,PSP2)

!     Purpose.
!     --------
!       Store physcis parameters in ECV structure (2D spectral version)

USE GEOMETRY_MOD, ONLY : GEOMETRY
USE YOMJBECV,     ONLY : YRECV5, YRECV0, ECV_SPTOGP, ECV_SPTOGP_INV
USE TYPE_ECV,     ONLY : TECVDIM, ECV_CONTAINER
USE YOMHOOK,      ONLY : LHOOK, DR_HOOK, JPHOOK

IMPLICIT NONE

TYPE(GEOMETRY)      ,INTENT(IN)    :: YDGEOMETRY
TYPE(ECV_CONTAINER) ,INTENT(INOUT) :: YDECV
INTEGER(KIND=JPIM)  ,INTENT(IN)    :: KECV
REAL(KIND=JPRB)     ,INTENT(IN)    :: PSP2(YDGEOMETRY%YRDIM%NSPEC2)


REAL(KIND=JPRB), ALLOCATABLE :: ZGPTMP(:,:,:)
REAL(KIND=JPRB), ALLOCATABLE :: ZGP5TMP(:,:,:)
INTEGER(KIND=JPIM)           :: JLEV, JSP
INTEGER(KIND=JPIM)           :: JKGLO, ICEND, IBL, JK
INTEGER(KIND=JPIM)           :: IPHYS
REAL(KIND=JPRB)              :: ZMASK
LOGICAL                      :: LLCHANGE

REAL(KIND=JPHOOK)            :: ZHOOK_HANDLE

!     ------------------------------------------------------------------

#include "abor1.intfb.h"

IF (LHOOK) CALL DR_HOOK('YOMJBECPHYSECV:ECPHYS_TO_ECV_SP2',0,ZHOOK_HANDLE)

ASSOCIATE(NPROMA=>YDGEOMETRY%YRDIM%NPROMA,&
        & NGPTOT=>YDGEOMETRY%YRGEM%NGPTOT,&
        & NRESOL=>YDGEOMETRY%YRDIM%NRESOL,&
        & NSPEC2=>YDGEOMETRY%YRDIM%NSPEC2,&
        & NGPBLKS=>YDGEOMETRY%YRDIM%NGPBLKS,&
        & NPSP=>YDGEOMETRY%YRMP%NPSP)

! 1. INITIALISATION


CALL LOOKUP(KECV,IPHYS,LLCHANGE)

! 2. STORE

IF (NPSP == 1) THEN
  DO JSP = 1, NSPEC2
    YDECV%RSPECV2D(KECV,JSP)=PSP2(JSP)
  ENDDO
ENDIF

! 3. APPLY LSM OR LOG CV IF NEEDED (grid point space)

IF (LLCHANGE) THEN
  !
  ! Initialisation
  !
  ALLOCATE(ZGP5TMP(NPROMA,1,NGPBLKS))
  ALLOCATE(ZGPTMP(NPROMA,1,NGPBLKS))
  !
  ! Transform into grid point space
  !
  CALL ECV_SPTOGP(YDGEOMETRY,YDECV,KECV,PGP2=ZGPTMP)
  !
  IF (YPHYS_ECV(IPHYS)%LLOGCV) THEN
    CALL ECV_SPTOGP(YDGEOMETRY,YRECV5,KECV,PGP2=ZGP5TMP)
  ELSE
    ZGP5TMP(:,:,:) = 1._JPRB
  ENDIF
  !
  ! Apply LSM and LOG cv
  !
!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(JKGLO,ICEND,IBL,JK,ZMASK)
  DO JKGLO=1,NGPTOT,NPROMA
    ICEND=MIN(NPROMA,NGPTOT-JKGLO+1)
    IBL=(JKGLO-1)/NPROMA+1
    DO JK=1,ICEND
      IF (YPHYS_ECV(IPHYS)%LLAND) THEN
        ZMASK = YRECV0%RLSM(JK,IBL)
      ELSE
        ZMASK = 1._JPRB
      ENDIF
      ZGPTMP(JK,1,IBL) = ZGPTMP(JK,1, IBL) * ZGP5TMP(JK,1, IBL) * ZMASK
    ENDDO
  ENDDO
!$OMP END PARALLEL DO
  !
  ! Back to spectral space
  !
  CALL ECV_SPTOGP_INV(YDGEOMETRY,YDECV,KECV,PGP2=ZGPTMP)
  !
  ! Free memory
  !
  DEALLOCATE(ZGP5TMP)
  DEALLOCATE(ZGPTMP)
ENDIF


END ASSOCIATE

IF (LHOOK) CALL DR_HOOK('YOMJBECPHYSECV:ECPHYS_TO_ECV_SP2',1,ZHOOK_HANDLE)

END SUBROUTINE ECPHYS_TO_ECV_SP2

