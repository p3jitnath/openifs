REAL(KIND=JPRB)   :: ZMASK
LOGICAL           :: LLWRITE

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!     ------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('YOMJBECPHYSECV:ECPHYS_WRMLPPG_GP3',0,ZHOOK_HANDLE)

