SUBROUTINE LOOKUP(KLEV,KPHYS,LDFIND)


  USE YOMHOOK,      ONLY : LHOOK, DR_HOOK, JPHOOK

  IMPLICIT NONE



  INTEGER(KIND=JPIM), INTENT(IN)  :: KLEV
  INTEGER(KIND=JPIM), INTENT(OUT) :: KPHYS
  LOGICAL           , INTENT(OUT) :: LDFIND

  REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!     ------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('YOMJBECPHYSECV:LOOKUP',0,ZHOOK_HANDLE)

  LDFIND = .FALSE.
  DO KPHYS = 1, NPHYS_ECV
    IF (YPHYS_ECV(KPHYS)%NINDEX == KLEV) THEN
      LDFIND = .TRUE.
      EXIT
    ENDIF
  ENDDO

IF (LHOOK) CALL DR_HOOK('YOMJBECPHYSECV:LOOKUP',1,ZHOOK_HANDLE)

END SUBROUTINE LOOKUP

