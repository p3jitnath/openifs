SUBROUTINE ECPHYS_TO_ECV_GP3(YDGEOMETRY,YDECV,KECV,KPTJB,PGP3)

!     Purpose.
!     --------
!       Store physcis parameters in ECV structure (girdpoint version)

USE GEOMETRY_MOD, ONLY : GEOMETRY
USE YOMJBECV,     ONLY : YRECV5, YRECV0
USE TYPE_ECV,     ONLY : TECVDIM, ECV_CONTAINER
USE YOMHOOK,      ONLY : LHOOK, DR_HOOK, JPHOOK

IMPLICIT NONE

TYPE(GEOMETRY)      ,INTENT(IN)    :: YDGEOMETRY
TYPE(ECV_CONTAINER) ,INTENT(INOUT) :: YDECV
INTEGER(KIND=JPIM)  ,INTENT(IN) :: KECV
INTEGER(KIND=JPIM)  ,INTENT(IN) :: KPTJB
REAL(KIND=JPRB)     ,INTENT(IN) :: PGP3(:,:,:)


INTEGER(KIND=JPIM):: JKGLO, ICEND, IBL, JK, JLEV, IPHYS, JINDEX
REAL(KIND=JPRB)   :: ZBCAP, ZMASK
LOGICAL           :: LLGET

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!     ------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('YOMJBECPHYSECV:ECPHYS_TO_ECV_GP3',0,ZHOOK_HANDLE)

ASSOCIATE(NPROMA=>YDGEOMETRY%YRDIM%NPROMA, &
        & NGPTOT=>YDGEOMETRY%YRGEM%NGPTOT, &
        & NFLEVG=>YDGEOMETRY%YRDIMV%NFLEVG)

DO JLEV=1, NFLEVG
  !
  CALL LOOKUP(JLEV,IPHYS,LLGET)
  JINDEX = YPHYS_ECV(IPHYS)%NINDEX
  !
  IF (LLGET) THEN
!$OMP PARALLEL DO SCHEDULE(STATIC) PRIVATE(JKGLO,ICEND,IBL,JK,ZMASK)
    DO JKGLO=1,NGPTOT,NPROMA
      ICEND=MIN(NPROMA,NGPTOT-JKGLO+1)
      IBL=(JKGLO-1)/NPROMA+1
      DO JK=1,ICEND
          ! Land-sea mask
          IF (YPHYS_ECV(IPHYS)%LLAND) THEN
            ZMASK = YRECV0%RLSM(JK,IBL)
          ELSE
            ZMASK = 1._JPRB
          ENDIF
          ! Log-normal control variable or not
          IF (YPHYS_ECV(IPHYS)%LLOGCV) THEN
                YDECV%RGPECV3D(KECV,JK,JINDEX,IBL) = PGP3(JK,(KPTJB-1)*NFLEVG+JLEV,IBL) &
                  &                              * YRECV5%RGPECV3D(KECV,JK,JINDEX,IBL)*ZMASK
          ELSE
            YDECV%RGPECV3D(KECV,JK,JINDEX,IBL) = PGP3(JK,(KPTJB-1)*NFLEVG+JLEV,IBL)*ZMASK
          ENDIF
      ENDDO
    ENDDO
!$OMP END PARALLEL DO
  ENDIF
ENDDO

END ASSOCIATE

IF (LHOOK) CALL DR_HOOK('YOMJBECPHYSECV:ECPHYS_TO_ECV_GP3',1,ZHOOK_HANDLE)

END SUBROUTINE ECPHYS_TO_ECV_GP3

