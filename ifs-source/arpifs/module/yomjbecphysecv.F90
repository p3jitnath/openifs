SUBROUTINE ECPHYS_UPPER_AIR_SP(YDGEOMETRY,KECV,PSPA2,PSPA3)

!     Purpose.
!     --------
!       Allow to cap the min value while calling UPPER_AIR (spectral)


USE GEOMETRY_MOD, ONLY : GEOMETRY
USE YOMJBECV    , ONLY : YRECV5, ECV_SPTOGP, ECV_SPTOGP_INV
USE YOMHOOK,      ONLY : LHOOK, DR_HOOK, JPHOOK

IMPLICIT NONE

TYPE(GEOMETRY)           , INTENT(IN) :: YDGEOMETRY
INTEGER(KIND=JPIM)       , INTENT(IN) :: KECV
REAL(KIND=JPRB), OPTIONAL, INTENT(IN) :: PSPA2(YDGEOMETRY%YRDIM%NSPEC2)
REAL(KIND=JPRB), OPTIONAL, INTENT(IN) :: PSPA3(YDGEOMETRY%YRDIMV%NFLEVL,YDGEOMETRY%YRDIM%NSPEC2)


INTEGER(KIND=JPIM)           :: JSP, JL, IST
INTEGER(KIND=JPIM)           :: JKGLO, ICEND, IBL, JK
INTEGER(KIND=JPIM)           :: JINDEX, JLEV, IPHYS
LOGICAL                      :: LLCHANGE
REAL(KIND=JPRB)              :: ZBCAP
REAL(KIND=JPRB), ALLOCATABLE :: ZGPTMP(:,:,:)
REAL(KIND=JPHOOK)            :: ZHOOK_HANDLE

!     ------------------------------------------------------------------

#include "abor1.intfb.h"

!     ------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('YOMJBECPHYSECV:ECPHYS_UPPER_AIR_SP',0,ZHOOK_HANDLE)

ASSOCIATE(NPROMA=>YDGEOMETRY%YRDIM%NPROMA,&
        & NSPEC2=>YDGEOMETRY%YRDIM%NSPEC2,&
        & NGPBLKS=>YDGEOMETRY%YRDIM%NGPBLKS,&
        & NGPTOT=>YDGEOMETRY%YRGEM%NGPTOT,&
        & NFLEVL=>YDGEOMETRY%YRDIMV%NFLEVL,&
        & NFLEVG=>YDGEOMETRY%YRDIMV%NFLEVG, &
        & NPSP=>YDGEOMETRY%YRMP%NPSP)

! 2. 2D FIELD

IF (PRESENT(PSPA2)) THEN
  !
  ! Adding
  !
  IF (NPSP == 1) THEN
    DO JSP = 1, NSPEC2
      YRECV5%RSPECV2D(KECV,JSP) = YRECV5%RSPECV2D(KECV,JSP) + PSPA2(JSP)
    ENDDO
  ENDIF
  !
  ! Apply bottom cap if needed
  !
  CALL LOOKUP(KECV,IPHYS,LLCHANGE)
  ZBCAP = YPHYS_ECV(IPHYS)%RBCAP
  IF (LLCHANGE .AND. ZBCAP /=  ZMISSING) THEN
    !
    ! Transform to GP
    !
    ALLOCATE(ZGPTMP(NPROMA,1,NGPBLKS))
    CALL ECV_SPTOGP(YDGEOMETRY,YRECV5,KECV,PGP2=ZGPTMP)
    !
    DO JKGLO=1,NGPTOT,NPROMA
      ICEND=MIN(NPROMA,NGPTOT-JKGLO+1)
      IBL=(JKGLO-1)/NPROMA+1
      DO JK=1,ICEND
         ZGPTMP(JK,1,IBL) = MAX(ZBCAP, ZGPTMP(JK,1, IBL))
      ENDDO
    ENDDO
    !
    CALL ECV_SPTOGP_INV(YDGEOMETRY,YRECV5,KECV,PGP2=ZGPTMP)
    DEALLOCATE(ZGPTMP)
  ENDIF
ENDIF

! 2. 3D FIELD

IF (PRESENT(PSPA3)) THEN
  DO JL=1,NFLEVL
    DO JSP = 1, NSPEC2
      YRECV5%RSPECV3D(KECV,JL,JSP) = YRECV5%RSPECV3D(KECV,JL,JSP) &
        & + PSPA3(JL,JSP)
    ENDDO
  ENDDO
  !
  ! Apply bottom cap if needed
  !
  DO JLEV=1,NFLEVG
    !
    CALL LOOKUP(JLEV,IPHYS,LLCHANGE)
    JINDEX = YPHYS_ECV(IPHYS)%NINDEX
    ZBCAP = YPHYS_ECV(IPHYS)%RBCAP
    IF (LLCHANGE .AND. ZBCAP /=  ZMISSING) THEN
      !
      ! Transform to GP
      !
      ALLOCATE(ZGPTMP(NPROMA,1,NGPBLKS))
      CALL ECV_SPTOGP(YDGEOMETRY,YRECV5,KECV,PGP2=ZGPTMP)
      !
      DO JKGLO=1,NGPTOT,NPROMA
        ICEND=MIN(NPROMA,NGPTOT-JKGLO+1)
        IBL=(JKGLO-1)/NPROMA+1
        DO JK=1,ICEND
           ZGPTMP(JK,1,IBL) = MAX(ZBCAP, ZGPTMP(JK,1, IBL))
        ENDDO
      ENDDO
      !
      CALL ECV_SPTOGP_INV(YDGEOMETRY,YRECV5,KECV,PGP3=ZGPTMP)
      DEALLOCATE(ZGPTMP)
    ENDIF
  ENDDO
ENDIF

END ASSOCIATE

IF (LHOOK) CALL DR_HOOK('YOMJBECPHYSECV:ECPHYS_UPPER_AIR_SP',1,ZHOOK_HANDLE)

END SUBROUTINE ECPHYS_UPPER_AIR_SP

