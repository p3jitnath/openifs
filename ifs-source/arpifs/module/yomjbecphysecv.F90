END INTERFACE

INTERFACE ECPHYS_WRMLPPG
MODULE PROCEDURE ECPHYS_WRMLPPG_SP2, ECPHYS_WRMLPPG_SP3, ECPHYS_WRMLPPG_GP3
END INTERFACE

