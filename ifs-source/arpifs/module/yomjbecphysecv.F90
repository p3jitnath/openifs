SUBROUTINE SUINFCE_ECPHYS(YDGEOMETRY,YD_JB_STRUCT)

!     Purpose.
!     --------
!       Change of background error standard deviation if log normal change of variable
!       and set it to 0 over ocean if needed

USE GEOMETRY_MOD, ONLY : GEOMETRY
USE YOMJG       , ONLY : TYPE_JB_STRUCT
USE YOMJBECV    , ONLY : YRECVGRIB, YRECVCONFIG, YRECV0, YRECV5, ECV_SPTOGP
USE YOMHOOK,      ONLY : LHOOK, DR_HOOK, JPHOOK

IMPLICIT NONE

TYPE(GEOMETRY)      ,INTENT(IN)    :: YDGEOMETRY
TYPE(TYPE_JB_STRUCT),INTENT(INOUT) :: YD_JB_STRUCT

REAL(KIND=JPRB), ALLOCATABLE :: ZFCE(:)
REAL(KIND=JPRB), ALLOCATABLE :: ZGPTMP(:,:,:)
INTEGER(KIND=JPIM)           :: JSTGLO, ISTC, ICEND, IBL, JK, JLEV, ILEVMAX
INTEGER(KIND=JPIM)           :: JECV, JF, IFCEBALPHA, IPHYS, JINDEX
LOGICAL                      :: LLCHANGE

REAL(KIND=JPHOOK)            :: ZHOOK_HANDLE

!     ------------------------------------------------------------------

#include "abor1.intfb.h"

!     ------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('YOMJBECPHYSECV:SUINFCE_ECPHYS',0,ZHOOK_HANDLE)

ASSOCIATE(NPROMA=>YDGEOMETRY%YRDIM%NPROMA, &
        & NGPBLKS=>YDGEOMETRY%YRDIM%NGPBLKS,&
        & NFLEVG=>YDGEOMETRY%YRDIMV%NFLEVG, &
        & NGPTOT=>YDGEOMETRY%YRGEM%NGPTOT, &
        & NOFCEF=>YD_JB_STRUCT%JB_DATA%NOFCEF, &
        & MECVGRB_ALL=>YRECVGRIB%MECVGRB_ALL)

! 1. INITIALISATION

ALLOCATE(ZFCE(NPROMA))

! 2. LOOP OVER THE ECV FIELDS

DO JECV = 1, YRECVCONFIG%NECV
  !
  ! If parameter estimation
  !
  IF (TRIM(YRECVCONFIG%YRCONFIG(JECV)%CSETDESC) ==  'EC_PHYS') THEN
    !
    ! Seach the index of the ECV field in YD_JB_STRUCT
    !
    IFCEBALPHA=HUGE(IFCEBALPHA)
    DO JF=1,YD_JB_STRUCT%CONFIG%N_SPJB_VARS
      IF (YD_JB_STRUCT%SPJB_VARS_INFO(JF)%L_IN_ECV) THEN
        IFCEBALPHA = YD_JB_STRUCT%SPJB_VARS_INFO(JF)%IPTFCE-1
        EXIT
      ENDIF
    ENDDO
    !
    ! Get the first guess in grid point
    !
    IF (YRECVCONFIG%YRCONFIG(JECV)%L_IN_2D_SP) THEN
      ILEVMAX = 1
      ALLOCATE(ZGPTMP(NPROMA,1,NGPBLKS))
      CALL ECV_SPTOGP(YDGEOMETRY,YRECV5,JECV,PGP2=ZGPTMP)
    ELSEIF (YRECVCONFIG%YRCONFIG(JECV)%L_IN_2D_GP) THEN
      CALL ABOR1('YOMJBECPHYSECV:SUINFCE_ECPHYS not yet implemented')
    ELSEIF (YRECVCONFIG%YRCONFIG(JECV)%L_IN_3D_SP) THEN
      ILEVMAX = NFLEVG
      ALLOCATE(ZGPTMP(NPROMA,NFLEVG,NGPBLKS))
      CALL ECV_SPTOGP(YDGEOMETRY,YRECV5,JECV,PGP3=ZGPTMP)
    ELSEIF (YRECVCONFIG%YRCONFIG(JECV)%L_IN_3D_GP) THEN
      ILEVMAX = NFLEVG
      ALLOCATE(ZGPTMP(NPROMA,NFLEVG,NGPBLKS))
      ZGPTMP(:,:,:) = YRECV5%RGPECV3D(JECV,:,:,:)
    ENDIF
    !
    ! Change of standard deviation if needed
    !
!$OMP PARALLEL  PRIVATE(JSTGLO,ICEND,IBL,JLEV,ZFCE,JK,IPHYS,LLCHANGE)
    DO JLEV=1,ILEVMAX
      !
      IF (YRECVCONFIG%YRCONFIG(JECV)%L_IN_2D_SP .AND. JINDEX /= 1) THEN
        CALL LOOKUP(JECV,IPHYS,LLCHANGE)
        JINDEX = 1
      ELSE
        CALL LOOKUP(JLEV,IPHYS,LLCHANGE)
        JINDEX = YPHYS_ECV(IPHYS)%NINDEX
      ENDIF
      !
      IF (LLCHANGE .AND. IFCEBALPHA /= HUGE(IFCEBALPHA)) THEN
!$OMP DO SCHEDULE(STATIC)
        DO JSTGLO=1,NGPTOT,NPROMA
          ISTC=1
          ICEND=MIN(NPROMA,NGPTOT-JSTGLO+1)
          IBL=(JSTGLO-1)/NPROMA+1
          ZFCE(ISTC:ICEND) = YD_JB_STRUCT%JB_DATA%FCE%BUF (ISTC:ICEND, IFCEBALPHA+JLEV, 1+(JSTGLO-1)/NPROMA)
          DO JK=ISTC,ICEND
            IF (YPHYS_ECV(IPHYS)%LLOGCV) THEN
              IF (ZGPTMP(JK,JINDEX,IBL) > TINY(1._JPRB)) THEN
                ZFCE(JK) = ZFCE(JK)/ZGPTMP(JK,JINDEX,IBL)
              ELSE
                ZFCE(JK) = 0._JPRB
              ENDIF
            ENDIF
            IF (YPHYS_ECV(IPHYS)%LLAND) THEN
              ZFCE(JK) = ZFCE(JK)*YRECV0%RLSM(JK,IBL)
            ENDIF
          ENDDO
          YD_JB_STRUCT%JB_DATA%FCE%BUF (ISTC:ICEND, IFCEBALPHA+JLEV, 1+(JSTGLO-1)/NPROMA) = ZFCE(ISTC:ICEND)
        ENDDO
!$OMP END DO
      ENDIF
    ENDDO
!$OMP END PARALLEL
    DEALLOCATE(ZGPTMP)
  ENDIF
ENDDO
DEALLOCATE(ZFCE)

END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('YOMJBECPHYSECV:SUINFCE_ECPHYS',1,ZHOOK_HANDLE)

END SUBROUTINE SUINFCE_ECPHYS

