SUBROUTINE GET_PHYS_ECV(YDGEOMETRY, YDECV, &
  & PHSDFOR, PCGPP, PCREC, LDCAP)

!     Purpose.
!     --------
!       Get the physics parameters from the ECV structure

USE GEOMETRY_MOD, ONLY : GEOMETRY
USE YOMHOOK     , ONLY : LHOOK, DR_HOOK, JPHOOK
USE YOMVAR      , ONLY : LECV
USE YOMJBECV    , ONLY : YRECVCONFIG, YRECV5, ECV_SPTOGP

IMPLICIT NONE

TYPE(GEOMETRY)     ,INTENT(IN)  :: YDGEOMETRY
TYPE(ECV_CONTAINER),INTENT(IN)  :: YDECV
REAL(KIND=JPRB)    ,INTENT(OUT) :: PHSDFOR(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIM%NGPBLKS)
REAL(KIND=JPRB)    ,INTENT(OUT) :: PCGPP(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIM%NGPBLKS)
REAL(KIND=JPRB)    ,INTENT(OUT) :: PCREC(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIM%NGPBLKS)
LOGICAL, OPTIONAL  ,INTENT(IN)  :: LDCAP

INTEGER(KIND=JPIM) :: JECV,  IPHYS, JINDEX, JROF
INTEGER(KIND=JPIM) :: JKGLO, ICEND, IBL, JK, ILEV
REAL(KIND=JPRB)    :: ZBCAP
REAL(KIND=JPRB)    :: ZGPTMP(YDGEOMETRY%YRDIM%NPROMA,YDGEOMETRY%YRDIMV%NFLEVG,YDGEOMETRY%YRDIM%NGPBLKS)
LOGICAL            :: LLCAP, LLGET

REAL(KIND=JPHOOK)  :: ZHOOK_HANDLE

!     ------------------------------------------------------------------

#include "abor1.intfb.h"

!     ------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('YOMJBECPHYSECV:GET_PHYS_ECV',0,ZHOOK_HANDLE)

ASSOCIATE(NPROMA=>YDGEOMETRY%YRDIM%NPROMA,&
        & NGPTOT=>YDGEOMETRY%YRGEM%NGPTOT)

! 1. INITIALISATION


PHSDFOR(:,:) = 0.0_JPRB
PCGPP(:,:) = 0.0_JPRB
PCREC(:,:) = 0.0_JPRB
ZGPTMP(:,:,:) = 0.0_JPRB

IF (PRESENT(LDCAP)) THEN
  LLCAP = LDCAP
ELSE
  LLCAP = .FALSE.
ENDIF

! 2. GET PHYSICS FIELDS

IF (LECV) THEN
  ! Loop over all the ECV field rto search  for the physics part
  DO JECV = 1, YRECVCONFIG%NECV
    IF (TRIM(YRECVCONFIG%YRCONFIG(JECV)%CSETDESC) ==  'EC_PHYS') THEN
      !
      ! Fields stored in 2D  spectral  ECV
      !
      IF (YRECVCONFIG%YRCONFIG(JECV)%L_IN_2D_SP) THEN
        CALL ECV_SPTOGP(YDGEOMETRY,YDECV,JECV,PGP2=ZGPTMP(:,1:1,:))
      !
      ! Fields stored in 2D  gridpoint  ECV
      !
      ELSEIF (YRECVCONFIG%YRCONFIG(JECV)%L_IN_2D_GP) THEN
        CALL ABOR1('YOMJBECPHYSECV:GET_PHYS_ECV not yet implemented')
      !
      ! Fields stored in 3D  spectral  ECV
      !
      ELSEIF (YRECVCONFIG%YRCONFIG(JECV)%L_IN_3D_SP) THEN
        CALL ECV_SPTOGP(YDGEOMETRY,YDECV,JECV,PGP3=ZGPTMP)
      !
      ! Fields stored in 3D  gridpoint  ECV
      !
      ELSEIF (YRECVCONFIG%YRCONFIG(JECV)%L_IN_3D_GP) THEN
        DO JKGLO=1,NGPTOT,NPROMA
          ICEND=MIN(NPROMA,NGPTOT-JKGLO+1)
          IBL=(JKGLO-1)/NPROMA+1
          DO JK=1,ICEND
            ZGPTMP(JK,:,IBL) = YDECV%RGPECV3D(JECV,JK,:,IBL)
          ENDDO
        ENDDO
      ENDIF
      !
      ! Extract the desired physics fields
      !
      DO IPHYS = 1, NPHYS_ECV
        JINDEX = YPHYS_ECV(IPHYS)%NINDEX
        LLGET = .FALSE.
        IF (YRECVCONFIG%YRCONFIG(JECV)%L_IN_2D_SP .AND. JINDEX == JECV) THEN
          ILEV=1
          LLGET = .TRUE.
        ELSEIF (YRECVCONFIG%YRCONFIG(JECV)%L_IN_3D_SP .OR. YRECVCONFIG%YRCONFIG(JECV)%L_IN_3D_GP) THEN
          ILEV=JINDEX
          LLGET = .TRUE.
        ENDIF
        IF (LLGET) THEN
          ZBCAP = YPHYS_ECV(IPHYS)%RBCAP
          DO JKGLO=1,NGPTOT,NPROMA
            ICEND=MIN(NPROMA,NGPTOT-JKGLO+1)
            IBL=(JKGLO-1)/NPROMA+1
            DO JK=1,ICEND
              IF (LLCAP .AND. ZBCAP /= ZMISSING) THEN
                ZGPTMP(JK,ILEV,IBL) = MAX(ZGPTMP(JK,ILEV,IBL), ZBCAP)
              ENDIF
              IF (TRIM(YPHYS_ECV(IPHYS)%CNAME) == 'SDFOR') THEN
                PHSDFOR(JK,IBL) = ZGPTMP(JK,ILEV,IBL)
              ELSEIF (TRIM(YPHYS_ECV(IPHYS)%CNAME) == 'BFAS_GPP' ) THEN
                PCGPP(JK,IBL) = ZGPTMP(JK,ILEV,IBL)
              ELSEIF (TRIM(YPHYS_ECV(IPHYS)%CNAME) == 'BFAS_REC' ) THEN
                PCREC(JK,IBL) = ZGPTMP(JK,ILEV,IBL)
              ELSE
                CALL ABOR1('YOMJBECPHYSECV:GET_PHYS_ECV CNAME UNKNOWN')
              ENDIF
            ENDDO
          ENDDO
        ENDIF
      ENDDO
    !
    !  End if EC_PHYS
    !
    ENDIF
  ENDDO
ENDIF

END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('YOMJBECPHYSECV:GET_PHYS_ECV',1,ZHOOK_HANDLE)

END SUBROUTINE GET_PHYS_ECV

