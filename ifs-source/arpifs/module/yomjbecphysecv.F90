SUBROUTINE ECPHYS_WRMLPPG_GP3(YDGEOMETRY,PECV,KECV,KGRIBCD,PREAL,KGRIBIOGG)

!     Purpose.
!     --------
!       Use land/sea mask if needed in WRMLPPG

USE GEOMETRY_MOD, ONLY : GEOMETRY
USE YOMJBECV    , ONLY : YRECV0, YRECV5
USE YOMHOOK,      ONLY : LHOOK, DR_HOOK, JPHOOK

IMPLICIT NONE

