SUBROUTINE ECPHYS_WRMLPPG_SP2(YDGEOMETRY,PECV,KECV,KGRIBCD,PREAL,KGRIBIOSH)

!     Purpose.
!     --------
!       Use land/sea mask if needed in WRMLPPG

USE GEOMETRY_MOD, ONLY : GEOMETRY
USE YOMJBECV    , ONLY : YRECV0
USE YOMMP0       , ONLY : MYSETV
USE YOMHOOK,      ONLY : LHOOK, DR_HOOK, JPHOOK

IMPLICIT NONE

TYPE(GEOMETRY)    ,INTENT(IN)  :: YDGEOMETRY
REAL(KIND=JPRB)   ,INTENT(IN)  :: PECV(YDGEOMETRY%YRDIM%NSPEC2)
INTEGER(KIND=JPIM),INTENT(IN)  :: KECV
INTEGER(KIND=JPIM),INTENT(IN)  :: KGRIBCD
REAL(KIND=JPRB)   ,INTENT(OUT) :: PREAL(YDGEOMETRY%YRDIM%NSPEC2,1)
INTEGER(KIND=JPIM),INTENT(OUT) :: KGRIBIOSH(4,YDGEOMETRY%YRDIMV%NFLEVG)


INTEGER(KIND=JPIM)           :: IFF, J, IPHYS
INTEGER(KIND=JPIM)           :: JSTGLO, IEND, IST, IBL
REAL(KIND=JPRB)              :: ZMASK
REAL(KIND=JPRB), ALLOCATABLE :: ZGPTMP(:,:,:)
REAL(KIND=JPRB), ALLOCATABLE :: ZSPTMP(:,:)
LOGICAL                      :: LLFIND

REAL(KIND=JPHOOK)            :: ZHOOK_HANDLE

!     ------------------------------------------------------------------

#include "inv_trans.h"
#include "dir_trans.h"
#include "abor1.intfb.h"

!     ------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('YOMJBECPHYSECV:ECPHYS_WRMLPPG_SP2',0,ZHOOK_HANDLE)

ASSOCIATE(NPROMA=>YDGEOMETRY%YRDIM%NPROMA,&
        & NGPTOT=>YDGEOMETRY%YRGEM%NGPTOT,&
        & NRESOL=>YDGEOMETRY%YRDIM%NRESOL,&
        & NSPEC2=>YDGEOMETRY%YRDIM%NSPEC2,&
        & NGPBLKS=>YDGEOMETRY%YRDIM%NGPBLKS,&
        & NPSP=>YDGEOMETRY%YRMP%NPSP,&
        & NBSETSP=>YDGEOMETRY%YRMP%NBSETSP)

! 1. COPY

KGRIBIOSH(:,:) = 0_JPIM
KGRIBIOSH(1,1) = KGRIBCD
KGRIBIOSH(2,1) = 1
KGRIBIOSH(3,1) = NBSETSP
KGRIBIOSH(4,1) = 1
PREAL(:,:) = 0.0_JPRB
IFF=0
IF( NBSETSP == MYSETV )THEN
  IFF=IFF+1
  DO J=1,NSPEC2
    PREAL(J,IFF) = PECV(J)
  ENDDO
  IF (NPSP /= 1) CALL ABOR1('YOMJBECPHYSECV:ECPHYS_WRMLPPG_SP2 problem with NPSP')
ENDIF

! 2. APPLY MASK IF NEEDED

CALL LOOKUP(KECV,IPHYS,LLFIND)
IF (LLFIND .AND. YPHYS_ECV(IPHYS)%LLAND) THEN
  !
  ! Initialisation
  !
  ALLOCATE(ZGPTMP(NPROMA,1,NGPBLKS))
  ALLOCATE(ZSPTMP(1,NSPEC2))
  !
  ! Transform into grid point space
  !
  ZSPTMP(1:IFF,:) = PREAL(:,1:IFF)
  CALL INV_TRANS (PSPSC2=ZSPTMP(1:IFF,:),&
    & KPROMA=NPROMA,KVSETSC2=(/NBSETSP/),KRESOL=NRESOL,&
    & PGP2=ZGPTMP)
  !
  ! Apply mask
  !
  DO JSTGLO=1,NGPTOT,NPROMA
    IST=1
    IEND=MIN(NPROMA,NGPTOT-JSTGLO+1)
    IBL=(JSTGLO-1)/NPROMA+1
    DO J=IST,IEND
      ZGPTMP(J,1,IBL)=ZGPTMP(J,1,IBL)*YRECV0%RLSM(J,IBL)
    ENDDO
  ENDDO
  !
  ! Back to spectral space
  !
  CALL DIR_TRANS (PSPSC2=ZSPTMP(1:IFF,:),&
     & KPROMA=NPROMA,KVSETSC2=(/NBSETSP/),KRESOL=NRESOL,&
     & PGP2=ZGPTMP)
  PREAL(:,1:IFF) = ZSPTMP(1:IFF,:)
  !
  ! Free memory
  !
  DEALLOCATE(ZGPTMP)
  DEALLOCATE(ZSPTMP)
ENDIF


END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('YOMJBECPHYSECV:ECPHYS_WRMLPPG_SP2',1,ZHOOK_HANDLE)

END SUBROUTINE ECPHYS_WRMLPPG_SP2

