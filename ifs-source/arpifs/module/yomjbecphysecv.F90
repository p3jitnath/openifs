SUBROUTINE ECPHYS_WRMLPPG_SP3(YDGEOMETRY,PECV,KECV,KGRIBCD,PREAL,KGRIBIOSH)

!     Purpose.
!     --------
!       Use land/sea mask if needed in WRMLPPG

USE GEOMETRY_MOD, ONLY : GEOMETRY
USE YOMJBECV    , ONLY : YRECV0
USE YOMMP0      , ONLY : MYSETV
USE YOMHOOK,      ONLY : LHOOK, DR_HOOK, JPHOOK

IMPLICIT NONE

TYPE(GEOMETRY)    ,INTENT(IN)  :: YDGEOMETRY
REAL(KIND=JPRB)   ,INTENT(IN)  :: PECV(YDGEOMETRY%YRDIMV%NFLEVG,YDGEOMETRY%YRDIM%NSPEC2)
INTEGER(KIND=JPIM),INTENT(IN)  :: KECV
INTEGER(KIND=JPIM),INTENT(IN)  :: KGRIBCD
REAL(KIND=JPRB)   ,INTENT(OUT) :: PREAL(YDGEOMETRY%YRDIM%NSPEC2,YDGEOMETRY%YRDIMV%NFLEVG)
INTEGER(KIND=JPIM),INTENT(OUT) :: KGRIBIOSH(4,YDGEOMETRY%YRDIMV%NFLEVG)


INTEGER(KIND=JPIM)           :: IFF, J, JLEV, IPHYS, JINDEX
INTEGER(KIND=JPIM)           :: JSTGLO, IEND, IST, IBL
REAL(KIND=JPRB)              :: ZMASK
REAL(KIND=JPRB), ALLOCATABLE :: ZGPTMP(:,:,:)
REAL(KIND=JPRB), ALLOCATABLE :: ZSPTMP(:,:)
LOGICAL                      :: LLFIND, LLCHANGELEV, LLCHANGE(YDGEOMETRY%YRDIMV%NFLEVG)

REAL(KIND=JPHOOK)            :: ZHOOK_HANDLE

!     ------------------------------------------------------------------

#include "inv_trans.h"
#include "dir_trans.h"

!     ------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('YOMJBECPHYSECV:ECPHYS_WRMLPPG_SP3',0,ZHOOK_HANDLE)

ASSOCIATE(NPROMA=>YDGEOMETRY%YRDIM%NPROMA,&
        & NGPTOT=>YDGEOMETRY%YRGEM%NGPTOT,&
        & NRESOL=>YDGEOMETRY%YRDIM%NRESOL,&
        & NSPEC2=>YDGEOMETRY%YRDIM%NSPEC2,&
        & NGPBLKS=>YDGEOMETRY%YRDIM%NGPBLKS,&
        & NFLEVG=>YDGEOMETRY%YRDIMV%NFLEVG,&
        & NBSETSP=>YDGEOMETRY%YRMP%NBSETSP,&
        & NPTRLL=>YDGEOMETRY%YRMP%NPTRLL,&
        & NBSETLEV=>YDGEOMETRY%YRMP%NBSETLEV)

! 1. COPY

IFF=0
KGRIBIOSH(:,:) = 0_JPIM
DO JLEV=1,NFLEVG
  KGRIBIOSH(1,JLEV) = KGRIBCD
  KGRIBIOSH(2,JLEV) = JLEV
  KGRIBIOSH(3,JLEV) = NBSETLEV(JLEV)
  KGRIBIOSH(4,JLEV) = JLEV-NPTRLL(NBSETLEV(JLEV))+1
  IF( NBSETLEV(JLEV) == MYSETV )THEN
    IFF=IFF+1
    DO J=1,NSPEC2
      PREAL(J,IFF) = PECV(KGRIBIOSH(4,JLEV),J)
    ENDDO
  ENDIF
ENDDO

! 2. CHECK IF LSM OR LOG CV IF NEEDED
DO JLEV=1, NFLEVG
  CALL LOOKUP(JLEV,IPHYS,LLCHANGELEV)
  IF (LLCHANGELEV .AND. YPHYS_ECV(IPHYS)%LLAND) THEN
    LLCHANGE(JLEV) = .TRUE.
  ELSE
    LLCHANGE(JLEV) = .FALSE.
  ENDIF
ENDDO

! 3. APPLY LSM OR LOG CV IF NEEDED (grid point space)

IF (ANY(LLCHANGE)) THEN
  !
  ! Initialisation
  !
  ALLOCATE(ZGPTMP(NPROMA,NFLEVG,NGPBLKS))
  ALLOCATE(ZSPTMP(NFLEVG,NSPEC2))
  DO JLEV=1,NFLEVG
    DO J=1,NSPEC2
      ZSPTMP(JLEV,J)=PREAL(J,JLEV)
    ENDDO
  ENDDO
  !
  ! Transform into grid point space
  !
  CALL INV_TRANS(PSPSCALAR=ZSPTMP,KVSETSC=NBSETLEV,&
   & KRESOL=NRESOL,KPROMA=NPROMA,PGP=ZGPTMP)
  !
  ! Apply change
  !
  DO JLEV=1, NFLEVG
    !
    CALL LOOKUP(JLEV,IPHYS,LLCHANGELEV)
    JINDEX = YPHYS_ECV(IPHYS)%NINDEX
    IF (YPHYS_ECV(IPHYS)%LLAND) THEN
      DO JSTGLO=1,NGPTOT,NPROMA
        IEND=MIN(NPROMA,NGPTOT-JSTGLO+1)
        IBL=(JSTGLO-1)/NPROMA+1
        DO J=1,IEND
          ZGPTMP(J,JINDEX,IBL) = ZGPTMP(J,JINDEX,IBL)*YRECV0%RLSM(J,IBL)
        ENDDO
      ENDDO
    ENDIF
  ENDDO
  !
  ! Back to spectral space
  !
  CALL DIR_TRANS(PSPSCALAR=ZSPTMP,KVSETSC=NBSETLEV,&
   & KRESOL=NRESOL,KPROMA=NPROMA,PGP=ZGPTMP)
  DO JLEV=1,NFLEVG
    DO J=1,NSPEC2
      PREAL(J,JLEV)=ZSPTMP(JLEV,J)
    ENDDO
  ENDDO
  !
  ! Free memory
  !
  DEALLOCATE(ZSPTMP)
  DEALLOCATE(ZGPTMP)
ENDIF

END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('YOMJBECPHYSECV:ECPHYS_WRMLPPG_SP3',1,ZHOOK_HANDLE)

END SUBROUTINE ECPHYS_WRMLPPG_SP3

