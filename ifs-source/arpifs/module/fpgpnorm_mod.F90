USE PARKIND1  ,ONLY : JPIM     ,JPRB
USE YOMHOOK   ,ONLY : LHOOK,   DR_HOOK, JPHOOK

IMPLICIT NONE

INTERFACE FPGPNORMX
  MODULE PROCEDURE FPGPNORM1, FPGPNORM2
END INTERFACE

PRIVATE
PUBLIC :: FPGPNORMX

CONTAINS

SUBROUTINE FPGPNORM1 (PREAL, PAVE, PMIN, PMAX, LDUNDF, PUNDEF, PEPSILON)

REAL(KIND=JPRB),    INTENT(IN)  :: PREAL (:) 
REAL(KIND=JPRB),    INTENT(OUT) :: PAVE 
REAL(KIND=JPRB),    INTENT(OUT) :: PMIN 
REAL(KIND=JPRB),    INTENT(OUT) :: PMAX
LOGICAL,            INTENT(IN)  :: LDUNDF
REAL(KIND=JPRB),    INTENT(IN)  :: PUNDEF
REAL(KIND=JPRB),    INTENT(IN), OPTIONAL :: PEPSILON

INTEGER(KIND=JPIM) :: ILEN

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('FPGPNORM_MOD:FPGPNORM1',0,ZHOOK_HANDLE)

IF (LDUNDF) THEN
  ILEN = COUNT (PREAL (:) /= PUNDEF)
  IF (ILEN == 0) THEN
    PAVE = 0._JPRB
    PMIN = 0._JPRB
    PMAX = 0._JPRB
  ELSE
    PAVE = SUM(PREAL, MASK=PREAL/=PUNDEF) / REAL (ILEN, JPRB)
    PMIN = MINVAL(PREAL, MASK=PREAL/=PUNDEF)
    PMAX = MAXVAL(PREAL, MASK=PREAL/=PUNDEF)
  ENDIF
ELSE
  ILEN = SIZE (PREAL, 1)
  PAVE = SUM(PREAL) / REAL (ILEN, JPRB)
  PMIN = MINVAL(PREAL)
  PMAX = MAXVAL(PREAL)
ENDIF

IF (PRESENT (PEPSILON)) THEN
  IF (ABS(PAVE) < PEPSILON) PAVE=0._JPRB
  IF (ABS(PMIN) < PEPSILON) PMIN=0._JPRB
  IF (ABS(PMAX) < PEPSILON) PMAX=0._JPRB
ENDIF


IF (LHOOK) CALL DR_HOOK('FPGPNORM_MOD:FPGPNORM1',1,ZHOOK_HANDLE)

END SUBROUTINE FPGPNORM1

SUBROUTINE FPGPNORM2 (PREAL, PAVE, PMIN, PMAX, LDUNDF, PUNDEF, KMASK, PEPSILON)

REAL(KIND=JPRB),    INTENT(IN)  :: PREAL (:,:) 
REAL(KIND=JPRB),    INTENT(OUT) :: PAVE (:) 
REAL(KIND=JPRB),    INTENT(OUT) :: PMIN (:) 
REAL(KIND=JPRB),    INTENT(OUT) :: PMAX (:) 
LOGICAL,            INTENT(IN)  :: LDUNDF (:)
REAL(KIND=JPRB),    INTENT(IN)  :: PUNDEF (:)
INTEGER(KIND=JPIM), INTENT(IN), OPTIONAL :: KMASK (:) 
REAL(KIND=JPRB),    INTENT(IN), OPTIONAL :: PEPSILON

INTEGER(KIND=JPIM) :: JFLD
INTEGER(KIND=JPIM) :: IFLDS
INTEGER(KIND=JPIM) :: ILEN
LOGICAL :: LLMASK

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('FPGPNORM_MOD:FPGPNORM2',0,ZHOOK_HANDLE)

IFLDS = SIZE (PREAL, 2)


DO JFLD = 1, IFLDS

  LLMASK = .TRUE.
  IF (PRESENT (KMASK)) LLMASK = KMASK(JFLD) == 1

  IF (LLMASK) THEN

    IF (LDUNDF (JFLD)) THEN
      ILEN = COUNT (PREAL(:,JFLD) /= PUNDEF (JFLD))
      IF (ILEN == 0) THEN
        PAVE(JFLD)=0._JPRB
        PMIN(JFLD)=0._JPRB
        PMAX(JFLD)=0._JPRB
      ELSE
        PAVE(JFLD)=SUM(PREAL(:,JFLD), MASK=PREAL(:,JFLD) /= PUNDEF (JFLD)) / REAL (ILEN, JPRB)
        PMIN(JFLD)=MINVAL(PREAL(:,JFLD), MASK=PREAL(:,JFLD) /= PUNDEF (JFLD))
        PMAX(JFLD)=MAXVAL(PREAL(:,JFLD), MASK=PREAL(:,JFLD) /= PUNDEF (JFLD))
      ENDIF
    ELSE
      PAVE(JFLD)=SUM(PREAL(:,JFLD)) / REAL (SIZE (PREAL, 1), JPRB)
      PMIN(JFLD)=MINVAL(PREAL(:,JFLD))
      PMAX(JFLD)=MAXVAL(PREAL(:,JFLD))
    ENDIF

    IF (PRESENT (PEPSILON)) THEN
      IF (ABS(PAVE(JFLD)) < PEPSILON) PAVE(JFLD)=0._JPRB
      IF (ABS(PMIN(JFLD)) < PEPSILON) PMIN(JFLD)=0._JPRB
      IF (ABS(PMAX(JFLD)) < PEPSILON) PMAX(JFLD)=0._JPRB
    ENDIF

  ENDIF

ENDDO

IF (LHOOK) CALL DR_HOOK('FPGPNORM_MOD:FPGPNORM2',1,ZHOOK_HANDLE)

END SUBROUTINE FPGPNORM2

END MODULE FPGPNORM_MOD
