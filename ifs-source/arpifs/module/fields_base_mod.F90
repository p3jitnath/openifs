SUBROUTINE FIELDS_GPNORM(SELF,CDGREP,KFIDS,PMAX,PMIN)
CLASS(FIELDS_BASE),      INTENT(INOUT) :: SELF
CHARACTER(LEN=*),        INTENT(IN)    :: CDGREP
INTEGER(KIND=JPIM),OPTIONAL,TARGET,INTENT(IN) :: KFIDS(:)
REAL(KIND=JPRB),OPTIONAL,INTENT(OUT)   :: PMAX
REAL(KIND=JPRB),OPTIONAL,INTENT(OUT)   :: PMIN

TYPE(FIELD_ACCESS)  :: YLFAC
REAL(KIND=JPRB) :: ZZ(1),ZMAXVAL_SELF,ZMINVAL_SELF
REAL(KIND=JPRB),ALLOCATABLE :: ZMEAN(:),ZMIN(:),ZMAX(:),ZGP(:,:,:),ZZMIN(:,:),ZZMAX(:,:)
REAL(KIND=JPRB),POINTER :: ZFIELD1(:),ZFIELD2(:,:)
INTEGER(KIND=JPIM) :: JCOL,JBLK,IKGLO,IST,IEND,IFIELDS,JFLD,ID,JL,JF,ITH,ITHMAX,IGPNMASTER,ITAG
INTEGER(KIND=JPIM),POINTER :: IFIDS(:)
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

#include "gpnorm_trans.h"
#include "egpnorm_trans.h"

IF (LHOOK) CALL DR_HOOK('FIELDS_BASE_MOD:FIELDS_GPNORM',0,ZHOOK_HANDLE)
ASSOCIATE(YDDIM=>SELF%GEOM%YRDIM,YDDIMV=>SELF%GEOM%YRDIMV,YDGEM=>SELF%GEOM%YRGEM)
ASSOCIATE(NGPBLKS=>YDDIM%NGPBLKS, NPROMA=>YDDIM%NPROMA,NGPTOT=>YDGEM%NGPTOT,NGPTOTG=>YDGEM%NGPTOTG,&
 & NRESOL=>YDDIM%NRESOL,NFLEVG=>YDDIMV%NFLEVG)
IF(PRESENT(KFIDS)) THEN
  IFIDS => KFIDS
ELSE
  IFIDS => SELF%VARS%FIELDIDS
ENDIF
IFIELDS = SIZE(IFIDS)
ALLOCATE(ZGP(NPROMA,IFIELDS,NGPBLKS))
ALLOCATE(ZMEAN(IFIELDS))
ALLOCATE(ZMIN(IFIELDS))
ALLOCATE(ZMAX(IFIELDS))

ZMEAN(:) = 0.0_JPRB

ITHMAX=OML_MAX_THREADS()
ALLOCATE(ZZMIN(IFIELDS,ITHMAX))
ALLOCATE(ZZMAX(IFIELDS,ITHMAX))
ZZMIN(:,:) = HUGE(ZZMIN)
ZZMAX(:,:) = -HUGE(ZZMAX)

!$OMP PARALLEL PRIVATE(JBLK,IKGLO,IST,IEND,JFLD,JF,JL,ID,ZFIELD1,ZFIELD2,ITH)
!$OMP DO SCHEDULE(STATIC)
DO JBLK=1,NGPBLKS
  IKGLO = 1+(JBLK-1)*NPROMA
  IST = 1
  IEND = MIN(NPROMA,NGPTOT-IKGLO+1)
  DO JFLD=1,IFIELDS
    ZGP(IST:IEND,JFLD,JBLK) = 0.0_JPRB
    ID = IFIDS(JFLD)
    IF(SELF%FIELD%METADATA(ID)%NDIMS == 1) THEN
      CALL SELF%FIELD%FIELD_OPEN_ONE(ID,JBLK,ZFIELD1)
      ZGP(IST:IEND,JFLD,JBLK) = ZGP(IST:IEND,JFLD,JBLK)+ZFIELD1(IST:IEND)
      ITH = OML_MY_THREAD()
      ZZMIN(JFLD,ITH) = MIN(ZZMIN(JFLD,ITH),MINVAL(ZFIELD1(IST:IEND)))
      ZZMAX(JFLD,ITH) = MAX(ZZMAX(JFLD,ITH),MAXVAL(ZFIELD1(IST:IEND)))
    ELSE
      CALL SELF%FIELD%FIELD_OPEN_ONE(ID,JBLK,ZFIELD2)
      DO JF=1,NFLEVG
        DO JL=IST,IEND
          ITH = OML_MY_THREAD()
          ZZMIN(JFLD,ITH) = MIN(ZZMIN(JFLD,ITH),ZFIELD2(JL,JF))
          ZZMAX(JFLD,ITH) = MAX(ZZMAX(JFLD,ITH),ZFIELD2(JL,JF))
          ZGP(JL,JFLD,JBLK) = ZGP(JL,JFLD,JBLK)+ZFIELD2(JL,JF)
        ENDDO
      ENDDO
    ENDIF
  ENDDO
ENDDO
!$OMP END DO
!$OMP END PARALLEL

DO JFLD=1,IFIELDS
  ZMIN(JFLD) = MINVAL(ZZMIN(JFLD,:))
  ZMAX(JFLD) = MAXVAL(ZZMAX(JFLD,:))
ENDDO

IF (LELAM) THEN
   CALL EGPNORM_TRANS(ZGP,IFIELDS,NPROMA,ZMEAN,ZMIN,ZMAX,.TRUE.,NRESOL)
ELSE
   CALL GPNORM_TRANS(ZGP,IFIELDS,NPROMA,ZMEAN,ZMIN,ZMAX,.TRUE.,NRESOL)
ENDIF

IF(PRESENT(PMAX)) THEN
  IF(IFIELDS>1) CALL ABOR1('FIELDS_BASE_MOD:FIELDS_GPNORM More than 1 variable present')
  PMAX=ZMAX(1)
  IGPNMASTER=1
  IF (NPROC > 1) THEN
    ITAG=12345
    CALL MPL_BROADCAST(PMAX,ITAG,IGPNMASTER,CDSTRING='FIELDS_BASE_MOD:FIELDS_GPNORM-MAX:')
  ENDIF
ENDIF

IF(PRESENT(PMIN)) THEN
  IF(IFIELDS>1) CALL ABOR1('FIELDS_BASE_MOD:FIELDS_GPNORM More than 1 variable present')
  PMIN=ZMIN(1)
  IGPNMASTER=1
  IF (NPROC > 1) THEN
    ITAG=12345
    CALL MPL_BROADCAST(PMIN,ITAG,IGPNMASTER,CDSTRING='FIELDS_BASE_MOD:FIELDS_GPNORM-MIN:')
  ENDIF
ENDIF

WRITE(NULOUT,*) CDGREP
DO JFLD=1,IFIELDS
  ID = IFIDS(JFLD)
  IF(SELF%FIELD%METADATA(ID)%NDIMS /= 1) THEN
    ZMEAN(JFLD) = ZMEAN(JFLD)/NFLEVG
  ENDIF
  WRITE(NULOUT,'(5(1X,A))') 'FDNORM',' ID  NAME    ','    AVERAGE         ',&
           & '     MINIMUM        ','      MAXIMUM        '
  WRITE(NULOUT,'(9X,A5,I4,1X,A,3(1X,E21.15))') 'AVE  ',ID,SELF%FIELD%METADATA(ID)%NAME,ZMEAN(JFLD),ZMIN(JFLD),ZMAX(JFLD)
ENDDO
CALL FLUSH(NULOUT)
END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('FIELDS_BASE_MOD:FIELDS_GPNORM',1,ZHOOK_HANDLE)

END SUBROUTINE FIELDS_GPNORM
!==========================================================================================

SUBROUTINE FIELDS_SCHUR_PROD(SELF,RHS)
