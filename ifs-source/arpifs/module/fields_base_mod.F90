DO J=1,SIZE(KSOURCE)
  IF(ANY(KREMOVE(:) == KSOURCE(J))) CYCLE
  INUM = INUM+1
  ITEMP(INUM) = KSOURCE(J)
ENDDO
ALLOCATE(INTREST(INUM))
INTREST(:) = ITEMP(1:INUM)
IF (LHOOK) CALL DR_HOOK('FIELDS_BASE_MOD:INTREST',1,ZHOOK_HANDLE)
END FUNCTION INTREST

