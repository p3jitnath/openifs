CLASS(FIELDS_BASE),       INTENT(INOUT) :: RHS
TYPE(FIELD_ACCESS), POINTER :: YLFAC,YLRHS
INTEGER(KIND=JPIM) :: JCOL,JBLK,ITID,IST,IEND,IBL,JL,IKGLO
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

