FUNCTION INTREST(KSOURCE,KREMOVE)

INTEGER(KIND=JPIM),INTENT(IN) :: KSOURCE(:)
INTEGER(KIND=JPIM),INTENT(IN) :: KREMOVE(:)

INTEGER(KIND=JPIM),ALLOCATABLE :: INTREST(:)
INTEGER(KIND=JPIM) :: ITEMP(SIZE(KSOURCE)),INUM,J
