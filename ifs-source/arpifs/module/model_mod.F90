!> Handle model configuration for the IFS model

MODULE MODEL_MOD

USE TYPE_MODEL, ONLY : MODEL
USE FIELDS_MOD, ONLY : FIELDS

IMPLICIT NONE

PRIVATE

PUBLIC :: MODEL, MODEL_SET,      MODEL_UNSET,  MODEL_CREATE,    MODEL_DELETE, &
 & MODEL_INIT,   MODEL_INI_PHYS, MODEL_STEP,   MODEL_STEP_TRAJ, MODEL_FINALIZE, &
 & MODEL_INITTL, MODEL_STEPTL,   MODEL_STEPAD, MODEL_INITAD

! ------------------------------------------------------------------------------

INTERFACE

  MODULE SUBROUTINE MODEL_SET(SELF)
    TYPE(MODEL), TARGET, INTENT(IN) :: SELF
  END SUBROUTINE

  MODULE SUBROUTINE MODEL_UNSET(SELF)
    TYPE(MODEL), INTENT(IN) :: SELF
  END SUBROUTINE

  MODULE SUBROUTINE MODEL_CREATE(SELF, YDGEOMETRY, PTSTEP, CDNAMELIST, LDLINEAR, KGFLCONF,K_SELF)
    USE PARKIND1,     ONLY : JPRB, JPIM
    USE GEOMETRY_MOD, ONLY : GEOMETRY
    TYPE(MODEL),            INTENT(INOUT) :: SELF
    TYPE(GEOMETRY), TARGET, INTENT(INOUT) :: YDGEOMETRY
    REAL(KIND=JPRB),        INTENT(IN)    :: PTSTEP
    CHARACTER(LEN=*),       INTENT(IN)    :: CDNAMELIST
    LOGICAL,                INTENT(IN)    :: LDLINEAR
    INTEGER(KIND=JPIM), INTENT(IN), OPTIONAL :: KGFLCONF
    INTEGER(KIND=JPIM), INTENT(IN), OPTIONAL :: K_SELF
  END SUBROUTINE

  MODULE SUBROUTINE MODEL_INIT(SELF, YDFIELDS, YDTIME, YDERRMOD_FIELDS)
    USE FIELDS_MOD       , ONLY : FIELDS
    USE ERRMOD_FIELDS_MOD, ONLY : ERRMOD_FIELDS
    USE DATETIME_TMP_MOD , ONLY : DATETIME_TMP
    TYPE(MODEL),                   INTENT(INOUT) :: SELF
    TYPE(FIELDS),                  INTENT(INOUT) :: YDFIELDS
    TYPE(DATETIME_TMP),            INTENT(IN)    :: YDTIME
    TYPE(ERRMOD_FIELDS), OPTIONAL, INTENT(INOUT) :: YDERRMOD_FIELDS
  END SUBROUTINE

  MODULE SUBROUTINE MODEL_INITAD(SELF, DX, YDTIME, KSECS, DXERRMOD)
    USE PARKIND1         , ONLY : JPIM
    USE FIELDS_MOD       , ONLY : FIELDS
    USE ERRMOD_FIELDS_MOD, ONLY : ERRMOD_FIELDS
    USE DATETIME_TMP_MOD , ONLY : DATETIME_TMP
    TYPE(MODEL),                   INTENT(INOUT) :: SELF
    TYPE(FIELDS),                  INTENT(INOUT) :: DX
    TYPE(DATETIME_TMP),            INTENT(INOUT) :: YDTIME
    INTEGER(KIND=JPIM),            INTENT(INOUT) :: KSECS
    TYPE(ERRMOD_FIELDS), OPTIONAL, INTENT(INOUT) :: DXERRMOD
  END SUBROUTINE

  MODULE SUBROUTINE MODEL_INITTL(SELF, DX, YDTIME, DXERRMOD)
    USE FIELDS_MOD       , ONLY : FIELDS
    USE ERRMOD_FIELDS_MOD, ONLY : ERRMOD_FIELDS
    USE DATETIME_TMP_MOD , ONLY : DATETIME_TMP
    TYPE(MODEL),                   INTENT(INOUT) :: SELF
    TYPE(FIELDS),                  INTENT(INOUT) :: DX
    TYPE(DATETIME_TMP),            INTENT(IN)    :: YDTIME
    TYPE(ERRMOD_FIELDS), OPTIONAL, INTENT(INOUT) :: DXERRMOD
  END SUBROUTINE

  MODULE SUBROUTINE MODEL_FINALIZE(SELF, YDFIELDS)
    USE FIELDS_MOD      , ONLY : FIELDS
    TYPE(MODEL)       , INTENT(INOUT) :: SELF
    TYPE(FIELDS)      , INTENT(INOUT) :: YDFIELDS
  END SUBROUTINE

  MODULE SUBROUTINE MODEL_DELETE(SELF)
    TYPE(MODEL), INTENT(INOUT) :: SELF
  END SUBROUTINE

  MODULE SUBROUTINE MODEL_INI_PHYS(SELF, YDFIELDS, YDTIME)
    USE FIELDS_MOD      , ONLY : FIELDS
    USE DATETIME_TMP_MOD, ONLY : DATETIME_TMP
    TYPE(MODEL)       , INTENT(INOUT) :: SELF
    TYPE(FIELDS)      , INTENT(INOUT) :: YDFIELDS
    TYPE(DATETIME_TMP), INTENT(IN)    :: YDTIME
  END SUBROUTINE

  MODULE SUBROUTINE MODEL_STEP(SELF, YDFIELDS, YDTIME, KSECS, YDERRMOD_FIELDS, LDCLUPD)
    USE PARKIND1         , ONLY : JPIM
    USE FIELDS_MOD       , ONLY : FIELDS
    USE ERRMOD_FIELDS_MOD, ONLY : ERRMOD_FIELDS
    USE DATETIME_TMP_MOD , ONLY : DATETIME_TMP
    TYPE(MODEL)        ,           INTENT(INOUT) :: SELF
    TYPE(FIELDS)       ,           INTENT(INOUT) :: YDFIELDS
    TYPE(DATETIME_TMP) ,           INTENT(INOUT) :: YDTIME
    INTEGER(KIND=JPIM) ,           INTENT(INOUT) :: KSECS
    TYPE(ERRMOD_FIELDS), OPTIONAL, INTENT(IN)    :: YDERRMOD_FIELDS
    LOGICAL            , OPTIONAL, INTENT(IN)    :: LDCLUPD
  END SUBROUTINE
  
  MODULE SUBROUTINE MODEL_STEP_TRAJ(SELF, LINMODEL, YDFIELDS, YDOOPSTRAJ, YDTIME, KSECS)
    USE PARKIND1        , ONLY : JPIM
    USE FIELDS_MOD      , ONLY : FIELDS
    USE YOMTRAJ_OOPS    , ONLY : TRAJ_TYPE_oops
    USE DATETIME_TMP_MOD, ONLY : DATETIME_TMP
    TYPE(MODEL)         , INTENT(INOUT) :: SELF
    TYPE(MODEL)         , INTENT(INOUT) :: LINMODEL
    TYPE(FIELDS)        , INTENT(INOUT) :: YDFIELDS
    TYPE(TRAJ_TYPE_OOPS), INTENT(INOUT) :: YDOOPSTRAJ
    TYPE(DATETIME_TMP)  , INTENT(INOUT) :: YDTIME
    INTEGER(KIND=JPIM)  , INTENT(INOUT) :: KSECS
  END SUBROUTINE

  MODULE SUBROUTINE MODEL_STEPTL(SELF, YDFIELDS, YDOOPSTRAJ, YDTIME, KSECS, YDERRMOD_FIELDS)
    USE PARKIND1         , ONLY : JPIM
    USE FIELDS_MOD       , ONLY : FIELDS
    USE ERRMOD_FIELDS_MOD, ONLY : ERRMOD_FIELDS
    USE YOMTRAJ_OOPS     , ONLY : TRAJ_TYPE_oops
    USE DATETIME_TMP_MOD , ONLY : DATETIME_TMP
    TYPE(MODEL)         ,           INTENT(INOUT) :: SELF
    TYPE(FIELDS)        ,           INTENT(INOUT) :: YDFIELDS
    TYPE(TRAJ_TYPE_oops),           INTENT(INOUT) :: YDOOPSTRAJ
    TYPE(DATETIME_TMP)  ,           INTENT(INOUT) :: YDTIME
    INTEGER(KIND=JPIM)  ,           INTENT(INOUT) :: KSECS
    TYPE(ERRMOD_FIELDS) , OPTIONAL, INTENT(IN)    :: YDERRMOD_FIELDS
  END SUBROUTINE

  MODULE SUBROUTINE MODEL_STEPAD(SELF, YDFIELDS, YDOOPSTRAJ, YDTIME, KSECS, YDERRMOD_FIELDS)
    USE PARKIND1         , ONLY : JPIM
    USE FIELDS_MOD       , ONLY : FIELDS
    USE ERRMOD_FIELDS_MOD, ONLY : ERRMOD_FIELDS
    USE YOMTRAJ_OOPS     , ONLY : TRAJ_TYPE_oops
    USE DATETIME_TMP_MOD , ONLY : DATETIME_TMP
    TYPE(MODEL)         ,           INTENT(INOUT) :: SELF
    TYPE(FIELDS)        ,           INTENT(INOUT) :: YDFIELDS
    TYPE(TRAJ_TYPE_oops),           INTENT(INOUT) :: YDOOPSTRAJ
    TYPE(DATETIME_TMP)  ,           INTENT(INOUT) :: YDTIME
    INTEGER(KIND=JPIM)  ,           INTENT(INOUT) :: KSECS
    TYPE(ERRMOD_FIELDS) , OPTIONAL, INTENT(INOUT) :: YDERRMOD_FIELDS
  END SUBROUTINE

END INTERFACE

CONTAINS

END MODULE MODEL_MOD
