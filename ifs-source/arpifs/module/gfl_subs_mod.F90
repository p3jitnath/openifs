SUBROUTINE SET_GFL_ATTR(YDDIMV,YGFL,YDCOMP,LDADV,LDMGRID,LDADV5,LDTDIABLIN,&
 & LDHORTURB,LDT9,LDPHY,LDPT,LDPC,&
 & LDADJUST0,LDADJUST1,KCOUPLING,PREFVALC,LDBIPER,CDSLINT)

!**** *SET_GFL_ATTR* Add attributes to previously setup GFL components

!     Purpose.
!     --------
!     Add further attributes to previously setup, by call to DEFINE_GFL_COMP, GFL components

!        Explicit arguments :
!        --------------------
!        LDADV   - TRUE if field to be advected
!        LDADV5  - TRUE if field to be advected by background wind only
!        LDTDIABLIN- TRUE if physics tendencies interpolated by lin. int.
!        LDHORTURB-TRUE if field is subject of 3D turbulence.
!        LDT9    - TRUE if field present in t-dt
!        LDPHY   - TRUE if field updated by physics
!        LDPT    - TRUE if field present in phy. tend.
!        LDPC    - TRUE if field in predictor/corrector time stepping treatment (3TL)
!        LDADJUST0  - TRUE if field to be adjusted at t
!        LDADJUST1  - TRUE if field to be adjusted at t+dt
!        KCOUPLING - 1 if field to be coupled, 0 if not, -1 if coupled with REFVALC
!        REVALC     - refernce value for coupling, used only in case NCOUPLING==-1
!        LDBIPER    - TRUE if field to be biperiodised
!        CDSLINT - S.L. interpolator

!     Author.
!     -------
!      Mats Hamrud  *ECMWF*

!     Modifications.
!     --------------
!      Original : 2003-03-01
!      Modifications:
!      03/07/09 C. Fischer - add Arome/Aladin attributes
!      2004-Nov F. Vana - update of CDSLINT
!      07/07/27 E. Holm - add LDADV5, i. e. advection by background in TL/AD
!      22-Feb-2011 F. Vana - LDPHYLIN, LDHORTURB attributes
!      09-Aug-2012 F. Vana - renaming LDPHYLIN to LDTDIABLIN
!      F. Vana    Oct-2018: SLPHYS cleaning
!-------------------------------------------------------------------------

USE YOM_YGFL , ONLY : TYPE_GFLD

TYPE(TDIMV)        ,INTENT(IN)           :: YDDIMV
TYPE(TYPE_GFLD)    ,INTENT(INOUT)        :: YGFL
TYPE(TYPE_GFL_COMP),INTENT(INOUT),TARGET :: YDCOMP
LOGICAL            ,INTENT(IN),OPTIONAL  :: LDADV
LOGICAL            ,INTENT(IN),OPTIONAL  :: LDMGRID
LOGICAL            ,INTENT(IN),OPTIONAL  :: LDADV5
LOGICAL            ,INTENT(IN),OPTIONAL  :: LDTDIABLIN
LOGICAL            ,INTENT(IN),OPTIONAL  :: LDHORTURB
LOGICAL            ,INTENT(IN),OPTIONAL  :: LDT9 
LOGICAL            ,INTENT(IN),OPTIONAL  :: LDPHY
LOGICAL            ,INTENT(IN),OPTIONAL  :: LDPT
LOGICAL            ,INTENT(IN),OPTIONAL  :: LDPC
LOGICAL            ,INTENT(IN),OPTIONAL  :: LDADJUST0
LOGICAL            ,INTENT(IN),OPTIONAL  :: LDADJUST1
INTEGER(KIND=JPIM) ,INTENT(IN),OPTIONAL  :: KCOUPLING
REAL(KIND=JPRB)    ,INTENT(IN),OPTIONAL  :: PREFVALC
LOGICAL            ,INTENT(IN),OPTIONAL  :: LDBIPER
CHARACTER(LEN=12)  ,INTENT(IN),OPTIONAL  :: CDSLINT


INTEGER(KIND=JPIM) :: IGFLPTR
INTEGER(KIND=JPIM) :: INODEFLD=-99
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!-------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('GFL_SUBS_MOD:SET_GFL_ATTR',0,ZHOOK_HANDLE)
ASSOCIATE(NFLEVG=>YDDIMV%NFLEVG, NFLSUL=>YDDIMV%NFLSUL)

IF(YDCOMP%MP < 1 .OR. YDCOMP%MP > YGFL%NUMFLDS) THEN
  WRITE(NULERR,*)' SET_GFL_ATTR: ',YDCOMP%MP,YDCOMP%CNAME
  CALL ABOR1('SET_GFL_ATTR: GFL COMPONENT NOT SET UP')
ELSE
  IGFLPTR=YDCOMP%MP
ENDIF

IF(PRESENT(LDADV)) THEN
  YDCOMP%LADV = LDADV
ENDIF
IF(YDCOMP%LADV) THEN
  IF(.NOT.YDCOMP%LT1) THEN
    CALL ABOR1(' GFL field to be advected but LT1=false')
  ENDIF
  YGFL%NUMFLDS_SL1 = YGFL%NUMFLDS_SL1+1
  YDCOMP%MP_SL1 = YGFL%NUMFLDS_SL1
  YDCOMP%MP_SLX = (YGFL%NUMFLDS_SL1-1)*(NFLEVG+2*NFLSUL)
ENDIF
  
IF(PRESENT(LDMGRID)) THEN
  YDCOMP%LMGRID = LDMGRID
ENDIF
  
IF(PRESENT(LDADV5)) THEN
  YDCOMP%LADV5 = LDADV5
ENDIF

IF(PRESENT(LDTDIABLIN)) THEN
  YDCOMP%LTDIABLIN = LDTDIABLIN
ELSE
  YDCOMP%LTDIABLIN = .FALSE.
ENDIF

IF(PRESENT(LDHORTURB)) THEN
  YDCOMP%LHORTURB = LDHORTURB
ELSE
  YDCOMP%LHORTURB = .FALSE.
ENDIF

! Other timelevels etc.

IF(PRESENT(LDT9)) THEN
  YDCOMP%LT9 = LDT9
ENDIF
IF(YDCOMP%LT9 .AND. YDCOMP%MP9 == NUNDEFLD ) THEN
  YGFL%NUMFLDS9 = YGFL%NUMFLDS9+1
  YGFL%NDIM  = YGFL%NDIM+1
  YGFL%NDIM9 = YGFL%NDIM9+1
  YDCOMP%MP9 = YGFL%NDIM0+YGFL%NUMFLDS9
  YDCOMP%MP9_PH = YDCOMP%MP9
ELSE
  YDCOMP%MP9 = YDCOMP%MP
  YDCOMP%MP9_PH = YDCOMP%MP9
!!$  WRITE(NULOUT,*) 'WARNING YDCOMP%MP9 = YDCOMP%MP',YDCOMP%MP9,YDCOMP%MP
ENDIF

IF(PRESENT(LDPHY)) THEN
  YDCOMP%LPHY = LDPHY
ENDIF
IF(YDCOMP%MPSLP == INODEFLD) THEN ! Can't be NUNDEFLD it equals to 1 when boundchecking
  IF(YDCOMP%LPHY) THEN
    IF(.NOT.YDCOMP%LT1) THEN
      CALL ABOR1(' GFL field to be modified by physics but LT1=false')
    ENDIF
    YGFL%NUMFLDSPHY = YGFL%NUMFLDSPHY+1
    YGFL%NDIMSLP = YGFL%NDIMSLP+1
    YDCOMP%MPSLP = YGFL%NUMFLDSPHY
  ELSE
    YDCOMP%MPSLP = NUNDEFLD
  ENDIF
ENDIF

IF(PRESENT(LDPT)) THEN
  YDCOMP%LPT = LDPT
ENDIF
IF(YDCOMP%MPPT == NUNDEFLD) THEN
  IF(YDCOMP%LPT) THEN
    YGFL%NUMFLDSPT = YGFL%NUMFLDSPT+1
    YGFL%NDIMPT = YGFL%NDIMPT+1
    YDCOMP%MPPT = YGFL%NUMFLDSPT
  ENDIF
ENDIF
IF(PRESENT(LDPC)) THEN
  YDCOMP%LPC = LDPC
ENDIF
IF(YDCOMP%MPPC == NUNDEFLD) THEN
  IF(YDCOMP%LPC) THEN
    YGFL%NUMFLDSPC = YGFL%NUMFLDSPC+1
    YGFL%NDIMPC = YGFL%NDIMPC+1
    YDCOMP%MPPC = YGFL%NUMFLDSPC
  ENDIF
ENDIF


! LAM attributes (do not involve extra dimensioning or pointers)

IF(PRESENT(LDADJUST0)) THEN
  YDCOMP%LADJUST0 = LDADJUST0
ENDIF
IF(PRESENT(LDADJUST1)) THEN
  YDCOMP%LADJUST1 = LDADJUST1
ENDIF
IF(PRESENT(KCOUPLING)) THEN
  YDCOMP%NCOUPLING = KCOUPLING
ENDIF
IF(PRESENT(PREFVALC)) THEN
  YDCOMP%REFVALC = PREFVALC
ENDIF
IF(PRESENT(LDBIPER)) THEN
  YDCOMP%LBIPER = LDBIPER
ENDIF

IF(PRESENT(CDSLINT)) THEN
  YDCOMP%CSLINT=CDSLINT
  IF(YDCOMP%MP_SPL == NUNDEFLD) THEN
    IF(CDSLINT == 'LAITVSPCQM  ') THEN
      YGFL%NUMFLDS_SPL = YGFL%NUMFLDS_SPL+1
      YGFL%NDIM_SPL = YGFL%NDIM_SPL+1
      YDCOMP%MP_SPL = YGFL%NUMFLDS_SPL
    ENDIF
  ENDIF
ENDIF

END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('GFL_SUBS_MOD:SET_GFL_ATTR',1,ZHOOK_HANDLE)

