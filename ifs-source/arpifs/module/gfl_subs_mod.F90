#include "suslb.intfb.h"
!     ------------------------------------------------------------------
IF (LHOOK) CALL DR_HOOK('GFL_SUBS_MOD:REACT_CLOUD_GFL',0,ZHOOK_HANDLE)
ASSOCIATE(YGFL=>YDMODEL%YRML_GCONF%YGFL,YDPHNC=>YDMODEL%YRML_PHY_SLIN%YRPHNC)

ASSOCIATE(NFLEVG=>YDDIMV%NFLEVG, NFLSUL=>YDDIMV%NFLSUL, &
 & YI=>YGFL%YI, YL=>YGFL%YL, YA=>YGFL%YA, NCHEM=>YGFL%NCHEM, &
 & YLCONV=>YGFL%YLCONV, YLRCH4=>YGFL%YLRCH4, YCOMP=>YGFL%YCOMP, & 
 & YRCONV=>YGFL%YRCONV, YCHEM=>YGFL%YCHEM, YICONV=>YGFL%YICONV, YR=>YGFL%YR, &
 & YSCONV=>YGFL%YSCONV, YS=>YGFL%YS, YLIMA=>YGFL%YLIMA, NLIMA=>YGFL%NLIMA, &
 & LENCLD2=>YDPHNC%LENCLD2,LEPCLD2=>YDPHNC%LEPCLD2)
IF(L_OOPS) CALL ABOR1('GFL_SUBS_MOD: REACT_CLOUD_GFL - SHOULD NEVER BE CALLED BY OOPS')
IF (L_CLD_DEACT) THEN
  LLGPL = YL%LGP
  LLGPLCONV = YLCONV%LGP
  LLGPICONV = YICONV%LGP
  LLGPRCONV = YRCONV%LGP
  LLGPSCONV = YSCONV%LGP
  LLGPI = YI%LGP
  LLGPA = YA%LGP
  LLGPR = YR%LGP
  LLGPS = YS%LGP
  LLGPLRCH4 = YLRCH4%LGP
  DO JCHEM=1,NCHEM
   IF(.NOT.YCHEM(JCHEM)%LASSIM) LLGPCHEM(JCHEM)=YCHEM(JCHEM)%LGP
  ENDDO

  DO JLIMA=1,NLIMA
   IF(.NOT.YLIMA(JLIMA)%LASSIM) LLGPLIMA(JLIMA)=YLIMA(JLIMA)%LGP
  ENDDO
  
  IF (.NOT.(LENCLD2.OR.LEPCLD2)) CALL COPY_GFL_COMP(YGFL,YL,YL_SAVE)
  CALL COPY_GFL_COMP(YGFL,YLCONV,YLCONV_SAVE)
  CALL COPY_GFL_COMP(YGFL,YICONV,YICONV_SAVE)
  CALL COPY_GFL_COMP(YGFL,YRCONV,YRCONV_SAVE)
  CALL COPY_GFL_COMP(YGFL,YSCONV,YSCONV_SAVE)
  IF (.NOT.(LENCLD2.OR.LEPCLD2)) CALL COPY_GFL_COMP(YGFL,YI,YI_SAVE)
  IF (.NOT.(LENCLD2.OR.LEPCLD2)) CALL COPY_GFL_COMP(YGFL,YA,YA_SAVE)
  CALL COPY_GFL_COMP(YGFL,YR,YR_SAVE)
  CALL COPY_GFL_COMP(YGFL,YS,YS_SAVE)
  CALL COPY_GFL_COMP(YGFL,YLRCH4,YLRCH4_SAVE)
  DO JCHEM=1,NCHEM
    IF(.NOT.YCHEM(JCHEM)%LASSIM) CALL COPY_GFL_COMP(YGFL,YCHEM(JCHEM),YCHEM_SAVE(JCHEM))
  ENDDO

  DO JLIMA=1,NLIMA
    IF(.NOT.YLIMA(JLIMA)%LASSIM) CALL COPY_GFL_COMP(YGFL,YLIMA(JLIMA),YLIMA_SAVE(JLIMA))
  ENDDO

  ! Following looks like a nonsense:
  ! As LLGPxx==Yxx%LGP, there is no way (.NOT. LX .AND. LX) becomes ever .true.
  IF (.NOT. LLGPL .AND. YL%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS+1
  IF (.NOT. LLGPLCONV .AND. YLCONV%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS+1
  IF (.NOT. LLGPICONV .AND. YICONV%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS+1
  IF (.NOT. LLGPRCONV .AND. YRCONV%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS+1
  IF (.NOT. LLGPSCONV .AND. YSCONV%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS+1
  IF (.NOT. LLGPI .AND. YI%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS+1
  IF (.NOT. LLGPA .AND. YA%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS+1
  IF (.NOT. LLGPR .AND. YR%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS+1
  IF (.NOT. LLGPS .AND. YS%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS+1
  IF (.NOT. LLGPLRCH4 .AND. YLRCH4%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS+1
  DO JCHEM=1,NCHEM
    IF(.NOT.YCHEM(JCHEM)%LASSIM.AND..NOT.LLGPCHEM(JCHEM).AND.YCHEM(JCHEM)%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS+1
  ENDDO

  DO JLIMA=1,NLIMA
    IF(.NOT.YLIMA(JLIMA)%LASSIM.AND..NOT.LLGPLIMA(JLIMA).AND.YLIMA(JLIMA)%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS+1
  ENDDO

  YGFL%NUMFLDS_SL1 = 0
  DO JGFL=1,YGFL%NUMFLDS
    YCOMP(JGFL)%MP_SL1 = NUNDEFLD
    IF(YCOMP(JGFL)%LADV) THEN
      YGFL%NUMFLDS_SL1 = YGFL%NUMFLDS_SL1+1
      YCOMP(JGFL)%MP_SL1 = YGFL%NUMFLDS_SL1
      YCOMP(JGFL)%MP_SLX = (YCOMP(JGFL)%MP_SL1-1)*(NFLEVG+2*NFLSUL)
    ENDIF
  ENDDO
  CALL SUSLB(YDDIMV,YDCVER,YDMODEL)

  L_CLD_DEACT=.FALSE.
  WRITE(NULOUT,*)' CLOUD FIELDS RE-ACTIVATAD, YGFL%NUMGPFLDS=',&
  & YGFL%NUMGPFLDS,' YGFL%NUMFLDS_SL1=', YGFL%NUMFLDS_SL1
ENDIF

END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('GFL_SUBS_MOD:REACT_CLOUD_GFL',1,ZHOOK_HANDLE)

!     ------------------------------------------------------------------
END SUBROUTINE REACT_CLOUD_GFL

