SUBROUTINE DEACT_CLOUD_GFL(YDDIMV,YDCVER,YDMODEL)

!**** *DEACT_CLOUD_GFL* Deactivate prognostic cloud variables

!     ------------------------------------------------------------------

USE TYPE_MODEL , ONLY : MODEL
USE YOMCVER    , ONLY : TCVER
TYPE(TDIMV), INTENT(IN) :: YDDIMV
TYPE(TCVER), INTENT(IN) :: YDCVER
TYPE(MODEL) ,INTENT(INOUT):: YDMODEL


INTEGER(KIND=JPIM) :: JGFL,JCHEM,JLIMA
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

#include "suslb.intfb.h"

IF (LHOOK) CALL DR_HOOK('GFL_SUBS_MOD:DEACT_CLOUD_GFL',0,ZHOOK_HANDLE)
ASSOCIATE(YGFL=>YDMODEL%YRML_GCONF%YGFL,YDSIMPHL=>YDMODEL%YRML_PHY_MF%YRSIMPHL, &
 & YDPHNC=>YDMODEL%YRML_PHY_SLIN%YRPHNC)

ASSOCIATE(NFLEVG=>YDDIMV%NFLEVG, NFLSUL=>YDDIMV%NFLSUL, &
 & YCOMP=>YGFL%YCOMP, YL=>YGFL%YL, YLCONV=>YGFL%YLCONV, YICONV=>YGFL%YICONV, &
 & YRCONV=>YGFL%YRCONV, YSCONV=>YGFL%YSCONV, YI=>YGFL%YI, YA=>YGFL%YA, &
 & YR=>YGFL%YR, YS=>YGFL%YS, YG=>YGFL%YG, YH=>YGFL%YH, YCPF=>YGFL%YCPF, &
 & YLRCH4=>YGFL%YLRCH4, YTKE=>YGFL%YTKE, YCVGQ=>YGFL%YCVGQ, &
 & YCHEM=>YGFL%YCHEM, NCHEM=>YGFL%NCHEM, LENCLD2=>YDPHNC%LENCLD2, &
 & LEPCLD2=>YDPHNC%LEPCLD2, &
 & LPROCLDTL=>YDSIMPHL%LPROCLDTL,YLIMA=>YGFL%YLIMA, NLIMA=>YGFL%NLIMA)
IF ((.NOT.L_CLD_DEACT .OR.L_OOPS) .AND.&
 & (YL%LACTIVE .OR. YI%LACTIVE .OR.YR%LACTIVE .OR. YS%LACTIVE .OR.&
 & YLCONV%LACTIVE .OR. YICONV%LACTIVE .OR. YRCONV%LACTIVE .OR. YSCONV%LACTIVE .OR.&
 &  YG%LACTIVE .OR. YH%LACTIVE .OR. YA%LACTIVE .OR. YCPF%LACTIVE .OR. YTKE%LACTIVE .OR. YCVGQ%LACTIVE) ) THEN
  IF(.NOT.L_OOPS) THEN
  IF (.NOT. (LENCLD2.OR.LEPCLD2)) CALL COPY_GFL_COMP(YGFL,YL_SAVE,YL)
  CALL COPY_GFL_COMP(YGFL,YLCONV_SAVE,YLCONV)
  CALL COPY_GFL_COMP(YGFL,YICONV_SAVE,YICONV)
  CALL COPY_GFL_COMP(YGFL,YRCONV_SAVE,YRCONV)
  CALL COPY_GFL_COMP(YGFL,YSCONV_SAVE,YSCONV)
  IF (.NOT. (LENCLD2.OR.LEPCLD2)) CALL COPY_GFL_COMP(YGFL,YI_SAVE,YI)
  CALL COPY_GFL_COMP(YGFL,YR_SAVE,YR)
  CALL COPY_GFL_COMP(YGFL,YS_SAVE,YS)
  CALL COPY_GFL_COMP(YGFL,YG_SAVE,YG)
  CALL COPY_GFL_COMP(YGFL,YH_SAVE,YH)
  IF (.NOT. (LENCLD2.OR.LEPCLD2)) CALL COPY_GFL_COMP(YGFL,YA_SAVE,YA)
  CALL COPY_GFL_COMP(YGFL,YCPF_SAVE,YCPF)
  CALL COPY_GFL_COMP(YGFL,YLRCH4_SAVE,YLRCH4)
  DO JCHEM=1,NCHEM
    IF(.NOT.YCHEM(JCHEM)%LASSIM) CALL COPY_GFL_COMP(YGFL,YCHEM_SAVE(JCHEM),YCHEM(JCHEM))
  ENDDO

  DO JLIMA=1,NLIMA
    IF(.NOT.YLIMA(JLIMA)%LASSIM) CALL COPY_GFL_COMP(YGFL,YLIMA_SAVE(JLIMA),YLIMA(JLIMA))
  ENDDO
  CALL COPY_GFL_COMP(YGFL,YTKE_SAVE,YTKE)
  CALL COPY_GFL_COMP(YGFL,YCVGQ_SAVE,YTKE)
  ENDIF
  IF( .NOT. (LENCLD2.OR.LEPCLD2)) THEN
    IF (YL%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS-1
    IF (YLCONV%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS-1
    IF (YICONV%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS-1
    IF (YRCONV%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS-1
    IF (YSCONV%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS-1
    IF (YI%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS-1
    IF (YR%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS-1
    IF (YS%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS-1
    IF (YG%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS-1
    IF (YH%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS-1
    IF (YA%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS-1
    IF (YCPF%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS-1
    IF (YLRCH4%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS-1
    DO JCHEM=1,NCHEM
      IF(YCHEM(JCHEM)%LGP.AND..NOT.YCHEM(JCHEM)%LASSIM) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS-1
    ENDDO

    DO JLIMA=1,NLIMA
      IF(YLIMA(JLIMA)%LGP.AND..NOT.YLIMA(JLIMA)%LASSIM) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS-1
    ENDDO
    IF (YTKE%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS-1
    IF (YCVGQ%LGP) YGFL%NUMGPFLDS=YGFL%NUMGPFLDS-1

    IF (YL%LT5) YGFL%NUMFLDS5=YGFL%NUMFLDS5-1
    IF (YLCONV%LT5) YGFL%NUMFLDS5=YGFL%NUMFLDS5-1
    IF (YICONV%LT5) YGFL%NUMFLDS5=YGFL%NUMFLDS5-1
    IF (YRCONV%LT5) YGFL%NUMFLDS5=YGFL%NUMFLDS5-1
    IF (YSCONV%LT5) YGFL%NUMFLDS5=YGFL%NUMFLDS5-1
    IF (YI%LT5) YGFL%NUMFLDS5=YGFL%NUMFLDS5-1
    IF (YR%LT5) YGFL%NUMFLDS5=YGFL%NUMFLDS5-1
    IF (YS%LT5) YGFL%NUMFLDS5=YGFL%NUMFLDS5-1
    IF (YG%LT5) YGFL%NUMFLDS5=YGFL%NUMFLDS5-1
    IF (YH%LT5) YGFL%NUMFLDS5=YGFL%NUMFLDS5-1
    IF (YA%LT5) YGFL%NUMFLDS5=YGFL%NUMFLDS5-1
    IF (YCPF%LT5) YGFL%NUMFLDS5=YGFL%NUMFLDS5-1
    IF (YLRCH4%LT5) YGFL%NUMFLDS5=YGFL%NUMFLDS5-1
    DO JCHEM=1,NCHEM
      IF (YCHEM(JCHEM)%LT5.AND..NOT.YCHEM(JCHEM)%LASSIM) YGFL%NUMFLDS5=YGFL%NUMFLDS5-1
    ENDDO

    DO JLIMA=1,NLIMA
      IF (YLIMA(JLIMA)%LT5.AND..NOT.YLIMA(JLIMA)%LASSIM) YGFL%NUMFLDS5=YGFL%NUMFLDS5-1
    ENDDO
    IF (YTKE%LT5) YGFL%NUMFLDS5=YGFL%NUMFLDS5-1
    IF (YCVGQ%LT5) YGFL%NUMFLDS5=YGFL%NUMFLDS5-1

    IF (.NOT. LPROCLDTL) THEN
      CALL FALSIFY_GFL_COMP(YGFL,YL)
      CALL FALSIFY_GFL_COMP(YGFL,YI)
    ENDIF
    CALL FALSIFY_GFL_COMP(YGFL,YLCONV)
    CALL FALSIFY_GFL_COMP(YGFL,YICONV)
    CALL FALSIFY_GFL_COMP(YGFL,YRCONV)
    CALL FALSIFY_GFL_COMP(YGFL,YSCONV)
    CALL FALSIFY_GFL_COMP(YGFL,YR)
    CALL FALSIFY_GFL_COMP(YGFL,YS)
    CALL FALSIFY_GFL_COMP(YGFL,YG)
    CALL FALSIFY_GFL_COMP(YGFL,YH)
    CALL FALSIFY_GFL_COMP(YGFL,YA)
    CALL FALSIFY_GFL_COMP(YGFL,YCPF)
    CALL FALSIFY_GFL_COMP(YGFL,YLRCH4)
    DO JCHEM=1,NCHEM
      IF (.NOT.YCHEM(JCHEM)%LASSIM) CALL FALSIFY_GFL_COMP(YGFL,YCHEM(JCHEM))
    ENDDO

    DO JLIMA=1,NLIMA
      IF (.NOT.YLIMA(JLIMA)%LASSIM) CALL FALSIFY_GFL_COMP(YGFL,YLIMA(JLIMA))
    ENDDO
    CALL FALSIFY_GFL_COMP(YGFL,YTKE)
    CALL FALSIFY_GFL_COMP(YGFL,YCVGQ)
  ELSE
    !CALL NOADVECT_COMP(YGFL,YLCONV)
    !CALL NOADVECT_COMP(YGFL,YICONV)
    CALL NOADVECT_COMP(YGFL,YRCONV)
    CALL NOADVECT_COMP(YGFL,YSCONV)
    CALL NOADVECT_COMP(YGFL,YR)
    CALL NOADVECT_COMP(YGFL,YS)
    CALL NOADVECT_COMP(YGFL,YG)
    CALL NOADVECT_COMP(YGFL,YH)
    CALL NOADVECT_COMP(YGFL,YCPF)
    CALL NOADVECT_COMP(YGFL,YLRCH4)
    DO JCHEM=1,NCHEM
      IF (.NOT.YCHEM(JCHEM)%LASSIM) CALL NOADVECT_COMP(YGFL,YCHEM(JCHEM))
    ENDDO

    DO JLIMA=1,NLIMA
      IF (.NOT.YLIMA(JLIMA)%LASSIM) CALL NOADVECT_COMP(YGFL,YLIMA(JLIMA))
    ENDDO
    CALL NOADVECT_COMP(YGFL,YTKE)
    CALL NOADVECT_COMP(YGFL,YCVGQ)
  ENDIF
  YGFL%NUMFLDS_SL1 = 0
  DO JGFL=1,YGFL%NUMFLDS
    YCOMP(JGFL)%MP_SL1 = NUNDEFLD
    IF(YCOMP(JGFL)%LADV) THEN
      YGFL%NUMFLDS_SL1 = YGFL%NUMFLDS_SL1+1
      YCOMP(JGFL)%MP_SL1 = YGFL%NUMFLDS_SL1
      YCOMP(JGFL)%MP_SLX = (YCOMP(JGFL)%MP_SL1-1)*(NFLEVG+2*NFLSUL)
    ENDIF
  ENDDO
  CALL SUSLB(YDDIMV,YDCVER,YDMODEL)

  L_CLD_DEACT=.TRUE.
  WRITE(NULOUT,*)' CLOUD FIELDS DE-ACTIVATED, YGFL%NUMGPFLDS=',&
  & YGFL%NUMGPFLDS,' YGFL%NUMFLDS_SL1=', YGFL%NUMFLDS_SL1
ENDIF

END ASSOCIATE
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('GFL_SUBS_MOD:DEACT_CLOUD_GFL',1,ZHOOK_HANDLE)

END SUBROUTINE DEACT_CLOUD_GFL

