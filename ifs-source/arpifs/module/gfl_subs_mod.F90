SUBROUTINE DEFINE_GFL_COMP(YGFL,YDCOMP,CDNAME,KGRIB,LDGP,KREQIN,PREFVALI,KFID,&
 & LDREQOUT,LDERS,LD5,LDT1,LDGPINGP,LDMGRID,LDTRAJIO,LDTHERMACT,PR,PRCP,&
 & KCOUPLO4,LDASSIM,LDDIFF,LDCONV,PMOLMASS,PEFOLD,PHENRYA,PHENRYB,&
 & KGRIBTC,KGRBFLXO,KGRBWDFLX,KGRBDDFLX,KGRIBDV,PBETAMFBC,LDMASSFIX,LDNEGFIX,LDCOMAD,LDIAG,&
 & LDWATER, LDPRECIP, PLZER, PWENO_ALPHA)  
!**** *DEFINE_GFL_COMP*  - Setup indivual GFL field
 
!     Purpose.
!     --------
!     Basic allocation of GFL descriptor structure (on first call)
!     Setup basic attributes of individual GFL component

!        Explicit arguments :
!        --------------------

!        YDCOMP  - field handle
!        CDNAME  - field ARPEGE name
!        KGRIB   - GRIB code
!        LDGP    - if TRUE gridpoint field
!        KREQIN  - 1 if required in input, 0 if not, -1 if initialised with refernence value
!        PREFVALI - reference value for initialisation in case NREQIN==-1
!        LDREQOUT- TRUE if requiered in output
!        LDERS   - TRUE if derivatives required (only possible for spectral field)
!        LD5     - TRUE if field needs to be present in trajectory (T5)
!        LD1     - TRUE if field needs to be present in t+dt array (GFLT1) 
!        LDTRAJIO- TRUE if field written to/from trajectory structure files
!        LDIAG   - TRUE if field is diagnostic at t (e.g. cloud fraction)
!         KCOUPLO4  - 1 / 2 if coupled to CTM by OASIS4 input/in&out , 0 false (obsolete)
!         LDASSIM    - TRUE if  used as Control Variable (CHEM, either monitored or assimilated)
!         KGRIBTC   - GRIB code of Total Column (CHEM)
!         KGRBFLXO  - GRIB code of Surface Flux (CHEM, output) 
!         KGRBWDFLX  - GRIB code of Wet Deposition Flux (CHEM, output) 
!         KGRBDDFLX  - GRIB code of Dry Deposition Flux (CHEM, output) 
!         KGRIBDV   - GRIB code of Deposition velocity (CHEM)
!         LDDIFF     - TRUE if diffusion  on (CHEM)
!         LDCONV     - TRUE if convection on (CHEM)
!         PMOLMASS  - Molar Mass (CHEM)
!         PEFOLD     -  Efolding decay time (CHEM)
!         PHENRYA   - Henry Constant a (CHEM)
!         PHENRYB   - Henry Constant b (CHEM)
!         LDCOMAD   - COMAD interpolation for GFL component
!         LDMASSFIX - Correct total mass after advection
!         PWENO_ALPHA - Alpha parameter (called p in literature) controlling WENO interpolation
!         PBETAMFBC - parameter for BC mass fixer
!         LDNEGFIX  - Correct negative values 
!         LDWATER   - Water species
!         LDPRECIP  - Precipitating water species
!         PLZER     - Latent heat change at 0K
!
!     Author.
!     -------
!      Mats Hamrud  *ECMWF*

!     Modifications.
!     --------------
!      Original : 2003-03-01
!      Modifications:
!      03/07/09 C. Fischer - add Arome/Aladin attributes
!      16/06/10 J Flemming, GFL attribute list extention 
!      R. El Khatib 24-May-2018 Bugfix YLASTCOMP for OOPS
!-------------------------------------------------------------------------

TYPE(TYPE_GFLD)           ,INTENT(INOUT) :: YGFL
TYPE(TYPE_GFL_COMP),TARGET,INTENT(INOUT) :: YDCOMP
CHARACTER(LEN=16),INTENT(IN) :: CDNAME
INTEGER(KIND=JPIM),INTENT(IN) :: KGRIB
INTEGER(KIND=JPIM),INTENT(IN) :: KREQIN
LOGICAL,INTENT(IN)::   LDREQOUT
LOGICAL,INTENT(IN) ::   LDGP
LOGICAL,INTENT(IN) ::   LDERS 
LOGICAL,INTENT(IN) ::   LD5 
LOGICAL,INTENT(IN) ::   LDT1
REAL(KIND=JPRB)           ,INTENT(IN),OPTIONAL :: PREFVALI
LOGICAL,INTENT(IN),OPTIONAL ::   LDGPINGP
LOGICAL,INTENT(IN),OPTIONAL ::   LDMGRID
LOGICAL,INTENT(IN),OPTIONAL ::   LDTRAJIO
LOGICAL,INTENT(IN),OPTIONAL ::   LDIAG
LOGICAL,INTENT(IN),OPTIONAL ::   LDTHERMACT
REAL(KIND=JPRB),INTENT(IN),OPTIONAL :: PR
REAL(KIND=JPRB),INTENT(IN),OPTIONAL :: PRCP
 
INTEGER(KIND=JPIM) ,INTENT(IN),OPTIONAL :: KFID        
INTEGER(KIND=JPIM) ,INTENT(IN),OPTIONAL :: KCOUPLO4        
LOGICAL ,INTENT(IN),OPTIONAL           :: LDASSIM          
INTEGER(KIND=JPIM),INTENT(IN),OPTIONAL :: KGRIBTC         
INTEGER(KIND=JPIM),INTENT(IN),OPTIONAL :: KGRBFLXO         
INTEGER(KIND=JPIM),INTENT(IN),OPTIONAL :: KGRBWDFLX         
INTEGER(KIND=JPIM),INTENT(IN),OPTIONAL :: KGRBDDFLX         
INTEGER(KIND=JPIM),INTENT(IN),OPTIONAL :: KGRIBDV         
LOGICAL  ,INTENT(IN),OPTIONAL          :: LDDIFF           
LOGICAL  ,INTENT(IN),OPTIONAL          :: LDCONV           
REAL(KIND=JPRB),INTENT(IN),OPTIONAL    :: PMOLMASS        
REAL(KIND=JPRB),INTENT(IN),OPTIONAL    :: PEFOLD        
REAL(KIND=JPRB),INTENT(IN),OPTIONAL    :: PHENRYA
REAL(KIND=JPRB),INTENT(IN),OPTIONAL    :: PHENRYB
LOGICAL, INTENT(IN),OPTIONAL           :: LDCOMAD
REAL(KIND=JPRB),INTENT(IN),OPTIONAL    :: PBETAMFBC
LOGICAL, INTENT(IN),OPTIONAL           :: LDNEGFIX
LOGICAL, INTENT(IN),OPTIONAL           :: LDMASSFIX
REAL(KIND=JPRB), INTENT(IN),OPTIONAL   :: PWENO_ALPHA
LOGICAL, INTENT(IN), OPTIONAL          :: LDWATER
LOGICAL, INTENT(IN), OPTIONAL          :: LDPRECIP
REAL(KIND=JPRB),INTENT(IN),OPTIONAL    :: PLZER

TYPE(TYPE_GFL_COMP),POINTER :: YPTRC     ! Temporary field pointer

LOGICAL :: LLP
INTEGER(KIND=JPIM) :: JGFL, ICURFLDPT, ICURFLDPC
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

!-------------------------------------------------------------------------

IF (LHOOK) CALL DR_HOOK('GFL_SUBS_MOD:DEFINE_GFL_COMP',0,ZHOOK_HANDLE)

LLP = NPRINTLEV >= 1 
!-------------------------------------------------------------------------

!      1. Define GFL component
!         --------------------

!      1.1 Some checks
IF(LDGP) THEN
  DO JGFL=1,YGFL%NUMFLDS
    IF(.NOT. YGFL%YCOMP(JGFL)%LGP) THEN
      !        Grid-point fields should be defined before any spectral field 
      CALL ABOR1('YOMMFL:DEFINE_GFL_COMP:GRIDPOINT BEFORE SPECTRAL')
    ENDIF
  ENDDO
ENDIF
IF(LDGP) THEN
!!$  IF(LDERS) THEN
!!$    !      Derivatives can only be defined for spectral fields
!!$    CALL ABOR1('YOMMFL:DEFINE_GFL_COMP:DERIVATIVES ONLY WITH SPECTRAL')
!!$  ENDIF
ENDIF
IF(YGFL%NUMFLDS == JPGFL) THEN
  WRITE(NULOUT,*) ' MAXIMUM NUMBER OF FIELDS ALREADY DEFINED'
  CALL ABOR1('YOMMFL: EXCEED NUMBER OF FIELDS')
ENDIF

!      1.2 Define field attributes

ICURFLDPT = YGFL%NUMFLDS+1
ICURFLDPC = YGFL%NUMFLDS+1

YDCOMP%LACTIVE = .TRUE.
YDCOMP%CNAME = CDNAME
YDCOMP%IGRBCODE = KGRIB
YDCOMP%NREQIN = KREQIN
IF (PRESENT(PREFVALI)) THEN
  YDCOMP%REFVALI = PREFVALI
ENDIF 
IF(PRESENT(KFID)) YDCOMP%IFID = KFID
YDCOMP%LREQOUT = LDREQOUT
YDCOMP%LGP = LDGP
YDCOMP%LSP= .NOT. YDCOMP%LGP
YDCOMP%LT5 = LD5
YDCOMP%LT1 = LDT1
YDCOMP%LCDERS  = LDERS
IF(PRESENT(LDGPINGP)) YDCOMP%LGPINGP=LDGPINGP
YDCOMP%LMGRID = .FALSE.
IF(PRESENT(LDMGRID)) YDCOMP%LMGRID=LDMGRID
IF(PRESENT(LDTRAJIO)) YDCOMP%LTRAJIO=LDTRAJIO
IF(PRESENT(LDIAG)) YDCOMP%LDIAG=LDIAG
IF(PRESENT(LDTHERMACT)) YDCOMP%LTHERMACT=LDTHERMACT
IF(YDCOMP%LTHERMACT) THEN
  IF(.NOT.PRESENT(PR))&
   &CALL ABOR1('GFL_SUBS_MOD:DEFINE_GFL_COMPONENT - PR MISSING')
  IF(.NOT.PRESENT(PRCP))&
   &CALL ABOR1('GFL_SUBS_MOD:DEFINE_GFL_COMPONENT - PRCP MISSING')
  YDCOMP%R = PR
  YDCOMP%RCP = PRCP
ENDIF


! GFL nanmel list extension  
YDCOMP%NCOUPLO4 = -1 
YDCOMP%LASSIM = .FALSE.
YDCOMP%LDIFF = .TRUE. 
YDCOMP%LCONV = .TRUE. 
YDCOMP%IGRIBTC = -9999 
YDCOMP%IGRBFLXO = -9999
YDCOMP%IGRBWDFLX = -9999
YDCOMP%IGRBDDFLX = -9999
YDCOMP%IGRIBDV = -9999
YDCOMP%RMOLMASS = -99999.9_JPRB
YDCOMP%REFOLD = -99999.9_JPRB
YDCOMP%HENRYA = -99999.9_JPRB
YDCOMP%HENRYB = -99999.9_JPRB
! end gems nl extension

YDCOMP%LCOMAD = .FALSE.
YDCOMP%LMASSFIX = .FALSE.
YDCOMP%WENO_ALPHA = 0.0_JPRB
YDCOMP%LNEGFIX = .FALSE.
YDCOMP%BETAMFBC = 1.0_JPRB

IF (PRESENT(KCOUPLO4)) YDCOMP%NCOUPLO4 = KCOUPLO4
IF (PRESENT(LDASSIM)) YDCOMP%LASSIM = LDASSIM
IF (PRESENT(LDDIFF)) YDCOMP%LDIFF = LDDIFF
IF (PRESENT(LDCONV)) YDCOMP%LCONV = LDCONV
IF (PRESENT(KGRIBTC)) YDCOMP%IGRIBTC = KGRIBTC
IF (PRESENT(KGRBFLXO)) YDCOMP%IGRBFLXO = KGRBFLXO
IF (PRESENT(KGRBWDFLX)) YDCOMP%IGRBWDFLX = KGRBWDFLX
IF (PRESENT(KGRBDDFLX)) YDCOMP%IGRBDDFLX = KGRBDDFLX
IF (PRESENT(KGRIBDV)) YDCOMP%IGRIBDV = KGRIBDV
IF (PRESENT(PMOLMASS)) YDCOMP%RMOLMASS = PMOLMASS
IF (PRESENT(PEFOLD)) YDCOMP%REFOLD = PEFOLD
IF (PRESENT(PHENRYA))  YDCOMP%HENRYA = PHENRYA
IF (PRESENT(PHENRYB))  YDCOMP%HENRYB = PHENRYB

IF (PRESENT(PBETAMFBC)) YDCOMP%BETAMFBC = PBETAMFBC
IF (PRESENT(LDMASSFIX)) YDCOMP%LMASSFIX = LDMASSFIX
IF (PRESENT(PWENO_ALPHA)) YDCOMP%WENO_ALPHA = PWENO_ALPHA
IF (PRESENT(LDCOMAD)) YDCOMP%LCOMAD = LDCOMAD
IF (PRESENT(LDNEGFIX)) YDCOMP%LNEGFIX = LDNEGFIX

IF (PRESENT(LDWATER)) YDCOMP%LWATER = LDWATER
IF (YDCOMP%LWATER) THEN
  IF (.NOT.PRESENT(LDPRECIP))&
   & CALL ABOR1('GFL_SUBS_MOD:DEFINE_GFL_COMPONENT - LDPRECIP MISSING')
  IF (.NOT.PRESENT(PLZER))&
   & CALL ABOR1('GFL_SUBS_MOD:DEFINE_GFL_COMPONENT - PLZER MISSING')
  IF (.NOT.PRESENT(PRCP))&
   & CALL ABOR1('GFL_SUBS_MOD:DEFINE_GFL_COMPONENT - PRCP MISSING')
  YDCOMP%LPRECIP = LDPRECIP
  YDCOMP%RLZER = PLZER
  YDCOMP%RCP = PRCP
ENDIF

!    1.3  Numbers of fields and dimensions
YGFL%NUMFLDS = YGFL%NUMFLDS+1
IF (YDCOMP%LT5) YGFL%NUMFLDS5 = YGFL%NUMFLDS5+1

IF(YDCOMP%LCDERS) THEN
  YGFL%NDIM = YGFL%NDIM+3
  YGFL%NDIM0 = YGFL%NDIM0+3
  YGFL%NDERS = YGFL%NDERS+1
  IF (YDCOMP%LT5) YGFL%NDIM5 = YGFL%NDIM5+3
ELSE
  YGFL%NDIM = YGFL%NDIM+1
  YGFL%NDIM0 = YGFL%NDIM0+1
  IF (YDCOMP%LT5) YGFL%NDIM5 = YGFL%NDIM5+1
ENDIF

IF(YDCOMP%LSP) THEN
  YGFL%NUMSPFLDS =YGFL%NUMSPFLDS+1
ELSE
  YGFL%NUMGPFLDS =YGFL%NUMGPFLDS+1
ENDIF

IF (YDCOMP%LT1)  THEN
  YGFL%NUMFLDS1 = YGFL%NUMFLDS1+1
  YGFL%NDIM1 = YGFL%NDIM1+1
  IF (YDCOMP%LSP) YGFL%NUMSPFLDS1 =YGFL%NUMSPFLDS1+1
ENDIF  

!    1.4  Define field "pointers"
YDCOMP%MP5 = NUNDEFLD
IF (YDCOMP%LGP) THEN
!  YDCOMP%MP = YGFL%NDIM0
  YDCOMP%MP = YGFL%NUMFLDS
  IF (YDCOMP%LT5) YDCOMP%MP5 = YGFL%NDIM5
ELSE
  YDCOMP%MP = YGFL%NUMFLDS
  IF (YDCOMP%LT5) YDCOMP%MP5 = YGFL%NUMFLDS5
ENDIF
IF (YDCOMP%LCDERS) THEN
  YDCOMP%MPM = YDCOMP%MP+YGFL%NDERS
  YDCOMP%MPL = YDCOMP%MP+2*YGFL%NDERS
  IF(YDCOMP%LT5) THEN
    YDCOMP%MP5M = YDCOMP%MP5+YGFL%NDERS
    YDCOMP%MP5L = YDCOMP%MP5+2*YGFL%NDERS
  ENDIF
ELSE
  YDCOMP%MPL = NUNDEFLD
  YDCOMP%MPM = NUNDEFLD
  YDCOMP%MP5L = NUNDEFLD
  YDCOMP%MP5M = NUNDEFLD
ENDIF

IF(YDCOMP%LSP) THEN
  YDCOMP%MPSP = YGFL%NUMSPFLDS
ELSE
  YDCOMP%MPSP = NUNDEFLD
ENDIF

IF (YDCOMP%LT1)  THEN
  YDCOMP%MP1 = YGFL%NUMFLDS1
ELSE
  YDCOMP%MP1 = NUNDEFLD
ENDIF  
  
!      1.6  Possibly reassign pointers (needed for multiple fields with derivatives)

IF(ASSOCIATED(YGFL%YLASTCOMP)) THEN
  YPTRC=>YGFL%YLASTCOMP
  DO
!    IF(.NOT.LDGP) THEN
      IF(LLP) WRITE(NULOUT,*)' DEFINE_GFL_COMP:CHECKING ',YPTRC%CNAME
      IF(YPTRC%LCDERS) THEN
        YPTRC%MPM = YPTRC%MPM+1
        IF(LDERS)THEN
          YPTRC%MPL = YPTRC%MPL+2
        ELSE
          YPTRC%MPL = YPTRC%MPL+1
        ENDIF
        IF(LLP) WRITE(NULOUT,*)' REASSIGNED MPL=',YPTRC%MPL,' MPM=',YPTRC%MPM
      ENDIF
      IF (YDCOMP%LT5) THEN
        IF(YPTRC%LT5) THEN
          IF(YPTRC%LCDERS) THEN
            YPTRC%MP5M = YPTRC%MP5M+1
            IF(LDERS)THEN
              YPTRC%MP5L = YPTRC%MP5L+2
            ELSE
              YPTRC%MP5L = YPTRC%MP5L+1
            ENDIF
          ENDIF
          IF(LLP) WRITE(NULOUT,*)' REASSIGNED MP5L=',YPTRC%MP5L,' MP5M=',YPTRC%MP5M
        ENDIF
      ENDIF

!    ENDIF
    IF(.NOT.ASSOCIATED(YPTRC%PREVIOUS)) EXIT
    YPTRC=>YPTRC%PREVIOUS
  ENDDO
ENDIF

!    1.7  Point to last defined field
YDCOMP%PREVIOUS=>YGFL%YLASTCOMP
YGFL%YLASTCOMP => YDCOMP

IF (LHOOK) CALL DR_HOOK('GFL_SUBS_MOD:DEFINE_GFL_COMP',1,ZHOOK_HANDLE)

!     ------------------------------------------------------------------
END SUBROUTINE DEFINE_GFL_COMP

