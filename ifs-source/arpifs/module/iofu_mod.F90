SUBROUTINE IOFU_COUNT(YDGEOMETRY,YDCFU,KACTION,KFNUM)

USE YOMCFU       , ONLY : TCFU
USE GEOMETRY_MOD , ONLY : GEOMETRY

IMPLICIT NONE

TYPE (GEOMETRY),     INTENT (IN)    :: YDGEOMETRY
TYPE(TCFU)          ,INTENT(INOUT)  :: YDCFU
INTEGER (KIND=JPIM), INTENT (IN)    :: KACTION
INTEGER (KIND=JPIM), INTENT (INOUT) :: KFNUM

INTEGER (KIND=JPIM) :: IFCUR

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('IOFU_MOD:IOFU_COUNT',0,ZHOOK_HANDLE)

IFCUR = 0
CALL IOFU_SELECT_WH(YDGEOMETRY,YDCFU,KACTION,KFCUR=IFCUR)
KFNUM = KFNUM + IFCUR

IF (LHOOK) CALL DR_HOOK('IOFU_MOD:IOFU_COUNT',1,ZHOOK_HANDLE)

END SUBROUTINE IOFU_COUNT

