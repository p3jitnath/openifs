END SUBROUTINE IOFU_SELECTDP

SUBROUTINE IOFU_SELECT_WH(YDGEOMETRY,YDCFU,KACTION,YDFLDSC,YDFLDGT,KFCUR)

!**** * IOFU_SELECT  * - ROUTINE TO SELECT CUMULATED FLUXES
!        14/01/2011 : Creation from wrfu.F90 ; P.Marguinaud
!        -----------------------------------------------------------

USE GEOMETRY_MOD , ONLY : GEOMETRY
USE YOMCFU   , ONLY : TCFU
USE YOMCT0   , ONLY : LRPLANE
USE YOMOPH0  , ONLY : CNMCA
USE YOMFA    , ONLY : NVGRIB, NBITPG
USE FA_MOD   , ONLY : JPPRCM

IMPLICIT NONE

TYPE (GEOMETRY),     INTENT (IN)                      :: YDGEOMETRY
TYPE(TCFU), TARGET  ,INTENT(INOUT)                    :: YDCFU
INTEGER (KIND=JPIM), INTENT (IN)                      :: KACTION
TYPE (IOFLDPTR),     INTENT (INOUT), TARGET, OPTIONAL :: YDFLDGT (:)
TYPE (IOFLDDESC),    INTENT (INOUT), TARGET, OPTIONAL :: YDFLDSC (:)
INTEGER (KIND=JPIM), INTENT (INOUT),         OPTIONAL :: KFCUR

TYPE (IOFLDDESC), POINTER :: YLFLDSC (:)

INTEGER (KIND=JPIM) :: JF, JL, IFLD
INTEGER (KIND=JPIM) :: IFCUR
INTEGER (KIND=JPIM) :: IREP, IPFAOVSZ

REAL(KIND=JPHOOK) :: ZHOOK_HANDLE

IF (LHOOK) CALL DR_HOOK('IOFU_MOD:IOFU_SELECT_WH',0,ZHOOK_HANDLE)

IFCUR = 0
IF (PRESENT (KFCUR)) IFCUR = KFCUR

IF (PRESENT (YDFLDSC)) THEN
  YLFLDSC => YDFLDSC
ELSEIF (PRESENT (YDFLDGT)) THEN
  YLFLDSC => YDFLDGT%YFLDDSC
ELSE
  YLFLDSC => NULL ()
ENDIF

CALL FASGRA (IREP, CNMCA, IPFAOVSZ)

IFLD = 0
DO JF=1,YDCFU%NTYPCFU
  DO JL=1,YDCFU%TYPE_CFU(JF)%ILEV
    IFCUR = IFCUR + 1
    IFLD = IFLD + 1
    IF (ASSOCIATED (YLFLDSC)) THEN
      YLFLDSC(IFCUR)%NFLSZ  = YDGEOMETRY%YRGEM%NGPTOTG
      YLFLDSC(IFCUR)%NSIZPK = YDGEOMETRY%YRGEM%NGPTOTG + JPPRCM * IPFAOVSZ
      YLFLDSC(IFCUR)%CPREF  = YDCFU%TYPE_CFU(JF)%CLPREF(JL)
      YLFLDSC(IFCUR)%ILEVG  = YDCFU%TYPE_CFU(JF)%INUMLEV(JL)
      YLFLDSC(IFCUR)%CSUFF  = YDCFU%TYPE_CFU(JF)%CLSUFF
      YLFLDSC(IFCUR)%NGRIBL = NVGRIB
      YLFLDSC(IFCUR)%JBITS  = NBITPG
    ENDIF
    IF (PRESENT (YDFLDGT)) THEN
      YDFLDGT(IFCUR)%ZFLDGP => YDCFU%GFUBUF (:,IFLD,:) 
! De-reduce vector fields
      YDFLDGT(IFCUR)%LDRDGP = LRPLANE .AND. (YDCFU%TYPE_CFU(JF)%IPARITY == 1)
    ENDIF
  ENDDO
ENDDO

IF (PRESENT (KFCUR)) KFCUR = IFCUR

IF (LHOOK) CALL DR_HOOK('IOFU_MOD:IOFU_SELECT_WH',1,ZHOOK_HANDLE)

END SUBROUTINE IOFU_SELECT_WH

END MODULE IOFU_MOD

