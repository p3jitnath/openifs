SUBROUTINE UPDNEMOOCEAN(YDMCC,YDEPHY,YDRIP,KPROMA,KGPTOT,KSTEP,PTSTEP,YDSURF,YDGEOMETRY)

!**** *UPDNEMOOCEAN*

!     PURPOSE.
!     --------

!     UPDATES THE SURFACE FIELDS FROM NEMO IN SINGLE EXECUTABLE MODE.

!**   INTERFACE.
!     ----------

!     CALL UPDNEMOOCEAN

!        EXPLICIT ARGUMENTS :
!        --------------------

!     METHOD.
!     -------

!     RETRIEVE FIELDS FROM NEMO VIA THE COUPLING INTERFACE
!     AND UPDATE IFS SURFACE DATA
   
!     EXTERNALS.
!     ----------

!     GETNEMO1WAY
!     GETNEMO

!     AUTHORS.
!     --------
!        K. MOGENSEN (ECMWF) DEC 2012.  (BASED ON ROUTINE UPDCLIE)
!        K.MOGENSEN/S.KEELEY 04-04-2014: OPTIONALLY USE FLAKE FOR SST/CI

!     MODIFICATIONS.
!     --------------
!      K. Yessad (July 2014): Move some variables.
!     F. Vana  05-Mar-2015  Support for single precision
!     ------------------------------------------------------------------

USE SURFACE_FIELDS_MIX , ONLY : TSURF
USE PARKIND1, ONLY : JPRD, JPIM, JPRB
USE PARKIND_OCEAN, ONLY : JPRO
USE YOMHOOK , ONLY : LHOOK, DR_HOOK, JPHOOK
USE YOMLUN  , ONLY : NULOUT
USE YOEPHY  , ONLY : TEPHY
USE YOMRIP0 , ONLY : NINDAT
USE YOMRIP  , ONLY : TRIP
USE YOMMCC  , ONLY : TMCC
USE YOMCST  , ONLY : RPI
USE GEOMETRY_MOD, ONLY : GEOMETRY

!     ------------------------------------------------------------------

IMPLICIT NONE

TYPE(TEPHY)        ,INTENT(INOUT):: YDEPHY
TYPE(TMCC)         ,INTENT(INOUT):: YDMCC
TYPE(TRIP)         ,INTENT(INOUT):: YDRIP
INTEGER(KIND=JPIM), INTENT(IN) :: KPROMA
INTEGER(KIND=JPIM), INTENT(IN) :: KGPTOT
INTEGER(KIND=JPIM), INTENT(IN) :: KSTEP
REAL(KIND=JPRB), INTENT(IN) :: PTSTEP
TYPE(TSURF), INTENT(INOUT) :: YDSURF
TYPE(GEOMETRY), INTENT(IN) :: YDGEOMETRY

!     ------------------------------------------------------------------
REAL(KIND=JPHOOK) :: ZHOOK_HANDLE
REAL(KIND=JPRB) :: ZRTFREEZSICE, ZRTMELTSICE, ZRCIMIN, ZTICE, ZR2D, ZLAT
REAL(KIND=JPRB) :: ZPOID1, ZPOID2, ZDSST0, ZTS, ZCI
REAL(KIND=JPRB) :: ZDAYS, ZCOEF
INTEGER(KIND=JPIM) :: JSTGLO, IEND, IST, IBL, JROF
INTEGER(KIND=JPIM) :: IJ0, IM0, IA0, IJOUR, IMOIS, IAN, ILMOIS(12),&
                    & IMT1, IJT1, IJT2
INTEGER(KIND=JPIM) :: IPAR139, IPAR31, IPAR131, IPAR132
LOGICAL :: LLICEO(0:KGPTOT), LLICEN(0:KGPTOT), LLAKE(0:KGPTOT)
REAL(KIND=JPRB), PARAMETER :: ZMDI=HUGE(ZMDI)

!     ------------------------------------------------------------------

#include "surf_inq.h"

#include "getnemo.intfb.h"
#include "getnemo1way.intfb.h"
#include "updcal.intfb.h"
#include "fcttim.func.h"
#include "readmask.intfb.h"

!     ------------------------------------------------------------------
IF (LHOOK) CALL DR_HOOK('UPDNEMOOCEAN',0,ZHOOK_HANDLE)
ASSOCIATE(LECURR=>YDEPHY%LECURR, LEFLAKE=>YDEPHY%LEFLAKE, LEOBC=>YDEPHY%LEOBC, &
 & LEOBCICE=>YDEPHY%LEOBCICE, LEOCCO=>YDEPHY%LEOCCO, LEOCLAKE=>YDEPHY%LEOCLAKE, &
 & LEOCWA=>YDEPHY%LEOCWA, LEOLAKESST=>YDEPHY%LEOLAKESST, YSURF=>YDEPHY%YSURF, &
 & CLIMR=>YDMCC%CLIMR, LMCCIEC=>YDMCC%LMCCIEC, LNEMO1WAY=>YDMCC%LNEMO1WAY, &
 & LNEMOLIMGET=>YDMCC%LNEMOLIMGET, LNEMOLIMTEMP=>YDMCC%LNEMOLIMTEMP, &
 & LNEMOLIMTLVL=>YDMCC%LNEMOLIMTLVL, LNEMOSSTFREEZ=>YDMCC%LNEMOSSTFREEZ, &
 & NCLIMR=>YDMCC%NCLIMR, NP1=>YDMCC%NP1, NP2=>YDMCC%NP2, OCEANBC=>YDMCC%OCEANBC, &
 & ZLAKE=>YDMCC%ZLAKE, NSTADD=>YDRIP%NSTADD, &
 & SD_VF=>YDSURF%SD_VF, SP_RR=>YDSURF%SP_RR, SP_SB=>YDSURF%SP_SB, &
 & SP_SL=>YDSURF%SP_SL, YSD_VF=>YDSURF%YSD_VF, YSP_RR=>YDSURF%YSP_RR, &
 & YSP_SB=>YDSURF%YSP_SB, YSP_SL=>YDSURF%YSP_SL, CPLNG_FLD=>YDMCC%CPLNG_FLD, &
 & SD_OC=>YDSURF%SD_OC, YSD_OC=>YDSURF%YSD_OC, LNEMOGRIBFLDS=>YDMCC%LNEMOGRIBFLDS, &
 & LNEMOPARTCOUP=>YDMCC%LNEMOPARTCOUP, &
 & RNPARTRAMPSTART=>YDMCC%RNPARTRAMPSTART, RNPARTRAMPLEN=>YDMCC%RNPARTRAMPLEN, &
 & LNEMOPARTMASK=>YDMCC%LNEMOPARTMASK, &
 & LNEMOPARTMASKFILE=>YDMCC%LNEMOPARTMASKFILE, &
 & CDNPARTMASKFILE => YDMCC%CDNPARTMASKFILE, &
 & RNPARTLAT=>YDMCC%RNPARTLAT, RNPARTLATSPREAD=>YDMCC%RNPARTLATSPREAD, &
 & LNEMOGRIBMASK=>YDMCC%LNEMOGRIBMASK)
!     ------------------------------------------------------------------

!*
!     SETTING CONSTANT VALUES.
!     ------------------------

CALL SURF_INQ(YSURF,PRTFREEZSICE=ZRTFREEZSICE,PRTMELTSICE=ZRTMELTSICE,&
 & PRCIMIN=ZRCIMIN)  

IPAR139 = NCLIMR+1
IPAR31  = NCLIMR+2
IPAR131 = NCLIMR+3
IPAR132 = NCLIMR+4


!*
!     UPDATE OCEAN FIELDS.
!     --------------------



IF(LNEMO1WAY) THEN

  CALL GETNEMO1WAY

ELSE


  WRITE(NULOUT,*)
  WRITE(NULOUT,*)'UPDNEMOOCEAN: updating SST and ICE from NEMO'

  CALL GETNEMO(YDGEOMETRY,YDMCC,YDEPHY,KGPTOT,KSTEP)

  ! PARTIAL COUPLING

  IF (LNEMOPARTCOUP) THEN

    ! COMPUTE DIFFERENCE AT STEP = 0

    IF (KSTEP==0) THEN

      IF(.NOT.ALLOCATED(YDMCC%PARTSST0)) THEN
        ALLOCATE(YDMCC%PARTSST0(KGPTOT))
      ENDIF
      DO JSTGLO=1,KGPTOT,KPROMA
        IEND=MIN(KPROMA,KGPTOT-JSTGLO+1)
        IST=1
        IBL=(JSTGLO-1)/KPROMA+1
        DO JROF=1,IEND
          ! ALL NON LAKE POINT IN FLAKE.
          IF ((SD_VF(JROF,YSD_VF%YLSM%MP,IBL)<=0.5_JPRB).AND.&
            & (SD_VF(JROF,YSD_VF%YCLK%MP,IBL)<=0.5_JPRB)) THEN
             YDMCC%PARTSST0(JSTGLO+JROF-1)=SD_VF(JROF,YSD_VF%YSST%MP,IBL)-&
              & CLIMR(JSTGLO+JROF-1,NP2,IPAR139)
          ELSE
            YDMCC%PARTSST0(JSTGLO+JROF-1)=0.0_JPRB
          ENDIF
        ENDDO
      ENDDO

      ! SETUP MASK
      IF(.NOT.ALLOCATED(YDMCC%PARTMASK)) THEN
        ALLOCATE(YDMCC%PARTMASK(KGPTOT))
      ENDIF
      IF(LNEMOPARTMASK.AND.LNEMOPARTMASKFILE) THEN
        ! READ MASK FILE
        CALL READMASK(YDGEOMETRY,CDNPARTMASKFILE,KGPTOT,YDMCC%PARTMASK(:))
      ELSEIF(LNEMOPARTMASK) THEN
        ! LATITUDE DEPENDENT MASKS
        ZR2D=180.0_JPRB/RPI
        DO JROF=1,KGPTOT
          ZLAT = YDGEOMETRY%YRGSGEOM_NB%GELAT(JROF)*ZR2D 
          IF (ABS(ZLAT)<=RNPARTLAT) THEN
            YDMCC%PARTMASK(JROF)=0.0_JPRB
          ELSEIF (ABS(ZLAT)<=RNPARTLAT+RNPARTLATSPREAD) THEN
            YDMCC%PARTMASK(JROF)=(ABS(ZLAT)-RNPARTLAT)/RNPARTLATSPREAD
          ELSE
            YDMCC%PARTMASK(JROF)=1.0_JPRB
          ENDIF
        ENDDO
      ELSE
        YDMCC%PARTMASK(:)=1.0_JPRB
      ENDIF
    ENDIF

    ! COMPUTE TIME CORRECTION            
    
    ZDAYS=KSTEP*PTSTEP/86400.0
    IF (ZDAYS<=RNPARTRAMPSTART) THEN
      ZCOEF=1.0_JPRB
    ELSEIF(ZDAYS<=RNPARTRAMPSTART+RNPARTRAMPLEN) THEN
      ZCOEF=1.0_JPRB-(ZDAYS-RNPARTRAMPSTART)/RNPARTRAMPLEN
    ELSE
      ZCOEF=0.0_JPRB
    ENDIF
    WRITE(NULOUT,*)'Partial coupling coefficient =',ZCOEF,ZDAYS,KSTEP,PTSTEP
    WRITE(NULOUT,*)

    ! APPLY CORRECTION

    DO JROF=1,KGPTOT
      CLIMR(JROF,NP2,IPAR139)=CLIMR(JROF,NP2,IPAR139)+&
         & ZCOEF*YDMCC%PARTSST0(JROF)*YDMCC%PARTMASK(JROF)
    ENDDO

  ENDIF

  ! SST TENDENCIES
     

  IF (LEOBC) THEN

    DO JROF=1,KGPTOT
      CLIMR(JROF,NP2,IPAR139)=CLIMR(JROF,NP2,IPAR139)+OCEANBC(JROF,1)
    ENDDO

  ENDIF

  ! ICE TENDENCIES

  IF (LEOBCICE) THEN

    DO JROF=1,KGPTOT
      CLIMR(JROF,NP2,IPAR31)=OCEANBC(JROF,2)
    ENDDO

  ENDIF

  ! OPTIONALLY REPLACE SST/ICE WITH LAKE GRIB INPUT 

  IF (LEOCLAKE) THEN

    ! TEMPORAL INTERPOLATION OF LAKE TEMPERATURES

    IF (LMCCIEC) THEN

      IJ0=NDD(NINDAT)
      IM0=NMM(NINDAT)
      IA0=NCCAA(NINDAT)
      CALL UPDCAL(IJ0,IM0,IA0,NSTADD,IJOUR,IMOIS,IAN,ILMOIS,NULOUT)
      IF(IJOUR > 15)THEN
        IMT1=IMOIS
        IJT1=15
        IJT2=15+ILMOIS(IMT1)
      ELSE
        IMT1=1+MOD(IMOIS+10,12)
        IJT1=15-ILMOIS(IMT1)
        IJT2=15
      ENDIF
      ZPOID1=REAL(IJT2-IJOUR,JPRB)/REAL(IJT2-IJT1,JPRB)
      ZPOID2=1.0_JPRB-ZPOID1

    ENDIF

    ! LAKE SST CORRECTION

    DO JSTGLO=1,KGPTOT,KPROMA
      IEND=MIN(KPROMA,KGPTOT-JSTGLO+1)
      IST=1
      IBL=(JSTGLO-1)/KPROMA+1
      DO JROF=1,IEND
        IF ( ZLAKE(JSTGLO+JROF-1,NP2,1) < 19591204.0_JPRB  ) THEN
          IF (LMCCIEC) THEN
            CLIMR(JSTGLO+JROF-1,NP2,IPAR139)=REAL(ZLAKE(JSTGLO+JROF-1,NP1,1)*ZPOID1&
               &                            +ZLAKE(JSTGLO+JROF-1,NP2,1)*ZPOID2,JPRD)
          ELSE
            CLIMR(JSTGLO+JROF-1,NP2,IPAR139)=REAL(ZLAKE(JSTGLO+JROF-1,NP2,1),JPRD)
          ENDIF
        ENDIF
      ENDDO
    ENDDO

    ! LAKE SEA ICE CORRECTION

    DO JSTGLO=1,KGPTOT,KPROMA
      IEND=MIN(KPROMA,KGPTOT-JSTGLO+1)
      IST=1
      IBL=(JSTGLO-1)/KPROMA+1
      DO JROF=1,IEND
        IF ( ZLAKE(JSTGLO+JROF-1,NP2,2) < 19591204.0_JPRB  ) THEN
          IF (LMCCIEC) THEN
            CLIMR(JSTGLO+JROF-1,NP2,IPAR31)=REAL(ZLAKE(JSTGLO+JROF-1,NP1,2)*ZPOID1&
               &                           +ZLAKE(JSTGLO+JROF-1,NP2,2)*ZPOID2,JPRD)
          ELSE
            CLIMR(JSTGLO+JROF-1,NP2,IPAR31)=REAL(ZLAKE(JSTGLO+JROF-1,NP2,2),JPRD)
          ENDIF
        ENDIF
      ENDDO
    ENDDO

    ! CURRENT ARE SET TO 0.0 OVER LAKES.

    IF (LECURR) THEN

      DO JSTGLO=1,KGPTOT,KPROMA
        IEND=MIN(KPROMA,KGPTOT-JSTGLO+1)
        IST=1
        IBL=(JSTGLO-1)/KPROMA+1
        DO JROF=1,IEND
          IF ( ZLAKE(JSTGLO+JROF-1,NP2,1) < 19591204.0_JPRB  ) THEN
            CLIMR(JSTGLO+JROF-1,NP2,IPAR131) = 0.0_JPRD
            CLIMR(JSTGLO+JROF-1,NP2,IPAR132) = 0.0_JPRD
          ENDIF
        ENDDO
      ENDDO

    ENDIF

    ! LIM FIELDS

    IF (LNEMOLIMGET) THEN

      ! NEMO SEAICE FIELDS ARE SET NEGATIVE TO IGNORE THEM IN SURF ETC.

      DO JSTGLO=1,KGPTOT,KPROMA
        IEND=MIN(KPROMA,KGPTOT-JSTGLO+1)
        IST=1
        IBL=(JSTGLO-1)/KPROMA+1
        DO JROF=1,IEND
          IF ( ZLAKE(JSTGLO+JROF-1,NP2,2) < 19591204.0_JPRB ) THEN
            CPLNG_FLD(YDMCC%IP_A_ICE_ALBEDO)%D(JSTGLO+JROF-1,1,1)     = -1.0_JPRO
            CPLNG_FLD(YDMCC%IP_A_ICE_THICKNESS)%D(JSTGLO+JROF-1,1,1)  = -1.0_JPRO
            CPLNG_FLD(YDMCC%IP_A_SNOW_THICKNESS)%D(JSTGLO+JROF-1,1,1) = -1.0_JPRO
            CPLNG_FLD(YDMCC%IP_A_SST)%D(JSTGLO+JROF-1,1,1)            = -1.0_JPRO
            CPLNG_FLD(YDMCC%IP_A_ICE_TEMP)%D(JSTGLO+JROF-1,1,1)       = -1.0_JPRO
            CPLNG_FLD(YDMCC%IP_A_ICE_FRAC)%D(JSTGLO+JROF-1,1,1)       = -1.0_JPRO
          ENDIF
        ENDDO
      ENDDO

    ENDIF

  ENDIF
  
  IF (LEFLAKE.AND.LEOLAKESST) THEN

    DO JSTGLO=1,KGPTOT,KPROMA

      IEND=MIN(KPROMA,KGPTOT-JSTGLO+1)
      IST=1
      IBL=(JSTGLO-1)/KPROMA+1

      DO JROF=1,IEND

        ! LAKE POINT IN FLAKE.
        IF (SD_VF(JROF,YSD_VF%YCLK%MP,IBL) > 0.5_JPRB) THEN
          ! SST
          CLIMR(JSTGLO+JROF-1,NP2,IPAR139)=REAL(SP_SL(JROF,YSP_SL%YLMLT%MP,IBL),JPRD)
          ! CI OVER LAKES DERIVED FROM ICE THICKNESS
          ! GOYETTE ET AL. (2000) ATMOS.-OCEAN 38, 481-503
          IF (SP_SL(JROF,YSP_SL%YLICD%MP,IBL) > 0.65_JPRB) THEN
            CLIMR(JSTGLO+JROF-1,NP2,IPAR31)=1.0_JPRD
          ELSEIF (SP_SL(JROF,YSP_SL%YLICD%MP,IBL) > 0.05_JPRB) THEN 
            CLIMR(JSTGLO+JROF-1,NP2,IPAR31)=REAL(1.287_JPRB*SQRT(SP_SL(JROF,YSP_SL%YLICD%MP,IBL)-0.05_JPRB)&
             & ,JPRD)
          ELSE
            CLIMR(JSTGLO+JROF-1,NP2,IPAR31)=0.0_JPRD
          ENDIF
        ENDIF

      ENDDO

    ENDDO

    IF (LECURR) THEN

      DO JSTGLO=1,KGPTOT,KPROMA
        IEND=MIN(KPROMA,KGPTOT-JSTGLO+1)
        IST=1
        IBL=(JSTGLO-1)/KPROMA+1
        DO JROF=1,IEND
          IF (SD_VF(JROF,YSD_VF%YCLK%MP,IBL) > 0.5_JPRB) THEN
            CLIMR(JSTGLO+JROF-1,NP2,IPAR131) = 0.0_JPRD
            CLIMR(JSTGLO+JROF-1,NP2,IPAR132) = 0.0_JPRD
          ENDIF
        ENDDO
      ENDDO

    ENDIF

    ! LIM FIELDS

    IF (LNEMOLIMGET) THEN

      ! NEMO SEAICE FIELDS ARE SET NEGATIVE TO IGNORE THEM IN SURF ETC.

      DO JSTGLO=1,KGPTOT,KPROMA
        IEND=MIN(KPROMA,KGPTOT-JSTGLO+1)
        IST=1
        IBL=(JSTGLO-1)/KPROMA+1
        DO JROF=1,IEND
          IF (SD_VF(JROF,YSD_VF%YCLK%MP,IBL) > 0.5_JPRB) THEN
            CPLNG_FLD(YDMCC%IP_A_ICE_ALBEDO)%D(JSTGLO+JROF-1,1,1)     = -1.0_JPRO
            CPLNG_FLD(YDMCC%IP_A_ICE_THICKNESS)%D(JSTGLO+JROF-1,1,1)  = -1.0_JPRO
            CPLNG_FLD(YDMCC%IP_A_SNOW_THICKNESS)%D(JSTGLO+JROF-1,1,1) = -1.0_JPRO
            CPLNG_FLD(YDMCC%IP_A_SST)%D(JSTGLO+JROF-1,1,1)            = -1.0_JPRO
            CPLNG_FLD(YDMCC%IP_A_ICE_TEMP)%D(JSTGLO+JROF-1,1,1)       = -1.0_JPRO
            CPLNG_FLD(YDMCC%IP_A_ICE_FRAC)%D(JSTGLO+JROF-1,1,1)       = -1.0_JPRO
          ENDIF
        ENDDO
      ENDDO

    ENDIF

  ENDIF

  ! UPDATE IFS OCEAN DATA WITH NEMO OCEAN DATA

  DO JSTGLO=1,KGPTOT,KPROMA

    IEND=MIN(KPROMA,KGPTOT-JSTGLO+1)
    IST=1
    IBL=(JSTGLO-1)/KPROMA+1

    ! FILL ARRAYS, ENSURING COMPATIBILITY OF ICE T, ICE COVER, SST AND SURFACE T 

    DO JROF=1,IEND

      ! OCEAN POINTS ONLY

      IF (SD_VF(JROF,YSD_VF%YLSM%MP,IBL) <= 0.5_JPRB) THEN

        ZTS = REAL(CLIMR(JSTGLO+JROF-1,NP2,IPAR139),JPRB)
        ZCI = REAL(CLIMR(JSTGLO+JROF-1,NP2,IPAR31),JPRB)
 
        !    DEFINE LOGICALS FOR RESOLVED LAKES 

        LLAKE(JROF) = (SD_VF(JROF,YSD_VF%YCLK%MP,IBL) > 0.5_JPRB) 
        
        ! DEFINE LOGICALS FOR OLD AND NEW SEA-ICE

        LLICEO(JROF) = SD_VF(JROF,YSD_VF%YCI%MP,IBL) > ZRCIMIN
        LLICEN(JROF) = ZCI > ZRCIMIN

        IF (LLICEN(JROF)) THEN
          SD_VF(JROF,YSD_VF%YCI%MP,IBL)=ZCI
        ELSE
          SD_VF(JROF,YSD_VF%YCI%MP,IBL)=0.
        ENDIF

        IF (LLICEN(JROF).AND.LLICEO(JROF)) THEN

          ! SEA-ICE TO SEA-ICE

          IF (LNEMOSSTFREEZ) THEN
            SD_VF(JROF,YSD_VF%YSST%MP,IBL) = ZRTFREEZSICE
          ELSE
            SD_VF(JROF,YSD_VF%YSST%MP,IBL) = MAX(ZTS,ZRTFREEZSICE)
          ENDIF
          SP_SB(JROF,1,YSP_SB%YT%MP,IBL) = SD_VF(JROF,YSD_VF%YCI%MP,IBL)*SP_SB(JROF,1,YSP_SB%YTL%MP,IBL)+&
           & (1.-SD_VF(JROF,YSD_VF%YCI%MP,IBL))*SD_VF(JROF,YSD_VF%YSST%MP,IBL)  
          SP_SB(JROF,2,YSP_SB%YT%MP,IBL) = SD_VF(JROF,YSD_VF%YCI%MP,IBL)*SP_SB(JROF,2,YSP_SB%YTL%MP,IBL)+&
           & (1.-SD_VF(JROF,YSD_VF%YCI%MP,IBL))*SD_VF(JROF,YSD_VF%YSST%MP,IBL)  
          SP_SB(JROF,3,YSP_SB%YT%MP,IBL) = SD_VF(JROF,YSD_VF%YCI%MP,IBL)*SP_SB(JROF,3,YSP_SB%YTL%MP,IBL)+&
           & (1.-SD_VF(JROF,YSD_VF%YCI%MP,IBL))*SD_VF(JROF,YSD_VF%YSST%MP,IBL)  
          SP_SB(JROF,4,YSP_SB%YT%MP,IBL) = SD_VF(JROF,YSD_VF%YCI%MP,IBL)*SP_SB(JROF,4,YSP_SB%YTL%MP,IBL)+&
           & (1.-SD_VF(JROF,YSD_VF%YCI%MP,IBL))*SD_VF(JROF,YSD_VF%YSST%MP,IBL)  
          SP_RR(JROF,YSP_RR%YT%MP,IBL)   = SP_SB(JROF,1,YSP_SB%YT%MP,IBL)

        ELSEIF (LLICEN(JROF).AND..NOT.LLICEO(JROF)) THEN

          ! SEA TO SEA-ICE

          IF (LNEMOSSTFREEZ) THEN
            SD_VF(JROF,YSD_VF%YSST%MP,IBL) = ZRTFREEZSICE
          ELSE
            SD_VF(JROF,YSD_VF%YSST%MP,IBL) = MAX(ZTS,ZRTFREEZSICE)
          ENDIF
          SP_SB(JROF,1,YSP_SB%YTL%MP,IBL) = ZRTFREEZSICE
          SP_SB(JROF,2,YSP_SB%YTL%MP,IBL) = ZRTFREEZSICE
          SP_SB(JROF,3,YSP_SB%YTL%MP,IBL) = ZRTFREEZSICE
          SP_SB(JROF,4,YSP_SB%YTL%MP,IBL) = ZRTFREEZSICE
          SP_SB(JROF,1,YSP_SB%YT%MP,IBL)  = SD_VF(JROF,YSD_VF%YCI%MP,IBL)*SP_SB(JROF,1,YSP_SB%YTL%MP,IBL)+&
            & (1.-SD_VF(JROF,YSD_VF%YCI%MP,IBL))*SD_VF(JROF,YSD_VF%YSST%MP,IBL)  
          SP_SB(JROF,2,YSP_SB%YT%MP,IBL)  = SD_VF(JROF,YSD_VF%YCI%MP,IBL)*SP_SB(JROF,2,YSP_SB%YTL%MP,IBL)+&
            & (1.-SD_VF(JROF,YSD_VF%YCI%MP,IBL))*SD_VF(JROF,YSD_VF%YSST%MP,IBL)  
          SP_SB(JROF,3,YSP_SB%YT%MP,IBL)  = SD_VF(JROF,YSD_VF%YCI%MP,IBL)*SP_SB(JROF,3,YSP_SB%YTL%MP,IBL)+&
           & (1.-SD_VF(JROF,YSD_VF%YCI%MP,IBL))*SD_VF(JROF,YSD_VF%YSST%MP,IBL)  
          SP_SB(JROF,4,YSP_SB%YT%MP,IBL)  = SD_VF(JROF,YSD_VF%YCI%MP,IBL)*SP_SB(JROF,4,YSP_SB%YTL%MP,IBL)+&
           & (1.-SD_VF(JROF,YSD_VF%YCI%MP,IBL))*SD_VF(JROF,YSD_VF%YSST%MP,IBL)  
          SP_RR(JROF,YSP_RR%YT%MP,IBL)    = SP_SB(JROF,1,YSP_SB%YT%MP,IBL)

        ELSE

          ! SEA TO SEA OR SEA-ICE TO SEA INCREASE SKIN WITH SAME AMOUNT AS SST

          IF (LEOCWA .OR. LEOCCO) THEN
            ZDSST0 = ZTS-SD_VF(JROF,YSD_VF%YSST%MP,IBL)
          ELSE 
            ZDSST0 = 0.
          ENDIF
          SD_VF(JROF,YSD_VF%YSST%MP,IBL)  = ZTS
          IF (LEFLAKE) THEN 
             IF (.NOT.LLAKE(JROF)) THEN
               SP_RR(JROF,YSP_RR%YT%MP,IBL) = SP_RR(JROF,YSP_RR%YT%MP,IBL)+ZDSST0
             ENDIF
          ELSE
             SP_RR(JROF,YSP_RR%YT%MP,IBL) = SP_RR(JROF,YSP_RR%YT%MP,IBL)+ZDSST0
          ENDIF
          SP_SB(JROF,1,YSP_SB%YT%MP,IBL)  = ZTS
          SP_SB(JROF,2,YSP_SB%YT%MP,IBL)  = ZTS
          SP_SB(JROF,3,YSP_SB%YT%MP,IBL)  = ZTS
          SP_SB(JROF,4,YSP_SB%YT%MP,IBL)  = ZTS
          SP_SB(JROF,1,YSP_SB%YTL%MP,IBL) = ZRTFREEZSICE
          SP_SB(JROF,2,YSP_SB%YTL%MP,IBL) = ZRTFREEZSICE
          SP_SB(JROF,3,YSP_SB%YTL%MP,IBL) = ZRTFREEZSICE
          SP_SB(JROF,4,YSP_SB%YTL%MP,IBL) = ZRTFREEZSICE

        ENDIF

      ENDIF

    ENDDO

    ! OPTIONALLY UPDATE SURFACE CURRENTS FOR COUPLING

    IF (LECURR) THEN

      DO JROF=1,IEND

        IF((SD_VF(JROF,YSD_VF%YLSM%MP,IBL) <= 0.5_JPRB).AND..NOT.LLICEN(JROF))THEN

          SD_VF(JROF,YSD_VF%YUCUR%MP,IBL) = REAL(CLIMR(JSTGLO+JROF-1,NP2,IPAR131),JPRB)
          SD_VF(JROF,YSD_VF%YVCUR%MP,IBL) = REAL(CLIMR(JSTGLO+JROF-1,NP2,IPAR132),JPRB)

        ELSE
          ! Currents under ice set to zero for computation of stress using relative winds
          SD_VF(JROF,YSD_VF%YUCUR%MP,IBL) = 0.0_JPRB
          SD_VF(JROF,YSD_VF%YVCUR%MP,IBL) = 0.0_JPRB

        ENDIF

      ENDDO

    ENDIF

  ENDDO

  ! OPTIONALLY UPDATE THE ICE TEMPERATURE WITH LIM VALUES.

  IF (LNEMOLIMGET.AND.LNEMOLIMTEMP) THEN
     
    DO JSTGLO=1,KGPTOT,KPROMA
      IEND=MIN(KPROMA,KGPTOT-JSTGLO+1)
      IST=1
      IBL=(JSTGLO-1)/KPROMA+1
      DO JROF =1,IEND
        ZTICE=CPLNG_FLD(YDMCC%IP_A_ICE_TEMP)%D(JSTGLO+JROF-1,1,1)
        IF((SD_VF(JROF,YSD_VF%YLSM%MP,IBL) <= 0.5_JPRB).AND.&
          &(ZTICE > 0.0_JPRB).AND.&
          &(SD_VF(JROF,YSD_VF%YCI%MP,IBL) > ZRCIMIN)) THEN
          SP_SB(JROF,1,YSP_SB%YTL%MP,IBL) = ZTICE
          SP_SB(JROF,1,YSP_SB%YT%MP,IBL)  = SD_VF(JROF,YSD_VF%YCI%MP,IBL)*SP_SB(JROF,1,YSP_SB%YTL%MP,IBL)+&
            & (1.-SD_VF(JROF,YSD_VF%YCI%MP,IBL))*SD_VF(JROF,YSD_VF%YSST%MP,IBL)  
        ENDIF
      ENDDO
    ENDDO

    IF (LNEMOLIMTLVL) THEN

       DO JSTGLO=1,KGPTOT,KPROMA
          IEND=MIN(KPROMA,KGPTOT-JSTGLO+1)
          IST=1
          IBL=(JSTGLO-1)/KPROMA+1
          DO JROF =1,IEND
             ZTICE=CPLNG_FLD(YDMCC%IP_A_ICE_TEMP)%D(JSTGLO+JROF-1,1,1)
             IF((SD_VF(JROF,YSD_VF%YLSM%MP,IBL) <= 0.5_JPRB).AND.&
                &(ZTICE > 0.0_JPRB).AND. &
                &(SD_VF(JROF,YSD_VF%YCI%MP,IBL) > ZRCIMIN)) THEN
                SP_SB(JROF,2,YSP_SB%YTL%MP,IBL) = &
                   & CPLNG_FLD(YDMCC%IP_A_ICE_TEMP_LVLS)%D(JSTGLO+JROF-1,1,1)
                SP_SB(JROF,3,YSP_SB%YTL%MP,IBL) = &
                   & CPLNG_FLD(YDMCC%IP_A_ICE_TEMP_LVLS)%D(JSTGLO+JROF-1,2,1)
                SP_SB(JROF,4,YSP_SB%YTL%MP,IBL) = &
                   & CPLNG_FLD(YDMCC%IP_A_ICE_TEMP_LVLS)%D(JSTGLO+JROF-1,3,1)
             ENDIF
          ENDDO
       ENDDO

    ENDIF

  ENDIF


  ! Diagnostic output

  DO JSTGLO=1,KGPTOT,KPROMA
    IEND=MIN(KPROMA,KGPTOT-JSTGLO+1)
    IST=1
    IBL=(JSTGLO-1)/KPROMA+1
    DO JROF =1,IEND

      ! Currents for diagnostic output taken from NEMO without zeroing under ice.

      SD_OC(JROF,YSD_OC%YUCURM%MP,IBL) = REAL(CLIMR(JSTGLO+JROF-1,NP2,IPAR131),JPRB)
      SD_OC(JROF,YSD_OC%YVCURM%MP,IBL) = REAL(CLIMR(JSTGLO+JROF-1,NP2,IPAR132),JPRB)
     
      ! Optional masking of diagnostic output

      IF (LNEMOGRIBMASK) THEN

        IF ((SD_VF(JROF,YSD_VF%YLSM%MP,IBL)>0.5_JPRB).OR.&
             &(SD_VF(JROF,YSD_VF%YCLK%MP,IBL)>0.5_JPRB)) THEN
          SD_OC(JROF,YSD_OC%YSSTM%MP,IBL)  = ZMDI
          SD_OC(JROF,YSD_OC%YCIM%MP,IBL)   = ZMDI
          SD_OC(JROF,YSD_OC%YUCURM%MP,IBL) = ZMDI
          SD_OC(JROF,YSD_OC%YVCURM%MP,IBL) = ZMDI
        ELSE
          SD_OC(JROF,YSD_OC%YSSTM%MP,IBL)  = SD_VF(JROF,YSD_VF%YSST%MP,IBL)
          SD_OC(JROF,YSD_OC%YCIM%MP,IBL)   = SD_VF(JROF,YSD_VF%YCI%MP,IBL)
        ENDIF

      ENDIF

    ENDDO
  ENDDO


  WRITE(NULOUT,*) 'UPDNEMOOCEAN finished'
  WRITE(NULOUT,*)
  CALL FLUSH(NULOUT)

ENDIF


!     ------------------------------------------------------------------
END ASSOCIATE
IF (LHOOK) CALL DR_HOOK('UPDNEMOOCEAN',1,ZHOOK_HANDLE)
END SUBROUTINE UPDNEMOOCEAN
