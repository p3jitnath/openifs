   & PVTTRC ,PVTSRC ,PVSTRC ,PVSSRC ,PVES, PVSMLT, PV10FG, PV10FG6, PVI10FG,&
   & PVLSPF ,PVVIMD,&
   & PVDSSHF,PVDSLHF,PVDSP  ,PVDMSL,&
   & PVDSSR ,PVDSTR ,PVDSSRD,PVDSTRD,PVDSSRDC,PVDSTRDC,PVDTSR ,PVDTTR,&
   & PVDUVDF,PVDPARF,PVDCAPE,PVDCAPES,PVDMXCAP6,PVDMXCAPS6,&
   & PVDPARCF,PVDTINCF,PVDFDIR,PVDDSRP,PVDCDIR,&
   & PVCBASE,PV0DEGL,PVM10DEGL,PVVISIH,PVCIN  ,PVKINDEX,PVTTINDEX,&
   & PVPDEPL,PVMUCAPE,PVMLCAPE50,PVMLCAPE100,PVMLCIN50,PVMLCIN100,&
   & PVCBASEA,PVCTOPC,PVTROPOTP,PVZTWETB0, PVZTWETB1,&
   & PVDEWSS,PVDNSSS,PVDE   ,PVDPEV   ,PVDLGWS,&
   & PVDMGWS,PVDGWD ,PVDMX2T,PVDMN2T,PVDMX2T6,PVDMN2T6,PVDRO,PVDSSRO,PVDSRO,&
   & PVDNEE ,PVDGPP ,PVDREC ,&
   & PVDCSF ,PVDLSSF,PVDMXTPR,PVDMNTPR,PVDMXTPR6,PVDMNTPR6,&
   & PVDTPR ,PVDLSRR,PVDCRR ,PVDLSSFR,PVDCSFR,PVDPTYPE,PVILSPF,&
   & PVDIEWSS,PVDINSSS,PVDISSHF,PVDIE,PVDVIWVE,PVDVIWVN,&
   & PVDTCW ,PVDTCWV,PVDTCLW,PVDTCIW,PVDTCRW,PVDTCSW,PVDTCSLW,PVDTCO3,&
   & PVDTCGHG,PVDTCCHEM,PVDTCAERO,&
   & PVDLITOTI,PVDLITOTA6,PVDLICGI,PVDLICGA6,&
   & PVDPTYPEOCC6, PSMCLBT, PSMCSBT)  
ENDIF

!     ------------------------------------------------------------------

!*        4.  DDH.
!             ----

!*    4.1 INITIALIZATION

IF ( LSDDH ) THEN
  DO JROF=KST,KEND
    IDDHI(JROF)=NDDHI(JROF+KSTGLO-1)
    ZDHSF(JROF)=HDSF(JROF+KSTGLO-1)
  ENDDO

!        INITIALISATION OF INTERMEDIATE COMPUTATION ARRAYS
!        IN ORDER TO MAKE THE ACCUMULATION EASIER

  DO JSU=1,NDHCVSUL
    DO JROF=KST,KEND
      ZDHCV(JROF,0   ,JSU)=0.0_JPRB
    ENDDO
  ENDDO

!        INITIALISATION IN CASE OF CONTROL OPTION

ENDIF

!*       4.2  COMP. OF PHYS. FIELDS AND SURFACE FLUXES

IF (LSDDH.AND..NOT.LINITER) THEN

!     CALCUL DES VENTS ZONAL ET MERIDIEN VRAIS, GEOGRAPHIQUES
  IF ( LHDHKS .OR. LHDMCI .OR. LHDENT ) THEN
    DO JLEV = 1, KFLEVG
      DO JROF=KST,KEND
        ZUZGEO(JROF,JLEV) = PGNORDM(JROF)*PUT9(JROF,JLEV)-&
         & PGNORDL(JROF)*PVT9(JROF,JLEV)
        ZVMGEO(JROF,JLEV) = PGNORDL(JROF)*PUT9(JROF,JLEV)+&
         & PGNORDM(JROF)*PVT9(JROF,JLEV)
      ENDDO
    ENDDO
  ENDIF

  DO JL=KST,KEND
    ZVDZ0F(JL) = PVDZ0F(JL) /RG
    ZVDZ0H(JL) = EXP(PVDLZ0H(JL))
  ENDDO
  DO JL=KST,KEND
    ZCPT1(JL)  =PENES(JL,1)
    ZCPT234(JL)=PENES(JL,2)+PENES(JL,3)+PENES(JL,4)
    ZWL1(JL)   =PQLINU0(JL,1)+PWRENU0(JL)
    ZW234(JL)  =PQLINU0(JL,2)+PQLINU0(JL,3)+PQLINU0(JL,4)
  ENDDO
  LLDYN=.FALSE.
  LLPHY=.TRUE.

  !LAI, soil type
  ZVDLAIL  => PSD_VF(1:KPROMA,YSD_VF%YLAIL%MP)
  ZVDLAIH  => PSD_VF(1:KPROMA,YSD_VF%YLAIH%MP)
  ZVDSOTY  => PSD_VF(1:KPROMA,YSD_VF%YSOTY%MP)

  !lake variables
  IF (LEFLAKE) THEN
     ZVDCLK  => PSD_VF(1:KPROMA,YSD_VF%YCLK%MP)
     ZVDDL   => PSD_VF(1:KPROMA,YSD_VF%YDL%MP)
  ENDIF

!cj begin change for new sfc DDH

  DO JROF=KST,KEND

! determine land-sea mask and

    IF(PVFLSM(JROF) > 0.5_JPRB) THEN
      ZLSM(JROF)=1.0_JPRB
    ELSE
      ZLSM(JROF)=0.0_JPRB
    ENDIF

! fill free-style array with appropriate variables

! 2m temperature
    PDHFSS(JROF,1)=PTCLS(JROF)
! 2m specific humidity
    PDHFSS(JROF,2)=PQCLS(JROF)
! 10m wind components
    PDHFSS(JROF,3)=PUCLS(JROF)
    PDHFSS(JROF,4)=PVCLS(JROF)
! skin temperature
    PDHFSS(JROF,5)=PTRENU0(JROF)
! forecast surface roughness length for momentum and heat
    PDHFSS(JROF,6)=ZVDZ0F(JROF)
    PDHFSS(JROF,7)=ZVDZ0H(JROF)
! forecast albedo
    PDHFSS(JROF,8)=PVDALB(JROF)
! boundary layer height
    PDHFSS(JROF,9)=PVDBLH(JROF)
! land-sea mask and sea-ice fraction
    PDHFSS(JROF,10)=ZLSM(JROF)
    PDHFSS(JROF,11)=PVFCI(JROF)
! wind gusts
    PDHFSS(JROF,12)=PV10FG(JROF)
    PDHFSS(JROF,25)=PI10FGCV(JROF)
! Orography
    PDHFSS(JROF,13)=POROG(JROF)
! Total cloud cover
    PDHFSS(JROF,14)=PVTCC(JROF)
! Low cloud cover
    PDHFSS(JROF,15)=PVLCC(JROF)
! Medium cloud cover
    PDHFSS(JROF,16)=PVMCC(JROF)
! High cloud cover
    PDHFSS(JROF,17)=PVHCC(JROF)
! convective available potential energy
    PDHFSS(JROF,18)=PCAPE(JROF,1)
! Cloud base level
    PDHFSS(JROF,19)=PCBASE(JROF)
! zero deg level
    PDHFSS(JROF,20)=P0DEGL(JROF)
! Horizontal visibility
    PDHFSS(JROF,21)=PVISIH(JROF)
! CIN
    PDHFSS(JROF,22)=PCIN(JROF,1)
! COnvective Indices
    PDHFSS(JROF,23)=PCONVIND(JROF,1)
    PDHFSS(JROF,24)=PCONVIND(JROF,2)
! Ceiling cloud base level aviation
    PDHFSS(JROF,26)=PCBASEA(JROF)
! Cloud top level convective
    PDHFSS(JROF,27)=PCTOPC(JROF)
! Height of 0,1deg wet bulb T
    PDHFSS(JROF,28)=PZTWETB(JROF,1)
    PDHFSS(JROF,29)=PZTWETB(JROF,2)
! -10 deg level
    PDHFSS(JROF,30)=PM10DEGL(JROF)
! most unstable and mixed-layer CAPE
    PDHFSS(JROF,31)=PCAPE(JROF,2)
    PDHFSS(JROF,32)=PCAPE(JROF,3)
    PDHFSS(JROF,33)=PCAPE(JROF,4)
! mixed-layer CIN
    PDHFSS(JROF,34)=PCIN(JROF,2)
    PDHFSS(JROF,35)=PCIN(JROF,3)
! departure level(pressure) of most unstable convective parcel
    PDHFSS(JROF,36)=PPDEPL(JROF)
! pressure at thermal tropopause
    PDHFSS(JROF,37)=PTROPOTP(JROF)

! the number of ECMWF permanent free style variables
    IFSVAR_ECMWF=37

! Total column AERO
    NFSVAR_AERO=IFSVAR_ECMWF + 1
    DO JAERO=1,NACTAERO
      PDHFSS(JROF,IFSVAR_ECMWF +  JAERO)=PVDTCAERO(JROF,JAERO)
    ENDDO
    IFSVAR_ECMWF=IFSVAR_ECMWF + NACTAERO

! Please if you introduce new variables always let the following
! GEMS diagnostics at the end of the free style variables
! and change the incrementation accordingly

! Total column GHG - Not permanent (conditional variables)
! IF CO2 is run together with CH4 the order in DDH will be
! CO2 first then CH4

    DO JGHG = 1, NGHG
      ICO2=MIN(NGHG,1)
      ICH4=MAX(NGHG,1)
      IF(YGHG(JGHG)%IGRBCODE == 210061) THEN
        PDHFSS(JROF,IFSVAR_ECMWF+ICO2)=PVDTCGHG(JROF,ICO2)
      ELSEIF(YGHG(JGHG)%IGRBCODE == 210062) THEN
        PDHFSS(JROF,IFSVAR_ECMWF+ICH4)=PVDTCGHG(JROF,ICH4)
      ENDIF
    ENDDO
    IFSVAR_ECMWF=IFSVAR_ECMWF + NGHG 

! surface downward shortwave and longwave radiation
    PDHFSS(JROF,NDHVFS+1)=PFRSOD(JROF)
    PDHFSS(JROF,NDHVFS+2)=PFRTHD(JROF)
! sunshine duration
    PDHFSS(JROF,NDHVFS+3)=PISUND(JROF)
! snow evaporation
    PDHFSS(JROF,NDHVFS+4)=PFEVN(JROF)
! snowmelt
    PDHFSS(JROF,NDHVFS+5)=PFONTE(JROF)
! large-scale rainfall
    PDHFSS(JROF,NDHVFS+6)=PFPLSL(JROF,KFLEVG)
! convective rainfall
    PDHFSS(JROF,NDHVFS+7)=PFPLCL(JROF,KFLEVG)
! large-scale snowfall
    PDHFSS(JROF,NDHVFS+8)=PFPLSN(JROF,KFLEVG)
! convective snowfall
    PDHFSS(JROF,NDHVFS+9)=PFPLCN(JROF,KFLEVG)
! surface sensible heat flux
    PDHFSS(JROF,NDHVFS+10)=PDIFTS(JROF,KFLEVG)
! surface latent heat flux
    PDHFSS(JROF,NDHVFS+11)=PFCLL(JROF)+PFCLN(JROF)
! surface solar radiation
    PDHFSS(JROF,NDHVFS+12)=PFRSO (JROF,KFLEVG)
! surface thermal radiation
    PDHFSS(JROF,NDHVFS+13)=PFRTH (JROF,KFLEVG)
! surface evaporation
    PDHFSS(JROF,NDHVFS+14)=PDIFTQ(JROF,KFLEVG)
! total runoff
    PDHFSS(JROF,NDHVFS+15)=PRUISS(JROF) + PRUISP(JROF)
! surface solar radiation, clear-sky
    PDHFSS(JROF,NDHVFS+16)=PISSRC(JROF)
! surface thermal radiation, clear-sky
    PDHFSS(JROF,NDHVFS+17)=PISTRC(JROF)
! Large-scale precipitation fraction
    PDHFSS(JROF,NDHVFS+18)=PILSPF(JROF)
! surface UV flux
    PDHFSS(JROF,NDHVFS+19)=PUVDF(JROF)
! surface Photosynth.Active Radiation
    PDHFSS(JROF,NDHVFS+20)=PPARF(JROF)
! surface Photosynth.Active Radiation - Clear Sky
    PDHFSS(JROF,NDHVFS+21)=PPARCF(JROF)
! TOA incident solar radiation
    PDHFSS(JROF,NDHVFS+22)=PTINCF(JROF)
! vertically integr. mass divergence
    PDHFSS(JROF,NDHVFS+23)=ZIVIMD(JROF)
! TOA SW radiation clearsky
    PDHFSS(JROF,NDHVFS+24)=PITSRC(JROF)
! TOA LW radiation clearsky
    PDHFSS(JROF,NDHVFS+25)=PITTRC(JROF)
! Surface direct SW radiation - total sky
    PDHFSS(JROF,NDHVFS+26)=PFDIR(JROF)
! Surface direct SW radiation - clear-sky
    PDHFSS(JROF,NDHVFS+27)=PCDIR(JROF)
! Surface clear-sky downward shortwave flux
    PDHFSS(JROF,NDHVFS+28)=PFRSODC(JROF)
! Surface clear-sky downward longwave flux
    PDHFSS(JROF,NDHVFS+29)=PFRTHDC(JROF)
! TOA SW radiation net
    PDHFSS(JROF,NDHVFS+30)=PFRSO(JROF,0)
! TOA LW radiation net
    PDHFSS(JROF,NDHVFS+31)=PFRTH (JROF,0)
! Leaf area index low
    PDHFSS(JROF,NDHVFS+32)=ZVDLAIL(JROF)
! Leaf area index high
    PDHFSS(JROF,NDHVFS+33)=ZVDLAIH(JROF)
! Dominant soil texture type
    PDHFSS(JROF,NDHVFS+34)=ZVDSOTY(JROF)
! Surface direct beam SW radiation - total sky
    PDHFSS(JROF,NDHVFS+35)=PDSRP(JROF)

! the number of ECMWF permanent free style fluxes
    IFSFLX_ECMWF=35

! Aerosols: fluxes from physical processes
    IF (NACTAERO > 0) THEN
      NFSFLX_AERO=IFSFLX_ECMWF + 1
      DO JAERO=1,NACTAERO
        DO JFLX=1,7
          PDHFSS(JROF,NDHVFS+IFSFLX_ECMWF + (JAERO-1)*7 + JFLX)=&
           & PAERODDF(JROF,JAERO,JFLX)
        ENDDO
      ENDDO
      IFSFLX_ECMWF=IFSFLX_ECMWF+NACTAERO*7
    ENDIF

    IF (LEFLAKE) THEN
       IFSFLX_ECMWF=IFSFLX_ECMWF+1
       PDHFSS(JROF,NDHVFS+IFSFLX_ECMWF)=PTLMLNU1(JROF)
       IFSFLX_ECMWF=IFSFLX_ECMWF+1
       PDHFSS(JROF,NDHVFS+IFSFLX_ECMWF)=PHLMNU1(JROF)
       IFSFLX_ECMWF=IFSFLX_ECMWF+1
       PDHFSS(JROF,NDHVFS+IFSFLX_ECMWF)=PTLBTNU1(JROF)
       IFSFLX_ECMWF=IFSFLX_ECMWF+1
       PDHFSS(JROF,NDHVFS+IFSFLX_ECMWF)=PTLMNNU1(JROF)
       IFSFLX_ECMWF=IFSFLX_ECMWF+1
       PDHFSS(JROF,NDHVFS+IFSFLX_ECMWF)=PTLSFNU1(JROF)
       IFSFLX_ECMWF=IFSFLX_ECMWF+1
       PDHFSS(JROF,NDHVFS+IFSFLX_ECMWF)=PTLICNU1(JROF)
       IFSFLX_ECMWF=IFSFLX_ECMWF+1
       PDHFSS(JROF,NDHVFS+IFSFLX_ECMWF)=PHLICNU1(JROF)
       IFSFLX_ECMWF=IFSFLX_ECMWF+1
       PDHFSS(JROF,NDHVFS+IFSFLX_ECMWF)=ZVDCLK(JROF)
       IFSFLX_ECMWF=IFSFLX_ECMWF+1
       PDHFSS(JROF,NDHVFS+IFSFLX_ECMWF)=ZVDDL(JROF)
    ENDIF

  ENDDO
!print 9421,(PDHFSS(JROF,NDHVFS+19),JROF=KST,KEND)
!  9421 FORMAT(1X,'PostphyD UVBF ',15E10.3)
!print 9422,(PDHFSS(JROF,NDHVFS+20),JROF=KST,KEND)
!  9422 FORMAT(1X,'PostphyD PARF ',15E10.3)
!print 9423,(PDHFSS(JROF,NDHVFS+21),JROF=KST,KEND)
!  9423 FORMAT(1X,'PostphyD CAPE ',15E10.3)
!print 9424,(PDHFSS(JROF,NDHVFS+22),JROF=KST,KEND)
!  9424 FORMAT(1X,'PostphyD PARCF ',15E10.3)
!print 9425,(PDHFSS(JROF,NDHVFS+23),JROF=KST,KEND)
!  9425 format(1x,'PostphyD TINCF ',15E10.3)
!print 9426,(PDHFSS(JROF,NDHVFS+24),JROF=KST,KEND)
!  9426 format(1x,'PostphyD VIMDF ',15E10.3)
!print 9427,(PDHFSS(JROF,NDHVFS+25),JROF=KST,KEND)
!  9427 format(1x,'PostphyD TSRC ',15E10.3)
!print 9428,(PDHFSS(JROF,NDHVFS+26),JROF=KST,KEND)
!  9428 format(1x,'PostphyD TTRC ',15E10.3)
!print 9426,(PDHFSS(JROF,NDHVFS+27),JROF=KST,KEND)
!  9426 format(1x,'PostphyD FDIR ',15E10.3)
!print 9426,(PDHFSS(JROF,NDHVFS+28),JROF=KST,KEND)
!  9426 format(1x,'PostphyD CDIR ',15E10.3)
!print 9429,(PDHFSS(JROF,NDHVFS+29),JROF=KST,KEND)
!  9429 format(1x,'PostphyD CBASE ',15E10.3)
!print 9430,(PDHFSS(JROF,NDHVFS+30),JROF=KST,KEND)
!  9430 format(1x,'PostphyD 0DEGL ',15E10.3)
!print 9431,(PDHFSS(JROF,NDHVFS+31),JROF=KST,KEND)
!  9431 format(1x,'PostphyD VISIH ',15E10.3)


  CALL CPPHDDHE(YDDIM,YDSURF,YDMODEL%YRML_DIAG,YDDPHY,YDMODEL%YRML_GCONF%YRRIP,YDMODEL%YRML_PHY_MF%YRPHY,KPROMA,KST,KEND,KFLEVG,&
   & PGNORDL,PGNORDM,ZUZGEO ,ZVMGEO ,&
   & PGELAM ,ZLSM   ,PVFCI  ,&
   & PDIFCQ ,PDIFCS ,PDIFTQ ,PDIFTS ,PFCQNG ,&
   & PFPLCL ,PFPLCN ,PFPLSL ,PFPLSN ,&
   & PFRSO  ,PFRTH  ,&
   & PFHPCL ,PFHPCN ,PFHPSL ,PFHPSN ,&
   & PSTRCU ,PSTRCV ,PSTRDU ,PSTRDV ,PSTRTU ,PSTRTV ,&
   & PFCQLNG, PFSQRF,   PFSQSF ,  PFCQRNG,  PFCQSNG,PFSQLTUR, PFSQITUR ,&
   & PFCQNNG,&
   & PFCCQL ,PFCCQN ,PFCSQL ,PFCSQN ,&
   & PDHTLS ,PDHTSS ,PDHTTS ,PDHTIS ,&
   & PDHSSS ,PDHIIS ,PDHWLS ,PDHFSS ,&
   & ZDHCV  ,ZDHCS)

!*    4.3   STORE/ACCUM. IN HORIZONTAL MEAN ARRAYS

  CALL CPCUDDH(YDMODEL%YRML_DIAG,YDMODEL%YRML_GCONF%YRRIP,KPROMA,KST,KEND,KFLEVG,IDDHI,ZDHSF,ZDHCV,ZDHCS,LLDYN,LLPHY,NDHCVSUL)
ENDIF

!* CHANGE GHG TOTAL COLUMN UNIT (kg/m2-->ppm for CO2 and ppb for CH4) (integral --> average)

IF(NGHG > 0) THEN
  ZRGI=1.0_JPRB/RG
  DO JL=KST,KEND
    ZSUMDELP=0.0_JPRB
    DO JLEV=1,KFLEVG
      ZSUMDELP=ZSUMDELP+PDELP9(JL,JLEV)*ZRGI
    ENDDO
    DO JGHG=1,NGHG
      IF(YGHG(JGHG)%IGRBCODE == NGRBGHG(1)) ZRMGHG=RMCO2
      IF(YGHG(JGHG)%IGRBCODE == NGRBGHG(2)) ZRMGHG=RMCH4/1.E3_JPRB
      IF(YGHG(JGHG)%IGRBCODE == NGRBGHG(3)) ZRMGHG=RMN2O
      PVDTCGHG(JL,JGHG)=PVDTCGHG(JL,JGHG)/ZSUMDELP*1.E6_JPRB*RMD/ZRMGHG
    ENDDO
  ENDDO
ENDIF

!     ------------------------------------------------------------------

END ASSOCIATE
END ASSOCIATE
