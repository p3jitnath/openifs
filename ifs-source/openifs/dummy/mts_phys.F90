 REAL(KIND=JPRB) ,INTENT(IN) :: POROG(KLON)
 REAL(KIND=JPRB) ,INTENT(IN) :: PTS(KLON)
 REAL(KIND=JPRB) ,INTENT(IN) :: PITM(KLON)
