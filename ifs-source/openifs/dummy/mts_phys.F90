SUBROUTINE MTS_PHYS(YDDIMV,YDRIP,KIDIA,KFDIA,KLON,KLEV,PGEMU,PGELAM,POROG,PAPRS,PAPRSF,&
 & PLNPR,PALPH,PU,PV,PT,PQ,PQO3,PQLI,PQICE,PNEB,PTS,PITM,&
 & PUCLS,PVCLS,PTCLS,PQCLS,&
 & PTBMSAT7,PTBMSAT8,PTBMSAT9,PTBGOES11,PTBGOES12,PTBMTSAT1)
USE YOMDIMV , ONLY : TDIMV
 USE PARKIND1 ,ONLY : JPIM ,JPRB
 USE YOMRIP , ONLY : TRIP
 TYPE(TDIMV) , INTENT(IN) :: YDDIMV
 TYPE(TRIP) , INTENT(IN) :: YDRIP
 INTEGER(KIND=JPIM), INTENT(IN) :: KIDIA
 INTEGER(KIND=JPIM), INTENT(IN) :: KFDIA
 INTEGER(KIND=JPIM), INTENT(IN) :: KLON
 INTEGER(KIND=JPIM), INTENT(IN) :: KLEV
