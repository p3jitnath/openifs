       SUBROUTINE GETDOM(KINDEX,KST,KDOM,PON,PPRES,PDELP,KPZL,KVERT)
C      SUBROUTINE GETDOM(KINDEX,KST,KDOM,PON,PDELP,KPZL,KVERT)
C
C     FILLS AND SCALES ONE ENTIRE FIELD FOR LIMITED DOMAINS
C
C
C     INPUT
C     -----
C
C     VAR        DIMENSION   DESCRIPTION
C
C  INTEGER
C
C     KINDEX                 INDEX OF THE VARIABLE TO BE PLOTTED
C     KST                    2 FOR FIRST STEP (NSTEPB)
C                            1 FOR SECOND STEP (NSTEPE)
C     KPZL                   DIMENSION OF PON
C     KDOM                   INDEX OF LIMITED DOMAIN
C
C
C     OUTPUT
C     ------
C
C  INTEGER
C
C     KVERT                  VERTICAL 'DIMENSION' OF PON 
c     KVERTC
C  REAL
C
C     PON        KPZL        ARRAY TO CONTAIN VALUES
C     PPRES      KPZL        ARRAY TO CONTAIN PRESSURE
C
C
C     
C     PEDRO VITERBO         E.C.M.W.F.    03/94.                            
C
#include "param.h"
C
      REAL PON(KPZL),PPRES(KPZL),PDELP(KPZL)
C      REAL PON(KPZL),PDELP(KPZL)
#include "comddh.h"
C
C
C     1. FILLS THE FIELD IN THE RIGHT ORDER
C
      CALL GETAREA(KINDEX,KST,KDOM,PON,PDELP,JPLEVP1,KVERT)
C      CALL GETAREA(KINDEX,KST,KDOM,PON,PPRES,PDELP,JPLEVP1,KVERT)
C
C     2. SECONDARY (SECOND) SCALING - GETS THE PRE-SPECIFIED UNITS
C
      IF(.NOT.LARCHIVE.AND.(GAIN(KINDEX).NE.1.OR.
     *   OFFSET(KINDEX).NE.0.)) THEN
         DO 210 J=1,KVERT
         PON(J)=GAIN(KINDEX)*(PON(J)-OFFSET(KINDEX))
 210     CONTINUE
      ENDIF

C
      RETURN
      END



