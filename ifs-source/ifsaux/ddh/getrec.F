      SUBROUTINE GETREC(CDRECNM,KBLOCK,KBLLEN,
     S CDDATA,KLENCHBL,PDATA,KLENFP,KINDEX,KLENIND,KLDIM,PLDATA,KLEN)
C
C****    RETRIEVE REAL RECORD
C
C  INPUT PARAMETERS
C
C         CDRECNM - RECORD NAME
C         KBLOCK  - BLOCK NUMBER (MASK NUMBER, ZONAL BLOCK)
C         KBLLEN  - BLOCK LENGTH (FROM RECORD 'BLOCKLENGTH')
C         CDDATA  - CHARACTER BLOCK FROM DECOPS
C         KLENCHBL- LENGTH OF CHARACTER BLOCK
C         KDATA   - INTEGER BLOCK FROM DECOPS
C         KLENFP  - LENGTH OF INGEGER BLOCK
C         KINDEX  - INDEX
C         KLENIND - LENGTH OF INDEX
C         KLDIM   - DIMENSION OF PLDATA
C
C  OUTPUT PARAMETERS
C
C         PLDATA  - OUTPUT DATA
C         KLEN    - NUMBER OF DATA VALUES
C                                                                               
C     MATS HAMRUD         E.C.M.W.F.    03/94.                            
C
C
      CHARACTER CDRECNM*12
      CHARACTER CDDATA*(*)
      INTEGER KINDEX(KLENIND)
      REAL PLDATA(KLDIM)
      REAL PDATA(KLENFP)
C
C*      SEARCH FOR RECORD
C        
      ILENRN=12
      IMREC=KLENCHBL/ILENRN
      DO 102 JREC=1,IMREC
      IREC=JREC
      IPTCHB=(IREC-1)*ILENRN+1
      IPTCHE=IPTCHB+ILENRN-1
      IF(CDDATA(IPTCHB:IPTCHE).EQ.CDRECNM) THEN
         GO TO 104
      ENDIF
 102  CONTINUE
      WRITE(0,*) ' RECORD NOT FOUND ',CDRECNM
      CALL ABORT
 104  CONTINUE
C
C         FIND POSITION AND LENGTH OF RECORD
C 
      IPT=2*IREC-1
      INDEX=KINDEX(IPT)
      IF(INDEX.LT.0) THEN
         WRITE(0,*) ' RECORD NOT REAL ',CDRECNM
         CALL ABORT
      ENDIF
      KLEN=0
      WRITE(0,*) IPT, KINDEX(IPT+1), KLEN, KLDIM
      KLEN=KINDEX(IPT+1)
      IF(KLEN.GT.KLDIM) THEN
         WRITE(0,*) 'GETREC: RECORD TOO LONG , RECORD=',CDRECNM
         WRITE(0,*) '          KLEN = ',KLEN
         WRITE(0,*) '          KLDIM = ',KLDIM
         WRITE(0,*) '          Increase JPLEVP1 in param.h'
      ENDIF   
      INDEX=INDEX+(KBLOCK-1)*KBLLEN

      IF(INDEX+KLEN-1.GT.KLENFP) THEN
         WRITE(0,*) ' GETREC : ADDRESS OUTSIDE REAL BLOCK '
         CALL ABORT
      ENDIF   
C
C         COPY RECORD TO OUTPUT ARRAY
C      
      DO 112 J=1,KLEN
      PLDATA(J)=PDATA(INDEX+J-1)
 112  CONTINUE
C
      RETURN
      END
