SUBROUTINE REAREC(CDFNAME,KDIMCH,KDIMFP,KDIMIN,CDDATA,KLENCH,PDATA,KLENFP,KDATA,KLENIN)
!
!****    READ DDH FILE AND DECODE
!
!  INPUT PARAMETERS
!
!     CDFNAME  - FILE NAME
!     KDIMCH   - "DIMENSION" OF CHARACTER BLOCK
!     KDIMFP   - DIMENSION OF REAL BLOCK
!     KDIMIN   - DIMENSION OF INTEGER BLOCK
!
!  OUTPUT PARAMETERS
!
!     CDDATA   - CHARACTER DATA BLOCK
!     KLENCH   - LENGTH OF ABOVE
!     PDATA    - REAL DATA BLOCK
!     KLENFP   - LENGTH OF ABOVE
!     KDATA    - INTEGER BLOCK
!     KLENIN   - LENGTH OF ABOVE
!                                                                               
!     MATS HAMRUD         E.C.M.W.F.    03/94.
!
!     MODIFIED BY:
!                            
!     PEDRO VITERBO       E.C.M.W.F.    05/94.
!            (WORKSTATION "PORTABLE" VERSION)
!     TOMAS WILHELMSSON   E.C.M.W.F.    11/19. 
!            (USE ECCODES)
!
!
  USE GRIB_API_INTERFACE
  USE PARKIND1, ONLY : JPRD
  IMPLICIT NONE

  CHARACTER CDFNAME*(*)
  INTEGER KDIMCH, KDIMFP, KDIMIN
  INTEGER KLENCH, KLENFP, KLENIN
  CHARACTER CDDATA*(*)
  INTEGER KDATA(KDIMIN)
  REAL(KIND=JPRD) ::  PDATA(KDIMFP)

  INTEGER IFILE, IHANDLE, J, ICHDATA(KDIMCH)

!      
!       OPEN A GRIB UNBLOCKED FILE
!      
  WRITE (*,*) " OPEN FILE ",CDFNAME
  CALL IGRIB_OPEN_FILE(IFILE,CDFNAME,'r')
!
!        READ THE PSEUDO-GRIB FILE
!      
  CALL IGRIB_NEW_FROM_FILE(IFILE,IHANDLE)
!
!        DECODE THE PSEUDO-GRIB FILE
!
  CALL IGRIB_GET_VALUE(IHANDLE,'numberOfCharacters',KLENCH)
  IF (KLENCH > KDIMCH) THEN
    PRINT *, ' CHARACTER ARRAY DIMENSION ',KDIMCH,' TOO SMALL FOR ',KLENCH
    CALL ABORT
  ENDIF
 
  CALL IGRIB_GET_VALUE(IHANDLE,'charValues',ICHDATA)
  ! DIAG charValues encoded as one byte integers in ecCodes 
  DO J=1,KLENCH
    CDDATA(J:J)=ACHAR(ICHDATA(J))
  ENDDO

  CALL IGRIB_GET_VALUE(IHANDLE,'numberOfFloats',KLENFP)
  IF (KLENFP > KDIMFP) THEN
    PRINT *, ' FLOAT ARRAY DIMENSION ',KDIMFP,' TOO SMALL FOR ',KLENFP
    CALL ABORT
  ENDIF

  CALL IGRIB_GET_VALUE(IHANDLE,'floatValues',PDATA)

  CALL IGRIB_GET_VALUE(IHANDLE,'numberOfIntegers',KLENIN)
  IF (KLENIN > KDIMIN) THEN
    PRINT *, ' INTEGER ARRAY DIMENSION ',KDIMIN,' TOO SMALL FOR ',KLENIN
    CALL ABORT
  ENDIF

  CALL IGRIB_GET_VALUE(IHANDLE,'integerValues',KDATA)

!
!    		CLOSE THE GRIB FILE
!
  CALL IGRIB_CLOSE_FILE(IFILE)

  RETURN

END SUBROUTINE REAREC
