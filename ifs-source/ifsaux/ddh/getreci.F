      SUBROUTINE GETRECI(CDRECNM,KBLOCK,KBLLEN,
     S CDDATA,KLENCHBL,KDATA,KLENIN,KINDEX,KLENIND,KLDIM,KLDATA,KLEN)
C
C****    RETRIEVE INTEGER RECORD
C
C  INPUT PARAMETERS
C
C         CDRECNM - RECORD NAME
C         KBLOCK  - BLOCK NUMBER (MASK NUMBER, ZONAL BLOCK)
C         KBLLEN  - BLOCK LENGTH (FROM RECORD 'BLOCKLENGTH')
C         CDDATA  - CHARACTER BLOCK FROM DECOPS
C         KLENCHBL- LENGTH OF CHARACTER BLOCK
C         KDATA   - INTEGER BLOCK FROM DECOPS
C         KLENIN  - LENGTH OF INGEGER BLOCK
C         KINDEX  - INDEX
C         KLENIND - LENGTH OF INDEX
C         KLDIM   - DIMENSION OF KLDATA
C
C  OUTPUT PARAMETERS
C
C         KLDATA  - OUTPUT DATA
C         KLEN    - NUMBER OF DATA VALUES
C                                                                               
C     MATS HAMRUD         E.C.M.W.F.    03/94.                            
C
C
      CHARACTER CDRECNM*12
      CHARACTER CDDATA*(*)
      INTEGER KINDEX(KLENIND)
      INTEGER KLDATA(KLDIM)
      INTEGER KDATA(KLENIN)
C
C*      SEARCH FOR RECORD
C
      ILENRN=12
      IMREC=KLENCHBL/ILENRN
      DO 102 JREC=1,IMREC
      IREC=JREC
      IPTCHB=(IREC-1)*ILENRN+1
      IPTCHE=IPTCHB+ILENRN-1
      IF(CDDATA(IPTCHB:IPTCHE).EQ.CDRECNM) THEN
         GO TO 104
      ENDIF
 102  CONTINUE
      WRITE(0,*) ' GETRECI : RECORD NOT FOUND ',CDRECNM
      CALL ABORT
 104  CONTINUE
C
C         FIND POSITION AND LENGTH OF RECORD
C 
      IPT=2*IREC-1
      INDEX=-KINDEX(IPT)
      IF(INDEX.LT.0) THEN
         WRITE(0,*) ' GETRECI : RECORD NOT INTEGER ',CDRECNM
         CALL ABORT
      ENDIF
      KLEN=KINDEX(IPT+1)
      IF(KLEN.GT.KLDIM) THEN
         WRITE(0,*) ' GETRECI : RECORD TOO LONG , RECORD= ',CDRECNM
      ENDIF   
      INDEX=INDEX+(KBLOCK-1)*KBLLEN
      IF(INDEX+KLEN-1.GT.KLENIN) THEN
         WRITE(0,*) ' GETRECI : ADDRESS OUTSIDE INTEGER BLOCK '
         CALL ABORT
      ENDIF   
C
C         COPY RECORD TO OUTPUT ARRAY
C      
      DO 112 J=1,KLEN
      KLDATA(J)=KDATA(INDEX+J-1)
 112  CONTINUE
C
C
      RETURN
      END
